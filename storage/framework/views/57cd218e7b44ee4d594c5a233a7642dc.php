            семени, свежее масло — из кислого молока, которое питает человека. Так всё в мире
            появляется одно за другим в непрерывной последовательности.<br>
            Внутренние факторы причинности — это неведение, желание, намерение и прочие подобные
            состояния. Все они входят в понятие причинности.<br>
            Проявления, возникающие из этих двух родов факторов, — это личность и индивидуальные
            вещи, составляющие её окружение. Однако сами по себе они не являются отдельными,
            отличными сущностями. Их различают лишь в силу неведения.<br>
            Причинность можно разделить на шесть элементов:<br>
            <ul class="list-none pl-2 space-y-1 ml-2">
