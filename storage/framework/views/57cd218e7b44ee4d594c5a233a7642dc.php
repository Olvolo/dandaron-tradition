                    условий.
                </li>
                <li>Причина как действие — предполагает наличие первичного принципа, подобного царю,
                    которому подчиняются все остальные.
                </li>
                <li>Причина как цель — подразумевает участие некоторой части объективного мира в
                    системе разума и существование, в котором продолжается непрерывная деятельность.
                </li>
