        Ланка-аватара-сутра или самосознание благородной мудрости</h1>

    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава I. О различениях</h3>

        <p>Вот что я слышал. Однажды Благословенный явился в замок Ланка, на вершине горы Малайя,
            посреди великого океана. В то же время чудесным образом собралось великое множество
            бодхисаттв-махасаттв из всех земель Будды, а также огромное количество бхикшу.</p>

        <p>Бодхисаттвы-махасаттвы во главе с Махамати были совершенными мастерами различных самадхи.
            Они овладели искусством медитации, обладали десятью силами и шестью психическими
            способностями. Унаследовав Дхарму из собственных рук Будды, они глубоко понимали природу
            этого объективного мира и умели применять разнообразные средства учения и дисциплины в
            соответствии с особенностями мышления и поведения живых существ, с которыми встречались.
            Они были прекрасны в своём совершенстве, знакомы с пятью дхармами, стремились к
            овладению восемью виджнянами и двумя разновидностями отсутствия субъекта.</p>

        <p>Благословенный, уловив умственное возмущение, происходившее в сознании собравшихся —
            подобное поверхности океана, колеблемой ветрами, — почувствовал в глубине своего сердца
            великое сострадание. Он улыбнулся и сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            В древние времена Татхагаты прошлых эпох, достигшие архатства и полного просветления,
            однажды собрались в зале Ланки, на вершине горы Малайя, и обсуждали Истинно-Благородную
            Мудрость, которая выходит за пределы рассудочного знания и недоступна пониманию обычных
            учеников и учителей, даже обладающих здравым смыслом. Её можно реализовать лишь в недрах
            высшей самадхи, в глубине сознания.<br>
            Сейчас я повторяю то же самое рассуждение об этой истине. Всё, что видимо в этом мире,
            лишено способности к действию, ибо всё в мире подобно сну, сну во сне или образу,
            чудесным образом спроецированному из ничего. Этого не понимают философы, тогда как те,
            кто видит действия таким образом, видят их подлинно. Те же, кто видит иначе, начинают
            производить различные умственные операции.<br>
            Вовлекаясь в мир сансары и принимая различия за реальность — подобно зрелищу
            собственного отражения в зеркале, собственной тени, образу в воде или эху, разносящемуся
            по долине, — они, не сумев отказаться от привязанности к реализации, продолжают
            различать и различать, и потому никогда не обретают покоя.<br>
            Под покоем же подразумевается прирождённое единство — единство высочайшей самадхи,
            достигаемое через вхождение в царство Благородной Мудрости, которую можно постичь лишь
            изнутри, из самых глубин сознания.
        </blockquote>

        <p>Тогда все бодхисаттвы-махасаттвы встали со своих мест и, почтительно поклонившись,
            восславили Будду.</p>

        <p>Бодхисаттва-махасаттва Махамати облачился в торжественное одеяние и, сложив ладони у
            груди, вознёс Ему следующие стихи:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            Когда ты смотришь на мир с совершенным пониманием и состраданием,<br>
            он должен являться тебе подобным небесному цветку,<br>
            о котором нельзя сказать: «Он возник» или «Он разрушен»,<br>
            ибо понятия бытия и небытия неприменимы к нему.<br><br>
            Когда ты смотришь на мир с совершенным пониманием и состраданием,<br>
            он должен казаться тебе подобным сну,<br>
            о котором нельзя сказать: «Он вечен» или «Он преходящ»,<br>
            ибо термины «бытие» и «небытие» к нему неприменимы.<br><br>
            Когда ты смотришь на все вещи с совершенным пониманием и состраданием,<br>
            они должны являться тебе подобными видениям,<br>
            выходящим за пределы человеческого ума,<br>
            ибо представления о бытии и небытии к ним не применимы.<br><br>
            Когда с совершенным пониманием и безграничным состраданием<br>
            ты видишь бессубъективность вещей и существ<br>
            и пребываешь свободным и чистым —<br>
            от всяких препятствий, от страстей,<br>
            от образованности и от эгоизма,<br>
            ты не погибаешь в Нирване,<br>
            но Нирвана пребывает в тебе.<br><br>
            Ибо Нирвана превосходит всякую двойственность:<br>
            знание и незнание, бытие и небытие.<br>
            Те, кто видит тебя таким —<br>
            безмятежным и свободным от всяких представлений, —<br>
            освобождаются от привязанностей<br>
            и очищаются от скверны —<br>
            как в этом мире, так и в мире ином.<br><br>
            В этом мире, чья природа подобна сну,<br>
            существуют хвала и хула,<br>
            но к высшей реальности Дхармакайи —<br>
            Тела Дхармы,<br>
            пребывающего за пределами чувств и различающего сознания, —<br>
            какая может быть хвала и какая хула?<br>
            О, ты, мудрейший из мудрых!
        </blockquote>

        <p>Затем бодхисаттва-махасаттва Махамати сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            О Благословенный! О Сугата! Архат! Полностью просветлённый! Ты видишь реализацию
            Благородной Мудрости, которая вне причин и вне методов философов, которая лишёна всяких
            предикатов и категорий — таких как бытие и небытие, единство и множественность,
            существование и несуществование, вечность и невечность. Она не имеет ничего общего с
            единичностью, множественностью или общностью, с ложными воображениями и иллюзиями
            всякого рода, порождаемыми самим умом, но является истиной высочайшей реальности.<br>
            Те, кто движется по пути мудрости, неуклонно следуя всем предписаниям, проходят этапы
            посвящения и в конце концов входят в состояние Татхагаты. Благодаря обетам, принятым
            изначально и соблюдаемым во всех своих усилиях, они искореняют всякое влияние
            бесконечных миров существования.<br>
            Пусть же и мы станем подобны кристаллу, который отражает разнообразные цвета, сохраняя
            при этом чистоту своей сущности! Благодаря этому наставлению я и прочие
            бодхисаттвы-махасаттвы сможем привести все разумные существа к подобному состоянию
            совершенства и добродетели.
        </blockquote>

        <p>Тогда Благословенный сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            Хорошо, очень хорошо, Махамати! И ещё раз — хорошо, воистину!<br>
            Именно благодаря твоему состраданию ко всем мирам и тому благу, которое принесёт твоё
            учение — проповедуемое как в человеческом облике, так и в небесном, — ты теперь способен
            выслушать то, что я поведаю по твоей просьбе. Итак, Махамати, слушай внимательно и верно
            размышляй о том, что я скажу, ибо я наставляю!
        </blockquote>

        <p>Тогда Махамати и все прочие бодхисаттвы-махасаттвы погрузились в слушание, стремясь
            постичь смысл учения Благословенного.</p>

        <p>Благословенный сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            Махамати, несведущие и простые люди, довольствуясь здравым смыслом и не зная, что этот
            мир есть лишь проявление ума, цепляются за множественность внешних объектов и утверждают
            понятия бытия и небытия, единого и многого, двойственного и недвойственного,
            существования и несуществования, вечности и невечности. При этом они полагают, будто
            обладают собственной природой субъекта, в котором всё возникает.<br>
            Однако всё, что возникает из различий ума, продолжает существовать благодаря инерции и
            привычке, и от этого рождаются и далее развиваются ложные представления. Эта
            действительность подобна миражу в пустыне: человек видит источники воды и считает их
            реальными. Подобное может поразить даже воображение животных — жаждущие, под влиянием
            палящих солнечных лучей, они бросаются в бешеный бег, гонясь за мнимыми источниками. Не
            зная, что эти источники — лишь галлюцинации в их разгорячённых умах, они не понимают,
            что воды там нет, и гибнут.<br>
            Точно так же, Махамати, несведущие и простые люди отличаются тем, что их умы пылают
            огнём жадности, гнева и глупости. Они находятся в мире многообразных форм, одержимые
            идеями рождения, роста и разрушения, не понимая по-настоящему, что означают
            «существующее» и «несуществующее». На них производят впечатление ложные различения и
            умственные построения, накопленные с безначальных времён. Для них становится привычкой
            цепляться за всё это, и потому они крепко держатся за него.<br>
            Всё это подобно городу гандхарвов, который несведущие принимают за реальный город, хотя
            на самом деле его нет. Город возникает лишь как видение, благодаря привязанности к
            впечатлениям памяти о ранее виденном городе, сохранённым в уме в виде семян. Об этом
            городе нельзя сказать, что он существует или не существует, — и в то же время можно
            сказать, что он и существует, и не существует одновременно.<br>
            Подобным же образом люди цепляются за воспоминания о ложных умственных построениях и
            учениях, накопленных с безначальных времён. Верующие придерживаются всевозможных
            убеждений, настаивают на идеях единства и множественности, бытия и небытия, и их мысли
            нечисты. Они не понимают, что всё это — лишь проявления ума.<br>
            Их можно уподобить человеку, которому приснилась страна, наполненная разными людьми —
            мужчинами и женщинами, слонами, лошадьми, повозками, пешеходами, деревнями, городами,
            сёлами, коровами, буйволами, домами, лесами, горами, реками и озёрами. Он движется по
            этой земле, переходит из города в город — но вдруг пробуждается и, полусонный,
            вспоминает город своих снов и размышляет об опыте, накопленном там.<br>
            Что же ты думаешь, Махамати: будет ли тот, кто позволяет своему уму блуждать среди
            нереальностей, видимых во сне, считаться мудрым или глупцом?<br>
            Точно так же несведущие простые умы, повторяя впечатления и ложные взгляды философов, не
            распознают и не понимают, что эти взгляды подобны идеям, возникающим во сне и
            порождаемым самим разумом. Поэтому они крепко держатся за свои понятия единства и
            множественности, бытия и небытия и так далее.<br>
            А ведь всё это подобно холсту художника: лишь несведущий может вообразить, будто видит
            на нём неровности, соответствующие горам и долинам, тогда как холст на самом деле
            плосок.<br>
            Подобным же образом существуют ныне люди, которые попадают под влияние ложных взглядов о
            единстве и множественности, обоюдности («и то, и другое») и необоюдности («ни то, ни
            другое»), правильность которых обусловлена
            инерцией привычки. Они коснеют в воображаемых представлениях и упорно утверждают, будто
            те, кто придерживается подлинного учения о перерождении — свободного от альтернатив
            бытия и небытия, — являются нигилистами, отрицающими всё. Утверждая подобное, они хотят
            погубить других, но ведут к гибели прежде всего самих себя.<br>
            Они не верят в закон причины и следствия. Эти последователи ложных взглядов обрывают
            благотворные причины и благотворные истоки в своём существе, что ведёт к прекращению
            состояния чистоты. Их умы затмеваются зловредными желаниями, и они теряют способность
            понимать превосходных существ.<br>
            Они подобны людям, у которых наступило помрачение зрения: видя на роговице якобы
            волосок, они восклицают друг другу: «Как он прелестен! Взгляните, как он удивителен!»
            Однако этот волосок никогда не существовал как внешний объект. Он практически не есть ни
            сущее, ни несущее — он и видим, и невидим.<br>
            Подобным же образом те, чьи умы подвержены различениям и находятся под влиянием ложных
            взглядов философов, отдают предпочтение реалистическим представлениям о бытии и небытии,
            противоречат подлинной Дхарме и ведут к разрушению себя и других.<br>
            Всё это подобно огненному колесу, кажущаяся непрерывность которого возникает при
            вращении головешки: здесь нет никакого колеса — оно лишь воображаемо теми, кто не
            понимает природы вращающейся головни. Нет здесь ни колеса, ни не-колеса — и всё же нечто
            видимо.<br>
            Подобным же образом те, кто привык слушать и внимать различным взглядам философов, могут
            считать произведённые вещи несуществующими, а разрушенные по необходимым причинам —
            существующими.<br>
            Это подобно зеркалу, отражающему цветы: образ, возникающий в отражении, соответствует
            условиям, в которых оно появляется, ибо само зеркало совершенно беспристрастно.
            Это подобно эху ревущего ветра, заглушающего звук человеческого голоса в ушах.
            Это подобно шуму движущейся воды, видимой в пустыне.<br>
            Точно так же различающий, рассудочный ум несведущих, влекомый ложными представлениями и
            спекуляциями, движется среди миражных волн рождения, роста и разрушения.
            Это подобно колдовской грозе, когда при помощи заклинаний заставляют деревянное или
            мёртвое тело дергаться, жестикулировать и произносить слова, хотя у него нет собственной
            силы.<br>
            Точно так же немногие несведущие и простые умы, доверяя ложным философским взглядам,
            полностью предаются идеям единства и множественности. Благодаря своей доверчивости они
            страдают, ибо она лишена основания.<br>
            По этой причине, о Махамати, ты и прочие бодхисаттвы-махасаттвы должны отбросить все
            различения, ведущие к понятиям рождения, обоюдности и необоюдности, бытия и небытия.
            Освободившись от оков инерции привычки, вы сможете достичь реальности, стяжав внутри
            себя Благородную Мудрость.
        </blockquote>

        <p>Тогда Махамати спросил Благословенного:</p>
        <p>— Почему же несведущие предаются различениям, а мудрые — нет?</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            Потому что несведущие цепляются за имена, знаки и идеи. Их умы движутся по этим руслам,
            они питаются множественностью объектов и впадают в ложное представление о субъекте — или
            душе, — которому всё это якобы принадлежит. Они играют в различения добра и зла среди
            видимостей и цепляются за приемлемое.<br>
            Из-за этой привязанности возникает неведение, порождается новая карма — карма жадности,
            гнева и глупости, — которая накапливается. Вследствие накопления кармы они становятся
            узниками мира, созданного их собственными различениями, и оказываются несвободными,
            неспособными вырваться из круга рождений и смертей.<br>
            Из-за своей грубости и глупости они не понимают, что все вещи подобны игре ума или
            миражу, отражению луны в воде; что нет никакой субстанции субъекта — она воображаема;
            что нет души, ибо ей ничего не принадлежит.<br>
            Все их утверждённые идеи возникают лишь из различий и из того, что существует лишь как
            видимость собственного ума. Они не понимают, что вещи не имеют ничего общего с
            качественностью и отождествлением, что они не подвержены рождению, пребыванию и
            разрушению.<br>
            Напротив, они утверждают: «Есть рождённые, есть сотворённые, а есть умершие
            безвозвратно». Это происходит потому, что они не подобны мудрым. Это происходит потому,
            что они несведущи и склонны лишь к различениям — и поэтому не подобны мудрым.
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава II. Ложное воображение и знание
            кажимостей</h3>

        <p>Затем к Благословенному обратился Бодхисаттва-Махасаттва Махамати и сказал ему:</p>
        <p>— Ты говорил нам о ложных взглядах. Не мог бы Ты описать их подробнее, дабы мы были во
            всеоружии и остерегались их?</p>


