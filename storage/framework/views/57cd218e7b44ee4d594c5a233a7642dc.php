            Логики утверждают, что существует нечто рождающееся и нечто дающее рождение, и благодаря
            их взаимодействию возникают причинные факторы: причина, субстанция, непрерывность,
            изменение и т.д. На этом основании они заключают, что возможно постепенное
            возникновение.<br>
            Однако это постепенное возникновение лишено реальности. Вся эта концепция основана на
            привязанности к идее «природы субъекта». Когда идея «моего „я“», тела, его места
            становится видимой, люди начинают противопоставлять объекты субъектам. Результатом этого
            становится не что иное, как «смотрение самим разумом». Внешний мир начинают
            рассматривать в аспектах вечности и всеобщности — но эти аспекты нереальны. Поэтому не
            может быть ни постепенного, ни единовременного возникновения вещей.<br>
            Лишь когда начинает действовать истинный разум и производит различение проявлений ума,
            можно условно говорить о «возникновении существования».<br>
            Поэтому, Махамати, следует воздерживаться от понятий постепенности и единовременности, а
            также от представлений о сочетаниях причинной активности.
        </blockquote>

        <p>Затем Махамати спросил:</p>
        <p>— Благословенный, к какому роду различений и мыслей следует применять термин «ложное
            воображение»?</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            Поскольку люди не понимают подлинной природы объективного мира, они придерживаются
            двойственного взгляда. Они воображают множественность внешних объектов, считают её
            реальной и привязываются к ней — и это становится у них привычкой.<br>
            Благодаря деятельности системы умственности — то есть ума и всего, что к нему
            принадлежит, — они производят различения и считают их реальными. Это ведёт к утверждению
            существования субъекта (души) и «принадлежащего ему».<br>
            Так система ума продолжает функционировать, питаемая привязанностями и привычкой к
            двойственным взглядам.<br>
            Люди придерживаются различных философских воззрений, основанных на этих ложных
            различениях — бытия и небытия, существования и несуществования. Отсюда и возникает то,
            что мы называем «ложным воображением».<br>
            Однако, Махамати, это различение не развивается и устраняется, если понять, что всё
            видимое — не более чем проявление ума. Ведь при таком понимании исчезает само различение
            между сущим и несущим.<br>
            Лишь ради несведущих, привыкших видеть множественность вещей, существующих на самом деле
            лишь в их уме, Я вынужден говорить, будто различение имеет место. Оно возникает из-за
            привязанности к аспектам множественности, характеризующим объекты.<br>
            Как иначе эти простые умы поймут, что в этом мире нет ничего, кроме проявлений разума?
            Как иначе они постигнут подлинную природу разума? Как иначе освободятся от ложных
            представлений о причинах и следствиях?<br>
            Как иначе они придут к верному пониманию и достижению состояния бодхисаттвы, достигнут
            точки поворота в глубочайшей седловине сознания и, в конце концов, обретут внутреннюю
            самореализацию Благородной Мудрости — превосходящую пять дхарм, три собственные природы
            и все идеи различаемой реальности?<br>
            Поэтому Я говорю: существует некое различение, возникающее в уме и привязывающееся к
            множественности вещей, которые сами по себе нереальны. И освобождение наступает лишь
            через полное понимание подлинной реальности.<br>
            Ложное воображение состоит в постепенном привязывании к кажимостям. Вещи различаются как
            имеющие форму, знаки, контуры; как обладающие цветом, теплотой, влажностью, подвижностью
            или жёсткостью.<br>
            Привязанность к объектам — это цепляние за внутренние и внешние вещи, как будто они
            реальны.<br>
            Привязанность к именам — это распознавание этих вещей по признакам их единичности и
            общности и убеждение, что определённые имена навечно закреплены за фиксированными
            объектами.<br>
            Учение о ложном воображении указывает: поскольку все вещи обусловлены причинами и
            энергией привычки, накопленной с безначальных времён, ум не может не воспринимать
            внешний мир.<br>
            И мы не можем видеть вещи иначе, как в аспектах единичности и общности. Именно из-за
            привязанности к этим ложным воображениям у нас возникает представление о множественности
            вещей.
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава III. Совершенное знание и
            знание реальности</h3>

        <p>Затем Махамати попросил Благословенного: - Расскажи нам о пяти дхармах так, чтобы мы
            могли полностью понять совершенную мудрость.</p>

        <p>Благословенный ответил: - Пять дхарм это: видимость, имя, различение, правильное
            знание и
            реальность. Под видимостью понимается то, что обнаруживается чувством и различающим
            умом
            и воспринимается как форма, звук, вкус, касание. Из таких видимостей формируются
            идеи,
            как, например, из глины, воды и т.п. -- кувшины.</p>

        <p>Говорят: «Это такая-то и такая-то вещь и ничто иное», называют имя. Видимости
            противопоставляются сравнением именами, когда говорится: «это слон, а это лошадь»,
            «это
            повозка», «это пешеход», «это человек», «это мужчина», «это женщина», или же: «это
            разум
            и все принадлежащее этому разуму, такие вещи, названные подобным образом, являются
            различаемыми вещами. Все эти различения приходят в видимость как взаимно зависящие,
            но
            пустые в отношении собственной сущности, т.е. как бы нерожденные. Таким образом, они
            становятся видимыми, каковы они на самом деле, т.е. как проявление самого разума:
            это и
            будет правильное знание. Благодаря знанию мудрые перестают рассматривать видимости и
            мена, с ними связанные, как реальности. Когда видимости и связанные с ними имена
            отбрасываются прочь, и все различения перестают действовать, то, что остается,
            является
            подлинной сущностной природой вещей, и, поскольку больше ничто не продуцируется в
            отношении природы этой сущности, она и называется «таковостью» или реальностью. Это
            универсальная, неразличимая, неразрушимая таковость является реальностью, которую
            по-разному характеризуют как «истинную сущность ума», «трансцендентальное
            понимание»,
            «благородную мудрость» и т.д. Это дхарма лишена всяческой образности, поскольку она
            является сущностной природой предельной реальности. Эта же дхарма, которую всегда
            проповедовали все Будды, в которой все вещи понимаются в полном согласии, но однако
            тем,
            кто обладает совершенным знанием и находится на пути к достижению превосходной
            мудрости
            Татхагаты.</p>

        <p>Затем Махамати спросил у Благословенного: - Будут ли эти собственные природыности
            всех
            вещей, т.е. идеи реалии рассматриваться нами как включенные в список пяти дхарм, или
            же
            они имеют собственные характеристики, совершенные в себе?</p>

        <p>Будда ответил: - Эти три собственных природы, восьмиричная система ума и двучленное
            отсутствие «я», все они включаются в пять дхарм. Собственное природности вещей, идей
            и
            пятиричная система уже соответствуют дхармам видимости, а шестиричная система уже
            соответствует дхармам видимости, имени и различения. Собственная природа вселенного
            (универсального) ума и реальности соответствует дхармам верного знания и таковости.
            Благодаря привязанности к тому, что видится самим умом, возникает активность,
            которая
            продолжается, благодаря энергии привячки и становится проявленной в системе ума.
            Из-за
            активности системы ума возникают представления о субъекте (или душе) и принадлежащей
            ему
            объективности. Возникают различения, привязанности, понятия о субъекте (душе),
            которые
            могут быть представлены наподобие солнца и исходящих от него лучей света. Под
            отсутствием субъективности в вещах подразумевается то, что элементы, образующие
            агрегат
            личности и ее объективного мира, должны характеризоваться природой майи и не могут
            иметь
            ничего, что можно было бы считать субстанцией «я». Стало быть, они не являются
            рожденными и не имеют собственной природы. Как же можно сказать о вещах, что они
            имеют
            собственную субъективную природу? Под бессубъективностью личности понимается то, что
            в
            агрегате составляющих личности нет ничего похожего из субстанции «эго», нет ничего,
            напоминающего субстанцию «эго», либо принадлежавшего ей. Система разума, которая
            является наиболее характерным признаком личности, образуется из неведения,
            различения,
            желания и действия. Ее деятельность продолжается благодаря схватыванию, постижению и
            привязыванию к объектам, как бы к реальным. Память об этих различениях, желаниях,
            привязанностях и действиях копится в универсальном уме с безначальных времен и все
            это
            накапливается там, где условия для видимости личности и ее окружения способствуют
            привнесению постоянных изменений в представлении о различениях от момента к моменту.
            Их
            проявления могут восприниматься как река, семя, лампа, облако, ветер, однако
            универсальный ум в своей способности накапливать держит все это вкупе. Он этим
            напоминает обезьянку, которая не может быть ни секунды в покое, всегда мечется в
            поисках
            пищи и хватает все без разбора. Он напоминает огонь, который никогда не знает
            насыщения.
            ОН напоминает мельничное колесо, которое находится в постоянном вращении под напором
            воды. Универсальный ум, оскверненный привычкой, подобен волшебнику, который вызывает
            фантомы вещей и показывает их людям движущимся и являющимся как бы сами по себе.
            Верное
            понимание вещей является необходимым для представления о бессубъективности личности.
            Можно различать четыре рода знания: знание кажимости, относительное знание,
            совершенное
            знание и трансцендентальное понимание. Знание кажимости принадлежит несведущим и
            простым
            людям, которые привязаны к понятиям бытия и небытия, которые напуганы мыслью о том,
            что
            существует нерожденное. Это знание образуется в соответствии с троичной комбинацией
            при
            сочетании дхарм, связывает множественности и объект, оно характеризуется
            привязанностью
            и накоплением, оно подвержено рождению и разрушению. Знание кажимости принадлежит
            обитателям и мира сего, которые занимаются рассуждениями, различениями,
            утверждениями и
            отрицаниями.</p>

        <p>Относительное знание принадлежит философам мира сего, оно возникает из способности
            ума
            усматривать отношения, которые несут в себе кажимости в их связи друг с другом.
            Когда ум
            увидит такое отношение, оно возбуждает в свою очередь способность ума располагать и
            оранжировать, сочетать и анализировать эти отношения, благодаря возможностям
            дискуссивной логики и воображения, по таковой причине ему, различающему, кажется,
            что он
            проникает в смыслы и значения вещей, в их внутренние связи, в их структуру.</p>

        <p>Совершенное знание принадлежит миру бодхисаттв, которые понимают все вещи единственно
            как
            проявления ума. Бодхисаттвы ясно понимают пустоту, нерожденность и бессубъективность
            всех вещей.</p>

        <p>Те, кто пришел к пониманию пяти дхарм и двоичной бессубъективности, близок к
            пониманию
            истины в ее безобразности. Совершенное знание различается в зависимости от стадии,
            на
            которой пребывает бодхисаттва, однако представляет собой как бы столбовую дорогу,
            ведущую в возвышенное состояние самореализации благородной мудрости. Совершенное
            знание
            (джняна) принадлежит тем бодхисаттвам, которые полностью свободны от двойственности
            бытия и небытия, нерожденности и не исчезновения, свободны от утверждений и
            отрицаний,
            которые благодаря самореализации приобрели подлинное понимание истинной
            бессубъективности и безобразности. Они больше не приводят никаких различений в мире,
            не
            подвержены причинным воздействиям: они считают правила причинности чем-то подобным
            воображаемому городу Гандхарвов. Для них мир подобен видениям и сну, он подобен и
            смерти
            сына бесплодной женщины. Для них нет ничего возникающего и развивающегося, ничего
            исчезающего.</p>

        <p>Мудрых, которые лелеют в себе совершенное знание, можно разделить на три класса:
            учеников, учителей, архатов. Обычные ученики отделяются от учителей. Поскольку
            обычные
            ученики продолжают различать понятия единичного и общего, а учителя отличаются от
            обычных учеников, хотя и выходят из их среды, когда понимают ошибочность разделения
            на
            единичное и общее, в то же время, они по-прежнему придерживаются понятия
            субъективности
            на том основании, что еще бывают вынуждены, как субъекты, уходить в уединение, в
            одиночестве. Архаты достигают своего состояния, когда осознают ошибочность всех и
            всяческих различений. Тут все ошибки распознаются и мудрые приходят к подлинному
            знанию
            истины, это и будет поворотным пунктом, находящимся в самой глубине их сознания. При
            этом ум освобождается и переходит к совершенной самореализации благородной мудрости.
            Однако, Махамати, если ты утверждаешь и настаиваешь на том, что есть такая вещь, как
            «благородная мудрость», это уже будет неверно, потому что всякое настаивание к
            утверждению в подобном роде имеет касательство к природе бытия и может быть
            характеризовано качеством порожденности, самое утверждение, «все вещи не рождены»,
            разрушает собственную истинность. То же самое справедливо для утверждения: «Все вещи
            пусты», «Все вещи лишены субъективной природы». Все они совершенно непригодны в
            качестве
            форм рассуждения. Но когда при этом указывается, что все вещи подобны сну или
            видению,
            то это, однако, означает, что одним способом вещи постигаются, а другим способом они
            не
            постигаются, т.е. что в неведении они постигаются, а в совершенном знании они не
            постигаются.</p>

        <p>Все утверждения и отрицания суть лишь конструкции нашей мысли, и они не являются
            рожденными. Даже утверждения об универсальном уме и благородности мудрости, которая
            есть
            высшая реальность, суть умственные конструкции и поэтому непорождены (не
            производны).
            Все так называемые «вещи» не могут составлять сущностное содержание универсального
            ума,
            ибо нет ни универсального ума, ни благородной мудрости, ни высшей реальности, как
            вещей
            для понимания мудрых, которые уходят из этого мира в сферу безобразности, в
            одинаковую
            чистоту, сами являются чистым пониманием; вот что для мудрых означает. «Все вещи» -
            они
            стираются прочь и при этом перестают существовать даже в состоянии
            безобразности.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава IV. Система ума</h3>

        <p>Затем Махамати попросил Благословенного: - Скажи, что следует понимать под умом
            (читта).</p>

        <p>Благословенный ответил: - Все вещи в этом мире, независимо от того будут ли они по
            внешней видимости хорошими или плохими, исполненными недостатков или безупречными,
            вызывающими действие или не вызывающими действия, производящими реакцию, либо не
            производящими реакций, воспринимаемыми либо не воспринимающими, могут быть разделены
            на
            два класса: дающие злостное истечение жизни и истечения добра. Злостное истечение
            жизни
            -- это пять элементов охватывания, которые образуют агрегат личности: форма,
            ощущение,
            восприятие, различение и сознание, которые воображаются как хорошие или плохие. Они
            дают
            начало энергии привычки в системе ума. Духовное достижение и радость самадхи,
            которые
            дают плод самапатти, приходят к мудрым, благодаря самореализации благородной
            мудрости, и
            накапливаются, принимают участие со своей стороны в явлениях этого троичного мира,
            называются неистечениями добра. Система ума, которая является источником истечения
            зла,
            состоит из пяти органов чувств и соответствующих им чувственных разумностей или
            виджнян,
            все они объединяются в различающем уме (мановиджняна).</p>

        <p>Много существуют бесконечно последовательности чувственных представлений
            проистекающих в
            различения актах мыслящего ума, который вновь сочетает их, и различает их, и
            начинает
            судить о них как о хороших или плохих. Затем следуют отвращение к ним, или желание
            их,
            или привязанность к ним, или действие ради них. Вся система непрерывно движется и
            тесно
            взаимосвязана в своих элементах. Однако она не позволяет видеть и понимать, что все
            видимое и воспринимаемое, как и схватывание, являются лишь проявлениями ее
            собственной
            активности и не имеют никакого иного основания. Так что ум ложно продолжает
            схватывать и
            различать различия в формах и качествах и ни на минуту не обретает спокойствия.</p>

        <p>В системе ума имеются три модуса активности, которые могут быть аналитически
            различными.
            Чувственные разумности функционируют, сохраняя свою исходную природу. Эти разумности
            образуют определенные эффекты и чувственные разумности, которые способны к
            эволюции.</p>

        <p>Под нормальным функционированием чувственных разумностей, схватывающих элементы во
            внешнем мире, благодаря чему ощущение и восприятие имеют место в разных степенях в
            каждом чувственном органе и каждой чувственной разумности, подразумевается то, что в
            порах кожи и даже в атомах, составляющих тело, имеется способность, благодаря
            которой
            охватывается вся сфера деятельности, что следует понимать на подобие зеркала,
            отражающего объекты, в этом нет возможности реализовать внешний мир через понимание
            того, чем он является, т.е. лишь проявлением самого ума. Второй модус активности
            проявляется в образовании эффекта, при котором все эти ощущения действуют проективно
            на
            различающий ум и это приводит к возникновению восприятий охватывания, привлечения,
            отвращения от действий и привычек. Третий модус активности имеет отношение к росту,
            развитию и изменению в системе ума, т.е. система ума подвергается действию
            собственной
            энергии привычки, которая накоплена с безначальных времен. Так, например, в глазу
            имеется энергия привычки «глазения», которая ведет к тому, что глаз предрасположен к
            схватыванию и привязыванию, к многообразию форм и видимостей. Подобным же образом,
            деятельность развивающейся, эволюционирующей системы ума на основе энергии привычки
            напоминает волны, возникающие как бы объективно на поверхности универсального
            разума,
            которые в свою очередь обусловливают активность и различия в системе ума. Кажимость,
            восприятие, привлечение, охватывание, действие, привычка, реакция, условие -- все
            это
            непрерывно проявляется, и все это функционирует в чувственных умственностях, в
            различающем уме и в универсальном уме, которые, таким образом, оказываются
            взаимосвязанными.</p>

        <p>Так, благодаря различениям, обладающим природой майи и не являющимися реальными, т.е.
            являющимся ложно воображаемым и продуктами ложного рассуждения. Всякая ложность
            постоянно возобновляет себя самое, в результате чего следует действия, накапливаются
            энергия привычки, и поэтому чистая поверхность универсального ума оказывается
            загрязненной. В результате система ума продолжает функционировать ускоренно,
            образуется
            эфирное тело, такова причина его порождения. Однако различающий ум не мыслит о том,
            что
            благодаря его собственным различениям и привязанностям обусловливается само
            существование тела и таким образом все чувственные умственности и различающий ум
            оказывается все более естественно взаимосвязанными и взаимообусловленными,
            становятся
            все более тесно приближенными друг к другу и образуют мир представления о
            деятельности
            собственного воображения. Подобно тому, как зеркало отражает образ, воспринимающие
            чувства схватывают видимости, которые различающий ум собирается соединить совокупно,
            а
            затем продолжает их вновь различать, называть и привязываться к ним. Между этими
            двумя
            функциями нет никакого разрыва, поскольку они взаимно обусловлены. Воспринимающие
            чувства схватываю то, что им свойственно, и в них происходит трансформация,
            структурное
            преобразование воспринимаемого с сохранением его инвариантности, поскольку ум
            продолжает
            сочетать, различать, давать оценки и действовать. Затем наступает череда энергии
            привычки, которая укрепляет ум в его непрерывной активности. Различающий ум,
            благодаря
            своей способности различать, судить, выбирать и рассуждать о чем-либо, развивается
            вместе с мышлением, или интеллектуальным умом.</p>

        <p>Имеются три разряда умственной активности: умственность, которая функционирует в
            связи с
            привязанностью к объектам и идеям, умственность, которая функционирует в связи с
            общими
            идеями, и умственность, которая исследует истинность общих идей.</p>

        <p>Умственность, которая функционирует в связи с привязанностью к объектам и к
            конкретным
            идеям, подразделяется, поскольку она происходит из различения. Они отличает ум от
            его
            собственных умственных процессов и принимает идеи из него как реальные и таким
            образом
            привязывается к ним. Разнообразие связей, т.е. совокупность ложных суждений,
            возникающих
            в результате и относящихся к бытию, множественности и единичности, ценностям и т.д.,
            ранее обладает сильнейшей схватывающей силой. Она продолжает расти благодаря энергии
            привычки, и таким образом различения продолжают утверждать и укреплять себя самое.
            Эти
            умственные процессы дают начало общим представлениям о теплоте, текучести,
            подвижности,
            твердости, характеризующим объектом различения. Между тем, упорное поддержание общих
            идей дает начало предположениям, допущениям, основаниям, определениям, иллюстрациям,
            доказательствам. Все это ведет к уничтожению об относительном знании и подтверждении
            веры в рождение, собственную природу и существование субъекта опыта. Под
            умственностью
            как исследующей функцией понимается интеллектуальный акт исследования, изучения
            общих
            заключений, оценки их истинности, значимости и достоверности. Это способность,
            которая
            ведет к пониманию, правильному знанию и указывает путь к самореализации.</p>

        <p>Затем Махамати спросил Благословенного: - Скажи нам теперь, Благословенный, какое
            отношение личность -- субъект имеет к системе ума?</p>

        <p>Благословенный ответил: - Для объяснения этого сперва необходимо поговорить о
            собственной
            природе пяти захватывающих агрегатов, которые образуют личность. Как я уже доказал
            вам,
            они пусты, не рождены, не обладают собственной природой. Эти пять агрегатов суть:
            форма,
            ощущение, восприятие, различение, сознание.</p>

        <p>К форме относится то, что образует так называемые «первые элементы», чем они ни были,
            четыре остальные агрегата не имеют формы, и поэтому их даже не следует рассматривать
            как
            четыре, поскольку они сливаются неразличимо, переходя один в другой. Все они подобны
            пространству, которое невозможно исчислять. Лишь благодаря воображению, они
            различаются
            и уподобляются пространству. Поскольку вещи имеют видимость бытия, видимость
            характерных
            признаков воспринимаемости, местопребывания, действенности, о них можно сказать, что
            рождены они и что они являются эффектами, результатами действий некоторых причин.
            Однако
            этого нельзя сказать о четырех невоспринимаемых чувственно оформлено агрегатах,
            поскольку они не имеют ни формы, ни характерных признаков. Эти четыре умтсвенных
            агрегата образуют личность, и при том вне пределов исчисляемости их состава и их
            структуры. Они вне сферы достигаемости четырех рациональных пропозиций логики
            (тетраламмы). Они не могут быть предицируемы как существующие или не существующие.
            Однако совместно они образуют то, что называется «ясным умом». Они еще более подобны
            майе, еще более подобны ему, чем все вещи, поскольку, будучи составом различающего
            смертного ума, они препятствуют самореализации благородной мудрости. Однако лишь не
            сведущие могут перечислять и думать о них, как образующих структуру личности или
            субъекта, мудрые же так не поступают. Это различение пяти агрегатов, как бы
            образующих
            личность, служит основанием для познания субъекта или души и основанием для
            возникновения ее желаний, и ее собственных интересов.</p>

        <p>От него нужно в конце концов отказаться и на его место поставить истинное
            представление о
            безобразности, о единичности, которое должно твердо укрепиться.</p>

        <p>Затем Махамати обратился к Благословенному и сказал: - Расскажи нам теперь об
            универсальном уме и об его отношении к низшей системе ума.</p>

        <p>Благословенный ответил: - Чувственные разумности и централизирующий их различительный
            ум
            взаимосвязаны. Они имеют отношение к внешнему миру, являющемуся проявлением его же
            самого и они дают начало восприятию, различению и схватыванию мира же подобных
            кажимостей. Универсальный же ум (Алая-Виджняна) превосходит всякую единичность и
            превосходит всякие определения.</p>

        <p>Универсальный ум совершенно чист в своей сущностной природе, он остается неизменным и
            свободным от всяких осквернений, от переменчивости, он не возмущен чувствами
            эгоизма, не
            возмущен различениями, желаниями отвращениями. Универсальный ум подобен огромному
            океану, на поверхности которого могут появляться волны и проявлять свои действия как
            приливы и отливы, однако его глубины всегда остаются неподвижными. Сам по себе он
            лишен
            личности и всего принадлежащего личности. Однако, ввиду осквернения, различного по
            его
            поверхности, он уподобляется актеру или деятелю, который играет разнообразнейшие
            роли, в
            том числе те, в которых происходит подлинное функционирование в схемах низшего ума.
            Принцип понимания оказывается разделенным и ум, функционируя как ум, порождает
            злостные
            истечения, и происходит индивидуализация. Возникает семичленная разделенность ума, а
            именно: интуиттивное самопонимание, мысль-умопостигаемое, желательно-различающее,
            состояния видения, слышания, вкуса, обоняния, осязания. Затем возникают
            взаимодействия и
            реакции всех перечисленных составляющих. Различающий ум является причиной действия
            чувственных умствований, он их поддерживает, благодаря им сам функционирует,
            поскольку
            он описывает их деятельность и становится привязанным к миру объектов, после того,
            по
            мере накопления энергии привычки, он все более и более оскверняет поверхность (лицо)
            универсального ума. Так универсальный ум оказывается хранилищем и разменным банком
            для
            всех этих накоплений и продуктов умственности и действия с безначальных времен.
            Между
            универсальным умом и ундивидуальным различающим умом находится интуитивный ум
            (манас),
            который зависит от универсального ума и является причиной всего следующего ниже,
            поддерживая все уходящее в отношение с тем и с другим. Он сопричастен
            универсальности
            универсального ума, он сопричастен его чистоте, он подобен ему, он выше формы, он
            выше
            моментальности. Именно благодаря деятельности интуитивного ума возникают,
            проявляются и
            реализуются благие истечения. При этом счастливым обстоятельством является то, что
            интуиция не моментальна, поскольку она связана с просветлением, где моментальность
            невозможна, и поскольку она несовместима с понятием мудрости и знания. Однако,
            интуитивный ум вступает в отношение с низшей системой ума, разделяет ее
            сопричастность
            опыту и рефлектирует ее активность. Интуитивный ум есть единое с универсальным умом
            на
            основании его сопричастности трансцендентальному пониманию (алая Виджняна) и он есть
            единое в системе ума, поскольку схватывает дифференцированное (виджняна).
            Интуитивный ум
            не имеет тела (или формы), принадлежащей ему самому, так же как он не имеет никаких
            признаков, по которым его можно было различать. Универсальный ум является его
            причиной и
            его опорой, однако он развивается параллельно развитию понятия субъекта и того, что
            принадлежит этому субъекту и ум цепляется за него и он оскверняется опознавательными
            признаками..</p>

        <p>Благодаря интуитивному уму и благодаря способности интуиции, представляющей собой
            смешанную способность тождества и различения, непостижимое мудрствование
            универсального
            ума открывается обыденному уму и становится постижимой истиной.</p>

        <p>Благодаря универсальному уму интуитивный ум не может служить источником ошибки.
            Различающий ум -- сознание подобно танцору -- волшебнику, для которого объективный
            мир
            служит сценой. Интуитивный же ум -- это мудрый щит, который путешествует, странствуя
            там
            же, где волшебник, и рефлектирует, соображает его пустоту и проходящий успех.
            Универсальный же ум ведет запись и знает все, что должно быть, и все, что может
            быть.
            Благодаря деятельности различающего ума, или сознания, возникают ошибки, развивается
            объективный мир, и все более утверждается понятие о субъекте и его самосознании.
            Если и
            когда различающий ум перестает действовать, вся система ума также перестает
            функционировать и универсальный ум остается в одиночестве.</p>

        <p>Отделавшись от различительного ума, он устраняет все и всяческие ошибки, поскольку
            тот
            оказывается их причиной.</p>

        <p>Затем Махамати сказал Благословенному: - Теперь, Благословенный, расскажи нам о том,
            что
            означают прекращения деятельности системы ума.</p>

        <p>Благословенный ответил: - Пять чувственных функций, различительная и мыслительная
            функции
            дают начало и образуют завершение каждого момента и следующего за ним. При их
            рождении
            оказывается различение, форма, видимость и объективность в тесной связи с внешними
            обстоятельствами.</p>

        <p>Воля к жизни есть их мать, неведение их сила -- отец. Путем установления именности
            форм,
            умножается жадность и желание и, таким образом, ум оказывается взаимообусловленным
            тем и
            другим и попадает в сеть условий. Благодаря привязанности и к именам, и к формам и
            непонимания того, что здесь нет никакой основы для деятельности ума, возникают
            ошибки и
            появляются продукции ложного воображения, возникают желания и страдания, чувства
            приятного и неприятного и, таким образом, путь к освобождению оказывается закрытым.
            Системы чувственных умствований и различающего ума на самом деле не испытывают
            удовольствия и неудовольствия, страдания и наслаждения. Они лишь воображают, будто
            бы
            они испытывают. Удовольствие и неудовольствие, чувство приятного и неприятного --
            это
            ложные, обманчивые реакции смертного ума, когда он схватывает объективные,
            воображаемые
            вещи мира. Имеются два пути, по которым возможно прекращение деятельности системы
            ума.</p>

        <p>В отношении форм и в отношении непрерывности. Чувственные органы функционируют в
            отношении форм, благодаря взаимодействию формы, контакта и схватывания. И они
            перестают
            функционировать, когда контакт нарушается. Что касается непрерывности, то когда эти
            взаимодействия формы, контакта и схватывания прекращаются, то больше не может быть
            непрерывного видения, слышания и прочих отправлений органов чувств. При прекращении
            же
            их функционирования исчезает и различение, когда же схватывание, различение и
            привязывание отсутствуют, все относящиеся к деятельности различающего ума
            прекращаются,
            прекращается и деятельность, прекращается накопление энергии привычки, и здесь
            больше не
            может быть накопления оскверняющей кармы, которая загрязняет лик универсального
            разума.</p>

        <p>В развивающейся системе смертного ума не может быть тождества природы универсального
            ума,
            поскольку если бы оно было, то прекращение деятельности нижней системы ума означало
            бы
            прекращение деятельности универсального ума. Однако они отличны друг от друга и
            универсальный ум нив коем случае не является причиной деятельности смертного ума.
            Нет и
            невозможно прекращение деятельности универсального ума в его чистой сущностной
            природе.
            Что прекращает свое функционирование, не является и не может являться универсальным
            умом
            в его сущностной природе. Однако прекращение возникновения эффекта и связанного с
            ним
            осквернения, загрязняющего поверхность универсального ума, накопленного благодаря
            энергии привычки, благодаря различающему мышлению, своей причиной имеет сам смертный
            ум.</p>

        <p>Не может быть прекращения деятельности божественного ума и его существования в самом
            себе, поскольку он является основой реальности, он является утробой истины.</p>

        <p>Под прекращением же деятельности чувственной умственности подразумевается вовсе не
            прекращение перцептивного функционирования, а прекращение различений и именований,
            способность к чему сосредоточена в различающем смертном уме и в качестве центра
            управления.</p>

        <p>Под прекращением деятельности системы ума, как целого, понимается прекращение
            различений,
            расчищение пути к пониманию необходимости отвратиться от разных привязанностей и в
            результате этого удаления всяких осквернений, прекращение накопления энергии
            привычки и
            дальнейшего загрязнения поверхности ума тем, что накапливалось на ней с изначальных
            времен благодаря различениям, привязанностям, даже ложному поведению и последующим
            дурным поступкам.</p>

        <p>Прекращение деятельности в данном аспекте и деятельности различающего ума в целом
            происходит тогда, когда прекращает существовать то, что поддерживает систему ума, а
            именно, различающий смертный ум, или рассудок. С прекращением его деятельности
            смертный
            ум в целом и весь мир майи, и мир желаний исчезают. Отделавшись от различающего
            смертного ума, мы обретаем Нирвану. Однако прекращение деятельности различающего
            сознания не может произойти до тех пор, пока не будет осуществлен решительный
            поворот в
            глубочайшей седловине нашего сознания. Умственная привычка людей видеть внешний
            объективный мир при помощи различающего ума должна быть отброшена прочь.</p>

        <p>Должна возникнуть новая привычка осознания истины в интуитивном уме, которая
            становится
            единой и которая утверждается в ней. До тех пор, пока эта интуитивное самопонимание
            благородной самой мудрости не будет достигнуто, эволюция системы ума будет
            продолжаться.
            Но когда наступит пора подлинного понимания пяти дхарм, трех субъективных природ и
            двоичной бессубъективности, тогда открывается путь к повороту и поворот близок.
            Когда
            наступает прекращение испытания приятного и неприятного, наслаждения и боли,
            наступает
            прекращение действия конфликтующих идей, возбуждающих нас интересов и эгоизма,
            наступает
            состояние успокоенности, и всей истины просветления и начинают полностью пониматься.
            Тогда больше не будет злостных истечений разума.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава V. О трансцендентальном понимании</h3>

        <p>Тогда Махамати сказал Благословенному: - Молю тебя, объясни, что образует
            трансцендентальное понимание?</p>

        <p>Благословенный ответил: - Трансцендентальное понимание -- это внутреннее состояние
            самореализации благородной мудрости. Оно достигается внезапно, интуитивно, в
            поворотном
            пункте, в точке, расположенной в глубочайшей седловине сознания.</p>

        <p>Оно не приходит ниоткуда и не уходит никуда. Оно подобно отражению луны в воде.
            Трансцендентальное понимание не подвержено ни рождению, ни разрушению. Оно не имеет
            отношения ни к сочетанию, ни к соответствиям. Оно не имеет отношения ни к
            привязанностям, ни к накоплениям. Оно превосходит все дуалистические концепции.</p>

        <p>Когда рассматривается трансцендентальное понимание, как таковое, следует иметь в виду
            четыре его аспекта: слово, значение, поучение и благородная премудрость
            (арьяпраджня).</p>

        <p>Слова используются для выражения значений, но не зависят от различающей деятельности
            сознания: память является их причиной, и поэтому при использовании звуков или букв,
            которыми обеспечивается взаимная передача, значаний, оказывается возможным их
            понимание.
            Слова -- это всего лишь символы, которые могут выражаться и полно интенционированные
            значения, ими, однако, могут и не выражать этого. Более того, слова можно понимать
            совсем не в том смысле, в котором заключение намерение интенция говорящего. Слова не
            отличны от значений, и не отличны от них и такого же рода отношение самих значений к
            словам. Если значение отлично от слов, то оно не может быть выражено при помощи
            слов.
            Однако значение поясняется благодаря словам, подобно тому, как вещи становятся
            видимыми
            при свете лампы. Слова подобны человеку, который приносит лампу для того, чтобы
            разглядеть получше свое имущество. При этом он может сказать, что вот это мое
            имущество.</p>

        <p>Точно также при помощи слов и речи у нас возникают различения. Бодхисаттвы могут
            вникать
            в смысл учения Татхагаты при помощи слов, вникнув же в смысл, они могут найти и
            прийти в
            экзальтированное состояние самореализации благородной мудрости, которая сама по себе
            свободна от всяческих словесных обозначений.</p>

        <p>Однако, как человек привязывается к буквальным значениям слов и цепляется за иллюзию,
            будто слова и значения находятся в соответствии и в жестком согласии друг с другом,
            особенно в таких вещах, как нирвана, которая не возникла и не исчезает, или будет,
            имеется в различении в средствах, ведущих к нирване.</p>

        <p>Четырех путях, пяти дхармах, трех субъективных, так он не сумеет понять подлинного
            смысла
            учения, запутается в утверждениях и опровержениях.</p>

        <p>Как можно видеть разнообразные объекты, различаемые нами во снах и в видениях, так же
            идеи и утверждения могут ошибочно различаться наяву, а ошибка эта ведет к
            приумножению
            ложности. Несведущие и простые умом заявляют, что значения слов не отличны от слов,
            что
            каковы слова, таковы и значения.</p>

        <p>Они думают, что поскольку значение лишено «тела», т.е. формой самой по себе, то оно
            не
            может быть отлично от слов, хотя оно есть не речь, а мысль, и, стало быть, они
            объявляют
            значение мысли тождественным словесному выражению. В этом они проявляют полное
            непонимание того, какова истинная природа слова.</p>

        <p>Они не понимают, что слова могут порождаться и исчезать, между тем как их значения --
            нет. Слова зависят от букв, а значения -- нет. Значения занимают особое положение,
            они
            не имеют никакой связи с существованием и несуществованием и не имеют субстрата, они
            не
            рождены. Татхагата не учит дхарме, зависящей от букв. Всякий, кто учит в зависимости
            от
            букв, будет простым болтуном, поскольку истина за словами, за буквами и вне книг.
            Это
            вовсе не означает, сто слова и книги не говорят о том, что находится в соответствии
            с
            истиной.</p>

        <p>Сказанное означает, что слова и книги зависят от способа их применения различающим
            сознанием, между тем, как значение и истина, т.е. смысл и правда, от этого не
            зависят.
            Кроме того, слова и книги могут быть по-разному истолкованы отдельными умами, а
            смысл и
            истина не могут быть по-разному истолкованы. Но, если истина не выражена в словах и
            не
            записана в книгах, то Писание, которое содержит значение истины, могло бы исчезнуть,
            а
            когда Писание исчезает, не будет ни учеников, ни учителей, не будет ни бодхисаттв,
            ни
            Будд, поскольку не будет кому учить, кого учить, чему учить. Однако, принимая это во
            внимание, не следует привязываться к словам Писания, поскольку они даже в
            канонических
            текстах иногда можно отклониться от прямого их смысла, что приведет к неверному
            функционированию умов живых существ. Я и прочие Татхагаты проводим реализацию
            религиозного собеседования с разными существами сообразно с их потребностями, их
            верой,
            их убеждениями, их характерами и типами. Мы, главы этих собеседований, ставим своей
            целью освободить их от зависимости, от мыслящей функции в системе разума, однако,
            все
            эти собеседования вовсе не могут занять место самореализации Благородной
            Мудрости.</p>

        <p>Когда нет понимания, то не породить его, а мир есть лишь объективация нашего ума.
            Поэтому
            все уподобления должны быть отброшены и должна быть понята безобразность истины.
            Тогда
            мы увидим, что она находится вне связи со словом, либо буквой.</p>

        <p>Несведущие и простые умом вводятся в заблуждение своими субъективными представлениями
            и
            ложными рассуждениями. Они продолжают плясать и скакать, они не способны понять, что
            беседа, проводимая в словах, лишь касается природы истинной самореализации, которая
            уже
            вне слов. Тем не менее, они способны понять самую истину, цепляясь за внешний мир,
            за
            его объективное значение. Они предпочитают изучать книги, которые могут служить
            разве
            лишь средствами для достижения истины. Они не понимают сущности того, как
            утвердиться в
            истине самореализации. Они не понимают, какова истина, отчего она не подвержена
            действиям из четырех логических пропозиций. Самореализация есть возвышенное
            состояние
            внутреннего достижения, которое превосходит всю специфику дуалистического мышления и
            которая вне системы ума с его логикой, с его рассуждениями, идеальными объектами и
            иллюстрациями.</p>

        <p>Татхагаты беседуют с несведущими, однако они поддерживают в них стремление интенцию к
            состоянию бодхисаттвы, который ищет самореализации благородной мудрости. Стало быть,
            каждый член -- ученик должен стремиться к тому, чтобы избавиться от магии слов и
            стать
            совершенным в полном соответствии со значениями, поскольку истина -- не в букве.
            Когда
            человек указывает кому-либо кончиком пальца на нечто, то кончик пальца по ошибке
            может
            быть принят за то, на что он указывается. Подобным же образом несведущие и простые
            умом,
            подобно детям, не могут даже в день своей смерти отказаться от идеи о том, что слова
            есть тот самый кончик пальца, который указывает на подлинное значение слов. Они не
            могут
            осознавать конечной реальности, что их интенция направлена на то, чтобы цепляться за
            слова, хотя эта интенция -- не более чем указательный палец. Слова, связанные с ними
            различения, привязывают к ужасному кругу рождений и перерождений. Они ввергают нас в
            мир
            рождений и смертей. Значения стоят более обособленно, и они-то ведут нас к
            нирване.</p>

        <p>Значение и смысл достигаются путем обучения, и чем больший успех достигнут в учении,
            тем
            больше мы понимаем значение и то, что за словами. Стало быть, пусть ищущие истины,
            постепенно и почтительно приближаются к тем, кто мудр и кто может их избавить от
            наваждения слов.</p>

        <p>Что же касается формы поучений, то имеются священнослужители и проповедники, которые
            придают большое значение ритуалам и церемониям, которые искусны и в различных
            напевах и
            весьма красноречивы, однако не их следует почитать, не к ним следует обращаться с
            благоговением, потому что получаемое от них -- не более, чем эмоциональное
            возбуждение и
            мирское удовольствие.</p>

        <p>Это и есть подлинная дхарма.</p>

        <p>Подобные проповедники, хитро и искусно манипулируя словами и фразами, прибегая к
            разнообразным рассуждениям и напевам, напоминающим болтовню ребенка, могут привести
            в
            еще большее несоответствие с истиной, чем то, в котором находится приближающийся к
            ним.
            Они не в состоянии привести в унисон со смыслом, они могут пробудить лишь некоторые
            чувства, некоторые эмоции, притупляя тем самым наш ум, потому что энергия ушла уже
            на
            чувства и эмоции. Тот, кто сам не понимает смысла вещей, может только запутать свой
            ум,
            выслушав подобные взгляды, особенно же взгляды, соответствующие дуалистическим
            представлениям. Без собственного понимания, без того, чтобы самому исследовать умом,
            без
            того, чтобы не быть привязанным к понятию субъективной природы внешних вещей, нельзя
            ничего узнать относительно Пути, нельзя узнать ни от кого другого, нельзя получить
            помощь от другого.</p>

        <p>Ввиду различения себя и его.</p>

        <p>Таким образом, священнослужители и народные проповедники, которые очень ловки в
            риторике
            и в жонглировании словами, но сами не свободны от бедствий рождения, старости,
            болезни,
            печали, отчаяния, страха, не способны также вести несведущих от тягот жизни при
            помощи
            многообразных слов, фраз, примеров, иллюстраций и выводов.</p>

        <p>Имеются еще так называемые философы -- материалисты.</p>

        <p>Им не нужно оказывать никакого уважения и не принимать их всерьез, потому что их
            учение,
            хотя оно может быть объяснено сотнями тысяч ловких фраз, не выходит за понятия этого
            мира и их содержание, и их цель -- отнюдь не освобождение от страданий -- напротив,
            они
            приводят к все большим и большим страданиям.</p>

        <p>Материалисты не признают и не понимают никакой истины, существующей по себе, они
            разделяются на множество школ, каждая из которых цепляется за собственный способ
            обсуждения и ожесточенно борется с другими. Однако есть нечто, чего не понять
            материалистам, что недостижимо для тех философов, которые не могут видеть
            первоосновы,
            т.е. отсутствия реальности во внешних объектах.</p>

        <p>Как же понимается, что помимо этого нет ничего, что за всем этим видится один лишь
            разум,
            что различение бытия и небытия должно быть оставлено, что внешний мир как объект
            восприятия есть порождение разума, то ничего не остается кроме единой
            реальности.</p>

        <p>Реальность же эта вовсе не подсудна материалистическим философам, она становится и
            относится к области Татхагатства. В таких вещах, которые могут быть воображаемы как
            приходящие и уходящие из системы ума, как исчезающие и появляющиеся, в таких вещах,
            с
            которыми связаны стремления, привязанность, интуитивное желание, а также философские
            гипотезы, теории, представления о месте, структуре, об осмысленных концепциях. Об
            атомном притяжении, представления об организме, о росте, о желании, о схватывании --
            все
            это относится в сфере материализма, оно не из моего понимания.</p>

        <p>Все эти вещи относятся к объектам мирских интересов, их нужно ощущать, нужно
            пощупать, их
            нужно потрогать, их нужно понять на вкус. Все эти вещи привлекают того, кто связан с
            внешним миром. Все эти вещи проявляются в элементах, образующих агрегат личности, и
            благодаря порождающей новое силе желания, они становятся источником разного рода
            несчастий, рождений и печалей, достойных оплакивания событий, боли, страдания,
            болезни,
