            где
            нет шумной толпы, и размышлять полагаясь лишь на самого и подвизаться в практике
            сосредоточения, добиваясь на всех этапах этого. Одиночество является характерной
            особенностью того пути, который ведет к достижению самореализации Благородной
            Мудрости.</p>

        <p>Что я называю это: единством пути и повозки, не потому, что это единое: путь и
            повозка,
            но потому, что лишь в одиночестве можно распознать и постичь путь, лежащий впереди
            едущей по нему повозки. До тех пор, пока ум будет отвлечен, он будет делать усилия
            для
            осознания. Здесь нельзя достигнуть высшего предела, какими бы мы средствами не
            пользовались.</p>

        <p>Когда ум одинок и спокоен, можно отказаться от всех различий внешнего мира и искать
            самореализации во внутренней сфере, в которой нет различия между путем и едущей по
            нему
            повозкой. Я могу говорить о «трех путях» или «трех повозках», для того, чтобы
            подвести к
            этому пониманию несведущего. Я не говорю о единственности пути и повозки, что нет
            пути,
            на котором ученики и учителя могли бы достичь состояния нирваны без поддержки.
            Согласно
            поучениям Татхагаты, ученики должны отделяться и проходить полный путь
            сосредоточения и
            тренировки, но все они пользуются множеством средств для достижения освобождения.
            Поскольку ревностные ученики и учителя еще не полностью разрушили энергию привычки
            как
            реально накопленную, некоторые из них встретят множество препятствий для
            различающего
            сознания и человеческих чувств, которые способны ввести в заблуждение, которые
            мешают им
            понять увиденную бессубъективность и которые делают для них непостижимыми
            превращения
            после смерти.</p>

        <p>Вот почему я проповедую о тройном пути, о тройной повозке, а не об единичности пути и
            повозки. И даже ревностные ученики, чтобы избавиться от всего этого, от зловредной
            энергии привычки, смогут познавать и видеть бессубъективность, когда не будут больше
            зависеть от имени, и даже от имени «блаженство самадхи», когда пробудятся для
            высшего
            царства. Пробудившийся в этом царстве понимает смысл достижения Благородной
            Мудрости, он
            уже не будет заблуждаться.</p>

        <p>Тогда буду говорить об единстве пути и повозки. Махамати, подлинное полное понимание
            единства пути и повозки, не может быть достигнуто без того, чтобы не прийти к этому
            состоянию. Его не моет подарить никто, даже сам великий Брахман, оно достигается
            лишь
            Татхагатами. Вот по этой причине я и говорю о единстве пути и повозки. Я не говорю
            об
            этом потому, что лучше всего идти к цели через рассуждения о состоянии нирваны.</p>

        <p>Затем Махамати попросил Благословенного, чтобы он рассказал о тех шагах, которые
            нужно
            пройти просветленному ученику для самореализации Благородной Мудрости.
            Благословенный
            ответил: - Начало состоит в понимании того, что внешний мир есть лишь проявление
            самого
            разума, что разум схватывает во внешнем мире то, что вне привычки к различению и к
            ложному различению. Ученик должен вести себя прилично, смотреть на вещи подлинно,
            понимать тот факт, что мир не от собственной природы, что он не рожден, что подобен
            проходящему мимо нас облаку, что он -- воображаемое колесо, которое мы видим при
            вращении головни. Он подобен Гандхарве, подобен луне, отраженной в водах океана,
            подобен
            видению, миражу, сну.</p>

        <p>Ученик должен прийти к пониманию, что ум в своей сущности пуст. Он не должен слушать
            рассуждений, основывающихся на воображаемых терминах различения. Он должен понимать,
            что
            универсальный разум имеет чистую сущность состояния безобразности, что лишь
            вследствие
            накопления осквернений на его поверхности телесные и местные свойства кажутся его
            проявлениями. В то время, как его собственная природа чиста, не подвержена никаким
            изменениям: ни подъему, ни падению, ни пребыванию в каком-либо месте, ни
            разрушению.</p>

        <p>Ученик должен полностью понимать, что подобные вещи приходят к существованию лишь в
            связи
            с представлением о субъекте (либо об атмане), которое заполняет сознательный разум.
            Итак, Махамати, пусть те ученики, которые желают осознать Благородную Мудрость,
            следуют
            по пути Татхагаты, отвлекутся от реальной необходимости всяческих различений, ложных
            рассуждений, о таких понятиях, как элементы, образующие агрегат личности и ее мир
            чувственности, о таких идеях, как причинность, возникновение, пребывание и
            разрушение.
            Пусть они упражняются в дисциплине дхьяны, которая ведет к осознанию Благородной
            Мудрости. Для упражнения в сосредоточении ревностный ученик должен удалиться в
            спокойное
            и уединенное место вспоминать привычки своей длинной жизни и прежних жизней, чтобы
            уловить различающее сознание в его естество, так чтобы можно было от него избавиться
            легко и спокойно, хотя самое это действие происходит не так то легко и не так-то
            быстро.</p>

        <p>Различают четыре рода сосредоточения и медитации (дхьяны). А это такие: дхьяна,
            практикуемая несведущими; дхьяна, посвященная изучению значения; дхьяна на образе
            таковости объекта в качестве объекта (татхата); и, наконец, дхьяна Татхагат.</p>

        <p>Дхьяна, практикуемая несведущими, представляет собой упражнение для тех, которые
            следуют
            примеру учеников и учителей, они тем самым не понимают ни смысла, ни цели этих
            примеров,
            и поэтому как бы сидят впустую, с пустыми умами. Дхьяна практикуется также теми,
            которые
            ненавидят собственное тело, смотрят из него как на тень, либо на скелет, полный
            страдания и нечистот, которые цепляются за понятия субъекта, стремятся к
            освобождению --
            только бы перестать мыслить и перестать испытывать психические отражения.</p>

        <p>Дхьяна, посвященная изучению значений, представляет собой упражнение для тех, которые
            поняв несостоятельность всех и всяческих идей, подобных идеям субъекта, иного и
            обоих,
            стремятся к аннигиляции тех самых идей, которые развивают философы, стремятся к
            тому,
            чтобы выйти за пределы двучленного представления о субъекте и объекте и прийти к
            двузначному представлению об отсутствии субъекта. Дхьяна представляет собой силу
            упражнений по изучению значений состояния бессубъективности и различению
            последовательных этапов состояния бодхисаттвы.</p>

        <p>Дхьяна о Татхагате, или таковости или же о единстве, или же о Божественном имени в
            качестве субъекта, практикуется теми ревностными учениками и учителями, которые
            полностью распознавши двоичную природу бессубъективности, безобразности Татхагаты,
            однако продолжают цепляться за представление о трансцендентальной Татхагате.</p>

        <p>Дхьяна представляет собой медитации тех, которые, вступив на стадию татхагатства,
            пребывают в троичном блаженстве, характеризуемом самосознанием Благородной Мудрости,
            посвящают себя благу всех живых существ и совершают неимоверно трудные подвиги для
            их
            освобождения. Это и есть чистейшая дхьяна Татхагат.</p>

        <p>Когда все меньшие вещи и идеи оказываются превзойденными и забытыми, остается лишь
            совершенное состояние безобразности, где Татхагата и Татхата сживаются в совершенном
            единстве.</p>

        <p>Тогда все Будды сходятся совместно, пребывая из всех стран Будды, и сияющими руками
            благословляют главу того, кто может быть восславлен как новый Татхагата.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава VII. Достижение самореализации</h3>

        <p>Затем Махамати попросил Благословенного: - Молю тебя, поведай нам, что представляет
            собой
            состояние самореализации?</p>

        <p>Благословенный ответил: - В жизни ревностного ученика можно различить два аспекта,
            которые существенно отличаются друг от друга, а именно: состояние постижения
            собственной
            природы, возникающей из различения себя и сферы сознания, которое оказывается
            связанным
            с субъектом, и второе, превосходное и возвышенное состояние самореализации
            благородной
            мудрости.</p>

        <p>Состояние достижения различения собственной природы вещей, идей и самости
            сопровождается
            переживанием удовольствия и неудовольствия, которое соответствует опыту или может
            быть
            анализировано и потому изложено в книгах по логике.</p>

        <p>Ставя себя в соответствие с представлениями о бессубъективности вещей, и отбрасывая
            ложные взгляды о собственной субъективности, можно отказаться от подобных мыслей и
            твердо держаться в непрерывно восходящем странствии по ступеням совершенства.</p>

        <p>Возвышенное состояние реализации, имеющее своим субъектом ревностного ученика в
            состоянии
            умственной концентрации, в котором он стремится отождествлять себя с Благородной
            Мудростью, представляет собой следующее: это усилие, которым учение стремится
            уничтожить
            все блуждающие мысли о представлениях, принадлежащих лишь внешней видимости вещей, а
            также все идеи об индивидуальном и общем, о страдании и непостоянстве.</p>

        <p>Вместо этого он должен культивировать все благороднейшие идеи об отсутствии «я», о
            пустоте и безобразности. Так он постепенно достигает осознания истины, свободной от
            чувств и навсегда безмятежной. Когда это активное усилие умственной концентрации
            окажется успешным, за ним последуют более пассивное, восприемлющее состояние
            самадхи, в
            котором ревностный ученик окажется введенным в блаженное вместилище Благородной
            Мудрости
            и испытывает плоды всяких превращений самапатти. Здесь ревностный ученик, прежде
            всего,
            испытывает возвышенное состояние реальности, однако при этом он еще не сумеет
            отказаться
            от энергии привычки, которая сможет вовлечь его в дальнейшее преобразование,
            связанное
            со смертью.</p>

        <p>Достигши этого возвышенного и блаженного состояния реализации, бодхисаттва не должен
            предаваться никаким наслаждениям, связанным с блаженством, потому что для подобных
            наслаждений неизменно предполагается их прекращение. Он должен с сочувствием думать
            о
            других существах и вечно хранить свежесть и чистоту своих первичных обетов.</p>

        <p>Он никогда не должен успокаиваться, никогда не должен наслаждаться блаженством
            самадхи.</p>

        <p>Однако, Махамати, как только ревностный ученик примет надлежащие меры к
            усовершенствованию на пути, ведущем к полной реализации, его начнет подстерегать
            опасность, против которой нужно быть настороже.</p>

        <p>Ученик может не до конца осознавать, что система разума из-за накопленной энергии
            привычки продолжает функционировать более или менее подсознательно, причем это
            продолжается в течение всей жизни.</p>

        <p>Ученик может иногда думать, что он полностью достиг желанной цели успокоения,
            полностью
            подавив активность системы разума. О, это ошибка, ибо, даже если активность ума
            подавлена, он по-прежнему продолжает функционировать, поскольку семена энергии
            привычки
            по-прежнему остаются в нем.</p>

        <p>То, что предполагается в качестве успокоения (амала), на самом деле является лишь не
            функционированием внешнего мира ума, т.е. мира вещей, к которым воля не питает
            привязанности. Итак, цель успокоения достигается не путем подавления всяческой
            активности ума, но лишь путем отказа от всех различений и всех привязанностей. Затем
            следует сказать, что имеются некоторые, кто боится страданий, связанных с
            различениями
            смерти и рождения: из страха эти немудрые единственно ищут нирваны.</p>

        <p>Они все же видят, что всякие вещи, подверженные различению, лишены реальности, и
            потому
            воображают, что нирвана представляет собой уничтожение чувств и сфер чувствования;
            они
            не понимают, что рождение и смерть с одной стороны, и нирвана, с другой, вовсе не
            отделены друг от друга. Они не знают нирваны как универсального разума в его
            чистоте.</p>

        <p>Поэтому такие глупые ученики, цепляющиеся за понятия нирваны как мира в себе,
            расположенного вне нас и зримого лишь одним умом, пренебрегают учением Татхагаты,
            касающимся внешнего мира, они по-прежнему продолжают вращаться в колесе рождений и
            смертей. Но когда они поймут глубинное учении о поворотном пункте в самой глубокой
            седловине сознания, тогда лишь придут к совершенной самореализации Благородной
            Мудрости,
            тогда лишь они начнут понимать. Подлинное функционирование ума представляет собой
            очень
            тонкую и трудную материю для понимания юных учеников. Даже учителя со всеми силами
            правильного понимания и способности самадхи нередко терпят поражение в попытках
            подобного понимания.</p>

        <p>Лишь Татхагаты и бодхисаттвы, твердо укрепившиеся на седьмой ступени просветления,
            могут
            полностью понять особенности работы ума. Эти ревностные ученики и учителя, которые
            желают полностью понять все аспекты различных стадий этой работы как состояния
            бодхисаттвы, при помощи верного знания должны добиваться полного самоубеждения в
            том,
            что все объекты различений представляют собой лишь видимости для ума, таким образом,
            они
            будут держаться в стороне от всяческих различений и ложных рассуждений, которые
            также
            представляют собой плоды активности ума. Они будут стремиться видеть вещи лишь в их
            истине, и, насаждая корни блага в землях Будды, не будут знать никаких пределов в
            виде
            вещей, заставляющих прибегать к различениям. Для достижения всего этого бодхисаттва
            должен стремиться к уединению, спокойствию, он должен избегать волнений, социального
            возбуждения. Пусть избегает состояния сонливости, пусть не стремиться к чтению
            многоумных трактатов и писаний мирских философов, пусть избегает общественных
            обрядов и
            церемоний, профессиональных священнослужителей. Он должен удаляться в уединенные
            места,
            в леса или на годы и предаваться практике различных духовных упражнений. Лишь таким
            путем он сумеет достигнуть в этом мире многообразия подлинного понимания того, как
            действует универсальный разум в своей сущности. Лишь окружив себя Буддами, словно
            лучшими друзьями, ревностный ученик станет способным понимать значение разума и его
            место в качестве опосредующего агента между внешним миром и универсальным
            разумом.</p>

        <p>Тогда он сумеет пересечь океан смертей и рождений, в котором возникают все вещи
            вследствие неведения, желания и деятельности. Добившись полного понимания того, как
            функционирует система разума, в предсубъективный период и в двоичной
            бессубъективности и
            утвердившись в собственной маре самореализации, не связываясь и привязанностью,
            достигаемой благодаря упражнениям, он пожнет все плоды, получаемые в результате
            правильного знания. Таким образом, он расчистит себе путь к дальнейшим успехам в
            состоянии бодхисаттвы и будет продвигаться по всем ступеням состояния бодхисаттвы.
            После
            этого ученику следует отказаться от понятия разума, который не достигается путем
            правильного знания, и которые в сравнении с Благородной Мудростью напоминают лишь
            поступь хромого осла. Тогда он взойдет на восьмую ступень состояния бодхисаттвы и
            сам
            себя научит Благородной Мудрости в ее трех аспектах.</p>

        <p>Эти аспекты следующие:</p>

        <ul class="list-disc pl-5 space-y-1 ml-8">
            <li class="my-2">во-первых, безобразность, которая появляется, когда все вещи,
                подлежащие рассмотрению в состоянии ученичества, учительства и философствования,
                полностью оказываются в его распоряжении, подчиненными его пониманию;
            </li>
            <li class="my-2">во-вторых, сила, которая дается ему всеми буддами, благодаря
                изначальным обетам, включая отождествление их жизни и их удела, с уделами и
                жизнью
                всех живых существ;
            </li>
            <li class="my-2">в-третьих, совершенная самореализация, т.е. такая, которая здесь
                может
                быть достигнута лишь в определенной степени.
            </li>
        </ul>

        <p>По мере того, как бодхисаттва преуспевает в отвращении от частных взглядов на вещи,
            включая и его собственную воображаемую субъективность, по мере того, как он понимает
            феноменальность всего и осознает состояния самадхи и самапатти, по мере того, как он
            смотрит на мир, как на видение и сон, по мере того, как он получает поддержку всех
            Будд,
            - бодхисаттва оказывается способным прийти к полному достижению состояния Татхагаты,
            которое и есть сама Благородная Мудрость. Таковы три составных части Благородной
            жизни,
            и эта троичнось приводит к совершенной самореализации Благородной Мудрости.</p>

        <p>Затем Махамати спросил у Благословенного: - Благословенный, будет ли очищение от злых
            истечений разума, которые возникают из-за цепляния за понятия объективного мира и
            эмпирического субъекта, будет ли это очищение постепенным или мгновенным.
            Благословенный
            ответил: - Различают три характерных истечения разума, а именно: злостное истечение,
            возникающее из-за жажды схватывания и привязанности, злостное истечение, возникающее
            вместе с иллюзиями разума и ненасытности эгоизма, и благие истечения, возникающие из
            Благородной Мудрости. Злостное истечение, происходящее в результате распознавания
            объектов внешнего мира, которые, по сути, являются лишь проявлениями ума, и к
            которым
            возрастает привязанность, может быть постепенно очищено.</p>

        <p>Здесь мгновенность недостижима. Доброе поведение может лишь очистить нам путь к
            ограничению и усиленному самоконтролю. Мы можем привести здесь пример горшечников,
            которые постепенно делают каждый горшок путем внимания и верно направленных усилий.
            Это
            также подобно мастерству комедии, где танцуют, поют, играют на флейте, подобно также
            искусству письма и всяким другим искусствам, но все эти искусства и умения можно
            приобрести лишь постепенно и путем трудолюбия и усердия. Наградой будет чистое
            понимание
            пустоты и преходящести всех вещей.</p>

        <p>Злостное истечение, возникающее из-за иллюзии разума и ненасытного эгоизма, относится
            к
            умственной жизни более непосредственно, в нем находятся такие вещи, как страх, гнев,
            ненависть и гордыня. Они также могут быть удалены, и очиститься от них можно путем
            изучения или медитации, т.е. все это достигается постоянно, но никак не мгновенно.
            Их
            можно уподобить плоду манго, который созревает очень медленно и постепенно. Они
            подобны
            траве, кустарникам, ветвям и деревьям, которые медленно и постепенно вырастают из
            земли.</p>

        <p>Нужно чтобы каждый следовал по пути изучения и сосредоточения постепенно, и
            собственными
            усилиями достигая успеха за успехом. Однако, благодаря первоначальным обетам
            бодхисаттв
            и всех татхагат, посвятивших себя благу и отождествивших жизнь с жизнями всех живых
            существ, которые должны быть ранее освобождены, можно получить от них поддержку и
            ободрение, однако даже при помощи татхагат очищение от злостных истечений разума все
            равно будет медленным и постепенным, оно требует энергии и терпения.</p>

        <p>Наградой будет постепенное понимание двоичной бессубъективности и терпеливое принятие
            этого как факта. Все же это приведет к подножию великого возвышенного пути к
            состоянию
            бодхисаттвы.</p>

        <p>Однако, благие неистечения, которые происходят вследствие самореализации Благородной
            Мудрости, представляют собой форму очищения, возникающего мгновенно и благодаря
            благодати татхагат.</p>

        <p>Одно подобно зеркалу, отражающему все формы и образы мгновенно и без различия того,
            что
            ему предстоит. Оно подобно солнцу или луне, открывающим все формы вещей, благодаря
            истекающему от них свету, мгновенно и беспристрастно освещающим все своим светом.
            Точно
            также татхагаты ведут ревностных учеников к состоянию безобразности. Все
            накопившиеся
            запасы энергии привычки и карты, которые собирались с безначальных времен благодаря
            привязанности к ложным взглядам, сохраняются благодаря приверженности учению о
            субъекте
            и о реальности объектов внешнего мира. Все эти вредные запасы уничтожаются, когда
            мгновенно раскрывается царство трансцендентального понимания, принадлежащее
            состоянию
            Буддовости.</p>

        <p>Точно также как универсальный разум, оскверненный накоплением энергии привычки и
            кармы,
            раскрывается во множественности субъектов и внешних объектов или целевых внешним
            миров,
            представляющих собой плоды воображения, - универсальный разум может быть очищен от
            всех
            этих загрязнений благодаря постепенному его очищению от злостных истечений,
            достигаемому
            путем усилий и изучения, сосредоточения, постепенной самореализации Благородной
            Мудрости.</p>

        <p>В конце концов, подобно тому, как сияет дхарма Будды, мгновенно пробиваясь лучами,
            точно
            также будет сияющей собственная природа, которая мгновенно засверкает в нас.
            Благодаря
            ей осознание у всех бодхисаттв достигается единым мигом, они оказываютс в своих
            царственных дворцах, на небесах Аканшите, все они мгновенно начинают излучать
            разнообразные сокровища духовного изобилия.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава VIII. Плод самореализации</h3>

        <p>Затем Махамати попросил у Благословенного, чтобы он поведал о том, каков плод
            самореализации Благородной Мудрости.</p>

        <p>Благословенный ответил: - Во-первых, приходит ясное понимание смысла и значения всех
            вещей, затем следует отчетливое осознание значения духовных идеалов, или парамит,
            благодаря чему бодхисаттва оказывается способным более глубоко войти в сферу
            безобразности, оказывается способным испытать высшее состояние самадхи и постепенно
            перейти к еще более высоким ступеням состояния бодхисаттвы. Испытавший опыт
            пребывания в
            поворотной точке, в глубочайшей седловине нашего сознания, бодхисаттва перейдет к
            упражнению в прочих самадхи, вплоть до высочайшей самадхи, называемой «ваджрапами»,
            которая принадлежит лишь опыту Татхагат в их превращениях. Бодхисаттва окажется
            способным войти в сферу сознания, лежащую вне сферы осознания системами разума, т.е.
            в
            сферу сознания Татхагатовости.</p>

        <p>Он окажется наделенным силами, или же психическими способностями самообладания,
            любовного
            сострадания, искусного умения делать верное и способностью вступать во все земли
            Будды.</p>

        <p>Прежде чем достигнуть самореализации Благородной Мудрости, он будет по-прежнему
            оставаться под влиянием субъективных интересов (эгоизма), но после того как он
            достигнет
            самореализации, он сумеет спонтанно реагировать на импульсы своего великого и
            сострадательно сердца, и будет располагать искуснейшими и безграничными средствами
            искреннего и целостного служения, освобождения всех существ.</p>

        <p>Затем Махамати попросил Благословенного рассказать о том, сохраняются ли силы
            Татхагат
            после того, как они достигают самореализации Благородной Мудрости.</p>

        <p>Благословенный ответил: - Есть два рода поддерживающих сил, которые находятся в
            распоряжении Татхагат и которые они предоставляют бодхисаттвам. Поддерживаемые этими
            силами, бодхисаттвы могут оказаться перед ними и оказать им честь путем задавания
            вопросов. Первым родом поддерживающей силы является собственное почитание и вера
            бодхисаттв Будде, благодаря чему Будды оказываются способными являться и оказывать
            поддержку своими собственными руками, нуждающимся в ней.</p>

        <p>Вторым родом поддерживающей силы является сила, которая истекает от Татхагат: эта
            сила
            позволяет бодхисаттвам достигать различных самадхи и самапатти и проходить сквозь
            них,
            не оскверняя своего блаженства.</p>

        <p>Будучи поддержан силой всех Будд, бодхисаттва даже на первой стадии оказывается
            способным
            достигать самадхи известной как «свет махаяны».</p>

        <p>В этой самадхи бодхисаттва оказывается способным осознавать присутствие Татхагат,
            приходящих из шестидесяти стран света в самых различных вместилищах. Он может
            наделять
            этой силой понятий, где мы можем получить лишь мирские блага, нужно лелеять в душе
            идеалы милосердия, благого поведения, терпения, энергичности, глубокомыслия и
            мудрости.
            Даже в мирской жизни практикование подобных добродетелей принесет в награду счастье
            и
            успех.</p>

        <p>Тем более в мире разума ревностные ученики и учителя должны практиковать то, что
            принесет
            им радость освобождения, просветления и мира, поскольку здесь эти парамиты
            основываются
            на правильном знании и ведут к пониманию нирваны, т.е. даже в том случае, если
            нирвана
            представляет собой лишь «мысль в себе».</p>

        <p>В мире разума парамиты становятся еще более идеальными и еще более близкими нашему
            чувствованию.</p>

        <p>Милосердие не должно более выражаться в том, чтобы отдавать нелицеприятные
            материальные
            дары, оно предполагает раздачу еще более драгоценных даров сочувствия и понимания.
            Благое поведение означает не только внешнее соблюдение пяти этических предписаний,
            но
            также и сознательное практикование скромности, простоты, самоограничения и
            самоотдачи в
            свете парамит. Терпение означает для всякого не только прощение других во внешних
            обстоятельствах и применимость друг к другу и к другим людям.</p>

        <p>Теперь нас уже призывают к терпению в отношении самого себя. Энергичность (вирвя)
            означает нечто большее, чем трудолюбие, либо внешнее проявление усердия. Она
            потребует
            значительно большего самоконтроля, поскольку задача стоит в том, чтобы следовать по
            пути, и в том, чтобы проявить всю дхарму в собственной жизни. Глубокомыслие означает
            глубокую сознательность, при которой все попытки различения значений, логической
            дедукции и рационализации уступают место интуиции в значении и в духе. Парамита
            мудрости, или праджняпарамита, больше не может рассматриваться как фактическая
            мудрость,
            или как чистая эрудиция. Она раскрывается в подлинном совершенстве всевключающего
            знания, которое есть любовь.</p>

        <p>Третий аспект парамит может быть рассматриваем в их идеальном совершенстве для
            Татхагат и
            может быть полностью понят лишь бодхисаттвами-махасаттвами, которые посвятили себя
            высшей духовной дисциплине, полностью осознали, что нет ничего в мире, что можно
            было бы
            видеть и что не исходило бы лишь от самого себя. В умах таких бодхисаттв-махасаттв
            все
            различения, все представления обо всей двойственности перестают функционировать.</p>

        <p>Схватывание и цепляние за вещи не проявляется.</p>

        <p>Освободившись от всяких привязанностей к единичным объектам и идеям, они в своих умах
            оказываются свободными и могут рассматривать пути благодетельствования и наделения
            счастьем других, в том числе всех разумных существ. Для бодхисаттвы-махасаттвы идеал
            милосердия означает отказ от собственной надежды на достижение состояния Татхагаты и
            нирваны, чтобы этим достижением могли воспользоваться другие.</p>

        <p>Сохраняя все связи с внешним объективным миром, Татхагаты в своем уме, тем не менее,
            не
            проводят различения между собственными интересами и интересами других, между добром
            и
            злом. У них действуют в умах чистая спонтанность и активность в совершенном
            поведении
            без приложения каких бы то ни было усилий. Для практикования терпения с полным
            знанием
            этого, с пониманием того, что схватывание и схватывающее суть ничто иное, как мысли,
            связанные с различением и с привязанностью, Татхагаты развивают в себе парамиту
            терпения.</p>

        <p>Для проявления энергии терпения они бодрствуют с первой частичности и до конца ночи,
            в
            соответствии с дисциплинированными правилами, у них при этом возникает способность
            действовать, не проводя никаких различий между удобствами и неубодствами. Так у них
            развивается парамита энергичности Татхагаты. У них нет также различения между собой
            и
            прочими о мыслях о нирване. Они крепятся умом, привязанным к нирване, направленным
            на
            нее. Это означает парамиту глубокомыслия.</p>

        <p>Что же касается праджняпарамиты, которая есть сама Благородная Мудрость, никто не в
            состоянии описать ее. Когда в самадхи ум перестает различать, остается лишь
            совершенное,
            исполненное любви состояние безобразности -- тогда и испытывается опыт поворотной
            точки,
            находящейся в глубочайшей глубине нашего сознания и для того, что достигнет
            самореализации Благородной Мудрости, это и будет праджняпарамитой.</p>

        <p>Затем Махамати попросил у Благословенного, чтобы тот рассказал о «тонком теле», о
            теле
            чистого умственного видения (маномайякая), которые способны приобрести бодхисаттвы,
            глядящие на мир самореализации Благородной Мудрости.</p>

        <p>Благословенный ответил: - Махамати, различают три разновидности трансцендентальных
            тел.
            Первое -- это тело, которое бодхисаттва приобретает в опыте самадхи и самапатти;
            второе
            -- это тело, которое приобретается от Татхагаты, в соответствии с классом живого
            существа, нуждающегося в поддержке.</p>

        <p>Это тело приобретается и совершенствуется спонтанно, без всяких усилий к достижению
            его,
            без всякого напряжения. Третье -- тело -- это единое тело для всех Татхагат, в
            котором
            они обретают интуицию Дхармакайи.</p>

        <p>Трансцендентальная личность, которая приходит к опыту самадхи, вступает на третью,
            четвертую, пятую стадии сосредоточения, когда все умствования в системе ума
            оказываются
            успокоенными и волны сознания больше не возбуждаются на поверхности универсального
            ума.
            Это состояние знающего ума, в котором он все-таки сознает собственное наличие в
            некоторой мере, это состояние блаженства испытывается при прекращении всяческой
            деятельности разума. Второй род трансцендентальной личности -- это тот, к которому
            принадлежат бодхисаттвы и татхагаты, как тела, подвергающиеся трансформации, при
            которой
            они являют свою верность обетам, принятую вначале в действии и в совершенствовании.
            Этим
            достигается на восьмой ступени состояние бодхисаттв, когда бодхисаттва обладает
            всепроникающей интуицией, прозревающей иллюзорную природу вещей. Когда он понимает
            дхарму безобразности, то обретает опыт поворотной точки в глубинах своего сознания и
            после этого оказывается способным испытывать высшие самадхи, вплоть до самой
            высочайшей.</p>

        <p>Вступив в сферу этих возвышенных самадхи, он достигает состояния такого неличного
            бытия,
            которое превосходит умственное сознание.</p>

        <p>Благодаря чему он приобретает естественную способность владения собой и способность
            действовать в том направлении, в котором желает, не прилагая никаких усилий, с
            быстротой, подобной быстроте мысли, и с такой мгновенностью, с какой могут сменять
            друг
            друга образы в зеркале.</p>

        <p>Это трансцендентальное тело не является плодом сочетания элементов, но оно все же в
            каком-то смысле аналогично тому, что может быть произведено: оно обладает некоторыми
            отличиями, которые проявляются и во внешнем мире форм, однако оно не имеет
            ограничений,
            связанных с подобными отличиями.</p>

        <p>Лишь обладание таким умственным телом делает возможным присутствие во всех собраниях
            и во
            всех странах Будды. Подобно тому как мысли мгновенно перемещаются без всяких
            препятствий
            с места на место, проникают сквозь стены, пересекают реки, проходят сквозь деревья и
            горы, подобно тому как память может продвигаться в прошлое и вспоминать некоторые
            сцены
            прошлого опыта -- также точно и ум, который продолжает функционировать в теле, может
            мысленно продвигаться в любом направлении на сотни тысяч йоджан. Точно также
            трансцендентальная личность, испытывающая состояние самадхи (ваджрабимба-упама),
            может
            быть наделена сверхъестественными способностями и психическими силами самовладения,
            благодаря чему она оказывается способной двигаться по благородному пути, ведущему в
            собрание Будд, причем двигаться свободно, по собственному желанию.</p>

        <p>Однако это желание более не является сосредоточенным на субъекте, это желание не
            связано
            с различениями и привязанностью. Для такой трансцендентальной личности более нет ее
            старого тела.</p>

        <p>Ее трансцендентальное воплощение соответствует духу принятых на себя обязательств
            принесения блага всем живым существам вплоть до их созревания для освобождения.</p>

        <p>Третий разряд трансцендентальных личностей настолько неописуем, что их можно осознать
            лишь благодаря интуиции дхармакайи, т.е. благодаря достижению интуиции безграничного
            и
            неразрушимого сознания универсальной разумности. Как только бодхисаттва-махасаттва
            достигает высочайших ступеней, он начинает понимать содержимое всех сокровищ
            Благородной
            Мудрости. Он приобретает непостижимые тела, превращаемые во все потребное, поскольку
            это
            есть подлинная природа всех Татхагат прошлого, настоящего и будущего.</p>

        <p>Так он оказывается сопричастным блаженному, мирному состоянию, пронизанному дхармой
            всех
            будд.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава IX. Об ученичестве и преемственности
            архатов</h3>

        <p>Затем Махамати попросил Благословенного рассказать о разновидностях и разрядах
            учеников.</p>

        <p>Благословенный ответил: - Существует много разновидностей и разрядов учеников,
            поскольку
            все они -- индивиды. Однако для удобства их можно разбить на две группы: учеников с
            преемственностью от архатов и учеников, известных как бодхисаттвы. Учеников с
            преемственностью от архатов могут быть рассмотрены опять таки в двух аспектах:</p>

        <p>во-первых, согласно периоду развития и количеству раз, которое им еще предстоит
            вернуться
            в эту жизнь вследствие перерождения;</p>

        <p>во-вторых, согласно степени их духовного прогресса. Их можно подразделить еще на три
            группировки: вошедших в поток, возвращающихся один раз и не возвращающихся.</p>

        <p>«Вошедшие в поток» - это такие ученики, которые освободились от привязанностей к
            низшим
            различениям, которые очистились от препятствий в этой жизни и полностью понимают
            значение аппата (бессубъективность), которые больше не цепляются за понятия
            индивидуального и общего ради собственного «я». Они настолько продвинулись по пути,
            что
            достигли шестой ступени совершенствования и уже готовы войти в состояние блаженства
            самадхи, они будут перерождены еще семь раз, либо пять раз, либо три раз, прежде чем
            смогут взойти на шестую ступень совершенствования.</p>

        <p>Однажды возвращающиеся -- это архаты, а невозвращающиеся -- бодхисаттвы, которые
            достигли
            седьмой ступени. Причина, по которой эти градации существуют, состоит в том, что
            имеются
            три ступени ложного воображения, а именно: вера в практическую мораль, сомнение и
            особый
            взгляд на свою индивидуальную личность.</p>

        <p>Когда все эти три препятствия оказываются превзойденными, можно достичь более высоких
            ступеней.</p>

        <p>Что касается практической морали, то несведущие и простые умом ученики подчиняются
            правилам морали милосердия, благочестия, покаяния и т.п. Поскольку желают таким
            способом
            достичь некоторых успехов в этом мире и найти счастье. Они надеются, что в следующий
            раз
            благодаря таким подвигам переродятся в более благоприятных условиях. Вошедшие в
            поток
            это те, которые не цепляются за моральную практику или надежду на награды, которые
            зафиксировали свои умы, направили их на возвышенное состояние самореализации.
            Причина,
            по которой люди посвящают себя моральным подвигам, состоит в том, что они желают
            избавиться таким способом от вредных истечений. Что же касается препятствий в виде
            сомнений в учении Будды, то они, эти сомнения, сохраняются до тех пор, пока не
            исчезнут
            все представления о различиях, которые делаются в разуме, когда же они исчезнут, то
            исчезнут и сомнения. Привязанность к взглядам на личность искореняется лишь
            вследствие
            того, что ученик приобретает более глубокое представление о бытии и небытии в их
            собственной природе и об отсутствии «я». Тем самым он отделывается от привязанности
            к
            собственному «я», от эгоизма, перестает отличать себя от других.</p>

        <p>Очистившись от трех привязанностей и освободившись от трех препятствий, вошедшие в
            поток
            оказываются свободными от жадности, гнева и глупости.</p>

        <p>Что касается однажды возвращающихся архатов, то для них прежде существовали
            различения
            форм, знаков, видимостей, но после того, как они постепенно преуспели в правильном
            понимании и перестали смотреть на единичные объекты в аспекте качества и различения
            качеств, после того, как они познакомились с учением о том, что знаки достижения,
            связанные с практикой дхьяны, по сути, пусты, они достигли такой стадии
            просветления,
            что для них больше нет нужды в перерождениях, и они способны положить конец этой
            жизни,
            связанной с цепляниями за собственные интересы.</p>

        <p>Свободные от груза ошибок и привязанностей, от чувств, которые с ними связаны, они
            навсегда свободны на пути вперед.</p>

        <p>Во втором аспекте учеников можно сгруппировать по ступеням достигнутого ими духовного
            прогресса. Здесь можно выделить четыре класса, а именно: учеников (шравана),
            учителей
            (паратьякабудда), архатов и бодхисаттв. Первый класс (учеников) объединяет тех, для
            которых оказалось трудным освоить незнакомые идеи. Их умы получают удовольствие от
            изучения и практикования вещей, относящихся к видимости того, что может быть
            подвергнуто
            различению. Однако они приходят в смятение от представления о непрерывной цепи
            причинности, они боятся, что, если рассматривать вместо субъекта агрегат, состоящий
            из
            дхарм, а вместо объективного мира -- нечто подобное иллюзии, пустой и лишенной «я»,
            то
            из этого им никак не выпутаться. Они могут продвинуться до пятой и шестой ступеней,
            на
            которых они способны избавиться от возбуждения чувств, однако у них нет возможности
            дойти до представления о том, что является причиной подобного возбуждения чувств.
            Поэтому они не в состоянии отделаться от цепляния за субъект и сопровождающие его
            привязанности, привычки и накопление энергии привычки. К тому же классу «учеников»
            относятся ревностные ученики иных вер и убеждений, которые цепляются за такие
            представления, как «атман», или душа в виде вечной сущности, верховный Атман, или
            личный
            бог и т.п., которые стремятся к нирване, находящейся в соответствии с этими
            представлениями. Есть иные ученики, более материалистически настроенные в своих
            представлениях, которые думают будто все вещи существуют в зависимости от причин и,
            стало быть, что нирвана также должна быть в зависимости от чего-либо.</p>

        <p>Но никто из этих учеников, какими бы ревностными они ни были, не может достигнуть
            понимания истины двузначной бессубъективности, и поэтому они ограничены в своих
            духовных
            прозрениях свободы и несвободы. Для них не может быть подлинного освобождения. Они
            не
            слишком доверяют самим себе, не могут приобрести подлинного знания нирваны, и не
            приобретут до тех пор, пока не научатся терпеливо принимать двувидную
            бессубъективности.</p>

        <p>Второй класс (учителей) -- это те, которые при высокой степени интеллектуального
            понимания истины, касающейся тех агрегатов, которые образуют личность, и внешнего
            мира
            соответствующего им, все еще исполнены страхом, в особенности, когда им приходится
            встретиться лицом к лицу со всей значимостью последствий, вытекающих из этих истин,
            и
            всех тех требований, которые накладываются на тех, кто понимает эти истины. Они хотя
            уже
            не привязаны к внешнему миру и его многообразным формам, представляющим возможность
            удовольствия и неудовольствия, а также проявления власти, но, тем не менее, боятся
            вновь
            запутаться во внешних обстоятельствах и социальных обязательствах. Их привлекают
            возможности, предоставляемые чистым делением, а именно: их привлекает обладание
            таинственными и сверхъестественными способностями, которыми наделена личность,
            проявляющаяся в разных местах в одно и то же время. Их привлекает возможность
            изъявления
            различных превращенных форм из своих тел.</p>

        <p>Для того, чтобы приобрести подобные силы и способности, они предаются уединенной
            жизни;
            однако этот класс учителей никогда не оказывается свободным от искушений, связанных
            с
            образованностью и эгоизмом. Поэтому их управления всегда находятся в соответствии с
            определенным характером того или иного ограничения. К их числу относится и множество
            ревностных учеников, которые обнаруживают высокую степень духовного просветления,
            характеризуются искренностью и желанием соответствовать всем требованиям,
            предъявленным
            им на отдельных ступенях. Когда они видят, что все образующее объективный мир, есть
            ничто иное, как проявление нашего разума, т.е. не имеет субъективной природы, но
            рождено
            и безлично, то принимают это без страха, когда же они видят, что их собственная душа
            также пуста, не рождена и субъективна, они тревожатся этим, приходят в смятение и
            строгими, целенаправленными стремлениями надеются устроить свою жизнь так, чтобы она
            вполне соответствовала требованиям благородных истин. Однако, все же они не могут
            забыть
            понятий, которые лежат в основе этих фактов, в особенности понятия сознательного
            «это»
            (субъекта) и его отношения к нирване. Поэтому они также относятся к классу вошедших
            в
            поток.</p>

        <p>Класс, известный как архаты, состоит из ревностных учителей, которые принадлежат к
            группировке однажды возвращающихся.</p>

        <p>Благодаря своему духовному прозрению, он достигают шестой и седьмой ступеней. Они
            полностью понимают истинную двоичность бессубъективности и безобразной реальности.
            Для
            них больше не существует никаких различений. Для них нет чувств, для них нет
            гордости и
            эгоизма. Они достигли возвышенного понимания глубины, они видят лишь безграничную
            реальность земель Будды.</p>

        <p>Путем внутреннего постижения собственной подлинной природы универсального разума, они
            постоянно очищаются от энергии привычки.</p>

        <p>Архат, достигший просветленности и вошедший в совершенство дхьяны и самадхи,
            направляет
            все свое внимание на достижение нирваны, однако, сама идея нирваны продолжает
            вызывать в
            нем ментальное возмущение, поскольку идея нирваны сама по себе ложна.</p>

        <p>Понятие нирваны раздваивается в его уме, и он отличает нирвану от себя самого, а себя
            от
            других. Он получает некоторые плоды самореализации, но по-прежнему думает и
            размышляет о
            дхьянах, о субъектах медитации, самадхи и их плодах. Он гордо говорит: «Все это
            путы,
            оковы, а я от них свободен». При этом он делает двойную ошибку: отрицает пороки
            «эго»,
            однако цепляется за его оковы.</p>

        <p>До тех пор, пока он продолжает различать понятия дхьяны, практики дхьяны, правильного
            знания и истины, до тех пор, пока его ум остается в состоянии заблуждения, он не
            получит
            полного освобождения. Освобождение приходит с представлением о безобразности. Он
            мастерски владеет дхьянами, он вступает в разные самадхи, однако, для достижения
            высших
            стадий он должен выйти за пределы дхьян, перейти в неизмеримое, в мир без форм, в
            мир
            блаженства самадхи и самапатти, ведуще к прекращению самого мышления.</p>

        <p>Практикующие дхьяну, сама дхьяна, субъект дхьяны, прекращение мышления, однократное
            возвращение и невозвращение, все это подразделяется в его уме и приводит его в
            состояние
            смятения. До тех пор, пока эти различения не будут покинуты, не может быть
            совершенного
            освобождения. Итак, архат, мастерски владеющий дхьянами, причастный самадхи, но не
            наделенный поддержкой Будд, не может войти в блаженство самадхи.</p>

        <p>Лишь при помощи поддержки Будд он вступает в нирвану. Ученик, учитель и архаты могут
            возноситься до шестой ступени просветления. Они думают, что этот троичный мир --
            ничто
            иное, как самый разум, они постигают, что здесь не к чему привязываться среди
            многообразия внешних объектов, поскольку все это -- плоды различительной активности
            самого разума. Они постигают, что здесь нет души, нет субъектов. Поэтому они в
            известной
            мере обретают успокоение, но это успокоение несовершенно, оно может быть мгновенным
            и не
            длится все их существование, ибо для них -- по-прежнему нечто производит результат,
            нечто схватывается, некто охватывает. В их сознании имеются некоторые следы дуализма
            и
            эгоизма. Хотя они свободны от активности функционирующих чувств, однако, привязаны
            энергией привычки к этим чувствам, отравлены вином самадхи, и пребывают в царстве
            злостных истечений. Совершенное успокоение возможно лишь на седьмой ступени. До тех
            пор,
            пока их умы находятся в смятении, они не могут прийти к ясной убежденности в том,
            что
            прекращение всякой множественности и всякой активности ведет к совершенному единству
            всех вещей. В их умах, как и в собственной природе людей, по-прежнему царят
            различения
            добра и зла. Поэтому их умы находятся в смятении, они не могут выйти за пределы
            шестой
            ступени.</p>

        <p>Однако и на шестой ступени всякое различение прекращается, лишь только они насыщаются
            благом самадхи и начинают лелеять мысль о нирване. Нирвана, насколько она возможна
            на
            шестой ступени, ими обретается: они вступают в свою нирвану, однако это вовсе не та
            нирвана, которая принадлежит Буддам.</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава Х. Состояние бодхисаттвы и его стадии</h3>

        <p>Затем Махамати спросил Благословенного о тех учениках, которые являются
            бодхисаттвами.</p>

        <p>Благословенный ответил: - Бодхисаттвы - это ревностные ученики, которые просветились
            благодаря своим усилиям по достижению самореализации Благородной Мудрости и которые
            приняли бы на себя обязанность просветлять других. Они пришли к ясному пониманию
            истины,
            что все вещи пусты, не рождены, подобны по своей природе иллюзии. Они перестали
            смотреть
            на вещи с различением и перестали рассматривать эти вещи в многообразных отношениях.
            Они
            полностью понимают истинное в двувидной бессубъективности, они приспособились к этой
            истине с терпеливым восприятием в себя, они достигли совершенной реализации
            безобразности, они пребывают в состоянии совершенного знания то, что достигается
            благодаря самореализации Благородной Мудрости. Отмеченные знаком «таковости», они
            вступают на первый этап пути бодхисаттвы. Первый этап называется «этапом радости»
            (прамудите). Вступившие на этот путь как бы переходят из мира светотени в царство, в
            котором нет теней. Это подобно переходу из этого места, где царят шум и смятение
            толпы
            многомиллионного города, в иное место, где царят спокойствие и тишина одиночества.
            Бодхисаттва чувствует внутри себя пробуждение щедрости великого сердца, в котором
            живет
            сострадание, и он принимает на себя десять первоначальных обетов: почитать всех Будд
            и
            служить им, распространять знания и практику дхармы, приветствовать всех будущих
            будд,
            практиковаться в шести парамитах, убеждать все живые существа принять дхарму,
            достигать
            совершенного понимания вселенной, достигать совершенного понимания взаимосвязанности
            живых существ, достигать совершенной самореализации всех Будд и Татхагат в природе
            измерения и в источнике их существования, овладеть всеми доступными средствами для
            исполнения этих обетов ради освобождения живых существ, добиваться высшего
            просветления
            благодаря полной самореализации Благородной Мудрости, восходить по ступеням, ведущим
            к
            состоянию Татхагаты. В духе этих обетов бодхисаттва постепенно восходит по ступеням,
            вплоть до шестой. Все ревностные ученики, учителя и архаты восходят одобным же
            путем,
            однако, они введенные в заблуждение блаженством самадхи и не располагая способностью
            и
            поддержкой, которые могут дать им лишь Будды, переходят в свою личную нирвану.</p>

        <p>Та же судьба ожидала бы и бодхисаттв, если бы не благодетельная поддержка Будд, им
            предоставляемая.</p>

        <p>Эта поддержка позволяет им отказываться от вступления в нирвану до тех пор, пока
            вместе с
            ними и прежде их не вступят в нирвану все живые существа.</p>

        <p>Татахагаты указывают им на добродетели буддства, которые недоступны пониманию
            интеллектуальным умам, они их поощряют и укрепляют в решимости не поддаваться
            соблазну
            наслаждения блаженством самадхи, в решимости множить усилия дальнейшему продвижению
            по
            ступеням просветления. Если бы бодхисаттва вступал в нирвану на этой стадии, он
            никогда
            не мог бы оказать помощь живым существам, а вступление в нирвану на этой стадии
            невозможно без помощи других будд, образующих несметное семейство Татахагат.</p>

        <p>Укрепление новой силой, приходящей им от Будд, обладая более пониманием того, что
            составляет природу и цель их продвижения и самореализации Благородной Мудрости, они
            вновь и вновь изучают природу системы разума, подвергают критическому осмыслению
            представление о субъективности личности и учитывают роль, которую имеют схватывание
            вещей, привязанность к вещам и инерции привычки к развертывающейся драме жизни. Они
            вновь и вновь критически анализируют иллюзорность четырехзначного логического
            анализа и
            критически рассматривают все элементы, входящие в природу просветления и
            самореализации.
            После этого они обретают новое в силе владения собой, и тогда лишь бодхисаттвы
            вступают
            в седьмую ступень -- «далекосяжности» (дурангама). Поддержанные и укрепленные на
            новой
            ступени силой всех Будд, бодхисаттвы вступают в блаженство самадхи в совершенном
            спокойствии.</p>

        <p>Благодаря принятым ими первоначальным обетам, они, движимые чувствами любви и
            сострадания, осознают, какую роль они играют в спасении всех живых существ, приняв
            на
            себя главные обеты. После этого они, хотя и не вступают в нирвану, но, поистине, уже
            находятся в нирване, поскольку в своих чувствах любви и сострадания не проводят
            никаких
            различений. Стало быть, для них и различения нирваны более не имеют цены.</p>

        <p>Поскольку трансцендентальная премудрость связывает воедино представления о настоящем,
            она
            уже тем образует подлинную реализацию совершенной Благородной Мудрости. Подлинность
            сознания проистекает непосредственно из Утробы Татхагаты, и поэтому бодхисаттвы
            приступают к решению своей основной задачи вполне уверенно и свободно, поскольку их
            природа -- это природа Благородной Мудрости. Это называется нирваной бодхисаттвы.
            Это и
            есть седьмая стадия -- стадия «далекосяжности».</p>

        <p>Восьмая стадия -- так называемая стадия «неотпадения» («свабхаума-ачала»). До этой
            стадии
            явления на поверхности универсального разума, связанные с накоплением энергии
            привычки,
            происходившими с безначальных времен в системе разума и во всем принадлежащем к ней,
            продолжали сохраняться и действовать, система разума функционировала в различении
            внешних объективных явлений, к которым можно было привязываться, благодаря чему
            функционирование и продолжалось.</p>

        <p>Однако при вхождении в состояние бодхисаттвы на третьей стадии достигается поворотный
            пункт в глубинах сознания, вследствие чего сосредоточенный на себе эгоизм уступает
            место
            универсальному сочувствию ко всем живым существам, в чем и происходит полная
            самореализация Благородной Мудрости. Наступает мгновенное прекращение всяческой
            деятельности, связанной с заблуждениями и активирующей систему разума: пляска волн,
            вызванных ветром энергии привычки на поверхности универсального разума,
            успокаивается
            навсегда, открываются внутреннее спокойствие и уединенность в непостижимом единстве
            Утробы Татхагаты. Стало быть, отныне не может быть внешнего взгляда на объективный
            мир
            при помощи чувств и чувственных разумностей. Не может быть различения отдельных
            понятий
            и идей, не может быть утверждения умствующего сознания, не может быть и накопления
            энергии привычки. Отныне может быть лишь внутренний опыт Благородной Мудрости,
            который
            достигается благодаря вступлению в совершенное единство. Утверждение себя на восьмой
            ступени «неотпадения» означает для бодхисаттвы то, что он вступает в блаженство
            девяти
            самадхи. Избегая пути учеников и учителей, поддавшихся искушению блаженства и
            вступивших
            в собственную нирвану, он, поддерживаемый своими обетами, трансцендентальной
            мудростью и
            начиная испытывать мощную помощь всех Будд, приступает к овладению высшими путями,
            ведущими к состоянию Татхагаты. Он переходит в блаженство самадхи для того, чтобы
            приобрести превращение формы и состояния Татхагаты и благодаря им способствовать
            освобождению всех существ. Махамати, если бы не существовала Утроба Татхагаты и не
            было
            божественного разума, тогда невозможно было бы ничему не возникать, ни исчезать в
            сумме
            агрегатов, образующих личность и ее внешний мир, не было бы ни возникновения, ни
            исчезновения, несведущих и святых, не было бы никакой задачи для бодхисаттв. Стало
            быть,
            двигаясь по пути самореализации и вступая в наслаждение самадхи, никогда не нужно
            отказываться от усердной работы ради освобождения всех живых существ, никогда не
            нужно
            отказываться от самоотверженной любви к ним. Если бы не так, все прошло бы, пропало
            бы
            впустую. Для философствующих представление от Утробе Татхагаты выглядит чем-то
            лишенным
            чистоты, скверным и связанным с внешними проявлениями. Но не так оно понимается
            Татхагатами, для них это вовсе не философская концепция, а переживание интуитивного
            опыта, настолько реально, насколько может быть реальным плод манго, лежащий на
            ладони
            руки. С прекращением деятельности системы ума и всего связанного с проводимыми в ней
            различиями наступает прекращение всяких усилий и ограничений. Это состояние можно
            уподобить состоянию человека во сне, который воображает, будто он пересекает реку и
            изо
            всех сил старается достичь этого, но внезапно для него наступает пробуждение.
            Пробудившись, он думает: было ли мое стремление пересечь реку реальным или
            нереальным?
            Достигши просветления, он знает, что оно не было ни реальным, ни нереальным.</p>

        <p>Когда бодхисаттва достигает восьмой ступени, он может видеть вещи воистину; более
            того,
            он может понимать значение всех подобных сну представлений о жизни, может знать, как
            мимо них пройти, как от них избавиться. Ведь с безначальных времен система разума
            схватывала множественность форм и условий и связанных с ними представлений, которые
            мыслящий ум продолжал различать, а эмпирический ум продолжал испытывать, схватывать
            в
            опыте и привязываться к ним. Из всего этого возникала и накапливалась энергия
            привычки,
            и с накоплением ее все более и более плотными становились иллюзии существования и
            несуществования, индивидуальности и общности, и все это продолжалось подобно сну,
            ложному воображению. Но теперь для бодхисаттвы на восьмой ступени жизнь прошла и
            воспринимается таковой, какова она было воистину, воспринимается подобной сну. Пока
            бодхисаттва и не пересек седьмой ступени, у него хотя и имеется интуитивное
            понимание
            подлинного смысла жизни, т.е. иллюзорной природы, однако его ум продолжает делать
            различения и испытывать привязанности. Он теплит привязанность к таким вещам,
            которые не
            существуют, и хотя в самом себе он больше не испытывает ревностного желания вещей,
            не
            испытывает импульсов схватывания, но тем не менее, представление о них по-прежнему
            сохраняется в нем: оно как бы пронизывает своим запахом его усилия практиковать
            учение
            Будды и добиваться освобождения всех существ.</p>

        <p>Но теперь, на восьмой ступени, даже и понятия о таких вещах отходят прочь, и все
            усилия и
            достижения оказываются ненужными. Нирвана бодхисаттвы -- это совершенное успокоение,
            хотя здесь полностью отсутствуют все представления о различениях и намерениях, но в
            то
            же время имеются свобода и потенциальная спонтанность, позволяющие достигнуть и
            терпеливо принимать все, все истины бессубъективности и безобразности, т.е.
            достигнуть
            необходимых результатов. Здесь совершенное одиночество, не возмущенное никакой
            степенью
            боязни непрерывной последовательности и непрерывной связи одного с другим. Оно
            лучистое,
            оно способность, оно свобода в собственной своей природе, т.е. в природе Благородной
            Мудрости, оно блаженно и мирно, оно совершенно, как совершенна любовь. Вступая на
            восьмую ступень, минуя поворотную точку в глубочайшей седловине сознания,
            бодхисаттва
            осознает, что приобрел второй вид трансцендентального тела (маномайякая), ибо
            переход от
            смертного тела к трансцендентальному телу не имеет ничего общего с обычной
            смертью.</p>

        <p>Старое тело продолжает функционировать, старый ум обслуживает потребности старого
            тела,
            но теперь он свободен от контроля смертного ума. Здесь имеет место непостижимое уму
            преображение смерти (Ачинтья-паринама-ачьюти), благодаря которому ложное
            представление о
            частной индивидуальной личности навсегда превосходится в связи с реализацией
            собственного единства с универсализованным разумом Татхагатовости, из которого
            осознается полнота безвозвратного освобождения. При этой реализации бодхисаттва
            оказывается полностью наделенным способностями и ислами Татхагаты, психическими
            способностями, возможностями самоконтроля -- и при этом оказывается превосходной
            опорой
            для поддержки всех живых существ в мире желаний (камма-дхату).</p>

        <p>Итак, Татхагата оказывается способным поддержать все существа в трансцендентальном
            мире,
            лишенном форме. Первые семь ступеней Пути Татхагаты относятся к сфере разума.
            Восьмая
            превосходит разум, однако имеет к нему еще некоторые касательства. Но девятая
            ступень
            трансцендентального понимания (сатхуманти) по причине своей совершенной разумности и
            внутренней глубины всматривающегося в безобразность божественного ума практиканта
            может
            быть достигнута лишь путем самореализации (самоосознания) Благородной Мудрости.</p>

        <p>Это и есть вступление в сферу Татхагаты. Постепенно бодхисаттва осознает природу
            Татхагаты и то, что он обладает всеми силами, всеми психическими способностями этой
            природы, самоконтролем, любовным состраданием, искусными средствами и возможностями
            для
            принесения счастья всем живым существам во всех землях Будды.</p>

        <p>Воспользовавшись этими новыми силами, бодхисаттва предпринимает воплощение в
            различные
            тела преображения, принимает различные облики ради благодеяния другим. Подобно тому,
            как
            в прежней умственной жизни воображение естественно формировалось у него из
            относительного познания, теперь, точно искусственное средство, оно возникает в нем
            спонтанно из трансцендентального понимания. Оно становится подобным магическому
            кристаллу, мгновенно отражающему соответствующие объекты его желаний, а не облики
            физических лиц. Бодхисаттва проходит сквозь все собрания будд и слушает их,
            участвует в
            их беседах, он видит подобную силу природы всех вещей, он размышляет об истинах,
            которые
            превосходят всяческое понимание сущего и несущего, которые не имеют отношения к
            смерти и
            рождению, не имеют отношения ни к личности, ни к исчезновению.</p>

        <p>Так эти проблемы понимаются Татхагатами, которые размышляют о Благородной Мудрости,
            выходящей за пределы умственного понимания учеников и учителей.</p>

        <p>Так он достигает сотен тысяч самадхи или даже сотен тысяч мириадов и многих мириадов
            самадхи, и в духе этих самадхи постоянно переходит из одной земли Будды в другую,
            воздавая дань восхищения всем Буддам, перерождаясь в царских дворцах, проявляясь в
            телах
            Будд, рассуждая со всеми о тройственном сокровище меньшего бодхисаттвы и принимая
            также
            участие в плодах самореализации Благородной Мудрости высших. Итак, переходя за
            пределы
            последней стадии бодхисаттвы, он становится самим Татхагатой, наделенным полнотой
            свободы дхармакайи.</p>

        <p>Десятая стадий принадлежит одним лишь Татхагатам.</p>

        <p>Здесь бодхисаттва обнаруживает себя как бы сидящим на лотосоподобном троне в
            превосходном, украшенном жемчугами дворце, окруженном бодхисаттвами разного ранга.
            Будды
            всех земель собираются вокруг него и своими чистыми благоуханными руками
            благословляют
            его, касаясь головы, давая ему рукоположение, посвящая его и признавая его равным
            себе.</p>

        <p>Затем они назначают для него некую землю Будды, которой он будет в совершенстве
            владеть,
            как своей собственностью.</p>

        <p>Десятая стадия называется «великим облаком истины» (Дхарма-магха). Она непостижима,
            недоступна пониманию умом. Лишь Татхагаты могут осознавать совершенную безобразность
            единства и одиночества этого состояния. Это «Махешвара», сияющая земля, чистая
            земля,
            земля безбрежных пространств, она окружена меньшими мирами, имеющими форму желаний
            (карма-дхаты), в которых бодхисаттвы по-прежнему трудятся и стремятся к соединению с
            высшим. Лучи Благородной Мудрости, имеющие собственную природу Татхагат,
            многоцветные,
            всепроникающие, благодетельные, превращающиеся в разные формы в трех зримых мирах,
            проникают в эти миры, преобразуют их прошлое, проникают и в иные миры, преобразуют
            их в
            будущем.</p>

        <p>Однако, в совершенном единении Благородной Мудрости нет ни градации, ни
            последовательности достижений, нет усилий, десятая стадия равна по существу первой,
            а
            первая -- восьмой, восьмая -- пятой, пятая -- седьмой.</p>

        <p>Какая же градация может быть здесь, в совершенной безобразности и в единении?</p>

        <p>И что же здесь может быть реальностью Благородной Мудрости, если не неизреченные
            возможности или потенции Дхарма-кайи?</p>

        <p>Здесь нет пределов, нет границ. Она превосходит и охватывает собой все земли
            Будды.</p>

        <p>Она проницает «аканишту» и все небесные дворцы (тушиты).</p>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава ХI. Состояние Татхагаты, которое и есть
            Благородная Мудрость</h3>

        <p>Затем Махамати обратился к Благословенному и попросил разъяснить ему сказанное в
            канонических книгах о том, что Будды не знают ни рождения, ни разрушения, что их
            называют нерожденными и что у них есть лишь имя Татхагаты. Он просил разъяснить, что
            же
            означает состояние Татхагаты, является ли оно сущностью или не сущностью.</p>

        <p>Благословенный ответил: - Татхагата не есть не сущность, однако, его нельзя и
            понимать
            неподобие прочих вещей: он не рождается, не исчезает, он не подвержен действию
            закона
            причинности, однако и не лишен никакого значения. Его можно называть нерожденным,
            однако, еще есть и иное имя для Татхагаты. Тело, представленное разумом
            (Мано-майя-кая),
            которое есть сущностное тело, по воле его принимает разные трансформации, в
            зависимости
            от потребностей деятельности по освобождению. Эта возможность вне понимания обычных
            учеников и учителей, она же и вне полного понимания тех бодхисаттв, которые достигли
            лишь седьмой ступени. Да, Махамати, «Нерожденный» есть синоним Татхагаты.</p>

        <p>Затем Махамати спросил: - Если Татхагата не рожден, то, по-видимому, у него не может
            быть
            никакой сущности, как же мы можем приписывать имя тому, что лишено сущности, и как
            можно
            его называть «некто», либо «нечто»?</p>

        <p>Благословенный ответил: - Объекты нередко называются разными именами в соответствии с
            теми аспектами, в которых они представляются.</p>

        <p>Так, бога Индру иногда называют Шакрой, а иногда Цурандарой.</p>

        <p>Эти различные имена используются взаимозаменяемо, а иногда они различаются. Однако
            объекты не следует воображать разными, поскольку они имеют разные имена.</p>

        <p>В то же время, они не лишены индивидуальности.</p>

        <p>Это справедливо и в отношении того, о чем я говорю.</p>

        <p>Когда я являюсь в этом мире терпения перед несведущими людьми, меня узнаю в
            неисчислимых
            обликах под бесчисленными именами. Люди обращаются ко мне под разными именами, не
            понимая, что все это имена одного и того же Татхагаты. Некоторые знают меня как
            Татхагату, другие -- как Самосущего, некоторые -- как аскета Гаутаму, некоторые --
            как
            Будду. Имеются и такие, которые еще называют меня Брахмой, Вишну, Ишварой. Некоторые
            видят во мне солнце и луну; некоторые считают меня наделенным десятью силами;
            некоторые
            считают Рамой, некоторые Индрой, другие Варуной.</p>

        <p>Есть еще и такие, которые говорят обо мне, как о Нерожденном, о Пустоте, о Таковости,
            об
            Истине, о Слове, о Реальности, о Конечном (или предельном) Принципе.</p>

        <p>Существуют и такие, которые называют меня Дхармакая, Нирвана и Вечность. Некоторые
            говорят обо мне как о Тождественности, Недвойственности, Неумирании, Бесформенности.
            Некоторые думают обо мне как об учении Будды: о страдании, о зависимости,
            освобождении и
            Благородном Пути. Некоторые считают меня Божественным Разумом и Благородной
            Мудростью.
            Так в этом мире и в прочих мирах меня знают под бесчисленным именем, но все они лишь
            служат чему-то вроде отражения луны в воде. Хотя все эти имена почетные,
            восхваляющие
            меня, они не дают полного понимания смысла, излучения тех слов, которые при этом
            используются.</p>

        <p>Они не дают собственной реализации мудрости, поскольку заставляют цепляться за слова,
            написанные в канонических книгах, или же за то, что говорится и подразумевается под
            этими словами, или же за то, что может быть воображаемо в связи с ними. Это не
            позволяет
            видеть, что использование имен есть лишь многообразие обращения к Татхагате. Авторы
            канонических книг нередко следуют одним лишь текстуальным значениям слова, тщетно
            стараясь достигнуть подлинного понимания. Не веря одному тексту, они устремляются к
            другому, надеясь в нем найти самоподтверждение своей истины, найти откровение. Итак,
            они
            не имеют достаточной веры в самореализации Благородной Мудрости.</p>

        <p>Затем Махамати сказал: - Молю тебя, Благословенный, поведай нам о собственной природе
            Татхагаты.</p>

        <p>Благословенный ответил: - Если описывать Татхагату такими выражениями, как созданный
            и
            несозданный, причина и действие, то мы можем его описать как ни созданного, ни не
            созданного, ни причины, ни действия. Если мы будем описывать его таким способом, то
            впадем в грех диалектического различения. Если Татхагата каким-то образом был
            создан, то
            он оказывается невечным. Если же он не создан, то всякие попытки реализовать
            состояние
            Татхагаты оказались бы тщетными. То, что не имеет ни причины, ни следствия, не
            обладает
            ни бытием, ни небытием, то что не есть ни бытие, ни небытие -- лишь внешние формы
            четырех пропозиций. Эти четыре пропозиции относятся к мирским способам
            словоупотребления
            или, что то же: вне них уже сфера слов, вроде представления о сыне бесплодной
            женщины.</p>

        <p>Точно так же все термины, относящиеся к Татхагате, могут быть поняты в подобном
            смысле.
            Когда говорится, что все вещи лишены субъективности, это означает, что все вещи
            лишены
            состояния.</p>

        <p>Каждая вещь может иметь собственную индивидуальность, так, существование формы лошади
            означает несуществование формы коровы. Это такое существование, которое имеет свою
            собственную природу, и, таким образом, отличает несведуще.</p>

        <p>Но, тем не менее, его собственная природа -- это природа, подобная сну или
            видению.</p>

        <p>Вот почему и несведущие и простые умом имеют привычку различать видимости и не могут
            понять значение бессубъективности. Лишь когда они отделяются от привычки давать
            различения и примут это как факт, т.е. поймут, что вещи пусты, рождены и не имеют
            собственной природы, они поймут смысл говоримого.</p>

        <p>Махамати, все эти выфражения применительно в Татхагатам лишены всякого смысла.</p>

        <p>А то, что не является ничем, не доступно никакому измерению. Ибо то, что недоступно
            измерению, превращается в бессмысленное слово. А пустое слово есть нечто
            нерожденное. А
            то, что не рождено, не может быть подвергнуто разрушению. А пространство не имеет ни
            причины, ни действия. То, что не является ни причиной, ни действием есть нечто
            необусловленное. А то же, что не обусловлено, превышает способности рассуждения о
            нем.</p>

        <p>А то, что вне способности рассуждения -- это и есть Татхагата. Собственная природа
            состояния Татхагаты удалена от возможности продуцирования, от возможности ее
            измерения.
            Собственная природа Татхагаты -- это Благородная Мудрость.</p>

        <p>Татхагата не вечен, не невечен. Во всяком утверждении по этому поводу содержится
            ошибка.</p>

        <p>Если сказать, что Татхагата вечен, тогда о нем должны быть сказаны некоторые формы
            творческой деятельности, ибо, согласно утверждению философов, творческая
            деятельность
            есть нечто несотворенное само по себе и вечное.</p>

        <p>Однако, Татхагаты не связаны ни с чем, называемым творческой деятельностью. И в этом
            смысле они не вечны. Если же сказать, что Татхагата не вечен, тогда он окажется
            связанным с вещами, которые созданы и которые сами по себе не вечны. По этим
            соображениям нельзя сказать, что Татхагата не вечен, не невечен. Нельзя также
            говорить,
            что Татхагата вечен в том смысле, в каком говорится о Вечности пространства, или в
            каком
            смысле можно сказать о вечности рогов зайца, ибо, будучи нереальным, представление о
            рогах зайца исключает всякую ассоциацию с идеями Вечности или о невечности. Это
            вовсе не
            относится к Татхагате, поскольку различение вечного и невечного связать с энергией
            привычки, возникающей в системе разума из элементов, образующих личность возникают
            из
            различения нереального, поэтому различение происходит по природе двойственности
            всякого
            понятия, в том числе, понятия вечности и невечности. Между тем Татхагаты не
            возникают из
            различения нереального.</p>

        <p>Итак, до тех пор, пока делаются различения понятий вечного и невечного, эти
            различения
            мешают пониманию Благородной Мудрости, основывающемся на осознании значения
            единства,
            одиночества.</p>

        <p>Однако есть еще один смысл, в котором можно говорить, что Татхагата вечен.
            Трансцендентальное понимание возникает при достижении просветления и обладает
            природой
            вечного. Эта сущность истины обнаруживается при просветлении и для тех, кто
            просветлен.
            Она реализуется как регулятивная и поддерживающая нас основа, как принцип
            реальности,
            который навсегда сохраняется. Трансцендентальное понимание достигается Татхагатами
            интуитивно при самореализации или Благородной Мудрости. Эта реализация есть их
            собственная природа. И в этом смысле Татхагаты вечны. Вечное, немыслимое состояние
            татхагатовости -- это таковая Благородной Мудрости, реализуемая внутри них самих.
            Она
            как вечна, так и не мыслима. Это подтверждается идеей причины и в то же время
            находится
            вне представления существования и не существования. Поскольку это возвышенное
            состояние
            соозначило с Благородной Мудростью, то оно имеет в ней свою собственную
            характеристику.</p>

        <p>Поскольку же это состояние выше реальности, оно имеет собственную причину -- в себе.
            Ибо
            Вечность не выводится из рассуждений, основывающихся на внешних представлениях о
            бытии и
            небытии, о Вечности и Невечности во внешнем смысле. Хотя она может быть подведена
            под
            тот же тип представления, что и пространство, прекращение и нирвана, она, тем не
            менее,
            вечна в себе и по-особому. Поскольку здесь нет никакой связи между существованиями и
            несуществованиями, здесь нет творца и его не нудно. Поскольку здесь нет никакого
            творения, здесь не может быть ни бытия, ни небытия.</p>

        <p>Однако, что открывается лишь в возвышенном состоянии Благородной Мудрости, которое и
            есть
            Подлинная Вечность. Когда разрушаются два вида чувств, и три вида препятствий на
            пути к
            освобождению устраняются, тогда полностью понимаются двувидность бессубъективности,
            и
            когда все непостижимые трансформации смерти в Бодхисаттве постигаются, тогда
            остается
            лишь собственно природа.</p>

        <p>Когда учение Дхармы оказывается понятным полностью и совершенно реализованным всеми
            учениками и учителями, тогда в глубинах сознания они осознают, что их собственная
            природа может быть открыта лишь как природа Татхагаты. В подлинном смысле слова
            существует четыре рода тождественностей, относящихся к природе Будды. Это буквальная
            тождественность, словесная тождественность, смысловая тождественность и существенная
            тождественность. Имя Будды производится следующим образом. Оно состоит из шести
            букв: Б,
            У, Д, Д, Х, А. Эти буквы одни и те же, когда они используются для обозначения любого
            Будды, любого Татхагаты.</p>

        <p>Но когда брахманы учат об использовании разных слов, то в отношении слов между ними
            имеется некоторая тождественность. В учении всех Татхагат сохраняется
            тождественность
            значения, тождественность смысла.</p>

        <p>Среди всех Будд поддерживается значение природы Буддства. Имеется тридцать два знака
            совершенства и превосходства и восемьдесят меньших знаков телесного совершенства,
            которое служит тому признаками. Нет никакого различия между ними, т.е. между всеми
            этими
            буквами за исключением того, что все они принимают разные трансформационные облики
            согласно разным предрасположениям существ, которые становятся их учениками, и
            которым
            надлежит освободиться собственными средствами. Но в своей сущности и предельной
            природе,
            которая есть Дхармакайя, все будды прошлого, настоящего, будущего образуют неделимое
            тождество.</p>

        <p>Махамати обратился к Благословенному и спросил: - Говорилось как-то Благословенным,
            что
            от ночи просветления до ночи паринирваны Будда не произнес ни одного слова, не
            сказал
            даже части слова. Каков глубокий смысл этого заверения?</p>

        <p>Благословенный ответил: - По двум причинам здесь имеется глубочайший и подлинный
            смысл. В
            свете самореализованной Благородной Мудрости и в свете истины высшей реальности.
            Самореализация Мудрости всеми Татхагатами -- это то же самое, что и моя собственная
            реализация Благородной Мудрости: здесь нет ни большего, ни меньшего, никаких
            различий. И
            все Татхагаты являются свидетелями самореализации как свободной от слов и от
            различений,
            в которых нет никаких идеалистических представлений, способных быть
            высказанными.</p>

        <p>Т.е. в нем все существо воспринимает учение Татхагаты через самореализацию
            Благородной
            Мудрости, а не через смыслоразличающие слова. Кроме того, Махамати, всегда
            существует
            пребывающая в Вечности реальность.</p>

        <p>Субстанция истины или Дарма-Дато навсегда пребывает независимо от того, появляется ли
            Татхагата в мире или нет. Точно так же навсегда пребывает основание всех вещей --
            Дармата, - которое вечно. Точно так же всегда пребывает реальность смысла --
            Парамарха,
            - которая поддерживает порядок в мире. То, что реализуемо мною и всеми прочими
            Татхагатами -- это именно та самая реальность, воплощенная в учении Дхарма-Кая. Она
            вечно пребывает в самоупорядоченности себя. Это же может быть названо таковостью или
            Татхагатами всех вещей, это же может быть названо реальнсотью вещей -- Будата, может
            быть названо Благородной Мудростью и истинной самой по себе.</p>

        <p>Солнце излучает свой блеск спонтанно и для всех поровну. Никакие слова не в состоянии
            объяснить этого его действия. Подобным же образом все Татхагаты излучают истину
            Благородной Мудрости, и никакое обращение к словам не способно это пояснить. По этой
            причине мною утверждается, что с ночи просветления до ночи паринирваны Татхагаты он
            не
            произнес ни одного слова и не намеревался произнести ни одного слова. То же самое
            справедливо для всех Будд.</p>

        <p>Тогда Махамати сказал: - Благословенный, ты говоришь о тождественности всех Будд. Но
            в
            других случаях тебе приходилось говорить о Тармате Будде, о Неминьянанге, о Ниамане
            Будде, когда они представлялись различными друг от друга. Может ли быть, что бы они
            были
            различениями?</p>

        <p>Благословенный ответил: - Я говорю о разных Буддах в противоположность взглядам тех
            философов, которые основывают свое учение о реальности внешнего мира форм, не
            представляя о различии и привязанностях, возникающих вследствие подобных
            различий.</p>

        <p>Я говорю о них, выступая против учений тех философов, поэтому мне приходится
            рассказывать
            о нирване Будде и о Будде трансформаций.</p>

        <p>Во многих превращениях состояния Татхагаты Нирвана Будды появляется и проявляет такие
            качества, как милосердие, поведение терпения, многомыслие и успокоенность. Но,
            говоря
            правильно, он учит верному пониманию иллюзорной природы элементов, образующих
            личность и
            внешний мир этой личности. Он учит подлинной природе системы разума как целого, он
            учит
            о различиях ее форм, о функциях и способах действования. В более глубоком смысле
