            Кто полностью познаёт такое истинно так, как оно есть, тот полностью отвергает тело, наделённое греховностью, обретает тело, не являющееся гнездилищем всех клеш, целиком и полностью очищенное, не имеющее явленности, несоединившееся, не имеющее осуществления. Следует знать, Манджушри, что это и является центральной идеей во всей полноте».
        </p>

