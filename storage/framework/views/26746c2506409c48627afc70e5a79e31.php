            «тело» греховности закрывается собранием дхарм подобно тому, как небо закрывается большим облаком, то десятую
            ступень следует называть Облаком Дхарм. (11) Поскольку благодаря избавлению от очень тонких покровов клеш и
            познаваемого целиком и полностью реализуется просветление — уяснение всех видов познаваемого беспристрастно
            и беспрепятственно, то одиннадцатую ступень следует называть ступенью Будды».
        </p>

        <p>
            «Бхагаван, сколько на тех ступенях имеется полных помрачений<sup>184</sup>? Сколько имеется греховностей
            несоответствующей стороны?»
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «Авалокитешвара, двадцать два полных помрачения и одиннадцать греховностей несоответствующей стороны.
            На первой ступени — (1) полная помраченность приверженности признанию индивида и дхармы и (2) полная
            осквернённость клешей плохой формы жизни<sup>185</sup>, и её греховность — несоответствующая сторона. На второй —
            (3) полная помраченность ошибки тонкого падения и (4) полная помраченность в отношении всевозможных видов
            полного созревания плода деяния, и её греховность — несоответствующая сторона. На третьей — (5) полная
            помраченность страсти желающей<sup>186</sup> и (6) полная помраченность в отношении полностью совершенной дхарани
            слушания, и её греховность — несоответствующая сторона. На четвёртой — (7) полная помраченность жажды
            самапатти и (8) полная помраченность жажды дхармы, и её греховность — несоответствующая сторона. На пятой —
            (9) полная помраченность осуществления в уме обращённости и необращённости только к сансаре, (10) полная
            помраченность осуществления в уме обращённости и необращённости только к Нирване, и её греховность —
            несоответствующая сторона. На шестой — (11) полная помраченность в отношении уяснения вхождения санскары,
            (12) полная помраченность возникновения множества признаков, и её греховность — несоответствующая сторона.
            На седьмой — (13) полная помраченность возникновения тонкого признака, (14) полная помраченность в отношении
            метода осуществления в уме только беспризнаковости, и её греховность — несоответствующая сторона. На восьмой —
            (15) полная помраченность мастерства в беспризнаковости, (16) полная помраченность невластвования над признаками,
            и её греховность — несоответствующая сторона. На девятой — (17) полная помраченность в отношении властвования<sup>187</sup>
            дхарани над неизмеримым объяснением Учения, неизмеримыми словами и буквами Учения, всё больше и больше
            развивающимися праджней и талантами, (18) полная помраченность в отношении властвования таланта, и её
            греховность — несоответствующая сторона. На десятой — (19) полная помраченность в отношении великой абхиджни,
            (20) полная помраченность в отношении проникновения в сокровенное и тонкое, и её греховность — несоответствующая
            сторона. На ступени Будды — (21) полная помраченность очень тонкой привязанности ко всему познаваемому,
            (22) полная помраченность преграды, и её греховность — несоответствующая сторона. Авалокитешвара, те ступени
            рассматриваются с этими двадцатью двумя полными помраченностями и одиннадцатью греховностями, а наивысшее,
            истинно совершенное Просветление не имеет их».
        </p>

        <p>
            «Поразительно, Бхагаван, сколь велика польза и велик плод наивысшего, истинно совершенного Просветления у того,
            кто полностью лишился такой великой сети полной помраченности Бодхисаттв, истинно ушёл от великой плотности
            греховности того и запечатал целиком и полностью пороки в наивысшем, истинно совершенном Просветлении!
            Бхагаван, сколько полностью чистых насчитывается у них?»
        </p>

        <p>
            «Восемь, Авалокитешвара: (1) полностью чистый высший помысел, (2) полностью чистый ум, (3) полностью чистое
            сострадание, (4) полностью чистая парамита, (5) полностью чистое видение и почитание Будды, (6) полностью чистое
            осуществление полного созревания существ, (7) полностью чистое рождение и (8) полностью чистое могущество.
        </p>

        <p>
            Следует знать, Авалокитешвара, что эти полностью чистые — от полностью чистого высшего помысла и до полностью
            чистого могущества — на первой ступени, на более высоких ступенях и на ступени Будды таковы, что от ступени к
            ступени становятся всё более и более полностью чистыми. При этом следует знать, что, кроме полностью чистого
            рождения на ступени Будды, по тем достоинствам, которые являются достоинствами первой ступени, более высокие
            ступени равны достоинствам той ступени, а по достоинствам своей ступени — превосходят. Все десять ступеней
            Бодхисаттв не имеют наивысших достоинств, а ступень Будды следует воистину признать имеющей наивысшие
            достоинства».
        </p>

        <p>
            «Бхагаван, почему рождение Бодхисаттвы следует называть самым высшим из всех рождений в мире?»
        </p>

        <p>
            «Из-за четырёх видов, Авалокитешвара: (1) из-за реализации целиком и полностью чистого корня добра, (2) из-за
            рассмотрения и принятия<sup>188</sup>, (3) из-за обладания состраданием, полностью спасающим всех существ, и (4) из-за
            неимения полного осквернения — самклеши — у самого и совершенного устранения полного осквернения других».
        </p>

        <p>
            «Бхагаван, почему Бодхисаттвы идут с большими пожеланиями, имеют полные пожелания и обладают силой
            пожелания?»
        </p>

        <p>
            «Из-за четырёх видов, Авалокитешвара. Итак, поскольку Бодхисаттвы сведущи в пребывании в нирванистическом
            блаженстве, могут быстро обрести его, отвергли то быстрое обретение и то пребывание в блаженстве и ради существ
            не идут туда, не нуждаются в том и творят полезное с мыслью о возникновении множества всевозможных страданий у
            существ ещё в течение долгого времени, постольку должны называться идущими с большими пожеланиями, имеющими
            полные пожелания и обладающими силой пожеланий».
        </p>

        <p>
            «Бхагаван, сколько у Бодхисаттв имеется предметов обучения<sup>189</sup>?»
        </p>

        <p>
            «Шесть, Авалокитешвара: отдача, нравственность, терпение, усердие, дхьяна и праджня».
        </p>

        <p>
            «Бхагаван, сколько из тех шести являются предметом обучения высшей нравственности? Сколько являются предметом
            обучения высшему уму<sup>190</sup>? Сколько являются предметом обучения высшей праджне?»
        </p>

        <p>
            «Следует знать, Авалокитешвара, что три первые являются предметом обучения высшей нравственности. Дхьяна
            является предметом обучения высшему уму. Праджня является предметом обучения высшей праджне. Я говорю, что
            усердие является присутствующим всюду».
        </p>

        <p>
            «Бхагаван, сколько из тех предметов обучения реализуют собрание заслуг, сколько — собрание мудрости?»
        </p>

        <p>
            «Авалокитешвара, те, которые являются предметом обучения высшей нравственности, реализуют собрание заслуг.
            То, которое является предметом обучения высшей праджне, реализует собрание мудрости. Я говорю, что усердие и
            дхьяна являются присутствующими всюду».
        </p>

        <p>
            «Бхагаван, каким образом должен обучаться тем шести предметам обучения Бодхисаттва?»
        </p>

        <p>
            «Посредством пяти видов, Авалокитешвара: (1) с самого же начала полностью уверовать в объяснения высшего
            Учения, касающиеся парамит, Бодхисаттва-питаки; (2) после этого реализовывать благодаря десяти видам практики
            Учения праджню, возникающую от слушания, обдумывания и созерцания; (3) сохранять потом мысль о Просветлении;
            (4) полагаться на Доброго Друга<sup>191</sup>; (5) непрерывно примыкать к стороне благого».
        </p>

        <p>
            «Бхагаван, почему следует считать, что те предметы обучения признаются в количестве шести?»
        </p>

        <p>
            «Благодаря двум видам, Авалокитешвара: поскольку приносят пользу существам и являются врагами клеш. При этом
            следует знать, что три являются приносящими пользу существам, а три являются врагами клеш. Здесь благодаря
            отдаче Бодхисаттва приносит пользу принесением пользы, состоящей в реализации наличия вещей у существ.
            Благодаря нравственности приносит пользу принесением пользы, состоящей в том, что не делает нуждающимися,
            не причиняет вреда и не наносит оскорблений. Благодаря терпению приносит пользу тем, что ничего не думает
            о ввергнувших в нужду, причинивших вред и нанёсших оскорбление. Посредством трёх этих он приносит существам
            пользу. Благодаря усердию полностью одолевает клеши и осуществляет присоединение к стороне благого, истинно
            победившей клеши, и клеши не могут оторвать его от присоединённости к стороне благого. Благодаря дхьяне
            полностью подавляет клеши. Благодаря праджне должным образом уничтожает находящиеся в потенции. Эти три
            являются врагами клеш».
        </p>

        <p>
            «Бхагаван, почему следует считать, что другие парамиты признаются в количестве четырёх?»
        </p>

        <p>
            «Потому, Авалокитешвара, что являются помощниками тех шести парамит. При этом Бодхисаттва, принеся существам
            пользу благодаря трём парамитам, устанавливает их в благом посредством мастерства в методе, реализующем четыре
            предмета привлечения существ<sup>192</sup>. Поэтому я указываю, что парамита мудрого метода является помощником и
            товарищем трёх. Авалокитешвара, если из-за обилия клеш Бодхисаттва не может постоянно созерцать в этой жизни,
            из-за малой силы высшего помысла, обусловленной низким уровнем здоровья и почитания, не может погружаться
            вовнутрь ума, из-за того, что не занимался созерцанием дхьяны на таком объекте, как прослушанная
            Бодхисаттва-питака, не может реализовать ушедшую от мира праджню, то пребывает, получая воистину малое собрание
            заслуг, и то осуществление пожелания с мыслью о малом числе клеш является его парамитой пожелания. И поскольку
            он будет иметь мало клеш и будет способным являть усердие, постольку парамита пожелания является помощником
            парамиты усердия. То, благодаря чему он, истинно полагаясь на высшее существо и опираясь на слушание Учения,
            обретает осуществление в уме, соответствующее истинному характеру сущего, и полностью избавившись от малой силы
            высшего помысла, обретает благодаря хорошему здоровью силу помысла, является его парамитой силы. И поскольку
            благодаря этому может погружаться вовнутрь ума, постольку парамита силы является помощником парамиты дхьяны.
            При этом осуществляется дхьяна на полном созерцании такого объекта, как прослушанная Бодхисаттва-питака. Это
            является его парамитой праджни. И поскольку благодаря тому будут способны полностью реализовать ушедшую от мира
            праджню, постольку я указываю, что парамита мудрости является помощником парамиты праджни».
        </p>

        <p>
            «Бхагаван, почему следует считать, что указанный порядок тех шести парамит должен быть именно таким?»
        </p>

        <p>
            «Потому, Авалокитешвара, что является опорой их реализации всё больше и больше. Если Бодхисаттва не обращает
            внимания на тело и достояние, то осуществляет истинное принятие нравственности. Если соблюдает нравственность,
            то будет обладать терпением. Если обладает терпением, то будет являть усердие. Если являет усердие, то будет
            реализовывать дхьяну. Если реализована дхьяна, то обретает ушедшую от мира праджню».
        </p>

        <p>
            «Бхагаван, на сколько видов подразделяется каждая из тех парамит?»
        </p>

        <p>
            «На три вида, Авалокитешвара. Три вида отдачи: (1) отдача Учения, (2) отдача вещей и (3) отдача
            неустрашимости. Три вида нравственности: (1) нравственность отворачивания от неблагого, (2) нравственность
            вхождения в благое и (3) нравственность осуществления вхождения в осуществление блага существ. Три вида
            терпения: (1) терпение, не сердящееся на причиняющего вред, (2) терпение, не думающее ничего о страдании, (3)
            терпение, истинно признающее Учение. Три вида усердия: (1) панцирное усердие, (2) усердие, присоединяющее к
            благому, (3) усердие, присоединяющее к осуществлению блага существ. Три вида дхьяны: (1) дхьяна, пребывающая
            в блаженстве — враге клеши и страдания благодаря неразмышлению и полному успокоению в покое; (2) дхьяна,
            полностью реализующая достоинства; (3) дхьяна, полностью реализующая благо существ. Три вида праджни:
            (1) видящая относительную истину, (2) видящая абсолютную истину и (3) видящая благо существ».
        </p>

        <p>
            «Бхагаван, почему те парамиты следует называть парамитами?»
        </p>

        <p>
            «По пяти причинам, Авалокитешвара: из-за (1) непривязанности, (2) несмотрения, (3) безгрешности,
            (4) недискурсивности и (5) полной отдачи. При этом (1) непривязанность — то, что является полной непривязанностью
            к предметам, не соответствующим парамите. (2) Несмотрение здесь — то, что является отсутствием мысли, связанной
            с полным созреванием плода реализации парамиты и принесением пользы в ответ. (3) При этом безгрешность —
            то, что является несмешанностью тех парамит с дхармой, обладающей полным осквернением, и полным отвержением
            не являющегося методом. (4) Недискурсивность здесь — то, что является отсутствием признания собственных
            признаков тех парамит за «соответствующие звуку»<sup>193</sup>. (5) Полная отдача заслуг здесь — то, что является
            желанием бодхисаттвовского плода реализации тех парамит и накопления благодаря этому заслуг».
        </p>

        <p>
            «Бхагаван, что является предметом, не соответствующим парамите?»
        </p>

        <p>
            «Авалокитешвара, следует знать, что тех предметов шесть: рассматривать чувственные радости, достояние,
            могущество и свои заслуги как полезное достоинство; наслаждаться всем, чего желает тело, речь и ум; из-за
            нетерпимости к критике и неусердия полностью отвлекаться на мысли о заслугах, мирские собрания и всевозможные
            мирские дела; рассматривать эмпирическую деятельность видения, слышания, анализирования и синтезирования
            как полезное достоинство».
        </p>

        <p>
            «Бхагаван, что является плодом полного созревания тех парамит?»
        </p>

        <p>
            «Следует знать, Авалокитешвара, что их шесть видов: (1) большое достояние, (2) приход в хорошие формы жизни,
            (3) незлобивость и неразличение, много радостей и наслаждений, (4) повелитель существ, (5) отсутствие вреда телу,
            (6) могущество и большая известность».
        </p>

        <p>
            «Бхагаван, что является в этих парамитах смешанным с дхармой, обладающей полным осквернением?»
        </p>

        <p>
            «Следует рассматривать это, Авалокитешвара, как четыре соединения: (1) соединение отсутствия сострадания,
            (2) соединение не являющегося соответствующим истинным характеристикам сущего, (3) соединение неосуществления
            постоянно, (4) соединение неосуществления благоговения. При этом соединение не являющегося соответствующим
            истинным характеристикам сущего таково — то, что является ослаблением относящегося к иной, чем та, парамите
            созерцания парамит иных, чем та».
        </p>

        <p>
            «Бхагаван, что является не являющимся методом?»
        </p>

        <p>
            «Авалокитешвара, когда Бодхисаттва приносит существам пользу посредством тех парамит, то если удовлетворяется
            простой реализацией принесения пользы вещами, отходит от неблагих предметов и не устанавливается в благом
            предмете, это является не являющимся методом. Почему же? Именно потому, Авалокитешвара, что не приносит
            пользы существам. Так, например, большие или же, возможно, малые нечистоты не могут по какой-либо причине
            сделаться благоухающими. Аналогично и существа, страдающие из-за наличия страдания санскар<sup>194</sup>, не
            могут сделаться блаженными по какой-либо причине, связанной с простым осуществлением принесения пользы
            посредством вещей, а то, что является установлением в благом, является именно высшим принесением им пользы».
        </p>

        <p>
            «Бхагаван, сколько имеется видов полной чистоты у тех парамит?»
        </p>

        <p>
            «Авалокитешвара, хотя я и не говорю, что кроме тех пяти у парамит имеются другие виды полной чистоты, однако
            объясню тебе виды полной чистоты, общие для всех парамит, и по каждой, опираясь на них. При этом следует знать,
            что видов полной чистоты, общих для всех парамит, — семь. Каковы же эти семь? Таковы: (1) совершенно не искать
            выгоды познания благодаря иному, чем те Учения Бодхисаттвы; (2) рассматривая те Учения, не делаться приверженным
            им; (3) не сомневаться и не колебаться: «Являются эти Учения и дхармы дающими истинное избавление от сансары
            в Просветлении или не являются?»; (4) не восхвалять себя и не порицать других, не относиться пренебрежительно;
            (5) не гордиться и не проявлять легкомыслия; (6) не воспринимать монаха, и вообще последователя Учения, как
            никчёмного и никудышнего; (7) не жадничать и не завидовать другим из-за тех Учений и дхарм.
        </p>

        <p>
            Следует знать, что полная чистота по каждой парамите в отдельности тоже имеет семь видов. Каковы же эти семь
            видов отдельных? Таковы.
        </p>

        <p>
            (1) Семь видов полной чистоты отдачи: (а) давать совершенно чистый дар посредством совершенно чистого предмета,
            который следует отдать; (б) давать совершенно чистый дар с совершенно чистой нравственностью; (в) с совершенно
            чистым взглядом; (г) с совершенно чистой мыслью; (д) с совершенно чистой речью; (е) с совершенно чистым знанием;
            (ж) с совершенной очищенностью от нечистоты. Бодхисаттвы пребывают, истинно приняв те, которые я объяснил так.
            Это — семь видов полной чистоты отдачи.
        </p>

        <p>
            (2) Семь видов полной чистоты нравственности таковы: (а) у Бодхисаттвы с обетом — мастерство во всех видах
            предметов обучения; (б) мастерство в выходе из падения; (в) при этом обладают истинной нравственностью;
            (г) непоколебимой нравственностью; (д) осуществляют постоянно; (е) входят постоянно; (ж) истинно принимают
            предметы обучения и обучаются им.
        </p>

        <p>
            (3) Семь видов полной чистоты терпения таковы: (а) при наличии всякого вреда со стороны всех других не сердиться,
            опираясь на понимание плода полного созревания своего деяния; (б) не воздавать тем же в ответ, не совершать
            поступки с причинением вреда руганью, избиениями, угрозами и оскорблениями; (в) не держаться агрессивных
            помыслов; (г) при раскаянии других совершенно не являть клеш; (д) не ожидать от них раскаяния; (е) не
            осуществлять терпение с мыслью об опасности и вещах; (ж) не отвергать осуществления принесения пользы.
        </p>

        <p>
            (4) Семь видов полной чистоты усердия таковы: (а) полностью понимать равенство усердия; (б) не восхвалять себя
            за то проявление усердия и не порицать других; (в) обладать мощью; (г) обладать усердием; (д) обладать
            активностью; (е) несгибаемая сила; (ж) не оставлять усердие в реализации благих дхарм.
        </p>

        <p>
            (5) Семь видов полной чистоты дхьяны таковы: (а) дхьяна на самадхи превосходного постижения признака;
            (б) дхьяна на самадхи полной реализации; (в) дхьяна на самадхи с присутствием обоих; (г) дхьяна на самадхи,
            возникающем благодаря силе; (д) дхьяна на самадхи непребывания; (е) дхьяна на самадхи осуществления
            совершенной очищенности; (ж) дхьяна на самадхи неизмеримого полного созерцания такого объекта, как
            Бодхисаттва-питака.
        </p>

        <p>
            (6) Семь видов полной чистоты праджни таковы: (а) полностью отвергнув крайность приписывания
            (принятия несуществующего за существующее) и крайность отрицания (нигилизма), та праджня достигает
            совершенства в срединном пути; (б) в отношении трёх дверей полного освобождения — пустотности, отсутствия
            признака и отсутствия желания — полностью понимать посредством той праджни смысл дверей полного освобождения
            именно так, как есть; (в) в отношении трёх сущностей — парикальпиты, паратантры и паринишпанны — полностью
            понимать смысл сущности истинно так, как есть; (г) в отношении трёх отсутствий сущности — признака, рождения
            и абсолютного — полностью понимать смысл отсутствия сущности истинно так, как есть; (д) в отношении пяти наук
            полностью понимать смысл относительной истины истинно так, как есть; (е) в отношении семи видов татхаты
            полностью понимать смысл абсолютной истины именно так, как есть; (ж) благодаря многократному пребыванию
            по методу в немышлении и непроявленности<sup>195</sup> и высшему видению, имеющему своим объектом безмерно
            смешанные дхармы, заниматься истинной реализацией дхармы, соответствующей Учению».
        </p>

        <p>
            «Бхагаван, что является деянием каждого из тех пяти видов?»
        </p>

        <p>
            «Их деяние, Авалокитешвара, тоже следует рассматривать в пяти видах. (1) Благодаря непривязанности осуществляют
            серьёзное отношение к этой жизни посредством постоянных занятий парамитами и деяния осуществления
            благоговения. (2) Благодаря несмотрению осуществляют полное овладение причинами наличия серьёзного отношения
            в последующих жизнях. (3) Благодаря безгрешности осуществляют созерцание полной реализации, полного очищения
            и полной очищенности парамит. (4) Благодаря недискурсивности быстро осуществляют полную реализацию парамит
            посредством того мастерства в методе. (5) Благодаря полной отдаче заслуг осуществляют обретение во всех жизнях
            до наивысшего, истинно совершенного Просветления парамиты вместе с желательным плодом полного созревания».
        </p>

        <p>
            «Бхагаван, какова сфера действия<sup>197</sup> тех парамит?»
        </p>

        <p>
            «То, Авалокитешвара, что является непривязанностью, несмотрением и полной отдачей. То, что является отсутствием
            полного осквернения, что является безгрешностью и недискурсивностью. То, что является очищенностью, что
            является исследованием и осуществлением. То, что является непоколебимостью, что является дхарматой совершеного
            неослабления ступивших на ступень. То, что является совершенно полной чистотой. То, что является,
            Авалокитешвара, относящимся к десяти ступеням и относящимся к ступени Будды».
        </p>

        <p>
            «Бхагаван, почему Бодхисаттва постоянно обладает не знающим истощения желательным плодом полного созревания
            парамиты и не знающей истощения парамитой?»
        </p>

        <p>
            «Потому, Авалокитешвара, что созерцает реализацию ступени, если одно опирается на другое».
        </p>

        <p>
            «Бхагаван, почему Бодхисаттвы не<sup>198</sup> пребывают в парамитах с верой в желательный плод полного созревания
            парамит?»
        </p>

        <p>
            «По пяти причинам, Авалокитешвара: (1) потому что парамиты являются причиной в высшей степени большого
            блаженства и наслаждения; (2) потому что являются причиной принесения пользы себе и другим; (3) потому что
            являются причиной желательного плода полного созревания того в последующих жизнях; (4) потому что являются
            основой отсутствия полного осквернения; (5) потому что являются дхарматой неизменности».
        </p>

        <p>
            «Бхагаван, что является мощью каждой из тех парамит?»
        </p>

        <p>
            «Авалокитешвара, мощь каждой из них следует рассматривать в пяти видах: при созерцании парамит полностью
            отвергаются такие виды несоответствующей стороны, как (1) жадность, (2) нарушение нравственности, (3)
            раздражительность и лень, (4) полное отвлечение, (5) взгляд; и (1) достигается наивысшее, истинно совершенное
            Просветление, (2) приносится польза себе и (3) другим в этой жизни, (4, 5) обретается не знающий истощения
            огромный желательный плод полного созревания в последующих жизнях».
        </p>

        <p>
            «Бхагаван, по какой причине возникают те парамиты, каким обладают плодом, каким обладают благом?»
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «Авалокитешвара, парамиты возникают по причине сострадания, обладают желательным для нас самих плодом
            полного созревания и таким плодом, как принесение пользы существам, обладают великим благом осуществления
            полной реализации великого Просветления».
        </p>

        <p>
            «Бхагаван, если Бодхисаттвы обладают неистощимым достоянием и являются сострадательными, то почему же в мире
            имеются бедные?»
        </p>

        <p>
            «Это, Авалокитешвара, как раз является злом собственной кармы существ. Иначе, как бы мир оказался таким
            страдающим при наличии у них постоянного предпринимания деяний и неистощимого достояния, если бы не было
            препятствования из-за собственных грехов существ? Так, например, Авалокитешвара, мучающиеся от телесной
            жажды олени видят озеро с водой высохшим. Это не является злом, возникшим из-за озера, а является злом —
            плодом собственной кармы тех оленей. Аналогично, то, что является наличием отсутствия плода, не является злом,
            возникшим из-за отдаваний Бодхисаттвы, подобных озёрам, но является злом, возникшим из-за собственной кармы
            подобных оленям существ, совершивших собственный грех».
        </p>

        <p>
            «Бхагаван, благодаря какой парамите Бодхисаттва воспринимает отсутствие сущности у дхарм?»
        </p>

        <p>
            «Воспринимает, Авалокитешвара, благодаря праджня-парамите».
        </p>

        <p>
            «Бхагаван, если благодаря праджня-парамите воспринимается отсутствие сущности, то почему же не воспринимается
            наделённость сущностью?»
        </p>

        <p>
            «Авалокитешвара, хотя я и не говорю, что отсутствие сущности воспринимается посредством сущности, однако,
            поскольку при отсутствии объяснения посредством букв невозможно указать то не имеющее сущности, не имеющее
            букв, постигаемое лично каждым, постольку говорю так: “Воспринимается отсутствие сущности”».
        </p>

        <p>
            «Бхагаван, если и парамитой называют, и близкой<sup>199</sup> парамитой называют, и великой парамитой называют,
            Бхагаван, то что же является парамитой, что является близкой парамитой, что является великой парамитой?»
        </p>

        <p>
            «Здесь, Авалокитешвара, Бодхисаттва обладает отдачей и другими благими дхармами, полностью созерцаемыми с
            неизмеримых времён. И хотя у него возникают клеши и он не может одолеть их, но он одолеет их в будущем благодаря
            благим дхармам. Итак, то имеющееся на ступени действия с верой-преданностью у обладающих малой и средней
            преданностью следует называть парамитой. А также, Бодхисаттва обладает теми благими дхармами, целиком и полностью
            созерцаемыми уже неизмеримое время, и у него возникают клеши, но он одолевает их и он не будет тем, кто должен
            одолеть их в будущем. Итак, то имеющееся, начиная с первой ступени, следует называть близкой парамитой. А также,
            обладает теми благими дхармами, созерцаемыми целиком и совершенно полностью уже неизмеримое время, и он
            является обладателем дхармы невозникновения клеш никогда-никогда. Итак, начиная с восьмой ступени это следует
            называть великой парамитой».
        </p>

        <p>
            «Бхагаван, сколько на тех ступенях имеется видов нахождения клеш в потенциальном состоянии<sup>200</sup>?»
        </p>

        <p>
            «Три вида, Авалокитешвара. Авалокитешвара, (1) истинно победивший спутника таков: на пяти ступенях возникают
            врождённые клеши, но поскольку тогда там нет возникновения сопутствующих им клеш, не являющихся врождёнными,
            постольку Бодхисаттву следует называть истинно победившим спутника. (2) Малая сила находящегося в потенции:
            поскольку на шестой и седьмой ступенях клеши проявляются незначительно — тонко — и, хотя и подавляются
            созерцанием, потом всё же проявляются. (3) Тонкое нахождение в потенции: поскольку на восьмой и более высоких
            ступенях клеши никогда-никогда не проявляются и пребывают только в покрове познаваемого».
        </p>

        <p>
            «Бхагаван, сколькими видами отвергнутой греховности характеризуются те пребывания клеш в потенциальном
            состоянии?»
        </p>

        <p>
            «Тремя видами, Авалокитешвара. Первое и второе характеризуются отвергнутостью греховности, подобной
            имеющемуся в скорлупе ореха и коре дерева, третье — отвергнутостью греховности, подобной имеющемуся в древесине.
            То, что является отвергнутостью греховности, подобной имеющемуся в сердцевине, относится к ситуации отсутствия
            клеш в потенциальном состоянии всегда и везде, относится, как я указывал, к ступени Будды».
        </p>

        <p>
            «Бхагаван, за сколько асанкхья кальп будут отвергнуты те виды греховности?»
        </p>

        <p>
            «Отвергаются за три асанкхьи, Авалокитешвара, или за сезон, месяц, полмесяца, сутки, день, яму, половину ямы,
            час, минуту, секунду, апаримиту кальп<sup>201</sup>».
        </p>

        <p>
            «Бхагаван, что следует признать являющимся признаком рождения клеши у Бодхисаттв на тех ступенях, что являющимся
            недостатком, что являющимся достоинством?»
        </p>

        <p>
            «Авалокитешвара, рождение клеши у Бодхисаттв имеет своим признаком отсутствие полного осквернения. Почему же?
            Потому что на первой ступени Бодхисаттва полностью постигает все Дхармовые пространства по отдельности и
            совершенно верно. Поэтому, поскольку клеша у Бодхисаттвы рождается исключительно сознательно<sup>202</sup>, а
            не бессознательно, постольку имеет своим признаком отсутствие полного осквернения. Поскольку не может породить
            страдания в своём потоке бытия, то является не имеющим недостатка. Поскольку является причиной полного
            отвержения страдания у мира существ, то является неизмеримым достоинством».
        </p>

        <p>
            «Бхагаван, если даже по рождению клеш у Бодхисаттв они превосходят корни добра всех обычных существ, Шраваков
            и Пратьекабудд, то стоит ли говорить о превосходстве в достоинствах иных, чем то? Поразительно, насколько
            велико благо Просветления!»
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «То, что является Колесницей Шраваков, и то, что является Великой Колесницей, — одна Колесница».
        </p>

        <p>
            «Бхагаван, какова здесь мысль?»
        </p>

        <p>
            «Авалокитешвара, те сущности всевозможных дхарм, которые я объяснял для Колесницы Шраваков, а именно:
            пять скандх, шесть внутренних аятан, шесть внешних аятан и т. д., я объяснял и для Великой Колесницы, указывая
            единый характер<sup>203</sup> Дхармового пространства. Поэтому я не говорю о разных Колесницах. При этом одни,
            которые признают смысл только как «соответствующий звуку», приписывают — принимают несуществующее за
            существующее; другие отрицают и признают Колесницы отличными. Обе эти группы не соглашаются друг с другом
            и спорят. Исходя из этого, я и утверждал так».
        </p>

        <p>
            Далее Бхагаван произнёс тогда эти стихи:
        </p>

        <blockquote class="border-l-2 border-sky-200 pl-4 my-4">
            <em>
                Те дхармы различных видов,<br />
                Которые объясняются в Махаяне и Хинаяне, — одного характера.<br />
                Поэтому я не говорю о разных Колесницах.<br />
                Признающие смысл соответствующим звуку<br />
                Приписывают и отрицают,<br />
                Думая: «Они несовместимы»,<br />
                Но различные мнения будут из-за невежества.<br />
                Подразделение по ступеням, имя, несоответствующая сторона,<br />
