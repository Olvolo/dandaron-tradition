            множественность дхату, отвержение и полное познание. Во многих разделах Бхагаван объяснял также собственный
            признак четырёх установлений памятования, объяснял также несоответствующую сторону, врага, созерцание,
            рождение неродившихся, пребывание, неоставление, укрепление, увеличение и развитие родившихся. Точно так же,
            как относящееся к четырём установлениям памятования, объяснял также относящееся к четырём истинным
            отвержениям, четырём основам риддхи, пяти способностям, пяти силам и семи членам Просветления.
        </p>

        <p>
            Во многих разделах Бхагаван объяснял также собственный признак восьмеричного пути святого, объяснял также
            несоответствующую сторону, врага, созерцание, рождение неродившихся, пребывание, неоставление, укрепление,
            увеличение и развитие родившихся.
        </p>

        <p>
            Бхагаван также объяснял: “Все дхармы не имеют сущности. Все дхармы не родились, не прекратились, изначально
            успокоены, по природе полностью нирванистичны”<sup>81</sup>. О чём думал Бхагаван, говоря: “Все дхармы не имеют
            сущности, все дхармы не родились, не прекратились, изначально успокоены, по природе полностью
            нирванистичны”? Я молю Бхагавана полностью объяснить ту идею<sup>82</sup>, исходя из которой Бхагаван сказал:
            “Все дхармы не имеют сущности, все дхармы не родились, не прекратились, изначально успокоены, по природе
            полностью нирванистичны”».
        </p>

        <p>
            Так сказал. И Бхагаван сказал Бодхисаттве Парамартхасамудгате так:
        </p>

        <p>
            «Твои мысли, Парамартхасамудгата, благие, родились в согласии с истинными характеристиками сущего —
            прекрасно, прекрасно! Парамартхасамудгата, ты решил спросить у Татхагаты об этом предмете, заботясь о пользе
            для множества существ и блаженстве для множества существ, из милосердия к миру, ради блага, пользы и
            блаженства богов с людьми и всех других существ — ты поступил прекрасно! Поэтому слушай,
            Парамартхасамудгата, а я объясню тебе, исходя из чего говорил: “Все дхармы не имеют сущности, все дхармы не
            родились, не прекратились, изначально успокоены, по природе полностью нирванистичны”.
        </p>

        <p>
            Парамартхасамудгата, я указывал: “Все дхармы не имеют сущности”, думая о трёх видах отсутствия сущности
            дхарм, а именно: признак не имеет сущности, рождение не имеет сущности, абсолютное не имеет сущности.
        </p>

        <p>
            Что же здесь, Парамартхасамудгата, за признак дхарм, не имеющий сущности? То, что является признаком
            парикальпита. Почему же? Поскольку он является признаком, устанавливаемым в именах и терминах, но не является
            полностью пребывающим благодаря собственному признаку, постольку его следует называть признаком, не имеющим
            сущности.
        </p>

        <p>
            Парамартхасамудгата, что же за рождение, не имеющее сущности? То, что является признаком дхарм паратантра.
            Почему же? Поскольку рождение появляется благодаря силе другого — причины, а не благодаря себе, постольку оно
            должно называться не имеющим сущности.
        </p>

        <p>
            Что же, Парамартхасамудгата, за абсолютное дхарм, не имеющее сущности? Те зависимо возникающие дхармы,
            которые не имеют сущности из-за отсутствия сущности у рождения, являются также не имеющими сущности из-за
            отсутствия сущности у абсолютного. Почему же? Парамартхасамудгата, я полностью объяснил, что абсолютным
            является то, что является совершенно чистым восприятием дхарм. И поскольку тот признак паратантра не является
            совершенно чистым восприятием, постольку сущность абсолютного следует называть отсутствующей.
        </p>

        <p>
            А также, Парамартхасамудгата, то, что является признаком дхарм паринишпанна, тоже следует называть не
            имеющим сущности абсолютным. Почему же? Парамартхасамудгата, то, что у дхарм является отсутствием Я дхармы,
            следует называть отсутствием их сущности. Это является абсолютным. И поскольку абсолютное полностью
            раскрывается через отсутствие сущности у всех дхарм, постольку абсолютное следует называть не имеющим
            сущности<sup>83</sup>.
        </p>

        <p>
            Здесь, Парамартхасамудгата, не имеющий сущности признак следует рассматривать точно так же, как, например,
            цветок в небе. Здесь, Парамартхасамудгата, не имеющее сущности рождение тоже следует рассматривать точно так
            же, как, например, сотворённую иллюзию. В связи с этим следует рассматривать и не имеющее сущности абсолютное.
        </p>

        <p>
            Здесь, Парамартхасамудгата, полностью раскрывающееся через отсутствие Я дхарм и вездесущее не имеющее
            сущности абсолютное тоже следует рассматривать точно так же, как, например, полностью раскрывающееся через
            отсутствие сущности формы — рупы — и вездесущее пространство.
        </p>

        <p>
            Исходя из этих трёх видов отсутствия сущности, Парамартхасамудгата, я и указал: “Все дхармы не имеют сущности”.
        </p>

        <p>
            Парамартхасамудгата, я указал: “Все дхармы не родились, не прекратились, изначально успокоены, по природе
            полностью нирванистичны”, думая при этом об отсутствии сущности признака. Почему же? То, что является не
            существующим благодаря собственному признаку, то является неродившимся. То, что является неродившимся, то
            является непрекратившимся. То, что является неродившимся и непрекратившимся, то является изначально
            успокоенным. То, что является изначально успокоенным, то является по природе полностью нирванистичным. У того,
            что является по природе полностью нирванистичным — ушедшим в Нирвану — ни в малейшей мере нет того, что
            “полностью ушло в Нирвану”. Поэтому, думая об отсутствии сущности признака, я указал: “Все дхармы не родились,
            не прекратились, изначально успокоены, по природе полностью нирванистичны”.
        </p>

        <p>
            А также, Парамартхасамудгата, я указал: “Все дхармы не родились, не прекратились, изначально успокоены, по
            природе полностью нирванистичны”, думая о том, что отсутствие сущности абсолютного полностью раскрывается
            через отсутствие Я дхарм. Почему же? Итак, раскрываемое через отсутствие Я дхарм отсутствие сущности
            абсолютного является именно полностью пребывающим во все времена, всегда, извечно. Как дхармата дхарм, оно
            является несоединившимся, полностью отделённым от клеш. То, что является несоединившимся, полностью
            пребывающим благодаря той дхармате во все времена, всегда, извечно, то является неродившимся и непрекратившимся,
            поскольку несоединившееся. Поскольку оно лишено всех клеш, то изначально успокоено и по природе полностью
            нирванистично. Поэтому я указал: “Все дхармы не родились, не прекратились, изначально успокоены, по природе
            полностью нирванистичны”, исходя из того, что отсутствие сущности абсолютного полностью раскрывается через
            отсутствие Я дхарм.
        </p>

        <p>
            Парамартхасамудгата, я имел в виду вот что. Если существа в мире существ видят сущность парикальпиты как
            отличную по сущности, видят сущность паратантры и сущность паринишпанны тоже как отличные по сущности, то,
            поскольку не признают три вида отсутствия сущности, существа, ложно признавая<sup>84</sup> сущность паратантры и
            паринишпанны за сущность парикальпиты, наклеивают на сущность паратантры и паринишпанны ярлык признака сущности
            парикальпиты.
        </p>

        <p>
            И как только наклеивают ярлык, так сразу же полностью замаранный наклеиванием ярлыков ум становится связанным
            с наклеенным ярлыком; или, благодаря наклеенному ярлыку, пребывающему в скрытом виде, признают сущность
            паратантры и паринишпанны за признак сущности парикальпиты. Как только признают, так сразу же, благодаря той
            причине и тому условию признания сущности паратантры сущностью парикальпиты, в последующем всюду порождают
            сущность паратантры. И благодаря той основе будут полностью осквернены полным осквернением клеши, будут
            полностью осквернены полным осквернением деяния и полным осквернением рождения<sup>85</sup>, и долгое время будут
            мотаться и перевоплощаться опять среди обитателей ада, опять среди животных, или опять среди претов, или опять
            среди богов, или опять среди асуров, или опять среди людей, поскольку не уходят из сансары.
        </p>

        <p>
            Здесь, Парамартхасамудгата, тем существам, которые с самого начала не порождали корень добра, совершенно не
            очищались от прегрешений, совершенно не делали себя созревшими, не имеют веры-почитания, не реализовывали
            истинно собрания заслуг и мудрости<sup>86</sup>, я объясняю Учение, исходя из идеи отсутствия сущности у рождения.
            Услышав это Учение, они, признав зависимо возникающие санскары<sup>86*</sup> непостоянными, признав их по самой
            своей сути ненадёжными, не заслуживающими доверия и изменчивыми, чувствуют отвращение, чувствуют неприязнь ко
            всем санскарам. Почувствовав отвращение, почувствовав неприязнь, отворачиваются от греха и не совершают то,
            что является неблагим, а полагаются на благое.
        </p>

        <p>
            По причине полагания на благое порождают корни добра, не порождённые ранее. Полностью очищаются от
            прегрешений, от которых совершенно не очищались ранее. Занимаются также осуществлением полного созревания
            самих себя, совершенно не созревших. Благодаря этой основе воистину реализуют многопочтительность и собрание
            заслуг и мудрости<sup>87</sup>.
        </p>

        <p>
            Хотя они, породив таким образом корень добра, воистину реализуют всё вплоть до собрания заслуг и мудрости,
            однако, поскольку при понимании того отсутствия сущности рождения не понимают двух других видов — отсутствия
            сущности признака и отсутствия сущности абсолютного — истинно так, как есть, то не будут настроены истинно
            отрицательно к санскарам, не будут истинно отделены от страсти, не будут истинно полностью освободившимися; не
            будут целиком освободившимися и от полного осквернения клеши, не будут целиком освободившимися и от полного
            осквернения деяния, не будут целиком освободившимися и от полного осквернения рождения.
        </p>

        <p>
            Поэтому Татхагата снова объясняет им Учение, исходя именно из отсутствия сущности признака и отсутствия
            сущности абсолютного, так, чтобы они воистину сделались отрицательно настроенными по отношению к санскарам,
            сделались отделившимися от страсти, сделались полностью освободившимися, истинно ушли от полного осквернения
            клеши, истинно ушли от полного осквернения деяния, истинно ушли от полного осквернения рождения.
        </p>

        <p>
            Поскольку, услышав это Учение, они не признают сущность паратантры признаком сущности парикальпиты (и это
            является отсутствием сущности рождения), и при этом признают, целиком и полностью раскрывают и познают истинно
            так, как есть, признак как не имеющий сущности, абсолютное как не имеющее сущности, и, таким образом,
            совершенно не замаранный наклеиванием ярлыков ум не связан с наклеиванием ярлыков, и благодаря уму, не
            имеющему ярлыков в скрытом виде, они постигают признак паратантры, порождают в этой жизни силу понимания, а в
            следующей осуществляют прекращение — ниродху, воистину прервав поток бытия. Благодаря этой основе воистину
            отворачиваются от всех санскар, истинно отделятся от страсти и истинно станут полностью свободными, целиком
            освободятся от полного осквернения клеши, полного осквернения деяния и полного осквернения рождения.
        </p>

        <p>
            Парамартхасамудгата, поскольку при этом существа Рода Колесницы Шраваков реализуют благодаря именно этому
            пути и обретут благодаря этой реализации Нирвану наивысшего блаженства, и существа Рода Колесницы
            Пратьекабудд, а также существа Рода Татхагаты<sup>89</sup> реализуют благодаря именно этому пути и обретут
            благодаря именно этой реализации Нирвану наивысшего блаженства, то это является единственным совершенно
            чистым путём Шраваков, Пратьекабудд и Бодхисаттв. И поскольку совершенно чистый — один и нет никакого второго,
            то, исходя из этого, я и объяснял только одну Колесницу.
        </p>

        <p>
            В мире существ нет отсутствия различных родов существ — существ, имеющих от природы слабые способности,
            имеющих средние способности и имеющих большие способности. Парамартхасамудгата, хотя бы все Будды и были
            усердны, но не могут поместить в Бодхиманду<sup>90</sup> следующего путём успокоения одиночек — индивида Рода
            Шраваков — и сделать так, чтобы он обрёл наивысшее, истинно совершенное Просветление. Почему же? Потому, что он
            по природе именно хинаянского Рода, поскольку имеет очень мало сострадания и очень страшится страдания.
        </p>

        <p>
            Как имеет очень мало сострадания, так и совершенно не будет он наклонным к осуществлению блага существ. Как
            сильно устрашен страданием, так и совершенно не будет наклонным ко всем реализациям санскар<sup>91</sup>. Совершенно
            не склонный к осуществлению блага существ и совершенно не склонный ко всем осуществлениям санскар не
            указывается мною как способный к наивысшему, истинно совершенному Просветлению. Поэтому он должен называться
            следующим путём успокоения одиночек.
        </p>

        <p>
            Того слушателя-Шравака, который полностью преобразится в Просветлении, я определяю на некотором основании
            как являющегося Бодхисаттвой: итак, потому что, если он, полностью освободившись таким образом от покрова
            клеш, побуждается Татхагатами, то осуществляет полное освобождение ума от покрова познаваемого<sup>92</sup>. Сначала же он
            полностью освобождается от покрова клеш, ориентируясь на собственное благо. Поэтому Татхагата относит его к
            Роду Шравака.
        </p>

        <p>
            Итак, Парамартхасамудгата, в объяснении должным образом Учения — моего превосходно изложенного,
            обуздывающего Учения<sup>93</sup>, совершенно полного, являющего целиком и полностью чистый помысел, — имеются
            различные уровни-ступени почитания-признавания у существ.
        </p>

        <p>
            При этом, Парамартхасамудгата, излагая сутры прямого смысла, Татхагата объясняет Учение, а именно: “Все
            дхармы не имеют сущности. Все дхармы не родились, не прекратились, изначально успокоены, по природе полностью
            нирванистичны”, исходя из тех трёх видов отсутствия сущности.
        </p>

        <p>
            (1) Если при этом то Учение услышат те существа, которые породили великий корень добра, полностью очистились
            от прегрешений, полностью созревшие, многопочтительные, истинно реализовавшие великое собрание заслуг и
            мудрости, то полностью поймут мою мысль и объяснение истинно так, как есть, признают в том Учении Учение.
            Постигнут праджней и смысл истинно так, как есть, и, благодаря тренировке в постижении того,
            быстро-быстро обретут в итоге целиком и полностью конечную цель. При этом они обретут и веру: “О, тот Бхагаван
            является истинно совершенным Буддой. Он стал целиком и полностью Буддой, превосходно постигнув все дхармы”.
        </p>

        <p>
            (2) Если при этом то Учение услышат те существа, которые не породили корень добра, не очистились полностью от
            прегрешений, не созрели полностью, не являются великими почитателями, не реализовали воистину великое собрание
            заслуг и мудрости, отличаются прямотой и непосредственностью, не могут понять и устранить, не считают свой
            взгляд высшим, то, хотя и не могут полностью понять мою мысль и объяснение истинно так, как есть, однако
            почитают то Учение и обретают веру.
        </p>

        <p>
            Они считают: “Эти изложенные Татхагатой сутры, являющие глубокое-глубокое, содержащие шуньяту, трудно увидеть,
