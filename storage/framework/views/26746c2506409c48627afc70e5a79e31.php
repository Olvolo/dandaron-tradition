        <h2 class="text-3xl font-bold text-center my-8">
            Глава 4. Субхути
        </h2>

        <p>
            Далее Бхагаван сказал почтенному Субхути: «Субхути, знаешь ли ты, сколько в мире существ
            имеется существ, которые занимаются истолкованием понятий и терминов, будучи поражены
            гордостью? Знаешь ли ты, сколько в мире существ имеется существ, которые занимаются
            истолкованием понятий и терминов, не имея гордости?»
        </p>

        <p>
            Субхути сказал:
        </p>

        <p>
            «Бхагаван, поскольку я думаю, что существ, которые занимаются истолкованием понятий и
            терминов, не имея гордости, в мире существ имеется чуть-чуть, то, Бхагаван, я думаю, что
            существ, которые занимаются истолкованием понятий и терминов, будучи поражены гордостью,
            в мире существ имеется неизмеримо, неисчислимо, неописуемо.
        </p>

        <p>
            Бхагаван, однажды, когда я пришёл в большой уединённый лес, там вокруг меня находилось
            очень много монахов, которые тоже обосновались в большом уединённом лесу. Утром я увидел,
            как монахи, сходясь друг с другом, занимаются истолкованием понятий и терминов, являя свои
            познания в рассмотрении различных видов дхарм: один там занимается истолкованием понятий
            и терминов, рассматривая скандхи, рассматривая признак скандх, рассматривая рождение
            скандх, рассматривая уничтожение скандх, рассматривая прекращение скандх и рассматривая
            реализацию прекращения скандх; точно так же, как первый рассматривает скандхи, второй
            рассматривает аятаны<sup>56</sup>; третий занимается истолкованием понятий и терминов,
            рассматривая зависимое возникновение<sup>57</sup>; четвёртый занимается истолкованием понятий и
            терминов, рассматривая еду, рассматривая признак еды, рассматривая рождение еды, рассматривая
            уничтожение еды, рассматривая прекращение еды и рассматривая реализацию прекращения еды;
            пятый занимается истолкованием понятий и терминов, рассматривая истину, рассматривая признак
            истины, рассматривая полное познание истины, рассматривая отвержение истины, рассматривая
            реализацию истины и рассматривая созерцание истины; шестой занимается истолкованием
            понятий и терминов, рассматривая дхату, рассматривая признак дхату, рассматривая
            разнообразие дхату, рассматривая множество дхату, рассматривая прекращение дхату и
            рассматривая реализацию прекращения дхату; седьмой занимается истолкованием понятий и
            терминов, рассматривая установление памятования, рассматривая признак установления
            памятования, рассматривая врага и сторону, не соответствующую<sup>58</sup> установлению
            памятования, рассматривая созерцание установления памятования, рассматривая рождение
            неродившегося установления памятования, рассматривая пребывание, неоставление,
            укрепление, увеличение и развитие родившегося установления памятования; точно так же,
            как седьмой — с установлениями памятования, восьмой занимается истолкованием понятий и
            терминов, рассматривая четыре истинные отвержения, четыре основы риддхи, пять способностей,
            пять сил, семь членов Просветления; девятый — рассматривая восьмичленный путь Просветления,
            рассматривая признак восьмичленного пути Просветления, рассматривая врага и несоответствующую
            сторону восьмичленного пути Просветления, рассматривая созерцание восьмичленного пути
            Просветления, рассматривая рождение неродившегося восьмичленного пути Просветления,
            рассматривая пребывание, неоставление, укрепление, увеличение и развитие родившегося
            восьмичленного пути Просветления.
        </p>

        <p>
            Увидев это, я подумал так: «Эти почтенные занимаются истолкованием понятий и терминов<sup>60</sup>,
            являя свои познания в рассмотрении различных видов дхарм; и, поскольку они не понимают
            такого признака абсолютного, как во-всем-одного-вкуса<sup>61</sup>, то все без исключения эти
            почтенные обладают гордостью, занимаются истолкованием понятий и терминов, будучи поражены
            гордостью.
        </p>

        <p>
            Бхагаван, по какой причине Бхагаван сказал так: «Признак абсолютного — тонкий, глубокий,
            труднопостижимый, в высшей степени труднопостижимый, [описывается словами] “во-всем-одного-вкуса”»?
            Это поразительно, насколько превосходно было сказано Бхагаваном!
        </p>

        <p>
            Бхагаван, если такой признак абсолютного, как во-всем-одного-вкуса, является, таким образом,
            труднопостижимым даже существами, вошедшими в эту религию Бхагавана и ставшими монахами,
            то стоит ли говорить о постижении тиртиками — внешними по отношению к этой религии?»
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «Именно так, Субхути, именно так! Я стал целиком и полностью Буддой, постигнув такой тонкий,
            в высшей степени тонкий, глубокий, в высшей степени глубокий, труднопостижимый, в высшей
            степени труднопостижимый признак абсолютного, как во-всем-одного-вкуса. Став же целиком и
            полностью Буддой, [я] назвал и пояснил его, провёл анализ и синтез, полностью продемонстрировал.
        </p>

        <p>
            Почему? Потому что я полностью объяснил, Субхути, что скандхи, двенадцать членов зависимого
            возникновения и еда являются абсолютным; и полностью объяснил, Субхути, что совершенно чистое
            восприятие<sup>62</sup> аятан, двенадцати членов зависимого возникновения и еды, истин<sup>63</sup>, дхату,
            четырёх установлений памятования, четырёх истинных отвержений, четырёх основ риддхи, пяти
            способностей, пяти сил, семи членов Просветления и, Субхути, восьмичленного пути святого,
            является абсолютным.
        </p>

        <p>
            То совершенно чистое восприятие скандх — тоже во-всем-одного-вкуса, признак не является
            отличным. То совершенно чистое восприятие всего этого — от аятан до восьмичленного пути
            святого — точно так же, как и скандх, во-всем-одного-вкуса, признак не является отличным.
            На этом основании, Субхути, ты должен признать, что абсолютным является то, что имеет признак
            во-всем-одного-вкуса.
        </p>

        <p>
            А также, Субхути, полностью постигнув татхату<sup>64</sup>, дхарму абсолютное, отсутствие Я<sup>65</sup> одной
            скандхи, практикующий йогу<sup>66</sup> монах не занимается поисками татхаты, абсолютного, отсутствия Я
            каждой из иных, чем та, скандх, дхату, аятан, двенадцати членов зависимого возникновения, еды,
            истин, установлений памятования, истинных отвержений, основ риддхи, способностей, сил, членов
            Просветления, восьми членов пути святого, но, благодаря именно этому следованию татхате, опоре
            на понимание недвойственного, занимается только истинным восприятием и непосредственным
            постижением такого признака абсолютного, как во-всем-одного-вкуса. С этой точки зрения, Субхути,
            ты тоже должен признать, что абсолютным является то, что имеет признак во-всем-одного-вкуса.
        </p>

        <p>
            А также, Субхути, если бы признаки татхаты, дхармы абсолютное, отсутствия Я тех дхарм являлись
            отличными так же, как являются взаимоотличными признаки этих скандх, аятан, двенадцати членов
