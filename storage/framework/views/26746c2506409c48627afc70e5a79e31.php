        <h2 class="text-3xl font-bold text-center my-8">Предисловие переводчика</h2>

        <p>
            На фоне значительного числа переводов буддийской литературы в отечественном востоковедении, на наш взгляд,
            относительно мало внимания уделялось переводу сутр — текстов, содержащих первоосновы вероучения буддистов.
            В настоящее время этот пробел начинает постепенно восполняться, что не в последнюю очередь связано с
            возрастанием интереса к буддизму во всём мире и в нашей стране. Этой цели служит и данный сборник,
            содержащий переводы с тибетского языка сутр различного тематического содержания. Представленные в нём тексты
            позволяют ознакомиться с положениями Учения Махаяны по первоисточникам.
        </p>

        <p>
            Буддийская литература очень обширна и представлена на языках многих народов мира. По традиции её подразделяют
            на содержащую проповедь самого Будды Шакьямуни и на произведения его последователей. Отдельные поучения
            Будды или ряд таких поучений, имеющих общую тематику и т. д., называют сутрами. У каждой сутры есть одно или
            несколько названий. В соответствии с «Сандхинирмочана-сутрой» (гл. 7) выделяют три группы сутр, которые
            называют тремя поворотами Колеса Учения. Это подразделение производится с точки зрения периода времени, когда
            они были проповеданы, содержащегося в них Учения и категории лиц, для которых они в основном предназначены.
        </p>

        <p>
            Первое Колесо содержит Учение о четырёх истинах святого — первооснову буддийской религии. Оно было «приведено
            во вращение», то есть проповедано в первый период после того, как отшельник Гаутама заявил, что обрёл
            Просветление и стал Буддой. Эти поучения предназначены в основном для лиц, наклонных к практике Хинаяны
            (Малой Колесницы) — одного из двух главных путей совершенствования, которые были указаны Буддой как ведущие
            к освобождению от страдания, Нирване. Характерной особенностью Хинаяны является то, что её последователи
            стремятся обрести Нирвану только для себя лично и не принимают ответственности за спасение других существ.
        </p>

        <p>
            Особое внимание на этом пути уделяется отрешённости от мира, строгому соблюдению монашеских обетов и
            созерцанию отсутствия «я» индивида. Конечной целью пути Хинаяны считается обретение положения Архата,
            полностью освободившегося от сансары — необходимости получать всё новые и новые рождения в феноменальном
            существовании. К этой группе текстов относятся такие сутры, как «Вибханга», «Махапаринирвана» и т. д.
        </p>

        <p>
            Два другие Колеса содержат объяснения Учения об отсутствии у вещей сущности, рождения и прекращения, наличии
            у них изначальной успокоенности и нирванистичности по природе. Эти сутры предназначены в основном для лиц,
            наклонных к практике Махаяны (Великой Колесницы) — второго главного буддийского пути совершенствования.
            Специфической характеристикой этого пути является то, что его последователи с самого начала порождают твёрдое
            намерение спасти от страдания всех существ, а достижение личной Нирваны служит только средством достижения
            этой цели. В центре внимания махаяниста находятся живые существа, их благо, и он стремится принести им
            максимальную пользу, опираясь на реализацию совершенств — парамит отдачи (наделения существ всем необходимым),
            нравственности, терпения, усердия, трансовых состояний (дхьяны), мудрости (праджни) и других методов.
            Полагают, что только этот путь ведёт к обретению положения Будды.
        </p>

        <p>
            Ко второму Колесу относят сутры, которые были проповеданы во второй период и содержат Учение, предназначенное
            в основном для наклонных к восприятию идей мадхьямики («срединной») — одной из четырёх главных буддийских
            философских школ, сложившихся в Индии. Базовым принципом мадхьямики считается признание лишённости (шуньяты)
            вещей их собственной сущности, собственного бытия и собственного признака. Мадхьямики полагают, что
            непосредственное постижение этого приводит к избавлению от неведения, страсти, гнева и других факторов
            (клеш), без которых невозможно функционирование сансары. В основной блок текстов этого Колеса входят сутры
            цикла Праджня-парамиты, «Акшаямати», «Самадхираджа», «Ратнакута», «Дашабхумика» и многие другие.
        </p>

        <p>
            Сутры третьего Колеса составляют поучения, которые были даны в третий период и предназначены в основном для
            наклонных к признанию идей школы виджнянавада (букв. «говорящей о сознании»). Освобождение, по мнению
            сторонников этой школы, может быть обретено благодаря постижению того, что все вещи являются
            манифестациями сознания. К этой группе относят такие сутры, как «Сандхинирмочана», «Ланкаватара» и ряд
            других.
        </p>

        <p>
            Представленные в данном сборнике тексты содержат изложение Учения Махаяны и относятся ко второму и третьему
            Колесу (хотя здесь и нет объяснения виджнянавадиновской теории, согласно которой все вещи являются только
            манифестациями сознания).
        </p>

        <p>
            Открывает сборник сутра «Истинное объяснение мысли» (Сандхинирмочана), которая относится к третьему Колесу
            Учения. Как явствует из названия, в ней содержится истинное объяснение Буддой тех мыслей, которые не были со
            всей явностью и полнотой разъяснены им в предшествующих поучениях, хотя и подразумевались при этом. Данная
            сутра отличается большой сложностью как в плане терминологии, так и в плане излагаемого в ней содержания.
            Поэтому её чтение должно предваряться достаточно серьёзным и обширным ознакомлением с буддийской
            литературой. В ней подробно объясняются признаки Абсолютного (гл. 1–4), различие между сознанием, умом и
            виджняной (гл. 5), подразделение сущего на три основных вида (гл. 6), детально разъясняются идеи отсутствия
            сущности у признака, рождения, абсолютного и т. д. (гл. 7), очень подробно излагаются концепции шаматхи,
            випашьяны, йоги (гл. 8), парамит и ступеней святости (гл. 9), а также концепции Будды и описание трёх Питак
            (Сутры, Винаи, Абхидхармы) (гл. 10). Ознакомление с этой сутрой позволяет получить достаточно полное
            представление о вероучении Махаяны.
        </p>

        <p>
            Сутра «Вопросы домохозяина Угры» (Гирахапати Угра париприччха) считается одной из немногих сохранившихся
            глав огромной сутры «Махаратнакута». Она обращает на себя внимание тем, что в ней очень подробно описывается,
            чем именно должны заниматься махаянисты — миряне, монахи и миряне, желающие заниматься в миру практикой
            отрешившихся от мира.
        </p>

        <p>
            Сутра «Творение Амитабхи» (Амитабхавьюха) также является одной из глав «Махаратнакуты» и входит в число трёх
            сутр, на которых в основном базируется столь популярное среди буддистов Китая, Японии и других стран учение
            амидаизма, указывающее лёгкий путь обретения освобождения благодаря перерождению в Сукхавати — чистой стране
            Будды Амитабхи. Хотя в Тибете, Монголии, Бурятии и других регионах амидаизм не получил широкого
            распространения, там существует множество молитв, практик и обрядов, направленных на обретение рождения в
            Сукхавати. В сутре рассказывается, как во времена Будды Локешварараджи монах Дхармакара, будущий Будда
            Амитабха, решил, что когда он станет Буддой, то у него будет «чистая» страна, воплощающая все достоинства
            всех других стран Будд. Описывается, каким образом он стал Буддой Амитабхой, и даётся подробное описание его
            страны Сукхавати.
        </p>

        <p>
            «Предсказание [на горе] Гошринга» и «Светильник, освещающий предсказания будущего» относятся к числу
            сутр-предсказаний (вьякарана). Первая из них представляет большой интерес в связи с проблемой уяснения
            буддийской концепции факторов, определяющих жизнь страны. Вторая сутра имеет явную эсхатологическую
            направленность. В ней описываются всевозможные бедствия и катастрофы, которые будут иметь место во времена
            всеобщего упадка и дегенерации людей — «через пятьсот лет, в период упадка религии Будды». Однако при этом
            подчёркивается вероятностный характер этих бедствий и указывается возможность их избежать.
        </p>

        <p>
            На тибетский язык эта сутра, очевидно, была переведена с китайского текста, который к тем временам дошёл в
            далеко не блестящем состоянии: некоторые части оказались утеряны, а другие перепутаны местами. В тексте
            рассказывается и легендарная история её появления в мире людей: её обнаружили в камне, который упал с неба
            на берег озера и раскололся.
        </p>

        <p>
            «Вопросы старицы» (Махалалика париприччха) — небольшая, но очень важная в философском плане сутра, которую
            нередко цитируют мадхьямики. Интерес она представляет и в плане уяснения одного из специфических путей
            освобождения, нашедшего отражение во многих молитвах, пожеланиях, ритуалах и т. д. Характерной особенностью
            этого пути является стремление обрести рождение в «чистой» стране какого-нибудь Будды, где в благоприятных
            условиях можно добиться больших успехов и, вплоть до Просветления, уже не попадать в «нечистые» страны с их
            страданием.
        </p>

        <p>
            «Вопросы мальчика Ратначандры» и «Сутра, полностью рассеивающая мрак десяти сторон» относятся к разряду
            сутр, в которых называются имена десяти Будд десяти сторон света (в разных вариантах), описываются их страны,
            указывается польза от произнесения их имён, поклонения и т. д. Три следующие сутры — «Вопросы Майтреи»,
            «Прибегание к Трем Драгоценностям» и «Кутагара — Многоэтажный домик» — являются образцами очень небольших
            по объёму сутр, каждая из которых посвящена объяснению только одного вопроса.
        </p>

        <p>
            Так, в первой из них разъясняется, каким образом можно стать «невозвращенцем» (авайвартика) — Бодхисаттвой,
            который не свернёт на путь Хинаяны или обычного существа и обязательно обретёт положение Будды. В «Прибегании
            к Трём Драгоценностям» указывается, какими заслугами будет обладать прибегающий с верой к Будде, Учению и
            Общине, а «Кутагара — Многоэтажный домик» посвящена описанию пользы, которую приносит подметание,
            делание мандалы ради Будды, поклонение, прибегание и соблюдение пяти обетов мирянина.
        </p>

        <p>
            Эти пять текстов включены в данный сборник потому, что являются типичными примерами определённых видов сутр.
            На их значение в религиозном плане указывает тот факт, что вместе с двумя предшествующими им сутрами они
            входят в «Сто шестьдесят семь дхарани и сутр, [являющихся самыми] высшими из [всего] чудесного и редкостного
            на Джамбудвипе» — очень популярный в Центральной Азии сборник сутр, дхарани, тантр, пратьянгир, восхвалений,
            молитв и пожеланий, который нередко используется также для ритуального чтения вслух.
        </p>

        <p>
            Перевод с тибетского языка текстов, вошедших в данный сборник, опирается на комплексное изучение многих сутр,
            а также ряда комментариев и исследовательских работ тибетских авторов. Во всех случаях, когда это позволяют
            средства русского языка, перевод следует тексту оригинала очень близко, что иногда приводит к несколько
            необычному для современного русского языка построению предложений.
        </p>

        <p>
            В этих текстах очень много специальной терминологии, для объяснения которой потребовалось бы очень много
            места. Поэтому комментарии сведены к необходимому минимуму и предполагают некоторое знакомство с буддийским
            Учением и терминологией. В настоящее время ещё не решена задача стандартизации перевода ключевых буддийских
            терминов на русский язык с учётом их истолкования разными буддийскими философскими школами и системами.
        </p>

        <p>
            Поэтому в ряде случаев более предпочтительным является употребление в переводе санскритских терминов, тем
            более что многие из них уже прочно обосновались в русскоязычной буддийской и буддологической литературе. Это
            объясняет, почему в данном переводе некоторые термины фигурируют только в их санскритской форме, а другие
            даются в сочетании (через дефис или в скобках) с русскими эквивалентами. Опыт показывает, что такая форма
            изложения переводимого текста значительно облегчает уяснение его содержания, которое даже в этом случае нередко
            представляет собой довольно сложную задачу.
        </p>

        <p>
            Перевод снабжён небольшим пояснением, комментариями и глоссарием. В списке литературы указываются основные
            источники, которые использовались при подготовке перевода и составлении комментариев. Работа над переводом
            этих сутр не была бы завершена без помощи С.&nbsp;П. Нестеркина, Н.&nbsp;С. Мункиной, И.&nbsp;Г. Васильевой и
            Л.&nbsp;А. Раднаевой, которым переводчик выражает свою глубокую благодарность.
        </p>

