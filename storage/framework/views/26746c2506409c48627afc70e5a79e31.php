            «Майтрея, прослушав должным образом те разделы Учения, которые я проповедал для Бодхисаттв, а именно: (1) раздел сутр, (2) раздел рассказываемого с мелодией — гейя (песня), (3) раздел пророчеств — вьякарана, (4) раздел стихов — гатха, (5) раздел торжественных произнесений — удана (превознесений), (6) раздел [бесед на] отдельные темы — нидана, (7) раздел разъясняющих притч — авадана, (8) раздел легенд — итивриттака, (9) раздел описания [прежних] рождений [Будды] — джатака, (10) раздел очень распространённого — вайпулья (содержащий махаянское Писание), (11) раздел чудесного — адбхутадхарма, (12) раздел объяснения приведения в порядок — упадеша<sup>107</sup>, Бодхисаттва заучивает [их] должным образом, читает чисто вслух нараспев, усердно изучает умом, и, полностью постигнув посредством видения, он пребывает один в уединении; истинно погрузившись вовнутрь<sup>108</sup>, осуществляет в уме те дхармы так, как [понял] при должном обдумывании, осуществляет в уме посредством осуществления непрерывно внутри в уме той мысли — мысли<sup>109</sup>, посредством которой осуществляет в уме. Так входит и пребывает в том много раз, и ту полную очищенность тела и полную очищенность ума<sup>110</sup>, которая возникает у него [при этом], следует называть состоянием успокоенности — шаматхой<sup>111</sup>. Если [действует] так, то Бодхисаттва является занимающимся поисками состояния успокоенности.
        </p>

        <p>
            Обретя ту полную очищенность тела и полную очищенность ума, он пребывает в том [состоянии]. Отвергнув вид мысли<sup>112</sup>, рассматривает те дхармы, которые обдумывал, как образ — объект самадхи внутри и достигает убеждённости. То, что является таким полным раскрытием того познаваемого смысла у тех образов — объектов самадхи, совершенно полным раскрытием, полным познанием, полным исследованием, принятием, признанием, раскрытием особенностей [и видов], рассматриванием и изучением, следует называть высшим видением — випашьяной<sup>113</sup>. Если [действует] так, то Бодхисаттва является постигающим высшее видение».
        </p>

        <p>
            [Майтрея сказал:]
        </p>

        <p>
            «Бхагаван, когда Бодхисаттва осуществляет в уме внутри ум (мысль), имеющий своим объектом ум (мысль), то как у него называется то осуществление в уме до того, как обретёт полную очищенность тела и полную очищенность ума?»
        </p>

        <p>
            [Бхагаван сказал:]
        </p>

        <p>
            «Майтрея, [это] не является состоянием успокоенности. Следует сказать, что является обладающим сходством с почитаемым соответствующим состоянию успокоенности<sup>114</sup>».
        </p>

        <p>
            [Майтрея сказал:]
        </p>

        <p>
            «Бхагаван, как должно называться то осуществление в уме осуществления в уме тех дхарм, которые должным образом обдумал, в качестве образа — объекта самадхи внутри до тех пор, пока тот Бодхисаттва не обрёл полной очищенности тела и ума?»
        </p>

        <p>
            [Бхагаван сказал:]
        </p>

        <p>
            «Майтрея, следует сказать, что [это] не является высшим видением, является обладающим сходством с почитаемым соответствующим высшему видению».
        </p>

        <p>
            «Бхагаван, следует ли называть путь состояния успокоенности и путь высшего видения отличными или следует называть не являющимися отличными?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «Майтрея, следует называть и отличными не являющимися, и не являющимися отличными не являющимися. Почему же не являются отличными? Потому что имеют своим объектом ум, созерцавший высшее видение. Почему же не являются не являющимися отличными? Потому что имеют своим объектом образ с размышлением».
        </p>

        <p>
            «Бхагаван, чем является то, что является образом — объектом самадхи, осуществляющего полное рассматривание? Следует называть ум отличным от него или следует называть не являющимся отличным?»
        </p>

        <p>
            «Майтрея, следует называть не являющимся отличным. Почему же не является отличным? Потому что тот образ — не что иное, как сознание. Майтрея, я указывал: “Виджняна полностью раскрывает воспринимаемое как являющееся лишь сознанием”».
        </p>

        <p>
            «Бхагаван, тот образ — объект самадхи: если материальное (форма) не является отличным от ума, то каким же образом тот ум познаёт тот же самый ум?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «Здесь, Майтрея, хотя никакая дхарма не осуществляет познание никакой дхармы, однако то, что является родившимся так умом, является так. Так, например, Майтрея, хотя, опираясь на материальное, и видят в целиком и полностью чистом диске зеркала материальное, но думают: “Видим образ — отражение”. При этом то материальное и то являющееся отражение являются отличными предметами. Аналогично, тот родившийся таким образом ум и то, что является именуемым образом — объектом самадхи, являются так, что одно является иным предметом, чем тот ум».
        </p>

        <p>
            «Бхагаван, следует ли называть не являющимся отличным от того ума то являющееся в качестве [имеющегося у] существ материального и т. д., которое пребывает [у нас] в качестве имеющего бытие психического образа?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «Майтрея, следует называть не являющимся отличным. Поскольку обладающие заблуждающимся умом простофили не постигают тот образ истинно так, как есть, — только как сознание, то понимают неверно».
        </p>

        <p>
            «Бхагаван, когда Бодхисаттва созерцает только высшее видение?»
        </p>

        <p>
            [Бхагаван] сказал: «Когда посредством непрерывного осуществления в уме действуют в отношении признака ума (мысли)».
        </p>

        <p>
            «Когда созерцается только состояние успокоенности?»
        </p>

        <p>
            [Бхагаван] сказал: «Когда посредством непрерывного осуществления в уме осуществляется в уме беспрепятственный ум [— сознавание]».
        </p>

        <p>
            «Когда будут сочетаться и сливаться в равности два — состояние успокоенности и высшее видение?»
        </p>

        <p>
            [Бхагаван] сказал: «Когда в уме осуществляется сосредоточенность ума<sup>115</sup>».
        </p>

        <p>
            «Бхагаван, что является признаком ума (мысли)?»
        </p>

        <p>
            «[То,] Майтрея, что является образом с размышлением — объект самадхи [при] созерцании высшего видения».
        </p>

        <p>
            «Что является беспрепятственным умом (мыслью)?»
        </p>

        <p>
            «[То,] Майтрея, что является умом, созерцающим образ [при] созерцании состояния успокоенности».
        </p>

        <p>
            «Что является сосредоточенностью ума?»
        </p>

        <p>
            «О том образе — объекте самадхи думают: “Это является только лишь сознанием”. Подумав то, осуществляют в уме как татхату. [То,] что является этим осуществлением в уме, и есть сосредоточенность ума».
        </p>

        <p>
            «Бхагаван, сколько видов у высшего видения?»
        </p>

        <p>
            «Три вида, Майтрея: (1) возникающее от признака, (2) возникающее от полного поиска и (3) возникающее от познания по отдельности<sup>116</sup>. (1) Каково же возникающее от признака? [То,] которое является высшим видением, осуществляющим в уме только образ с размышлением — объект самадхи. (2) Каково же возникающее от полного поиска? [То,] которое является высшим видением, осуществляющим в уме ради осуществления полного познания должным образом тех дхарм, совершенно не познанных должным образом там посредством праджни. (3) Каково же возникающее от познания по отдельности? [То,] которое является высшим видением, осуществляющим в уме [исследование] дхарм, полностью постигнутых там должным образом посредством праджни, ради осуществления полного обретения блаженства должным образом благодаря полному освобождению».
        </p>

        <p>
            «Сколько видов у состояния успокоенности?»
        </p>

        <p>
            [Бхагаван] сказал: «Поскольку следует за тем беспрепятственным умом (мыслью), то его тоже следует описывать в трёх видах. А также, Майтрея, состояние успокоенности имеет восемь видов: первой дхьяны, второй, третьей и четвёртой дхьяны, аятаны беспредельного пространства, аятаны беспредельного сознания, аятаны отсутствия чего бы то ни было и аятаны отсутствия представления — неотсутствия представления<sup>117</sup>. Есть ещё четыре вида: безмерной любви, сострадания, радости и безмерного равного отношения».
        </p>

        <p>
            «Бхагаван, если состояние успокоенности и высшее видение и пребывающими в дхарме называть следует, и непребывающими в дхарме называть следует, то какие являются пребывающими в дхарме, какие являются непребывающими в дхарме?»
        </p>

        <p>
            «Майтрея, состояние успокоенности и высшее видение, реализуемые благодаря следованию признаку той дхармы, которую восприняли и обдумали, являются пребывающими в дхарме. Состояние успокоенности и высшее видение, которые реализуются независимо от воспринятой и обдуманной дхармы и опираются на другое наставление и последующее разъяснение, а именно — соответствующее [идее] полного разложения, или полного загнивания [тела], или подобное [этим], или соответствующее [этой идее]: “Все санскары непостоянны”, или этой: “Они мучительны”, или: этой: “Все дхармы не имеют Я”, или этой: “Нирвана — успокоение”<sup>119</sup>, или подобное [этим], — эти состояние успокоенности и высшее видение следует признать являющимися непребывающими в дхарме.
        </p>

        <p>
            При этом, Майтрея, Бодхисаттву, который следует дхарме, опираясь на состояние успокоенности и высшее видение, пребывающие в дхарме, я признаю обладающим большими способностями. Следующего с верой, опираясь на непребывающие в дхарме, признаю имеющим малые способности».
        </p>

        <p>
            «Бхагаван, если состояние успокоенности и высшее видение и имеющими своим объектом несмешанную дхарму называть следует, и имеющими своим объектом смешанную дхарму называть следует, то какие являются имеющими своим объектом несмешанную дхарму<sup>120</sup>, какие являются имеющими своим объектом смешанную дхарму?»
        </p>

        <p>
            «Майтрея, если Бодхисаттва занимается созерцанием состояния успокоенности и высшего видения, имея своим объектом отдельные [содержащиеся в] разделе сутр и т. д. дхармы из числа дхарм, которые воспринял и обдумал, это является состоянием успокоенности и высшим видением, имеющими своим объектом несмешанную дхарму. Если те [содержащиеся в] разделе сутр и т. д. дхармы выбирает, набирает, собирает и сосредотачивает вместе, и все эти дхармы — входящие в татхату, относящиеся к татхате, принадлежащие татхате, входящие в Просветление, относящиеся к Просветлению, принадлежащие Просветлению, входящие в нирванистическое, относящиеся к нирванистическому, принадлежащие нирванистическому, входящие в являющееся предметом, относящиеся к являющемуся предметом, принадлежащие являющемуся предметом, — все эти дхармы осуществляет в уме: “Неизмеримые, неисчислимые благие дхармы описываются полным описанием”, — это является состоянием успокоенности и высшим видением, имеющими своим объектом смешанную дхарму».
        </p>

        <p>
            «Бхагаван, если состояние успокоенности и высшее видение и имеющими своим объектом мало смешанную дхарму называют, и имеющими своим объектом сильно смешанную дхарму называют, и имеющими своим объектом безмерно смешанную дхарму<sup>121</sup> называют, то какие имеют своим объектом мало смешанную дхарму, какие имеют своим объектом сильно смешанную дхарму, какие имеют своим объектом безмерно смешанную дхарму?»
        </p>

        <p>
            «Майтрея, следует признать, что состояние успокоенности и высшее видение, которые выбирают отдельные дхармы из Писания — от раздела сутр и до раздела очень распространённого — вайпулья, раздела чудесного — адбхутадхарма и раздела объяснения приведения в порядок — упадеша, собирают вместе и осуществляют в уме, имеют своим объектом мало смешанную дхарму. Если те разделы сутр и т. д. — сколько получили и обдумали — берут по отдельности, соединяют в одно и осуществляют в уме, то их следует признать имеющими своим объектом сильно смешанную дхарму. Соединяющие вместе и осуществляющие в уме безмерные объяснения Учения Татхагат, безмерные слова и буквы Учения, всё больше и больше развивающиеся безмерные праджню и таланты, следует признать имеющими своим объектом безмерно смешанную дхарму».
        </p>

        <p>
            «Бхагаван, благодаря чему Бодхисаттва является тем, кто обретает состояние успокоенности и высшее видение, имеющие своим объектом смешанную дхарму?»
        </p>

        <p>
            «Майтрея, следует признать, что это обретают благодаря пяти причинам, а именно: (1) когда осуществляют в уме, то занимаются уничтожением всех опор греховности в каждое мгновение; (2) полностью отвергнув всевозможные санскары, обретают радование всем радостям Учения; (3) истинно познают сияние Учения, безмерное в десяти сторонах<sup>122</sup> и имеющее вид совершенно непрерывного; (4) достигают полного совершенства в тех не являющихся плодом воображения признаках, соответствующих элементу совершенно чистого обладания полностью реализованным необходимым; (5) ради осуществления обретения, полной реализации и полного осуществления Дхармакаи занимаются воистину полным овладением наивысочайшей, наипревосходнейшей причины».
        </p>

        <p>
            «Бхагаван, где осуществляется постижение и где осуществляется обретение того состояния успокоенности и высшего видения, имеющих своим объектом смешанную дхарму?»
        </p>

        <p>
            [Бхагаван] сказал: «Следует знать, Майтрея, что постигаются на первой ступени святости Бодхисаттвы — Полностью Радостной, а обретаются на третьей ступени — Творящей Свет. Кроме того, Майтрея, Бодхисаттва-новичок тоже должен обучаться этому и не оставлять осуществления этого в уме».
        </p>

        <p>
            «Бхагаван, каким образом состояние успокоенности и высшее видение являются тем, что будет самадхи с витаркой и вичарой<sup>124</sup>? Каким образом являются тем, что будет только с вичарой и без витарки? Каким образом являются тем, что будет без витарки и без вичары?»
        </p>

        <p>
            «Майтрея, то, что является состоянием успокоенности и высшим видением, связанными с последующим исследованием дхарм, которые были восприняты, обдуманы и исследованы, исследованием, уясняющим их признаки и “вкушающим грубое”, является самадхи с витаркой и с вичарой. То, что является состоянием успокоенности и высшим видением, хотя и не связанными с последующим исследованием, которое уясняет признаки тех же дхарм и “вкушает грубое”, однако связанными с последующим исследованием, которое только приблизительно вспоминает явление того и “вкушает тонкое”, является самадхи только с вичарой и без витарки. То, что является состоянием успокоенности и высшим видением, полностью исследующими признак тех дхарм посредством осуществления в уме вкушения дхармы как всегда и везде появляющегося само собой<sup>125</sup>, является самадхи без витарки и без вичары<sup>126</sup>.
        </p>

        <p>
            А также, Майтрея, то, что является состоянием успокоенности и высшим видением, возникшими от полного поиска, является самадхи с витаркой и с вичарой. То, что является возникшим от познания по отдельности, является самадхи только с вичарой и без витарки. То, что является имеющим своим объектом смешанную дхарму, является самадхи без витарки и без вичары».
        </p>

        <p>
            «Бхагаван, что является причиной состояния успокоенности? Что является причиной полного восприятия? Что является причиной равного отношения?»
        </p>

        <p>
            «Майтрея, причиной состояния успокоенности следует называть то, что является осуществлением в уме дхарм, соответствующих становлению полностью неудовлетворённым, и осуществлением того беспрепятственного ума, когда ум возбужден или наклонён к возбуждённости. Причиной полного восприятия, Майтрея, следует называть то, что является осуществлением в уме дхарм, соответствующих полному радованию, и осуществлением признака ума, когда ум заторможен<sup>128</sup> или наклонён к заторможенности. Майтрея, причиной равного отношения следует называть то, что является возникающим спонтанно осуществлением в уме, когда ум сам собой входит в отсутствие осквернённости второстепенной клеши<sup>130</sup> двух тех, и может иметь место при реализации только состояния успокоенности, или реализации только высшего видения, или на пути реализации их слияния воедино<sup>129</sup>».
        </p>

        <p>
            «Бхагаван, если созерцающий состояние успокоенности и высшее видение Бодхисаттва является истинно понимающим по отдельности<sup>131</sup> дхармы и истинно понимающим по отдельности смыслы, то каким образом является истинно понимающим по отдельности дхармы, каким образом является истинно понимающим по отдельности смыслы?»
        </p>

        <p>
            «Майтрея, истинно понимающими отдельное являются благодаря пяти видам: благодаря (1) имени, (2) слову, (3) букве, (4) отдельному и (5) соединённому. (1) Что же за имя? То, что является употребляемым в качестве имени для сущности и видов дхарм полного осквернения и совершенного очищения. (2) Что же за слово? То, что является опирающимся на совокупность тех имён<sup>132</sup> в качестве места и опоры для обозначения смыслов предметов полного осквернения и совершенного очищения. (3) Что же за буква? То, что является буквой — местом и опорой их обоих. (4) Что же за истинное понимание отдельного благодаря тем отдельным? То, что является имеющим своим объектом несмешанное. (5) Что же за истинное понимание отдельного благодаря соединённому? То, что является истинным пониманием в уме отдельного благодаря осуществлению в уме, имеющем своим объектом смешанное. Если их всех соединить вместе, то это следует называть истинным пониманием отдельного. Если так, то является истинным пониманием дхармы.
        </p>

        <p>
            Бодхисаттва является истинно понимающим отдельные смыслы благодаря десяти видам: (1) сколько именно имеется, (2) существующее истинно, (3) смысл воспринимающего, (4) смысл воспринимаемого, (5) смысл места, (6) смысл обладаемого, (7) смысл ложного, (8) смысл неложного, (9) смысл полного осквернения и (10) смысл совершенного очищения<sup>133</sup>.
        </p>

        <p>
            (1) При этом, Майтрея, то число, которое является конечным результатом всех классификаций дхарм полного осквернения и совершенного очищения, является числом, указывающим, сколько имеется смыслов. Так, число скандх — пять. Число внутренних аятан исчерпывается шестью, число внешних аятан — тоже только шестью, и так далее.
        </p>

        <p>
            (2) При этом, Майтрея, то, что является татхатой тех дхарм полного осквернения и совершенного очищения, является существующим именно так, как есть. Их семь видов: (а) татхата возникновения — то, что является отсутствием начала и конца у санскар; (б) татхата признака — то, что у всех дхарм является отсутствием индивидуального Я и отсутствием Я — сущности дхармы; (в) татхата понимания — то, что является пониманием санскар; (г) татхата места или состояния — является истиной страдания, которую объяснил я; (д) татхата осуществления ложного — является истиной возникновения всего, которую объяснил я; (е) татхата полного очищения — является истиной прекращения, которую объяснил я; (ж) татхата осуществления истинного — является истиной пути, которую объяснил я.
        </p>

        <p>
            При этом, Майтрея, то, что является татхатой возникновения, и то, что является татхатой места, и то, что является татхатой осуществления ложного, сходны и одинаковы у всех существ. При этом, Майтрея, то, что является татхатой признака, и то, что является татхатой понимания, сходно и одинаково у всех дхарм. Майтрея, то, что является татхатой полного очищения, сходно и одинаково во всех Просветлениях — и которое является Просветлением Шравака, и которое является Просветлением Пратьекабудды, и которое является наивысшим, истинно совершенным Просветлением<sup>133</sup>. При этом, Майтрея, та татхата осуществления истинного сходна и одинакова у праджни при соединении состояния успокоенности и высшего видения, имеющих своим объектом несмешанные дхармы всего услышанного.
        </p>

        <p>
            (3) При этом, Майтрея, смысл воспринимающего — пять аятан, обладающих объектом, сознание, ум, виджняна и психические дхармы.
        </p>

        <p>
            (4) При этом, Майтрея, смыслом воспринимаемого являются шесть внешних аятан<sup>134</sup>. А также, Майтрея, смысл воспринимающего является также смыслом воспринимаемого.
        </p>

        <p>
            (5) Здесь, Майтрея, смысл места — то, что является областью мира<sup>135</sup>. Если пребывают где-то, то этим оказывается область мира. Так, город, сто городов, тысяча их, сто тысяч их, земля, достигающая моря, сто их, тысяча их, сто тысяч их, Джамбудвипа<sup>136</sup>, сто их, тысяча их, сто тысяч их, четырёхматериковый мир, сто их, тысяча их, сто тысяч их, область мира из тысячи миров, сто их, тысяча их, сто тысяч их, область мира Срединная, состоящая из миллиона миров, сто их, тысяча их, сто тысяч их, область мира Великая, состоящая из миллиарда миров, сто их, тысяча их, сто тысяч их, десять миллионов их, миллиард их, десять миллиардов их, триллион их, асанкхья их, сто асанкхья их, тысяча асанкхья их, сто тысяч асанкхья их, столько апаримита асанкхья областей мира десяти сторон, сколько атомов в ста тысячах асанкхья областей мира Великих, состоящих из миллиарда миров<sup>137</sup>.
        </p>

        <p>
            (6) При этом, Майтрея, смысл обладаемого — то, что является полностью владеемым и вещами, указанными мною ради осуществления полного обладания.
        </p>

        <p>
            (7) При этом, Майтрея, смысл ложного — то, что является имеющим место в отношении тех смыслов воспринимающего и т. д. ложным представлением, ложной мыслью, ложным взглядом на непостоянное как на постоянное, и ложным представлением, ложной мыслью, ложным взглядом на страдание как блаженство, на нечистое как чистое и на не имеющее Я как на имеющее Я<sup>138</sup>.
        </p>

        <p>
            (8) При этом, Майтрея, смысл неложного — обратное, его следует признать противоположным этому.
        </p>

        <p>
            (9) При этом, Майтрея, смысл полного осквернения имеет три вида: (а) полное осквернение клеши обитателей трёх миров, (б) полное осквернение деяния и (в) полное осквернение рождения.
        </p>

        <p>
            (10) При этом, Майтрея, смысл совершенного очищения — то, что является дхармами, соответствующими стороне Просветления, реализуемыми ради осуществления отделения от трёх видов полного осквернения.
        </p>

        <p>
            Следует признать, Майтрея, что этими десятью видами охватываются все смыслы.
        </p>

        <p>
            А также, Майтрея, Бодхисаттва является истинно понимающим отдельные смыслы благодаря пяти видам. Каковы же эти пять видов смысла? (1) Предмет осуществления полного познания, (2) полностью познаваемый смысл, (3) полное познание, (4) обретение плода полного познания и (5) осуществление полного понимания его.
        </p>

        <p>
            (1) Здесь, Майтрея, предмет осуществления полного познания следует рассматривать как являющийся всем познаваемым — тем, что познаётся, а именно: именуемое скандхами, или именуемое внутренними аятанами, или именуемое внешними аятанами, или тому подобное и так далее.
        </p>

        <p>
            (2) Здесь, Майтрея, полностью познаваемый смысл — познаваемое истинно так, как есть, познаваемое в стольких-то видах, а именно: относительное, абсолютное, недостаток, достоинство, условие, время, признак рождения, пребывания и уничтожения, болезнь и так далее, страдание, возникновение всего и так далее, татхата, истинный предел<sup>139</sup>, Дхармовое пространство, соединение, разделение, давание общего ответа, полное раскрытие, спрашивание и давание ответа, определение, тайное, известное, такое и тому подобное — всё это следует признать полностью познаваемым смыслом.
        </p>

        <p>
            (3) Здесь, Майтрея, полное познание — то, что является осуществлением восприятия и овладения двумя этими, дхармами, соответствующими стороне Просветления, а именно: четырьмя установлениями памятования, четырьмя истинными отвержениями и так далее.
        </p>

        <p>
            (4) Здесь, Майтрея, обретение плода полного познания — то, что является реализуемым, а именно: обуздание страсти, гнева и невежества, полное избавление от страсти, гнева и невежества, плоды шраманства<sup>140</sup> и те достоинства Шравака и Татхагаты, мирские и ушедшие от мира, обычные и необычные, которые я указывал.
        </p>

        <p>
            (5) Здесь, Майтрея, осуществление полного понимания — то, что является познанием полного освобождения благодаря именно тем уяснённым и реализованным дхармам и подробным объяснением другим, истинным объяснением.
        </p>

        <p>
            Следует знать, Майтрея, что эти пять видов смысла объединяют все смыслы.
        </p>

        <p>
            А также, Майтрея, Бодхисаттва является истинно понимающим отдельные смыслы благодаря четырём видам. Каким же четырём видам смысла? (1) Благодаря смыслу получения мысли, (2) смыслу вкушения, (3) смыслу полного понимания, (4) смыслу полного осквернения и совершенного очищения. Следует знать, Майтрея, что эти четыре вида смысла соединяют в себе все смыслы.
        </p>

        <p>
            А также, Майтрея, Бодхисаттва является истинно понимающим отдельные смыслы благодаря трём видам. Каким же трём видам смысла? Благодаря (А) смыслу слов, (Б) смыслу смысла и (В) смыслу дхату.
        </p>

        <p>
            (А) Здесь, Майтрея, следует знать, что смыслом слов является совокупность человеческих имён и так далее.
        </p>

        <p>
            (Б) Здесь, Майтрея, смысл смысла следует рассматривать в десяти видах: (1) признак именно того, что есть<sup>141</sup>, (2) признак полного познания, (3) признак отвержения, (4) признак реализации, (5) признак созерцания, (6) признак полного раскрытия видов тех — признака именно того и так далее, (7) признак места и связанного с местом, (8) признак дхармы, препятствующей полному познанию и так далее, (9) признак дхармы согласующейся, (10) признак зла совершенного незнания и так далее и пользы полного познания и так далее.
        </p>

        <p>
            (В) Здесь, Майтрея, смысл дхату — пять дхату: (1) дхату мира, (2) дхату существ, (3) дхату дхармы, (4) дхату обуздания и (5) дхату метода обуздания.
        </p>

        <p>
            Следует знать, Майтрея, что эти три вида смысла тоже соединяют в себе все смыслы».
        </p>

        <p>
            «Бхагаван, какие имеются отличия у того, что является истинным пониманием отдельных смыслов благодаря праджне, возникшей в результате слушания, и того, что является истинным пониманием отдельных смыслов благодаря праджне, возникшей в результате обдумывания, и, Бхагаван, того, что является истинным пониманием отдельных смыслов благодаря праджне, возникшей в результате созерцания состояния успокоенности и высшего видения?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «(1) Майтрея, благодаря праджне, возникшей в результате слушания, Бодхисаттва осуществляет такое истинное понимание отдельных смыслов, что пребывает в слове, принимает мысль сказанного за “соответствующую звуку”, не имеет понимания подразумеваемого<sup>142</sup>, не уясняет, пребывает в согласии с полным освобождением, но не реализует полное освобождение. (2) Майтрея, благодаря праджне, возникшей в результате обдумывания, Бодхисаттва осуществляет такое истинное понимание отдельных смыслов, что пребывает в слове полностью и не принимает мысль сказанного за “соответствующую звуку”, имеет понимание подразумеваемого, уясняет, пребывает в большом согласии с полным освобождением, но не осуществляет полное освобождение. (3) Майтрея, благодаря праджне, возникшей в результате созерцания, Бодхисаттва осуществляет такое истинное понимание отдельных смыслов, что пребывает в слове и не является пребывающим в слове, не принимает высказанную мысль за “соответствующую звуку”, имеет понимание подразумеваемого, уясняет посредством образа — объекта самадхи, соответствующего предмету познания, пребывает в очень большом согласии с полным освобождением, осуществляет полное освобождение. Майтрея, Бхагаваном сказано: “Таковы их отличия”».
        </p>

        <p>
            «Бхагаван, что является познанием, что является видением истинного понимания по отдельности дхармы и истинного понимания отдельного смысла созерцания состояния успокоенности и высшего видения?»
        </p>

        <p>
            «Майтрея, хотя я и объяснял познание и видение во многих разделах, однако объясню вкратце. Познанием является то, что является праджней при наличии состояния успокоенности и высшего видения, имеющих своим объектом смешанную дхарму. Видением является то, что является праджней при наличии состояния успокоенности и высшего видения, имеющих своим объектом несмешанную дхарму».
        </p>

        <p>
            «Бхагаван, благодаря какому осуществлению в уме, какой признак и каким образом полностью устраняет Бодхисаттва, созерцающий состояние успокоенности и высшее видение?»
        </p>

        <p>
            «Майтрея, полностью устраняет признак дхармы и признак смысла благодаря осуществлению в уме татхаты. Полностью устраняет благодаря тому, что не воспринимает у имени сущность имени и воистину не видит признака пребывания того. Точно так же, как имя, следует рассматривать и все слова, буквы и смыслы. Полностью устраняет благодаря тому, что не воспринимает и у других факторов вплоть до дхату их сущность и сущность дхату, и воистину не видит признака пребывания того».
        </p>

        <p>
            «Что же получается, Бхагаван? Разве полностью устраняет и то, что является признаком истинного понимания отдельного смысла татхаты?»
        </p>

        <p>
            «Майтрея, если у истинного понимания отдельного смысла татхаты признак отсутствует и не воспринимается, то что же тогда будет полностью устраняться? Майтрея, истинное понимание отдельного смысла татхаты одолевает все признаки дхармы и смысла, но я не говорю, что оно является тем, что тоже одолевается чем-то».
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «Подобно тому, как благодаря примеру сосуда, содержащего мутную воду, примера не чистого полностью зеркала и примера пруда с волнами невозможно понять признак собственного бытия, а обратное тому — можно, так и благодаря не созерцавшему уму невозможно понять именно то, что истинно, а благодаря созерцавшему — можно».
        </p>

        <p>
            «Бхагаван, что здесь является познанием умом? Исходя из чего названа татхата и какая?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «Майтрея, познаний умом три вида: (1) познание умом, возникшее в результате слушания, (2) познание умом, возникшее благодаря обдумыванию, (3) познание умом, возникшее благодаря созерцанию. Было полностью объяснено, исходя из татхаты полного понимания».
        </p>

        <p>
            «Бхагаван, сколько признаков являются трудноустраняемыми полностью у Бодхисаттвы, истинно понимающего таким образом отдельные дхармы и смыслы и предпринявшего полное устранение признака? Благодаря чему они полностью устраняются?»
        </p>

        <p>
            «Десять, Майтрея. Они полностью устраняются благодаря шуньяте. Какие же десять? (1) То, что является всевозможными признаками слов и букв у осуществляющего истинное понимание отдельного смысла дхармы, полностью устраняется шуньятой всех дхарм. (2) То, что является признаком рождения, уничтожения, пребывания и вхождения в поток превращения в другое у осуществляющего истинное понимание отдельного смысла посредством татхаты места, полностью устраняется шуньятой признака и шуньятой не имеющего начала и конца<sup>143</sup>. (3) То, что является признаком взгляда на совокупность разрушимого<sup>144</sup> и признаком думания: “Я” у осуществляющего истинное понимание отдельного смысла воспринимающего, полностью устраняется шуньятой внутреннего и шуньятой невоспринимаемого<sup>145</sup>. (4) То, что является признаком взгляда на обладаемое у осуществляющего истинное понимание отдельного смысла воспринимаемого, полностью устраняется шуньятой внешнего. (5) То, что является признаком внутреннего блаженства и признаком внешней привлекательности у осуществляющего истинное понимание отдельного, обладающего уважением мужчин и женщин и вещами ради полного наслаждения, полностью устраняется шуньятой внешнего и шуньятой собственного бытия. (6) То, что является признаком неизмеримого у осуществляющего истинное понимание отдельного смысла места, полностью устраняется шуньятой великого<sup>146</sup>. (7) То, что является признаком полного освобождения, обладающего внутренним успокоением, опираясь на не имеющее формы, полностью устраняется шуньятой соединившегося (санскрита). (8) То, что является признаком отсутствия индивидуального Я, признаком отсутствия Я дхарм, признаком полного понимания и признаком абсолютного у осуществляющего истинное понимание отдельного смысла татхаты признака, полностью устраняется шуньятой ушедшего от крайностей, шуньятой отсутствия вещи, шуньятой сущности отсутствия вещи и шуньятой абсолютного<sup>147</sup>. (9) То, что является признаком несоединившегося (асанскрита) и признаком отсутствия изменений<sup>148</sup> у осуществляющего истинное понимание отдельного смысла татхаты совершенного очищения, полностью устраняется шуньятой несоединившегося и шуньятой неоставляемого. (10) То, что является признаком шуньяты у осуществляющего в уме ту шуньяту — врага того признака, полностью устраняется шуньятой шуньяты<sup>149</sup>».
        </p>

        <p>
            «Бхагаван, если полностью устраняют десять видов признаков, то какой признак полностью устраняют и от какого признака уз полностью освободятся?»
        </p>

        <p>
            «Майтрея, полностью устраняют признак образа — объекта самадхи и полностью освободятся от признака полного осквернения, это тоже полностью устраняется. Следует знать, Майтрея, что те шуньяты фактически являются врагами тех признаков. Каждая же шуньята не будет не являться и врагом всех признаков. Так, например, Майтрея, хотя признак и не осуществляет реализацию стороны полного осквернения — самклеши от неведения и до старости и смерти<sup>150</sup>, однако является близким и ближайшим условием. Поэтому его следует рассматривать так же, как и то, что указывается в качестве осуществляющего реализацию санскары непосредственно<sup>151</sup>».
        </p>

        <p>
            «Бхагаван, что познают Бодхисаттвы в Махаяне, чтобы благодаря отсутствию гордости кажущимся обладанием<sup>152</sup> признаком шуньяты не отходить от шуньяты, а собирать признак шуньяты, и что является этим собиранием?»
        </p>

        <p>
            Далее Бхагаван одобрил Бодхисаттву Майтрею:
        </p>

        <p>
            «Майтрея, ты спрашиваешь у Татхагаты таким образом об этом предмете ради того, чтобы Бодхисаттвы совершенно не отходили от шуньяты, — прекрасно, прекрасно! Почему же? Потому, Майтрея, что полностью отошедший от шуньяты Бодхисаттва полностью отойдёт также и от всего в Махаяне. Поэтому, Майтрея, очень внимательно слушай, объясню тебе собирание признака шуньяты.
        </p>

        <p>
            Майтрея, то, что является признаком паратантра, признаком паринишпанна и признаком парикальпита полного осквернения и совершенного очищения во всех видах, признаком совершенно полного отделения и восприятием того, следует называть объяснением признака шуньяты в Махаяне».
        </p>

        <p>
            «Бхагаван, сколько самадхи собирают состояние успокоенности и высшее видение?»
        </p>

        <p>
            [Бхагаван] сказал:
        </p>

        <p>
            «Следует знать, Майтрея, что собирают все те многие виды самадхи<sup>154</sup> Шраваков, Бодхисаттв и Татхагат, которые я объяснял».
        </p>

        <p>
            «Бхагаван, благодаря какой причине возникают состояние успокоенности и высшее видение?»
        </p>

        <p>
            «Майтрея, возникают по причине совершенно чистой нравственности, возникают по причине совершенно чистого взгляда, возникшего благодаря слушанию и обдумыванию».
        </p>

        <p>
            «Бхагаван, что следует называть их плодом?»
        </p>

        <p>
            «Майтрея, плодом является совершенно чистый ум, плодом является совершенно чистая праджня. А также, Майтрея, следует знать, что плодом состояния успокоенности и высшего видения являются также все мирские и ушедшие от мира благие дхармы Шраваков, или Бодхисаттв, или Татхагат».
        </p>

        <p>
            «Бхагаван, что является деянием состояния успокоенности и высшего видения?»
        </p>

        <p>
            «Майтрея, осуществляют полное освобождение от двух видов уз — уз признака и уз греховности».
        </p>

        <p>
            «Бхагаван, сколько из тех пяти видов препятствий<sup>153</sup>, которые были названы Бхагаваном, являются препятствиями состоянию успокоенности, сколько являются препятствиями высшему видению, сколько являются препятствиями обоим?»
        </p>

        <p>
            «Майтрея, взгляд на тело и достояние<sup>154</sup> является препятствием состоянию успокоенности. Недостижение, хотя бы и желал, беседы святого<sup>155</sup> является препятствием высшему видению. Пребывание в смешении<sup>156</sup> и удовлетворённость даже малейшим являются препятствием их обоих. При этом даже из-за одного не осуществляется присоединение. Из-за единственного не будет окончательного присоединения».
        </p>

        <p>
            «Бхагаван, сколько из тех пяти преград<sup>156</sup>, которые имеются, являются преградами состояния успокоенности, сколько являются преградами высшего видения, сколько являются преградами обоих?»
        </p>

        <p>
            «Майтрея, возбужденность и сожаление являются преградами состоянию успокоенности. Отуманенность, сонливость и сомнение<sup>157</sup> являются преградами высшему видению. Стремление к желаемому и агрессивность<sup>158</sup> являются помехой обоим».
        </p>

        <p>
            «Бхагаван, когда путь состояния успокоенности является полностью чистым?»
        </p>

        <p>
            «Майтрея, когда отуманенность и сонливость полностью уничтожены должным образом».
        </p>

        <p>
            «Бхагаван, когда путь высшего видения является полностью чистым?»
        </p>

        <p>
            «Майтрея, когда возбужденность и сожаление полностью уничтожены».
        </p>

        <p>
            «Бхагаван, посредством скольких видов у Бодхисаттвы, занимающегося практикой состояния успокоенности и высшего видения, происходит полное отвлечение ума<sup>159</sup>?»
        </p>

        <p>
            «Посредством пяти видов, Майтрея: (1) посредством полного отвлечения осуществления в уме, (2) полного отвлечения мысли наружу, (3) полного отвлечения мысли внутри, (4) полного отвлечения признака и (5) полного отвлечения греховности.
        </p>

        <p>
            (1) Майтрея, если Бодхисаттва оставляет осуществление в уме, относящееся к Махаяне, и полностью впадает в осуществление в уме, относящееся к Шраваку и Пратьекабудде, то это является полным отвлечением осуществления в уме. (2) Если очень увлечённо предаётся мыслям о пяти внешних чувственных достоинствах<sup>160</sup>, людских делах, признаках, различиях, клешах, второстепенных клешах и внешних объектах, то это является полным отвлечением ума наружу. (3) Если из-за заторможенности, вызванной отуманенностью и сонливостью, или из-за вкушения блаженства самапатти, или из-за какой-либо второстепенной клеши самапатти становится клешным, то это является полным отвлечением ума внутри. (4) Если осуществляют в уме признак внутреннего объекта созерцания, опираясь на признак внешний, то это является полным отвлечением признака. (5) Если, из-за действия греховности, в отношении ощущения, возникшего в результате осуществления в уме внутреннего, гордятся: “Я”<sup>161</sup>, то это является полным отвлечением греховности».
        </p>

        <p>
            «Бхагаван, врагами чего являются состояние успокоенности и высшее видение от первой ступени Бодхисаттвы и до ступени Татхагаты?»
        </p>

        <p>
            «Майтрея, на первой ступени состояние успокоенности и высшее видение являются врагами полного осквернения клеши, деяния и рождения в плохой форме жизни, на второй — полного возникновения ошибки тонкого падения, на третьей — страсти желания<sup>162</sup>, на четвёртой — жажды самапатти и жажды дхармы, на пятой — необращённости и обращённости только к сансарному и нирванистическому, на шестой — полного возникновения множества признаков, на седьмой — полного возникновения тонкого признака, на восьмой — усилий в отношении отсутствия признака и неовладения властью над признаком, на девятой — неовладения властью над объяснением Учения во всех видах, на десятой — необретения истинного понимания отдельного — полностью совершенной Дхармакаи. Майтрея, на ступени Татхагаты состояние успокоенности и высшее видение являются врагами очень тонкого, в высшей степени весьма тонкого покрова клеш и познаваемого. Уничтожив должным образом это, обретают беспристрастное ко всему и беспрепятственное<sup>163</sup> познание и видение, пребывают в созерцании полной реализации необходимого, в целиком и полностью чистой Дхармакае».
        </p>

        <p>
            «Бхагаван, каким образом Бодхисаттва должен реализовывать состояние успокоенности и высшее видение, чтобы запечатать целиком и полностью пороки в наивысшем, истинно совершенном Просветлении?»
        </p>

        <p>
            Бхагаван сказал:
        </p>

        <p>
            «Здесь, Майтрея, обретя состояние успокоенности и высшее видение, Бодхисаттва приступает к реализации непосредственного постижения семи татхат, и умом, установленным в равности<sup>163</sup> при сосредоточении на дхармах, которые были услышаны и обдуманы, осуществляет в уме внутри татхату, воспринятую, должным образом обдуманную и являющуюся тем, при сосредоточении на чём устанавливались в равности, и если при таком осуществлении в уме татхаты ум устанавливается в высшем равном отношении даже ко всем возникающим тонким признакам, то стоит ли говорить об отношении к грубым признакам?
        </p>

        <p>
            Здесь, Майтрея, тонкие признаки таковы: признак получения ума, или признак вкушения, или признак полного понимания, или признак полного осквернения и совершенного очищения, или признак внутренний, или признак внешний, или признак этих обоих, или признак: “Должен содействовать благу всех существ”, или признак познания, или признак татхаты, страдания, возникновения всего, прекращения и пути, или признак соединившегося — санскрита, или признак несоединившегося — асанскрита, или признак постоянства, или признак непостоянства, или признак бытия, наделённого страданием и изменением, или признак бытия неизменности того, или признак несходства признаков соединившегося, или признак собственного признака того, или, узнав именуемое “всё-всё”, признак всего, или признак отсутствия индивидуального Я, или признак отсутствия Я дхарм. Устанавливается в высшей степени равном отношении ума к тем полностью возникающим, которые были указаны.
        </p>

        <p>
            Входит таким образом и много пребывает в том. Время от времени осуществляет полное очищение ума от препятствий, преград и отвлечений. При этом у каждого внутри происходит личное индивидуальное постижение, будут рождаться семь видов познания, постигающего по отдельности семь видов татхат. Это является Путём Видения<sup>164</sup> того.
        </p>

        <p>
            Обретя это, Бодхисаттва является вошедшим в безошибочность видения истинности, является родившимся в Роду Татхагаты, обретает первую ступень и вкушает пользу той ступени. Благодаря обретению состояния успокоенности и высшего видения прежде, он обретает два вида предметов созерцания — образ с размышлением и образ без размышления. Если так, то, обретя Путь Видения, обретает такой предмет созерцания, как “граница предмета”.
        </p>

        <p>
            Когда он всё дальше и дальше продвигается по Пути Созерцания<sup>166</sup> и осуществляет в уме те три вида предметов созерцания, то осуществляет полное устранение всех соответствующих полному осквернению признаков посредством полного устранения признаков внутренних<sup>167</sup> по типу появления сына благодаря сыну, так же, как, например, некто обнаруживает очень толстого ребёнка благодаря очень тонкому ребёнку. Когда полностью устраняет признаки, то полностью устраняет и греховность. Уничтожив должным образом все признаки и греховность, постепенно всё более и более полностью очищает ум подобно золоту, запечатывает целиком и полностью пороки в наивысшем, истинно совершенном Просветлении и обретает такой предмет созерцания, как полная реализация необходимого.
        </p>

        <p>
            Майтрея, если Бодхисаттва реализует состояние успокоенности и высшее видение таким образом, то запечатает целиком и полностью пороки в наивысшем, истинно совершенном Просветлении».
        </p>

        <p>
            «Каким образом занимается реализацией Бодхисаттва, если является полностью реализующим великую мощь<sup>168</sup> Бодхисаттвы?»
        </p>

        <p>
            «Майтрея, полностью реализующим великую мощь Бодхисаттвы является Бодхисаттва, сведущий в шести предметах: (А) сведущий в рождении сознания, (Б) сведущий в пребывании, (В) сведущий в побуждении, (Г) сведущий в увеличении, (Д) сведущий в уменьшении, (Е) сведущий в методе.
        </p>

        <p>
            (А) Каким же образом являются сведущими в рождении сознания<sup>169</sup>? Познав шестнадцать видов рождения сознания, являются сведущими в рождении сознания истинно так, как есть. Шестнадцать видов рождения сознания здесь таковы. (1) Рождение сознания в виде прочного и сосуда, а именно — сознания в виде получаемого. (2) Рождение сознания в виде различных видов объектов<sup>171</sup>, а именно — рождение сознания в виде объектов познающего ума — виджняны познающего ума, воспринимающего вместе цветоформу и другие объекты, воспринимающего вместе внешние и внутренние объекты, созерцающего вместе множество самадхи за одну секунду, мгновение, миг, видящего множество стран Будд, видящего множество Татхагат. (3) Рождение сознания в виде малого признака объекта, а именно — содержащегося в Мире Желаний. (4) Рождение сознания в виде являющегося великим признаком объекта, а именно — содержащегося в Мире Форм. (5) Рождение сознания в виде безмерного признака объекта, а именно — относящегося к аятанам безграничного пространства и безграничного сознания. (6) Рождение сознания в виде тонкого признака объекта, а именно — относящегося к аятане отсутствия чего бы то ни было. (7) Рождение сознания в виде конечного признака объекта, а именно — относящегося к аятане отсутствия представления — неотсутствия представления. (8) Рождение сознания в виде не имеющего признака, а именно — относящегося к ушедшему от мира и прекращённости — ниродхе. (9) Рождение в виде обладающего страданием, а именно — обитателя ада. (10) Рождение в виде обладающего смешанным ощущением, а именно — пребывающего в Мире Желаний. (11) Рождение в виде обладающего радостью, а именно — пребывающего в первой дхьяне и второй дхьяне. (12) Рождение в виде обладающего блаженством, а именно — пребывающего в третьей дхьяне. (13) Рождение в виде обладающего и страданием не являющимся, и блаженством не являющимся, а именно — пребывающего в четвёртой дхьяне и далее до аятаны отсутствия представлений — неотсутствия представлений. (14) Рождение в виде обладающего клешей, а именно — обладающего клешей и второстепенной клешей. (15) Рождение в виде обладающего благой дхармой, а именно — обладающего верой и так далее. (16) Рождение в виде обладающего нейтральным, а именно — не имеющего обоих этих.
        </p>

        <p>
            (Б) Каким же образом являются сведущими в пребывании? Если полностью знают татхату явления сознания в виде указанного истинно так, как есть.
        </p>

        <p>
            (В) Каким же образом являются сведущими в побуждении? Если полностью знают истинно так, как есть, два вида уз — узы признака и узы греховности. Полностью узнав это, являются сведущими в том: “Надо побуждать этот ум избавиться от того”.
        </p>

        <p>
            (Г) Каким же образом являются сведущими в увеличении? Когда рождается и увеличивается то, что является умом — врагом признака и греховности, являются сведущими в увеличении: “Рождается и увеличивается”.
        </p>

        <p>
            (Д) Каким же образом являются сведущими в уменьшении? Когда уменьшается и убывает тот ум, являющийся обладающим полным осквернением признака и греховности, являющихся несоответствующей ему стороной, то являются сведущими: “Уменьшается и убывает”.
        </p>

        <p>
            (Е) Каким же образом являются сведущими в методе? Если занимаются созерцанием полного освобождения, аятан превосходства и аятан истощённости.
        </p>

        <p>
            Если так, Майтрея, то Бодхисаттвы являются полностью реализовавшими, полностью реализующими и теми, кто полностью реализует великую мощь Бодхисаттвы».
        </p>

        <p>
            «Бхагаван, что сказал Бхагаван? [Бхагаван сказал:] “В сфере Нирваны без остатка скандх полностью прекращаются все ощущения”. Бхагаван, что это за истинные ощущения?»
        </p>

        <p>
            «Если вкратце, Майтрея, то прекращаются два вида ощущений: ведающие греховность места или состояния и ведающие объект — плод того. При этом ведающие греховность места признаются в четырёх видах: ведающие греховность Мира Форм, ведающие греховность Мира Бесформенного, ведающие греховность реализованного плода, ведающие греховность нереализованного плода. При этом реализованный плод — то, что является теперешним. Нереализованный плод — то, что является причиной будущего.
        </p>

        <p>
