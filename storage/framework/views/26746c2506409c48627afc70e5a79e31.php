        <h2 class="text-3xl font-bold text-center my-8">
            Глава 3. Сувишуддхамати
        </h2>

        <p>
            Далее. Бодхисаттва Сувишуддхамати — Целиком и Полностью Чистый Ум — сказал Бхагавану так:
        </p>

        <p>
            «Бхагаван, по какой причине Бхагаван сказал: “Абсолютное — тонкое, глубокое,
            воистину ушло от оценивания в качестве отличного и не являющегося отличным — таков его
            труднопостижимый признак”?
            Это поразительно, насколько превосходно сказал Бхагаван!
        </p>

        <p>
            Бхагаван, я видел, как в некое место пришло очень много достигших ступени
            адхимуктичарья — “действия с верой”<sup>47</sup> Бодхисаттв, чтобы обсудить между собой вопрос:
            являются ли санскары<sup>48</sup> и абсолютное отличными или не являются отличными.
            Придя, они собрались вместе.
        </p>

        <p>
            Один из Бодхисаттв сказал: «Признак санскара<sup>49</sup> и признак абсолютного не являются
            отличными».
            Другой сказал: «Признак санскара и признак абсолютного не являются не-отличными,
            но всё же являются отличными».
            Третий же, сомневающийся, колеблющийся, сказал:
            «Кто из тех, кто говорит, что “они отличны”, и тех, кто говорит, что “они не отличны”, —
            пришёл к небытию? Кто пришёл к ложному? Кто вошёл в соответствие с истинным характером сущего<sup>50</sup>?
            Кто же вошёл в несоответствие?»
        </p>

        <p>
            Увидев их, я подумал так, Бхагаван:
            «Все эти дети Рода — глупые простаки, несведущие, неразумные, вошедшие в несоответствие с истинным характером сущего,
            не понимающие, что абсолютное и санскары воистину ушли от оценивания как “отличные” и “не отличные” —
            таков тонкий признак».
        </p>

        <p>
            Так сказал он. И Бхагаван сказал Бодхисаттве Сувишуддхамати так:
        </p>

        <p>
            «Именно так, Сувишуддхамати, именно так!
            Все эти сыновья Рода — глупые простаки, неуяснившие, несведущие, вошедшие в несоответствие
            с истинным характером сущего, не понимающие, что абсолютное и санскары таким образом
            воистину ушли от оценивания как отличных и не отличных.
            Почему?
            Потому что те, кто рассматривают санскары таким образом, не являются постигающими абсолютное
            и не реализуют абсолютное.
        </p>

        <p>
            Почему?
            Сувишуддхамати, если бы признак санскара и признак абсолютного не были отличными,
            тогда обычные люди также были бы видящими истину, и, будучи обычными людьми,
            обретали бы реализацию и блаженство наивысшей Нирваны, и также запечатывали бы
            полностью пороки<sup>51</sup> в наивысшем истинно совершенном Просветлении.
        </p>

        <p>
            Если бы признак санскара и признак абсолютного были отличными, тогда видящие истину
            не были бы лишены признака санскара<sup>52</sup>.
            Если видящий истину не лишён признака санскара — он не будет полностью свободен от уз признака.
            Если не свободен от уз признака — не будет полностью свободен от уз греховности<sup>53</sup>.
            Если он не свободен от этих двух уз — не обретёт реализации и блаженства наивысшей Нирваны,
            и не запечатает полностью пороки в наивысшем истинно совершенном Просветлении.
        </p>

        <p>
            Поскольку обычные существа не являются видящими истину и, будучи обычными,
            не обретают реализации и блаженства Нирваны и не запечатывают пороки,
            — нельзя говорить: «Признак санскара и признак абсолютного не являются отличными».
            Следовательно, те, кто так говорят — вошедшие в несоответствие с истинным характером сущего.
        </p>

        <p>
            С другой стороны, Сувишуддхамати:
            поскольку видящие истину лишены, а не не лишены признака санскара;
            полностью свободны, а не несвободны от уз признака;
            полностью свободны, а не несвободны от уз греховности —
            то нельзя говорить, что признак санскара и признак абсолютного являются отличными.
            И те, кто так говорят, также входят в несоответствие с истинным характером сущего.
        </p>

        <p>
            Кроме того, Сувишуддхамати, если бы признак санскара и признак абсолютного не были отличными,
            тогда признак полной осквернённости (самклеша) относился бы и к абсолютному так же,
            как он относится к санскарам.
        </p>

        <p>
            Если же они были бы отличными, тогда признак абсолютного не был бы общим признаком
            для всех санскар.
        </p>

        <p>
            Поскольку же признак абсолютного не относится к полной осквернённости
            и является общим признаком для всех санскар,
            — нельзя сказать ни что «они отличны», ни что «не отличны».
            И те, кто утверждают и то, и другое — вошли в несоответствие с истинным характером сущего.
        </p>

        <p>
            Далее, Сувишуддхамати:
            если бы признаки санскара и абсолютного не были отличными, то все санскары не имели бы видов,
            точно так же как абсолютное не имеет видов в отношении санскар.
            И йоги не искали бы абсолютного как более высокого по сравнению с видимым, слышимым,
            анализируемым и синтезируемым.
        </p>

        <p>
            Если бы признаки санскара и абсолютного были отличными, тогда отсутствие Я и отсутствие сущности
            не являлось бы признаком абсолютного;
            и признаки полной осквернённости и совершенной очищенности были бы одновременно.
        </p>

        <p>
            Но поскольку санскары имеют виды,
            и йоги ищут абсолютное как высшее,
            и абсолютное раскрывается через отсутствие Я у санскар,
            и признаки полной осквернённости и совершенной очищенности не существуют одновременно —
            постольку нельзя утверждать ни «отличны», ни «не отличны».
        </p>

        <p>
            Так, например, Сувишуддхамати:
            белизну раковины — трудно признать не отличной от самой раковины или отличной от неё.
            Желтизна золота — подобна этому.
            Мелодичность звука вины — также: сложно признать звук и его признак не отличными или отличными.
            Приятность запаха чёрного алоэ — такова же.
            Жгучесть чёрного перца — такова же.
            Терпкость миробаланы — подобна жгучести перца.
        </p>

        <p>
            Точно так же мягкость хлопковой ваты, вкус сливок в масле — их трудно признать
            отличными или не отличными от соответствующих предметов.
        </p>

        <p>
            Так же и непостоянство у санскар, страдательность у всего со скверной<sup>55</sup>,
            и отсутствие сущности — Я — у всех дхарм:
            эти признаки также невозможно признать отличными или не отличными.
        </p>

        <p>
            Точно так же, Сувишуддхамати, признаки страсти, гнева, невежества, признака полной осквернённости —
            их невозможно признать отличными или не отличными от самих явлений.
        </p>

        <p>
            Аналогично, признак санскары и признак абсолютного — нельзя признать ни отличными,
            ни не отличными.
        </p>

        <p>
            Сувишуддхамати, я стал целиком и полностью Буддой, постигнув признак абсолютного:
            тонкий, в высшей степени тонкий, глубокий, крайне труднопостижимый;
            он воистину ушёл от оценивания как отличного и не отличного.
            Став же Буддой, я назвал и пояснил его, провёл анализ и синтез, полностью продемонстрировал его».
        </p>

        <p>
            Далее Бхагаван произнёс тогда эти стихи:
        </p>

        <p>
            <i>
                Признак абсолютного — отделённость
                от оценивания как “одно” или “отличные”.
                Признаки элемента санскары и абсолютного
                кто признаёт “одним” или “отличными” —
                те входят в несоответствие с истинным характером сущего.
                Натренировавшись в высшем видении (випашьяна) живых существ
                и в состоянии успокоенности (шаматха),
                полностью освободятся от уз греховности
                и уз признака.
            </i>
        </p>

