        <h2 class="text-3xl font-bold text-center my-8">
            Святая сутра Махаяны «Истинное объяснение мысли». Введение
        </h2>

        <p>
            Поклоняюсь всем Буддам и Бодхисаттвам.
        </p>

        <p>
            Так я слышал однажды: Бхагаван пребывает во дворце, созданном из сильно горящих семи видов
            драгоценностей, полностью имеющем великие лучи света, целиком наполняющие неизмеримые области мира,
            с целиком и полностью отделанными различными местами, совершенно устанавливающем безграничность,
            с полностью непрерывающимся мандалом<sup>1</sup>, воистину ушедшем от имеющегося в трёх Мирах<sup>2</sup>
            объекта, возникшем благодаря корню добра<sup>3</sup> того ушедшего от мира<sup>4</sup> Учителя,
            [обладающего] признаком полного понимания, совершенно полного понимания владычествования
            [над дхармами], обители Татхагаты<sup>5</sup>, посещаемой Общиной неизмеримых Бодхисаттв<sup>6</sup>,
            посещаемой беспредельным числом богов, нагов, якшей, гандхарвов, асуров, гаруд, киннаров, махорагов,
            людей и духов<sup>7</sup>; неизменном в великой радости и блаженстве вкуса Учения; пребывающем в творении
            истинного обретения всех благ для всех существ, лишённом всякого вреда нечистоты клеш<sup>8</sup>, полностью
            отвергшем всех мар<sup>9</sup>, высшем из всех творений, созданном благословением<sup>10</sup> Татхагаты,
            воистину возникшем благодаря великому памятованию, уму и постижению; являющемся колесницей великого
            состояния успокоенности и высшего видения; явившемся из трёх дверей полного освобождения<sup>11</sup> — пустотности,
            отсутствия признака и отсутствия желания; творении, украшенном великим царём драгоценных лотосов и
            беспредельной совокупностью достоинств.
        </p>

        <p>
            Бхагаван, обладающий превосходно постигающим умом, достойного поведения, не имеющий двух, совершенно вошедший
            в дхарму<sup>12</sup> отсутствия признака, пребывающий пребыванием Будды, обретший состояние равенства со всеми
            Буддами; сведущий в постижении, не имеющем преград; обладающий дхармой невозвращенца<sup>13</sup>; неодолимый объектом;
            полностью установленный, полностью устанавливающий неохватное умом; реализовавший равенство в трёх временах;
