        <p>
            Каковы же двадцать девять предметов? (1) Предмет — группировка санскар с точки зрения стороны полного осквернения, (2) предмет — порядок их вхождения, (3) предмет — причина появления в последующем после давания им имени «индивид», (4) предмет — причина появления в последующем после давания имени «дхарма», (5) предмет — направление ума на объект медитации с точки зрения стороны совершенного очищения, (6) предмет — усердие в этом, (7) предмет — состояния сознания, (8) предмет — пребывание в блаженстве в этой жизни, (9) предмет — созерцание метода истинного ухода от всех страданий, (10) предмет — полное познание того, имеющее три вида: (а) полное познание такого предмета, как ложное, (б) полное познание такого предмета, как реализация ложного в отношении внешних существ, начиная с их восприятия в качестве обладателей разума и т. д., (в) полное познание такого предмета, как отсутствие внутри гордости кажущимся; (11) предмет — объект или состояние созерцания; (12) предмет — реализуемое; (13) предмет — созерцание; (14) предмет — уделение тому особого внимания; (15) предмет — виды того; (16) предмет — восприятие того; (17) предмет — сведущесть в исследовании отвергнутого и неотвергнутого; (18) предмет — полная отвлекаемость от того; (19) предмет — полная неотвлекаемость от того; (20) предмет — состояние полной неотвлекаемости; (21) предмет — разъяснение йоги полного спасения благодаря созерцанию; (22) предмет — польза созерцания; (23) предмет — прочность его; (24) предмет — собирание могущества святого; (25) предмет — подразделение на сторону святости и сансары; (26) предмет — полное постижение именно того (таттвы); (27) предмет — истинное обретение Нирваны; (28) предмет — основное в превосходно изложенном обуздывающем Учении по сравнению со взглядом, признаваемым истинным у мирских, и по сравнению со взглядами, признаваемыми истинными у иноверцев; (29) предмет — полная деградация из-за несозерцания того.
        </p>

        <p>
            Итак, Манджушри, в результате несозерцания превосходно изложенного обуздывающего Учения будет деградация, а из-за ошибки взгляда<sup>209</sup> — нет. Манджушри, то, что является моим полным объяснением индивидуального спасения — пратимокши Шраваков и Бодхисаттв или обладания индивидуальным спасением, — является предметом Винаи».
        </p>

        <p>
            «Бхагаван, на сколько видов следует признавать подразделяемой мрогимокшу (обет индивидуального спасения)?»
        </p>

        <p>
            «На семь видов, Манджушри: (1) объяснение ритуала истинного принятия, (2) объяснение такого предмета, как состояние поражения, (3) объяснение такого предмета, как состояние падения, (4) объяснение сущности падения, (5) объяснение сущности непадения, (6) объяснение выхода из падения, (7) объяснение оставления обета.
        </p>

        <p>
            Манджушри, то, где я описал, полностью раскрыл и объяснил одиннадцать видов признаков, следует называть Матрикой. Каковы же здесь одиннадцать признаков? (1) Признак относительное, (2) признак абсолютное, (3) признак созерцание дхарм, соответствующих стороне Просветления, (4) признак вид, (5) признак сущность, (6) признак плод того, (7) признак полное наименование вкушения того, (8) признак дхарма, препятствующая тому, (9) признак дхарма, соответствующая тому, (10) признак зло того, (11) признак польза того.
        </p>

        <p>
            При этом, Манджушри, (1) признак относительное следует рассматривать в трёх видах: (а) объяснение индивида, (б) объяснение сущности парикальпиты, (в) объяснение действия, движения и эффекта действия<sup>210</sup> дхарм.
        </p>

        <p>
            (2) При этом признак абсолютное следует рассматривать как объяснение семи видов татхаты.
        </p>

        <p>
            (3) Признак созерцание следует рассматривать как объяснение всех видов предметов познания.
        </p>

        <p>
            (4) Признак вид следует рассматривать как объяснение восьми видов предметов рассмотрения<sup>211</sup>. Каковы же восемь видов предметов рассмотрения? (а) истина, (б) предмет, (в) недостаток, (г) достоинство, (д) характер (способ), (е) вхождение, (ж) доказательство, (з) подразделение и (и) развитие.
        </p>

        <p>
            (а) Истина здесь — то, что является татхатой. (б) Предмет<sup>212</sup> — то, что является определением<sup>213</sup> индивида, или определением сущности парикальпиты, или определением общего объяснения, полного раскрытия, задавания вопросов и давания ответов, или определением устанавливаемого, или определением даваемых ответов в отношении тайного и полностью раскрываемого. (в) Недостатки — то зло дхарм полного осквернения, которое я объяснял во многих разделах. (г) Достоинства — та полезность дхарм совершенного очищения, которую я объяснял во многих разделах. (д) Характер (способ) следует рассматривать в шести видах: характер смысла именно того (таттвы), характер обретения, характер объяснения, характер полного отвержения двух крайностей, характер неохватности умом, характер медитирования. (е) Вхождение — три времени, три признака соединившегося — санскрита — и четыре условия. (ж) Доказательство<sup>214</sup> следует рассматривать в четырёх видах: доказательство зависимости, доказательство осуществления действия, доказательство наличия в силу приемлемости, доказательство дхарматы.
        </p>

        <p>
            При этом доказательство зависимости — то, что является причинами и что является условиями возникновения и последующего наименования санскар — это является доказательством зависимости. То, что является причинами и что является условиями осуществления признания обретения или реализации дхарм, или того, что сделается из родившихся, является доказательством наличия в силу приемлемости. Если вкратце, то это доказательство наличия в силу приемлемости также имеет два вида: полностью чистое и полностью нечистое.
        </p>

        <p>
            При этом признаков полностью чистого — пять, признаков полностью нечистого — семь. Каковы же здесь пять признаков полностью чистого? (1) Признак — непосредственное восприятие того, (2) признак — непосредственное восприятие пребывания в том, (3) признак — подходящий пример собственного рода, (4) признак — полная реализация и (5) признак поучающего объяснения целиком и полностью чистой проповеди.
        </p>

        <p>
            (1) Признаком непосредственного восприятия<sup>215</sup> при этом является непосредственное восприятие в мире непостоянства всех санскар, страдательности всех санскар, отсутствия сущности-Я всех дхарм и то, что является соответствующим подобному.
        </p>

        <p>
            (2) Признаком непосредственного восприятия пребывания в том является то, что является непосредственным восприятием достоверности<sup>216</sup> мгновенности всех санскар, существования потустороннего мира<sup>217</sup>, непропадания кармических плодов благих и неблагих деяний, непостоянства пребывания в том, и что является непосредственным восприятием пребывания всевозможных существ во всевозможных деяниях, и что является непосредственным восприятием пребывания наслаждающихся и страдающих существ в благих и неблагих деяниях — карме<sup>218</sup>, и благодаря чему делается умозаключение о невоспринимаемом непосредственно, и что является соответствующим подобному.
        </p>

        <p>
            (3) Следует знать, что признаком подходящего примера собственного рода является то, что является подходящим восприятием известного во всех мирах наличия смерти и рождения у внутренних и внешних санскар, подходящим восприятием страдания рождения и т. д., подходящим восприятием отсутствия независимости, подходящим восприятием богатства и скудости, признаваемых во всём мире даже оппонентами, и соответствующим подобному.
        </p>

        <p>
            (4) Следует знать, что признаком полной реализации<sup>219</sup> является то, что таким образом является признаком непосредственного восприятия того, признаком непосредственного восприятия пребывания в том и признаком подходящего примера собственного рода, поскольку устанавливаются вместе в отношении реализуемого<sup>220</sup>.
        </p>

        <p>
            (5) Следует знать, Манджушри, что признаком поучающего объяснения целиком и полностью чистой проповеди<sup>221</sup> является то поучающее объяснение, которое изложено всеведущим, а именно: «Нирвана — успокоение», и что соответствует подобному.
        </p>

        <p>
            Поскольку так, то благодаря этим пяти видам признаков такой предмет рассмотрения, как доказательство приемлемости, признаётся полностью чистым, и поскольку оно полностью чистое, то надо полагаться на него.
        </p>

        <p>
            «Бхагаван, сколько видов признаков следует признавать у всеведущего?»
        </p>

        <p>
            «Пять, Манджушри. (1) Если появляется где-либо, то в мире становится полностью известным как всеведущий; (2) обладает тридцатью двумя признаками великого существа; (3) благодаря десяти силам уничтожает все сомнения у всех существ; (4) и то, что является отсутствием у всех противников возможности возражать и оспаривать слова того объясняющего Учение благодаря четырём неустрашимостям; (5) являет в своём обуздывающем Учении восьмичленный путь святого и являет четыре принципа шрамана<sup>222</sup>. Если так, то возникновение, признаки, уничтожение сомнений, невозможность возражать и опровергать, восприятие<sup>223</sup> шрамана следует признать пятью признаками всеведущего.
        </p>

        <p>
            Итак, то доказательство наличия в силу приемлемости является полностью чистым по пяти признакам благодаря непосредственному верному познанию, опосредованному верному познанию<sup>224</sup> и верному познанию, опирающемуся на вызывающее доверие поучение<sup>225</sup>.
        </p>

        <p>
            Каковы же семь видов признаков полностью нечистого? (1) Признак восприятие в соответствии с иным, чем то; (2) признак восприятие не в соответствии с иным, чем то; (3) признак восприятие в соответствии со всем; (4) признак восприятие не в соответствии со всем; (5) признак подходящий пример другого рода; (6) признак полная нереализованность; (7) признак объяснение целиком и полностью нечистой проповеди.
        </p>

        <p>
            Признаком восприятия в соответствии со всем является то, что является осуществлением осознавания всех дхарм посредством виджняны ума. Признаком восприятия не в соответствии со всем является то, что является установлением не в соответствии с признаками знака, сущности, деяния, дхармы, причины и плода, с признаком несоответствующих, с обоюдными признаками из-за какого бы то ни было несоответствия.
        </p>

        <p>
            При этом, Манджушри, поскольку у признака восприятия в соответствии с иным, чем то, вместе с примером имеется признак восприятия не в соответствии со всем, постольку то следует называть признаком полной нереализованности, раз то не устанавливается вместе в отношении реализуемого. Поскольку у признака восприятия не в соответствии с иным, чем то, вместе с примером тоже имеется признак восприятия в соответствии со всем, постольку и то следует называть признаком полной нереализованности, раз не устанавливаются вместе в отношении реализуемого. Из-за полной нереализованности является полностью нечистым познанием посредством доказательства. Следует знать, что признак полагания на целиком и полностью нечистую проповедь по природе является полностью нечистым.
        </p>

        <p>
            При этом доказательством татхаты является то, что является — пришли Татхагаты или же не пришли — пребыванием сферы татхаты ради осуществления пребывания дхармы.
        </p>

        <p>
            При этом (з) подразделение и (и) развитие вкратце таковы: дхарма, указываемая одним словом, полностью раскрывается последующими словами, и то, что осуществляет приход к концу.
        </p>

        <p>
            (5) При этом установления памятований и другие соответствующие стороне Просветления дхармы, которые я объяснил и которые воспринимаются как объекты, наделённые видом, являются признаком их сущности. (6) Признаком обретения плода того является то, что является реализацией мирских и ушедших от мира достоинств — плодов того — благодаря отвержению клеш у существ мирских и ушедших от мира. (7) То подробное описание, изложение и истинное объяснение другим того, что является истинным пониманием отдельного полного освобождения того, является признаком полного наименования вкушения того. (8) То, что является дхармами обладателя клеш, типа предметов, создающих помехи созерцанию тех соответствующих стороне Просветления дхарм, является признаком дхарм, препятствующих тому. (9) Те дхармы, которые содействуют тем дхармам, являются дхармами, соответствующими тому. (10) То, что является недостатками<sup>226</sup> препятствующих, является признаком зла того. (11) То, Манджушри, что является достоинствами соответствующих, следует признать признаком пользы того».
        </p>

        <p>
            Далее. Бодхисаттва Манджушри сказал Бхагавану так:
        </p>

        <p>
            «Бхагаван, молю объяснить в полноте центральную идею<sup>227</sup> тех бодхисаттвовских разделов Писания — раздела Сутр, Винаи и Матрики, ту центральную идею, благодаря которой Бодхисаттвы проникнут в сокрытую мысль изреченных Татхагатой глубоких Учений, ту центральную идею, которая не является общей с идеями иноверцев и обычных людей».
        </p>

        <p>
            «Раз так, Манджушри, то слушай, объясню тебе в полноте центральную идею, чтобы Бодхисаттвы проникли в мои тайны. Манджушри, всё то, что является дхармами полного осквернения и что является дхармами совершенного очищения, не имеет движения, не имеет индивида, и поэтому я объясняю дхармы как не имеющие действия никогда-нигде-ни под каким видом. Дхармы полного осквернения не являются тем, что прежде было полностью осквернённым, а потом станет совершенно очищенным, и дхармы совершенного очищения не являются тем, что потом — совершенно чисто, а прежде было полностью осквернённым. Но при этом простаки, обычные существа, опираясь на таящийся в глубинах взгляд, признающий наделённое греховностью тело обладающим сущностью дхармы или индивида, воспринимают Я и Моё, и поскольку так, то возникает осуществление ложного: “Вижу. Слышу. Обоняю. Вкушаю. Осязаю. Сознаю. Ем. Действую. Полное осквернение. Совершенное очищение”.
        </p>

