        <p>Шарипутра! Для всех дхарм пустота – их сущностный признак. Они не рождаются и не гибнут,
            не загрязняются и не очищаются, не увеличиваются и не уменьшаются. Поэтому в пустоте нет
            группы чувственно воспринимаемого, нет групп чувства, представлений, формирующих
            факторов и сознания,<a href="#citation-3" id="ref-3"
                                  class="citation-mark"><sup>3</sup></a> нет способностей
            зрительного, слухового, обонятельного, вкусового, осязательного и умственного
            восприятия,<a
                href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a> нет зримого,
            слышимого, обоняемого, ощущаемого вкусом, осязаемого<a href="#citation-5" id="ref-5"
                                                                   class="citation-mark"><sup>5</sup></a>
            и нет дхарм; нет ничего от сферы зрительного восприятия и до сферы умственного
            восприятия.<a href="#citation-6" id="ref-6" class="citation-mark"><sup>6</sup></a></p>

        <p>Нет неведения и нет прекращения неведения и так вплоть до отсутствия старости и смерти и
            отсутствия прекращения старости и смерти.<a href="#citation-7" id="ref-7"
                                                       class="citation-mark"><sup>7</sup></a></p>

        <p>Нет страдания, причины страдания, уничтожения страдания и пути, ведущего к прекращению
            страданий.<a href="#citation-8" id="ref-8" class="citation-mark"><sup>8</sup></a></p>

        <p>Нет мудрости и нет обретения, и нет ничего обретаемого.</p>

        <p>По той причине, что бодхисаттвы опираются на праджня-парамиту, в их сознании отсутствуют
            препятствия. А поскольку отсутствуют препятствия, то отсутствует и страх. Они удалили и
            опрокинули все иллюзии и обрели окончательную нирвану. Все Будды трех времен<a
                href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a> по причине
            опоры на праджня-парамиту обрели аннутара-самьяк-самбодхи.</p>

        <p>Посему знай, что праджня-парамита – это великая божественная мантра, это мантра великого
            пробуждения,<a href="#citation-10" id="ref-10" class="citation-mark"><sup>10</sup></a>
            это наивысшая мантра, это несравненная мантра, наделенная истинной сутью, а не
            пустопорожняя. Поэтому и называется она мантрой праджня-парамиты.</p>

        <p>Эта мантра гласит:</p>

