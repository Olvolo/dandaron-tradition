    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <p>Предлагаемый вниманию читателей небольшой текст является, тем не менее, одним из
            важнейших текстов буддизма Махаяны. Он относится к классу так называемых
            праджня-парамитских сутр, то есть канонических текстов, повествующих о высшей
            интуитивной премудрости, совершенном понимании, переводящем на другой берег
            существования, то есть в нирвану. Данная сутра не просто один из текстов данного класса.
            Она представляет собой как бы квинтэссенцию учения о Запредельной Премудрости, кратким и
            сжатым изложением его сути, сердцевины, сердца. Отсюда и название сутры.</p>

        <p>Как и всякая праджняпарамитская сутра, данный текст не просто излагает определенную
            доктрину, но как бы стремится породить в изучающем ее человеке особое, высшее состояние
            сознания, состояние непосредственного переживания, видения реальности, как она есть. А
            это состояние как раз и есть Праджня-парамита, Запредельная Премудрость. Сутра, как и
            разработанное на основе праджняпарамитских текстов философское учение мадхьямики
            (шуньявады), называет эту реальность шуньятой (пустотой) – неописываемой и невыразимой в
            категориях и понятиях (которые суть ментальные конструкты) реальностью.</p>

        <p>Сутра однозначно, в своеобразной "шоковой" манере провозглашает условность и
            относительность фундаментальнейших положений раннебуддийского учения (Хинаяна,
            Тхеравада), ставших ко времени ее создания буквально святыми для всех буддистов, и их
            неприменимость к истинной реальности, как она есть. С точки зрения этой истины
            (парамартха сатья) все элементы психики изначально упокоены и пребывают в нирване, а все
            живые существа уже здесь и теперь являются Буддами: сансара (круговерть
            рождений-смертей) и нирвана суть одно и то же. Усмотрение различий между ними – плод
            заблуждения, впрочем, тоже пустого и иллюзорного, лишенного своей собственной
            природы.</p>

        <p><strong>«Сутра сердца праджня-парамиты»</strong> (Праджня-парамита хридая сутра; кит.
            Божоболомидо синь цзин) является одной из наиболее популярных и почитаемых сутр
            дальневосточной Махаяны, особенно в школе Чань (Дзэн). Она и ранее переводилась на
            русский язык (с санскрита А.А.Терентьевым – "Сутра сердца Праджня-парамиты" и ее место в
            истории буддийской философии // Буддизм: история и культура. М., 1989 и с тибетского –
            С.Ю.Лепеховым – Идеи шуньявады в коротких сутрах Праджняпарамиты // Психологические
            аспекты буддизма. Новосибирск, 1991). С китайского языка текст переводится впервые.
            Данный перевод выполнен Е.А.Торчиновым с китайской версии великого китайского
            переводчика и философа 7 века Сюань-цзана, включенной им в его компендиум
            праджняпарамитских текстов Маха праджня-парамита сутра.</p>

        <p>Китайский текст не только отражает ее ранние санскритские версии, но и особенности
            восприятия и понимания сутры на Дальнем Востоке, поскольку не только в Китае, но и в
            Корее, и в Японии и во Вьетнаме языком буддийского канона был классический литературный
            китайский язык.</p>

