                    <li>на вершине, украшенной драгоценными камнями.</li>
                </ul>
                <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                    <li>Этот великолепный город Ланка</li>
                    <li>украшен разнообразными драгоценными камнями,</li>
                    <li>окружён горными вершинами,</li>
                    <li>свеж и прекрасен,</li>
                    <li>покрыт сетью драгоценностей.</li>
                </ul>
                <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                    <li>Бхагаван! Здесь якши,</li>
                    <li>свободные от пороков жадности,</li>
                    <li>размышляют о постигнутом в глубине ума (<em>pratyātmavedya</em>),</li>
                    <li>приносят дары Буддам прошлого.</li>
                    <li>Они веруют в учение Махаяны (<em>Mahāyāna</em>)</li>
                    <li>и стремятся дисциплинировать друг друга.</li>
                </ul>
