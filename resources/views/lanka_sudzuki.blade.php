                <li>Как нет различия между океаном и его волнами, так в уме нет развёртывания
                    виджнян.
                </li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Карма накапливается умом, размышляется манасом, распознаётся мановиджняной, а
                    видимый мир различается пятью виджнянами.
                </li>
            </ul>
        </blockquote>

        <h4>Учение о пяти дхармах, трёх свабхавах и двойной безличности</h4>
        <p>Затем Махамати попросил:</p>
        <p>— Наставь меня, Бхагаван, о той глубочайшей дхарме, которая объясняет <strong>ум</strong>
            (<em>citta</em>), <strong>манас</strong> (<em>manas</em>), <strong>мановиджняну</strong>
            (<em>manovijñāna</em>), <strong>пять дхарм</strong>, <strong>три свабхавы</strong> и
            <strong>знаки</strong> (<em>lakṣaṇa</em>); которая практикуется Буддами и бодхисаттвами;
            которая отделена от состояния ума, признающего мир чем-то внешним по отношению к самому
            уму; и которая, разрушая все так называемые истины, установленные словами и
            рассуждениями, составляет суть учения всех Будд.</p>

        <p>Бхагаван ответил:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Причины, по которым возникает глазо-виджняна, — четыре. Каковы они? (1) Цепляние за
                внешний мир, не зная, что он есть сам ум; (2) Прикрепление к форме и силе привычки,
                накопленной с незапамятных времён ложным умопостроением; (3) Собственная природа,
                присущая виджняне; (4) Жажда разнообразных форм и проявлений.</p>
            <p>Именно по этим четырём причинам, Махамати, волны развёртывающихся виджнян
                возбуждаются на Алайя-виджняне, подобной водам потопа. То же самое — и с другими
                виджнянами чувств. Это сознание возникает мгновенно или постепенно в каждом органе
                чувств, включая атомы и поры кожи; поле чувств постигается, как зеркало, отражающее
                объекты, или как океан, охваченный ветром.</p>
            <p>Подобно этому, Махамати, волны океана ума непрерывно колеблемы ветром объектности;
                причина, действие и форма взаимно обуславливают друг друга нераздельно;
                функционирующие виджняны и изначальная виджняна таким образом неразрывно связаны. И
                поскольку собственная природа формы и прочего не постигнута, Махамати, возникает
                система пяти виджнян.</p>
            <p>Вместе с этой системой пяти виджнян возникает то, что называется <strong>мановиджняной</strong>
                (<em>manovijñāna</em>) — функция сознания, которая различает объективный мир и чётко
                определяет отдельные проявления, и именно через неё зарождается физическое тело. Но
                ни мановиджняна, ни другие виджняны не осознают, что они взаимно обусловлены и
                возникают из цепляния за различение проекций самого ума. Так виджняны функционируют,
                тесно связаны, различая мир представлений».</p>
        </blockquote>

        <h4>Предупреждение о ложном понимании самадхи</h4>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Так же, Махамати, йогины, входя в состояние умиротворения (<em>samāpatti</em>), не
                осознают действия тонкой силы привычки внутри себя; ибо они думают, что входят в
                умиротворение, уничтожая виджняны. Но на самом деле они пребывают в таком состоянии,
                не уничтожая виджняны, которые всё ещё существуют, поскольку семена силы привычки не
                уничтожены; и то, что они считают уничтожением, на деле есть лишь нефункционирование
                внешнего мира, к которому они более не привязаны.</p>
            <p>Таково, Махамати, тонкое действие Алайя-виджняны, которую, кроме Татхагат и тех
                бодхисаттв, что утвердились на ступенях, трудно постигнуть. Особенно тем, кто
                практикует дисциплину шравак, пратьекабудд и тиртиков — даже с их силами самадхи и
                внемирской мудрости — трудно различить её».</p>
        </blockquote>
        <h4>Учение о пяти дхармах, трёх свабхавах и двойной безличности</h4>
