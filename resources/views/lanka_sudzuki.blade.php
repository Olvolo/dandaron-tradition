        <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
            <h4>Ответ Бхагавана и чудесное видение</h4>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                    <li>«Превосходно, превосходно, Владыка Ланки!</li>
                    <li>Вновь превосходно, Владыка Ланки!</li>
                    <li>Так должен дисциплинировать себя йогин (<em>yogin</em>).</li>
                    <li>Татхагат и все вещи следует созерцать так, как созерцаешь их ты;</li>
                    <li>иначе — это нигилизм (<em>uccheda-dṛṣṭi</em>).</li>
                    <li>Все вещи следует постигать, превосходя ум, манас и виджняны (<em>citta,
                            manas,
                            vijñāna</em>),
                    </li>
                    <li>как это делаешь ты.</li>
                    <li>Смотри внутрь — не привязывайся к букве и поверхностному взгляду на вещи.
                    </li>
                    <li>Не впадай в состояния, концепции, переживания, взгляды и самадхи
                        (<em>samādhi</em>)
                    </li>
                    <li>шравак (<em>śrāvaka</em>), пратьекабудд (<em>pratyekabuddha</em>) и тиртиков
                        (<em>tīrthika</em>).
                    </li>
                    <li>Не питай склонности к пустословию и остроумию.</li>
                    <li>Не лелей понятие собственной сущности (<em>svabhāva</em>),</li>
                    <li>не думай о тщеславии владычества,</li>
                    <li>не пребывай в дхьянах, подобных шести мирским дхьяна и прочим.</li>
                </ul>
            </blockquote>
            <p>Тогда Владыка Ланки немедленно совершил обращение (<em>parāvṛtti</em>) в глубине ума,
                постигнув, что мир — не что иное, как его собственный ум (<em>citta</em>). Он
                утвердился
                в сфере, свободной от умопостроений (<em>nirvikalpa-gocara</em>), движим силой
                накопленных в прошлом добродетелей, обрёл искусство в понимании всех текстов,
                получил
                способность видеть вещи как они есть (<em>yathābhūta-jñāna</em>), стал независим от
                других, созерцал вещи собственной мудростью (<em>buddhi</em>), обрёл прозрение,
                лишённое
                умопостроений (<em>nirvikalpa-jñāna</em>), более не полагался на других, стал
                великим
                йогином дисциплины (<em>yogācāra</em>), способным проявляться во всех совершенных
                формах, глубоко познал все искусные средства (<em>upāya-kauśalya</em>), постиг
                характеристики каждой ступени (<em>bhūmi</em>), чтобы умело преодолевать их,
                возрадовался, созерцая природу ума, манаса и мановиджняны (<em>citta, manas,
                    manovijñāna</em>), обрёл взгляд, позволяющий разорвать тройную преемственность (<em>trisantati</em>),
                познал, как опровергать всякий аргумент тиртиков о причинности, полностью постиг
                Татхагатагарбху (<em>Tathāgatagarbha</em>), ступень Буддхудства
                (<em>buddhabhūmi</em>),
                сокровенное постижение (<em>adhyātma</em>), и обрёл пребывание в знании Будды (<em>buddhajñāna</em>).
            </p>
            <p>[В тот миг] с небес раздался голос:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <p>«Это подлежит постижению только внутренне» (<em>pratyātma-vedanīya</em>).</p>
            </blockquote>
