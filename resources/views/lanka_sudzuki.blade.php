                <strong>воспринимающая</strong> и <strong>различающая объект</strong>. Подобно тому
                как зеркало отражает формы, воспринимающее виджняна воспринимает [объекты]. Между
                воспринимающим и различающим виджняна нет различия; они взаимно обуславливают друг
                друга.</p>
            <p>Воспринимающее виджняна функционирует вследствие трансформации [ума], вызванной
                таинственной <strong>силой привычки</strong> (*vāsanā*), а различающее виджняна
                функционирует из-за того, что ум различает объективный мир и накопил силу привычки
                посредством ложного умопостроения с незапамятных времён».</p>
