        <h4>Пять дхарм и три свабхавы</h4>
        <p>Затем Бхагаван пояснил:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Махамати, существует <strong>пять дхарм</strong> (<em>pañca-dharma</em>):</p>
            <ol class="list-decimal pl-6">
                <li><em>Нимитта</em> — <strong>форма</strong> (внешние знаки, проявления),</li>
                <li><em>Нама</em> — <strong>имя</strong>,</li>
                <li><em>Викальпа</em> — <strong>умопостроение</strong>,</li>
                <li><em>Самьяк-джняна</em> — <strong>правильное знание</strong>,</li>
                <li><em>Татхата</em> — <strong>таковость</strong>.</li>
            </ol>
            <p>И три свабхавы (<em>trisvabhāva</em>):</p>
            <ol class="list-decimal pl-6">
                <li><em>Парикальпита</em> — <strong>воображаемая природа</strong> — возникает из
                    цепляния за имена и формы;
                </li>
                <li><em>Паратантра</em> — <strong>зависимая природа</strong> — возникает из взаимной
                    обусловленности причин и условий;
                </li>
                <li><em>Паринишпанна</em> — <strong>совершенная природа</strong> — есть таковость,
                    постигаемая благородной мудростью.
                </li>
            </ol>
            <p>Махамати, воображаемая природа — это то, что возникает из цепляния за имена и
                объекты. Зависимая природа — это поток взаимообусловленных виджнян. Совершенная
                природа — это недвойственное знание, в котором нет ни субъекта, ни объекта. Именно в
                ней пребывает Татхагатагарбха (<em>Tathāgatagarbha</em>)».</p>
        </blockquote>

        <h4>Двойное отсутствие „я“</h4>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Махамати, <strong>отсутствие „я“ в личности</strong> (<em>pudgala-nairātmya</em>)
                означает, что в пяти скандхах нет ни „я“, ни принадлежащего „я“. Они возникают из
                неведения, кармы и желания, и поддерживаются за счёт цепляния за объекты.</p>
            <p><strong>Отсутствие „я“ в дхармах</strong> (<em>dharma-nairātmya</em>) означает, что и
                сами дхармы — скандхи, дхату, аятаны — лишены собственной сущности. Они — лишь
                агрегаты, обусловленные взаимно, без создателя и без самобытия.</p>
            <p>Когда бодхисаттва постигает эти два вида отсутствия „я“, он вступает на первую
                ступень и, постепенно восходя, достигает десятой ступени — Дхармамегхи. Там, воссев
                на великий лотосовый трон, украшенный драгоценными камнями, он получает помазание от
                всех Будд и обретает Дхармакаю (<em>Dharmakāya</em>), пребывающую в недвойственности
                и свободную от всех умопостроений».</p>
        </blockquote>

        <h4>Сущность Татхагатагарбхи</h4>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Махамати, Татхагатагарбха — не есть „я“, как учат тиртики. Она —
                <strong>пустота</strong> (<em>śūnyatā</em>), <strong>таковость</strong>
                (<em>tathatā</em>), <strong>незапятнанность</strong>, <strong>независимость от
                    причин и условий</strong>. Я учил о ней, чтобы те, кто боится учения об
                отсутствии „я“, не упали в нигилизм и обрели доверие к Махаяне.</p>
            <p>Татхагатагарбха — как драгоценный камень, завёрнутый в грязную ткань. Ткань — это
                скандхи, дхату, аятаны, испачканные жадностью, гневом и заблуждением. Но камень
                внутри — чист. Так и в каждом существе — сокрыта сущность Будды, не рождённая, не
                уничтожимая, не двойственная.</p>
            <p>Поэтому, Махамати, не смешивай Татхагатагарбху с „я“ философов. То — вечный творец, а
                это — недвойственное знание, постигаемое только внутренне».</p>
        </blockquote>
