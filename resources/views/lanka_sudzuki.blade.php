            <h4>Смех Будды и вопрос Махамати</h4>
            <p>Тогда бодхисаттва-махасаттва Махамати, которого прежде просил Равана вопрошать Будду
                о
                Его сокровенном постижении, движимый состраданием к Раване, зная помыслы собрания
                бодхисаттв и видя, что существа будущего будут смущены из-за своей привязанности к
                словесному учению (<em>deśanā-mārga</em>), из-за цепляния за букву как за [полное
                соответствие] смыслу (<em>artha</em>), из-за привязанности к силам дисциплины
                шравак,
                пратьекабудд и тиртиков — что приведёт их к недоумению: «Как может Татхагата,
                пребывая в
                внемирском состоянии сознания (<em>lokottara-vijñāna</em>), вдруг расхохотаться?» —
                Махамати, дабы утолить их любопытство, задал Будде следующий вопрос:</p>
            <p>— По какой причине, по какому поводу возник этот смех?</p>
            <p>Сказал Бхагаван:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <p>Превосходно, превосходно, Махамати! Вновь превосходно, Махамати! Видя мир таким,
                    как
                    он есть (<em>yathābhūtam</em>), и желая просветить тех, кто в прошлом, настоящем
                    и
                    будущем пал в ложные взгляды, ты берёшься задать Мне этот вопрос. Так и должно
                    быть
                    у мудрых, кто вопрошает ради себя и других.</p>
                <p>О Махамати! Равана, Владыка Ланки, задавал двойной вопрос (<em>dvaya-praśna</em>)
                    прошлым Татхагатам — Арахантам, Саманвагатам; и ныне он желает задать Мне тот же
                    двойной вопрос, дабы уяснить его различение, достижение и пределы (<em>viveka,
                        adhigama, gati</em>). Это — то, чего не вкусил никто, кто практикует
                    медитацию
                    шравак, пратьекабудд и тиртиков. И в будущем любознательный десятиглавый будет
                    задавать тот же вопрос Буддам грядущего.</p>
            </blockquote>

        </div>
        <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
            <h4>Вопрос Раваны о дхарме и адхарме</h4>
            <p>Зная это, Бхагаван обратился к Владыке Ланки:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <p>Вопрошай, Владыка Ланки! Татхагата дарует тебе разрешение. Не медли! Каковы бы ни
                    были вопросы, что ты желаешь задать, Я отвечу на каждый из них с
                    рассудительностью,
                    дабы удовлетворить твой ум (<em>citta</em>).</p>
                <p>Сохрани своё местопребывание мысли (<em>citta-sthiti</em>) свободным от ложного
                    умопостроения (<em>vikalpa</em>); зри ясно, что надлежит преодолеть на каждой
                    ступени (<em>bhūmi</em>); постигай вещи мудростью (<em>buddhi</em>); познав
                    подлинную природу (<em>dharmatā</em>) в себе самом, пребывай в блаженстве
                    самадхи
                    (<em>samādhi-sukha</em>); будучи объят Буддами в самадхи, пребывай в блаженстве
                    умиротворения (<em>prasrabdhi</em>); превзойди самадхи и постижение, достигнутое
                    шраваками и пратьекабуддами; утвердись на ступенях Ачала (<em>Acalā</em>),
                    Садхумати
                    (<em>Sādhumatī</em>) и Дхармамегха (<em>Dharmameghā</em>); глубоко познай
                    отсутствие
                    «я» во всех дхармах (<em>sarva-dharma-nairātmya</em>) в его истинном значении;
                    будь
                    посвящён Буддами в самадхи в великом чертоге лотосов-драгоценностей.</p>
                <p>Окружённый бодхисаттвами, восседающими на разнообразных лотосах, поддерживаемых
                    благодатью (<em>adhiṣṭhāna</em>) Будд, ты увидишь себя сидящим на лотосе, а
                    каждый
                    из бодхисаттв — взирающим на тебя лицом к лицу. Это — царство, превосходящее
                    воображение (<em>acintya-gocara</em>). Продумай достойный замысел
                    (<em>caryā-upāya</em>), утверди себя на ступени дисциплины, включающей все
                    искусные
                    средства (<em>upāya-kauśalya</em>), дабы постигнуть то царство, что превосходит
                    воображение; и обретёшь ступень Татхагаты (<em>tathāgata-bhūmi</em>), где
                    способен
                    проявляться в бесчисленных формах — зрелище, никогда не виданное шраваками,
                    пратьекабуддами, тиртиками, Брахмой, Индрой, Упендрой и другими.</p>
            </blockquote>
            <p>Владыка ракшасов, обновлённый в своих украшениях, полный сияния и красоты, в диадеме,
                браслетах и ожерелье, нанизанном на нить ваджры, сказал:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <p>Говорится: даже дхармы надлежит оставить, не говоря уже об адхармах
                    (<em>adharmāḥ</em>). Бхагаван! Почему же существует эта двойственность
                    (<em>dvaya</em>), ради которой нас призывают к отказу? Что такое адхармы? Что
                    такое
                    дхармы? Как может существовать двойственность вещей, подлежащих оставлению —
                    двойственность, возникающая из умопостроения (<em>vikalpa</em>), из приписывания
                    собственной сущности там, где её нет, из [ложного различения] сотворённого (<em>bhautika</em>)
                    и несотворённого (<em>asaṃskṛta</em>), поскольку природа Алайя-виджняны — вне
                    умопостроений?</p>
                <p>Подобно тому как волосок в воздухе (<em>keśonḍuka</em>) принимается за реально
                    существующий, так и понятие двойственности принадлежит сфере ума, не до конца
                    очищенного от умопостроений. Если это так, как же может быть какой-либо отказ
                    [от
                    дхарм и адхарм]?</p>
            </blockquote>
        </div>
        <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
            <h4>Ответ Бхагавана: всё — проявление ума</h4>
            <p>Сказал Бхагаван:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                <p>Владыка Ланки! Разве не видишь ты, что различение вещей — как, например, горшков
                    (<em>ghaṭa</em>) и иных хрупких предметов, природа которых — непостоянство (<em>anityatā</em>),
                    — возникает в сфере умопостроения (<em>vikalpa-gocara</em>) невежд? Если так,
                    разве
                    не следует понимать это именно так?</p>
                <p>Именно из-за умопостроения невежд возникает различение дхармы и адхармы (<em>dharma-adharma</em>).
                    Однако благородная мудрость (<em>ārya-jñāna</em>) не постигается через такое
                    видение.</p>
                <p>Что же такое дхармы (<em>dharma</em>)? Это — то, что различают тиртики
                    (<em>tīrthika</em>), шраваки (<em>śrāvaka</em>), пратьекабудды
                    (<em>pratyekabuddha</em>) и невежды своими умопостроениями. Они полагают, что
                    такие
                    дхармы, как качество (<em>guṇa</em>), сущность (<em>dravya</em>) и прочие,
                    возникают
                    от причин — и эти представления надлежит оставить.</p>
                <p>Ибо такие дхармы — не реальность, а лишь проявления (<em>lakṣaṇa</em>). Из-за
                    цепляния за проявления собственные проявления ума принимаются за реальность
                    (<em>dharmatā</em>).
                </p>
                <p>Такие вещи, как горшки, — продукты умопостроения невежд; они не существуют; их
                    сущность недостижима. Такой взгляд на вещи известен как оставление
                    (<em>tyāga</em>).
                </p>
                <p>Что же такое адхармы (<em>adharma</em>)? Это — дхармы, лишённые собственной
                    сущности
                    (<em>niḥsvabhāva</em>), не рождённые умопостроением, превосходящие причинность.
                    В
                    них нет двойственности «реального» и «нереального». Это — оставление дхарм.</p>
                <p>Что значит — недостижимость дхарм? Подобно рогам зайца, осла, верблюда, коня, или
                    младенцу бесплодной женщины — таковы дхармы, чья природа недостижима (<em>anupalabdha</em>).
                    Они не являются предметом мышления как нечто реальное, ибо их суть — лишь имена
                    (<em>nāma-mātra</em>). В обыденной речи о них говорят, но не следует цепляться
                    за
                    них, как за реально существующие горшки и прочее.</p>
                <p>Так же, как эти иллюзорные объекты надлежит оставить — ибо они непостижимы умом (<em>vijñāna</em>),
                    — так и все формы умопостроения (<em>bhāva-vikalpa</em>) надлежит оставить. Это
                    и
                    называется: оставление дхарм и адхарм.</p>
                <p>Татхагаты, пребывая в реальности (<em>dharmatā</em>), не порождают измышлений,
                    превосходят умопостроение и тщетные рассуждения, и не предаются различению
                    отдельных
                    форм (<em>rūpa</em>) — кроме случаев, когда ради просвещения невежд и ради их
                    блага
                    они раскрывают реальность.</p>
                <p>Владыка Ланки! Существа — лишь проявления (<em>nimitta</em>), подобны
                    изображениям на
                    стене — в них нет чувствительности, нет сознания. Владыка Ланки! Всё в мире
                    лишено
                    действия (<em>akriyā</em>), ибо всё лишено реальности (<em>asat</em>). Здесь нет
                    ни
                    слышащего, ни слышимого. Владыка Ланки! Всё в мире — подобно иллюзии
                    (<em>māyā</em>),
                    магически преобразованному образу. Этого не постигают тиртики и невежды.</p>
                <p>Владыка Ланки! Тот, кто так видит — видит истинно. Кто видит иначе — ходит в
                    умопостроении; цепляясь за него, держится двойственности. Это — как видеть своё
                    отражение в зеркале, тень в воде, лунный отсвет, тень в доме, эхо в ущелье.
                    Люди,
                    ухватывая собственные тени умопостроения, утверждают различение дхармы и адхармы
                    и,
                    не совершая оставления двойственности, продолжают различать — и никогда не
                    обретают
                    умиротворения. А умиротворение (<em>śama</em>) — это единонаправленность (<em>ekāgratā</em>),
                    а единонаправленность рождает высшее самадхи (<em>parama-samādhi</em>), которое
                    обретается, входя в утробу Татхагатства (<em>tathāgata-garbha</em>), — царство
                    благородной мудрости, постигаемой в глубине ума (<em>adhyātma-ārya-jñāna</em>).
                </p>
            </blockquote>
        </div>
