        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава II. Собрание всех
            дхарм</h3>
        <h4>Восхваление Махамати и введение в сто восемь вопросов</h4>
        <p>В то время бодхисаттва-махасаттва Махамати, посетивший все буддийские земли, вместе со
            всеми бодхисаттвами, поднялся со своего места силой Будд, накинул верхнюю одежду на одно
            плечо, опустил правое колено на землю, сложил ладони, обратился лицом к Бхагавану, с
            почтением поклонился Ему и восхвалил Его следующими стихами:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда Ты взираешь на мир Своей внемирской мудростью и состраданием,</li>
                <li>он для Тебя — подобен цветку в небе,</li>
                <li>о котором нельзя сказать, рождён он или уничтожен,</li>
                <li>ибо к нему не применимы категории бытия и небытия.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда Ты взираешь на все вещи Своей внемирской мудростью и состраданием,</li>
                <li>они для Тебя — подобны видениям,</li>
                <li>недоступным умственному постижению,</li>
                <li>ибо к ним не применимы категории бытия и небытия.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда Ты взираешь на мир Своей внемирской мудростью и состраданием,</li>
                <li>он для Тебя всегда — подобен сну,</li>
                <li>о котором нельзя сказать, вечен он или уничтожим,</li>
                <li>ибо к нему не применимы категории бытия и небытия.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>В Дхармакае, чья собственная природа — подобна видению или сну,</li>
                <li>что можно восхвалять?</li>
                <li>Когда не возникает мысли ни о существовании,</li>
                <li>ни об отсутствии собственной сущности — тогда есть восхваление.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Как можно восхвалять или порицать то,</li>
                <li>чья форма невидима,</li>
                <li>поскольку оно превосходит чувства и их объекты,</li>
                <li>о Муни?</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Твоя внемирская мудрость и сострадание,</li>
                <li>превосходящие форму,</li>
                <li>постигают отсутствие «я» в вещах и личностях,</li>
                <li>и Ты Сам всегда чист и свободен</li>
                <li>от препятствий страстей и знания.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Ты не исчезаешь в Нирване,</li>
                <li>и Нирвана не пребывает в Тебе;</li>
                <li>ибо она превосходит двойственность</li>
                <li>познающего и познаваемого, бытия и небытия.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Те, кто увидит Муни столь спокойным</li>
                <li>и превосходящим рождение [и смерть],</li>
                <li>очистятся от привязанности</li>
                <li>и будут незапятнанными как в этом, так и в ином мире.</li>
            </ul>
        </blockquote>

        <p>В то время бодхисаттва-махасаттва Махамати, восславив Бхагавана такими стихами, назвал
            Своё имя Бхагавану:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Я — Махамати, Бхагаван,</li>
                <li>и глубоко познал учение Махаяны.</li>
                <li>Желаю задать Тебе,</li>
                <li>наиболее красноречивому, сто восемь вопросов.</li>
            </ul>
        </blockquote>

        <p>Услышав его слова, Будда — наилучший знаток мира — окинул взглядом всё собрание и
            обратился к сыну Сугаты:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>— Вопрошайте, сыны Победоносного, и Махамати! Вы спрашиваете — и Я наставлю вас в
                самопостижении.</p>
        </blockquote>

        <p>В тот миг бодхисаттва-махасаттва Махамати, получив от Бхагавана разрешение говорить,
            поклонился у ногам Бхагавана и спросил:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl text-left pl-4 my-4">
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Как можно очиститься от ложного умопостроения? Откуда оно возникает? Как можно
                    распознать ошибки? Откуда они берутся?</li>
                <li>Откуда возникают земли, преобразования, проявления и тиртики? Почему существует
                    состояние безобразности (<em>nirākāra</em>)? Откуда градации? Откуда сыны
                    Победоносного?</li>
                <li>Где путь освобождения? Кто связан? Чем он избавляется? Каково умственное
                    состояние тех, кто практикует дхьяны? Откуда тройная колесница
                    (<em>triyāna</em>)?</li>
                <li>Что рождается от причинности? Что есть следствие? Что есть причина [или
                    деятель]? Откуда учение о двойственности? Откуда оно возникает?</li>
                <li>Почему практика умиротворения бесформенности? И практика полного угасания?
                    Почему угасание мыслей? И как пробуждаются от этого?</li>
                <li>Как возникает действие (<em>karma</em>)? Откуда поведение тех, кто цепляется
                    за тело? Откуда [этот] видимый [мир]? Откуда условия? Откуда вход на ступени?
                </li>
                <li>Кто пронзает эту тройную сферу существования? Что есть обитель? Что есть тело?
                    Откуда возникает то, что пребывает? Откуда сын Будды?</li>
                <li>Кто обретает сверхъестественные способности, самовладение, самадхи? Как ум
                    умиротворяется? Молю, скажи, о Победоносный, подобный быку!</li>
                <li>Что такое Алайя? Откуда Мановиджняна? Как возникает видимый [мир]? Как он
                    перестаёт быть видимым?</li>
                <li>Откуда семьи и бесфамильные? Что значит «всё — лишь ум» (<em>cittamātra</em>)?
                    Установление знаков? Откуда [учение об] отсутствии "я"?</li>
                <li>Почему нет бытия? Какое учение согласуется с обыденным мышлением? Как можно
                    прекратить лелеять вечность (<em>śāśvata-dṛṣṭi</em>) и нигилизм
                    (<em>uccheda-dṛṣṭi</em>)?</li>
                <li>Что означает пустота (<em>śūnyatā</em>)? Что такое мгновенное уничтожение
                    (<em>kṣaṇika-vināśa</em>)? Откуда <strong>Утроба</strong> (<em>garbha</em>)?
                    Откуда <strong>устойчивость мира</strong>?</li>
                <li>Почему мир подобен <strong>видению</strong> и <strong>сну</strong>?</li>
                <li>Как он подобен <strong>городу гандхарвов</strong> (<em>gandharva-nagara</em>)?
                </li>
                <li>Почему его следует считать подобным <strong>миражу</strong>, <strong>отражению
                        луны в воде</strong>? Молю, скажи.</li>
                <li>Что такое <strong>элементы просветления</strong> (<em>bodhyaṅga</em>)? Откуда
                    <strong>составляющие просветления</strong>?</li>
                <li>Откуда <strong>революция</strong> и <strong>смута царства</strong>?</li>
                <li>Как возникает <strong>реалистический взгляд на существование</strong>
                    (<em>bhava-dṛṣṭi</em>)?</li>
                <li>Что значит, что мир <strong>превосходит рождение и смерть</strong>?</li>
                <li>Или что он подобен <strong>цветку в небе</strong>?</li>
                <li>Как Ты это понимаешь? Почему это <strong>превосходит слова</strong>?</li>
                <li>Как это <strong>не подлежит умопостроению</strong>?</li>
                <li>Как оно подобно <strong>небу</strong>?</li>
                <li>Скольких видов <strong>таковость</strong> (<em>tathatā</em>)?</li>
                <li>Насколько многообразен <strong>ум</strong> (<em>citta</em>)?</li>
                <li>Сколько <strong>Парамит</strong> (<em>pāramitā</em>)?</li>
                <li>Откуда <strong>градации ступеней</strong> (<em>bhūmi</em>)?</li>
                <li>Что такое <strong>состояние безобразности</strong> (<em>animitta</em>)?</li>
                <li>Откуда <strong>двойное отсутствие „я“</strong>?</li>
                <li>Как очищаются от <strong>препятствия знания</strong> (<em>jñeyāvaraṇa</em>)?
                </li>
                <li>Скольких видов <strong>знание</strong> (<em>jñāna</em>)? О Владыка!</li>
                <li>Сколько <strong>моральных предписаний</strong>? Сколько <strong>форм
                        бытия</strong>?</li>
                <li>Откуда <strong>семьи</strong>, рождённые из <strong>золота, драгоценного камня,
                        жемчуга</strong>?</li>
                <li>От кого рождается <strong>речь</strong>?</li>
                <li>Откуда <strong>различение существ</strong>?</li>
                <li>Откуда <strong>науки, должности, искусства</strong>?</li>
                <li>И <strong>кем</strong> они проявляются?</li>
                <li>Скольких видов <strong>гатхи</strong> (<em>gāthā</em>)? Что такое
                    <strong>проза</strong>? Что такое <strong>размер</strong> (<em>chandas</em>)?
                    Скольких видов <strong>рассуждение и толкование</strong>?</li>
                <li>Сколько разновидностей <strong>пищи и питья</strong>? Откуда возникает
                    <strong>половое влечение</strong>? Откуда <strong>цари, верховные правители и
                        владыки земель</strong>?</li>
                <li>Как <strong>царь охраняет своё царство</strong>? Скольких родов <strong>небесных
                        существ</strong> (<em>deva</em>)? Откуда <strong>земля, звёзды, созвездия,
                        луна и солнце</strong>?</li>
                <li>Скольких видов <strong>освобождение</strong> (<em>nirvāṇa</em>)? Скольких видов
