        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Есть два способа, Махамати, в которых происходит возникновение, пребывание и
                угасание виджнян, и этого не понимают тиртики. То есть: угасание происходит в
                аспекте <strong>преемственности</strong> (*santati*) и в аспекте
                <strong>формы</strong> (*ākāra*). В возникновении виджнян также различимы эти два:
                возникновение в аспекте преемственности и возникновение в аспекте формы. В
                пребывании — также: одно относится к преемственности, другое — к форме.</p>
            <p>Далее, Махамати, в виджнянах различимы три модуса: (1) виджняна как <strong>развёртывающееся</strong>
                (*pravṛtti-vijñāna*), (2) виджняна как <strong>производящее следствия</strong>
                (*vipāka-vijñāna*), и (3) виджняна как <strong>пребывающее в своей изначальной
