    <h1 class="text-center text-blue-900 font-bold text-2xl sm:text-2xl md:text-2xl mt-6 mb-6">
        Ланка-аватара-сутра или самосознание благородной мудрости</h1>

    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава I. О
            различениях</h3>

        <p>Вот что я слышал. Однажды Благословенный явился в замок Ланка, на вершине горы Малайя,
            посреди великого океана. В то же время чудесным образом собралось великое множество
            бодхисаттв-махасаттв из всех земель Будды, а также огромное количество
            бхикшу.</p>

        <p>Бодхисаттвы-махасаттвы во главе с Махамати были совершенными мастерами различных самадхи.
            Они овладели искусством медитации, обладали десятью силами (санскр. <em>daśa-bala</em>)
            и шестью сверхъестественными способностями (абхиджня, санскр. <em>abhijñā</em>).
            Унаследовав Дхарму из собственных рук Будды, они глубоко понимали природу этого
            явленного мира и умели применять разнообразные средства учения и дисциплины в
            соответствии с особенностями мышления и поведения живых существ, с которыми встречались.
            Они были прекрасны в своём совершенстве, знакомы с пятью дхармами
            (санскр. <em>pañca-dharma</em>), стремились к овладению восемью
            виджнянами и двумя видами отсутствия «я» (санскр. <em>dvy-anātman</em>).</p>

        <p>Благословенный, уловив умственное возмущение, происходившее в сознании собравшихся —
            подобное поверхности океана, колеблемой ветрами, — почувствовал в глубине своего сердца
            великое сострадание. Он улыбнулся и сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>В древние времена Татхагаты прошлых эпох, достигшие архатства и полного просветления,
                однажды собрались в зале Ланки, на вершине горы Малайя, и обсуждали Благородную
                Мудрость (арья-праджня, санскр. <em>ārya-prajñā</em>), которая выходит за пределы
                рассудочного знания философов, а также за пределы состояния сознания шраваков и
                пратьекабудд. Её можно реализовать лишь погрузившись в высшую самадхи, в глубины ума.</p>
            <p>Сейчас я повторяю то же самое рассуждение об этой истине. Всё, что видимо в этом
                мире, лишено способности к действию, ибо всё в мире подобно сну, сну внутри сна или
                образу, чудесным образом сотворённому из ничего. Этого не понимают философы,
                тогда как те, кто видит действия таким образом, видят их подлинно. Те же, кто видит
                иначе, начинают порождать различные умственные действия.</p>
            <p>Вовлекаясь в мир сансары и принимая различия за реальность — подобно зрелищу
                собственного отражения в зеркале, собственной тени, образу в воде или эху в долине,
                — они, не сумев отказаться от привязанности к восприятию реальности,
                продолжают различать и различать, и потому никогда не обретают покоя.</p>
            <p>Под покоем же подразумевается изначальное единство — единство высочайшей самадхи,
                достигаемое через вхождение в сферу Благородной Мудрости, которую можно постичь
                лишь изнутри, из самых глубин сознания.</p>
        </blockquote>

        <p>Тогда все бодхисаттвы-махасаттвы встали со своих мест и, почтительно поклонившись,
            восславили Будду.</p>

        <p>Бодхисаттва-махасаттва Махамати облачился в торжественные одеяния и, сложив
            ладони у груди, восхвалил Его в следующих стихах:</p>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда ты смотришь на мир с совершенным пониманием и состраданием,</li>
                <li>он должен являться тебе подобным небесному цветку,</li>
                <li>о котором нельзя сказать: «Он возник» или «Он разрушен»,</li>
                <li>ибо понятия бытия и небытия неприменимы к нему.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда ты смотришь на мир с совершенным пониманием и состраданием,</li>
                <li>он должен казаться тебе подобным сну,</li>
                <li>о котором нельзя сказать: «Он вечен» или «Он преходящ»,</li>
                <li>ибо термины «бытие» и «небытие» к нему неприменимы.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда ты смотришь на все явления с совершенным пониманием и состраданием,</li>
                <li>они должны являться тебе подобными видениям,</li>
                <li>выходящим за пределы человеческого ума,</li>
                <li>ибо представления о бытии и небытии к ним не применимы.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Когда с совершенным пониманием и безграничным состраданием</li>
                <li>ты видишь, что ни в явлениях, ни в существах нет «я»,</li>
                <li>и пребываешь свободным и чистым —</li>
                <li>от всяких препятствий, от страстей,</li>
                <li>от учёности и от эгоизма,</li>
                <li>ты не растворяешься в Нирване,</li>
                <li>но Нирвана пребывает в тебе.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>Ибо Нирвана превосходит всякую двойственность:</li>
                <li>знание и незнание, бытие и небытие.</li>
                <li>Те, кто видит тебя таким —</li>
                <li>безмятежным и свободным от всяких представлений, —</li>
                <li>освобождаются от привязанностей</li>
                <li>и очищаются от скверны —</li>
                <li>как в этом мире, так и в мире ином.</li>
            </ul>
            <ul class="list-none mb-8 pl-2 space-y-2 ml-2">
                <li>В этом мире, чья природа подобна сну,</li>
                <li>присутствуют хвала и хула,</li>
                <li>но к высшей реальности Дхармакайи (Тела Дхармы), —</li>
                <li>пребывающего за пределами чувств и различающего сознания, —</li>
                <li>какая может быть хвала и какая хула?</li>
                <li>О, ты, мудрейший из мудрых!</li>
            </ul>
        </blockquote>

        <p>Затем бодхисаттва-махасаттва Махамати сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>О Благословенный! О Сугата! Архат! Полностью просветлённый! Ты видишь реализацию
                Благородной Мудрости, которая вне причин и вне методов философов, которая лишёна
                всяких предикатов и категорий — таких как бытие и небытие, единство и
                множественность, существование и несуществование, вечность и невечность. Она не
                имеет ничего общего с единичным, множественным и всеобщим, с ложными
                воображениями и иллюзиями всякого рода, порождаемыми самим умом
                (читта, санскр. <em>citta</em>),
                но является истиной высочайшей реальности (татхата, санскр. <em>tathatā</em>).</p>
            <p>Те, кто движется по пути мудрости, неуклонно следуя всем предписаниям, проходят
                стадии
                посвящения и в конце концов входят в состояние Татхагаты. Благодаря обетам, принятым
                изначально и соблюдаемым во всех своих усилиях, они искореняют всякое влияние
                бесконечных миров существования.</p>
            <p>Пусть же и мы станем подобны кристаллу, который отражает разнообразные цвета,
                сохраняя при этом чистоту своей сущности! Благодаря этому наставлению я и прочие
                бодхисаттвы-махасаттвы сможем привести все чувствующие существа к подобному
                состоянию совершенства и добродетели.</p>
        </blockquote>

        <p>Тогда Благословенный сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Хорошо, очень хорошо, Махамати! И ещё раз — хорошо, воистину!</p>
            <p>Именно благодаря твоему состраданию ко всем мирам и тому благу, которое принесёт твоё
                учение — проповедуемое как в человеческом облике, так и в небесном, — ты теперь
                способен выслушать то, что я поведаю по твоей просьбе. Итак, Махамати, слушай
                внимательно и верно размышляй о том, что я скажу, ибо я наставляю!</p>
        </blockquote>

        <p>Тогда Махамати и все прочие бодхисаттвы-махасаттвы погрузились в слушание, стремясь
            постичь смысл учения Благословенного.</p>

        <p>Благословенный сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Махамати, несведущие и простые люди, довольствуясь здравым смыслом и не зная, что
                этот мир есть лишь проявление ума (читта-матра, санскр. <em>citta-mātra</em>),
                цепляются за множественность внешних объектов и утверждают понятия бытия и небытия,
                единого и многого, двойственного и недвойственного, существования и несуществования,
                вечности и невечности. При этом они полагают, будто обладают собственной природой
                субъекта, в котором всё возникает.</p>
            <p>Однако всё, что возникает из различий ума, продолжает существовать благодаря инерции
                и привычке, и от этого рождаются и далее развиваются ложные представления. Эта
                действительность подобна миражу в пустыне: человек видит источники воды и считает их
                реальными. Подобное может поразить даже воображение животных — жаждущие, под
                влиянием палящих солнечных лучей, они бросаются в бешеный бег, гонясь за мнимыми
                источниками. Не зная, что эти источники — лишь галлюцинации в их разгорячённых умах,
                они не понимают, что воды там нет, и гибнут.</p>
            <p>Точно так же, Махамати, несведущие и простые люди отличаются тем, что их умы пылают
                огнём жадности, гнева и глупости. Они пребывают в мире многообразных форм, одержимые
                идеями рождения, становления и разрушения, не понимая по-настоящему, что означают
                «существующее» и «несуществующее». На них производят впечатление ложные различения и
                умственные построения, накопленные с безначальных времён. Для них становится
                привычкой цепляться за всё это, и потому они крепко держатся за него.</p>
            <p>Всё это подобно городу гандхарвов (иллюзорному городу, санскр.
                <em>gandharva-nagara</em>), который несведущие принимают за реальный город, хотя на
                самом деле его нет. Город возникает лишь как видение, благодаря привязанности к
                впечатлениям памяти о ранее виденном городе, сохранённым в уме в виде семян (васана,
                санскр. <em>vāsanā</em>). Об этом городе нельзя сказать, что он существует или не
                существует, — и в то же время можно сказать, что он и существует, и не существует
                одновременно.</p>
            <p>Подобным же образом люди цепляются за воспоминания о ложных умственных построениях и
                учениях, накопленных с безначальных времён. Верующие придерживаются всевозможных
                убеждений, настаивают на идеях единства и множественности/тождественности и
                различия, бытия и небытия, и их мысли нечисты. Они не понимают, что всё это — лишь
                проявления ума.</p>
            <p>Их можно уподобить человеку, которому приснилась страна, наполненная разными людьми —
                мужчинами и женщинами, слонами, лошадьми, повозками, пешеходами, деревнями,
                городами, сёлами, коровами, буйволами, домами, лесами, горами, реками и озёрами. Он
                движется по этой земле, переходит из города в город — но вдруг пробуждается и,
                полусонный, вспоминает город своих снов и размышляет об опыте, накопленном там.</p>
            <p>Что же ты думаешь, Махамати: будет ли тот, кто позволяет своему уму блуждать среди
                нереальностей, видимых во сне, считаться мудрым или глупцом?</p>
            <p>Точно так же несведущие простые умы, повторяя впечатления и ложные взгляды философов,
                не распознают и не понимают, что эти взгляды подобны идеям, возникающим во сне и
                порождаемым самим умом. Поэтому они крепко держатся за свои понятия единства и
                множественности, бытия и небытия и так далее.</p>
            <p>А ведь всё это подобно холсту художника: лишь несведущий может вообразить, будто
                видит на нём неровности, соответствующие горам и долинам, тогда как холст на самом
                деле плосок.</p>
            <p>Подобным же образом существуют ныне люди, которые попадают под влияние ложных
                взглядов о единстве и множественности/тождественности и различии, обоюдности («и
                то, и другое», санскр. <em>ubhaya</em>) и необоюдности («ни то, ни другое», санскр.
                <em>anubhaya</em>), правильность которых обусловлена инерцией привычки. Они коснеют
                в воображаемых представлениях и упорно утверждают, будто те, кто придерживается
                подлинного учения о перерождении — свободного от альтернатив бытия и небытия, —
                являются нигилистами, отрицающими всё. Утверждая подобное, они хотят погубить
                других, но ведут к гибели прежде всего самих себя.</p>
            <p>Они не верят в закон причины и следствия (пратитья-самутпада, санскр.
                <em>pratītya-samutpāda</em>). Эти последователи ложных взглядов обрывают
                благотворные причины и благотворные истоки в своём существе, что ведёт к прекращению
                состояния чистоты. Их умы затмеваются зловредными желаниями, и они теряют
                способность понимать превосходных существ.</p>
            <p>Они подобны людям, у которых наступило помрачение зрения: видя на роговице якобы
                волосок, они восклицают друг другу: «Как он прелестен! Взгляните, как он
                удивителен!» Однако этот волосок никогда не существовал как внешний объект. Он
                практически не есть ни сущее, ни несущее — он и видим, и невидим.</p>
            <p>Подобным же образом те, чьи умы подвержены различениям и находятся под влиянием
                ложных взглядов философов, отдают предпочтение реалистическим представлениям о бытии
                и небытии, противоречат подлинной Дхарме и ведут к разрушению себя и других.</p>
            <p>Всё это подобно огненному колесу, кажущийся образ которого возникает при
                вращении головешки: здесь нет никакого колеса — оно лишь воображаемо теми, кто не
                понимает природы вращающейся головни. Нет здесь ни колеса, ни не-колеса — и всё же
                нечто видимо.</p>
            <p>Подобным же образом те, кто привык слушать и внимать различным взглядам философов,
                могут считать вещи, рождённые от причин, — иллюзорными и несуществующими, тогда как
                уничтоженные вещи они признают реальными, поскольку «они были».</p>
            <p>Это подобно зеркалу, отражающему цветы: образ, возникающий в отражении, соответствует
                условиям, в которых он появляется, ибо само зеркало совершенно беспристрастно. Это
                подобно эху в ущелье: звук возникает без говорящего. Это подобно миражу в пустыне:
                вода видима, но неуловима.</p>
            <p>Точно так же различающий, рассудочный ум несведущих, влекомый ложными представлениями
                и спекуляциями, движется среди подобных миражу волн рождения, становления и
                разрушения. Это подобно мёртвому телу, заставляемому магией двигаться,
                жестикулировать и произносить слова, хотя у него нет собственной силы.</p>
            <p>Точно так же несведущие и простые умом, доверяя ложным философским взглядам,
                полностью предаются идеям единства и множественности. Вследствие своей доверчивости
                они страдают, ибо эти представления лишены основания.</p>
            <p>По этой причине, о Махамати, ты и прочие бодхисаттвы-махасаттвы должны отбросить все
                различения, ведущие к понятиям рождения, обоюдности и необоюдности, бытия и небытия.
                Освободившись от оков инерции привычки, вы сможете достичь реальности, стяжав внутри
                себя Благородную Мудрость.</p>
        </blockquote>

        <p>Тогда Махамати спросил Благословенного:</p>
        <p>— Почему же несведущие предаются различениям, а мудрые — нет?</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Потому что несведущие цепляются за имена, знаки и идеи. Их умы движутся по этим
                руслам, они питаются множественностью объектов и впадают в ложное представление о
                субъекте — или душе, — которому всё это якобы принадлежит. Они играют в различения
                добра и зла среди видимостей и цепляются за приемлемое.</p>
            <p>Из-за этой привязанности возникает неведение (авидья, санскр. <em>avidyā</em>),
                порождается новая карма — карма жадности, гнева и глупости, — которая накапливается.
                Вследствие накопления кармы они становятся узниками мира, созданного их собственными
                различениями, и оказываются несвободными, неспособными вырваться из круга рождений и
                смертей (сансара, санскр. <em>saṃsāra</em>).</p>
            <p>Из-за своей грубости и глупости они не понимают, что все вещи подобны игре ума или
                миражу, отражению луны в воде; что нет никакой субстанции субъекта — она
                воображаема; что нет души, ибо ей ничего не принадлежит.</p>
            <p>Все их утверждённые идеи возникают лишь из различий и из того, что существует лишь
                как видимость собственного ума. Они не понимают, что вещи не имеют ничего общего с
                качественностью и отождествлением, что они не подвержены рождению, пребыванию и
                разрушению.</p>
            <p>Напротив, они утверждают: «Есть рождённые, есть сотворённые, а есть умершие
                безвозвратно». Это происходит потому, что они не подобны мудрым. Это происходит
                потому, что они несведущи и склонны лишь к различениям — и поэтому не подобны
                мудрым.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава II. Ложное
            воображение и условное знание</h3>

        <p>Затем к Благословенному обратился бодхисаттва-махасаттва Махамати и сказал ему:</p>
        <p>— Ты говорил нам о ложных взглядах. Не мог бы Ты описать их подробнее, чтобы мы могли
            распознавать их и остерегались их?</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Махамати, суть ошибочности тех ложных учений, которым обычно следуют философы,
                заключается в следующем. Они не позволяют различить, что мир проявлений возникает
                из самого ума (читта, санскр. <em>citta</em>) и как именно он возникает. Они не
                позволяют понять, что весь поток умственной деятельности также порождается самим
                умом. Однако, принимая различные проявления ума за нечто реальное, философы начинают
                различать их — подобно простым людям, полагающимся на здравый смысл. Отсюда в их
                умах возникает двойственность и представления о бытии и небытии, ибо они не знают
                того факта, что всё это — единая, неделимая сущность.
            </p>
            <p>Напротив, Моё учение основывается на понимании того, что мир проявлений подобен
                вид<strong>е</strong>нию и является проявлением самого ума. Оно настаивает на
                прекращении неведения (авидья, санскр. <em>avidyā</em>), исчезновении желаний,
                прекращении действий и прекращении действия причинности
                (пратитья-самутпада, санскр. <em>pratītya-samutpāda</em>). Я
                учу о прекращении страданий, порождаемых различениями в этом тройственном мире.</p>
            <p>Некоторые учёные-брахманы, допускающие, что нечто может возникнуть из ничего,
                утверждают, будто существует некая субстанция, связанная причинностью и пребывающая
                во времени. Они говорят, что элементы этой субстанции образуют личность и её
                окружение, имеют своё происхождение и развитие в рамках причинности, а затем,
                просуществовав подобным образом, исчезают.</p>
            <p>Существуют также учения, придерживающиеся разрушительных нигилистических взглядов
                относительно таких понятий, как непрерывность, деятельность, разрушение,
                существование, Нирвана, Благородный путь, карма, созревание плодов кармы и истина.
                Почему? Потому что они ещё не достигли интуитивного постижения самой истины и не
                обладают ясным видением истинной природы ума. Они подобны разбитому кувшину,
                который более не способен выполнять функции сосуда. Они подобны сожжённому
                семени, которое больше не прорастёт.</p>
            <p>Однако элементы, образующие личность и её окружение, которые они считают
                подверженными изменениям, на самом деле не способны к беспрерывным превращениям. Их
                взгляды основаны на ложных различениях в мире проявлений, а не на подлинном
                понимании.</p>
            <p>Далее, если принять за истину, что из ничего может возникнуть нечто, и что в
                умственном потоке, благодаря сочетанию трёх условий (объекта, органа чувств и
                сознания), может появиться
                некое состояние, тогда можно было бы утверждать то же самое о всякой несуществующей
                вещи. Можно было бы сказать, что у черепахи растут волосы или из песка течёт
                масло. Однако такие утверждения вряд ли будут приняты кем-либо. По сути, они
                означают утверждение абсолютного небытия.</p>
            <p>Отсюда следует, что причина, действие и результат, о которых они говорят, совершенно
                лишены действенности, а потому и их различение бытия и небытия также
                лишено основания.</p>
            <p>Если же они допускают возможность сочетания разнородных производящих причин, то им
                следует признать и первичность закона причины и следствия — то есть то, что нечто
                возникает из чего-то, а не из ничего. Поскольку же утверждается существование мира
                как относительного, в нём должна действовать непрерывно возобновляющаяся цепь
                причинности, которую ни при каких обстоятельствах нельзя отрицать.</p>
            <p>Следовательно, мы не можем говорить, будто что-либо возникает из ничего, и не можем
                утверждать, что эта последовательность когда-либо завершится.</p>
            <p>Однако до тех пор, пока эти учёные остаются на своих философских позициях, все их
                доводы — хотя они и согласуются с правилами логики и их авторитетными писаниями —
                являются лишь привычными проявлениями памяти, порождёнными ложным распознаванием
                несуществующих различий, к которым они вечно цепляются.</p>
            <p>Более того — и что хуже всего — простые, здравомыслящие люди могут быть отравлены
                этими ложными взглядами и начать провозглашать этот ложный способ мышления,
                проповедуемый несведущими, подлинным путём, ведущим к всезнанию.</p>
            <p>Между тем наставление Татхагаты не основано на утверждениях или опровержениях,
                выражаемых лишь словами и соответствующих формальной логике.</p>
            <p>Существуют четыре формы утверждений о том, что нечто якобы присутствует в вещах, ещё
                не перешедших в существование. А именно:</p>
            <ul class="list-disc pl-2 space-y-1 ml-2 mb-4">
                <li>Утверждение относительно единичных признаков, которые на самом деле не
                    существуют;
                </li>
                <li>Утверждение относительно объектов, которые не существуют;</li>
                <li>Утверждение о несуществовании причин;</li>
                <li>Утверждение, основанное на ложных философских воззрениях.</li>
            </ul>
            <p>Под «опровержением» здесь не следует понимать ошибку, возникающую вследствие
                неведения и некорректного исследования и основанную на подобных допущениях.</p>
            <p>Утверждение об единичных признаках, которые на самом деле не существуют, относится к
                различительным признакам, воспринимаемым глазами, ушами, носом и прочими чувствами
                как указывающие на единичность или общность элементов, составляющих личность и
                внешний мир. Принимая эти знаки за реальные и цепляясь за них, человек может
                подпасть под власть привычки утверждать: «Эти вещи именно таковы и не могут быть
                иными».</p>
            <p>Утверждение относительно объектов, которые не существуют, возникает из привязанности
                к ассоциациям знаков единичности и общности. Сам по себе объект не обладает ни
                существованием, ни несуществованием. Он вне альтернатив «бытия» и «небытия». О нём
                следует думать, как о рогах зайца, лошади или верблюда, которые никогда не
                существовали.</p>
            <p>Несведущие различают объекты, поскольку их разум недостаточно остёр для проникновения
                в истину. То, что они видят, — лишь проявление их собственного ума.</p>
            <p>Утверждение о несуществующей причине предполагает возможность беспричинного
                возникновения первого элемента в умственном потоке, который затем становится
                подобным миражу или сну внутри сна. Некоторые философы утверждают, будто изначально
                порождённая деятельность ума начинает функционировать при наличии глаз, внешних
                форм, света и памяти, и это функционирование продолжается некоторое время, а затем
                прекращается. Это — пример утверждения о возникновении без причины.</p>
            <p>Утверждение, основанное на философских взглядах относительно элементов, составляющих
                личность и её окружение, но на самом деле отсутствующих, предполагает существование
                субъекта бытия — души или живого существа как колыбели духа. Это — пример
                ложных философских взглядов.</p>
            <p>Именно здесь сочетание этих предположений с различением воображаемых признаков
                индивидуальности, их группировка, наделение именем и привязанность к ним как к
                объектам — всё это, поддерживаемое инерцией привычки (васана, санскр.
                <em>vāsanā</em>), накопленной с безначальных времён, приводит к тому, что один
                ложный взгляд наслаивается на другой. А основа их всех — лишь ложное воображение
                (парикальпита, санскр. <em>parikalpita</em>).</p>
            <p>По этой причине бодхисаттвам следует избегать всяких дискуссий, касающихся
                утверждений о «тайнах», основанных исключительно на словах и формальной логике.</p>
            <p>Словесное различение возникает в результате согласованной деятельности мозга,
                грудной клетки, носа, горла, нёба, губ, языка, зубов и гортани. Слова ни отличны, ни
                не отличны от различающего суждения. Они возникают, поскольку их причины —
                различающие суждения.</p>
            <p>Если бы слова были полностью отделены от различающего сознания, они не
                могли бы возникать из него. Если же они были бы с ним совершенно тождественны, они
                не смогли бы указывать на какой-либо смысл, ибо не отличались бы от самого акта
                различения. Следовательно, слова возникают в зависимости от причин и условий,
                взаимно обусловлены и подвержены изменению. Они подвержены рождению и разрушению,
                поскольку их значение не постоянно, а меняется в зависимости от ума, порождающего и
                воспринимающего их.</p>
            <p>Существуют четыре вида словесных различений. Их необходимо понимать как ложные
                и нереальные, и потому следует не попадать под их влияние.</p>
            <ul class="list-disc pl-2 space-y-1 ml-2 mb-4">
                <li>Первый вид — слова-указатели единичных признаков, возникающие из различения форм
                    и знаков, принимаемых за реальные сами по себе, вследствие чего к ним
                    привязываются.
                </li>
                <li>Второй вид — слова, служащие памяти: они возникают из нереального окружения,
                    всплывающего в уме при воспоминании прежнего опыта.
                </li>
                <li>Третий вид — слова, возникающие из привязанности к сложным различениям и
                    умственным построениям.
                </li>
                <li>Четвёртый вид — слова, вырастающие из унаследованных предрассудков, подобно
                    семенам энергий привычки, накопленных с безначальных времён, или имеющие
                    происхождение в некоем давно забытом истоке ложного представления и ложных
                    умствований, источник которого ушёл за пределы памяти.
                </li>
            </ul>
            <p>Существуют также слова, которым не соответствуют никакие объекты — например, «рога
                зайца», «сын бесплодной женщины» и тому подобное. Таких вещей в действительности не
                существует, хотя мы
                и произносим эти слова. Следовательно, слова — условные обозначения.</p>
            <p>В некоторых землях Будды вообще нет слов. Там идеи понимаются и указываются лишь
                пристальным взглядом; в других — жестом; в иных — гримасой лица, движением глаз,
                смехом, зевотой, прочисткой горла или даже дрожью голоса.</p>
            <p>Например, в земле Будды — Татхагаты Самантабхадры — бодхисаттвы, благодаря джняне
                (прямому познанию, санскр. <em>jñāna</em>), проникают за пределы слов и идей и
                понимают, что все вещи не рождены. Испытывая разнообразные возвышенные состояния
                самадхи, они превосходят необходимость в словах.</p>
            <p>Даже в этом мире существуют высокоорганизованные существа — подобные муравьям и
                пчёлам, — чья деятельность протекает успешно и слаженно без помощи слов как средства
                общения.</p>
            <p>Нет, Махамати, подлинность вещей не зависит от истинности значений слов.<br>
                Кроме того, существуют аспекты, относящиеся к словам как таковым:</p>
            <ul class="list-disc pl-2 space-y-1 ml-2 mb-4">
                <li>звуковая форма (варна, санскр. <em>varṇa</em>),</li>
                <li>уровень имени (наман, санскр. <em>nāman</em>),</li>
                <li>уровень высказывания (пада, санскр. <em>pada</em>).</li>
            </ul>
            <p>Звуковая форма — это то, благодаря чему слова и предложения могут быть составлены и
                рекомбинированы. Некоторые слоги служат мнемоническим целям, другие выбираются
                произвольно.</p>
            <p>Уровень имени — это условное обозначение объекта — то, что мы называем «именем».
                Именно имя придаёт слову видимость субстанциальности, хотя на самом деле оно — лишь
                соглашение.</p>
            <p>Уровень высказывания — это завершённый в смысловом отношении фрагмент, выражаемый
                предложением или фразой, имеющей определённое значение. Такие высказывания подобны
                отпечаткам на дороге, по которой прошли слоны, лошади, олени, скот, козы и прочие
                животные: они оставляют след, но сами по себе не есть ни животные, ни дорога.<br>
                Однако ни слова, ни предложения не способны точно выразить значение. Ибо слова — это
                лишь сладкие, гармоничные звуки, произвольно избранные для обозначения/представления
                вещей. Они не могут быть самими вещами, а вещи, в свою очередь, сами суть лишь
                проявления ума.</p>
            <p>Различение значения, основанное на ложном воображении, — это представление о том, что
                эти гармоничные звуки, называемые «словами» или «предложениями», и зависящие от
                того, какие субъекты предполагаются стоящими за ними, отражают некие реалии,
                обладающие неизменной сущностью. Это — ошибочное понимание.</p>
            <p>Ученикам следует остерегаться соблазнов слов и высказываний, их ложных, иллюзорных
                значений. Именно так несведущие и тупые умы впутываются в заблуждения, оказываясь в
                беспомощном положении, подобном слону, увязшему в глубокой трясине.</p>
            <p>Слова и предложения также возникают по закону причин и условий и взаимно обусловлены.
                Они не могут выразить высшую истину (парамартха-сатья, санскр.
                <em>paramārtha-satya</em>).
                Более того, в высшей истине нет различий, которые следовало бы распознавать,
                и нет ничего, что требовало бы выражения словами.</p>
            <p>Высшая истина — это состояние возвышенного блаженства и безмятежности. Оно не
                поддаётся словесным различениям, и из него нельзя извлечь смысл посредством
                утверждения или описания.</p>
            <p>У Татхагат есть способ постижения смысла, превосходящий слова — а именно через
                прямое познание Благородной Мудрости (арья-праджня, санскр. <em>ārya-prajñā</em>).
            </p>
        </blockquote>

        <p>Затем Махамати спросил Благословенного:</p>
        <p>— Молю Тебя, скажи нам о причинности всех явлений, чтобы я и все прочие бодхисаттвы могли
            увидеть её природу и более не были вынуждены различать последовательные и мгновенные
            факторы возникновения.</p>

        <p>Благословенный ответил ему:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Существуют два рода факторов, благодаря которым явления приобретают видимое
                существование: внешние и внутренние.</p>
            <p>Внешние факторы — это комок глины, гончарное колесо, приводной шнур, вода, гончар,
                его труд и сочетание всего этого, благодаря чему возникает кувшин. Кувшин образуется
                из глины, ткань — из нитей, циновка — из волокон благоухающей травы. Росток
                возникает из семени, свежее масло — из кислого молока, которое питает человека. Так
                всё в мире появляется одно за другим в непрерывной последовательности.</p>
            <p>Внутренние факторы причинности — это неведение, желание, намерение и прочие подобные
                состояния. Все они входят в понятие причинности.</p>
            <p>Проявления, возникающие из этих двух родов факторов, — это личность и индивидуальные
                вещи, составляющие её окружение. Однако сами по себе они не являются отдельными,
                отличными сущностями. Их различают лишь в силу неведения.</p>
            <p>Причинность можно разделить на шесть элементов:</p>
            <ul class="list-disc pl-2 space-y-1 ml-2 mb-4">
                <li>Причина как отсутствие различий (авишеша, санскр. <em>aviśeṣa</em>) означает,
                    что если нет различий, нет и способности к сочетанию; без сочетания —
                    нет и проявления; если же оно возникает, то подвержено разрушению.
                </li>
                <li>Причина как зависимость — предполагает наличие материальных элементов (дхату,
                    санскр. <em>dhātu</em>).
                </li>
                <li>Причина как возможность — требует благоприятного сочетания внешних и внутренних
                    условий.
                </li>
                <li>Причина как действие — предполагает наличие первичного принципа, подобного царю,
                    которому подчиняются все остальные.
                </li>
                <li>Причина как цель — подразумевает участие некоторой части мира проявлений в
                    системе ума и существование, в котором продолжается непрерывная деятельность.
                </li>
                <li>Причина как проявленность — означает, что благодаря способности различения в
                    умственном потоке происходит выявление индивидуальных признаков и форм бытия —
                    подобно тому, как при свете лампады зрение обнаруживает предметы.
                </li>
            </ul>
            <p>Все эти причины возникают благодаря различениям, совершаемым несведущими и простыми
                умами. Следовательно, они не указывают на реальность таких понятий, как постепенное,
                мгновенное или одновременное возникновение.</p>
            <p>Если бы постепенное возникновение существования было возможно, это доказывало бы его
                абсурдность: ведь нет никакой основополагающей субстанции, которой принадлежали бы
                отдельные признаки — а значит, постепенное возникновение невозможно.</p>
            <p>Если же утверждать единовременное возникновение всего, тогда нельзя провести различие
                между причиной и следствием — и тогда не будет того, что можно было бы назвать
                причиной. Пока ребёнок не рождён, отцовство лишено реального значения.</p>
            <p>Логики утверждают, что существует нечто рождающееся и нечто дающее рождение, и
                благодаря их взаимодействию возникают причинные факторы: причина, субстанция,
                непрерывность, изменение и т.д. На этом основании они заключают, что возможно
                постепенное возникновение.</p>
            <p>Однако это постепенное возникновение лишено реальности. Вся эта концепция основана на
                привязанности к идее «природы субъекта». Когда идея «моего „я“», тела, его места
                становится видимой, люди начинают противопоставлять объекты субъектам. Результатом
                этого становится не что иное, как «смотрение самим умом». Внешний мир начинают
                рассматривать в аспектах вечности и всеобщности — но эти аспекты нереальны. Поэтому
                не может быть ни постепенного, ни единовременного возникновения явлений.</p>
            <p>Лишь когда начинает действовать истинный разум и производит различение проявлений
                ума, можно условно говорить о «возникновении существования».</p>
            <p>Поэтому, Махамати, следует воздерживаться от понятий постепенности и
                единовременности, а также от представлений о сочетаниях причинной активности.</p>
        </blockquote>

        <p>Затем Махамати спросил:</p>
        <p>— Благословенный, к каким видам различений и представлений следует применять термин
            «ложное воображение»?</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Поскольку люди не понимают подлинной природы мира проявлений, они придерживаются
                двойственного взгляда. Они воображают множественность внешних объектов, считают её
                реальной и привязываются к ней — и это становится у них привычкой.</p>
            <p>Благодаря деятельности ума — то есть читты и всего, что к ней принадлежит, — они
                производят различения и считают их реальными. Это ведёт к утверждению существования
                субъекта (атмана, санскр. <em>ātman</em>) и «принадлежащего ему».</p>
            <p>Так ум продолжает функционировать, питаемый привязанностями и привычкой к
                двойственным взглядам.</p>
            <p>Люди придерживаются различных философских воззрений, основанных на этих ложных
                различениях — бытия и небытия, существования и несуществования. Отсюда и возникает
                то, что мы называем «ложным воображением».</p>
            <p>Однако, Махамати, это различение не развивается и устраняется, если понять, что всё
                видимое — не более чем проявление ума. Ведь при таком понимании исчезает само
                различение между сущим и несущим.</p>
            <p>Лишь ради несведущих, привыкших видеть множественность вещей, существующих на самом
                деле лишь в их уме, Я вынужден говорить, будто различение имеет место. Оно возникает
                из-за привязанности к аспектам множественности, характеризующим объекты.</p>
            <p>Как иначе эти простые умы поймут, что в этом мире нет ничего, кроме проявлений ума?
                Как иначе они постигнут подлинную природу ума? Как иначе освободятся от
                ложных представлений о причинах и следствиях?</p>
            <p>Как иначе они придут к верному пониманию и достижению состояния бодхисаттвы,
                достигнут точки поворота в глубочайшей седловине сознания и, в конце концов, обретут
                прямое постижение Благородной Мудрости — превосходящую пять дхарм (санскр.
                <em>pañca-dharma</em>), три собственные природы (свабхава, санскр. <em>svabhāva</em>)
                и все идеи различаемой реальности?</p>
            <p>Поэтому Я говорю: существует некое различение, возникающее в уме и привязывающееся к
                множественности явлений, которые сами по себе нереальны. И освобождение наступает
                лишь через полное понимание подлинной реальности.</p>
            <p>Ложное воображение (парикальпита, санскр. <em>parikalpita</em>) состоит в постепенном
                привязывании к проявлениям (самврити, санскр. <em>saṃvṛti</em>). Проявления
                различаются как имеющие форму, знаки, контуры; как
                обладающие цветом, теплотой, влажностью, подвижностью или жёсткостью.</p>
            <p>Привязанность к объектам — это цепляние за внутренние и внешние проявления, как будто
                они реальны.</p>
            <p>Привязанность к именам — это распознавание этих явлений по признакам их единичности и
                общности и убеждение, что определённые имена навечно закреплены за фиксированными
                объектами.</p>
            <p>Учение о ложном воображении указывает: поскольку все явления обусловлены причинами и
                энергией привычки, накопленной с безначальных времён, ум не может не воспринимать
                внешний мир.</p>
            <p>И мы не можем видеть всё иначе, как в аспектах единичности и общности. Именно из-за
                привязанности к этим ложным идеям у нас возникает представление о
                множественности проявляющегося.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава III. Об истинном
            познании и таковости</h3>

        <p>Затем Махамати попросил Благословенного:</p>
        <p>— Расскажи нам о пяти дхармах так, чтобы мы могли полностью понять совершенную
            мудрость.</p>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Пять дхарм (санскр. <em>pañca-dharma</em>) — это: признак/образ (намитта, санскр.
                <em>nimitta</em>), имя (наман, санскр. <em>nāman</em>), различение
                (викальпа, санскр. <em>vikalpa</em>), совершенное знание
                (самьяк-джняна, санскр. <em>samyag-jñāna</em>) и таковость
                (татхата, санскр. <em>tathatā</em>).</p>
            <p>Под признаком/образом понимается то, что обнаруживается чувствами и различающим
                умом и воспринимается как форма, звук, вкус, запах, касание. Из таких
                признаков/образов формируются идеи, как, например, из глины, воды и прочего
                создают кувшины.</p>
            <p>Говорят: «Это такая-то вещь и ничто иное» — и дают ей имя. Признаки/образы
                различаются посредством имён: «это слон, а это лошадь», «это повозка», «это
                пешеход», «это человек», «это мужчина», «это женщина» — или же: «это ум и всё,
                что к нему относится». Такие вещи, названные подобным образом, являются
                различаемыми. Все эти различения возникают как взаимозависимые, но пустые в
                отношении собственной сущности, то есть нерождённые. Таким образом, они
                проявляются такими, каковы они на самом деле, — как проявление самого ума. Это и
                есть правильное знание.</p>
            <p>Благодаря этому знанию мудрые перестают рассматривать признаки/образы и связанные
                с ними имена как реальные. Когда признаки/образы и имена отбрасываются, и все
                различения прекращают действовать, то, что остаётся, — это подлинная сущностная
                природа вещей. Поскольку в отношении этой природы больше ничего не создаётся, она
                называется «таковостью» или «реальностью».</p>
            <p>Эта универсальная, неразличимая, неразрушимая таковость — реальность, которую
                по-разному именуют как «истинную сущность ума» (читта-матра, санскр.
                <em>citta-mātra</em>), «запредельное постижение» (праджня-парамита, "совершенство
                мудрости", санскр. <em>prajñā-pāramitā</em>), «благородную мудрость»
                (арья-праджня, санскр. <em>ārya-prajñā</em>)
                и тому подобное. Она лишена всякой образности (анимитта, санскр. <em>animitta</em>),
                ибо является сущностной природой высшей истины (парамартха-сатья, санскр.
                <em>paramārtha-satya</em>). Эту дхарму всегда проповедуют все Будды, и в ней
                всё понимается в полном согласии теми, кто обладает совершенным знанием и находится
                на пути к достижению превосходной мудрости Татхагаты.</p>
        </blockquote>

        <p>Затем Махамати спросил у Благословенного:</p>
        <p>— Будут ли три собственные природы (трисвабхава, санскр. <em>trisvabhāva</em>) всех
            явлений — то есть представления о реальности — рассматриваться нами как включённые в
            пять дхарм, или же они имеют собственные характеристики, независимые?</p>

        <p>Будда ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Эти три собственные природы, восемь сознаний (ашта-виджняна, санскр.
                <em>aṣṭa-vijñāna</em>) и двойное отсутствие «я» (два-анатман, санскр.
                <em>dvayānātman</em>) — всё это включается в пять дхарм.</p>
            <p>Имя и признак/образ соответствуют воображаемой природе (парикальпита-свабхава, санскр. <em>parikalpita-svabhāva</em>).
                Ум и всё, что с ним соотносится — зависимые от проекции для своего существования и
                возникающие вместе с именем — подобно солнцу и его лучам — и поддерживаемые
                различением их разнообразных признаков, составляют зависимую природу
                (паратантра-свабхава, санскр. <em>paratantra-svabhāva</em>).
                Правильное знание (самьяк-джняна, санскр. <em>samyag-jñāna</em>) и таковость, будучи неразрушимыми, составляют совершенную природу
                (паринишпанна-свабхава, санскр. <em>pariniṣpanna-svabhāva</em>).</p>
            <p>Благодаря привязанности к тому, что видится самим умом, возникает активность, которая
                продолжается благодаря энергии привычки (васана, санскр. <em>vāsanā</em>) и
                проявляется в деятельности ума. Из-за этой активности ум порождает представления о
                субъекте (атмане, санскр. <em>ātman</em>) и соотносящейся ему объективности.
                Возникают различения, привязанности, понятия о «я» — подобно солнцу и его лучам.</p>
            <p>Под отсутствием «я» в дхармах подразумевается, что сами дхармы — будь то элементы,
                образующие агрегаты личности (скандхи, санскр. <em>skandha</em>), или компоненты её
                мира проявлений, — иллюзорны и не содержат ничего, что можно было бы
                считать субстанцией «я». Следовательно, они не рождены и не имеют собственной
                природы. Как можно утверждать, что явления обладают собственной сущностью
                «я»?</p>
            <p>Под отсутствием «я» в личности понимается то, что в пяти скандхах, составляющих лишь
                условное обозначение «личность», нет ничего, что можно было бы принять за субстанцию
                «эго» — ни саму по себе, ни принадлежащую ей.</p>
            <p>Деятельность ума, являющаяся наиболее характерным признаком личности, формируется из
                неведения (авидья, санскр. <em>avidyā</em>), различения, желания и действия. Её
                деятельность продолжается благодаря схватыванию, восприятию и привязанности к
                объектам, воспринимаемым как реальные. Память об этих различениях, желаниях,
                привязанностях и действиях накапливается в Алайя-виджняне (санскр.
                <em>ālaya-vijñāna</em>) с безначальных времён и сохраняется в условиях, способствующих проявлению личности и её окружения, постоянно изменяя представления от
                момента к моменту.</p>
            <p>Их проявления можно уподобить реке, семени, лампе, облаку, ветру — однако
                Алайя-виджняна, благодаря своей способности накапливать впечатления, удерживает всё
                это воедино. Алайя подобна обезьяне, не знающей ни мгновения покоя, мечущейся в
                поисках пищи и хватающей всё без разбора. Она подобна огню, который никогда не
                насыщается. Она подобна мельничному колесу, вечно вращающемуся под напором воды.
                Алайя-виджняна, осквернённая привычкой, подобна волшебнику, вызывающему фантомы
                вещей и демонстрирующему их людям как нечто движущееся и существующее само по
                себе.</p>
            <p>Верное понимание вещей необходимо для постижения бессубъективности личности.</p>
            <p>Можно различать четыре рода знания: условное знание (самврити, санскр.
                <em>saṃvṛti</em>), относительное знание, совершенное знание (арья-джняна, санскр.
                <em>ārya-jñāna</em>) и трансцендентальное понимание (праджня-парамита).</p>
            <p>Условное знание принадлежит несведущим и простым людям, привязанным к понятиям
                бытия и небытия, испытывающим страх перед мыслью о нерождённом. Оно формируется в
                соответствии с тройным сочетанием (объект, орган чувств, сознание),
                связывает множественность и объект, характеризуется привязанностью и накоплением [кармы],
                подвержено рождению и разрушению. Это знание принадлежит обитателям этого мира,
                занятым рассуждениями, различениями, утверждениями и отрицаниями.</p>
            <p>Относительное знание принадлежит философам этого мира. Оно возникает из способности
                ума видеть отношения между явлениями. Замечая такие связи, ум возбуждает
                способность упорядочивать, сочетать и анализировать их посредством
                рассудочной логики и воображения. Поэтому различающему уму кажется, будто он
                проникает во внутренние связи и структуру вещей.</p>
            <p>Совершенное знание принадлежит бодхисаттвам, которые понимают все вещи исключительно
                как проявления ума. Бодхисаттвы ясно постигают пустотность (шуньята, санскр.
                <em>śūnyatā</em>), нерождённость и бессубъективность всех явлений.</p>
            <p>Тот, кто постиг пять дхарм и двойное отсутствие «я», близок к пониманию истины в её
                безóбразности. Совершенное знание различается в зависимости от стадии бодхисаттвы,
                но представляет собой, как бы то ни было, прямую дорогу, ведущую к возвышенному
                состоянию прямого постижения благородной мудрости.</p>
            <p>Совершенное знание (арья-джняна) принадлежит тем бодхисаттвам, кто полностью свободен
                от двойственности бытия и небытия, нерождённости и неисчезновения, свободен от
                утверждений и отрицаний и благодаря прямому постижению обрёл подлинное понимание
                истинной бессубъективности и безóбразности. Они более не производят различений в
                мире и не подвержены причинным воздействиям; для них закон причинности подобен
                воображаемому городу гандхарвов. Их мир — видение или сон, подобен смерти сына
                бесплодной женщины. Для них нет ни возникновения, ни развития, ни исчезновения.</p>
            <p>Мудрых, взращивающих в себе совершенное знание, можно разделить на три класса:
                слушающих (шраваки, санскр. <em>śrāvaka</em>), пратьекабудд (пратйекабудды, санскр. <em>pratyekabuddha</em>)
                и архатов. Шраваки отличаются от пратьекабудд. Шраваки продолжают различать
                единичное и общее, тогда как пратьекабудды, выйдя из их среды, осознают ошибочность этого
                разделения. Однако они ещё придерживаются идеи субъективности, поскольку как
                субъекты вынуждены уходить в уединение и одиночество.</p>
            <p>Архаты достигают своего состояния, осознав ошибочность всех различений. В этот момент
                все заблуждения распознаются, и мудрые приходят к подлинному знанию истины — к точке поворота в самой глубине их сознания. Тогда ум освобождается и переходит к
                совершенному прямому постижению благородной мудрости.</p>
            <p>Однако, Махамати, если ты утверждаешь и настаиваешь, будто существует такая вещь, как
                «благородная мудрость», — это уже неверно. Всякое настаивание на подобном
                утверждении связано с природой бытия и несёт в себе признак порождённости. Само
                утверждение: «Все вещи не рождены» — противоречит самому себе. То же верно и для высказываний: «Все вещи пусты», «Все вещи лишены субъективной
                природы». Все они непригодны как формы рассуждения.</p>
            <p>Но когда указывается, что все вещи подобны сну или видению, это означает: в одном аспекте вещи постигаются, в другом — нет. То есть в неведении они постигаются, а в
                совершенном знании — не постигаются.</p>
            <p>Все утверждения и отрицания — лишь конструкции ума, и сами они не рождены. Даже
                утверждения об Алайя-виджняне и благородной мудрости как высшей реальности —
                умственные конструкции, а потому непорождены.</p>
            <p>Все так называемые «вещи» не могут составлять сущностное содержание Алайя-виджняны,
                ибо нет ни Алайя-виджняны, ни благородной мудрости, ни высшей реальности как
                объектов для понимания. Мудрые, переходя из этого мира в сферу безóбразности (анимитта,
                санскр. <em>animitta</em>), пребывают в одинаковой чистоте и сами являются чистым
                пониманием. Вот что означает для мудрых: «все вещи» — они исчезают и
                перестают существовать даже в состоянии безóбразности.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава IV. Система ума</h3>

        <p>Затем Махамати попросил Благословенного:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Скажи, что следует понимать под умом (читта, санскр. <em>citta</em>).</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Все вещи в этом мире — будь они внешне хорошими или плохими, исполненными недостатков
                или безупречными, вызывающими действие или пассивными, производящими реакцию или не
                производящими, воспринимаемыми или невоспринимаемыми — могут быть разделены на два
                класса: дающие злостные истечения (ашрава, санскр. <em>āsrava</em>) и дающие
                неистечения добра (анашрава, санскр. <em>anāsrava</em>).</p>
            <p>Злостные истечения — это пять охватывающих агрегатов (панча-скандха, санскр. <em>pañca-skandha</em>),
                формирующих личность: форма (рупа, санскр. <em>rūpa</em>), ощущение (ведана, санскр.
                <em>vedanā</em>), восприятие (саньджа, санскр. <em>saṃjñā</em>), различение
                (санскара, санскр. <em>saṃskāra</em>) и сознание (виджняна, санскр. <em>vijñāna</em>).
                Они воображаются как хорошие или плохие и порождают энергию привычки (васана,
                санскр. <em>vāsanā</em>) в потоке ума.</p>
            <p>Духовные достижения и радость самадхи (санскр. <em>samādhi</em>), приносящие плод
                самапатти (санскр. <em>samāpatti</em>), возникают у мудрых благодаря прямому постижению
                благородной мудрости и накапливаются, участвуя в явлениях этого троичного мира. Они
                называются неистечениями добра.</p>
            <p>Деятельность ума, являющаяся источником злостных истечений, состоит из пяти органов
                чувств и соответствующих им чувственных виджнян (джняна-индрия, санскр. <em>jñānendriya</em>).
                Все они объединяются в различающем уме (мановиджняна, санскр. <em>mano-vijñāna</em>).
            </p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Бесконечная последовательность чувственных представлений проистекает в актах мышления
                мановиджняны, которая вновь сочетает их, различает и судит о них как о хороших или
                плохих. Затем возникают отвращение к ним или желание, привязанность или действие
                ради них. Весь поток ума непрерывно движется и тесно взаимосвязан в своих
                элементах.</p>
            <p>Однако он не позволяет видеть и понимать, что всё видимое, воспринимаемое и
                схватываемое — лишь проявление его собственной активности и не имеет иного
                основания. Так ум ложно продолжает схватывать и различать различия в формах и
                качествах и ни на мгновение не обретает покоя.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>В потоке ума имеются три модуса активности, которые можно аналитически различить.</p>
            <p>Чувственные виджняны функционируют, сохраняя свою исходную природу. Они порождают
                определённые эффекты и способны к эволюции.</p>
            <p>Первый модус — нормальное функционирование чувственных виджнян, схватывающих элементы
                внешнего мира, благодаря чему ощущение и восприятие возникают в разной степени в
                каждом органе чувств и соответствующей виджняне. Даже в порах кожи и атомах тела
                присутствует способность охватывать всю сферу деятельности — подобно зеркалу,
                отражающему объекты. Однако в этом нет возможности реализовать внешний мир через
                понимание того, что он — лишь проявление самого ума.</p>
            <p>Второй модус — проективное действие ощущений на мановиджняну, порождающее восприятия
                охватывания, привлечения, отвращения, действия и привычек.</p>
            <p>Третий модус — рост, развитие и изменение в потоке ума под действием энергии
                привычки, накопленной с безначальных времён. Например, в глазу присутствует энергия
                привычки «глазения», предрасполагающая к схватыванию и привязыванию к многообразию
                форм и видимостей.</p>
            <p>Так деятельность развивающегося потока ума на основе энергии привычки напоминает
                волны, возникающие на поверхности Алайя-виджняны, которые, в свою очередь,
                обусловливают активность и различия в потоке ума.</p>
            <p>Проявления, восприятие, привлечение, охватывание, действие, привычка, реакция,
                условие
                — всё это непрерывно проявляется и функционирует в чувственных виджнянах, в
                мановиджняне и в Алайя-виджняне, которые таким образом оказываются
                взаимосвязанными.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Так, благодаря различениям, обладающим природой майи и не являющимся реальными —
                будучи ложно воображаемыми и продуктами ложного рассуждения, — всякая ложность
                постоянно возобновляет саму себя. В результате возникают действия, накапливается
                энергия привычки, и чистая поверхность Алайя-виджняны оказывается загрязнённой.</p>
            <p>Поток ума продолжает функционировать ускоренно и порождает эфирное тело — такова
                причина его возникновения.</p>
            <p>Однако мановиджняна не осознаёт, что именно её собственные различения и привязанности
                обусловливают существование тела. Так чувственные виджняны и мановиджняна становятся
                всё более тесно взаимосвязанными и взаимообусловленными, формируя мир представлений,
                рождённый собственным воображением.</p>
            <p>Подобно зеркалу, отражающему образ, воспринимающие чувства схватывают видимости,
                которые мановиджняна собирает, вновь различает, именует и к которым привязывается.
                Между этими двумя функциями нет разрыва, ибо они взаимно обусловлены.</p>
            <p>Воспринимающие чувства схватывают то, что им свойственно, и в них происходит
                трансформация — структурное преобразование воспринимаемого с сохранением его
                инвариантности, поскольку ум продолжает сочетать, различать, давать оценки и
                действовать.</p>
            <p>Затем наступает очередь энергии привычки, укрепляющей ум в его непрерывной
                активности. Мановиджняна, благодаря способности различать, судить, выбирать и
                рассуждать, развивается вместе с мышлением — интеллектуальным умом.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Существуют три разряда умственной активности:</p>
            <p>— умственность, функционирующая в связи с привязанностью к объектам и конкретным
                идеям;</p>
            <p>— умственность, функционирующая в связи с общими идеями;</p>
            <p>— умственность, исследующая истинность общих идей.</p>
            <p>Первая подразделяется, ибо возникает из различения. Она отличает ум от его
                собственных процессов и принимает идеи как реальные, привязываясь к ним.
                Совокупность ложных суждений о бытии, множественности, единичности, ценностях и т.д.
                обладает сильнейшей схватывающей силой. Она растёт благодаря энергии привычки, и
                различения утверждают сами себя.</p>
            <p>Эти процессы порождают общие представления о теплоте, текучести, подвижности,
                твёрдости — характеристиках объектов различения.</p>
            <p>Упорное поддержание общих идей ведёт к предположениям, допущениям, определениям,
                иллюстрациям и доказательствам. Всё это уничтожает относительное знание и укрепляет
                веру в рождение, собственную природу и существование субъекта опыта.</p>
            <p>Умственность как исследующая функция — это интеллектуальный акт изучения общих
                заключений, оценки их истинности, значимости и достоверности. Эта способность ведёт
                к пониманию, правильному знанию и указывает путь к прямому постижению.</p>
        </blockquote>

        <p>Затем Махамати спросил Благословенного:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Скажи нам теперь, Благословенный, какое отношение личность — субъект — имеет к потоку
                ума?</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Для объяснения этого необходимо сначала рассмотреть собственную природу пяти
                охватывающих агрегатов, формирующих личность. Как Я уже показал, они пусты,
                нерождены и не обладают собственной природой. Эти пять агрегатов — форма, ощущение,
                восприятие, различение и сознание.</p>
            <p>К форме относятся так называемые «первичные элементы» (махабхута, санскр. <em>mahābhūta</em>).
                Остальные четыре агрегата бесформенны, и их даже не следует считать четырьмя, ибо
                они сливаются неразличимо, переходя друг в друга. Все они подобны пространству,
                которое невозможно исчислить. Лишь благодаря воображению их различают и уподобляют
                пространству.</p>
            <p>Поскольку вещи обладают видимостью бытия, признаков, местопребывания и действенности,
                о них говорят как о рождённых и как об эффектах причинных действий. Однако этого
                нельзя сказать о четырёх бесформенных агрегатах — они лишены формы и характерных
                признаков.</p>
            <p>Эти четыре умственных агрегата образуют личность вне пределов исчисления их состава и
                структуры. Они недоступны четырём рациональным пропозициям логики (тетралемме) и не
                могут быть предикативно определены как существующие или несуществующие.</p>
            <p>Вместе они образуют то, что называется «ясным умом» (виджняна-прасада, санскр. <em>vijñāna-prasāda</em>).
                Они ещё более подобны майе, чем все прочие вещи, ибо, будучи составом различающего
                смертного ума, препятствуют прямому постижению благородной мудрости.</p>
            <p>Лишь несведущие перечисляют их как структуру личности или субъекта; мудрые так не
                поступают. Это различение пяти агрегатов служит основанием для идеи субъекта или
                души, а также для возникновения её желаний и интересов.</p>
            <p>От него необходимо в конце концов отказаться и заменить истинным представлением о
                безóбразности и единстве, которое должно твёрдо укорениться.</p>
        </blockquote>

        <p>Затем Махамати обратился к Благословенному и сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Расскажи нам теперь об Алайя-виджняне и о её отношении к низшему потоку ума.</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Чувственные виджняны и централизующая их мановиджняна взаимосвязаны. Они относятся к
                внешнему миру, который является проявлением самого ума, и порождают восприятие,
                различение и схватывание подобных проявлений.</p>
            <p>Алайя-виджняна (санскр. <em>ālaya-vijñāna</em>) превосходит всякую единичность и
                любые определения.</p>
            <p>Она совершенно чиста в своей сущностной природе, неизменна, свободна от осквернений и
                переменчивости, не возмущена эгоизмом, различениями, желаниями или отвращениями.
                Алайя подобна огромному океану: на её поверхности возникают волны, проявляющие
                приливы и отливы, но глубины её всегда остаются неподвижными.</p>
            <p>Сама по себе она лишена личности и всего, что к ней принадлежит. Однако из-за
                осквернений на её поверхности она уподобляется актёру, играющему бесчисленные роли,
                включая те, где функционирует низший ум.</p>
            <p>Принцип понимания разделяется, и ум, функционируя как ум, порождает злостные
                истечения и индивидуализацию. Возникает семичленная структура ума: интуитивное
                самопонимание, мысль-умопостигаемое, желательно-различающее, а также состояния
                видения, слышания, вкуса, обоняния и осязания. Затем возникают их взаимодействия и
                реакции.</p>
            <p>Мановиджняна является причиной действия чувственных виджнян, поддерживает их и сама
                функционирует благодаря им, описывая их деятельность и привязываясь к миру объектов.
                По мере накопления энергии привычки она всё более оскверняет «лик»
                Алайя-виджняны.</p>
            <p>Так Алайя-виджняна становится хранилищем и разменным банком для всех накоплений
                умственности и действия с безначальных времён.</p>
            <p>Между Алайя-виджняной и индивидуальной мановиджняной находится интуитивный ум (манас,
                санскр. <em>manas</em>). Он зависит от Алайи и является причиной всего, что
                находится ниже, поддерживая связь с обоими. Он сопричастен универсальности и чистоте
                Алайи, подобен ей, выше формы и выше моментальности.</p>
            <p>Благодаря деятельности манаса возникают, проявляются и реализуются благие истечения.
                Счастье его в том, что он не моментален, ибо связан с просветлением, где
                моментальность невозможна и несовместима с мудростью.</p>
            <p>Однако манас вступает в связь с низшим потоком ума, разделяет её опыт и отражает её
                активность. Он еди́н с Алайей через сопричастность трансцендентальному пониманию и
                еди́н в системе ума, ибо схватывает дифференцированное (виджняна).</p>
            <p>Манас не имеет собственного тела или признаков, по которым его можно было бы
                различить. Алайя — его причина и опора, но он развивается параллельно понятию
                субъекта и того, что ему принадлежит, и оскверняется опознавательными
                признаками.</p>
            <p>Благодаря манасу и его способности — смешанной способности тождества и различения —
                непостижимое мудрствование Алайи открывается обыденному уму и становится постижимой
                истиной.</p>
            <p>Благодаря Алайе манас не может быть источником ошибки.</p>
            <p>Мановиджняна — сознание — подобна танцору-волшебнику, для которого мир проявлений —
                сцена. Манас же — мудрый щит, странствующий там же, где волшебник, и отражающий его
                пустотность и мимолётный успех.</p>
            <p>Алайя ведёт запись и знает всё, что должно быть и что может быть.</p>
            <p>Благодаря деятельности мановиджняны возникают ошибки, развивается мир проявлений и
                утверждается понятие субъекта и его самосознания. Если мановиджняна перестаёт
                действовать, весь поток ума прекращает функционировать, и Алайя остаётся в
                одиночестве.</p>
            <p>Отделавшись от мановиджняны, он устраняет все ошибки, ибо та — их причина.</p>
        </blockquote>

        <p>Затем Махамати сказал Благословенному:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Теперь, Благословенный, расскажи нам, что означает прекращение деятельности потока
                ума.</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Пять чувственных функций, различающая и мыслительная функции порождают и завершают
                каждый момент и следующий за ним. При их возникновении появляются различение, форма,
                видимость и объективность в тесной связи с внешними обстоятельствами.</p>
            <p>Воля к жизни — их мать, неведение — их сила и отец. Через установление имён форм
                умножаются жадность и желание, и ум оказывается взаимообусловленным, попадая в сеть
                условий.</p>
            <p>Из-за привязанности к именам и формам и непонимания того, что здесь нет основания для
                деятельности ума, возникают ошибки, порождаются продукты ложного воображения,
                желания, страдания, чувства приятного и неприятного — и путь к освобождению
                оказывается закрытым.</p>
            <p>Потоки чувственных виджнян и мановиджняны на самом деле не испытывают удовольствия,
                неудовольствия, страдания или наслаждения. Они лишь воображают, будто испытывают их.
                Эти чувства — ложные, обманчивые реакции смертного ума, схватывающего воображаемые
                объекты мира.</p>
            <p>Существуют два пути прекращения деятельности потока ума: через формы и через
                непрерывность.</p>
            <p>Чувственные органы функционируют в отношении форм благодаря взаимодействию формы,
                контакта и схватывания. Они перестают функционировать, когда контакт нарушается.</p>
            <p>Что до непрерывности: когда взаимодействия формы, контакта и схватывания
                прекращаются, больше не может быть непрерывного видения, слышания и прочих функций
                органов чувств. С прекращением их функционирования исчезает и различение; когда же
                отсутствуют схватывание, различение и привязывание, прекращается вся деятельность
                мановиджняны, прекращается действие, накопление энергии привычки и, следовательно,
                накопление оскверняющей кармы, загрязняющей «лик» Алайи.</p>
            <p>В развивающемся потоке смертного ума не может быть тождества с природой Алайи. Иначе
                прекращение деятельности низшего ума означало бы прекращение Алайи. Но они различны,
                и Алайя ни в коем случае не является причиной деятельности смертного ума.</p>
            <p>Прекращение деятельности Алайи в её чистой сущностной природе невозможно. То, что
                прекращает функционировать, — не есть Алайя в её сущности. Прекращается лишь
                возникновение эффектов и осквернений, накопленных на поверхности Алайи благодаря
                энергии привычки и различающему мышлению, и причиной этого является сам смертный
                ум.</p>
            <p>Деятельность божественного ума — его существование в самом себе — не может
                прекратиться, ибо он есть основа реальности, утроба истины.</p>
            <p>Под прекращением деятельности чувственных виджнян понимается не прекращение
                перцептивного функционирования, а прекращение различений и именований — способность,
                сосредоточенная в мановиджняне как центре управления.</p>
            <p>Под прекращением деятельности потока ума в целом понимается прекращение различений,
                расчистка пути к пониманию необходимости отвращения от привязанностей, удаление всех
                осквернений, прекращение накопления энергии привычки и дальнейшего загрязнения
                поверхности ума тем, что накапливалось на ней с безначальных времён через
                различения, привязанности, ложное поведение и дурные поступки.</p>
            <p>Прекращение деятельности мановиджняны происходит, когда исчезает то, что поддерживает
                поток ума, — сама мановиджняна, или рассудок. С прекращением её деятельности
                смертный ум в целом и весь мир майи, мир желаний, исчезают. Отделавшись от
                мановиджняны, мы обретаем Нирвану.</p>
            <p>Однако прекращение деятельности различающего сознания невозможно до тех пор, пока не
                совершится решительный поворот в глубочайшей седловине нашего сознания. Умственная
                привычка видеть внешний мир проявлений через мановиджняну должна быть
                отброшена.</p>
            <p>Должна возникнуть новая привычка — осознание истины в интуитивном уме (манасе),
                которая становится единой и утверждается в ней.</p>
            <p>До тех пор, пока не будет достигнуто интуитивное самопонимание благородной мудрости,
                эволюция потока ума будет продолжаться. Но когда настанет подлинное понимание пяти
                дхарм, трёх собственных природ и двойного отсутствия «я», откроется путь к повороту,
                и поворот будет близок.</p>
            <p>Когда прекратится переживание приятного и неприятного, наслаждения и боли, когда
                утихнут конфликтующие идеи, возбуждаемые интересами и эгоизмом, наступит состояние
                успокоенности, и вся истина просветления станет полностью понятной. Тогда больше не
                будет злостных истечений разума.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава V. О
            трансцендентальном понимании</h3>

        <p>Тогда Махамати сказал Благословенному:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Молю Тебя, объясни, что образует трансцендентальное понимание?</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Трансцендентальное понимание — это внутреннее состояние прямого постижения благородной
                мудрости. Оно достигается внезапно, интуитивно, в поворотном пункте, расположенном в
                глубочайшей седловине сознания.</p>
            <p>Оно не приходит ниоткуда и не уходит никуда. Оно подобно отражению луны в воде.
                Трансцендентальное понимание не подвержено ни рождению, ни разрушению. Оно не
                связано ни с сочетаниями, ни с соответствиями, ни с привязанностями, ни с
                накоплениями. Оно превосходит все дуалистические концепции.</p>
            <p>Когда рассматривается трансцендентальное понимание как таковое, следует иметь в виду
                его четыре аспекта: слово, значение, поучение и благородная мудрость
                (Арья-праджня).</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Слова используются для выражения значений, но не зависят от различающей деятельности
                сознания. Память является их причиной, и поэтому при использовании звуков или букв,
                обеспечивающих взаимную передачу значений, становится возможным их понимание.</p>
            <p>Слова — это всего лишь символы. Они могут выражать намеренно задуманные значения, но
                могут и не выражать их. Более того, слова могут быть поняты совсем не так, как
                замыслил говорящий.</p>
            <p>Слова не отличны от значений, и значения не отличны от слов. Если бы значение было
                отлично от слов, оно не могло бы быть выражено словами. Однако значение раскрывается
                благодаря словам, подобно тому как вещи становятся видимыми при свете лампы.</p>
            <p>Слова подобны человеку, который приносит лампу, чтобы лучше разглядеть своё
                имущество, и затем говорит: «Вот это — моё имущество».</p>
            <p>Точно так же при помощи слов и речи у нас возникают различения. Бодхисаттвы могут
                вникать в смысл учения Татхагаты через слова, а вникнув в смысл, приходят к
                экзальтированному состоянию прямого постижения благородной мудрости, которая сама по
                себе свободна от всяких словесных обозначений.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Однако человек, привязываясь к буквальному значению слов и цепляясь за иллюзию, будто
                слова и значения жёстко соответствуют друг другу — особенно в таких понятиях, как
                Нирвана, которая не возникает и не исчезает, или в различении средств, ведущих к
                Нирване, — не сумеет понять подлинного смысла учения. Он запутается в утверждениях и
                опровержениях.</p>
            <p>Подобно тому как разнообразные объекты, различаемые во снах и видениях, ошибочно
                принимаются за реальные, так же идеи и утверждения могут ошибочно различаться наяву
                — и эта ошибка ведёт к умножению ложности.</p>
            <p>Несведущие и простые умом заявляют, будто значение не отличается от слова, и что
                слова и значения тождественны. Они думают: поскольку значение лишено «тела», то есть
                формы, оно не может быть отлично от слова. Хотя значение — это не речь, а мысль, они
                объявляют мысль тождественной её словесному выражению. В этом проявляется полное
                непонимание истинной природы слова.</p>
            <p>Они не понимают, что слова могут рождаться и исчезать, тогда как их значения — нет.
                Слова зависят от букв, а значения — нет. Значения занимают особое положение: они не
                связаны с существованием и несуществованием, не имеют субстрата и не рождены.</p>
            <p>Татхагата не учит Дхарме, зависящей от букв. Всякий, кто учит, опираясь на буквы,
                будет лишь болтуном, ибо истина — за словами, за буквами и вне книг. Это не
                означает, что слова и книги не могут говорить о том, что согласуется с истиной.</p>
            <p>Сказанное означает, что слова и книги зависят от способа их применения различающим
                сознанием, тогда как значение и истина — независимы. Слова и книги могут по-разному
                истолковываться разными умами, но смысл и истина не подлежат различным
                толкованиям.</p>
            <p>Однако если истина не выражена в словах и не записана в книгах, Писание, содержащее
                её значение, исчезнет. А с исчезновением Писания не будет ни шраваков, ни пратьекабудд,
                ни бодхисаттв, ни Будд — ибо не будет того, чему учить, кого учить и кто будет
                учить.</p>
            <p>Принимая это во внимание, не следует привязываться к словам Писания. Даже в
                канонических текстах слова иногда отклоняются от прямого смысла, что может привести
                к неверному функционированию умов живых существ.</p>
            <p>Я и прочие Татхагаты ведём религиозные беседы с разными существами в соответствии с
                их потребностями, верой, убеждениями, характерами и типами. Наша цель — освободить
                их от зависимости и от мыслящей функции в системе ума. Однако никакие беседы не
                могут заменить самореализацию Благородной Мудрости.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Когда нет понимания, его нельзя породить извне, ибо мир есть лишь объективация нашего
                ума. Поэтому все уподобления должны быть отброшены, и должна быть постигнута
                безóбразность истины. Тогда мы увидим, что она находится вне связи со словом и
                буквой.</p>
            <p>Несведущие и простые умом вводятся в заблуждение своими субъективными представлениями
                и ложными рассуждениями. Они продолжают «плясать и скакать», не способные понять,
                что беседа в словах лишь указывает на природу истинного прямого постижения, которая уже
                находится вне слов.</p>
            <p>Тем не менее, цепляясь за внешний мир и его объективное значение, они способны
                понимать истину лишь внешне. Они предпочитают изучать книги, которые могут служить
                лишь средствами к достижению истины, но не понимают сущности того, как утвердиться в
                истине прямого постижения.</p>
            <p>Они не постигают, что истина не подвержена четырём логическим пропозициям.
                Самореализация — это возвышенное состояние внутреннего достижения, превосходящее
                дуалистическое мышление, логику, рассуждения, идеальные объекты и иллюстрации.</p>
            <p>Татхагаты беседуют с несведущими, чтобы пробудить в них стремление к состоянию
                бодхисаттвы, ищущего прямое постижение благородной мудрости. Поэтому каждый ученик
                должен стремиться избавиться от магии слов и стать совершенным в соответствии со
                смыслом, ибо истина — не в букве.</p>
            <p>Когда человек указывает пальцем на что-либо, его палец может быть ошибочно принят за
                то, на что он указывает. Подобно этому, несведущие и простые умом, подобно детям,
                даже в день своей смерти не могут отказаться от идеи, что слова — это сам
                указывающий палец, а не средство, указывающее на значение. Они не способны осознать
                конечную реальность, ибо их ум цепляется за слова, хотя эти слова — не более чем
                указательный палец.</p>
            <p>Слова и связанные с ними различения ввергают в ужасный круг рождений и смертей.
                Значения же более обособленны — и именно они ведут к Нирване.</p>
            <p>Значение и смысл достигаются обучением, и чем больших успехов достигает человек в
                учении, тем глубже он понимает то, что скрыто за словами. Поэтому ищущие истину
                должны постепенно и почтительно приближаться к тем, кто мудр и может избавить их от
                наваждения слов.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что же касается формы поучений, то существуют священнослужители и проповедники,
                придающие большое значение ритуалам и церемониям, искусные в напевах и
                красноречивые. Однако их не следует почитать и не к ним следует обращаться с
                благоговением, ибо то, что от них получают, — не более чем эмоциональное возбуждение
                и мирское наслаждение.</p>
            <p>Подобные проповедники, хитро манипулируя словами и фразами, прибегая к рассуждениям и
                напевам, напоминающим детскую болтовню, могут ещё больше отдалить слушателей от
                истины. Они не способны привести ум в согласие со смыслом, а лишь пробуждают чувства
                и эмоции, притупляя тем самым ясность ума.</p>
            <p>Тот, кто сам не понимает смысла вещей, может лишь запутать свой ум, выслушав подобные
                взгляды — особенно дуалистические. Без собственного понимания, без исследования
                умом, без освобождения от привязанности к идее субъективной природы внешних вещей
                невозможно постичь Путь — ни от кого другого, ни с чьей-либо помощью.</p>
            <p>Такие священнослужители и народные проповедники, искусные в риторике, но сами не
                свободные от бедствий рождения, старости, болезни, печали, отчаяния и страха, не
                способны вести несведущих к освобождению — сколь бы ни были разнообразны их слова,
                примеры и выводы.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Существуют также так называемые философы-материалисты. Им не следует оказывать
                уважения и не следует принимать их всерьёз, ибо их учение, сколь бы оно ни было
                украшено сотнями тысяч ловких фраз, не выходит за пределы этого мира. Их цель —
                вовсе не освобождение от страданий; напротив, они ведут к ещё большим
                страданиям.</p>
            <p>Материалисты не признают никакой истины, существующей по себе. Они разделяются на
                множество школ, каждая из которых цепляется за свой способ рассуждения и ожесточённо
                борется с другими.</p>
            <p>Однако есть то, чего не могут понять материалисты и что недоступно философам, не
                видящим первоосновы — отсутствия реальности во внешних объектах.</p>
            <p>Если не понимать, что помимо этого нет ничего, что за всем этим видится лишь один
                разум, что различение бытия и небытия должно быть оставлено, что внешний мир как
                объект восприятия есть порождение ума, — тогда остаётся лишь иллюзия
                множественности.</p>
            <p>Эта единая реальность вовсе не подвластна материалистическим философам; она
                принадлежит области Татхагатства.</p>
            <p>Все вещи, которые воображаются как приходящие и уходящие, исчезающие и появляющиеся,
                — всё, с чем связаны стремления, привязанность, интуитивное желание, философские
                гипотезы, теории, представления о месте, структуре, атомном притяжении, организме,
                росте, желании, схватывании, — всё это относится к сфере материализма и не
                принадлежит моему учению.</p>
            <p>Эти вещи касаются мирских интересов: их нужно ощущать, щупать, трогать, пробовать на
                вкус. Они привлекают того, кто привязан к внешнему миру. Все они проявляются в
                агрегатах личности и, порождаемые силой желания, становятся источником несчастий,
                рождений, печалей, боли, страдания, болезни, старости и смерти.</p>
            <p>Эти вещи принадлежат мирским наслаждениям и встречаются на путях философов, но эти
                пути — не пути Дхармы.</p>
            <p>Когда постигается подлинная бессубъективность вещей и личности, когда прекращается
                различение ради утверждения «себя», нижняя система мышления перестаёт
                функционировать. Бодхисаттва проходит стадии и состояния одно за другим и становится
                хозяином самого себя и всех вещей благодаря добродетели своей жизни и спонтанной,
                лучистой способности действовать без усилий.</p>
            <p>Итак, Дхарма, которая есть благородная мудрость, превосходит всех философов.</p>
        </blockquote>

        <p>Затем Махамати обратился к Благословенному:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>В писаниях упоминается Утроба Татхагаты, и говорится, что рождающееся в ней по своей
                природе является ярким и чистым, первоначальным, незапятнанным и наделённым всеми
                знаками совершенства. Она описывается как драгоценная жемчужина, которая, однако,
                постепенно оказывается завёрнутой в облачение, загрязнённое жадностью, гневом,
                глупостью и продуктами ложного воображения.</p>
            <p>Нас учили, что природа Будды имманентна каждому существу, что она вечна, неизменна,
                благодетельна. Разве то, что рождается в Утробе Татхагаты, — это не то же самое, что
                субстанция Атмана, о которой учат философы? Божественный Атман, говорят они, также
                может быть назван вечным, неразрушимым, неизменным, негибнущим, незапятнанным.</p>
            <p>Каково же здесь различие?</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Нет, Махамати, Моя Утроба Татхагаты — это отнюдь не то же самое, что божественный
                Атман, о котором учат брахманы.</p>
            <p>Когда Я учу о состоянии Татхагаты в смысле Дхармакайи, конечного единения, Нирваны,
                пустоты, нерождённости, невыразимости, в качестве лишенного всяких волевых усилий, —
                Я имею в виду нечто иное.</p>
            <p>Причина, по которой Я проповедую учение об Утробе Татхагаты, — дать несведущим и
                простым умом отбросить страх перед учением об отсутствии «я» и понять состояние
                неразличения и безóбразности.</p>
            <p>Религиозное учение о Татхагате подобно деятельности горшечника: благодаря своему
                умению, с помощью гончарного круга, прута, воды и глины, он создаёт из одного куска
                глины разнообразные сосуды. Подобно этому, Татхагаты, обладая искусными средствами,
                могут проповедовать благородную мудрость в самых разных выражениях и символах.</p>
            <p>Они могут учить об отсутствии «я», чтобы отвратить учеников от соблазнов и
                заблуждений на пути к прямому постижению.</p>
            <p>Учение об Утробе Татхагаты излагается, чтобы пробудить философов от сна, в котором
                они цепляются за понятие божественного Атмана как трансцендентной личности. Оно
                призвано помешать их умам привязываться к воображаемой душе как к чему-то самосущему
                и ускорить их пробуждение к полному просветлению.</p>
            <p>Все понятия — причина, последовательность, атомы, первичные элементы, личная душа,
                высший дух, Господь, творец — суть продукты ложного воображения. Они — проявления
                ума и ничего более.</p>
            <p>Нет, Махамати, учение об Утробе Татхагаты не тождественно философскому представлению
                об Атмане.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Бодхисаттва — это тот, кто овладел учением о Татхагате. Находясь в уединении и следуя
                путём трансцендентальной мудрости к Нирване, он, благодаря сосредоточению, мышлению
                и пребыванию на Пути, в конце концов достигает поворотного пункта — источника
                энергии привычки. После этого он посвящает жизнь бескорыстным и превосходным
                делам.</p>
            <p>Его ум сосредоточен на состоянии буддхства. Он полностью постигает благородную истину
                прямого постижения и становится совершенным хозяином своему уму.</p>
            <p>Он подобен жемчужине, способной принимать самые разные облики и проявления. Он может
                входить в умы существ, чтобы помогать им, и постепенно проходит все стадии, пока не
                достигнет совершенной разумности Татхагаты.</p>
            <p>Тем не менее, трансцендентальное понимание (Алайя-виджняна) — это ещё не благородная
                мудрость (Арья-праджня). Это лишь интуитивное сознание её. Благодаря мудрости
                достигается совершенное состояние безóбразности — «Утроба Таковости», то есть все
                сохраняющий, божественный ум (Алайя-виджняна), который в своей чистой сущности
                всегда пребывает в совершенном терпении и невозмутимом спокойствии.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава VI.
            Самореализация</h3>

        <p>Затем Махамати попросил Благословенного поведать о природе прямого постижения, благодаря
            которой достигается трансцендентальное понимание.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный сказал:</p>
            <p>Трансцендентальное понимание возникает, когда интеллектуальный ум достигает верхней
                границы своих возможностей, и если при этом все вещи понимаются в их подлинной
                сущностной природе, а процессы умствования — основанные на обособленных идеях,
                различениях и суждениях — преодолеваются обращением к высшей способности осознания,
                тогда проявляются более высокие способности.</p>
            <p>В интуитивном уме (то есть в манасе) присутствует способность, служащая
                соединительным звеном между интеллектуальным умом и универсальным умом. Хотя здесь
                нет индивидуализированного органа, подобного интеллектуальному уму, существует нечто
                гораздо более высокое — прямая связь с универсальным умом.</p>
            <p>Хотя интуиция не даёт информации для анализа и различения, она дарует нечто более
                возвышенное: самореализацию через отождествление.</p>
        </blockquote>

        <p>Затем Махамати спросил Благословенного о том, каким ясным пониманием должен обладать
            ревностный ученик, чтобы преуспеть в практике, ведущей к прямому постижению.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Есть четыре основы, благодаря которым ревностный ученик может достичь прямое
                постижение Благородной Мудрости и стать бодхисаттвой-махасаттвой:</p>
            <p>Во-первых, нужно ясно понимать, что все вещи суть лишь проявления самого ума.</p>
            <p>Во-вторых, нужно отказаться от понятий рождения, пребывания и исчезновения.</p>
            <p>В-третьих, нужно глубоко постигнуть отсутствие «я» (анатман).</p>
            <p>В-четвёртых, нужно иметь подлинное представление о том, что есть самореализация
                Благородной Мудрости.</p>
            <p>Благодаря этим четырём пониманиям ревностный ученик может стать бодхисаттвой и
                достигнуть трансцендентального состояния.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что касается первого, он должен распознавать и быть твёрдо убеждённым в том, что этот
                тройственный мир — не что иное, как сложное проявление его собственной умственной
                активности. В нём нет субъективности, ничто ему не принадлежит; у него нет желаний,
                стремлений, приходов или уходов.</p>
            <p>Он должен понимать и принимать как факт, что этот тройственный мир проявляется и
                воображается реальным лишь под влиянием энергии привычки, накопленной с безначальных
                времён в памяти благодаря ложному воображению, ложному рассуждению, привязанности к
                объектам и реакциям на тесную взаимосвязь с идеями о теле, принадлежащем телу и
                пребывающем в теле.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что касается второго, он должен понимать и быть убеждённым, что вещи, воспринимаемые
                как формы, подобны видениям в мираже или во сне. У них нет собственной субстанции;
                они не рождены и потому не обладают собственной природой. Они существуют лишь в силу
                представления о сложной сети причин и зависимостей, возникающей благодаря различению
                и привязанности, и имеющей источник в системе ума со всем, что к ней
                принадлежит.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что касается третьего, он должен понимать и терпеливо принимать тот факт, что его
                собственный ум и его личность — также порождения ума, умственные конструкции,
                лишённые субстанциональности, нерождённые и бессубъективные.</p>
            <p>При ясном понимании всего этого становится возможным постижение истинной
                безóбразности.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что касается четвёртого, нужно иметь подлинное представление о сущности
                прямого постижения Благородной Мудрости.</p>
            <p>Она несравнима ни с представлением, достигаемым чувственным умом, ни с сознанием
                различающего интеллектуального ума. Оба предполагают различие между субъектом и
                объектом, и знание, достигаемое таким путём, характеризуется категориями единичного
                и общего.</p>
            <p>Самореализация же основана на тождестве и единстве. Здесь нечего различать и нечего
                отвергать.</p>
            <p>Но чтобы войти в это состояние и пребывать в нём как бодхисаттва, необходимо
                освободиться от всех предрассудков, от привязанности к вещам, идеям и самости.</p>
        </blockquote>

        <p>Затем Махамати обратился к Благословенному и попросил поведать о характеристике глубоких
            привязанностей к существованию, дабы можно было от них избавиться.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Когда пытаются понять значение вещей при помощи слов и логических различений, они
                следуют неизмеримо глубоким истокам существования, укоренённым в сознании. Например,
                существует привязанность к знакам единичности, причинности, понятиям бытия и
                небытия, рождения и смерти, действия и бездействия.</p>
            <p>Привычка к различению сама по себе — основа, на которой держится большинство
                философов.</p>
            <p>Особенно глубоко укоренены в умах всех существ три вида привязанности: жадность, гнев
                и пресыщенность, которая, в свою очередь, является основой похоти, страха и
                гордости.</p>
            <p>Во взаимной связи с ними находится способность различения и желания, порождающая всё,
                что сопутствует этим состояниям: возбуждение, любовь к удобству, желание личной
                жизни и перерождений на пяти путях существования — всё это продолжается благодаря
                непрерывным привязанностям.</p>
            <p>Если все привязанности разрушены, не останется никаких знаков привязанности, и мы
                выйдем за пределы различения вещей как существующих и несуществующих.</p>
            <p>Когда эта истина полностью осознаётся, все привязанности разрушаются.</p>
            <p>Однако, пока действует это тройственное сочетание — привязанность, различение и
                энергия привычки, — система мышления непрерывно функционирует, не прекращаясь ни на
                миг.</p>
            <p>Благодаря этому возникает ощущаемая уверенность в самосуществовании и воля к
                жизни.</p>
            <p>Когда же тройственное сочетание причин, возбуждающих ум, перестаёт действовать,
                наступает освобождение, и больше не возникает никаких сочетаний.</p>
            <p>Когда существование и несуществование внешнего мира распознаются как порождения
                самого ума, бодхисаттва оказывается подготовленным войти в состояние безóбразности,
                узрев в нём путь к пустоте, которая раскрывает все различия и глубоко укоренённые
                привязанности как иллюзорные.</p>
            <p>Тогда он не видит никаких знаков укоренения привязанности или источников вращения
                сансары.</p>
            <p>Он не видит ничего, что связывало бы его или препятствовало освобождению.</p>
            <p>Он видит, что именно другие существа сами стремятся к связям, к рабству, а не к
                освобождению.</p>
            <p>Поскольку во всех вещах нет субстанции, за которую можно ухватиться, там нечего
                хватать. Однако несведущие и простые умы, не распознавая этого, продолжают цепляться
                за них.</p>
            <p>Они подобны шелкопряду, который сам ткёт кокон из нитей различения, обволакивает им
                себя и замыкается в этой темнице, словно зачарованный.</p>
            <p>Мудрые же, у которых нет знаков привязанности и которые свободны от них, понимают,
                что все вещи лишены собственной субстанциональности и потому не подлежат
                различению.</p>
            <p>Махамати, ты и все прочие бодхисаттвы должны пребывать в этой уверенности и видеть
                все вещи с точки зрения единства.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Махамати, когда ты и прочие бодхисаттвы поймёте разницу между существующими и
                несуществующими привязанностями, у вас появятся наилучшие средства избегать
                привязанности к словам, через которые мы схватываем значения.</p>
            <p>Свободные от власти слов, вы сможете утвердиться в состоянии, называемом поворотным
                пунктом в глубочайшей седловине сознания, и тогда достигнете прямое постижение
                Благородной Мудрости.</p>
            <p>Вы сможете войти во все земли Татхагат и во все собрания. Вас отметят знаками силы,
                владения собой, высшими способностями, мудростью и мощью.</p>
            <p>Вы будете сиять разнообразными лучами своих преображённых тел, подобно солнцу и луне
                — без всяких усилий с вашей стороны.</p>
            <p>Вы уподобитесь могучему кристаллу, исполняющему желания, и на каждом этапе достижения
                будете видеть вещи такими, каковы они есть в совершенном единении, неосквернённые
                самосознанием.</p>
            <p>Видя все вещи подобными сну, вы сможете входить в состояние Татхагаты и проповедовать
                Дхарму мирам живых существ в согласии с их потребностями.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Махамати, существует два аспекта прямого постижения: учение о ней и сама
                самореализация.</p>
            <p>Учение преподаётся по-разному — в десяти направлениях, разделах или наставлениях —
                для тех, кто склонен к разнообразию. Для этого требуются различные средства, дабы
                пробудить во всех существах подлинное постижение сущности Дхармы.</p>
            <p>Учение предназначено, чтобы оберегать от ложных дуалистических представлений о бытии
                и небытии, единстве и иначе́сти.</p>
            <p>Сама же реализация — это внутренний опыт, не имеющий связи с низшей системой разума,
                словесным различением, идеями или философскими спекуляциями.</p>
            <p>Этот опыт сияет собственным ясным светом. Он раскрывает ошибочность и вздорность как
                умственных конструкций, так и основанных на них учений. Он делает бессильными
                злобные влияния извне и ведёт безошибочно в царства блага и благих неистечений.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Махамати, когда ревностный бодхисаттва соблюдает все эти требования, для его блага
                откроется путь к совершенному достижению прямого постижения Благородной Мудрости, и он
                полностью овладеет её плодами.</p>
        </blockquote>

        <p>Затем Махамати попросил Благословенного рассказать о единстве пути и повозки, ведущем к
            достижению Благородной Мудрости.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Чтобы легче избавиться от различения и ложных рассуждений, бодхисаттве следует
                пребывать в уединении и покое, в месте, свободном от шумной толпы, и размышлять,
                полагаясь лишь на самого себя, упражняясь в практике сосредоточения на всех
                этапах.</p>
            <p>Одиночество — характерная черта пути, ведущего к прямому постижению Благородной
                Мудрости.</p>
            <p>Я говорю об «единстве пути и повозки» не потому, что путь и повозка — одно, а потому,
                что лишь в одиночестве можно распознать и постичь путь, по которому движется
                повозка.</p>
            <p>Пока ум отвлечён, он будет лишь напрягаться в усилиях, но не достигнет высшего
                предела, какими бы средствами ни пользовался.</p>
            <p>Когда ум спокоен и одинок, можно отбросить все различия внешнего мира и искать
                самореализацию во внутренней сфере, где нет различия между путём и повозкой.</p>
            <p>Я упоминаю «три пути» или «три повозки», чтобы вести к этому пониманию несведущих. Я
                не отрицаю множественность средств: ученики и учителя достигают Нирваны при
                поддержке наставлений.</p>
            <p>Согласно учению Татхагат, все проходят путь сосредоточения и тренировки, используя
                разнообразные средства для освобождения.</p>
            <p>Поскольку ревностные ученики и учителя ещё не полностью разрушили накопленную энергию
                привычки, некоторые сталкиваются с препятствиями: различающее сознание и чувства
                вводят их в заблуждение, мешают постичь бессубъективность и понять превращения после
                смерти.</p>
            <p>Вот почему Я проповедую о тройном пути и тройной повозке, а не об их
                единственности.</p>
            <p>Даже ревностные ученики, чтобы избавиться от зловредной энергии привычки, должны
                постичь бессубъективность — и тогда они перестанут зависеть даже от имени
                «блаженство самадхи», пробудившись в высшем царстве.</p>
            <p>Пробудившийся в этом царстве понимает смысл достижения Благородной Мудрости и уже не
                заблуждается.</p>
            <p>Только тогда Я говорю об единстве пути и повозки.</p>
            <p>Махамати, подлинное понимание этого единства невозможно без достижения самого
                состояния. Его не может подарить никто — даже великий Брахма. Оно достигается лишь
                Татхагатами.</p>
            <p>Именно поэтому Я говорю об единстве пути и повозки — не потому, что путь к цели лежит
                через рассуждения о Нирване.</p>
        </blockquote>

        <p>Затем Махамати попросил Благословенного поведать о шагах, которые должен пройти
            просветлённый ученик ради прямого постижения Благородной Мудрости.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Начало — в понимании, что внешний мир есть лишь проявление самого разума, и что ум
                схватывает во внешнем мире то, что порождается привычкой к различению и ложному
                воображению.</p>
            <p>Ученик должен вести себя прилично, видеть вещи подлинно и понимать, что мир не
                обладает собственной природой, не рождён и подобен:</p>
            <p>— облаку, проходящему мимо;<br>
                — воображаемому колесу, возникающему при вращении головни;<br>
                — городу гандхарвов;<br>
                — луне, отражённой в океане;<br>
                — видению, миражу, сну.</p>
            <p>Он должен постигнуть, что ум в своей сущности пуст. Он не должен внимать
                рассуждениям, основанным на воображаемых терминах различения.</p>
            <p>Он должен понимать, что универсальный ум обладает чистой природой безóбразности, и
                лишь из-за накопленных осквернений на его поверхности возникают видимости телесных и
                местных свойств.</p>
            <p>Его собственная природа чиста, не подвержена изменениям: ни подъёму, ни падению, ни
                пребыванию где-либо, ни разрушению.</p>
            <p>Он должен ясно видеть, что подобные вещи возникают лишь в связи с представлением о
                субъекте (или Атмане), заполняющем сознательный разум.</p>
            <p>Итак, Махамати, пусть те ученики, кто желает осознать Благородную Мудрость, следуют
                путём Татхагаты. Пусть они отвратятся от необходимости различений, ложных
                рассуждений о агрегатах личности, чувственном мире, причинности, возникновении,
                пребывании и разрушении.</p>
            <p>Пусть они упражняются в дхьяне, ведущей к осознанию Благородной Мудрости.</p>
            <p>Для этого ревностный ученик должен удалиться в спокойное, уединённое место, вспомнить
                привычки этой и прежних жизней, чтобы уловить различающее сознание в его естестве и
                легко, спокойно избавиться от него — хотя само это действие не так просто и не так
                быстро.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Различают четыре рода сосредоточения (дхьяны):</p>
            <p>1. Дхьяна несведущих — практикуется теми, кто следует примеру шраваков и пратьекабудд, не
                понимая смысла и цели. Они сидят, как бы с пустыми умами. Её также практикуют те,
                кто ненавидит тело, видит в нём лишь страдание и нечистоты, цепляется за идею «я» и
                стремится лишь к прекращению мышления и психических отражений.</p>
            <p>2. Дхьяна, посвящённая изучению значений, — практикуется теми, кто, узрев
                несостоятельность идей о субъекте, ином и обоих, стремится аннигилировать
                философские конструкции и выйти за пределы двойственности субъекта и объекта,
                приходя к постижению двойного отсутствия «я».</p>
            <p>3. Дхьяна Татхаты — медитация на таковость, практикуемая ревностными учениками,
                которые, полностью распознав двойственную природу бессубъективности и безóбразность
                Татхагаты, всё же цепляются за представление о трансцендентной Татхагате.</p>
            <p>4. Дхьяна Татхагат — чистейшая медитация тех, кто, вступив на стадию татхагатства,
                пребывает в троичном блаженстве самосознания Благородной Мудрости, посвящает себя
                благу всех существ и совершает неимоверные подвиги ради их освобождения.</p>
            <p>Когда все меньшие вещи и идеи превзойдены и забыты, остаётся лишь совершенное
                состояние безóбразности, где Татхагата и Татхата сливаются в абсолютном
                единстве.</p>
            <p>Тогда все Будды сходятся из всех стран Будды и, сияющими руками благославляя,
                касаются главы того, кого можно восславить как нового Татхагату.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава VII. Достижение
            прямого постижения</h3>

        <p>Затем Махамати попросил Благословенного:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Молю Тебя, поведай нам, что представляет собой состояние прямого постижения?</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>В жизни ревностного ученика можно различить два аспекта, существенно отличающихся
                друг от друга:</p>
            <p>— состояние постижения собственной природы, возникающее из различения «я» и сферы
                сознания, связанное с представлением о субъекте;</p>
            <p>— и второе, превосходное и возвышенное состояние прямого постижения Благородной
                Мудрости.</p>
            <p>Первое сопровождается переживанием удовольствия и неудовольствия, поддаётся анализу и
                может быть изложено в логических трактатах.</p>
            <p>Ставя себя в соответствие с представлениями о бессубъективности вещей и отбрасывая
                ложные взгляды о собственном «я», можно отрешиться от подобных мыслей и твёрдо
                продвигаться в непрерывном восхождении по ступеням совершенства.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Возвышенное состояние прямого постижения, в котором ревностный ученик в глубокой
                умственной концентрации стремится отождествиться с Благородной Мудростью,
                представляет собой следующее:</p>
            <p>это усилие, направленное на уничтожение всех блуждающих мыслей о внешней видимости
                вещей, а также всех идей об индивидуальном и общем, о страдании и непостоянстве.</p>
            <p>Вместо этого он должен культивировать благороднейшие идеи — об отсутствии «я», о
                пустоте и безóбразности. Так он постепенно достигает осознания истины, свободной от
                чувств и навеки безмятежной.</p>
            <p>Когда это активное усилие умственной концентрации оказывается успешным, за ним
                следует более пассивное, восприимчивое состояние самадхи, в котором ученик вводится
                в блаженное вместилище Благородной Мудрости и испытывает плоды различных превращений
                самапатти.</p>
            <p>Здесь он прежде всего переживает возвышенное состояние реальности, но ещё не способен
                полностью избавиться от энергии привычки, которая может вовлечь его в дальнейшие
                преобразования, связанные со смертью.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Достигнув этого возвышенного и блаженного состояния, бодхисаттва не должен
                предаваться наслаждениям, связанным с блаженством самадхи, ибо всякое наслаждение
                неизбежно предполагает своё прекращение.</p>
            <p>Он должен с состраданием думать о других существах и вечно хранить свежесть и чистоту
                своих первоначальных обетов.</p>
            <p>Он никогда не должен успокаиваться, никогда не должен наслаждаться блаженством
                самадхи ради него самого.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Однако, Махамати, как только ученик начнёт продвигаться по пути к полному
                прямому постижению, ему будет угрожать опасность, против которой нужно быть
                настороже.</p>
            <p>Он может не до конца осознавать, что система ума, из-за накопленной энергии привычки,
                продолжает функционировать более или менее подсознательно на протяжении всей
                жизни.</p>
            <p>Иногда он может подумать, будто полностью достиг цели — успокоения, полностью подавив
                активность ума. О, это ошибка! Даже если внешняя активность ума подавлена, он всё же
                продолжает функционировать, ибо семена энергии привычки остаются в нём.</p>
            <p>То, что принимается за успокоение (амала), на самом деле является лишь прекращением
                функционирования внешнего аспекта ума — мира вещей, к которым воля не питает
                привязанности.</p>
            <p>Истинная цель достигается не подавлением ума, а отказом от всех различений и
                привязанностей.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Есть те, кто из страха перед страданиями, связанными с рождением и смертью, ищут
                Нирвану как убежище. Они видят, что все различаемые вещи лишены реальности, и потому
                воображают, будто Нирвана — это уничтожение чувств и сфер чувствования.</p>
            <p>Они не понимают, что рождение и смерть, с одной стороны, и Нирвана — с другой, не
                отделены друг от друга. Они не знают Нирваны как чистоту универсального ума.</p>
            <p>Такие глупые ученики, цепляющиеся за понятие Нирваны как внешнего мира, зримого лишь
                умом, пренебрегают учением Татхагат о внешнем мире и продолжают вращаться в колесе
                рождений и смертей.</p>
            <p>Лишь осознав глубинное учение о поворотном пункте в глубочайшей седловине сознания,
                они придут к совершенному прямому постижению Благородной Мудрости.</p>
            <p>Подлинное функционирование ума — тонкая и трудная материя даже для пратьекабудд,
                обладающих силой правильного понимания и самадхи. Лишь Татхагаты и бодхисаттвы,
                твёрдо укрепившиеся на седьмой ступени, полностью постигают его особенности.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Ревностные ученики и учителя, желающие постичь все аспекты работы ума на путях
                бодхисаттвы, должны, благодаря верному знанию, утвердиться в том, что все объекты
                различения — лишь видимости ума.</p>
            <p>Так они удержатся в стороне от всяких различений и ложных рассуждений, являющихся
                плодами умственной активности.</p>
            <p>Они будут стремиться видеть вещи в их истине и, насаждая корни блага в землях Будды,
                не будут знать пределов, навязываемых различениями.</p>
            <p>Для этого бодхисаттва должен стремиться к уединению и спокойствию, избегать волнений
                и социального возбуждения.</p>
            <p>Пусть избегает сонливости, не стремится к чтению многословных трактатов мирских
                философов, избегает общественных обрядов, церемоний и профессиональных
                священнослужителей.</p>
            <p>Пусть удаляется в уединённые места — в леса или на горы — и предаётся практике
                духовных упражнений.</p>
            <p>Лишь таким путём он постигнет в этом мире подлинное понимание того, как действует
                универсальный ум в своей сущности.</p>
            <p>Лишь окружив себя Буддами, как лучшими друзьями, ученик сумеет понять природу разума
                как посредника между внешним миром и универсальным умом.</p>
            <p>Тогда он пересечёт океан рождений и смертей, порождаемый неведением, желанием и
                действием.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Добившись полного понимания функционирования системы ума — в её предсубъективной фазе
                и в двойном отсутствии «я» — и утвердившись в собственном прямом постижении, не
                цепляясь за плоды практики, он пожнёт все плоды правильного знания.</p>
            <p>Так он расчистит путь к дальнейшим успехам на стезе бодхисаттвы и будет продвигаться
                по всем её ступеням.</p>
            <p>Затем ему следует отбросить понятие разума, недоступного правильному знанию, — ибо по
                сравнению с Благородной Мудростью оно подобно поступи хромого осла.</p>
            <p>Тогда он взойдёт на восьмую ступень бодхисаттвы и сам наставит себя в трёх аспектах
                Благородной Мудрости.</p>
        </blockquote>

        <p>Эти аспекты следующие:</p>

        <ul class="list-disc pl-5 space-y-1 ml-8">
            <li class="my-2">во-первых, <strong>безóбразность</strong> — когда все объекты,
                подлежащие рассмотрению на путях ученичества, учительства и философствования,
                полностью подчинены его пониманию;
            </li>
            <li class="my-2">во-вторых, <strong>сила</strong> — даруемая всеми Буддами благодаря
                изначальным обетам, включая отождествление их жизни и удела с жизнями и уделами всех
                живых существ;
            </li>
            <li class="my-2">в-третьих, <strong>совершенная самореализация</strong> — та, что здесь
                может быть достигнута лишь в определённой степени.
            </li>
        </ul>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>По мере того как бодхисаттва отвращается от частных взглядов, включая воображаемое
                «я»; по мере того как он понимает феноменальность всего, переживает состояния
                самадхи и самапатти, видит мир как сон и видение и получает поддержку всех Будд — он
                становится способен достичь состояния Татхагаты, которое и есть сама Благородная
                Мудрость.</p>
            <p>Таковы три составные части Благородной жизни, и эта троичность ведёт к совершенному
                прямому постижению Благородной Мудрости.</p>
        </blockquote>

        <p>Затем Махамати спросил у Благословенного:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный, будет ли очищение от злых истечений разума, возникающих из цепляния
                за мир проявлений и эмпирическое «я», постепенным или мгновенным?</p>
        </blockquote>

        <p>Благословенный ответил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Различают три вида истечений разума:</p>
            <p>— злостное истечение, возникающее из жажды схватывания и привязанности;</p>
            <p>— злостное истечение, порождаемое иллюзиями ума и ненасытным эгоизмом;</p>
            <p>— благие неистечения, возникающие из Благородной Мудрости.</p>
            <p>Первое — очищение от привязанности к объектам внешнего мира, которые на самом деле
                суть проявления ума, — достигается постепенно. Здесь мгновенность недостижима.</p>
            <p>Доброе поведение лишь расчищает путь к самоконтролю. Это подобно работе горшечника,
                который постепенно лепит каждый сосуд вниманием и усердием. Так же обстоит дело с
                искусствами — танцем, пением, игрой на флейте, письмом: всё достигается постепенно,
                трудолюбием и усердием.</p>
            <p>Награда — чистое понимание пустоты и преходящести всех вещей.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Второе — очищение от страхов, гнева, ненависти, гордыни, коренящихся в иллюзии ума и
                эгоизме, — также происходит постепенно. Их можно уподобить плоду манго, созревающему
                медленно, или дереву, растущему из земли понемногу.</p>
            <p>Каждый должен следовать путём изучения и сосредоточения, достигая успеха за успехом
                собственными усилиями.</p>
            <p>Даже при поддержке Татхагат — благодаря их изначальным обетам и отождествлению с
                жизнями существ — очищение остаётся медленным и требует терпения.</p>
            <p>Награда — постепенное постижение двойного отсутствия «я» и терпеливое принятие этого
                как факта, ведущее к подножию великого пути бодхисаттвы.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Однако благие неистечения, возникающие вследствие прямого постижения Благородной
                Мудрости, представляют собой очищение, происходящее мгновенно — по благодати
                Татхагат.</p>
            <p>Оно подобно зеркалу, отражающему все формы мгновенно и беспристрастно. Оно подобно
                солнцу или луне, чей свет мгновенно освещает все вещи.</p>
            <p>Точно так же Татхагаты ведут шраваков к состоянию безóбразности. Все накопленные с
                безначальных времён запасы энергии привычки и кармы — порождённые привязанностью к
                ложным взглядам и верой в реальность внешних объектов — мгновенно уничтожаются при
                раскрытии царства трансцендентального понимания, присущего состоянию Буддовости.</p>
            <p>Универсальный ум, осквернённый привычкой и кармой, проявляется как множественность
                субъектов и миров. Но он может быть очищен — постепенно, через усилия, изучение,
                сосредоточение и самореализацию.</p>
            <p>В конце концов, подобно тому как Дхарма Будды мгновенно пронзает тьму своими лучами,
                так и собственная природа ученика мгновенно засверкает в нём.</p>
            <p>Благодаря этому все бодхисаттвы одномоментно достигают осознания, оказываются в своих
                царственных дворцах на небесах Аканшиты и мгновенно излучают разнообразные сокровища
                духовного изобилия.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава VIII. Плод
            прямого постижения</h3>

        <p>Затем Махамати попросил Благословенного поведать о том, каков плод прямого постижения
            Благородной Мудрости.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Во-первых, приходит ясное понимание смысла и значения всех вещей. Затем следует
                отчётливое осознание духовных идеалов — парамит, — благодаря чему бодхисаттва
                способен глубже войти в сферу безóбразности, испытать высшее состояние самадхи и
                постепенно перейти к ещё более возвышенным ступеням бодхисаттвы.</p>
            <p>Испытав пребывание в поворотной точке, в глубочайшей седловине сознания, бодхисаттва
                приступает к упражнению в различных самадхи, вплоть до высочайшей — «ваджропама»
                (подобной алмазу), принадлежащей лишь опыту Татхагат в их превращениях.</p>
            <p>Он становится способным войти в сферу сознания, лежащую вне досягаемости систем
                разума, — в сферу сознания Татхагатовости.</p>
            <p>Он наделяется силами — психическими способностями самообладания, любящего
                сострадания, искусного умения действовать верно и способностью входить во все земли
                Будды.</p>
            <p>До прямого постижения Благородной Мудрости он остаётся под влиянием субъективных
                интересов (эгоизма). Но достигнув её, он спонтанно откликается на импульсы великого
                и сострадательного сердца и располагает безграничными, искуснейшими средствами
                искреннего и целостного служения ради освобождения всех существ.</p>
        </blockquote>

        <p>Затем Махамати попросил Благословенного рассказать, сохраняются ли силы Татхагат после их
            прямого постижения Благородной Мудрости.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Существуют два рода поддерживающих сил, которыми Татхагаты наделяют бодхисаттв.
                Благодаря им бодхисаттвы могут предстать перед Буддами и воздать им почести, задавая
                вопросы.</p>
            <p>Первая сила — это собственная вера и почитание бодхисаттвы Будды. Благодаря ей Будды
                способны являться и оказывать поддержку собственными руками тем, кто в ней
                нуждается.</p>
            <p>Вторая сила исходит непосредственно от Татхагат и позволяет бодхисаттвам достигать
                различных состояний самадхи и самапатти, проходя сквозь них, не оскверняя своего
                блаженства.</p>
            <p>Поддерживаемый силой всех Будд, даже бодхисаттва на первой ступени способен войти в
                самадхи, именуемую «свет Махаяны».</p>
            <p>В этой самадхи он осознаёт присутствие Татхагат, приходящих из шестидесяти миров в
                самых различных вместилищах.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Вместо стремления к мирским благам, следует лелеять в душе идеалы парамит:
                милосердия, благого поведения, терпения, энергичности, глубокомыслия и мудрости.</p>
            <p>Даже в мирской жизни их практика приносит счастье и успех.</p>
            <p>Тем более в сфере разума ревностные ученики и учителя должны культивировать то, что
                дарует радость освобождения, просветления и мира. Здесь парамиты основаны на
                правильном знании и ведут к постижению Нирваны — даже если Нирвана пока остаётся
                лишь «мыслью в уме».</p>
            <p>В мире разума парамиты становятся ещё более возвышенными и близкими нашему
                внутреннему чувствованию.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Милосердие более не ограничивается дарением материальных вещей, но выражается в
                раздаче более драгоценных даров — сочувствия и понимания.</p>
            <p>Благое поведение означает не только соблюдение пяти этических обетов, но и
                сознательную практику скромности, простоты, самоограничения и самоотдачи в свете
                парамит.</p>
            <p>Терпение — это не только прощение других во внешних обстоятельствах, но и терпение по
                отношению к самому себе.</p>
            <p>Энергичность (вирья) — это не просто усердие, но глубокий самоконтроль, направленный
                на то, чтобы следовать Пути и воплощать Дхарму в собственной жизни.</p>
            <p>Глубокомыслие — это состояние сознательности, в котором логические рассуждения
                уступают место интуиции, проникающей в дух и значение.</p>
            <p>Парамита мудрости (праджня-парамита) — это не эрудиция и не интеллектуальное знание.
                Она раскрывается как совершенное, всевключающее знание, которое есть любовь.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Третий аспект парамит — их идеальное совершенство у Татхагат — может быть постигнут
                лишь бодхисаттвами-махасаттвами, посвятившими себя высшей духовной дисциплине и
                полностью осознавшими, что всё, что видимо в мире, исходит лишь из самого ума.</p>
            <p>В их сознании все различения и представления о двойственности прекращают
                функционировать. Схватывание и цепляние за вещи не возникают.</p>
            <p>Освободившись от привязанностей к единичным объектам и идеям, они обретают внутреннюю
                свободу и способны созерцать пути принесения блага и счастья всем разумным
                существам.</p>
            <p>Для бодхисаттвы-махасаттвы идеал милосердия означает отказ от собственной надежды на
                достижение состояния Татхагаты и Нирваны ради того, чтобы другие могли
                воспользоваться этим достижением.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Татхагаты, сохраняя связь с внешним миром, не проводят различий между собственными
                интересами и интересами других, между добром и злом. В их умах действуют чистая
                спонтанность и совершенная активность без малейшего усилия.</p>
            <p>Парамита терпения у Татхагат развивается из полного понимания, что и «схватывающий»,
                и «схватываемое» — лишь мысли, порождённые различением и привязанностью.</p>
            <p>Парамита энергичности проявляется в бодрствовании от первой стражи до конца ночи в
                соответствии с дисциплиной, порождая способность действовать без различения между
                удобством и неудобством.</p>
            <p>У них нет разделения между «я» и «другими» в мыслях о Нирване. Их ум утверждён в
                Нирване, направлен на неё — и это есть парамита глубокомыслия.</p>
            <p>Что же до праджня-парамиты — самой Благородной Мудрости, — никто не может описать её
                словами.</p>
            <p>Когда в самадхи ум прекращает различать, остаётся лишь совершенное, исполненное любви
                состояние безóбразности. В этот миг переживается поворотная точка в глубочайшей
                седловине сознания — и это и есть праджня-парамита.</p>
        </blockquote>

        <p>Затем Махамати попросил Благословенного поведать о «тонком теле» — теле чистого
            умственного видения (маномайя-кайя), которое способны обрести бодхисаттвы, созерцающие
            мир прямого постижения Благородной Мудрости.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Махамати, различают три вида трансцендентальных тел.</p>
            <p>Первое — тело, приобретаемое бодхисаттвой в опыте самадхи и самапатти.</p>
            <p>Второе — тело, даруемое Татхагатами в соответствии с нуждами живых существ, требующих
                поддержки. Оно возникает и совершенствуется спонтанно, без усилий и напряжения.</p>
            <p>Третье — единое тело всех Татхагат, в котором они обретают интуицию Дхармакайи.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Трансцендентальная личность, входящая в опыт самадхи, достигает третьей, четвёртой и
                пятой ступеней сосредоточения, когда вся умственная деятельность утихает, и волны
                сознания перестают возмущать поверхность универсального ума.</p>
            <p>Это состояние «знающего ума», где ещё сохраняется некоторое осознание собственного
                присутствия, сопровождаемое блаженством, возникающим при прекращении деятельности
                разума.</p>
            <p>Второй род трансцендентальной личности — тот, к которому принадлежат бодхисаттвы и
                Татхагаты в их преображённых телах, являющих верность изначальным обетам.</p>
            <p>Это достигается на восьмой ступени, когда бодхисаттва обладает всепроникающей
                интуицией, прозревающей иллюзорную природу вещей.</p>
            <p>Постигнув Дхарму безóбразности, он переживает поворотную точку в глубинах сознания и
                становится способен входить в высшие самадхи, вплоть до высочайшей.</p>
            <p>Войдя в эту сферу, он достигает состояния неличного бытия, превосходящего умственное
                сознание.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благодаря этому он обретает естественное самообладание и способность действовать по
                желанию — без усилий, с быстротой мысли и мгновенностью смены отражений в
                зеркале.</p>
            <p>Это трансцендентальное тело не рождается от сочетания элементов, но в некотором
                смысле аналогично творимому: оно может проявлять внешние отличия, однако не
                ограничено ими.</p>
            <p>Лишь обладание маномайя-кайей делает возможным присутствие во всех собраниях и во
                всех землях Будды.</p>
            <p>Подобно тому как мысль мгновенно перемещается сквозь стены, реки, деревья и горы,
                подобно тому как память проникает в прошлое, — так и ум в тонком теле способен
                мысленно преодолевать сотни тысяч йоджан.</p>
            <p>Точно так же трансцендентальная личность в самадхи «ваджропама» наделяется
                сверхъестественными способностями и психическими силами, позволяющими свободно, по
                собственному желанию, следовать по благородному пути к собраниям Будд.</p>
            <p>Однако это желание уже не сосредоточено на «я», не связано с различениями и
                привязанностью. Для такой личности старое тело более не существует.</p>
            <p>Её трансцендентальное воплощение соответствует духу обетов — приносить благо всем
                живым существам до их полного созревания для освобождения.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Третий род трансцендентальных личностей настолько неописуем, что может быть осознан
                лишь через интуицию Дхармакайи — то есть через постижение безграничного и
                неразрушимого сознания универсальной разумности.</p>
            <p>Как только бодхисаттва-махасаттва достигает высочайших ступеней, он начинает
                постигать содержимое всех сокровищ Благородной Мудрости.</p>
            <p>Он обретает непостижимые тела, способные превращаться во всё необходимое, ибо такова
                подлинная природа всех Татхагат — прошлых, настоящих и будущих.</p>
            <p>Так он становится сопричастным блаженному, мирному состоянию, пронизанному Дхармой
                всех Будд.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава IX. Об ученичестве и
            преемственности архатов</h3>

        <p>Затем Махамати попросил Благословенного рассказать о разновидностях и разрядах
            учеников.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Существует множество разновидностей шраваков, ибо все они — индивиды. Однако для
                удобства их можно разделить на две группы: шраваков с преемственностью от архатов и
                шраваков, известных как бодхисаттвы.</p>
            <p>шраваков с преемственностью от архатов можно рассмотреть в двух аспектах:</p>
            <p>— во-первых, по количеству перерождений, которые им ещё предстоит пройти;</p>
            <p>— во-вторых, по степени их духовного прогресса.</p>
            <p>По первому признаку их подразделяют на три категории: вошедших в поток, однажды
                возвращающихся и невозвращающихся.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Вошедшие в поток» — это ученики, освободившиеся от привязанностей к грубым
                различениям, очистившиеся от препятствий в этой жизни и полностью постигшие значение
                анаатмы (отсутствия «я»). Они более не цепляются за понятия индивидуального и общего
                ради собственного «я».</p>
            <p>Они настолько продвинулись по Пути, что достигли шестой ступени совершенствования и
                готовы войти в состояние блаженства самадхи. Им предстоит переродиться ещё семь,
                пять или три раза, прежде чем окончательно взойти на шестую ступень.</p>
            <p>«Однажды возвращающиеся» — это те, кто достигнет архатства после одного перерождения.
                «Невозвращающиеся» — те, кто больше не родится в чувственном мире; в махаянской
                традиции они отождествляются с бодхисаттвами, достигшими седьмой ступени.</p>
            <p>Эти градации существуют потому, что есть три коренных преграды, порождённые ложным
                воображением: вера в собственную мораль как средство, сомнение в учении и
                привязанность к представлению о личной душе («я»).</p>
