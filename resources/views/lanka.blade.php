            <p>Для философов Утроба Татхагаты кажется скверной и связанной с внешними проявлениями.
                Но для Татхагат это не философская концепция, а интуитивное переживание — столь же
                реальное, как плод манго на ладони.</p>
            <p>С прекращением деятельности системы ума и всех её различений исчезают усилия и
                ограничения. Это подобно человеку во сне, который напряжённо пытается пересечь реку
                — но внезапно просыпается. Пробудившись, он спрашивает: «Было ли моё стремление
                реальным или нереальным?» Достигнув просветления, он знает: оно было ни тем, ни
                другим.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Когда бодхисаттва достигает восьмой ступени, он видит вещи воистину. Он понимает
                сущность всех снообразных представлений о жизни, знает, как пройти мимо них и
                избавиться от них.</p>
            <p>С безначальных времён система ума схватывала множественность форм, а мыслящий ум
                различал их, а эмпирический — испытывал и цеплялся. Из этого накапливалась энергия
                привычки, уплотняя иллюзии бытия, индивидуальности, общности — всё это было подобно
                сну, ложному воображению.</p>
            <p>Теперь, на восьмой ступени, жизнь воспринимается такой, какова она есть — подобной
                сну.</p>
            <p>До седьмой ступени, несмотря на интуитивное понимание иллюзорности мира, ум всё ещё
                различал и цеплялся. Он теплил привязанность к несуществующему, и хотя желание и
                схватывание исчезли, само представление о вещах сохранялось, как бы пропитывая его
                практику «запахом».</p>
            <p>Но теперь, на восьмой ступени, даже понятие о таких вещах отпадает. Все усилия и
                достижения становятся ненужными.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>«Нирвана бодхисаттвы» — это совершенное успокоение. Здесь нет различений и намерений,
                но есть свобода и спонтанная потенция постигать и терпеливо принимать все истины
                бессубъективности и безóбразности.</p>
            <p>Здесь — совершенное одиночество, не возмущаемое страхом перед непрерывностью
                причинности. Оно лучисто, свободно, блаженно и мирно — совершенно, как совершенна
                любовь.</p>
            <p>Вступая на восьмую ступень и минуя поворотную точку в глубочайшей седловине сознания,
                бодхисаттва обретает второе трансцендентальное тело — маномайя-кайю.</p>
            <p>Переход от смертного тела к трансцендентальному не есть обычная смерть. Старое тело и
                ум продолжают функционировать, но больше не подчиняются смертному уму.</p>
            <p>Здесь происходит непостижимое преображение смерти (Ачинтья-паринама-ачьюти): ложное
                представление о личной индивидуальности навеки преодолевается в единстве с
                универсальным умом Татхагатовости.</p>
            <p>В этом откровении бодхисаттва полностью наделяется силами, психическими способностями
                и самообладанием Татхагат и становится опорой для всех существ в мире желаний
                (кама-дхату).</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Так Татхагата способен поддерживать всех существ даже в трансцендентальных,
                бесформенных мирах.</p>
            <p>Первые семь ступеней принадлежат сфере разума. Восьмая превосходит разум, но ещё
                сохраняет с ним связь. Девятая ступень — «Садхумати» — «совершенное разумение» —
                достигается лишь через самореализацию Благородной Мудрости, вглубь созерцания
                безóбразности божественного ума.</p>
            <p>Это — вступление в сферу Татхагаты.</p>
            <p>Постепенно бодхисаттва постигает природу Татхагаты и обретает все её силы:
                психические способности, самообладание, любящее сострадание, искусные средства — всё
                для принесения блага всем существам во всех землях Будды.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Обретя эти силы, он воплощается в тела преображения, принимает разные облики ради
                блага других. Подобно тому как ранее воображение рождалось из относительного знания,
                теперь оно возникает спонтанно из трансцендентального понимания — как магический
                кристалл, мгновенно отражающий объекты желания, а не физические формы.</p>
            <p>Он проходит через все собрания Будд, слушает их беседы, видит природу всех вещей и
                размышляет об истинах, превосходящих понятия существования и несуществования,
                рождения и смерти, личности и исчезновения.</p>
            <p>Так понимают Татхагаты, размышляя о Благородной Мудрости, недоступной ученикам и
                учителям.</p>
            <p>Он достигает сотен тысяч, мириад и мириад мириад самадхи и в их силе переходит из
                одной земли Будды в другую, воздаёт почести всем Буддам, рождается в царских
                дворцах, проявляется в телах Будд, наставляет о Трёх Драгоценностях и участвует в
                плодах прямого постижения высших бодхисаттв.</p>
            <p>Так, переступив последнюю ступень, он становится самим Татхагатой, наделённым
                полнотой свободы Дхармакайи.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Десятая ступень принадлежит одним лишь Татхагатам.</p>
            <p>Здесь бодхисаттва видит себя восседающим на лотосоподобном троне в превосходном
                дворце, украшенном жемчугом, окружённого бодхисаттвами всех рангов.</p>
            <p>Будды всех земель собираются вокруг него, и чистыми, благоуханными руками касаются
                его головы, даруя рукоположение, посвящая и признавая его равным себе.</p>
            <p>Затем они назначают ему землю Будды, которой он будет в совершенстве владеть, как
                собственной.</p>
            <p>Десятая ступень называется «Дхармамегха» — «облако Дхармы». Она непостижима,
                недоступна уму. Лишь Татхагаты осознают совершенную безóбразность и единство этого
                состояния.</p>
            <p>Это — «Махешвара», сияющая чистая земля, земля безбрежных пространств, окружённая
                мирами желаний (кама-дхату), где бодхисаттвы трудятся, стремясь к соединению с
                высшим.</p>
            <p>Лучи Благородной Мудрости — многоцветные, всепроникающие, благодетельные —
                преображают прошлое и будущее всех миров.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Но в совершенном единстве Благородной Мудрости нет градации, последовательности,
                усилий. Десятая ступень по существу равна первой, первая — восьмой, восьмая — пятой,
                пятая — седьмой.</p>
            <p>Какая градация возможна в совершенной безóбразности и единстве?</p>
            <p>Что есть реальность Благородной Мудрости, как не неизречённые потенции
                Дхармакайи?</p>
            <p>Здесь нет пределов. Она превосходит и охватывает все земли Будды.</p>
            <p>Она пронизывает Акаништху и все небесные дворцы Тушиты.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава XI. Состояние
            Татхагаты, которое и есть Благородная Мудрость</h3>

        <p>Затем Махамати обратился к Благословенному и попросил разъяснить сказанное в канонических
            книгах о том, что Будды не знают ни рождения, ни разрушения, что их называют
            нерождёнными и что у них есть лишь имя «Татхагата». Он спросил, что означает состояние
            Татхагаты — является ли оно сущностью или не-сущностью.</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Татхагата — не не-сущность, но и не следует понимать Его подобно прочим вещам. Он не
                рождён, не исчезает, не подвластен закону причинности, и в то же время не лишён
                значения.</p>
            <p>Его можно называть «Нерождённым», но у Него есть и иные имена. Тело, созданное умом
                (маномайя-кайя), — сущностное тело, — по Своей воле принимает бесчисленные
                преображения ради освобождения существ.</p>
            <p>Эта возможность недоступна шравакам и пратьекабуддам, а также бодхисаттвам,
                достигшим лишь седьмой ступени.</p>
            <p>Да, Махамати, «Нерождённый» — синоним Татхагаты.</p>
        </blockquote>

        <p>Затем Махамати спросил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Если Татхагата не рождён, у Него, видимо, нет сущности. Как же можно давать имя тому,
                что лишено сущности? Как называть Его «некто» или «нечто»?</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Объекты часто именуют по-разному в зависимости от аспектов, в которых они
                проявляются. Так, Индру называют Шакрой или Сурендра. Эти имена взаимозаменяемы, но
                объект от этого не становится множественным.</p>
            <p>То же верно и для Татхагаты.</p>
            <p>Когда Я являюсь в этом мире терпения перед несведущими, Меня узнают в бесчисленных
                обликах и под бесчисленными именами: как Татхагату, Самбодхисаттву, аскета Гаутаму,
                Будду.</p>
            <p>Некоторые зовут Меня Брахмой, Вишну, Ишварой; иные видят во Мне солнце и луну; одни —
                десять сил, другие — Раму, Индру, Варуну.</p>
            <p>Есть те, кто называет Меня Нерождённым, Пустотой, Таковостью, Истиной, Словом,
                Реальностью, Предельным Принципом.</p>
            <p>Иные именуют Меня Дхармакайей, Нирваной, Вечностью, Тождественностью,
                Недвойственностью, Неумиранием, Бесформенностью.</p>
            <p>Некоторые отождествляют Меня с учением о Страдании, Зависимом Возникновении,
                Освобождении и Благородном Пути. Иные — с Божественным Разумом и Благородной
                Мудростью.</p>
            <p>Так во всех мирах Меня знают под бесчисленными именами — но все они подобны отражению
                луны в воде.</p>
            <p>Хотя эти имена почётны, они не дают подлинного понимания и не ведут к прямому постижению
                Мудрости. Напротив, они заставляют цепляться за слова, за их буквальный смысл или
                воображаемые значения.</p>
            <p>Авторы канонических текстов часто следуют лишь внешнему значению слов, перескакивая
                от одного текста к другому в поисках подтверждения, но не обретают веры в
                самореализацию Благородной Мудрости.</p>
        </blockquote>

        <p>Затем Махамати сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Молю Тебя, Благословенный, поведай нам о собственной природе Татхагаты.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Если описывать Татхагату как «созданного» или «несозданного», «причину» или
                «действие», мы впадём в ошибку диалектического различения.</p>
            <p>Если Татхагата создан — Он непостоянен. Если не создан — Его невозможно реализовать.
                Но то, что вне причины и следствия, вне бытия и небытия, выходит за пределы четырёх
                пропозиций логики — подобно «сыну бесплодной женщины».</p>
            <p>Все термины, применяемые к Татхагате, следует понимать в этом свете.</p>
            <p>Когда говорится, что вещи лишены «я», это означает, что они лишены собственной
                сущности. Форма лошади отличается от формы коровы, но и та, и другая — подобны сну
                или видению.</p>
            <p>Несведущие различают видимости и не понимают бессубъективности. Лишь отбросив
                привычку к различению и приняв, что все вещи пусты, нерождены и лишены собственной
                природы, можно постичь смысл.</p>
            <p>Махамати, все эти выражения о Татхагате лишены смысла в буквальном понимании. То, что
                не является «нечем», не поддаётся измерению. А то, что неизмеримо, становится пустым
                словом — а пустое слово нерождено и не подлежит разрушению.</p>
            <p>Пространство не имеет причины и действия; то, что не есть причина и не есть действие,
                — необусловлено и превосходит рассудок.</p>
            <p>А то, что вне рассудка, — есть Татхагата. Его природа удалена от возникновения и
                измерения. Собственная природа Татхагаты — это Благородная Мудрость.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Татхагата не вечен и не невечен. Всякое утверждение на этот счёт ошибочно.</p>
            <p>Если Он вечен, значит, Он связан с творческой деятельностью — но Татхагаты свободны
                от неё. Если невечен — Он подобен сотворённым вещам, что также неверно.</p>
            <p>Различение «вечного» и «невечного» — плод энергии привычки, порождённой системой ума.
                Татхагаты не возникают из такого различения.</p>
            <p>До тех пор, пока ум различает «вечное» и «невечное», он не способен постичь
                Благородную Мудрость, основанную на осознании единства и безóбразности.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Однако есть иной смысл, в котором Татхагата вечен.</p>
            <p>Трансцендентальное понимание, достигаемое при просветлении, обладает природой
                вечного. Эта истина открыта просветлённым как поддерживающий и регулирующий принцип
                реальности, сохраняющийся вовеки.</p>
            <p>Она реализуется интуитивно через самореализацию Благородной Мудрости и есть
                собственная природа Татхагат.</p>
            <p>В этом смысле Татхагаты вечны. Их состояние — немыслимая, вечная Таковость,
                нераздельная с Благородной Мудростью.</p>
            <p>Это состояние выше внешних представлений о бытии и небытии. Хотя его можно сравнить с
                пространством, Нирваной или прекращением, оно вечно по своей внутренней природе.</p>
            <p>Здесь нет творца, ибо нет творения; нет бытия и небытия. То, что открывается в высшем
                состоянии Благородной Мудрости, — есть Подлинная Вечность.</p>
            <p>Когда устранены два вида чувств и три препятствия, когда постигнуты двойное
                отсутствие «я» и непостижимые преображения смерти, остаётся лишь собственная
                природа.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Когда Дхарма полностью понята и реализована, ученики осознают в глубине сознания, что
                их собственная природа — это природа Татхагаты.</p>
            <p>В подлинном смысле существует четыре рода тождественности природы Будды: буквальная,
                словесная, смысловая и существенная.</p>
            <p>Имя «Будда» состоит из шести звуков, но эти звуки одинаковы для всех Татхагат. Среди
                всех Будд сохраняется тождественность смысла и значения.</p>
            <p>Все Будды обладают тридцатью двумя знаками величия и восьмидесятью второстепенными
                признаками совершенства — но эти формы принимаются в соответствии с
                предрасположенностью существ, подлежащих освобождению.</p>
            <p>В своей предельной природе — Дхармакайе — все Будды прошлого, настоящего и будущего
                образуют неделимое единство.</p>
        </blockquote>

        <p>Махамати спросил:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Говорилось, Благословенный, что от ночи просветления до ночи паринирваны Ты не
                произнёс ни слова. Каков глубокий смысл этого утверждения?</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Есть два смысла: в свете самореализованной Благородной Мудрости и в свете высшей
                реальности.</p>
            <p>Самореализация Мудрости у всех Татхагат одинакова — без различий, без слов, без
                идеалистических представлений, подлежащих выражению.</p>
            <p>Истина постигается через самореализацию, а не через словесные различения.</p>
            <p>Кроме того, Махамати, существует вечная реальность — Дхарма-дхату, или Таковость
                (Дхармататха), — которая пребывает независимо от появления Татхагат в мире.</p>
            <p>Высшая истина (Парамартха) вечно поддерживает порядок мира. То, что реализуют все
                Татхагаты, — это эта самая реальность, воплощённая в Дхармакайе.</p>
            <p>Солнце излучает свет спонтанно — без слов. Так и Татхагаты излучают истину
                Благородной Мудрости без слов.</p>
            <p>Поэтому Я утверждаю: от просветления до паринирваны Татхагата не произнёс ни единого
                слова. То же верно для всех Будд.</p>
        </blockquote>

        <p>Тогда Махамати сказал:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный, Ты говоришь о тождественности всех Будд. Но иной раз Ты упоминал
                Дхармататха-Будду, Нирманакайю, Самбхогакайю как различные. Не означает ли это
                различия?</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Я говорю о разных аспектах Будды, чтобы противостоять философам, верящим в реальность
                внешнего мира. Поэтому Я описываю Три Кайи.</p>
            <p><strong>Нирманакайя</strong> — тело преображения — проявляется в мирах, открывая
                качества милосердия, терпения, мудрости. Но по сути оно учит: все элементы личности
                и мира — иллюзорны; система ума — нерождена; различения — плод привычки.</p>
            <p>Нирманакайя символизирует и дифференциацию, и интеграцию: она анализирует сложное и
                упрощает его, но также объединяет всё через сострадание.</p>
            <p><strong>Самбхогакайя</strong> — тело блаженства — проявляется ради бодхисаттв, чтобы
                сделать непостижимое понятным. Через неё раскрываются нерождённость, пустота,
                бессубъективность.</p>
            <p><strong>Дхармакайя</strong> — тело истины — есть собственная природа Татхагаты:
                совершенное уединение, абсолютная безмятежность, непостижимая, неизречённая,
                необусловленная.</p>
            <p>Она — предельный принцип реальности, из которого всё возникает и в который всё
                возвращается. Она подобна центральному светилу, освещающему всё сущее.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Этот предельный принцип Дхармакайи проявляется в семи аспектах:</p>
            <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
                <li class="my-2">Как <strong>читта-гочара</strong> — сфера духовного опыта,
                    вместилище всех Татхагат в их миссии освобождения. Здесь Благородная Мудрость
                    проявляется как принцип, излечивающий привязанность к «я».
                </li>
                <li class="my-2">Как <strong>виджняна-гочара</strong> — сфера двойственности: мир
                    рождений и смертей, где действуют чувства, восприятие, различение, желание,
                    привязанность и страдание.
                </li>
                <li class="my-2">Под влиянием жадности, гнева и пресыщения этот мир погружён в
                    дуализм, но за ним уже сияет объединяющий принцип милосердия.
                </li>
                <li class="my-2">В высшем царстве, подобном миру ума, преобладают интересы сердца:
                    сострадание и самоотдача превосходят интеллект.
                </li>
                <li class="my-2">В духовном царстве бодхисаттва постигает природу Будды. Здесь
                    проявляется совершенная любовь (карма), отбрасывается последнее цепляние за
                    «субъект», и наступает самореализация Благородной Мудрости.
                </li>
                <li class="my-2">В высшей сфере — <strong>праджня-гочара</strong> — активный аспект
                    абсолютного принципа, где мудрость и любовь находятся в совершенном равновесии,
                    единстве и гармонии.
                </li>
            </ol>
            <p>Таковы семь аспектов Дхармакайи. Благодаря им все существа проявляются в совершенстве
                и возвращаются к непостижимому единству, где нет индивидуальности, начала,
                последовательности или конца.</p>
            <p>Мы называем это Дхармакайей, Таковостью, Нирваной — но все эти имена указывают на
                одну и ту же Благородную Мудрость.</p>
            <p>Махамати, ты и все бодхисаттвы должны избегать ложных рассуждений философов и
                стремиться к прямому постижению Благородной Мудрости.</p>
        </blockquote>

        <h3 class="text-2xl text-center font-bold text-sky-900 mt-6 mb-4">Глава XII и последняя.
            Нирвана</h3>

        <p>Затем Махамати попросил Благословенного:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Расскажи нам о Нирване.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный сказал:</p>
            <p>Термин «Нирвана» употребляется во многих значениях, и всех, кто его использует, можно
                разделить на четыре группы.</p>
            <p>Во-первых — люди, страдающие от страха страданий, которые видят в Нирване средство
                избавления от этого страха.</p>
            <p>Во-вторых — философы, пытающиеся ввести в Нирвану различения.</p>
            <p>В-третьих — ученики, думающие о Нирване лишь в отношении самих себя.</p>
            <p>В-четвёртых — архаты, которые, страдая и боясь страданий, воображают Нирвану как
                будущее уничтожение всех чувств и сознания. Они полагают, что Нирвана — это
                прекращение деятельности ума.</p>
            <p>Они не понимают, что универсальный ум и Нирвана — едины, и что даже сансара и Нирвана
                — не разделены. Оставаясь несведущими, они цепляются за внешние признаки Нирваны и
                продолжают вращаться в колесе рождений и смертей.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что до философов: у них на самом деле нет Нирваны.</p>
            <p>Одни полагают, что Нирвана наступает, когда прекращает функционировать система ума и
                агрегаты личности.</p>
            <p>Другие ищут её там, где нет различий между миром и его непостоянством.</p>
            <p>Некоторые представляют Нирвану как состояние, где нет воспоминаний — подобно
                погашенной лампе или сожжённому семени, не способному прорасти.</p>
            <p>Они считают, что с исчезновением субстрата страдания исчезают и различия. Но это не
                Нирвана, ибо Нирвана — не простое уничтожение и не пустота.</p>
            <p>Иные воображают, что Нирвана — это прекращение дуалистических взглядов: нет
                познающего и познаваемого, нет постоянства и непостоянства, добра и зла, чувств и
                познания.</p>
            <p>Некоторые, видя в теле источник страдания, ищут покоя в бесформенных мирах.</p>
            <p>Другие полагают, что если всё вечно и неизменно, то это и есть Нирвана.</p>
            <p>Есть и такие, кто отождествляет Нирвану с вечной душой (Атманом) или высшим духом, в
                который всё возвращается.</p>
            <p>Глупцы заявляют, что существуют две первичные субстанции — материя и душа, — и что из
                их взаимодействия возникает мир.</p>
            <p>Некоторые считают, что мир возник самопроизвольно, без причины.</p>
            <p>Цепляясь за эти заблуждения, они никогда не придут к пробуждению. Каждый, опираясь на
                свой авторитетный источник, искажает истину, ибо ищет её не там, где она есть.</p>
            <p>Результат — бесплодное блуждание, растущая путаница и смятение ума.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Что до шраваков и пратьекабудд, цепляющихся за «я» и ищущих Нирвану лишь для себя: их
                Нирвана — это вечное блаженство самадхи, достигаемое в уединении.</p>
            <p>Они понимают, что мир — проявление ума, и отказываются от социальных связей. Они
                практикуют духовные упражнения и ищут самореализацию собственными усилиями.</p>
            <p>Они достигают шестой ступени самадхи и её блаженства, но по-прежнему цепляются за
                эгоизм. Они не достигают поворотного пункта в глубочайшей седловине сознания и
                остаются под властью мыслящего ума и энергии привычки.</p>
            <p>Цепляясь за блаженство самадхи, они входят в свою Нирвану — но это не Нирвана
                Татхагат. Это «вошедшие в поток», которые ещё должны вернуться в сансару.</p>
        </blockquote>

        <p>Тогда Махамати сказал Благословенному:</p>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Когда бодхисаттва отказывается от своего блага ради освобождения всех существ, он
                объединяется со всеми живыми существами. Но при этом накапливается неисчерпаемый
                запас незрелой кармы. Молю Тебя, Благословенный, поведай: как бодхисаттва может
                питать уверенность в Нирване — для себя и для других? Что такое Нирвана
                бодхисаттвы?</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Благословенный ответил:</p>
            <p>Махамати, эта уверенность — не результат логики или исчисления. Это не
                интеллектуальное состояние, а особое состояние сердца, рождённое внутренним
                пониманием.</p>
            <p>Она возникает, когда все препятствия познанию устранены, сознание очищено, и двойное
                отсутствие «я» принимается как факт.</p>
            <p>Когда ум больше не различает, когда исчезает жажда жизни, похоть, стремление к
                познанию, желание вечного существования — тогда устраняется четырёхвидное
                желание.</p>
            <p>Энергия привычки перестаёт накапливаться, лицо универсального ума проясняется, и
                бодхисаттва достигает самосознания Благородной Мудрости — и это есть подлинная
                уверенность в Нирване.</p>
            <p>Учение о Нирване, в которой не остаётся никакого субстрата, раскрывается ради тех
                шраваков, кто ещё цепляется за мысль о личной Нирване. Оно призвано вдохновить их на
                полное посвящение миссии бодхисаттвы — освобождению всех существ.</p>
            <p>Преображённые формы Будд (Нирманакайя) проявляются, чтобы утешить робких, посрамить
                эгоистов, вдохновить на сострадание и пробудить уверенность в будущей поддержке со
                стороны Будд.</p>
            <p>Однако эти формы — не сущность Дхармататха-Будды. Истина Благородной Мудрости
                принадлежит сфере Дхармататха-Будды.</p>
            <p>Для бодхисаттв седьмой и восьмой ступеней трансцендентальное понимание открывается
                через Дхармататха-Будду, указывающую путь.</p>
            <p>В совершенному прямому постижению Благородной Мудрости, рождённой непостижимым
                преображением смерти, воля становится всецело направленной на других.</p>
            <p>Здесь нет жизни для себя — есть лишь всеобщая жизнь бодхисаттвы, проявляемая в его
                преображениях.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>В этом совершенстве Благородной Мудрости бодхисаттва постигает: все Будды — едины, и
                для Будд нет Нирваны как отдельного состояния.</p>
            <p>Великая Паринирвана Будды — не разрушение и не смерть. Если бы она была разрушением,
                это означало бы продолжение существования. Но так как нет разрушения, нет и
                действия, нет исчезновения, нет ухода, нет привязанности и нет непривязанности.</p>
            <p>Для Татхагат нет Нирваны — ибо Нирвана есть там, где понимается, что всё видимое —
                лишь проявление самого ума.</p>
            <p>Нирвана — там, где ум больше не цепляется за двойственность, где нет стремления к
                схватыванию, желания, привязанности к внешнему.</p>
            <p>Нирвана — там, где отбрасывается мыслящий ум со всеми его различениями,
                привязанностями и эгоизмом.</p>
            <p>Она — там, где логические средства, лишь питающие рассудок, теряют силу; где само
                понятие истины больше не вызывает путаницы.</p>
            <p>Нирвана — там, где преодолены четыре логические пропозиции, где открыто
                непосредственное вместилище реальности.</p>
            <p>Она — там, где устранены два вида чувств и три препятствия, где терпеливо принято
                двойное отсутствие «я».</p>
            <p>Нирвана — там, где достигнут поворотный пункт в глубочайшей седловине сознания и
                полностью осуществлена самореализация Благородной Мудрости.</p>
            <p>Вот что такое Нирвана Татхагат.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Нирвана — там, где пройдены все ступени бодхисаттвы, где бодхисаттвы, поддерживаемые
                Буддами, испытывают блаженство самадхи и возвращаются в мир ради других.</p>
            <p>Нирвана — там, где сострадание ко всем превосходит мысли о себе, где пройдены все
                ступени к Татхагате.</p>
            <p>Нирвана — это царство Дхармататхи, сфера проявления Благородной Мудрости, в которой
                состояние Будды выражается как совершенная любовь ко всем.</p>
            <p>Она — проявление этой любви, в которой Татхагата являет Себя как Благородную Мудрость
                ради просветления всех существ.</p>
            <p>Вот что поистине есть Нирвана.</p>
        </blockquote>

        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Есть два рода существ, которые не вступают в Нирвану Татхагат:</p>
            <p>— те, кто отвергает идеал бодхисаттвы, заявляя, что он не соответствует сутрам и не
                ведёт к освобождению;</p>
            <p>— и подлинные бодхисаттвы, которые, верные своим изначальным обетам, говорят: «Пока
                все существа не обретут Нирвану, Я не войду в неё Сам».</p>
            <p>Они намеренно удерживают себя от вхождения в Нирвану. Но ни одно существо не остаётся
                вне охвата милости таких Татхагат.</p>
            <p>Наступит день, когда каждое существо окажется под влиянием мудрости и любви
                Татхагаты. В преображённой форме оно получит запас блага, чтобы восходить по
                ступеням просветления.</p>
            <p>И как только существо поймёт, что с ним произошло, для него наступит Нирвана Татхагат
                — ибо она состоит в Благородной Мудрости, главная истина которой гласит: все вещи
                изначально находятся в Нирване и всегда пребывают в ней.</p>
        </blockquote>
        <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
            <p>Тогда бодхисаттва Махамати, услышав наставление Благословенного, возрадовался и
                восхвалил Его. Вместе со всем собранием — великими бодхисаттвами, бхикшу, царями,
                брахманами и мирянами — он прочно утвердился в учении о прямом постижении Благородной Мудрости.</p>
            <p>И Благословенный, видя, что собрание прониклось глубиной Дхармы, улыбнулся — и из уст
                Его излился свет, озаривший миллионы миров.</p>
            <p>Так завершилось наставление в замке Ланка, на вершине горы Малайя, посреди великого
                океана.</p>
            <p class="mt-4 font-semibold">Так окончена «Ланка-аватара-сутра».</p>
        </blockquote>

