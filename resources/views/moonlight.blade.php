        <h3 id="practice" class="text-center font-bold text-xl my-6">II. Практика</h3>
        <p>Здесь дано разъяснение метода очищения от падений и грехов с помощью созерцания и чтения
            мантры Ваджрасаттвы.</p>

        <h4 class="font-bold text-lg my-4">1. Предварительная практика</h4>
        <ol class="list-none list-outside ml-8 pl-5 space-y-4">
            <li class="my-2">
                <h5 id="1.1" class="font-bold text-lg my-2">a) Общая предварительная практика</h5>
                <p>Основа пути – вверение себя духовному наставнику. Размышляй о благоприятном
                    человеческом рождении, его восьми свободах и десяти дарах. Обдумывай
                    непостоянство и смерть через “три корня”, девять рассуждений и три
                    убеждения.</p>
                <p>Размышляй о кармических законах причины и следствия, о мучениях и невзгодах
                    дурной участи.</p>
                <p>Благодаря этому зароди в своем уме стремление к освобождению.</p>
                <p>Поскольку спасителями от этого являются только Три Драгоценности, то необходимо с
                    верой в них, полностью оставив (стремление к мирскому), искренне принять в них
                    Прибежище. Следует созерцать две бодхисаттвовские мысли, изучать шесть парамит и
                    собрание четырех добродетелей и очистить себя должным образом, породив в своем
                    сознании самадхи – союз покоя (шаматхи) и ясного видения (випашьяны).</p>
            </li>
            <li class="my-2">
                <h5 id="1.2" class="font-bold text-lg my-2">b) Особая предварительная практика</h5>
                <p>Совершив очищение сознания обычным путем как было указано ранее, следует прежде
                    всего получить абхишек путем вхождения в мандалу какого-либо идама:
                    Гухьясамаджи, Чакрасамвары или Ямандаги, но в данном случае – Ваджрасаттвы.</p>
                <p>Например, как деревянный корпус вины, кожа и прочее, собранные вместе, не звучат
                    без струн, так же, если читаешь мантры и созерцаешь собрание божеств, но не
                    получил благословения, то высшие сиддхи не обретешь. Так в “Будда Капалатантре”
                    (тиб. <em>dpal sangs rgyas thod pa’i rgyud</em>,
                    санскр. <em>Buddhakapālatantra</em>) говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>К примеру, полностью собранная вина</li>
                        <li>Без струны не звучит.</li>
                        <li>Также без абхишека</li>
                        <li>Мантру и дхьяну не завершить.</li>
                    </ul>
                </blockquote>
                <p>Даже если получил абхишек, но только самого этого не достаточно; необходимо
                    положить в основу сохранение принятых обетов личного спасения и обетов
                    Бодхисаттвы, а также сохранение всех обетов и клятв Мантры.</p>
                <p>Кроме того, если</p>
                <ul class="list-none mb-8 pl-2 ml-2">
                    <li>не тренировал ум в бодхисаттвовской мысли;</li>
                    <li>сомневался, не веря в Тантру и сокровенные наставления;</li>
                    <li>не хранил, согласно проповеданному Победоносным, клятвы и обеты;</li>
                    <li>не верил твердо божеству и Учителю, т.е. совершал какую-либо из четырех
                        ошибок,
                    </li>
                </ul>
                <p>то даже если старался осуществить практику служения и достижения в течение сотен
                    кальп, завершить ее не удастся.
                </p>
                <p>В Тантре “Ваджрного острия” (тиб. <em>rGyud rdo rje rtse mo</em>, санскр.
                    <em>Vajraśekhara-rahasya-guhya-yoga-tantra</em>) говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Хотя и практиковал с усердием</li>
                        <li>На протяжении сотен кальп,</li>
                        <li>И даже родившись в каком-либо из миров кем-то из четырех типов
                            личности,
                        </li>
                        <li>Но полного освобождения не достигнешь.</li>
                        <li>Не зародивший бодхичитту,</li>
                        <li>Сомневающийся,</li>
                        <li>Не исполняющий согласно проповеданному,</li>
                        <li>Не верящий – плод не завершит.</li>
                    </ul>
                </blockquote>
            </li>
            <li class="my-2">
                <h5 id="1.3" class="font-bold text-lg my-2">c) Полезные свойства созерцания и чтения
                    мантры</h5>
                <p>Если произносил мантру в точном соответствии с методом чтения стослоговой мантры,
                    созерцая Ваджрасаттву – воплощение Тела, Речи и Мысли всех будд, то нарушения
                    несоблюдения трех обетов и грехи коренных падений Мантраяны возрастать не будут,
                    а обретешь благословение, потому нарушений будет всё меньше и меньше. Почему же
                    они не будут увеличиваться? Разъясняется, что если в какой-либо день совершается
                    хотя бы одно падение, будь то коренное тантрическое падение или нарушение
                    собрания обетов, то на следующий день это падение увеличится вдвое, а на
                    следующий день возрастет еще более. Если же каждый день созерцать Ваджрасаттву и
                    читать в соответствии с методом стослоговую мантру двадцать один раз, то, хотя
                    это прежнее падение остается ошибкой, но его всевозрастающего увеличения, как
                    удвоение на следующий день, не возникнет.</p>
                <p>Если читать мантру согласно методу 100000 раз, то сам полностью очистишься от
                    всех грехов и осквернений, искоренив их. Если же полностью и не избавишься от
                    ошибок, то плоды этих грехов и падений – причина испытания страдания
                    бесчисленных рождений в аду и прочего – в этой жизни станут гораздо меньшим
                    вредом, подобным головной боли; а если и родишься в следующей жизни в дурной
                    участи, то
                    не будешь испытывать невыносимого страдания в течение огромного времени, а
                    испытав страдание лишь короткое время, освободишься. В “Украшении Сердца”
                    (тиб. <em>sNying po rgyan</em>) говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Если представлять должным образом Ваджрасаттву и</li>
                        <li>Читать каждый день по двадцать одному разу</li>
                        <li>Стослоговую мантру в соответствии с методом,</li>
                        <li>То благословение возрастет,</li>
                        <li>А падения (от несоблюдения обетов) не увеличатся, –</li>
                        <li>Так проповедано великими йогами, потому</li>
                        <li>Следует практиковать и между периодами созерцания.</li>
                        <li>Если так произнесешь мантру сто тысяч раз,</li>
                        <li>То полностью себя очистишь.</li>
                    </ul>
                </blockquote>

                <p>Также в “Тантре Свода Высшей Таковости” (тиб. <em>'Phags-pa de-kho-na-nyid
                        bsdus-pa'i rgyud</em>, санскр. <em>Sarvatathāgata-tattva-saṃgraha-nāma-tantra</em>)
                    говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Если читал стослоговую мантру,</li>
                        <li>То, очистив грехи немедленного воздаяния,</li>
                        <li>Обмана всех Татхагат,</li>
                        <li>Пренебрежения Святым Учением и</li>
                        <li>Всех ошибочно совершаемых действий,</li>
                        <li>Йогин станет нераздельным (устойчивым) с Ваджрасаттвой</li>
                        <li>и обретет все сиддхи, и</li>
                        <li>Кроме того, на протяжении всей жизни,следуя высшему божеству (идаму),
                        </li>
                        <li>Завершишь два Собрания (Мудрости и Добродетели),</li>
                        <li>Очистишь кармические омрачения и</li>
                        <li>Достигнешь любой желаемой цели...</li>
                    </ul>
                </blockquote>
                <p>так Учителем разъяснены непостижимые умом полезные качества [созерцания и чтения
                    мантры Ваджрасаттвы].</p>
            </li>
        </ol>

        <h4 class="font-bold text-lg my-4">2. Основная часть</h4>
        <ol class="list-none list-outside ml-8 pl-5 space-y-4">
            <li class="my-2">
                <h5 id="2.1" class="font-bold text-lg my-2">a) Метод практики в начале периода
                    созерцания</h5>
                <p>Чтобы не возникали препятствия во время исполнения практики служения божеству,
                    проводимой несколько дней, и во время следования по пути, необходимо принять
                    Прибежище в Трех Драгоценностях, совершить им подношения и вознести молитву,
                    почтить Сангху собранием накопленного, читать собрание дхарани, 100000 имен
                    [Манджушри] (<em>Манджушри-намасамгити</em>) и священные тексты, возвести ступу,
                    поднести сто жертвенных светильников, выполнить три части ритуала подношения ста
                    торм, давать милостыню нищим, выкупать жизнь, воскуривать благовония и т.п. при
                    каждом удобном случае.</p>
                <p>Если описывать духовного друга, дающего наставления, то в «Тантре Вопрошания
                    Субаху» (тиб. <em>gZugs bzang gis zhus pa’i rgyud</em>, санскр. <em>Ārya-Subāhu-paripṛcchā-nāma-tantra</em>)
                    сказано:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Как колесница с колесом одним</li>
                        <li>Хоть и имеет лошадь, но по дороге не поедет,</li>
                        <li>Так и йогин без (духовного) Друга –</li>
                        <li>Хоть тело есть, но сиддхи не достигнет.</li>
                        <li>Тот, кто умен, красив и очень чист,</li>
                        <li>Благороден, Ученью предан,</li>
                        <li>Отважен, смел, кто чувства подчинил,</li>
                        <li>Приятен речью, щедр и сострадателен,</li>
                        <li>Кто к голоду и жажде терпелив и клеши обуздал,</li>
                        <li>Брахманов и чужих богов не почитает,</li>
                        <li>Искусен в средствах, благодарен,</li>
                        <li>Триратне верит – тот “Друг”.</li>
                        <li>Но обладающих достоинствами всеми</li>
                        <li>В эпоху смутную крайне трудно встретить,</li>
                        <li>Потому доверия достоин и такой тантрийский Друг,</li>
                        <li>Который половиной, четвертью иль даже восьмой долей святых достоинств
                            обладает.
                        </li>
                    </ul>
                </blockquote>

                <p>Даже если не найдешь такого Друга, обладающего всеми качествами, но имеющий
                    добрый нрав, проявляющий веру в Три Драгоценности и чистоту, проповедующий в
                    разговоре и знаками, способный понимать и поступающий согласно понятому – такой
                    Друг необходим.</p>
                <p>Кроме того, когда долгое время пребываешь в уединении или возникает необходимость
                    идти в иное место, то из всего перечисленного выше для преодоления препятствий в
                    завершении соответствующего периода достаточно исполнять первый и второй
                    пункт.</p>
                <p>Так очищение от грехов и падений – успокоение деятельности ума, то в каком бы
                    удобном месте не была возведена палатка для созерцания, в какой бы день – первый
                    день новолуния или иной, или в день благоприятного расположения планет – следует
                    все должным образом очистить внутри, снаружи и вокруг, вымыв ароматной водой с
                    сандалом, разложить цветы и воскурить благовония всех видов. Шантипой
                    сказано:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Обитель дхьяны</li>
                        <li>Следует очистить и наполнить ароматом,</li>
                        <li>Поставив там прекрасный сандал и иное,</li>
                        <li>А также разложив цветы.</li>
                    </ul>
                </blockquote>
                <p>Внутри приготовленного места располагают опору Тела, Речи и Ума – искусно
                    выполненное изображение великолепного Ваджрасаттвы. Сверху украшают балдахином,
                    зонтом, хоругвью (гьялцэн), знаменем (бадан) и другим. Перед ним на подставке
                    сверху чистый (белый) круглый балин, подносимый Ваджрасаттве, балин – личному
                    дхармапале, две воды и семь внешних подношений для совершения подношения; на
                    низкой подставке расположен балин божеству местности и балин вредящему духу. На
                    подставке перед собой – колокольчик, справа – ваджр, правее этого – габала с
                    веществами внутреннего подношения. Кроме того следует приготовить зерно
                    процветания для рассыпания, ароматные смолы и зерна белой горчицы.</p>
                <p><strong>Четки:</strong> В «Ваджрадака-тантре» (тиб. <em>rDo rje mkha' 'gro'i
                        rgyud</em>) говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Согласно методу четки</li>
                        <li>Изготавливаются из раковины и равнинной ляпис-лазури,</li>
                        <li>Что удлиняет срок жизни и укрепляет здоровье,</li>
                        <li>А четки из семян дерева бодхи (будут способствовать)</li>
                        <li>Успокоению, возрастанию, привлечению, покорению и разрушению.</li>
                    </ul>
                </blockquote>
                <p>В “Тантре Ваджрного Острия” сказано:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Четки из семян дерева бодхи неисчислимы.</li>
                    </ul>
                </blockquote>
                <p><strong>Нить для четок:</strong> В “Тантре Четырех Сидений” (тиб. <em>gDan
                        bzhi-pa'i rgyud</em>, санскр. <em>Caturpīṭha-nāma-guhya-tantra</em>)
                    говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Сиддхи освобождения будут достигнуты,</li>
                        <li>Если использовать четки, нанизанные</li>
                        <li>Молодой, красивой девушкой,</li>
                        <li>Обладающей верой, с приятной речью.</li>
                    </ul>
                </blockquote>

                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Нити – это методы Будды,</li>
                        <li>Шарики – Архаты,</li>
                        <li>Нижняя часть указана как ступа,</li>
                        <li>Верхняя – Дхармадхату, так представлять.</li>
                        <li>Представляйте, что правый палец – ваджр,</li>
                        <li>Созерцай левый как лепесток лотоса.</li>
                        <li>При соединении ваджра и лотоса</li>
                        <li>Между ними располагай бусины четок.</li>
                        <li>Так представляя их все одну за другой</li>
                        <li>Четки следует благословить.</li>
                    </ul>
                </blockquote>

                <p>Четки расположи в середине соединенных ладоней и, произнеся семь раз:<br>
                    <strong class="font-mono text-red-700">OM RUCHIRA MANI PRAVARTAYA
                        HUM</strong><br>
                    – дунь (на них).</p>
                <p>“Если начитывать мантры используя четки, благословленные таким образом, то будут
                    произноситься все сокровенные мантры, проповеданные всеми Татхагатами, и будут
                    начитывать по десять миллионов, сто миллиардов, сто тысяч раз снова и снова
                    многократно”, – так разъясняется в “Дхарани Несравненного Дворца” (тиб. <em>gZhal
                        med khang gi gZungs</em>). “Более того, если породить Ваджрную Речь или
                    соединиться с ней, то четки обретут природу Ваджрной Речи”, – так следует
                    представлять.</p>
                <p>Затем можно обозначить периметр границы произвольно, т.е. пройти по границе земли
                    выбранной для уединения и не оспариваемой другими, развесив куски материи.</p>
                <p>Выставив перед входом дома, где созерцаешь, или в четырех сторонах уединенного
                    места созерцания четыре знака, порождаю их как четырех Махарадж, а выставив один
                    знак уединенного места созерцания порождаю его как Вирудхаку. Из этих двух
                    правил, если придерживаться последнего, но выставив знак границы в открытом
