                <p><strong class="font-mono text-red-700">OM SUMBHA NISUMBHA HUM | GRIHNA GRIHNA HUM
                        | GRIHNAPAYA GRIHNAPAYA HUM | ANAYA HO BHAGAVAN VIDYARAJA HUM
