                    <strong class="font-mono text-red-700">OM VIRUDHAKA SAPARIVARA PADYAM PRATICCHA
                        SVAHA</strong><br>
                    <strong class="font-mono text-red-700">OM VIRUDHAKA SAPARIVARA PUSHPE PRATICCHA
