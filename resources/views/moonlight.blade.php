                    (хадак).</p>
                <p><strong class="font-mono text-red-700">OM AH HUM</strong> – три раза произнес и
                    благословил.
                </p>
                <p>Произнесением<br>
                    <strong class="font-mono text-red-700">OM VAJRA AMRITA KUNDALI HANA HANA HUM
                        PHAT</strong><br>
                    – очищаю,<br>
                    <strong class="font-mono text-red-700">OM SVABHAVA SHUDDHA SARVA DHARMA SVABHAVA
                        SHUDDHO 'HAM</strong><br>
                    – очистил, они превращаются в шуньяту.</p>
                <p>Из шуньевого пространства – махараджа Вирудхака (охранитель юга) с телом синего
                    цвета, одноликий, двурукий. В руке держит меч. Окружен собранием вампиров.</p>
                <p>На его макушке – слог <strong class="font-mono text-red-700">OM</strong>,<br>
                    в горле – <strong class="font-mono text-red-700">AH</strong>,<br>
                    в сердце –<strong class="font-mono text-red-700">HUM</strong>.<br>
                    Из сердечного <strong class="font-mono text-red-700">HUM</strong> лучи света
