            </li>
            <li class="my-2">
                <h5 id="2.2" class="font-bold text-lg my-2">b) Основная часть практики</h5>
                <p>На удобном сиденье, выпрямив тело, скрестив или не скрещивая ног, как удобно,
                    следует сесть – как говорил Великий Преподобный Цзонхава.</p>
                <p>Представляй впереди в небе перед собой как Высшее Прибежище своего коренного
                    Учителя и Ваджрасаттву, неразделимых; вокруг – собрание Учителей, Идамов, Будд,
                    Бодхисаттв, дакинь, хранителей и защитников Учения, пребывающих, словно
                    скопление облаков.</p>
                <p>Представляй, что от настоящего момента и вплоть до обретения сущности пробуждения
                    принимаю как Прибежище Три Драгоценности!</p>
                <p>«Да освобожу от страданий сансары всех живых существ! Войду в наивысшее состояние
                    будды, истощившее все недостатки и полностью завершившее все достоинства. Ради
                    этого следует устремить свой ум к наивысшему пробуждению», – думая так,
                    произношу три раза:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Принимаю как Прибежище Три Драгоценности!</li>
                        <li>Да освобожу я всех живых существ!</li>
                        <li>Да достигну я состояния Пробуждения!</li>
                        <li>Да проявится истинная Бодхичитта!</li>
                    </ul>
                </blockquote>
                <p>Представляя страдания тягот и лишений сансары, как общие, так и отдельные,
                    испытываемые мною и другими живыми существами, ищу защиты от этих печалей у
                    Ваджрасаттвы – божества, олицетворяющего Три Драгоценности. Не имея сомнений в
                    Триратне как Прибежище, не имея иной надежды, кроме Них, искренне прихожу как к
                    Прибежищу к Трем Драгоценностям – это сила опоры.</p>
                <p>Поскольку из имеющихся нескольких способов созерцания Ваджрасаттвы – на макушке,
                    как самого себя и перед собой – способ созерцания на макушке наилучший для
                    представления основного очищения грехов нисходящим потоком нектара, испытания
                    блаженства и т.д., то сейчас описывается метод созерцания Ваджрасаттвы над своей
                    макушкой:</p>
                <p><strong class="font-mono text-red-700">OM SVABHAVA SHUDDHA SARVA DHARMA SVABHAVA
                        SHUDDHO 'HAM</strong></p>
                <p>Все дхармы превращаются в шуньяту. Из шуньевого пространства в одно мгновение я
                    сам проясняюсь Ваджрасаттвой с сияющей на макушке <strong
                        class="font-mono text-red-700">PAM</strong>. Из нее – лотос с буквой <strong
                        class="font-mono text-red-700">А</strong>, из нее – лунный диск с буквой
                    <strong class="font-mono text-red-700">HUM</strong> на нем. Из нее – белый
                    пятиконечный ваджр,
                    отмеченный в центре <strong class="font-mono text-red-700">HUM</strong>. Из
                    ваджра свет
                    распространился и два действия совершил. Из полностью собравшегося света
                    прояснился Ваджрасаттва – белотелый, одноликий, двурукий, держит ваджр и
                    колокольчик. Мать (Юм) – Белая Ваджратопа (Ваджрная Гордость) – одноликая, с
                    двумя руками, держит дигуг и габалу, обнимает Отца. Оба украшены шелковыми
                    одеяниями и различными драгоценными украшениями. Отец (Яб) сидит в ваджрасане. У
                    обоих на макушке – <strong class="font-mono text-red-700">ОM</strong>, в горле –
                    <strong class="font-mono text-red-700">AH</strong>, в сердце – <strong
                        class="font-mono text-red-700">HUM</strong>. Из сердечного <strong
                        class="font-mono text-red-700">HUM</strong> свет вышел и джнянасаттву,
                    подобного представляемому Ваджрасаттве, пригласил.</p>
                <p><strong class="font-mono text-red-700">JAH HUM BAM HO</strong> – они
                    недвойственными стали.
                </p>
                <p>Снова из сердечного <strong class="font-mono text-red-700">HUM</strong> свет
                    распространился и
                    божеств абхишека пригласил.</p>
                <p>“Прошу всех Татхагат истинно даровать сейчас абхишек!” – так взмолился и Они,
                    подняв бумбу, полную нектара Мудрости, льют его мне на макушку, произнося:<br>
                    <strong class="font-mono text-red-700">OM SARVA TATHAGATA ABHISHEKATA SAMAYA
                        SHRIYE HUM</strong> – так даруют абхишек.</p>
                <p>Тело наполняется нектаром Мудрости и от избытка нектара на макушке Акшобхья
                    становится головным украшением.</p>
                <p>В сердце Ваджрасаттвы на лунном диске буква <strong
                        class="font-mono text-red-700">HUM</strong>, вокруг нее по краю диска
                    вращается
                    стобуквенная мантра.</p>
                <p>“О, Бхагаван Ваджрасаттва! Прошу очистить меня и всех живых существ от всех
                    грехов и нарушений обета. Сильно раскаявшись в накоплении яда ранее совершенных
                    грехов, исстрадавшийся, взываю о помощи”, – это сила полного раскаяния.</p>
                <p>После такой мольбы лучи света из сердечного <strong
                        class="font-mono text-red-700">HUM</strong> и четок-мантры распространились;
                    всех живых
                    существ от грехов-скверн очистили, а Буддам и Бодхисаттвам поднесли
                    удовлетворяющее подношение. Все достоинства Их Тела, Речи и Мысли, полностью
                    собранные светом, входят в четки-мантру и <strong
                        class="font-mono text-red-700">HUM</strong>.
                    Из них непрерывно исходит поток белого нектара, выходя из места соединения Отца
                    и Матери, и входит в меня через отверстие Брахмы, полностью наполняя мое тело
                    потоком нектара Мудрости; все осквернения трех входов очистились – так
                    представляй очищение.</p>
                <p>Если желаешь несколько расширить порядок созерцания садханы, изложенный ранее, то
                    к моменту: “Из <strong class="font-mono text-red-700">PAM</strong> на макушке –
                    лотос”:</p>
                <p>“Я проясняюсь Идамом, на макушке – сущность Мудрости недвойственности Блаженства
                    и Шуньяты в виде белой буквы <strong class="font-mono
                    text-red-700">PAM</strong>. От ее
                    плавления в свете – белый лотос с восьмью лепестками, оттеняемый красным цветом,
                    зеленым центром и красно-желтыми тычинками. Из воды над ним – пузырьки, из них –
                    сущность Мудрости Великого Блаженства предстает в виде белой буквы <strong
                        class="font-mono text-red-700">А</strong>. От ее плавления в свете – лунный
                    диск –
                    белохрустальная мандала, подобная белой холодной луне в полнолуние. Над ней
                    сущность – Мудрость Недвойственности, сердце всех будд трех времен – в виде
                    белой буквы <strong class="font-mono text-red-700">HUM</strong>. От ее плавления
                    в свете –
                    белый пятиконечный ваджр, центр пупа которого отмечен белой буквой <strong
                        class="font-mono text-red-700">HUM</strong>”, – так представляй.</p>
                <p>Что касается момента “Из ваджра свет распространился и два действия совершил” –
                    Из ваджра лучи света безмерно распространились. На остриях некоторых лучей
                    распространились бесчисленные богини жертвы, держащие в поднятых руках
                    многоцветные стяги и поднесли жертву Буддам и бодхисаттвам десяти направлений,
                    чем породили в их умах высшее незапятнанное блаженство. Снова лучи света
                    распространяются и несут на некоторых своих остриях Ваджрасаттв, Отца и Мать,
