            <li class="my-2">
                <h5 id="3.3" class="font-bold text-lg my-2">c) Порядок исполнения, и что необходимо
                    делать</h5>
                <p>Йога промежуточного периода:</p>
                <p>Действовать, представляя, что все проявленное, в сущности не разделимое с
                    достоинством гордости Ваджрасаттвы, есть Ваджрасаттва, а все деяния трех видов –
                    мудры и мантры – это йога поведения.</p>
                <p><em>Во время еды:</em></p>
                <p>Благословив еду и питье как балин божеству, представляя рот как жертвенный очаг,
                    две руки – как два сосуда, используемые в огненной пудже, еду и питье – как
                    десять внешних подношений, Я – собрание всех божеств, ем и пью, согласно методу
                    подношения сердечному самадхисаттве, – такова йога еды.</p>
                <p><em>Во время засыпания:</em></p>
                <p>В конце бодрствования собирай все, не отвлекая ум иным, подобно собиранию в Ясный
                    Свет в заключительный период садханы, и, представляя, что оставшееся по природе
                    – шуньята, сам, как союз интуиции и метода, засыпай, – это йога засыпания.</p>
                <p><em>Во время пробуждения:</em></p>
                <p>Представляй, что пробуждаешься из Ясного Света в Теле Божества благодаря песне
                    богини, – такова йога пробуждения.</p>
                <p><em>Во время омовения:</em></p>
                <p>Совершай как во время абхишека, – это йога омовения. Мантру следует произносить
                    до тех пор, пока не станут видимы признаки очищения грехов благодаря такому
                    методу.</p>
                <p>Признаки таковы:</p>
                <p>Снится, что блюешь плохой пищей; пьешь кислое и обычное молоко и т.п. и блюешь;
                    видишь солнце и луну; ходишь по небу; горит огонь; видишь буйвола, мудрого
                    мирянина, общину монахов и монахинь; дерево, дающее молоко, слона, царя, гору,
                    львиный трон, воспарение дворца, слушание Учения, – если так снится, то это
                    знаки очищения грехов, так говорится в «Дхарани Побуждающей силы» (тиб.
                    <em>sKul byed kyi gzungs</em>).</p>
                <p>Кроме того, “если в период созерцания йоги божества уменьшается голод и уходят
                    болезни, возникает высшее знание, к тому же имеешь здоровый вид, спокоен,
                    хороший сон и снится достоверное, при чтении мантры приходит радость, усталость
                    уменьшается, чувствуешь аромат и хороший) вкус, все усилия направляешь к
                    добродетели, возрастает благоговение к божеству, то это знаки /правильного/
                    исполнения созерцания и чтения мантры божества”, – так проповедовал Лекдуб (тиб.
                    <em>Legs-grub</em>) из Цана.</p>
                <p>В «Тантре Последующего Сосредоточения» (тиб. <em>bSam gtan phyi ma</em>, санскр.
                    <em>Dhyānottara-tantra</em>) говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Неверие и леность,</li>
                        <li>Подавленность голодом и жаждой,</li>
                        <li>Необузданность и чрезвычайная привязанность ума,</li>
                        <li>Сомнения в действии,</li>
                        <li>Отсутствие стремления к дхьяне и чтению мантр,</li>
                        <li>Любовь к пустой болтовне,</li>
                        <li>Стремление к бездействию,</li>
                        <li>Обуяность злым духом,</li>
                        <li>Дурной сон и иное – причина преклонения иному божеству.</li>
                        <li>Успокоение привязанности и ненависти,</li>
                        <li>Надменности и гордость,</li>
                        <li>Поглощенность ума произнесением мантры –</li>
                        <li>Такова основа ясного постижения божества.</li>
                    </ul>
                </blockquote>
                <p>Говорится, что если в промежуточный период читаешь мантры, хотя и не считаешь
                    число начитываемых мантр, но так как необходимо не создавать препятствия, то
                    следует их начитывать. В “Коренной Тантре Чакрасамвары” говорится:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Если всегда читать мантру,</li>
                        <li>Лежишь ли и не спишь, сидишь,</li>
                        <li>Кушаешь или что-либо делаешь,</li>
                        <li>То в том препятствия не будет.</li>
                    </ul>
                </blockquote>
                <p>С самого начала начитывания определенного количества мантр следует их читать,
                    выполнив прежде созерцание йоги божества во время регулярных занятий, разделив
                    каждые сутки на четыре периода созерцания, пока не завершишь начитывание
                    необходимого количества.</p>
                <p>Четыре периода таковы: рассвет, полдень, вечер, ночь – это последовательность
                    созерцания.</p>
                <p>В период созерцания не следует кушать и пить, ходить и стоять, делать что-либо,
                    разговаривать, а следует хранить молчание.</p>
                <p>В промежуточный период можно есть и пить, ходить, стоять и т.п., не следует много
                    говорить в это время и удовлетворить свои небольшие потребности. В «Тантре
                    Вопрошания Субаху» (тиб. <em>gZugs bzang gis zhus pa’i rgyud</em>, санскр.
                    <em>Ārya-Subāhu-paripṛcchā-nāma-tantra</em>) сказано:</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Практикующему Мантраяну никогда не следует говорить попусту.</li>
                        <li>Если есть необходимость что-либо сказать, “покрывай” это мантрой,</li>
                        <li>А завершив начитывание, следует говорить мало.</li>
                    </ul>
                </blockquote>
                <p>Ежедневно на рассвете приди к Прибежищу, произнеси Семичленную молиту и раскаяние
                    в уходе от бодхичитты, выполни йогу Учителя как будды и искренне взмолись
                    Учителям, Буддам и Бодхисаттвам.</p>
                <p>Затем:</p>
                <p>“Принимаю как Высшее Прибежище Три Драгоценности!” – произнеся, следует согласно
                    методу выразить пожелание покровительства. В завершение выражения такого
                    пожелания входи в действия промежуточного периода.</p>
                <p>Во время второго и третьего периода созерцания:</p>
                <p>“Принимаю как Прибежище Три Драгоценности!” – произнеси три раза и к Прибежищу
                    прихожу.</p>
                <p>“Пусть все живые существа пребывают в блаженстве,” и т.д. произнеся, созерцай
                    четыре Безмерных.</p>
                <p>Произнесением <strong class="font-mono text-red-700">OM SVABHAVA SHUDDHA SARVA
                        DHARMA SVABHAVA SHUDDHO 'HAM</strong>, осознав шуньяту всех явлений, вступай
                    в
                    созерцание и чтение мантры Ваджрасаттвы. Следует читать стослоговую мантру,
                    затем просьбу о прощении грехов, выполнить йогу растворения и выразить
                    благопожелания о защите.</p>
                <p>В заключительный период созерцания, как и в прежние периоды, совершив подношения
                    балина и полную молитву о посвящении заслуг, по завершению просьбы о прощении
                    грехов читай стослоговую мантру.</p>
                <p>Из моего сердечного <strong class="font-mono text-red-700">HUM</strong> лучи
