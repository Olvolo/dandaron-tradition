                <p>– собрав, следует поднести.</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Великосильного, Великого Героя,</li>
                        <li>Синеликого, меч держащего,</li>
                        <li>Вирудхаку с почтением восхваляю.</li>
                        <li>Махараджа Вирудхака со свитой!</li>
                        <li>Помоги мне осуществить совершенные благие деяния</li>
                        <li>И устрани препятствия к их осуществлению! –</li>
