                    балин, две воды и остальные необходимые жертвенные предметы.</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Учителя, Будду, Дхарму и Сангху я сам вместе со всеми живыми существами
                            принимаю как Прибежище!
                        </li>
                        <li>Ради достижения полного Пробуждения на благо всех живых</li>
                        <li>Устремлю свой ум к безупречному наивысшему Пробуждению! –</li>
                    </ul>
                </blockquote>
                <p>так повторив три раза, следует породить мысль о Прибежище.</p>
                <blockquote class="border-l-4 border-sky-600 pl-6 my-6 text-gray-800 text-xl">
                    <ul class="list-none mb-8 pl-2 ml-2">
                        <li>Пусть все живые существа пребывают в блаженстве и с причиной к
                            блаженству.
                        </li>
                        <li>Пусть все живые существа пребывают без страданий и без причин к
                            страданию.
                        </li>
                        <li>Пусть все живые существа пребывают в блаженстве без страдания.</li>
                        <li>Пусть все живые существа пребывают в беспристрастности, оставив две
                            крайности – далекое и близкое, отторжение и привязанность.
                        </li>
                    </ul>
                </blockquote>
                <p>– так созерцай Четыре Безмерных.</p>
                <p>Затем произношу: «Я сам становлюсь Ваджрасаттвой».</p>
                <p>Обладая гордостью Ваджрасаттвы или какого-либо идама окропи водой жертвенные
                    тормы и мантрой<br>
                    <strong class="font-mono text-red-700">OM VAJRA AMRITA KUNDALI HANA HANA HUM
                        PHAT</strong><br>
                    – следует очистить, а мантрой<br>
                    <strong class="font-mono text-red-700">OM SVABHAVA SHUDDHA SARVA DHARMA SVABHAVA
                        SHUDDHO 'HAM</strong><br>
                    – они очищаются, становятся шуньятой.</p>
                <p>Из шуньевого пространства – <strong class="font-mono text-red-700">BHRUm</strong>,
                    из него –
                    огромные драгоценные сосуды-вместилища. Внутри каждого из них – <strong
                        class="font-mono text-red-700">OM</strong>. <strong
                        class="font-mono text-red-700">OM</strong> тает в
