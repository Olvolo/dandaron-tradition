                <p>В день выхода из затворничества, в первый период, выполнив созерцание и чтение
                    мантры согласно разъясненному ранее один раз; совершив подношение жертвы и
                    балина божеству затворничества, уходишь и “переходишь с одной стороны на
                    другую”, покидая нетронутую землю. Собранный балин личному Идаму используй как
