        <p>Учение о космосе буддизм рассматривает на духовной, а не на материальной основе.</p>
        <p><em>Абсолютная истина</em> — Дхармакая — Мудрость Дхармового пространства — тело
            дхьянибудды Акшобхьи — Космическое Тело Будды.</p>
        <p><em>Относительная истина</em> — феноменальный мир. Непостоянство всех элементов (дхарм).
            Постоянного ничего нет в мире, вечно все меняется и всегда находится в движении, любые
            теории невечны и непостоянны, с годами и веками они сменяются более лучшими и точными.
            Современная наука считает, что абсолютная истина есть точное отражение действительности.
            Но наука не может прийти к абсолюту, так как её истина объективна, научное знание не
            выходит за пределы материи.</p>
        <p>Относительная истина — это сансара, вечно движущаяся, меняющаяся и развивающаяся.
            Конечная ее цель — достижение абсолютной истины — <em>нирваны</em>.</p>
        <p><em>Адибудда</em> — сущность его Ясный Свет — нирваническое существо, которое никогда не
            было в сансаре, никогда не было загрязненным. Если же вместилищем Дхармакаи является
            шунья, то вместилищем Адибудды является уже сама Дхармакая.</p>
        <p>Дхармакая находится в шунье. Адибудда находится в Дхармакае, сущность Дхармакаи — шунья.
            Хотя Адибудда находится в шунье шуньяты, его сущность не является шуньей.</p>
        <p>Адибудда Ваджрадхара, цвет синий, Юм синего цвета, в правой руке держит ваджр, в левой —
            дильбу, Юм в правой руке держит дигуг, в левой — капалу.</p>
        <p><em>Алая-виджняна</em> — «душа» загрязненная, частица Адибудды. Когда сансара
            «отделилась» от нирваны, загрязненные авидьей (неведением) алая-виджняны «остались»,
            образовали собою сансару. Те, которые не были загрязнены авидьей и познали свою
            сущность, реализовали нирвану.</p>
        <p>Мудрость и неведение (авидья) вместерожденны, они всегда вместе. В загрязненной
            алая-виджняне постоянно рождается мудрость в виде стремления к своему первоначальному
            положению, к нирване.</p>

