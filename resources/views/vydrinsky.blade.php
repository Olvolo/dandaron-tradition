        <p>Все, что окружает нас, состоит из четырех основных элементов. Это — воздух, вода, огонь и
            земля. В самом начале сансара образовалась из этих четырех стихий, и затем появились
            другие элементы. Элемент «воздух» первоначально был не как воздух, но имел свойство
            двигаться. Элемент «вода» первоначально был не как вода, но имел свойство притягивать,
            т.е. соединять. Элемент «огонь» первоначально был не как огонь, но имел свойство
            разрушения. Элемент «земля» первоначально был не как земля, но имел свойство
            затвердевать. От этих элементов образовались все другие элементы, начиная от микрочастиц
            и вплоть до атомов и молекул. Это есть образование грубых элементов. Так образовалась
            сансара. Все имеет свойство четырех первоэлементов. Когда очищено <em>неведение</em>
            (авидья), исчезнет фильтр (субъективного восприятия), и все, что окружает нас, будет
            видно в своей первооснове: тогда не будет ни красивого, ни некрасивого, ни ценного, ни
            неценного. Все будет равно по своему содержанию.</p>

