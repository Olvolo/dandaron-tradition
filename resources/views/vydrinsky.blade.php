        <h4 class="text-center text-gray-800 font-bold text-xl my-4">АВТОРСКИЙ КОММЕНТАРИЙ К ДАННОМУ
            МЕТОДУ</h4>
        <p></p>
        <p><strong>Ваджрасаттва</strong> — Ваджрная, твердая мысль.</p>
        <p>Обретению достоинства Ваджрасаттвы способствуют пять дхьянибудд.</p>
        <p><em>Дигуг</em> — нож для разрезания толстых слоев неведения.</p>
        <p><em>Тодба (капала)</em> — символ проповедования тантрийского учения.</p>
        <p><em>Арупадхату</em> — мир бесформенных духов, находящихся в постоянном созерцании, у них
            есть клеша «неведение» (авидья), от чего они считают себя выше богов. Возраст их
            исчисляется сотнями и более триллионами лет. Иногда они погружаются в глубочайший сон
            самадхи. Этот сон продолжается несколько сотен миллиардов лет. Когда они умирают, они
            невыносимо страдают и могут попасть в «ад». <em>Рупадхату</em> — мир тонких форм,
            состоит из первоэлементов, там обитают высшие духи, они считают себя богами, но
            находятся ниже арупадхату. <em>Камадхату</em> — мир грубых форм. Здесь существует клеша
            «страсть» и рождение через половую связь. Камадхату состоит из высших и низших миров,
            всего их шесть:</p>
