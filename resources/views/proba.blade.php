        <h3>
            Различие подходов
        </h3>
        <p>Подобной разработки темы махабхути в
            Абхидхарме мы не найдём. И абхидхармисты не нуждались в ней. Они делали
            акцент на различительном аспекте человеческого познания, что давало
            в аспекте освобождения от мучительных
            аффективных состояний надёжный, но медленно достижимый эффект. Их
            безошибочный удар наносился по всей
            конструкции человеческой личности (мира). Разделение дхармического
            комплекса индивидуума на пять скандх
            концентрировало усилия, а главное, на самом близком, заметном,
            сансарном уровне вскрывало узловые направления.
        </p>
        <p>Тантристам этого было недостаточно, они
            предпочитали взрыв изнутри. На внешнем уровне махабхути входят в
            рупа-скандху, но след их уводит вглубь, вплоть до уровня Дхармакаи,
            и пронизывает всю метафизическую постройку
            буддийского мира. Теория махабхути — это “атомная физика” буддизма,
            переходящая на уровне <em>пяти светов</em> в
            “квантовую”. Вот по этим глубинным аспектам буддийского психокосмоса
            и направляют удар тантристы. Они быстро на
            практике с помощью созерцаний и ритуалов меняют природу махабхути,
            достигают уровня <em>пяти светов</em> и в
            итоге, пользуясь удачным выражением (переводом) В. Васильева,
            исчезают в “радужном теле”.
