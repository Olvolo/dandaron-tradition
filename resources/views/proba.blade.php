        <h3>
            Махабхути в Абхидхарме
        </h3>
        <p>
            Теория пяти элементов, пяти стихий, из которых построен мир, восходит к древнейшему
            периоду индийской мысли. Как составная часть эта теория вошла и в буддийскую философию.
        </p>
        <p>
            В качестве пяти дхарм – единичных сущностей – пять элементов входят в Абхидхарму,
            перечисляющую, согласно традиции Васубандху, набор 75 дхарм, полностью описывающих бытие
            личности вместе с переживаемым ею миром.
        </p>
        <p>
            В традиции тантр пять элементов являются составной частью мандал – космопсихологических
            схем, разворачивающих в сознании йогина по специально записанным рецептам –
            <em>садханам</em> (тиб. <em>sgrub thabs</em>) – точно регламентированный порядок
            созерцания.
        </p>
        <p>
            Нашей задачей будет раскрытие теории махабхути на уровне Абхидхармы и Тантры и сравнение
            этих уровней.
        </p>
        <p>
            Абхидхарма понимается как <em>чистое знание;</em> знание, с помощью которого достигается
            состояние бодхи (пробуждение). Это различающее знание истинной природы дхарм, без
            исследования которых нет возможности избавиться от аффективных состояний сознания (от
            клеш), из-за которых живые существа, непрерывно страдая, блуждают в океане бытия –
            сансаре.
        </p>
        <p>
            Абхидхарма определяет <strong>великие элементы</strong> — <strong>махабхути</strong>
            — как фундаментальные субстанции, которые по своей внутренней природе суть твердость,
            влажность, тепло и движение. Они называются<em>великими</em>, потому что служат
            субстратом всей<em>проявленной</em>, протяженной, или <em>грубой</em>материи. Их
            называют <em>великими</em>, потому что чувственно воспринимаемые комплексы земли, воды,
            огня и воздуха возникают благодаря проявлению в каждом из них функции соответствующего
            элемента. Иначе можно сказать, что твердость условно называется элементом земли,
            влажность – элементом воды, тепло – элементом огня, движение – элементом ветра
            (воздуха). Их называют фундаментальными или первоэлементами, ибо их качества
            рассматриваются как более первичные, чем остальные качества предметов. В хинаянской
            традиции таких качеств четыре: земля, вода, огонь, воздух. В Махаяне к ним добавляется
            пространство – <em>акаша</em> (санскр. <em>ākāśa</em>) и иногда сознание
            –<em>виджняна</em> (санскр. <em>vijñāna</em>).
        </p>
        <!-- Elements Classification -->
        <h4>Определение элементов</h4>
        <p>
            При анализе личности, с точки зрения существования индивидуального <em>Я</em>, весь
            набор дхарм, образующих индивидуум, делится на две основные группы: дхармы, не
            подверженные бытию (<em>асамскрита</em>, санскр. <em>asaṃskṛta</em>) – т.н. <em>постоянные
                элементы</em>, и дхармы, подверженные бытию (<em>самскрита</em>, санскр. <em>saṃskṛta</em>)
            – т.н. <em>непостоянные элементы</em>.
        </p>
        <p>
            Из махабхути к непостоянным элементам относят четыре дхармы: землю, воду, огонь, воздух;
            к постоянным элементам – пространство. Четыре махабхути группы непостоянных элементов –
            земля, вода, огонь, воздух – в буддийских текстах совершенно утратили свое значение
            первостихий, из которых построен мир. О. О. Розенберг<em>(1, с. 154)</em> пишет, что
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            махабхути являются характерным примером неоднократно уже отмеченной двойственности
            значения буддийских терминов, вызванной тем, что термин, предназначавшийся первоначально
            для описания физических и физиологических явлений, сохраняется впоследствии и для
            описания психологических.
        </blockquote>
        <p>Вот одно из определений четырех элементов:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Земля (твердость) – энергия, дающая вещам твердость, сопротивляемость против внешнего
            толчка. Вода (соединение) – энергия соединяющая, она контролирует упорядоченную
            пространственную конфигурацию элементарных частиц. Огонь (разрушение) – элемент
            разрушения, идет постоянно против воды (соединения). В результате столкновения их бывают
            скачки в природе. Воздух (движение) – самый философский элемент –
            <strong>движение</strong> (6, с. 3).
        </blockquote>
        <p>
            В данном случае четыре махабхути рассматриваются как четыре типа сил, которые в качестве
            дхарм необходимо участвуют в любом дхармовом образовании.
        </p>
        <h4>
            Махабхути и скандхи
        </h4>
        <p>
            Проведя дальнейший анализ непостоянных, подверженных бытию элементов через призму пяти
            скандх, пяти психосоматических групп, образующих то, что мы называем личностью с
            переживаемым ею внешним миром, обнаруживаем следующее распределение махабхути в
            скандхах.
        </p>
        <p>
            В <strong>рупа-скандху</strong> входят все чувственные элементы всех трех времен:
            прошлого, настоящего и будущего. О рупа-скандхе Розенберг пишет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            <p>
                Рупа, потому что они разрушаются или гибнут. Спрашивается только, в каком смысле эти
                дхармы “гибнут” или “разрушаются”. В ответ на данный вопрос поясняется: они “гибнут”
                в том смысле, что находятся в зависимости от “махабхут”, одновременно с которыми они
                рождаются и образуют состав “атома”. Таким образом, рупа-дхармами называются те
                дхармы, которые обусловлены универсальными элементами. Без
                <strong>махабхути</strong> они не могут устоять: как только исчезают махабхути, то
                вслед за ними исчезают и остальные рупа, родившиеся вместе с ними. С
                термином<strong>рупа</strong> для буддистов ассоциируется поэтому понятие<strong>несамостоятельности</strong>,
                в смысле зависимости от четырех универсальных элементов.
            </p>
            <p class="my-4">
                Это отнюдь не означает, что так называемые<strong>несамостоятельные</strong> дхармы
                составлены из махабхути, или что они их “продукты” . Также это не означает, что
                махабхути — носители других дхарм. Зависимость лишь сказывается в том, что они в
                моменте возникают вместе, одновременно, и что ни одна из рупа-дхарм не “рождается”
                без того, чтобы в тот же момент не родились бы и четыре махабхути (1, с. 68-69).
            </p>
        </blockquote>
        <p>
            Махабхути связаны со структурой <em>рупа-скандхи</em> следующим
            образом.<em>Рупа-скандха</em> (группа формы, или группа чувственного) делится на
            внутреннее и внешнее проявление формы.
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            <p class="my-4">
                Внешнее проявление распадается на восемь баз (аятан). В теории элементов бытия мы
                имеем дело с анализом личности человека, ибо только в нем, а не в неодушевленных
                объектах содержатся все элементы, т.е. и чувственные, и сознание, и процессы. Вместе
                с тем совершенно очевидно, что живое существо анализируется целиком, т.е. не только
                его “материальное” тело и психическая жизнь, но то все объективное, что оно называет
                внешним миром. Восемь <strong>аятан</strong>, или основ, баз внешних объектов, и
                являются проявлением всех качеств внешних объектов, при посредстве которых в данный
                определенный момент происходит восприятие (схватывание) сознанием всего
                объективного. Эти базы перечисляются в следующем порядке:
            </p>
            <ol class="prose list-decimal list-outside ml-2 space-y-1 pl-2">
                <li class="my-2">база видимого, т.е. формы, объема, цвета;</li>
                <li class="my-2">база слышимого, т.е. база звука;</li>
                <li class="my-2">база обоняемого, т.е. запаха</li>
                <li class="my-2">база вкушаемого;</li>
                <li class="my-2">база осязаемого;</li>
                <li class="my-2">база сложной формы;</li>
                <li class="my-2">база простой формы;</li>
                <li class="my-2">база четырех <strong>великих элементов</strong>.</li>
            </ol>
            <p class="text-right">(3, с. 37-39)</p>
        </blockquote>

        <p>
            В свою очередь, база формы (<em>рупа-аятана</em>) состоит из цвета и пространственной
            формы (восемь типов формы).
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            База осязаемого зависит от состояния живого существа, т.е. от того, находится ли живое
            существо в утробном состоянии или вышло из него. В первом случае его состояние –
            состояние без эмоций, т.е. оно имеет все элементы в некомбинированном виде и по существу
            соприкасается лишь с четырьмя <strong>великими элементами (3, с. 41)</strong>.
        </blockquote>
        <p>
            Внутреннее проявление рупа-скандхи связано с действием пяти органов чувств
            (<em>индрий</em>): органа зрения, органа слуха, органа обоняния, органа вкуса и органа
            осязания. Взаимосвязь индрий с махабхути обнаруживается при анализе структуры
            тантрийских мандал, о чем речь будет идти ниже.
        </p>
        <p>
            С махабхути связан входящий в рупа-скандху элемент<em>авиджняпти</em>, или
            необнаруживаемый невидимый элемент. Это то, «что нельзя показать другим», это элемент,
            лежащий в основе характера личности, поскольку характер проявляется в телесных актах
            данного лица. Авиджняпти, по мнению О. О. Розенберга, отнесен к рупа-скандхе именно
            потому, что он обусловлен четырьмя махабхути. «Ибо если нет махабхути, то нет и
            остальных рупа, не появляется и авиджняпти» (1, с. 169).
        </p>
        <p>
            Согласно Абхидхарме, <em>авиджняпти</em> – это обусловленный махабхути элемент, не
            обнаруживаемый для постороннего наблюдателя способом, устанавливающий связь между
            действиями личности в благоприятном или неблагоприятном отношении к ее освобождению из
            потока бытия. Исходя из этого, Розенберг непосредственно связывал авиджняпти с теорией
            кармы. Т.е. авиджняпти, обусловленный четырьмя махабхути, является как бы дхармическим
            субстратом кармы, ее содержательным аспектом, в отличие от механического субстрата
            кармы, который реализуется <em>самскара-дхармами</em> – элементами-двигателями.
        </p>
        <p>
            <strong><em>Ведана-скандха</em></strong>, или группа ощущения, рассматривается как
            аффективные реакции личности. Объекты ощущения принадлежат к пяти классам: зрению,
            слуху, обонянию, вкусу и осязанию.
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Буддагоша делит их на <strong>асампаттарупа</strong>, возникающие тогда, когда организм
            не вступает в контакт с объективными источниками ощущений, как объекты зрения и слуха, и<em>сампаттарупа</em>
            – объекты обоняния, вкуса и др., являющиеся лишь видоизменением осязания (2, с. 341).
        </blockquote>
        <p>
            Природа объектов описывается на абхидхармическом уровне как составная часть
            рупа-скандхи, т.е. обусловлена четырьмя махабхути. Столкновение объекта с органом
            восприятия рождает ощущение. То есть ощущения косвенным образом через индрии как пять
            органов чувств и через восемь баз рупа-скандхи связаны с четырьмя махабхути. Любые
            изменения четырех махабхути и соответствующие изменения рупа-скандхи необходимо меняют
            наши ощущения, то есть <em>ведана-скандху</em>.
        </p>
        <p>
            Связь ведана-скандхи с четырьмя махабхути не зависит от типа анализа природы ощущений,
            возникают ли они внутри субъекта, как считают виджнянавадины, или же они есть реакции на
            независимые источники объективных раздражителей.
        </p>
        <p>
            Уместно заметить, что так как дальнейшее изложение тантрийской проблематики махабхути
            будет дано по «Ваджрабхайраватантре» – ведущей системе цзонхавинской школы, то наше
            изложение будет следовать принятому у цзонхавинцев философскому направлению
            мадхьямиков-прасангиков. Прасангики утверждают, что пустая форма и качества вещей не
            суть продукты нашего субъективного сознания, как утверждают идеалисты-виджнянавадины.
            Эти пустые формы и качества вещей существуют объективно и независимо от нашего сознания,
            но они эфемерны и иллюзорны, и потому – шуньи. Отсутствие конкретного Я вещей за
            простыми формами и свойствами представители прасангики называют <em>шуньей внешнего</em>.
            Субъективный и объективный миры по своему содержанию не отличаются, ибо они есть шунья,
            однако они отличаются по внешнему проявлению в пространстве и во времени. У истоков
            мадхьямики-прасангики стояли Буддапалита, Чандракирти и Шантидэва.
        </p>
        <p>
            <strong><em>Санджня-скандха</em></strong> есть выделенные в отдельную группу из
            психических процессов дхармы различения, позволяющие сознанию отличать предметы по
            признакам. Без этих дхарм все казалось бы одинаковым, и познание было бы невозможным.
            Осуществляя разделение элементов по признакам, эти дхармы как бы разрушают дхармический
            комплекс явления, осуществляя аналитический аспект мышления, и по принципу действия
            похожи на свойства махабхуты огонь как элемента разрушения.
        </p>
        <p>
            Что касается <strong><em>самскара-скандхи</em></strong> (группа двигателей), то
            Абхидхарма непосредственно не подтверждает связи этой группы с махабхути. Но так как
            самскара-дхармы, из которых состоит эта группа, есть своеобразные силы-двигатели,
            заставляющие другие дхармы вступать в связь между собой, то можно соотнести их с
            махабхути воздух как элементом движения.
        </p>
        <p>
            О <em>воздухе</em> Розенберг пишет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Среди четырех элементов особое место занимает <strong>ветер</strong>, он имеет двоякое
            значение: во-первых,<strong>ветер</strong> есть носитель, субстрат осязаемых элементов
            <strong>тяжело</strong>и <strong>легко</strong>; во-вторых, он является субстратом
            явления<strong>движения</strong>. Так как дхармы рождаются только на момент, они не в
            состоянии передвигаться, на опыте же движение наблюдается; поэтому надо допустить, что
            дхарма, проявившаяся в один момент здесь, в следующий момент проявится в другом месте.
            Проблема движения и значение<strong>ветра</strong> более подробно рассматривается в
            “Абхидхармакоше” в связи со спором о том, что такое карма, является ли карма только
            “организованностью” или “формой”, в которой расположены дхармы данного потока сознания,
            или же “движением” (1, с. 164-165).
        </blockquote>
        <p>
            Среди психических процессов (<em>читта-сампраюкта-самскара</em>) есть дхарма
            <em>четана</em>, или т. н. активность <em>сознания</em>. Дхарма <em>четана</em>
            заставляет сознание действовать. Розенберг определяет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Карма – это психическая дхарма <em>четана</em>, а также ее производное (1, с. 193).
        </blockquote>
        <p>
            Т. е. с элементом <em>воздух</em> и с проблемой кармы связаны авиджняпти и
            самскара-скандха. Эта взаимосвязь подтверждается и на уровне Тантры.
        </p>
        <p>
            <strong><em>Виджняна-скандха</em></strong>, согласно “Абхидхармакоше”, не связана с
            махабхути или, как пишет С. Радхакришнан: </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Ряд виджняны отличается от веданы, или от ощущений, он автономен и независим от
            физических процессов (2, с. 345).
        </blockquote>
        <p>
            Эта <em>независимость</em> есть независимость от четырех махабхути, но небытийный
            элемент <em>акаша</em>, или пространство, сложным образом связан с виджняной, что
            обнаруживается при детальном анализе буддийской теории сознания.
        </p>
        <p>
            Поток сознания состоит из непрерывной самостоятельной цепи дхарм, образующих ту или иную
            личность.
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Разделение дхармы сознания на шесть разновидностей, или шесть <strong>виджнян</strong>,
            чисто условно. Т.н. шесть сознаний, составляющих шесть элементов (<strong>дхату</strong>)
            наряду с шестью <strong>индриями</strong> и шестью <strong>вишаями</strong>, в сущности
            только одна дхарма, шестиединая дхарма сознания (1, с. 182).
        </blockquote>
        <p>
            К этому добавляется еще <em>манас</em> – сознание предыдущего момента. Виджнянавадины
            добавляют <em>восьмое сознание</em>, или сознание-вместилище – <em>алая-виджняну</em>.
        </p>
        <p>
            Таким образом, сознание состоит из следующих элементов:
        </p>
        <ul class="prose list-disc pl-2 space-y-1 ml-2 mb-4">
            <li class="my-2">Виджняна зрительного восприятия,</li>
            <li class="my-2">Слухового восприятия,</li>
            <li class="my-2">Обонятельного восприятия,</li>
            <li class="my-2">Вкусового восприятия,</li>
            <li class="my-2">Осязательного восприятия</li>
            <li class="my-2">Восприятия предыдущего момента (манас),</li>
            <li class="my-2">Омраченного восприятия,</li>
            <li class="my-2">Алая-виджняна.</li>
        </ul>

        <p class="my-4">
            Алая-виджняна является как бы зародышем всех элементов и существует в своей идеальной
            форме. Не совпадая с первыми семью элементами сознания, она стоит отдельно от них и
            является частицей нирваны, поэтому не причастна к эмпирическому бытию; с точки зрения
            теории дхарм, не испытывает волнения. Все предыдущие виджняны могут быть подавлены и
            исчезнут, но алая-виджняна остается неизменной. Алая-виджняна как постоянный элемент
            стоит в одном ряду с акашей.
        </p>
        <p>
            Собственно виджняну, которая есть алая-виджняна, не надо путать с виджняна-скандхой.
            Виджняна-скандха хотя и не зависит от четырех махабхути, но может через субъективные
            факторы (индрии) вступать с ними в связь. Алая-виджняна и четыре махабхути не соотносимы
            вообще никаким образом. Связь алая-виджняны с акашей удобно рассмотреть после
            ознакомления с представлениями буддистов о пространстве.
        </p>
        <h4>Пространство</h4>
        <p>
            Согласно палийскому канону, пространство (санскр. <em>ākāśa</em>) не причисляется к так
            называемым великим материальным элементам (санскр. <em>mahābhūta</em>, или
            <em>dhātu</em>), которых насчитывается только четыре.
        </p>
        <p>
            В сутрах встречаются места, из которых можно вывести заключение, что акаша считается
            пятым элементом материи.
        </p>
        <p>
            В “Dhammasangani” пространство характеризуется как небесная сфера, пустое пространство,
            и подчеркивается обстоятельство, что оно не имеет ничего общего с четырьмя элементами
            группы материи. Здесь, в том числе в согласии с Буддагошей, пространство является особой
            субстанцией, имеющей три измерения и определяющей внешнее расположение объектов; так же,
            как и весь внешний мир, пространство здесь определяется как одна из скандх, так
            называемая <em>скандха видимых форм</em> (санскр. <em>rūpaskandha</em>).
        </p>
        <p>
            Во времена Нагарджуны буддисты насчитывали шесть основных элементов бытия (санскр. <em>dhātu</em>).
            К четырем элементам материи присоединялись еще акаша и виджняна.
        </p>
        <p>
            Чандракирти определяет пространство как “отсутствие заграждения” (санскр.
            <em>anāvaraṇam</em>), то есть пустота.
        </p>
        <p>
            Ф. И. Щербатской, ссылаясь на Дхармакирти, упоминает два вида пространства:
            “субъективное пространство”, пространственное определение объекта, и “безграничное и
            безначальное пространство, главным признаком которого является неизменяемость” (4, с.
            115).
        </p>
        <p>
            Дхармакирти считает акашу – пространство – вечным, но не бытующим элементом. Все, что
            существует, не вечно. Обратная связь этих понятий: все, что вечно, не существует.
            Пространство вечно и потому не существует.
        </p>
        <p>
            Абхидхарма относит пятый великий элемент – <em>акашу</em>, в отличие от четырех
            других – земли, воды, огня и воздуха – к категории постоянных или не подверженных
            бытию элементов – <em>асамскрита-дхармам</em>.
        </p>
        <p>
            Рассмотрим сначала «субъективное пространство», которое, согласно Дхармакирти, прежде
            всего отлично от акаши своим непостоянством, а Буддагошей отождествляется с
            рупа-скандхой.
        </p>
        <p>
            Понятие формы – <em>рупы</em> подробно рассматривается в связи с понятием атома. Атом
            определяется как мельчайшая единица чувственного переживания. Он состоит из четырех
            махабхути, а также из видимого, обоняемого, вкушаемого и осязаемого. Он может состоять
            из девяти элементов, если добавить, скажем, чувственный элемент осязания
            (<em>кая-индрия</em>), если добавить звук – будет десять элементов; если добавить зрение
            – одиннадцать. Восемь элементов – это обязательный минимум, из которого состоит атом.
            <em>Ану</em>, т.е. атом, как мельчайшая пространственная частица может состоять из 8, 9,
            10, 11 и т.д. элементов.
        </p>

        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Сами рупа-дхармы непространственны. Пространственность (длинное, короткое) и форма
            (круглое, квадратное и т.д.) есть самостоятельные дхармы. Только в соединении с этими
            (чисто пространственными) дхармами другие дхармы, например <strong>видимое</strong>,
            кажется протяженным <em>(1, с. 1, 60-161)</em>.
        </blockquote>

        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Так же как момент может быть наполнен большим или меньшим количеством рождающихся и
            исчезающих дхарм, так и атом в качестве пространственного момента может быть наполнен
            большим или меньшим количеством рупа-дхарм (1, с. 157).
        </blockquote>

        <p>
            Таким образом, Розенберг пришел к выводу,
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            что не рупа-дхарма состоит из атомов, а наоборот, в <strong>атом</strong>, т.е.
            мельчайшую часть пространства, входит известное число рупа-дхарм, самих по себе
            непространственных (1, с. 157).
        </blockquote>

        <p>
            Пространство, определяемое терминами <em>ану</em> и<em>рупа-аятана</em> (цвет, форма)
            есть пространство, изменяемое, состоящее из разного количества дхарм, подверженных
            бытию. Это как бы <em>чувственное пространство</em>, воспринимаемое нами в ощущении и
            никак не связанное с пятым великим элементом – акашей. Чувственное пространство похоже
            на пространство эвклидовой геометрии, и к нему приблизительно применим принцип
            производности пространства от материи, если последней условно считать рупа-дхармы. Но
            оно и отлично от пространства классической физики, ибо его никак нельзя назвать
            абсолютным в силу его изменяемости и обусловленности в конечном итоге четырьмя
            махабхути: землей, водой, огнем и воздухом. Если считать дхармы реально существующими,
            то чувственное пространство субстанционально.
        </p>
        <p>
            Чувственное пространство, согласно Васубандху, нельзя считать дхармой; нет такой дхармы,
            которая так называлась бы – <em>чувственное пространство</em>. Рассматривая элемент
            пространства с позиции существования такого понятия, как <em>чувственное
                пространство</em>, Абхидхарма связывает его с понятием промежуток, пустотой, где
            пустота трактуется как свет и темнота. Эта пустота не есть шунья, пустота (пространство)
            рассматривается здесь как производное от материи. Чувственное пространство как граница
            объектов, их форма, не есть дхарма, оно трактуется в Абхидхарме как рупа-аятана
            (категория цвета и формы), т.е. как категория зрительно воспринимаемых объектов.
        </p>
        <p>
            Под <em>пространством как махабхути</em> – акашей – пятым великим элементом – понимается
            один неделимый элемент, т.е. относящийся к классу <em>асамскрита</em>. Согласно
            Васубандху, в число асамскрита-дхарм входят также <em>пратисанкхьяниродха</em> –
            состояние успокоения, вызванное исследованием каждой дхармы в отдельности и <em>апратисанкхьяниродха</em>–
            состояние успокоенности без исследования каждой дхармы в отдельности. К этой же группе
            относится и<em>истина пути</em>. Все эти четыре элемента не связаны с волнением и не
            приводят к аффектам сознания.
        </p>
        <p>
            Акаша есть одна из метафизических констант буддизма, она свободна от различий и
            ограничений.
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Это – вечная, всеохватывающая положительная субстанция. Она существует, хотя и не имеет
            формы (рупы) и не является материальной вещью (<strong>васту</strong>) (2, с. 528-529).
        </blockquote>

        <p>
            Это определение С. Радхакришнана не точно: пространство-акаша действительно считается
            буддистами вечным, но его нельзя назвать существующим, ибо оно есть дхарма, не
            подверженная бытию (смотри замечание Дхармакирти). Субстанцией акаша также не является.
        </p>
        <p>
            В тантрийских мандалах сознание и акаша локализованы в центре. Аффективному сознанию
            соответствует непостоянное чувственное пространство цвета-формы предметов,
            просветленному сознанию соответствует акаша как постоянный элемент. Или, согласно
            виджнянаваде, алая-виджняне соответствует акаша.
        </p>

        <h3>Махабхути в тантре</h3>
        <p>
            Рассмотрим, какое место махабхути занимают в Тантре. Один из наиболее распространенных
            текстов в Тантре – садханы. <em>Садхана</em> описывает процесс вызова божества,
            жертвоприношение, порядок постройки дворца с окружением, отождествление йогина с идамом,
            мантры и их действие, традицию данной тантры, процесс выхода идама из йогина и молитвы.
        </p>
        <p>
            Для примера обратимся к садхане Ваджрабхайравы (Ямандаги) – основного идама
            цзонхавинского направления класса ануттарайогатантры (тиб. <em>dPal rdo rje 'jigs byed
                lha bcu gsum kyi sgrub thabs bsdus pa zhes bya ba bzhugs so</em>). Садхана
            Тринадцати божеств Ямандаги делится на три периода:
        </p>
        <ul class="prose list-none pl-2 space-y-1 ml-2 mb-4">
            <li class="my-2"><strong><em>Период Дхармакаи</em></strong> – ритуал приготовления
                нектара (тиб. <em>bdud rtsi</em>), процесс превращения четырех махабхути в Три Тела
                Будды, заканчивается словами: «Так, собрав мудрость, созерцать путь смерти как путь
                Дхармакаи».
            </li>
            <li class="my-2"><strong><em>Период Самбхогакаи</em></strong> – приводит к появлению в
                сердце йогина джнянасаттвы Манджушри, заканчивается словами: «Этим достигается
                видение Манджушри в бардо». (Бардо – промежуточное состояние после смерти и до
                нового рождения.)
            </li>
            <li class="my-2"><strong><em>Период Нирманакаи</em></strong> – процесс возникновения
                Тринадцати божеств Ваджрабхайравы, заканчивается превращением йогина в идама.
            </li>
        </ul>
        <h4>
            Махабхути в структуре мандала
        </h4>
        <p>
            Основная информация о махабхути содержится во втором периоде, где излагается структура
            дворца Ямандаги и мандалы. Период Самбхогакаи начинается с возникновения воздушной
            мандалы, затем возникают последовательно огненная мандала, водяная и земляная.
            Графически они изображаются как концентрические круги вокруг дворца, окрашенные
            соответственно в голубой, красный, белый и желтый цвета, и являются фундаментом для
            постройки самого дворца.
        </p>
        <p>
            Период Нирманакаи показывает связь махабхути с тринадцатью божествами садханы Ямандаги.
            Тринадцать Ямандаг, дхьяни-будды, клеши, мудрости, скандхи, силы, махабхути и индрии
            располагаются в мандале следующим образом (<a
                href="/anthology/montlevic-vladimir-mixailovic/articles-montlevich/articles-montlevich-buddology/svodnuiu-tablicu-trinadcati-bozestv-sri-vadzrabxairavy"
                class="text-blue-500 hover:underline">смотрите Сводную таблицу Тринадцати Божеств
                Шри Ваджрабхайравы</a>).
        </p>
        <!-- Mandala Analysis -->
        <p>Внутренние двери дворца:</p>
        <ul class="prose list-none pl-2 space-y-1 ml-2 mb-4">
            <li>
                <ul class="pl-2 space-y-1">
                    <li class="my-2">Восток: Невежественный Ямандага, Вайрочана, неведение,
                        Зерцалоподобная мудрость, очищающая рупа-скандху.
                    </li>
                    <li class="my-2">Юг: Гордый (Жадный) Ямандага, Ратнасамбхава, гордость,
                        Равномерная мудрость, очищающая ведана-скандху.
                    </li>
                    <li class="my-2">Запад: Страстный Ямандага, Амитабха, страсть, Мудрость,
                        познающая каждую вещь в отдельности (Аналитическая мудрость), очищающая
                        санджня-скандху.
                    </li>
                    <li class="my-2">Север: Завистливый Ямандага, Амогхасиддхи, зависть, Творческая
                        мудрость, исправляющая карму и очищающая санскара-скандху.
                    </li>
                </ul>
            </li>
            <li>
                <ul class="pl-2 space-y-1">
                    <li class="my-2">Центр: Энергичный Великий Ваджрабхайрава, Акшобхья, гнев,
                        Мудрость дхармового пространства (Интуиция, познающая шунью), очищающая
                        виджняна-скандху, акаша, осязание.
                    </li>
                    <li class="my-2">Восток (внешние двери): Молоток-Ямандага, Акшобхья, уничтожает
                        всех пятерых скандховых мар, сила веры.
                    </li>
                    <li class="my-2">Юг (внешние двери): Дубина-Ямандага, Вайрочана, уничтожает всех
                        пятерых клешевых мар, сила терпения.
                    </li>
                    <li class="my-2">Запад (внешние двери): Лотосный Ямандага, Амитабха, уничтожает
                        Мару-хозяина смерти, сила памяти (сознания).
                    </li>
                    <li class="my-2">Север (внешние двери): Меч-Ямандага, Амогхасиддхи, уничтожает
                        черных мар, мешающих созерцанию, и Мару-сына небожителя, сила самадхи.
                    </li>
                </ul>
            </li>
            <li>
                <ul class="pl-2 space-y-1">
                    <li class="my-2">Юго-восток: Зазигагармо, Вайрочана, милосердие (кротость),
                        земля, зрение.
                    </li>
                    <li class="my-2">Юго-запад: Пхагма, Ваджрадхара, сострадание, вода, слух.</li>
                    <li class="my-2">Северо-запад: Янжинма, Амитабха, радость, огонь, обоняние.</li>
                    <li class="my-2">Северо-восток: Гоорижанку, Амогхасиддхи, спокойствие, воздух,
                        вкус.
                    </li>
                </ul>
            </li>
        </ul>

        <p>
            Из приведенной структуры мандалы Ямандаги двух периодов – Самбхогакаи и Нирманакаи,
            видно, что махабхути неоднократно связаны с различными аспектами мандалы:
        </p>

        <ul class="prose list-disc pl-2 space-y-1 ml-2 mb-4">
            <li class="my-2">Внешнее окружение дворца: четыре махабхути (земля, вода, огонь,
                воздух).
            </li>
            <li class="my-2">Четыре женских Ямандаги промежуточных направлений (Зазигагармо и др.):
                четыре махабхути как восьмая база внешнего проявления формы (составная часть
                рупа-скандхи), поставленные в соответствие с четырьмя базами воспринимаемых объектов
                (видимое, слышимое, обоняемое, вкушаемое).
            </li>
            <li class="my-2">Центр мандалы: (акаша) локализована с базой осязаемого и
                виджняна-скандхой.
            </li>
            <li class="my-2">Центр мандалы: четыре махабхути, входящие в осязаемое, подгруппу без
                эмоций (т. н. утробное состояние).
            </li>
            <li class="my-2">Внутренние двери дворца и центр: комплекс махабхути, связанный со всеми
                пятью скандхами, как это было рассмотрено в аспекте Абхидхармы.
            </li>
            <li class="my-2">Восточные внешние двери: махабхути в связи с пятью скандхами.</li>
            <li class="my-2">Южные внешние двери: махабхути в связи с клешами.</li>
        </ul>

        <p>
            Группировка махабхути в Абхидхарме наличествует и в структуре мандалы Ямандаги. Причем
            махабхути садханы периода Самбхогакаи (постройка дворца) перечислены в обратном
            Абхидхарме порядке – воздух, огонь, вода и земля, а в процессе их очищения (период
            Нирманакаи) их порядок совпадает с абхидхармическим – земля, вода, огонь, воздух.
        </p>
        <p>
            Неявно соотнесен со структурой мандалы элемент авиджняпти. Поскольку он связан с кармой
            личности, то его можно поставить в соответствие с Завистливым Ямандагой, уничтожающим
            зависть и дающим йогину мудрость творческую, исправляющую карму.
        </p>
        <p>
            Структура мандалы проясняет вопрос о связи махабхути с индриями (внутреннее проявление
            рупа-скандхи):
        </p>

        <ul class="prose list-disc pl-2 space-y-1 ml-2 mb-4">
            <li class="my-2">Земля – зрение</li>
            <li class="my-2">Вода – слух</li>
            <li class="my-2">Огонь – обоняние</li>
            <li class="my-2">Воздух – вкус</li>
            <li class="my-2">Чувственное пространство (рупа-аятана) – осязание</li>
        </ul>

        <p>
            Эти пары соответствуют в мандале угловым женским Ямандагам. Чтение их мантр очищает
            соответствующую махабхути и меняет свойство связанного с ним индрия, лишая последний
            изменчивости и придавая каждому органу восприятия постоянное значение. (Йогин,
            укротивший, очистивший, например, махабхути огонь, обретает постоянство восприятия
            запаха.)
        </p>
        <p>
            Поскольку Тантра вообще, а Тантра класса ануттарайоги в особенности характеризуется
            неуходом от сансары и использует не обходные пути к нирване (аскеза, постепенность,
            архатство), а, напротив, предпочтение отдает кратким, лобовым методам, вступая в
            непосредственный контакт и борьбу с сансарой; то повышенное значение и подробная
            разработка темы махабхути закономерны.
        </p>
        <p>
            Действительно, внешнее проявление личности, группирующееся в рупа-скандхе, полностью
            базируется на четырех махабхути. Очищение четырех махабхути разрушает, изменяет
            рупа-скандху. При этом сансара, полная мучений, превращается в так называемую <em>светлую
                сансару</em> (<em>сансару</em> в аспекте тождества сансары и нирваны). Йогин
            обретает малые
            сиддхи и реализует принцип отсутствия индивидуального<em>Я</em>, вера в которое
            создается неведением с помощью рупа-скандхи.
        </p>
        <p>
            Абхидхарма помогает понять, какую роль махабхути играют в структуре личности, но
            остается в стороне вопрос о том, тождественны ли махабхути, входящие в разные
            абхидхармические группы. Тантра дает по сравнению с Абхидхармой более подробную и
            разработанную теорию махабхути.
        </p>
        <h4>Теория махабхути в работе Нацог-Рандола</h4>
        <p>
            Теория махабхути подробно описана в работе Нацог-Рандола (Лончена Рабжама) «Карнатантра
            – опора Великого Учения, называемая “Зерцало механизма глубокого содержания”» (5, л.
            1а-38а).
        </p>
        <p>
            Описывая период до разделения сансары и нирваны, Нацог-Рандол пишет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Во времена, когда еще не прояснился Будда в результате познания, не
            стал он известен через тело и мудрость,
            время давно прошедшее, когда не было ничего, тогда вишая своим
            содержанием была абсолютно очищенной шуньей в
            дхармовом пространстве, не была загрязнена ни тонкими, ни грубыми
            махабхути. Была чиста и без грязи, как
            зеркало, очищенное от грязи (ржавчины), и стала основой появления
            всего, как ясная шунья, беспрепятственный,
            безначальный ум, наполненный теми пятью качествами, как светильник
            внутри кувшина. Просветленный внутри
            пространства воздух мудрости пришел в движение. Воздух
            трансформировался в трансцендентальном центре
            непрерывной актуальности дхармовой сущности. Здесь образовалась
            точка расхождения сансары и нирваны (5, л. 2а).
        </blockquote>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Итак, совершенный дух (нирвана и Адибудда), отделяясь и уходя от непросветленного
            вследствие необходимого творчества, создал движение, которое является одним из основных
            моментов созидания.
        </blockquote>

        <p>
            Движение (воздух) стоит в начале бытия, и перечень махабхути не случайно начинается с
            элемента воздух.
        </p>

        <p>
            Тантрийская теория махабхути подтверждает вывод О. О. Розенберга об обусловленности ими
            рупа-скандхи. Но в отличие от Абхидхармы речь идет не об одной рупа-скандхе, а конкретно
            обо всех скандхах, в связь с которыми поставлены так называемые <em>Пять Великих
                Махабхути Мудрости</em>.
        </p>
        <h4>Интенсификации</h4>
        <p>
            В теории махабхути Тантра пользуется специальными терминами. Рассмотрим термин <em>интенсификация</em>.
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Если спросить, что бывает внутри пространства, то нужно знать, что там есть реальность
            разума, и имеет она беспрепятственное развитие в шунье. Это есть интенсификация (разума)
            (5, л. 256).
        </blockquote>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            От одной Дхармакаи проявляются многие, которые развиваются по правилам интенсификации
            разума. Об этом проповедовал Учитель.
        </blockquote>
        <p>
            Дифференциацию, или дальнейшее развитие, Дхармакаи Нацог-Рандол излагает так:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            От интенсификации Разума вырастают Пять Мудростей, их интенсификация суть Пять
            Светов, а от них появились Пять Махабхути (от света появилась материя – <strong>Б.
                Д.</strong>),
            от интенсификации Пяти Махабхути вышло пространство и превратилось в форму. От
            интенсификации последней появилось разнообразие в природе, цвет и т.д. От бинду
            (спермы) Пяти Светов вышли Пять Махабхути в виде интенсификации (или в виде динамики их
            существования). От последней появились скандха, дхату, аятана и
            другие (5, л. 26а).
        </blockquote>

        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            От бинду Пяти Светов, от четырех махабхути вместе с небом (пространством) возникли
            пристрастия пяти скандх (5, л. 326).
        </blockquote>
        <p>
            Повторяя смысл сказанного, Нацог-Рандол пишет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Во всех предписаниях проповеданных тантр указано об интенсификации,
            или динамике существования. Я об этом
            говорю следующее: интенсификация разума суть Пять Мудростей, их
            интенсификация суть Пять Светов. Динамикой
            существования Пяти Светов являются Пять Махабхути. Интенсификация
            Пяти Махабхути – это то, что мы видим
            (феноменальный мир). Например, динамикой существования неба являются
            облака и воздух, простор и
            распахнутость. Также в динамике существования воздуха имеются
            компоненты, как то: свистящие звуки, легкость
            и движение. В динамике существования воды имеются компоненты
            (свойства) в виде звуков шуршания (звуки
            течения воды), мокрота и соединяющее свойство. От интенсификации
            земли появляются всякие формы и свойства
            твердости и сухости. От интенсификации огня появляются жар и
            завершающий процесс через сжигание. От
            собственной интенсификации концентрированных махабхути появляются
            плодоносные деревья, леса (джунгли),
            лекарственные и благовонные травы, цветы, корнеплоды. Энергия Пяти
            Светов, объективизируясь, проявляется
            вовне, а динамика существования бинду проявляется внутри.
            (Бинду-сперма – белая, желтая, зеленая, синяя и
            красная). От белой спермы возникает мясо и кровь, от желтой –
            создаются волосы на теле, красная проявляется
            как тепловая энергия тела, зеленая становится дыханием, от синей –
            элементы и жидкости в суставах. Форма
            (рупа-скандха) проявляется таким образом. Это есть проявление ее в
            своей интенсификации. Проявление пяти
            скандх и пяти ядов осуществляется через индрии (тиб. <em>dbang po</em>),
            опора индрии (тиб. <em>dbang rten</em>) –
            вишая, виджняны. Подобную возможность проявления называли энергией,
            или силой. То, что подобным образом
            проявляется, есть махабхути (5, л. 26а–26б).
        </blockquote>
        <p>
            Итак, налицо следующий ряд последовательной дифференциации Дхармакаи
            в сансару, который мы представим в виде
            схемы:
        </p>
        <!-- Схема 1: Процесс Интенсификации от Дхармакаи до Сансары -->
        <div class="diagram-container">
            <img src="/images/articles/montlevich/buddology/dharmakaya_sansara.svg"
