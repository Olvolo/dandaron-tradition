        <h4>
            Пространства и формы образуются из махабхути.
        </h4>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Возможность проявления (света) называется <strong>энергией</strong>, или
            <strong>силой</strong>. То, что таким образом проявляется, есть махабхути.
        </blockquote>
        <p>
            Следовательно, <em>пять кувшинов Света</em>, трактуемых Дандароном
            как <em>кривизна Света</em>, есть та <em>энергия</em>,
            или <em>сила</em>, которая образует из Пяти Светов Пять Махабхути. В
            современной физике ей соответствует, очевидно, гравитация.
        </p>
        <p>
            Пространство данной схемы дифференциации Дхармакаи не есть акаша и не тождественно
            <em>чувственному пространству</em> рупа-скандхи. Если чувственное пространство
            условно можно считать вторичным по
            отношению к рупа-дхармам, то данное пространство (тиб. <em>snod</em>)
            предшествует формам. Если чувственное пространство аналогично
            пространству классической физики и к нему применимы законы
            эвклидовой геометрии, то данное пространство как
            результат <em>кривизны света</em>, благодаря которой <em>свет</em>
            стал вместилищем, пространством (сосудом),
            более похоже на пространство общей теории относительности с
            соответствующей ей неэвклидовой геометрией. Это
            пространство-сосуд напоминает пространство замкнутой, конечной
            вселенной современных космогонических теорий.
            Это – то пространство, имея в виду которое Эйнштейн говорил, что нет
            материи, а лишь существует кривизна
            пространства. В нашем контексте <em>свет</em> реализует свою <em>кривизну</em>
            в пространство через махабхути.
        </p>
        <p>
            Не углубляясь в проблему пространства, отметим, что нами уже выявлено в буддийской
            теории три типа
            пространства: <em>акаша</em>, <em>пространство-сосуд</em> и <em>чувственное
                пространство</em>.
        </p>
        <h4>
            Дифференциация Дхармакаи
        </h4>
        <p>
            Вернемся к процессу дифференциации Дхармакаи. Махабхути, реализованные от Пяти Светов,
            называются
            <em>внешними</em>, от бинду – <em>внутренними</em>. Разобраться во
            всех разновидностях типов махабхути помогает глава книги
            Нацог-Рандола, описывающая их свойства и дающая подробную классификацию:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            <p class="my-4">
                Если проповедовать по отраслям учение о махабхути, то имеются семь:
            </p>
            <ol class="prose list-decimal list-outside ml-2 mb-4 space-y-1 pl-2">
                <li class="my-2">реальность;</li>
                <li class="my-2">подлинные слова;</li>
                <li class="my-2">различие;</li>
                <li class="my-2">иллюстрирующие примеры;</li>
                <li class="my-2">свойства;</li>
                <li class="my-2">закон роста;</li>
                <li class="my-2">закон увядания.</li>
            </ol>

            <ul class="prose list-none pl-2 space-y-1 my-4 ml-2">
                <li class="my-2">Реальность махабхути суть то, что беспрерывно проявляется динамика
                    появления сансары и нирваны от основ существования.
                </li>
                <li class="my-2">Реальности <strong>чистого (махабхути)</strong> проявляются в
                    непрерывных
                    актуальностях самих Пяти Мудростей.
                </li>
                <li class="my-2">Реальности <strong>осадочных (махабхути)</strong> проявляются в
                    виде земли, воды,
                    огня, воздуха и неба (эфира).
                </li>
                <li class="my-2">Подлинные слова определяются так: поскольку беспрерывно появляется
                    радость (творчество) от актуальности, это и есть <strong>махабхути</strong>.
                </li>
                <li class="my-2">От внутреннего очищения собственного проявления мудрости появляется
                    чистота (махабхути), но неожиданное появление (махабхути) через заблуждение
                    пристрастия есть <strong>осадочные махабхути</strong>.
                </li>
            </ul>

            <p class="my-2">Если различить их, то их четыре:</p>

            <ul class="prose list-disc pl-2 space-y-1 ml-2">
                <li class="my-2"><strong>чистые</strong> (прозрачные, тиб. <em>dwangs ma</em>);</li>
                <li class="my-2"><strong>осадочные</strong> (тиб. <em>snyigs ma</em>);</li>
                <li class="my-2"><strong>осадок чистоты</strong> (тиб. <em>dwangs ma'i snyigs
                        pa</em>);
                </li>
                <li class="my-2"><strong>чистота осадка</strong> (тиб. <em>snyigs ma'i dwangs
                        ma</em>);
                </li>
            </ul>
            <p class="my-4">
                <strong>Чистые</strong>, или <strong>прозрачные</strong>, суть
                трансцендентно-имманентные Пять Великих Махабхути: они суть
            </p>
            <ul class="prose list-none pl-2 space-y-1 my-4 ml-2">
                <li class="my-2">не твердая земля,</li>
                <li class="my-2">не мокрая вода,</li>
                <li class="my-2">не горячий огонь,</li>
                <li class="my-2">воздух без колебаний и</li>
                <li class="my-2">эфир (небо) с нераспространяющейся актуальностью.</li>
            </ul>
            <ul class="prose list-none pl-2 space-y-1 my-4 ml-2">
                <li class="my-2">Деятельности, совершающие кармовые результаты, становятся
                    источником появления божественного тела мудрости, поэтому это есть <strong>тайные
                        махабхути</strong> абсолютно очищенной мудрости.
                </li>
                <li class="my-2">Осадки в большинстве находятся вместе с умом и познанием
                    (тиб. <em>rig shes</em>).
                </li>
                <li class="my-2">Твердая земля, мокрая вода, горячий огонь, колеблющийся воздух,
                    разлитое небо и внутренние махабхуты становятся опорами тела.
                </li>
                <li class="my-2"><strong>Осадок чистоты</strong> в виде мяса и кости есть земля.
                    Кровь, жидкость в
                    суставах (тиб. <em>chu ser</em>) и сперма есть вода. Теплота есть огонь,
                    дыхание – воздух, внутреннее пустое пространство есть махабхути небо
                    (пространство).
                </li>
                <li class="my-2">В <strong>чистоту</strong> (прозрачность) осадка входят осадки
                    внешних махабхути,
                    формы, которые проявляются в пяти элементах – земле, воде, огне, воздухе и небе
                    (пространстве).
                </li>
                <li class="my-2">Прозрачности же их являются пятью цветами радуги. Собрав все
                    вместе,
                    можно дать такой олицетворяющий пример <strong>чистоты осадка: видимость
                        солнечного света</strong> или <strong>от хрусталя исходит свет</strong> (5,
                    л. 366).
                </li>
            </ul>

            <p class="my-4">О <strong>внутренних</strong> свойствах (махабхути). Если различить, то
                внутренних свойств два:<br>
                свойство Пяти Великих Махабхути мудрости заключается в том, что, имея пять цветов
                разума своей актуальностью, держит тело (будды) и сущность просветления и<br>
                становится основой непоколебимого.<br>
                Если различить, то</p>
            <ul class="prose list-none pl-2 space-y-1 ml-2 mb-4">
                <li class="my-2">не будучи твердым – крепкий,</li>
                <li class="my-2">не будучи мокрым – собирающий,</li>
                <li class="my-2">не имея колебания – движется,</li>
                <li class="my-2">не будучи горячим – может сжечь и имеет не распространяющуюся
                    актуальность.
                </li>
            </ul>
            <p>
                Великие Махабхути Пяти Мудростей суть:
            </p>
            <ul class="prose list-none pl-2 space-y-1 ml-2 mb-4">
                <li class="my-2">Земля – Будда-Лочана (санскр. <em>Buddha-Locanā</em>, тиб. <em>Sangs
                        rgyas spyan</em>)
                </li>
                <li class="my-2">Вода – Мамаки (санскр. <em>Māmakī</em>, тиб. <em>Ma ma ki</em>)
                </li>
                <li class="my-2">Огонь – Пандари (санскр. <em>Pāṇḍarā</em>, тиб. <em>Gos dkar
                        mo</em>)
                </li>
                <li class="my-2">Воздух – Тара (санскр. <em>Tārā</em>, тиб. <em>sGrol ma</em>)</li>
                <li class="my-2">Небо – Дхату-Швари (санскр. <em>Dhātvīśvarī</em>, тиб. <em>dByings
                        phyug ma</em>)
                </li>
            </ul>
            <p>
                Эти пять матерей сидели в очищенном вместилище вместе со всеми буддами, входящими в
                пять генезисов. От их лучезарной формы непрерывной актуальности проявились пять
                осадков, они суть формы непрерывной актуальности пяти матерей, и они выросли там от
                основы. В пространстве, составленном из абсолютного очищения пяти матерей, вмещаются
                все дхармы <strong>светлой сансары</strong>, или <strong>сансары и нирваны</strong>.
                Поэтому всю <strong>светлую сансару</strong> называют вместилищем матери –<strong>Бхага</strong>
                (санскр. <em>bhaga</em>). Свойство пяти осадочных махабхути суть то, что они
                проявляются как опора для выхода единичного, отличного от света, формы-изображения.
                Если различить другие дхармы, находящиеся в пространстве махабхути, то они
                следующие: твердый, крепкий, жидкий, горячий, колеблющийся и просторный, почему и
                открывает отверстие. Махабхути сосуда и содержания (тиб. <em>snod bcud</em>)
                становятся как махабхути сна.
            </p>
            <p>
                Если взять пять чистых, то там, где сущность <strong>пяти светов</strong> осваивает сердце,
                там же
                они и теряются в основе своей, это похоже на <strong>свет внутри хрусталя</strong>. От (этого света)
                свет актуальности формы-изображения приходит в движение при помощи ветра мудрости
                (тиб. <em>ye shes rlung</em>) и, таким образом, вырастает сам. Если сказать порядок
                выхода осадка, то от света-формы-изображения выросли пять осадков:
            </p>
            <ul class="prose list-disc pl-2 space-y-1 ml-2 mb-4">
                <li class="my-2">От белого света нетвердой земли вышла твердая земля (5, л. 37а).</li>
                <li class="my-2">От желтого света немокрой воды вышла мокрая вода.</li>
                <li class="my-2">От красного света негорячего огня вышел горячий огонь.</li>
                <li class="my-2">От зеленого света неподвижного воздуха вышел движущийся воздух.</li>
                <li class="my-2">От синего света неразлитого (нерасходящегося) неба выросло
                    расходящееся и открывающееся отверстие неба.</li>
            </ul>
            <p>
                Способы освобождения заключаются в том, что освобождаются
                (махабхути) от того пространства, от которого
                вышли. Это потому, что махабхути являются необходимой основой,
                фундаментом (всего объективного). Здесь
                освобождаются пять махабхути от пространства формы-изображения.
                В тот период, когда просветляется <strong>светлая
                    сансара</strong> (тиб. <em>snang srid</em>, вместилище), тогда угасают
                ошибочные проявления. Пять светов
                формы-изображения, исконно блестящих (чистых), входят и
                растворяются каждый в своем пространстве, через это
                Ясный Свет, просветляясь, проявляется. Это есть время, когда
                разум занял свое место и показал в
                трансцендентно-имманентном дупле драгоценности собственную
                основу и фундамент. Если исследовать в этом
                направлении, то великие махабхуты – победоносные – присущи
                каждому живому по своей сущности. Поскольку в них
                концентрирован свет-направление, то от них возникли четыре
                промежуточных света-направления: и верхние, и
                нижние (возникновение четырехмерного пространства). От цвета не
                реального, но особенного, проявляется
                отлично от других основа размышления и так же она изменяется.
                “То, что пыль (мельчайшие частицы) не имеет
                движения, есть самая вершина всех пяти махабхути”. Так сказано.
            </p>
            <p>
                Так проповедано в “Созидающем Всё” <strong>(Kun byed)</strong>. Расширенное
                пояснение к этому дано в “Освещении Всего”:
            </p>
            <blockquote
                class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
                Я покажу реальность подлинных слов, различие махабхути вместе с
                примерами. Но ты задержи их в памяти! О
                реальности нужно рассказать с точки зрения различий, но она есть
                чистота и осадок махабхути. Реальности
                чистого есть пять мудростей. Реальность осадка суть (махабхути)
                земля, вода, огонь, воздух и неба – эти
                пять. Подлинные слова махабхути выявляются как динамика
                существования разума, от динамики существования
                возникают сансара и нирвана, поэтому и есть махабхути. Если
                различить, то получится тетралемма <strong>(тиб. <em>mu bzhi</em>):</strong> чистота
                (ясность), осадок, осадок чистоты, чистота осадка — эти четыре. О, Бодхисаттва,
                воспринимай хорошо! Чистота махабхути суть пять мудростей, а
                осадок суть земля, вода, огонь и воздух. Осадок
                чистоты суть кости, мясо и прочее. Чистота осадка есть радуга на
                небе. Примером служат лучезарность
                солнечного света или подобно тому, как от хрусталя исходит луч».
            </blockquote>
            <p>
                Так сказано.
            </p>
            <blockquote
                class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
                Слушай, держатель ваджра! Свойство земли есть твердость,
                свойство воды – мокрота, свойство огня –
                горячесть, свойство воздуха – колебание, свойство неба –
                просторность. Они производят действия на основе
                этих же свойств. От чистого элемента <strong>земля</strong> выходит белый свет, а
                от него проявляется осадочная земля. Так,
                от чистых махабхути выходят четыре вида светов; они же
                проявляются в виде четырех осадков. Свойства чистых
                махабхути суть (земля), будучи нетвердой, – крепкая; (вода),
                будучи немокрой, – собирающая; (воздух), не
                имея
                колебаний, движется; (<strong>огонь</strong>), будучи негорячим, может жечь;
                (небо) не распространяется, но имеет
                актуальность. Сущности дхармы и Дхармакаи неизменны: элемент
                <strong>земля</strong> не твердый, но крепкий. Сущности дхармы и
                Дхармакаи не имеют различия – они очищены, прозрачны и не имеют
                грязи: будучи не мокрая, но собирающая, <strong>вода</strong>
                есть неразличимость сущности дхармы и Дхармакаи. <strong>Воздух</strong> без
                колебаний, но безостановочный. То, что воздух,
                не имея колебаний, движется, означает: приходит туда, куда не
                шел. Поскольку нет двойственности между
                сущностями дхармы и Дхармакаи, то мудрости исходят от
                пространства собственной реальности. Поскольку не
                концентрированы абсолютные сомнения, проявляющиеся для других,
                то существует не горячий, но обжигающий
                <strong>огонь</strong>. То, что просветлилось, хотя не показывается другим,
                означает, что распространяющаяся мудрость вместе
                с актуальностью имеет непрерывную актуальность, не
                распространяющуюся по всему <strong>небу</strong>. Пять махабхути осадка
                спасаются в <strong>пяти светах</strong>. <strong>Пять светов</strong> растворяются во всех порах
                чистого. Хотя так они не определяются, но все это делают по порядку».
            </blockquote>
            <p>
                Так проповедано.
            </p>
            <p>
                Здесь от пяти махабхути чистоты вырастают их непрерывные актуальности, и последние заблудились, превращаясь в осадки (которые дали развитие мира в течение трех мировых периодов – кальп) (5, л. 36а-38а).
            </p>
        </blockquote>

        <p>
            Буддизм не проводит анализа объективного или субъективного по отдельности, он анализирует мир в целом. Так, опора тела состоит из осадочных, т.е. внешних и внутренних махабхути; таким образом, обе линии дифференциации Дхармакаи в аспекте сосуда и содержания разделены лишь в теории. В реальности они неразрывны.
        </p>
        <p>
            Цитированная выше глава Нацог-Рандола о махабхути подробно объясняет, как “ошиблись” свет и бинду; это есть классификация махабхути по четырем типам:
        </p>
        <p>
            <span class="underline"><strong>Четыре типа махабхути</strong></span>: чистые, осадочные, осадок чистоты, чистота осадка.
        </p>
        <ol class="prose list-upper-roman list-inside pl-8 text-gray-700">
            <li class="my-2"><strong>Чистые махабхути</strong> – трансцендентно-имманентные Пять Великих Махабхути:
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Нетвердая земля;</li>
                    <li class="my-2">Немокрая вода;</li>
                    <li class="my-2">Негорячий огонь;</li>
                    <li class="my-2">Воздух без колебаний;</li>
                    <li class="my-2">Небо без распространенности.</li>
                </ol>
                <p class="my-2">Это есть <em>тайные махабхути</em> абсолютно очищенной мудрости.</p>
            </li>
            <li class="my-2"><strong>Осадочные махабхути</strong> (или внешние):
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Твердая земля;</li>
                    <li class="my-2">Мокрая вода;</li>
                    <li class="my-2">Горячий огон;ь</li>
                    <li class="my-2">Колеблющийся воздух;</li>
                    <li class="my-2">Распахнутое небо.</li>
                </ol>
            </li>
            <li class="my-2"><strong>Осадок чистоты</strong> (внутренние махабхути):
                <ol class="list-decimal list-inside pl-6 space-y-1">
                    <li class="my-2">Осадок чистоты в виде мяса и костей есть земля;</li>
                    <li class="my-2">Осадок чистоты в виде крови и жидкости в суставах есть вода;</li>
                    <li class="my-2">Осадок чистоты в виде теплоты есть огонь;</li>
                    <li class="my-2">Осадок чистоты в виде дыхания есть возду;х</li>
                    <li class="my-2">Осадок чистоты в виде внутреннего пустого пространства есть небо.
                    </li>
                </ol>
                <p class="my-2">Осадочные махабхути и осадок чистоты (собственно внешние и
                    внутренние махабхути) становятся опорами тела.</p>
            </li>
            <li class="my-2"><strong>Чистота осадка</strong>. В чистоту осадка входят остатки
                внешних махабхути, формы которых проявляются в пяти элементах – земле, воде, огне,
                воздухе и пространстве: это и есть прозрачность осадка – пять цветов радуги.
            </li>
        </ol>
