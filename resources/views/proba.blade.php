        <h2 class="text-center text-gray-800 font-bold text-xl my-4">Введение</h2>
        <p>Гунчен-Чжамьян-Шадпа, известный в тибетской научной традиции под именем Агван-Цзондуй,
            был выдающимся философом периода после реформации Цзонхавы. Впоследствии, по получении
            ламаистского ученого звания, он стал называться Шадпа-Дорчже (по-санскритски — <em>Маньчжу-Гхоша-Хаса-Ваджра</em>,
            тиб. транслит <em>manydzu gho sha ha sa badzra</em>)<a href="#citation-1" id="ref-1"
                                                                   class="citation-mark"><sup>1</sup></a>.
        </p>
        <p>Гунчен родился в 1649 году в Амдо. Отца его звали Хадал (тиб. <em>kha 'dal</em>)<a
                href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>, мать —
            Тарможам (тиб. <em>thar mo byams</em>). По свидетельству биографа Гунчена, его родители
            были трудолюбивыми людьми, отличались добродушием и благородством.</p>
        <p>Тринадцати лет (1662) Гунчен был отдан в ученики к ламе Ешей-Чжамцо, у которого он усвоил
            элементарную грамоту. Шестнадцати лет он начал изучать сочинения йогов, а
            восемнадцати-двадцати лет (1667–1669) учился у ламы Чжалсарая-Римпочэ, занимаясь наряду
            с философией йогов также и медициной. В тот же период он занимался изучением
            санскритского языка.</p>
        <p>В 1669 г. Гунчен уехал в Центральный Тибет, предпринял путешествие в знаменитые монастыри
            Брайбун и Сэра, восторгался ученостью лам и красотою убранства этих монастырей. В
            следующем году он становится учеником гэбши Лодой-Чжамцо, поступает на факультет
            философии Цанит, углубляет свои познания в области философии йогов.</p>
        <p>К этому периоду относится знакомство Гунчена со знаменитым пятым далай-ламой
            Агван-Лобсан-Чжамцо (1617–1682), который оказал большое влияние на окончательное
            формирование его философского мировоззрения. В это же время он занимается у философа
            Сультима по различным вопросам философии йогов. В 1671 г. изучает
            «Праджня-парамиту».</p>
        <p>Затем в течение трех лет (1671–1674) Гунчен изучал основные философские разделы буддизма:
            «Праджня-Парамита», «Абхидхармакоша», «Виная», «Хинаяна» и «Махаяна», а также и «Большой
            Ламрим».</p>
        <p>В двадцать пять лет (1674) Гунчен сдал экзамены на степень <em>габчжи</em>, после чего
            изучал философию «Виная» под руководством Баранза-Дана.</p>
        <p>В 1674–1678 гг. Гунчен совершенствовал свои познания в области философии йогов под
            руководством Лодой-Чжамцо и Чжамцо-Лобсан-Чойдона, изучал теорию разума по «Абхидхарме»
            и «Логике» Дигнаги и Дхармакирти.</p>
        <p>В 1679 году он изучает теорию <em>шуньи</em> мадхьямиков.</p>
        <p>Знаменательным событием в жизни Гунчена явилось его участие в известной философской
            дискуссии, в которой он вышел победителем и тем самым закрепил за собою имя крупного
            буддийского ученого-философа<a href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>.
            Неслучайно после этого (в 1680 г.) учеником Гунчена по тантрийской мистике становится
            один из важных тибетских князей.</p>
        <p>Тридцати двух лет (1681 г.) в монастырях Галдан и Брайбун Гунчен изучал тантрийские
            учения.</p>
        <p>Важным моментом в научной деятельности Гунчена явились 1682–1702 гг., в течение которых
            он ведет суровую отшельническую жизнь в пещере одной из тибетских гор. Здесь он пытается
            проникнуть в тайны тантрийской мистики и разных систем буддийской философии.
            Отшельническая жизнь положительно сказалась на творческой деятельности философа. В это
            время он начинает писать свои выдающиеся сочинения. Растет популярность Гунчена как
            ученого-философа, у которого еще в 1683 г. многие ламы получают разъяснения по наиболее
            трудным разделам «Большого Ламрима». В 1684 г. по рекомендации своего учителя
            Лодой-Чжамцо Гунчен посетил монастырь Галдан, где читал лекции по теории <em>праджня-парамиты</em>
            и мадхьямики. В следующем, 1685 г., Гунчен был приглашен для консультаций в главную
            резиденцию далай-ламы VI (1683–1706) Потала по вопросам философии и религиозной мистики
            буддизма.</p>
        <p>Первые ученые труды Гунчена вышли в 1686 г. (они вошли в первый том описываемого нами
            собрания сочинений).</p>
        <p>На тридцать девятом году жизни (1688) Гунчен закончил комментарии к «Большому Ламриму», а
            через три года (1691) начал писать свою знаменитую «историю философии»<a
                href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>.</p>
        <p>В 1689 году Гунчен получил княжеский титул, установил личный контакт с правителем Тибета
            времен смуты при далай-ламе VI Санчжай-Чжамцо<a href="#citation-5" id="ref-5"
                                                            class="citation-mark"><sup>5</sup></a>,
            был приглашен строить монастырь у себя на родине, в Амдо. В связи с этим значительно
            возрос авторитет Гунчена как ученого и духовного лица.</p>
        <p>В 1702 г. Гунчен написал известное сочинение «Цадма Намдал»<a href="#citation-6"
                                                                         id="ref-6"
                                                                         class="citation-mark"><sup>6</sup></a>
            по весьма своеобразному заказу. Дэсрид Санчжай-Чжамцо, рассчитывая на неудачу Гунчена и
            желая тем самым сконфузить его, предложил ему написать труд по логике. Однако, вопреки
            ожиданию грозного правителя, ученый блестяще справился со своей задачей, создав
            фундаментальный труд в данной области.</p>
        <p>Уже в возрасте пятидесяти трех лет Гунчен отказывается от отшельнического образа жизни и
            избирается хамбо-ламой в монастыре Гоман. Здесь он проповедовал пять разделов буддизма
            («Виная», «Праджня-Парамита», «Абхидхарма», «Хинаяна», «Махаяна»), а также «Логику»
            («Цадма Намдал»).</p>
        <p>Чжамьян-Шадпа выступал за укрепление контактов между местными лхасскими властями и
            центральным пекинским правительством. Когда далай-лама вознамерился поехать в Китай,
            ламы трех крупных монастырей Тибета выступили против этой поездки. Гунчен же поддерживал
            намерение далай-ламы посетить Пекин, за что его стали обвинять в прокитайской
            политике.</p>
        <p>В 1710 году Гунчен написал сочинение по теории <em>праджня-парамиты</em>, являющееся
            одним из самых крупных его научных трудов. В 1711 году он основал монастырь
            Лавран-Даши-Ешил. В этом же году он приехал на родину в Амдо, где был встречен весьма
            торжественно.</p>
        <p>Шестидесяти трех лет (1712) он получил почетное имя Гоман-Эрдэни-Номун-Хан.</p>
        <p>В 1723 году, на семьдесят четвертом году жизни, Гунчен-Чжамьян-Шадпа умер.</p>

        <h2 class="text-center text-gray-800 font-bold text-xl my-4">(Философские взгляды)</h2>

        <p>Жизнь и деятельность Гунчена протекала в сложной политической обстановке, когда в
            последней четверти XVII — начале XVIII вв. в Тибете шла упорная и продолжительная борьба
            между религиозными сектами «красношапочников» (тиб. <em>кадампа</em>) и
            «желтошапочников» (тиб. <em>гэлугпа</em>). Они боролись за политическую и духовную
            власть не только в Тибете, но и в Монголии. Из этой борьбы победительницей вышла секта
            <em>гэлугпа</em>, основанная в XIV в. известным реформатором Цзонхавой. Она установила
            свое влияние в Центральном Тибете, Амдо и Монголии. Борьба между <em>кадампа</em> и <em>гэлугпа</em>
            в Тибете шла также по линии богословской и философской. Противники часто вступали в
            открытые диспуты со взаимной критикой религиозно-философских взглядов, что находило свое
            отражение в их сочинениях. В монастырских школах Лхасы длительное время господствовала
            идеология <em>кадампской</em> секты. Наиболее видными буддийскими учебными заведениями
            тогдашнего Тибета были школы монастырей Сэра (тиб. <em>se ra</em>) и Лосолинг (тиб. <em>blo
                mtsho gling</em>). На философских факультетах указанных монастырей были приняты
            уставы и учебники (тиб. <em>yig cha</em>) известных ученых лам <em>кадампской</em> секты
            из монастыря Сэра — Чжебцзун-Чойчжи-Чжалцана (1469–1546) и Панчен-Соднам-Дратпы
            (1448–1554). Сухой традиционный метод преподавания спекулятивной метафизики буддизма,
            формальное изучение логики не удовлетворяли последователей цзонхавинской секты <em>гэлугпа</em>.
            Одним из первых с критикой <em>кадампской</em> учебной программы, литературы и методики
            преподавания выступил Гунчен. Он высказал мысль, что логика и теория познания не
            являются самостоятельными дисциплинами, а в совокупности представляют метод, при помощи
            которого могут быть познаны разные философские истины. Кроме того, он создает
            классический труд по истории философии «Дубта-ченпо», который в монголо-бурятских
            монастырях считался одним из основных учебников по буддийской философии. Характерной
            особенностью данного труда Гунчена является то, что в нем подробно излагаются почти все
            философские системы древней Индии как буддийские, так и небуддийские. Историческое
            значение философского труда Гунчена «Дубта-ченпо» состоит в том, что он раскрывает
            историю идеологической борьбы в древней Индии вообще, историю борьбы буддийской
            метафизики с индийским материализмом «локаятиков» в частности. Значение этого труда
            возрастает еще и потому, что содержащийся в нем богатейший материал, собранный ученым
            большей частью из недошедших до нашего времени древнеиндийских сочинений на санскритском
            языке, почти полностью документирован.</p>
        <p>Гунчен был универсальным ученым. В 15-ти томах его собрания сочинений мы находим трактаты
            по языкознанию, логике, истории, истории философии, философии мадхьямиков, йогачаров,
            исследования по «Абхидхармакоше» и другие.</p>
        <p>Большую роль сыграл Гунчен в популяризации учения о логике индийских мыслителей Дигнаги и
            Дхармакирти в Тибете и его дальнейшем развитии, создав три замечательных учебника по
            логике и теории познания.</p>
        <ol class="list-decimal list-outside ml-8 space-y-1 pl-5">
            <li class="my-2">«Дуйчен» (тиб. <em>bsdus chen</em>) — «Практический учебник логики для
                разных возрастов».
            </li>
            <li class="my-2">«Дагриг» (тиб. <em>rtags rigs</em>) — «Краткий учебник логики».</li>
            <li class="my-2">«Лориг» (тиб. <em>blo rigs</em>) — «Краткий учебник по теории
                познания».
            </li>
        </ol>
        <p>Все три учебника входят в общую книгу по логике «Намдэл-тачжот» (тиб. <em>rnam 'grel
                mtha' dpyod</em>) — «Исследование предела логического умозаключения».</p>
        <p>В 1689–1690 гг., изучая «Праджня-парамиту» и «Ламрим-ченпо» Цзонхавы, Гунчен написал ряд
            трудов по данной проблеме.</p>
        <p>В 1692–1696 годах он пишет исследования по философской концепции школы йогачаров
            («виджнянавадинов»), основанной индийскими философами Асангой (тиб. <em>thogs med</em>),
            Васубандху и Дигнагой. При характеристике теории йогачаров Гунчен отходит от взглядов
            основателей данной школы под влиянием философских воззрений Буддапалиты, Джово-Атиши и
            Цзонхавы.</p>
        <p>Обладая огромной эрудицией, отлично зная санскритский язык, Гунчен разрабатывал самые
            сложные вопросы того времени.</p>

        <h3>Неизданный фрагмент из предисловия к «Описанию сочинений Гунчена
            Чжамьян-Шадпа-Дорчже»</h3>

        <p>Дхармы — это моменты (непрерывности) непрерывного. Сама непрерывность по Чжамьян-Шадпе
            всё же существует. Этим он отличает взгляды мадхьямики от виджнянавады. Повторяя и
            разъясняя выражение Нагарджуны, он утверждает: то, что непонятно — нереально, стало быть
            сознание (виджняна) — не реально, ибо мы не сможем представить о нём ничего
            определённого.</p>
        <p>Развивая далее теорию отношений: отношения непонятны. Стало быть вселенная, или мир
            опыта, — это только видимость. Это лишь комбинация (колебания) непонятных отношений.
            Материя и дух, пространство и время, причина и субстанция, движение и покой — все они
            подобны безосновательному сооружению видения, которое не оставляет после себя никаких
            следов, отсюда вывод: нет ничего реального — ни духовного, ни недуховного.</p>
        <p>Если весь мир опыта нереален, то познающее <em>Я</em> и познание нереальны — все они
            шунья.
            Мадхьямика: «Я (познающий), объект (познаваемое) и познание взаимозависимы. Реальность
            одного из них зависит от реальности двух других, и если одно из них ложно, то ложными
            должны быть и оба остальные (как ложно отцовство при ложности существования детей)».</p>
        <p>Подлинная реальность или нирвана непознаваема, ибо не имеет воспринимаемых свойств,
            запредельна времени и пространству и потому бесконечна. Бесконечность обладает
            положительным качеством как основным атрибутом нирваны.</p>
        <p>Сансара, или волнение дхарм, безначальна, ибо всякое проявление её вызвано предшествующей
            причиной, но она имеет конец, ибо не всякое действие имеет следствие.</p>
        <p>В каждом континууме потока дхарм имеются дхармы, являющиеся путем к покою, сама
            алая-виджняна, различающая суетность от несуетности и её спутники. Суетные элементы
            подавляются.</p>
        <p>«Факт зависимого происхождения есть шунья! Нет элементов (дхарм), которые не зависели бы
            от некоторого другого условия в отношении своего происхождения, поэтому нет вещей и
            явлений (собрания дхарм), которые не есть шунья!» Так как все вещи феноменального мира
            являются множеством дхарм, следующих одна за другой в непрерывной последовательности, то
            весь феноменальный мир есть шунья... Индивидуальное бытие человека... вся психическая
            жизнь... являются скоплением психических дхарм, поэтому в самом индивидууме нет места
            для индивидуального <em>Я</em>, стало быть это тоже шунья. Эмпирическая индивидуальная жизнь от
            рождения до смерти — один из бесконечных эпизодов на пути развития по восходящей...
            определённого комплекса дхарм, принимающего то одну, то другую форму на своём пути к
            нирване по законам двенадцатичленной формулы трансфигурации.</p>
