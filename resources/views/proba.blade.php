            <p>Учение Будды было раскрыто последователями в двух аспектах: философская сущность
                учения и учение о великих путях. Однако тантрийское учение объемлет оба эти
                направления, но способы достижения нирваны построены иначе.
            </p>
            <p>Тантрийское учение проникает в массу двумя порядками. Риддхический порядок — это
                такой порядок, который потенциально заложен в самом тантрийском учении. Как говорят
