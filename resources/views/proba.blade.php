                и Лалитаваджрой из страны дакинь. В тантризме Учитель, традиционно-непрерывно
                передающий тайну тантры, имеет особое значение. По этому поводу Цзонхава говорит:
            </p>
            <blockquote class="border-l-4 border-gray-500 pl-2 my-6 text-gray-800 text-lg">
                Солнечные лучи, какие бы сильные и горячие они ни были, без выпуклого стекла не
                дадут огня, так же учение Бхагавана без Учителя невозможно для освоения и практики.
            </blockquote>
            <p>
                Тантрийский Учитель для ученика является более, чем просто личностью, которой
                случилось стать наставником последнего. В определенном смысле Учитель — это взятая в
                целом личность ученика, призывающая его идти по пути тантрийского учения, хотя в то
                же время Учитель является отдельным историческим лицом. В диалоге Тилопы и Наропы<a
                    href="#citation-3" id="ref-3" class="citation-mark"><sup>3</sup></a>
                есть место, где Тилопа говорит следующее:
            </p>
            <blockquote class="border-l-4 border-gray-500 pl-2 my-6 text-gray-800 text-lg">
                Даже когда ты встретил меня в виде прокаженной женщины, мы не были порознь, мы были,
