                Именно вследствие различия в их кармах люди неодинаковы: некоторые долговечны,
                некоторые недолговечны, некоторые здоровы, некоторые болезненны.
            </blockquote>
            <p>История индивида начинается не тогда, когда он рождается, она осуществлялась уже в
                течение бесконечных рядов перерождений. Поэтому люди, равно как и все живые существа
                в сансаре, представляют собой временные звенья в длинной цепи причин и следствий,
                где нет ни одного звена, независимого от остальных. Таким образом, человек, который
                встречается в этой жизни с тантрийским Учителем и, получая от него посвящение,
