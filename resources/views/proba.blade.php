            Мудрости как синтезе в её отношении к пустому ригпа говорится как о мудрости,
            постигающей дхармовое пространство.</p>
        <p>Ригпа – первый, вычленяемый из синтеза, «намёк» на конкретное – ЗНА-/-jnya –,
