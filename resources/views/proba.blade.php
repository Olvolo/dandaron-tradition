            <p>Тантрийское учение преподавалось самим Бхагаваном Учителю или небесным девам
                (дакиням), от которых опять-таки приходило к Учителю. Как гласит легенда,
                тантрийское учение Гухьясамаджи было преподано Бхагаваном царю Индрабодхе, а система
