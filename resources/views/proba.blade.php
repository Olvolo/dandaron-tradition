        <h3 class="text-center text-gray-800 font-bold text-xl my-4">Том I. Разные сочинения</h3>

        <h4 class="text-gray-800 font-bold text-lg my-3">1. rJe btsun 'jam dbyangs bzhad pa'i rdo
            rje rnam thar bka' rtsom tshigs bcad ma/</h4>
        <p class="my-4 italic">Автобиография Чжебцзун-Чжамьян-Шадпа-Дорчже, составленная в стихах.</p>
        <p><strong>Содержание.</strong> Указываются место рождения, родители ученого, в каком
            возрасте, где и у какого ламы, по какому разделу буддизма учился и какие степени получал
            (7 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">2. Kun mkhyen 'jam dbyangs bzhad pa bla
            ma'i rnam thar dang/ bya ka lan ta ga'i dris lan la/</h4>
        <p class="my-4 italic">Биография ламы Гунчен-Чжамьян-Шадпы<a href="#citation-7" id="ref-7"
                                                 class="citation-mark"><sup>7</sup></a>.</p>
        <p><strong>Содержание.</strong> Повествование о жизни и деятельности Гунчена в Тибете и
            описание его счастливых сновидений и радостных предчувствий. По существу, не биография,
            а скорее легенда о событиях его жизни (4 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">3. rJe btsun tsong kha pa chen po'i mnal
            lam gyi mchan 'grel rdo rje'i tshig 'grol/</h4>
        <p class="my-4 italic">Толкование сна великого Цзонхавы и освобождение «алмазных» слов от связки.</p>
        <p><strong>Содержание.</strong> 3-го числа по-тибетски — зимнего последнего месяца,
            по-монгольски — в месяц быка, Гунчен увидел сон, будто бы два божества, Майдари и
            Манджушри, преподнесли ему полную чашу священной воды и сказали: «После великого Атиши
            ты появился через 310 лет, которого мы нашли», «...как ваджра Манджушри, один из высших
            богов из 17 божеств, ты становишься вершиной стихии мира, поэтому ты выполнишь надежду
            человечества». Эта книга написана в монастыре Олхай-Самдан-лин (3 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">4. Chos sde chen po dga' ldan gyi dkar
            chag</h4>
        <p class="my-4 italic">Гарчаг монастыря Чойдэ-ченпо Галдан<a href="#citation-8" id="ref-8"
                                                 class="citation-mark"><sup>8</sup></a>.</p>
        <p><strong>Содержание.</strong> Гарчаг имущества и книг монастыря Галдан (14 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">5. Bstan pa'i gsal byed chen po bod du rim
            gyis byung ba'i lo tshigs re mig tu bkod pa'i tshigs lung tshegs chung rtogs byed gser
            gyi nyi ma 'od zer bkra ba/</h4>
        <p class="my-4 italic">Таблица годов рождения тибетских деятелей религии, выполненная для того, чтобы легче
            ориентироваться во времени их жизни, называемая «Ясные лучи золотого солнца».</p>
        <p><strong>Содержание.</strong> Автор пишет о самом древнем периоде жизни человечества до
            появления понятия о боге и до рождения Будды. Он указывает, что Будда родился в Индии в
            то время, когда человек жил в среднем 100 лет. Описывая историю человечества, автор
            говорит о том, что ригданы (тиб. <em>rigs ldan</em>) — мифические цари — царствовали в
            сказочной стране Шамбала. Первый ригдан жил при жизни Будды, после него идут по порядку
            25 ригданов, каждый из которых будто бы царствовал по целых 100 лет. Автор довел историю
            до 19 ригдана по имени Намнон (тиб. <em>rnam mnon</em>) (11 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">6. Bstan rtsis re'u mig<a
                href="#citation-9" id="ref-9" class="citation-mark"><sup>9</sup></a></h4>
        <p class="my-4 italic">Хронологическая таблица.</p>
        <p><strong>Содержание.</strong> Сообщается, что после Будды через 403 года было принято
            исчисление года по шестидесятилетнему циклу Рабчжун (тиб. <em>rab byung</em>). Первый
            год первого Рабчжуна по-тибетски называется мэ-йой (тиб. <em>me yos</em>) — год
            огня-зайца. В таблице приводятся даты рождения и смерти ученых и деятелей религии с
            первого Рабчжуна до 29 года 12-го Рабчжуна, т.е. года дерева-овцы (тиб. <em>shing
                lug</em>) (26 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">7. Bla ma rnams la gsol 'debs tshul gyi
            skor/</h4>
        <p class="my-4 italic font-bold">Раздел, содержащий хвалебные гимны учителям.</p>
        <p><strong>Содержание.</strong> Воздается хвала 25 разным учителям автора. Среди них он
            особенно выделяет Ригдан-Чжилхор-Гунчжи-Дагпо-Чхабдаг-Лама-Гончока (тиб. <em>rigs dang
                dkyil 'khor kun gyi bdag po khyab bdag bla ma dkon mchog</em>) и ламу
            Агван-Лобсан-Чойдан-Балсанпо (тиб. <em>bla ma ngag dbang blo bzang chos ldan dpal bzang
                po</em>) (23 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">8. Sangs rgyas byang sems yi dam chos srung
            gi bstod tshogs/</h4>
        <p class="my-4 italic font-bold">Восхваления Будды, бодхисаттвы, идама и гениев-хранителей
            религии.</p>
        <p><strong>Содержание.</strong> Автор восхваляет Будду под девизом: «Благословение для
            свершения и ускорения всех дел и торжества победы над злым духом». Сочинение написано на
            юге Лхасы весною в месяце тигра (тиб. <em>stag zla</em>) (20 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">9. Grub mtha'i rnam par bzhag pa 'khrul
            spong gdong lnga'i sgra dbyangs kun mkhyen lam bzang gsal ba'i rin chen sgron me/</h4>
        <p class="my-4 italic font-bold">Общее учение по истории философии, называемое «Отзвук рычания льва, не допускающий
            ошибок, светильник из чинтамани, освещающий путь всезнания».</p>
        <p><strong>Содержание.</strong> Сочинение состоит из 13 частей. Первая часть, объемом в 2,5
            листа, посвящена общей истории философии. Следующие шесть частей, объемом в 7 листов,
            содержат описания шести основных философских школ брахманизма, а последние шесть частей
            — четырех основных буддийских философских школ: вайбхашиков, саутрантиков, йогачаров и
            мадхьямиков (27 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">10. Bstod shog snyan dngags kyi skor/</h4>
        <p class="my-4 italic font-bold">Раздел, содержащий приятные восхваления.</p>
        <p><strong>Содержание.</strong> В сочинении семь отдельных статей, первая из которых
            посвящается восхвалению далай-ламы, вторая — Лодой-Ринчен-Чжунгнэ и др. (10 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">11. Snyan dngags kyi tshogs bcad 'ga'
            zhig/</h4>
        <p class="my-4 italic font-bold">Некоторые стихотворные восхваления.</p>
        <p><strong>Содержание.</strong> В двух первых стихотворениях в сказочной форме автор
            повествует о своих учителях, а в следующих стихотворениях — воздает хвалу им (3 лл.).
        </p>

        <h4 class="text-gray-800 font-bold text-lg my-3">12. Snyan dngags gsal bar byed pa'i bstan
            bcos dbyangs can zhal lung nyi ma 'bum gyi 'od can/</h4>
        <p class="my-4 italic font-bold">Шастра, разъясняющая поэзию, непосредственная проповедь
            богини Чжанчжан (Сарасвати)<a href="#citation-10" id="ref-10"
                                 class="citation-mark"><sup>10</sup></a>, сто тысяч
            лучей солнца.</p>
        <p><strong>Содержание.</strong> Сочинение состоит из 25 отдельных частей, представляющих
            собою разные поучения. В последней — 25 части — автор выступает против 10 черных
            греховных деяний и излагает методику «великого простора для распространения познания»
            (30 лл.).</p>

        <h4 class="text-gray-800 font-bold text-lg my-3">13. 'Phrin yig gi rnam par bzhag pa blo
            gsal rna rgyan si ndhu ra'i 'phreng mdzes/</h4>
        <p class="my-4 italic font-bold">Общее учение о письмах, ушное украшение разумных, называемое «Прекрасные четки из
            драгоценностей синдхуры».</p>
        <p><strong>Содержание.</strong> В начале сочинения излагается история происхождения
            наставительных писем, затем рассказывается, как шел процесс передачи этих традиций в
            последующие времена. В разделе истории происхождения излагается методика составления
            подобных писем. Во втором разделе, посвященном настоящему периоду наставительных писем,
            имеется пять частей. В первой из них говорится о том, как надо передавать эти письма
            дальше, во второй — как получать их и отвечать на них, в третьей — методика передачи
            тайных писем, в четвертой — как и где (внутри или снаружи) ставить печати и знаки
            отличия, в пятой — что во всем этом процессе написания писем должны быть только хорошие
            наставления (14 лл.).</p>

