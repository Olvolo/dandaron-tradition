        <h3>Пять великих</h3>
        <p>
            Для тех, кто хочет разобраться в сложном здании буддийской метафизики, кто желает
            распутать сложное переплетение символизма многочисленных мандал и необозримого
            разнообразия атрибутов, форм и цветовых соотношений, нужно ясно увидеть и запомнить
            стройное здание Абхидхармы – этой своеобразной “таблицы Менделеева” буддизма. И здесь
            стоит обратить внимание на особое положение пяти махабхути – этих удивительных
            первоэлементов, роль которых столь фундаментальна, что ни одна дхарма не может
            возникнуть без “поддержки” этих основополагающих сил бытия. Поэтому они и называются
            МАХА, то есть ВЕЛИКИМИ. Каждой дхарме из классического набора в семьдесят пять дхарм
            можно приписать доминанту одного из этой всесильной пятерки: доминанту пространства,
            земли, воздуха, огня или воды; и в конечном счете взаимодействие семидесяти пяти
            элементов Абхидхармы сводится к взаимодействию <em>пяти великих</em>. Эта тенденция
            свести сложность бытия к взаимодействию пяти махабхути получила законченное развитие в
            тантре, где непрерывный механизм реальности сансары и нирваны описывается как
            “разделение” первичной проявленности Ясного Света на радугу Пяти Светов – матрицу всего
            сущего.
        </p>
        <p>
