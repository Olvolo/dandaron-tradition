            <p>Центральной проблемой буддийского учения в любой из трех колесниц (ян) является уход
                из безначальной и многострадальной сансары к конечному идеалу — нирване. Метод, или
                путь, такого перехода в каждой яне свой, и даже само понятие нирваны в них различно.
                Махаянистическая теория нирваны сходится с тантрийской теорией, т.к. обе утверждают,
                что нирвана — это такое состояние, которое является трансцендентным, т.е. находится
                за пределами разумного познания. О нирване мы не можем сказать, возникла она или не
                возникла, и может ли она возникнуть; она не есть прошедшая, настоящая или будущая.
                Сам Будда не давал конкретного объяснения сущности нирваны.
                Будда не ставил прямо вопроса о природе нирваны. Он считал, что его миссия состоит
                не столько в том, чтобы раскрыть тайны блаженства, сколько в том, чтобы привлечь
                людей к его осуществлению<a href="#citation-1" id="ref-1"
                                            class="citation-mark"><sup>1</sup></a>.</p>
            <p>Нирвана для махаянистов есть только разрушение пламени вожделения, ненависти и
                невежества, она отнюдь не означает абсолютного небытия; нирвана есть абсолютное
                совершенство бытия.
            </p>
            <p>Буддийские мыслители, ученые объясняют: нирвана есть ничто иное, как поток
