                случайная личность быть не может, так же как нельзя принять в ученики первого
                встречного. Ученик, кармическое существо, должен прийти с глубоко прочувствованной
                мыслью о необходимости наставлений. Учитель же должен осуществить в себе все то,
                чему он собирается учить. Только при этих условиях могут существовать полезные
                взаимоотношения, из которых возникает то, что будет иметь высочайшее значение для
                дальнейшей жизни индивида.
            </p>
            <p>Ученик, принявший тантрийское учение, совершенствуется, углубляется в
                сосредоточении, при этом читает мантру, которая очищает его язык от всякой скверны.
                Весь процесс является <em>практикой углубления мысли</em>,
                или<em>сосредоточением</em>.
                Прежде всего, это простой факт направления своего внимания на что-либо. Внимание как
                психический)
                элемент есть простейший вид самоуглубления.</p>
            <p>Путем специальных упражнений (в нашу задачу не входит их описание) обычная
                способность внимания может быть развита. В
                этом случае достигается совершенно особое состояние. Созерцаемый предмет или мысль,
                на которую усиленно направляется внимание, начинает занимать сознание целиком,
                выстесняя из него остальное содержание. Таким путем достигается устойчивость
                созерцаемого предмета в сознании, состояние транса, завершающееся иногда
                каталепсией.</p>
            <p>Как мы уже говорили, реальность нирваны не может быть доказана
                логически, но может быть установлена с помощью созерцания и просветления.
                Погруженный в сосредоточенное созерцание йогин <em>видит</em> при помощи истинной
                природы своей алая-виджняны (что соответствует в школе йогачаров индивидуальному
                <em>Я</em>),
                которая, подобно свету, делает ясной сущность нирваны. Учитель, достигший
                посредством йогической практики определенных результатов, может дать кармическому
                ученику, если тот окажется подходящим сосудом, посвящение в тантрийские тайны. Это
                тайное тантрийское учение есть ключ к секрету состояния нирваны, есть метод контроля
                и подавления необузданного сознания, руководство к достижению непосредственного
                интуитивного восприятия реальности.
            </p>
            <p>Существует много различных методов йоги<a href="#citation-5" id="ref-5"
               class="citation-mark"><sup>5</sup></a>.
                Наиболее характерные из них следующие: (1) тренировка в регулировании дыхания
                (пранаяма), заключающаяся в способности задерживать вдох и выдох, что способствует
                созерцанию; (2) отвлечение чувств от внешних объектов и полное подчинение их сознанию
                (пратьяхара); (3) фиксация внимания на образе тантрийского божества (дхьяна);
                (4) созерцание — непрерывное (без отвлечения) рассматривание объекта (дхарана);
                (5) глубочайшее сосредоточение ума, когда он утрачивает представление о себе, и в
                нем остается только объект созерцания (самадхи). Так йогин через сосредоточение и
                размышление может прийти к высшему состоянию сознания, в котором ему является
                мудрость — осознание недвойственности блаженства и шуньи (тиб. <em>bde stong dbyer
                med kyi ye shes</em>). Достижением такого состояния сознания ищущий снимает
                представление об относительности и условности сансары, и перед ним раскрывается тождественность
                нирваны и сансары.
