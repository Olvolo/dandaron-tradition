        </div>

        <p>
            Дифференциация Дхармакаи осуществляется посредством ряда процессов, или интенсификаций.
            Комментируя процесс III, Б. Д. Дандарон пишет: «От света появилась материя». Об этом же
            Нацог-Рандол:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            Свет ошибся, превращаясь в сосуды (пространство) и содержание (объекты).
        </blockquote>
        <p>
            Наша схема не полна, ибо не учитывает развитие по линии так называемого содержания. В
            состояние III входят компоненты: Пять Светов и бинду (семя) Света. Нацог-Рандол об этом
            пишет:
        </p>
        <blockquote class="prose border-l-4 border-gray-300 pl-4 my-4 text-gray-700">
            От Света содержания вышли Света формы и очертания, от пяти кувшинов Света, которые
            являются Светом Радости и Светом Пяти Тел, преобразовалась сперма и в момент проявления
            просветилась.<br>
            И через пристрастия к ней заблудились Пять Светов вишая и сперма как опора
            тела. В период повторного анализа (повторения)<strong>свет</strong> и
            <strong>бинду</strong>двинулись и
            расширились вверх. От этого <strong>свет</strong> становится
            вместилищем,<strong>сперма</strong> стала
            <strong>телом</strong>. От этого ошибочные проявления тел и земли, и камней очистились.
            В конце
            <strong>пять кувшинов света содержания</strong>, очищаясь внутри, в себе, становятся –
            это есть
            механизм <strong>становления</strong>. На основании вышесказанного посредством
            восприятия <strong>света</strong>
            (в свете) свет формы и очертания вместе с бинду являются собственным фундаментом
            становления, и, опираясь на пристрастия к ним, показались сосуды и содержания
            (пространства и объекты), и блаженства, и муки. Это – истина (5, л. ЗЗа).
        </blockquote>
        <p>
            Из этого отрывка видно, что приведенная нами схема действительно дает лишь общий принцип
            развития Дхармакаи: Дхармакая (Разум) – Пять Мудростей – Пять Светов – Пять Махабхути –
            Пространство – Форма – сансара, но при этом не просматриваются подробности развития по
            линии формы (сосуда) и по линии содержания (соки).
        </p>
        <p>
            Дифференциация по линии содержания, начиная с состояния III, выглядит следующим образом:
