            <p>Итак, в нашей схеме показан кармический порядок совершенствования индивида с момента
                получения посвящения от Учителя посредством повторения мантры (8) вслед за Учителем.
                После посвящения ищущий переходит к действию, т. е. к йогической практике (7), и
                практика эта, имея основанием тантрийское учение (4) и его реализацию в Учителе (5),
                приводит его к состоянию Будды (нирваны), обладающего Тремя Телами (1, 2, 3),
                которые олицетворяют собой сущность закона.
            </p>
            <p>Данная схема составлена в результате чтения тантрийской литературы, в особенности
                сочинения Цзонхавы «Агрим»(тиб. <em>sNgags rim chen po</em>).
            </p>
