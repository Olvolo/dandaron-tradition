        <h2 class="text-3xl font-bold text-center my-8">Ронгзом Махапандита</h2>

        <p>Ронгзом Шойжи Занпо (тиб. <em>Rong-zom Chos-kyi-bzang-po</em>, <em>Rong-zom
                Mahapandita</em>, 1012-1088) был
            восприемником многих тайных наставлений, включая наставления Падмасамбхавы, переданные
            через традицию таких учеников Гуру Ринпоче, как Нанам Дорже Дуджом (тиб. <em>sNa-nam
                rDo-rje-bdud-'joms</em>), Харчен Палжи Ванчуг (тиб. <em>mKhar-chen
                dPal-gyi-dbang-phyug</em>), а также и их учеников, которые передали учение Ронгбану
            Ринчен Сультиму (тиб. <em>Rong-ban Rin-chen-tshul-khrims</em>) – отцу Ронгзома. Кроме
            этого Ронгзом имел посвящение в традицию, идущую от Вайрочаны и продолженную через Жалмо
            Юду Ньингпо, и далее через Лаченпо Гонпасала (тиб. <em>Bla-chen-po dGongs-pa-gsal</em>),
            Нуб Бартана (тиб. <em>sNubs dPa'-brtan</em>), Язи Бонтона (тиб. <em>Ya-zi Bon-ston</em>)
            – к нему самому. Это одна из линий передачи раздела ума (тиб. <em>Sems-sde</em>)
            атийоги.</p>

        <p>Ронгзом родился во времена Зурчунпы (1014-1074) в долине Кхунронг (тиб.
            <em>Khungs-rong</em>), на границе с нижним Цангом (тиб. <em>gTsang</em>). Он был признан
            перерожденцем великого учёного Ачарьи Смритиджнянакирти, приехавшего в Кхам за несколько
            лет до этого; Ачарья был известен тем, что написал несколько трактатов по грамматике,
            перевёл ряд тантр и составил к ним многочисленные комментарии.</p>

        <p>Кроме того, Ронгзома также почитали как эманацию Будды, некоторые – как воплощение
            Манджушри. Атиша (982-1054) познакомился с Ронгзомом вскоре после своего приезда в Тибет
            и обнаружил в нём знаки, свидетельствующие, что он может быть воплощением его индийского
            учителя Нагпопы. Ронгзом Шойжи Занпо почитался всеми как Учитель и ученый ни кем не
            превзойденный по мудрости и глубине познаний.</p>

        <p>Рассказывают, что, играя с детьми, он часто повторял наизусть наставления своего гуру
            Гартона Сультима Занпо (тиб. <em>'Gar-ston Tshul-khrims-bzang-po</em>), услышав их перед
            этим лишь один раз. В детстве он постоянно тянулся ко всем учёным ламам, научился
            говорить на многих диалектах. Ронгзом быстро освоил все области знаний, запоминая
            прочитанное с одного раза, и так постиг сутры, шастры и тантры; научился цитировать
            труднейшие тексты, не пропуская ни слова. Кроме того, изучив логику, медицину, поэтику,
            мирские и духовные науки, санскрит и лингвистику, Ронгзом создал систему адекватного
            перевода на тибетский множества технических терминов из области научной буддийской
            литературы.</p>

        <p>В своих работах Ронгзом никогда не противоречил Писанию, разуму и наставлениям учителей.
            Его суждения никто из современников не мог оспорить, и его высказывания были
            неопровержимы. Как наставник он слыл строгим и в то же время сострадательным, он многих
            наставил в знании по теории и практике ваджраяны. Благодаря ему были обнаружены тексты
            эффективных методов реализации на пути мантраяны. Считалось, что кто бы ни избирал его
            Учителем, каждый, в конце концов, следуя его советам, достигал уровня самых высоких
            учений.</p>

        <p>Многие переводчики и учёные посещали проповеди Ронгзома и находили, что он величайший
            знаток Дхармы. Как то раз, во время чтения «Гухьямулагарбхатантры», коренного текста
            махайоги, Ронгзом сказал: «Если бы у нас был санскритский текст для сверки, то следовало
            бы читать следующим образом ... Но так как санскритский оригинал нам не доступен, то
            помощи ждать неоткуда». Горуб Лоцзава Гэлонг Шойжи Шейраб (тиб. <em>Go-rub Lo-tsa-ba
                sGe-slong Chos-kyi-shes-rab</em>) запомнил эти слова, и когда позднее он достал
            санскритский оригинал и сверил его с тем, что говорил Учитель как раз в этом месте, то
            обнаружил, что это полностью совпадает с санскритским текстом.</p>

        <p>Выдающимся учеником Ронгзома был Марба-лоцзава Шойжи Ванчуг (тиб. <em>Mar-ba Lo-tsa-ba
                Chos-kyi dBang-phyug</em>, 1012-1096).</p>

        <p>Выполняя свой учительский долг, Ронгзом написал три текста, в которых изложил тайные
            заповеди своих учителей. Также он написал сочинение, называемое «Упадеша к дзогченовской
            теории и практике» (тиб. <em>rDzogs-pa-chen-po'i lta-sgom man-ngag</em>), ряд
            комментариев на Высшие Тантры и переводы новоиндийских тантр.</p>

        <p>В его время проходили острые теоретические споры, в которых участвовали учёные из четырёх
            районов страны; многие участники этих дебатов считали, что рождённый в Тибете не имеет
            права писать духовные сочинения. Но после того, как учёнейшие мужи Тибета ознакомились с
            работой Ронгзома «Введение в систему махайоги», они испытали недоуменное восхищение от
            эрудиции и глубочайшего интеллекта автора и внимательно выслушали его наставления и
            трактовки.</p>

        <p>Ронгзом получил посвящение в атийогу по особой линии передачи от сиддха Аро Ешей Жуннея
            (тиб. <em>A-ro Ye-shes-byung-gnas</em>), обладавшего тайными наставлениями седьмого
            звена в цепи передачи, идущей из Индии, а также наставлениями седьмого звена китайской
            традиции Хэшана Махаянадэвы. Сиддха передал эти наставления Чогру Занкар Зодхуру (тиб.
            <em>Cog-ro Zangs-dkar-mdzod-khur</em>) и Язи Бонтону (тиб. <em>Ya-zi Bon-ston</em>); оба
            они, в свою очередь, (спустя некоторое время) обучили этим практикам и теориям Ронгзома.
            Взятые вместе обе эти традиции стали известны как «Традиция дзогчена согласно методам из
            Кхама».</p>

        <p>Имел Ронгзом посвящения и по линии Вималамитры, передавшего сокровенные наставления
            Ньянгу Тиннезину Занпо (тиб. <em>Nyang Ting-nge-dzin bzang-po</em>), а также
            Ма-Ринченчогу и Ньягу Джнянакумаре. В свою очередь, они через Ху Жанчуб-ода (тиб. <em>Khu
                Byang-chub-'od</em>) и Жунпо Жиг-ода (тиб. <em>Khyung-po Byig-'od</em>) передали
            наставления Ронгзому Шойжи Занпо.</p>

        <p>Таким образом, спустя триста лет после прибытия в Тибет Падмасамбхавы, все линии передачи
            ньингмапинской традиции и её ответвлений вновь соединились в лице Ронгзома Шойжи Занпо;
            от него же распространилась квинтэссенция Внутренних Тантр (тиб.
            <em>nang-rgyud-sde-gsum</em>) в соответствии с традицией Кхам.</p>
    </div>
