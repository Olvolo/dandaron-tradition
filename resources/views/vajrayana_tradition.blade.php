        <h2 class="text-3xl font-bold text-center my-8">Раздел ума атийоги</h2>

        <p>Раздел ума (тиб. <em>sems-sde</em>) атийоги имеет дело с реализацией истинной природы
            ума. В Семде входит
            восемнадцать тантр, посвятительские ритуалы и садханы с различными уровнями практики.
            Пять из тантр раздела ума связаны с именем Вималамитры и тринадцать – с Вайрочаной. В
            традиции Семде, а также Лонде (тиб. <em>klong-sde</em>), или «Традиции нескончаемого
            опыта бытия», Вайрочана является центральной фигурой. Эти разделы атийоги были также
            распространены в линии Зур (тиб. <em>Zur-lugs-pa</em>), пришедшие туда через передачу,
            восходящую к ученикам Падмасамбхавы. Некоторые из этих учений пришли непосредственно из
            Индии в результате живого контакта индийских учителей и их учеников из Тибета.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Вайрочана</h2>

        <p>Вайрочана для получения передачи атийоги был послан царем Тисрондэцаном к Шри Симхе в
            сандаловый лес на берегу озера Данакоша в Уддияне. Вайрочане, чтобы ему разрешили
            увидеться с великим йогином, обитавшим в девятиэтажном храме, пришлось
            продемонстрировать свои сиддхи йогине, что охраняла это сооружение. Поднеся Шри Симхе
            дары, Вайрочана попросил наставлений в Высшие Тантры. После долгого размышления Шри
            Симха согласился объяснить ему тайный и глубинный аспекты Учения. Всё это происходило по
            ночам, ибо правитель этих мест запретил под угрозой смерти распространение таких учений,
            как атийога. Днём же всё было как положено – Вайрочана с учёными пандитами изучал
            общепринятые философские аспекты буддизма.</p>

        <p>Свои наставления по атийоге Шри Симха написал козьим молоком на белой ткани и объяснил
            Вайрочане, как нужно сделать эти невидимые записи видимыми, держа их над дымом. Так
            учение было передано в Тибет, но секретность его сохранялась.</p>

        <p>Кроме упомянутых восемнадцати наставлений (тантр) Шри Симха передал Вайрочане так
            называемый Раздел Нескончаемого опыта бытия, или Лонде в трёх традициях: «черной»,
            «белой» и «многоцветной» (тиб. <em>nag, dkar, khra</em>). Шри Симха учил его и Разделу
            Наставлений, или Упадеше (тиб. <em>Man-ngag-gi-sde</em>), а также посвящениям (тиб. <em>dbang-bskur</em>)
            и разъяснениям (тиб. <em>man-ngag</em>) к шестидесяти Тантрам Раздела Жуд (тиб. <em>rgyud-sde</em>).
            После того как Вайрочана в совершенстве освоил все эти учения, Шри Симха обучил его трём
            путям поиска наставлений, четырём способам распространения и четырём способам
            нераспространения полученного.</p>

        <p>Позднее Вайрочана в созерцательном видении получил наставления от Гараба Дорже, первого
            среди людей получившего передачу атийоги. Так, получив кратчайшую передачу Учения,
            Вайрочана достиг нирваны. Затем, используя искусство быстрого бега (тиб. <em>rkang-mgyogs</em>),
            он вернулся в Тибет и тщательно практиковал наставления своих индийских учителей.</p>

        <p>Дав наставления атийоги царю Тисрондэцану, Вайрочана, по стечению обстоятельств, три раза
            давал посвящения в Семде в Кхаме. Сначала он передал учение в обители Натха (тиб. <em>Natha</em>)
            Жалмо Юде Ньингпо (тиб. <em>rGyal-mo gYu-sgra sNying-po</em>) из монастыря Жалронг
            Даглагон (тиб. <em>rGyal-rong Brag-la-mgon</em>); затем в уединении Тагцекхар (тиб. <em>sTag-rtse-mkhar</em>),
            что в Цабаронге, – Сантону Ешей-ламе (тиб. <em>gSang-ston Ye-shes-bla-ma</em>); и затем
            – медику Санжею Гонпо (тиб. <em>Sangs-rgyas-mgon-po</em>) в деревушке Дагмар-дзонг (тиб.
            <em>Brag-dmar-rdzong</em>) в районе Тонхунгронга (тиб. <em>sTong-khung-rong</em>).
            Позднее, перебравшись в Уй (тиб. <em>dBus</em>), Центральный Тибет, он наставлял
            тибетскую царицу Лиза Шейраб Донму (тиб. <em>Li-bza' Shes-rab-sgron-ma</em>), родом из
            Хотана. В это время он перевел «Семде Нгажур» (тиб. <em>Sems-sde snga-'gyur</em>) –
            [ранние переводы с санскрита Раздела Ума атийоги].</p>

        <p>Ньяг Джнянакумара, из рода Ньяг в провинции Ярлунг, пять раз получал наставления у
            Вайрочаны и Юды Ньингпо. Ньяг Джнянакумара получил так называемую "Позднюю традицию" у
            Вималамитры.</p>

        <p>Так сошлись вместе четыре потока Высшего Учения: поток широкого (пространного) объяснения
            коренных текстов вместе с краткими комментариями, поток устных наставлений и первичных
            записей, поток Блаженства и Силы вместе с текстами методов и руководств к ним, поток
            Практики и Аскезы вместе с мантрами, защищающими Священные Наставления. Ньяг
            Джнянакумара давал наставления Согпо Палжи Ешею (тиб. <em>Sog-po dPal-gyi-ye-shes</em>)
            и многим другим ученикам. От Согпо традиция через линию Зур перешла к Нубчену Санжей
            Ешею и далее к Добугпе.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Раздел нескончаемого опыта бытия, или раздел
            пространства атийоги</h2>

        <p>Каждый из четырёх подразделов Лонде (тиб. <em>klong-sde</em>) рассчитан на развитие
            такого созерцательного
            сознания, при котором всё ясно, и широко отраженное в уме становится лучистым,
            превращённым в прозрачное сияние. Все тантры, учения, садханы и практики этого раздела
            переживаются внутренне как естественное саморожденное и самотворящее движение
            индивидуума к Высшей Реальности Бытия.</p>

        <p>Среди текстов, относящихся к Лонде, следует назвать «Махатантру Ваджрасаттвы,
            беспредельную и равностную, как небо» (тиб. <em>rDo-rje-sems-dpa' nam-mkha'i
                mtha'-dang-mnyam-pa'i rgyud chen-po</em>), состоящую из девяти "сфер" (тиб. <em>klong</em>).
            Вот эти девять «сфер»:</p>

        <ul class="list-none pl-2 space-y-1 ml-2">
            <li>– сфера Видения (тиб. <em>lta-ba'i klong</em>);</li>
            <li>– сфера Практики (тиб. <em>spyod-pa'i klong</em>);</li>
            <li>– сфера Мандалы (тиб. <em>dkyil-'khor-gyi klong</em>);</li>
            <li>– сфера Могущества (тиб. <em>dbang-gi klong</em>);</li>
            <li>– сфера Обета (тиб. <em>dam-tshig-gi klong</em>);</li>
            <li>– сфера Созерцания (тиб. <em>sgrub-pa'i klong</em>);</li>
            <li>– сфера Действия (тиб. <em>phrin-las-kyi klong</em>);</li>
            <li>– сфера Ступеней Пути (тиб. <em>sa-lam-gyi klong</em>);</li>
            <li>– сфера Плода (тиб. <em>'bras-bu'i klong</em>).</li>
        </ul>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Панг Мифам Гонпо</h2>

        <p>Традиция Лонде восходит к Вайрочане, который передал наставления «Дорже Зампа» (тиб. <em>rDo-rje
                zam-pa</em>) – «Учение о Ваджрном мосте» [так иногда называют учения Раздела
            Пространства] Пангу Мипаму Гонпо (тиб. <em>sPangs Mi-pham-mgon-po</em>) в соответствии с
            несколькими линиями передачи, включая линию Ешея Санбы (тиб. <em>Ye-shes gSang-ba</em>).
            Мипам Гонпо в молодости не занимался созерцательной практикой, но к старости стал
            знаменитым практиком. Для созерцания он использовал специальный медитативный пояс и
            подставку под подбородок, чтобы держать своё престарелое тело в вертикальном положении.
            Следуя советам учителя, он постиг метод «непроисхождения» и прожил более ста лет.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Нанлам Жанчуб Жалцан и Шойжи Хужур</h2>

        <p>Главным учеником Мипама Гонпо был монах Нанлам Жанчуб Жалцан (тиб. <em>Ngan-lam
                Byang-chub rGyal-mtshan</em>), ставший на путь ученичества в возрасте шестидесяти
            семи лет. В свою очередь, его главным учеником был Занам Ринченжиг (тиб. <em>Za-ngam
                Rin-chen-dbyig</em>), уроженец Верхнего Амдо. Занам вместе с Нанламом жили в скале
            под названием Лис и Лев (тиб. <em>Wa-seng-ge</em>). Далее традиция перешла к Шойжи
            Хужуру Салбачогу (тиб. <em>Chos-kyi-khu-'gyur gSal-ba'i-mchog</em>) из Ярлунга, который
            получил посвящение от Занама, будучи пятидесятисемилетним монахом.</p>

        <p>Упомянутые учителя покинули этот мир в один год; это случилось на горе Лисы и Льва, их
            тела растаяли, как туман или радуга (тиб. <em>'ja'-lus</em>). Нанламу Жанчубу Жалцану к
            этому времени исполнилось сто семьдесят два года, Занаму – сто сорок четыре и Хужуру –
            сто семьдесят.</p>

    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">


        <h2 class="text-3xl font-bold text-center my-8">Ньянг Жанчубдаг</h2>

        <p>Главным учеником Шойжи Хужура был монах Ньянг Жанчубдаг (тиб. <em>Nyang
                Byang-chub-grags</em>), уроженец Верхнего Юбруга (тиб. <em>Yu-'brug</em>), он
            получил посвящение в сорок два года. Позже, проживая в Чимпху, что в Самье, Жанчубдаг
            встретил престарелого монаха по имени Ньянг Шейраб Жунней (тиб. <em>Nyang
                Shes-rab-'byung-gnas</em>), который также даровал ему линию передачи Учения. Когда
            Ньянг Жанчубдаг обитал на священной горе Пхунпо (тиб. <em>Phung-po</em>) в Жама Неукха
            (тиб. <em>rGya-ma ne'u-kha</em>), что в Цанге (тиб. <em>gTsang</em>), его тело иногда
            принимало вид легкого облачка, однажды же видели, как он растворился в воздухе над
            крутым склоном горы.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Ньянг Шейраб Жунней и Ачарья Багом</h2>

        <p>Учеником Ньянга Шейраба Жуннея был Ачарья Багом (тиб. <em>sBa-sgom</em>), уроженец Ломо
            (тиб. <em>Lo-mo</em>) из клана Ба (тиб. <em>sBa</em>). Родители отдали его в шестнадцать
            лет под духовное покровительство Ньянга Шейраба, чтобы оградить от опасностей
            гражданской войны, идущей в то время. Именно в это время их посетил Ньянг Жанчуб. Он
            появился очень памятно – за ним покорно шёл дикий олень; с тех пор его часто называли
            «Ньянг с оленем».</p>

        <p>Когда Шейраб Жунней и Багом пришли на гору Пхунпо, они увидели, как Ньянг Жанчубдаг
            исчез, а затем появился в виде мощного вихря, который некоторое время покрутившись, стал
            огнём, затем небольшим количеством воды и, наконец, заполнил миску, используемую для
            подношений. Продолжая демонстрировать сиддхи дотемна, он внезапно принял обычный вид и
            объяснил, что до тех пор, пока грубые махабхуты не очищены, тонкие махабхуты не
            растворены, такие явления, что он демонстрировал, не могут происходить. Но если понятен
            объект, который нельзя созерцать, и можно так практиковать, не отвлекаясь, то
            продемонстрированных способностей не так уж трудно достигнуть.</p>

        <p>У Ачарьи Багома была ученица Ганмо (тиб. <em>Gang-mo</em>). Однажды он сказал ей, что не
            верит в то, что видимые предметы существуют на самом деле. Говоря так, он ударил по
            камню рукой, и она вошла в камень по локоть. Затем он покачнулся всем телом и вошел в
            скалу. След от его тела ещё долго можно было видеть на этой скале.</p>

        <p>В конце жизни Шейраб Жунней наставлял Багома: «В моё отсутствие внимательно следи за
            вершиной Лхари». Уже много лет спустя, когда однажды Шейраб Жунней не вернулся из
            прогулки в горы, Багом обнаружил, что учитель ушёл, не оставив и следа от тела. Лишь
            одежду и чётки из священного фикуса, висящие на ветвях можжевельника, нашел Багом в
            горах.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Дзенг Дхармабодхи</h2>

        <p>Когда Багом созерцал в небольшой пещере Зурари (тиб. <em>Zu-ra-ri</em>), его посетил
            Дзенг Дхармабодхи (1052 – 1168) и признал в нём Высшего Учителя (Махагуру). Багом сказал
            Дзенгу, что владеет секретным посвящением «Ваджрный мост» (тиб. <em>rDo-rje zam-pa</em>),
            и если понять его суть хотя бы на одно мгновение, то обретешь просветление ещё в этой
            жизни. Он рассказал, что учение «Дорже Зампа» передавалось по непрерывной линии передачи
            учителями, реализовавшими «радужное тело». Багом, хранивший до этих пор сие учение в
            строжайшей тайне, передал его Дзенгу Дхармабодхи полностью через четыре посвящения.</p>

        <p>Дзенг Дхармабодхи, странствуя с продвинутыми йогами, изучил множество учений разных школ,
            и к нему стали приходить ученики-сиддхи – и мужчины, и женщины. Он научился виртуозно
            превращать своё тело в сферическую радугу и мгновенно перемещаться на любое расстояние.
            Получив от Багома все тайные наставления, он научился соединять ум с небом, при этом всё
            объективное лишалось привкуса двойственности. Кроме того, он практиковал так называемые
            «Шесть учений Наропы», получив их от дакини Нигумы, особенно практику снов (тиб. <em>rmi-lam</em>)
            и махамудру. В конце жизни он достиг видения Амитабхи, даровавшего ему конечное
            понимание многих методов совершенствования.</p>

        <p>Однажды, попав в город Монгар (тиб. <em>Mon-'gar</em>), расположенный на берегу
            заледеневшей реки, Дзенг вздумал перейти реку по льду, но лёд под ним проломился и он
            оказался в ледяной воде. Тело его погрузилось в воду, шипя как раскалённое железо, и в
            воздух поднялся столб пара. Видевшие это не могли представить, как можно было без вреда
            здоровью находиться в такой воде. Он же, выбравшись на сушу, только и сказал: «Конечно,
            я замерз».</p>

        <p>Дзенг прославился подобными чудесами и суровой аскетической практикой. Прожил он до ста
            семнадцати лет. С него началась и продолжается до сих пор устная традиция передачи
            учения «Дорже Зампа».</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h2 class="text-3xl font-bold text-center my-8">Дзенг Джосрай</h2>

        <p>Главным учеником Дзенга был Дзенг Джосрай (тиб. <em>Dzeng Jo-sras</em>). Он начал поиск
            учителя совсем молодым человеком. Три раза он просил «Дорже Зампа» у Дзенга Дхармабодхи
            и, наконец, получил. После сего, по уговору с учителем, это учение передавал уже он сам.
            Дзенг Дхармабодхи и Дзенг Джосрай сотрудничали восемнадцать лет. У них был общий ученик
            – Ачарья Кунзан (тиб. <em>Kun-bzang</em>), который прослушал текст «Дорже Зампа»
            тридцать пять раз. Он был неутомимым созерцателем. Ему принадлежат детальные комментарии
            на коренные тексты. Его ученик Сотон (тиб. <em>So-ston</em>) первым составил письменный
            комментарий на «Дорже Зампа», восходящий по основным положениям к Вайрочане через линию
            передач просветленных учителей школы ньингмапа.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Распространение учения атийоги Раздела
            Упадеша</h2>

        <p>Раздел Руководства, или Раздел Упадеши (тиб. <em>Man-ngag-sde</em>), был впервые получен
            Гарабом Дорже (тиб. <em>dGa'-rab
                rDo-rje</em>), Нирманакаей, то есть проявленной эманацией Ваджрасаттвы. Он был
            первым в ряду людей, кто считается Видьядхарой (букв. «Держатель Знания»).</p>

        <p>Преемником Гараба Дорже был Жамбал Шейнен (Манджушримитра). Он передал учение Шри Симхе.
            Кроме того, Шри Симха воспринял традицию непосредственно от Гараба Дорже во время
            созерцания. Шри Симха был учителем Джнянасутры (тиб. <em>Ye-shes-mdo</em>), Буддхагухьи
            (тиб. <em>Sangs-rgyas gSang-ba</em>), Вималамитры и Вайрочаны.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h2 class="text-3xl font-bold text-center my-8">Падмасамбхава</h2>

        <p>Главным учителем в этой линии преемственности почитается Падмасамбхава как живой пример
            глубочайшего смысла состояния Будды. Он явил собой три проявления: Дхармакаю,
            Самбхогакаю и Нирманакаю. Его главными учениками в линии тайных наставлений, известных
            как Хандо Ньинтиг (тиб. <em>mKha'-'gro sNying-thig</em>), были Шри Симха и Жамбал
            Шейнен. Учения Хандо Ньинтиг Падмасамбхава представил и практически доказал их
            могущество при дворе царя Тисрондэцана, вернув к жизни принцессу Лхачан Падмасал (тиб.
            <em>lHa-lcan Padma-gsal</em>), дочь царя. Падмасамбхава подошёл к мертвому ребенку и
            красным меловым порошком написал на груди у девочки слог <strong>ХРИ</strong> и затем
            усилием
            созерцательного сосредоточения Гуру Ринпоче вернул девочку к жизни. Затем он даровал
            Падмасал посвящение (тиб. <em>dbang-bskur</em>) в Хандо Ньинтиг уровня «Тайных
            наставлений» (тиб. <em>Man-ngag-mkha'-'gro'i sNying-thig</em>). Эти сокровенные учения
            Падмасамбхава позже был вынужден спрятать в тайном месте, дав им этим существование в
            виде терма – спрятанных сокровищ. После Падмасамбхавы учения Хандо Ньинтиг до времени
            выпали из нашего пространственно-временного измерения, их распространение, естественно,
            прекратилось. Но в ином измерении эти учения продолжали жить среди бодхисаттв и
            просветленных существ. Так они сберегались для будущего, когда начали дароваться людям
            особыми способами через дакинь и тертонов – учителей-открывателей терма.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h2 class="text-3xl font-bold text-center my-8">Передача Шри Симхой Раздела Упадеши
            Вималамитре</h2>

        <p>Задолго до того как совершить путешествие в Тибет, Вималамитра и Джнянасутра (тиб. <em>Ye-shes-mdo</em>)
            имели видение Ваджрасаттвы. Ваджрасаттва поручил им отправиться к храму Сфера Прозрения
            (тиб. <em>Byang-chub-zhing</em>) в Китае и пройти обучение у Шри Симхи, если они желают
            обрести состояние будды в этой жизни.</p>

        <p>Вималамитра проучился у Шри Симхи двадцать лет, в течение которых он усвоил Главное
            Учение (тиб. <em>sNying-thig</em>, букв. Средоточие Сущности) Раздела Маннагде (Упадеша)
            дзогчена и другие высокопочитаемые и охраняемые наставления. По возвращении в Индию
            Вималамитра встретился с Джнянасутрой и пересказал ему всё, чему он научился за двадцать
            лет у тантрийского Видьядхары. По совету Вималамитры Джнянасутра тоже отправился в Китай
            в тот храм, где обитал Шри Симха. Так как он знал секрет созерцательного ускорения
            передвижения (тиб. <em>rkang-mgyogs</em>), то смог преодолеть путь длиною в девять
            месяцев всего лишь за один день. Он проучился у Шри Симхи шестнадцать лет, получая
            наставления Раздела Упадеши.</p>

        <p>Однажды услышав странный звук, Джнянасутра взглянул наверх и увидел Шри Симху, сидящего в
            радужном сиянии прямо в воздухе. Образ Учителя медленно таял, прежде чем окончательно
            исчезнуть; Шри Симха передал Джнянасутре книгу «Зербу Дунпа» (тиб. <em>gZer-bu
                bDun-pa</em>, букв. «Семь Гвоздей»), содержащую наставления по методу достижения
            недвойственности, и поручил ему разыскать 4-й Раздел Упадеши, предположительно сокрытый
            в колонне храма Даши Дигдо (тиб. <em>bKra-shis-khrigs-sgro</em>, букв. «Врата Счастья и
            Изобилия»). После этого Джнянасутра уединился на кладбище Хашинг (тиб.
            <em>Bha-shing</em>), где дал Вималамитре посвящение в атийогу, соответствующие тексты и
            устные пояснения. Вималамитра же отправился в Западную Индию в город Бхирьял (тиб. <em>Bhir-ya-la</em>),
            где раджа Дхармапала по случаю его прибытия устроил религиозный праздник. Затем он
            отправился к северу от города в Рабту Нанжед (тиб. <em>Rab-tu-snang-byed</em>) – в место
            погребений, там он практиковал Ньинтиг дампа (тиб. <em>sNying-thig-gi gdams-pa</em>,
            букв. «Наставления о Средоточии Сущности») – это тончайшая нюансировка наставлений,
            ведущих к совершенству. Вималамитра оставался на кладбище в течение тринадцати лет, в
            результате он обрёл так называемое «неизменное тело» и много раз демонстрировал свои
            необычные способности. Позже он переехал в Капилавасту, наставляя раджу Индрабодхи, где
            и пребывал, пока ему не исполнилось двести лет.</p>

        <p>По приглашению Тисрондэцана Вималамитра, высокообразованный учёный, переехал в Тибет.
            Здесь ему оказали торжественный приём, и он занял почётное место в зале переводов в
            монастыре в Самье, где предпринял перевод секретных и несекретных текстов с санскрита на
            тибетский язык.</p>

        <p>Вималамитра провёл в Тибете тринадцать лет, работая в тесном сотрудничестве с
            Падмасамбхавой и Вайрочаной. Эти три Великих Учителя стали первыми переводчиками текстов
            дзогчена и установили традицию передачи сокровенных устных наставлений. В конце жизни
            Вималамитра удалился на гору Утайшань (тиб. <em>Ri-bo-rtse-lnga</em>), что на границе с
            Китаем, где и почил – при этом его тело исчезло в радужном свете. Многие тибетские
            подвижники, следуя практикам, детально изложенным в трудах Падмасамбхавы и Вималамитры,
            обрели необычайные способности и также покинули этот мир, не оставив и следа от своего
            физического тела.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Ньянг Тиннезин Занпо</h2>

        <p>Перед последним уединением на Утайшане Вималамитра передал наставления по Ньинтигу царю
            Тисрондэцану и другу его детства Ньянгу Тиннезину Занпо (тиб. <em>Nyang Ting-nge-'dzin
                bzang-po</em>). Ньянг построил монастырь Шваилхакан (тиб. <em>Zhva'i lha-khang</em>),
            а в нём – храм Уйрусва (тиб. <em>dBus-ru-zwa</em>), где спрятал наставления по Ньинтигу.
            Ньянг Тиннезин Занпо стал важнейшей фигурой в период правления Тисрондэцана (755-797) и
            выдающимся защитником растущей общины буддистов. В возрасте пятидесяти пяти лет (836 г.)
            он был казнен Ландармой, ибо отказался прекратить буддийскую практику. На следующий
            после казни день к удивлению всех в небе появились радуги и произошли другие природные
            необычные явления.</p>

        <p>Ньянг Тиннезин Занпо передал «Традицию Слов» (тиб. <em>Tshig-brgyud</em>) Брому
            Ринченбару (тиб. <em>'Brom Rin-chen-bar</em>), который, в свою очередь, передал её Бэй
            Лодою Ванчугу (тиб. <em>sBas Blo-gros dBang-phyug</em>). Спустя некоторое время сокрытые
            Ньянгом наставления были обнаружены и по ним занимался Данма Лхунжал (тиб. <em>lDang-ma
                Lhun-rgyal</em>), который затем передал их известнейшему Жецуну Сэнге Ванчугу (тиб.
            <em>rJe-btsun Seng-ge-dbang-phyug</em>) из Верхнего Ньянга и объяснил ему семь уровней
            этих наставлений. Сэнге Ванчуг передал посвящение «Традиции Слов» практикующему на скале
            Тидо (тиб. <em>Ti-sgro</em>), что на Зо (тиб. <em>gZo</em>, Маленькое Озеро), а также
            Ньянгу Кадампе (тиб. <em>Nyang bKa'-gdams-pa</em>), ушедшему затем в радужном теле.</p>

        <p>Жецун Сэнге Ванчуг обитал на горном хребте между Сангом (тиб. <em>Sang</em>) и Уюгом
            (тиб. <em>U-yug</em>), много практиковал и обрёл чудесные способности. Он спрятал записи
            секретных наставлений в трёх потайных местах: в Уюге, в <em>Lang-gro'i
                'chad-pa-ltag</em> и в <em>Jal-gyi-phu</em>.</p>

        <p>Спустя тридцать лет <strong>Жегом Нагпо</strong> обнаружил эти наставления и стал по ним
            заниматься.
            Подобные же сокрытые записи нашёл и Сангпарайпа (тиб. <em>Sangs-pa Ras-pa</em>) и
            передал другим. Ещё спустя некоторое время, когда Занг Даши Дорже (тиб. <em>Zang
                bKra-shis rDo-rje</em>, он же Жалва Шангтон (тиб. <em>rGyal-ba-zhang-ston</em>,
            1097-1167)) пребывал в Верхнем Ньянге, бодхисаттва Ваджрасадху (Чёрный Кузнец) явился
            ему
            и привёл на почтённое собрание на высокой горе в западной части Уюга. Там в пещере,
            выходящей на север, Занг Даши Дорже обнаружил тексты, спрятанные Жецуном Сэнге Ванчугом.
            Кроме того, он нашел тайные сокрытые тексты, спрятанные Жалжипху (тиб.
            <em>Jal-gyi-phu</em>), а в пещере Чимпху – сокровища самого Вималамитры. Все это он
            передавал тем, кто лично просил у него посвящений и наставлений. Сам же некоторые тайные
            наставления получил непосредственно от Жегома Нагпо.</p>

        <p><strong>Хайпа Нибум</strong> (тиб. <em>mKhas-pa Nyi-'bum</em>, 1158-1213), сын Занга Даши
            Дорже,
            учился у
            собственного отца одиннадцать лет, а также у других учителей, среди которых особо стоит
            отметить Сакья Дагпа Жалцана (тиб. <em>Sa-skya Grags-pa rGyal-mtshan</em>). Завершив
            изучение Ньинтиг, он написал шастру «Великое значение слов» (тиб. <em>Tshig-don
                chen-mo</em>).</p>

        <p><strong>Гуру Жобер</strong> (тиб. <em>Gu-ru Jo-'ber</em>, 1196-1231), сын Хайпа Нибума,
            получил в юности
            полный цикл посвящений в Ньинтиг. В восемнадцать лет он также принял посвящение в цикл
            Чакрасамвары и учение Махамудры от Сакья Панченя. Вскоре по окончании учебы, в возрасте
            36-ти лет, ему предстали Ваджрапани и Авалокитешвара в видении в венце из света; позже
            он имел видение Амитаюса; вскоре после этого он покинул мир.</p>

        <p><strong>Дулшиг Сэнге Жалпа</strong> (тиб. <em>'Khrul-zhig Seng-ge-rgyal-pa</em>) получил
            полные
            наставления по учению Ньинтиг от Гуру Жобера. Кроме того, он изучил многие из Старых и
            Новых Тантр, а также имел посвящения в системы Махамудры и Шижед (тиб. <em>Zhi-byed</em>).
            Он в течение долгих лет усердно созерцал в уединенных убежищах в долинах между гор и
            воспитал много способных учеников.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h2 class="text-3xl font-bold text-center my-8">Мелон Дорже</h2>

        <p>Главным учеником Дулшига Сэнге был Дубчен Мелон Дорже (1243-1303), который обрёл дар
            предвидения в очень молодом возрасте. Ещё будучи отроком, он получил посвящения от
            целого ряда знаменитых учителей. В возрасте восемнадцати лет, когда он пребывал в
            местечке Сангежаб, Сэнге Бонпо (тиб. <em>Seng-ge-dbon-po</em>) дал ему Ньинтиг, вслед за
            этим в течение шести дней и ночей ему постоянно являлся Ваджрасаттва. В процессе
            посвящения, таким образом, он получал наставления и во время сна. Спустя некоторое время
            стал наследником многих сокрытых до него текстов – тершой (тиб. <em>gter-chos</em>),
            таких, как, например, Ваджраварахи (тиб. <em>rDo-rje Phag-mo</em>) [йога] Санжейрайпы
            (тиб. <em>Sangs-rgyas-ras-pa</em>). Он обрёл многие благословения через видение
            излучающих свет образов таких идамов и учителей, как Хаягрива, Ваджраварахи, Тара,
            Авалокитешвара, Самантабхадра (бодхисаттва), Ваджрасаттва, Вималамитра, Падмасамбхава,
            Залунгпа, Санжейрайпа и Догон Ринпоче (тиб. <em>'Gro-mgon Rin-po-che</em>); все они
            являлись ему в радужном сиянии.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Кумараджа</h2>

        <p>Основным учеником Мелона Дорже был Великий Видьядхара Кумараджа (тиб. <em>Rig-'dzin-chen-po
                Ku-ma-ra-dza</em>, 1266-1343), который в будущем станет учителем Лонченпы и молодого
            перерожденца Ранжуна Дорже (III Кармапы). Ещё с детства Кумараджа выказывал большой ум,
            легко научился писать и читать. В семь лет он уже был посвящён в системы Хеваджры и
            Чакрасамвары; к девяти годам – в цикл Авалокитешвары в стиле традиции ньингмапы. Позже,
            при посвящении в духовный сан, он получил имя Шонну Жалпо (тиб. <em>Zhon-nu
                rGyal-po</em>), изучил Винаю, Шесть доктрин Наропы и другие системы у ламы Дагсебы
            (тиб. <em>Grags-se-ba</em>). Кроме того, он обучался живописи и стал знаменит как
            художник. Впоследствии получил много других наставлений и ньингмапинских посвящений в
            Тантры от Шунгмы Сакдара (тиб. <em>Khyung-ma Sak-dar</em>).</p>

        <p>Великий Видьядхара Кумараджа получил от Мелона Дорже толкование махамудры и добился
            йогического видения, соединив свой ум с Учением. Во сне Кумараджа беседовал с
            Падмасамбхавой и получил от него тайные наставления. Потом Кумараджа отправился в
            монастырь Цурпу (тиб. <em>mTshur-phu</em>) и у лам Ньянрэя (тиб. <em>gNyan-ras</em>) и
            Дарма Гонпы (тиб. <em>Dar-ma-mgon-po</em>) изучил доктрину школы кармапа. Затем ему
            повстречались два махасиддха – Уржанпа (тиб. <em>U-rgyan-pa</em>) и Ранжун Дорже;
            последний стал монахом с семилетнего возраста. От махасиддхов он получил множество
            посвящений: точно известно, что от Ньянрэя он получил весь Ньинтиг и такое учение
            дзогчена, как «Зерцало тайной сути» (тиб. <em>gSang-ba gnad-kyi me-long</em>). Позже,
            будучи в Карчу (тиб. <em>mKar-chu</em>), он завершил изучение Ньинтига, получив устные
            пояснения от Мелона Дорже. В Цурпу Кумараджа передал Ньинтиг Ранжуну Дорже. От Ачарьи
            Гомпы (тиб. <em>sGom-pa</em>), принадлежащего традиции Жегома Нагпы (тиб. <em>rJe-sgom
                Nag-po</em>), он получил «Санхор» (тиб. <em>gSang-skor</em>, букв. Тайный цикл) и
            «Самтан Мигдонме» (тиб. <em>bSam-gtan Mig-gi sgron-me</em>, «Светильник Глаза
            Созерцания»). Кумараджа был искусен в преподавании Ньинтига, используя специфическую для
            этой системы терминологию; много времени он провёл в горах, в созерцании в уединенных
            обителях. Ушёл он в возрасте семидесяти восьми лет.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">

        <h2 class="text-3xl font-bold text-center my-8">Лончен Рабжампа</h2>

        <p>Гунчен Лончен Рабжампа (или тиб. <em>Dri-med-'od-zer</em>, 1308-1363) является самой
            значительной фигурой всей традиции дзогчена, ибо упорядочил все философские положения,
            понятия и их психологическое применение в созерцании и, в конце концов, создал единую
            гармоничную систему. По отцовской линии он был наследником традиции, идущей от Рог
            Шейраб-ода, чья духовная преемственность восходит к Ешей Ванпо Срунгу (тиб. <em>Ye-shes
                dBang-po-srung</em>), одному из семи монахов (тиб. <em>sad-mi-bdun</em>), получивших
            посвящение в 790-м году от Шантаракшиты. Кроме того, он был связан с родом Жалвы
            Чогбанга (тиб. <em>rGyal-ba mChog-dbang</em>), одного из двадцати пяти учеников
            Падмасамбхавы, тем учеником, который в совершенстве овладел садханой Хаягривы (тиб. <em>Padma-gsung</em>)
            и который переродился как Дуйсум Кьенпа (тиб. <em>Dus-gsum-mkhyen-pa</em>), величайший
            ученик Гампопы.</p>

        <p>По линии матери Бромзамы Соднамжан (тиб. <em>'Brom-gza'-ma bSod-nams-rgyan</em>) Лонченпа
            унаследовал традицию Бромтонпы (тиб. <em>'Brom-ston rGyal-ba'i-'byung-gnas</em>,
            1005-1064) – основного последователя Атиши в Тибете.</p>

        <p>В молодости Лонченпа изучил многие учения, в том числе «Пять Учений» Майтрейи и «Семь
            трактатов» Дхармакирти, в объяснении которых был впоследствии знаменит. Среди
            современников был известен как «тот, кто знает много стихов», за свои обширные познания
            Корзины Сутр.</p>

        <p>В 1319 г. Лонченпа получил религиозный сан в монастыре Самье, в церемонии участвовал
            настоятель Сандуб Ринчен (тиб. <em>bSam-grub Rin-chen</em>) и лама Кунга Одсер (тиб.
            <em>Kun-dga' 'Od-zer</em>). Именно тогда Лонченпа получил имя Сультим Лодой (тиб. <em>Tshul-khrims
                Blo-gros</em>). В течение нескольких лет он интенсивно обучался у знаменитейших
            учителей своего времени. В совершенстве постигнув традицию Ньинтиг, он учился вместе с
            Ранжуном Дорже (1284-1339) [у Кумараджи] и у него самого, а также у ламы Дампа Соднам
            Жалцана (тиб. <em>Dam-pa bSod-nams rGyal-mtshan</em>, 1312-1375); оба они передали ему
            так называемую «Новую традицию» (тиб. <em>gSar-ma</em>). За свою эрудицию он получил
            имена Агги Ванпо из Самье (тиб. <em>Ngag-gi-dbang-po</em>) и Лончен Рабжампа, которыми
            он подписывал некоторые из своих сочинений.</p>

        <p>Несмотря на свою молодость, Лонченпа стал настоятелем монастыря Самье; но скоро он
            отстранился от своих обязанностей, покинул монастырь и удалился в горы, где вёл жизнь
            простого человека. В течение всей жизни ему много раз являлись бодхисаттвы и
            просвещённые Учителя. Когда ему уже было около тридцати лет, он имел видение
            Падмасамбхавы и его супруги Ешей Цогьял (тиб. <em>Ye-shes mtsho-rgyal</em>). Во время
            этого видения он получил от них следующие имена: от Падмасамбхавы – имя Дримед Одсер, а
            от его супруги – Дорже Зижид (тиб. <em>rDo-rje dZi-brjid</em>). Как раз в этот период он
            начал созерцать Хандо Ньинтиг (тиб. <em>mKha'-'gro snying-thig</em>) – учение, связанное
            с именем Падмасамбхавы, что имело сильнейшее влияние на его дальнейшее развитие. В
            подобном этому учению стиле он написал свою собственную работу «Хандо Янтиг»
            (тиб. <em>mKha'-'gro yang-tig</em>).</p>

        <p>Проведя пять месяцев в темноте пещер, он снова посетил Самье и встретил своего Главного
            Учител – Ригзина Кумараджу. При встрече Кумараджа сказал ему: «Сегодня ночью мне
            приснился сон, что прилетела прекрасная птица, это предвосхитило твое появление. Ты –
            держатель духовной традиции моего Учения, и тебе будут переданы тайные посвящения».
            После этой встречи они провели некоторое время вместе, переходя с места на место девять
            раз в течение одной весны, путешествуя из одной пустынной долины в другую.</p>

        <p>Лонченпа в этот период пережил много несчастий и тягот, созерцая в уединённых пещерах,
            таких, как Чимпху (тиб. <em>mChims-phu</em>), и получая посвящения от Кумараджи. Во
            время снегопадов он мог прикрывать свое тело лишь шерстяным мешком, используя днём как
            подстилку. В присутствии Кумараджи он безошибочно анализировал и делал блестящие выводы
            из наставлений, демонстрируя удивительные способности в трёх аспектах: учении, диспуте и
            сочинительстве. Он известен как автор переводов, комментариев и собственных текстов.</p>

        <p>Мелон Дорже (1243-1303) передал учение Вималамитры Кумарадже; оно было суммировано в
            тексте «Вима Ньинтиг»” (тиб. <em>Bi-ma snying-thig</em>) и вновь открыто Данмой
            Лхунжалом (тиб. <em>lDang-ma lHun-rgyal</em>). Кумараджа, будучи перерожденцем
            Вималамитры, передал эти учения Лончену Рабжампе, который, в свою очередь, тщательно
            изучив, свёл их в две работы – «Хандо Янтиг» (тиб. <em>mKha'-'gro yang-tig</em>) и «Лама
            Янтиг» (тиб. <em>Bla-ma yang-tig</em>), а затем все подытожил в самой глубокой своей
            работе «Забмо Янтиг» (тиб. <em>Zab-mo yang-tig</em>). Несмотря на глубину и сложность
            учения, к которому обратился Лонченпа, его трактовки и объяснения отличались ясностью и
            доступностью.</p>

        <p>В последующие годы Лонченпа написал тридцать пять книг по системе Ньинтиг, озаглавив весь
            цикл как «Лама Янтиг» (тиб. <em>Bla-ma yang-tig</em>). В течение всей жизни он написал
            двести шестьдесят три работы, из которых сохранилось только двадцать пять. Эти тексты
            представляют собой унифицированный свод всех буддийских учений и практик, ибо Лонченпа
            имел посвящения от учителей обеих традиций: и ньингма, и сарма (новой традиции). Его
            работы по дзогчену являются наиболее глубокими, популярными и совершенными творениями с
            точки зрения теории и поэзии во всей истории тибетской буддийской литературы. Лонченпа
            был величайшим мастером логических построений и ясности изложения сложнейших положений
            теории атийоги.</p>

        <p>Среди его небольших работ выделяется цикл из трёх текстов: «Нгалсо Корсум» (тиб. <em>Ngal-gso
                skor-gsum</em>), «Рандол Корсум» (тиб. <em>Rang-grol skor-gsum</em>) и «Мунсел
            Корсум» (тиб. <em>Mun-sel skor-gsum</em>). В них особое внимание уделяется освоению
            истинной свободы на протяжении всего периода духовного развития, причём подчеркивается,
            что достижение её реализуется через правильное сосредоточение на созерцаемом божестве.
            Все его практические рекомендации базируются на теоретических положениях таких его
            работ, как «Семь Сокровищ» (тиб. <em>mDzod bdun</em>) и «Ньинтиг Ябши» (тиб. <em>sNying-thig
                ya-bzhi</em>), содержащих квинтэссенцию философии и психологии ньингмапы. Его перу
            также принадлежит несколько литургических текстов для проведения ритуала подношения
            (puja) и рекомендаций по созерцанию в стиле атийоги, а также молитвы, поэтические
            описания впечатлений от созерцательной практика, дидактические рассказы в стихах,
            восхваления бодхисаттв, трактаты по истории буддизма (тиб. <em>chos-'byung</em>), теории
            и практике главнейших садхан.</p>

        <p>Из-за своей оппозиции к правящей в Тибете группировке Лонченпа был вынужден около десяти
            лет скрываться в Бутане. Но это вынужденное бегство обернулось великой пользой учению.
            Он основал монастыри Тарпалинг недалеко от Бутана и Шархотинг Ринченлинг (тиб. <em>Shar-kho-thing
                Rin-chen-gling</em>) и Самтанлинг (тиб. <em>bSam-gtan-gling</em>) – в Паро (тиб.
            <em>sPa-gro</em>). Таким образом, из Бутана учения ньингмапы распространились далее и в
            Непал.</p>

        <p>Во время путешествий Лонченпа возродил и основал ряд других монастырей и созерцательных
            центров по всему Тибету и Бутану. Значительна и его роль в основании и реставрации
            монастырских поселений Лхарингбраг (тиб. <em>lHa-ring-brag</em>), Уржандзонг (тиб. <em>O-rgyan-rdzong</em>)
            и Швалхакханг (тиб. <em>Zhva'i-lha-khang</em>). Главной его заслугой считается
            возрождение монастря Cамье, первого образовательного центра в Тибете, основанного в 768
            г., где начиналась ранняя переводческая деятельность в первый период распространения
            Дхармы.</p>

        <p>В конце концов, когда к власти пришла династия Пагмоду (тиб. <em>Phag-mo-gru</em>),
            Лонченпе было разрешено вернуться в Тибет. Перед возвращением на родину он дал
            посвящение в Ньинтиг множеству учеников, собравшихся на берегу реки Шичу (тиб. <em>sKyi-chu</em>)
            в Верхнем Уру (тиб. <em>dbU-ru</em>). Именно в этот период он получил титул Всеведующий
            – Кунчен (тиб. <em>Kun-mkhyen</em>). Его также почитали как одно из трёх воплощений
            Манджушри и прямое перерождение Вималамитры. Посвятив и обучив высшим тайным учениям
            одновременно более сорока тысяч учеников, он стал называться Манджушри Тибета. Многие из
            его учеников достигли просветления.</p>

        <p>Свои последние годы Лонченпа провел недалеко от монастыря Самье, наблюдая за ремонтом
            ступ и предаваясь созерцанию в пещере Чимпху, служившую ранее прибежищем
            Падмасамбхаве.</p>

        <p>В возрасте пятидесяти шести лет Лонченпа дал ученикам последние наставления и покинул
            этот мир в Уржандзонге, что в Ганри Тодкаре – в своём излюбленном месте. Это событие
            сопровождало много чудесных явлений; все кругом было наполнено благоуханием, воздух
            напоен музыкой, звучащей и снаружи, и внутри домов, распустились цветы, хотя была зима.
            В том месте, где обычно учил Лонченпа, растаяли лёд и снег. Среди его праха после
            кремации были обнаружены пять несгоревших твёрдых, как алмаз, предмета, каждый из
            которых имел свой цвет радуги.</p>

        <p>Благодаря лаконичному, глубоко философскому и во многом новаторскому языку Лонченпы
            учение дзогчена стало более доступным и ясным. Его устные наставления дошли до наших
            дней через цепь выдающихся мастеров дзогчена: это – Уржан Тердаг Лингпа (1634-1714),
            Жигмед Лингпа (1730-1790) и далее – Петрул Ринпоче (р. 1808), Лама Мипам (1848-1912),
            Аджом Бругпа (=тиб. <em>Rig-'dzin Nag-tso-rang-grol</em>), Второй Жамьян Кьенце Ванпо
            (тиб. <em>'Jam-dbyangs mKhyen-brtse'i-dbang-po = Chos-kyi-blo-gros</em>, р. 1896),
            Журмед Дорже (тиб. <em>A-'dzom rGyal-sras 'Gyur-med rDo-rje</em>, p. 1895) и Чогтул
            Ринпоче (тиб. <em>Dar-lhang mChog-sprul Chos-kyi-zla-ba</em>, p. 1893).</p>
    </div>
