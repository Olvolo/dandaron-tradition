        <h2 class="text-3xl font-bold text-center my-8">Терма, или скрытые сокровища</h2>

        <p>«<em>Gter</em>» – это любая находка, пробуждающая высокое устремление к пробуждению.
            Падмасамбхава изложил восемнадцать классов терма, включающих бесчисленное разнообразие
            видов <em>gter</em>. Содержание одной-единственной, любой терма суммирует квинтэссенцию
            всего буддийского учения, излагая своё практическое применение на уровне Внутренних
            Тантр. Поскольку они соотносимы со временем своего обнаружения, они всегда оказываются
            своевременными и оптимальными для текущего настоящего.</p>

        <p>Вообще же термин «<em>gter</em>» приложим ко всему, что достойно сохранения. Терма обычно
            имеет вид текста или фрагмента рукописей, но им может быть статуя, мощи, ритуальный
            предмет, все, что имеет духовную значимость. Терма может быть и природным явлением –
            деревом, скалой, знаком на земле. Терма могут иметь вид серебряных и золотых даров,
            драгоценных камней, которые могут быть обменены, например, на материалы, необходимые для
            постройки храма или ступы. Также терма могут стать ключом для расшифровки текста или
            отрывка, имеющего отношение к Падмасамбхаве и его ученикам.</p>

        <p>После распространения учений, берущих начало в садханах Восьми Херук, Падмасамбхава
            спрятал эти тексты, и они превратились в «спрятанные сокровища» – терма. Таким же
            образом он сокрыл собрание особых текстов Лончен Ньинтиг (тиб. <em>Klong-chen
                snying-thig</em>). Поскольку основные тексты терма, что были позже раскрыты,
            восходят к Падмасамбхаве, его почитают Первым мастером терма. Принцип терма и метод их
            раскрытия характерен исключительно для школы ньингмапа. Большинство тертонов (тиб. <em>gter-ston</em>)
            считаются воплощениями Падмасамбхавы или его учеников – Восьми Великих Ачарий.</p>

        <p>Супруга Падмасамбхавы Ешей Цогьл (тиб. <em>Ye-shes mtsho-rgyal</em>) записала некоторые
            наставления традиции кама и запечатлела в своей памяти. Затем она сложила написанные
            тексты в удобном месте и помогла Падмасамбхаве их спрятать. Эти укрытые сокровища,
            постоянно действующее откровение, позднее находились теми, кто благодаря процессу
            «воплощения» направлялись самим Падмасамбхавой, чтобы найти их в нужном месте и в нужное
            время. Такие тексты и принято называть терма. Бодхисаттва, наделённый особым видом
            просветленности и считающийся воплощением Падмасамбхавы, является тертоном –
            открывателем терма. Он способен восстановить, правильно расшифровать и объяснить теорию
            и практику, содержащиеся в раскрытых текстах или даже в их фрагментах. Говоря западным
            языком, тертона можно назвать кудесником, поскольку он обладает особой духовной силой.
            Благодаря усилиям тертонов возникают новые формы Учения, и само Учение как бы
            видоизменяет свою форму согласно постоянно меняющимся условиям мира.</p>

        <p>Ум человека стремится интерпретировать Дхарму непротиворечивым для себя образом. Из-за
            этого суть Учения может оказаться искажённой, неверно понятой и посему может потерять
            свою духовную силу. Тогда и приходят тертоны, которые очищают, дают новую трактовку и
            насыщают новой энергией значение изначальных Учений.</p>

        <p>Итак, учителя терма не только находят скрытые сокровища, но разъясняют их и порой издают,
            а сам факт обнаружения часто предварительно предсказывается. Например, Падмасамбхава
            предсказал появление трех «старых», восьми «великих», двадцати одного «могущественного»,
            ста восьми «посредничающих» и тысячи «помогающих» тертонов. Для них Падмасамбхавой или
            его учениками был создан намёк, или ключ к расшифровке (тиб. <em>kha-byang</em> или <em>lde-mig</em>),
            терма, указывающий на его происхождение, что всегда облегчало поиск этих новых и
            необычных учений.</p>

        <p>Большинство учений терма были записаны в символической и закодированной форме, известной
            как «язык дакинь». Такая запись не могла быть расшифрована теми, кто не имел на это
            особых полномочий от Падмасамбхавы через процесс «воплощенчества», или эманации (тиб.
            <em>dgongs-gter</em>).</p>

        <p>Обычно помощь приходила от дакинь, они нашептывали тертонам суть найденного текста. Очень
            часто тертону помогало благоприятное окружение или супруга. Более того, многие тертоны
            были психически нацелены просветленным сознанием на воскрешение некоторых особо
            необычных и тайных учений, воспринятых ими часто бессознательно от Падмасамбхавы и
            предназначенных для соответствующих исторических этапов духовной эволюции
            человечества.</p>

        <p>Многие терма уже открыты, но тем не менее есть ещё множество сокровищ, спрятанных
            Падмасамбхавой для настоящего и для грядущих времен. Такие учения надёжно защищены от
            несвоевременного или ложного раскрытия с помощью чисто механических приёмов и формул,
            так называемого «часового механизма» (тиб. <em>gtsug-las-khan</em>) и могут быть
            правильно раскрыты и поняты лишь теми, кто упомянут в предсказаниях или указаниях
            Великих Учителей.</p>

        <p>Ранними формами терма были Сутры Будды Шакьямуни, спрятанные нагами и впоследствии
            раскрытые Нагарджуной во втором столетии, как и было предсказано Блаженным. Но особенно
            принцип непрекращающегося откровения получил широкое распространение именно в
            Тибете.</p>

        <p>Как было предсказано Падмасамбхавой в далёком восьмом веке, то есть в период раннего
            (тиб. <em>snga-dar</em>) распространения Дхармы, терма и тертоны будут появляться
            приблизительно в ХII-ХIII столетиях, в период позднего (тиб. <em>phyi-dar</em>)
            распространения Дхармы. Первыми тертонами того времени были Ньянграл Нима Одсер (тиб.
            <em>Nyang-ral nyi-ma 'od-zer</em>, 1124-1192) и Гуру Шойжи Ванчуг (тиб. <em>Chos-kyi
                dbang-phyug</em>, 1212-1270). Их называют часто Солнцем и Луной, а найденные ими
            терма — Верхними и Нижними Сокровищами (тиб. <em>gTer kha gong 'og</em>). Эти тертоны
            стоят в начале первого периода обнаружения терма. Их находки и терма последующих
            тертонов были собраны и в пятнадцатом столетии изданы Ратна Лингпой (тиб. <em>Ratna-gling-pa</em>).
            Это собрание текстов терма с добавлением Ратна Лингпы получило название «Южного
            Сокровища» (тиб. <em>Lho-gter</em>).</p>

        <p>В середине XV века на севере в правящей фамилии родился учитель Ригзин Годем Жулчан (тиб.
            <em>Rig-'dzin rGod-dem 'phyul-can</em>, 1337-1409). Он считается третьим из так
            называемых старых тертонов. Он сам открыл много терма и впоследствии собрал и издал
            сборник терма, известный как «Северные Сокровища» (тиб. <em>Byang-gter</em>). Три старых
            тертона – Нима Одсер, Гуру Шойжи Ванчуг и Ригзин Годем почитаются как воплощение Ума,
            Речи и Тела Падмасамбхавы.</p>

        <p>Восемью Великими тертонами, или Лингпа (тиб. <em>gling-pa</em>), последовательно
            появившимися, начиная с XIV столетия, были: Ратна Лингпа, Падма (Кунжон, тиб.
            <em>Kun-skyong</em>) Лингпа, Уржан Лингпа (1360), Санжей Лингпа (1340-1396),
            Дорже Лингпа (1346-1405), Карма
            Лингпа (XIV в.), Уржан Дорже Лингпа (1403-1479), Уржан Падма Лингпа (р. 1450).</p>

        <p>Период между Лонченом Рабжампой (1308–1363) и Уржаном Тердаг Лингпой (Журмед Дорже,
            1634-1714) характерен небывалым расцветом традиции терма. Это было время появления Ста
            восьми тертонов-посредников.</p>

        <p>Другими выдающимися лингпа, предсказанными Падмасамбхавой, были Лэйпро Лингпа (тиб. <em>Las-'phro
                gling-pa</em>, он же Агван Шойжал Ванпо (тиб. <em>Ngag-dbang chos-rgyal
                dbang-po</em>), Самтан Дечен Лингпа (тиб. <em>bSam-gtan bDe-chen gling-pa</em>),
            Шигпо Лингпа (тиб. <em>Zhig-po gling-pa</em>, он же Намха Цеван Жалпо – тиб.
            <em>Nam-mkha' tshe-dbang rgyal-po</em>) и Дуддул Лингпа
            (тиб. <em>bDud-'dul ling-pa</em>).</p>

        <p>Выдающимся лингпа был Гунчен Жигмед Лингпа (тиб. <em>Kun-mkhyen 'Jig-med gling-pa</em>,
            1729-1798). Он имел много раз видение Лонченпы и стал составителем и главным издателем
            сборника «Лончен Ньинтиг» (тиб. <em>Klong-chen snying-thig</em>), а также автором
            окончательной редакции фундаментального собрания всех текстов школы ньингмапа «Ньингма
            Жудбум» (тиб. <em>rNying-ma rgyud-'bum</em>).</p>

        <p>Поздними тертонами XIX в. были Шогжур Дечен Жигпо Лингпа (тиб. <em>mChog-gyur
                bDe-chen-zig-po-gling-pa</em>, 1829-1870), Жамьян Кьенце Ванпо (тиб. <em>'Jam-dbyangs
                mKhyen-brtse'i-dbang-po</em>, он же Падма Одсал Догаг Лингпа (тиб. <em>Padma
                'od-gsal-mdo-sngags-gling-pa</em>, 1820-1892) и Жамгон Контрул Лодой Таяй (тиб. <em>'Jam-mgon
                sKong-sprul Blo-gros-mtha'-yas</em> (он же Падма Гарван Ионтан Жамцо – тиб. <em>Padma-gar-dbang
                Yon-tan-rgya-mtsho</em>, 1811-1899)) – главный компилятор текстов терма. Именно
            Жамгон Контрул позже составил 62-томный труд Ринчен Терзод (тиб. <em>Rin-chen
                gter-mdzod</em>), в который вошло в том числе и большинство терма, собранные в
            «Ранний период». Но тем не менее есть много терма, которые не вошли в это собрание.
            Кроме того, и это восхитительно и перспективно, есть множество учений терма, которые ещё
            предстоит раскрыть тертонам из так называемой Тысячи Помогающих тертонов, также
            предсказанных Падмасамбхавой. Многие из них еще не появились.</p>

        <p>Хотя во всех крупных монастырях школы ньингма теории и практике терма уделяется
            значительное внимание, сама традиция значительно шире, чем об этом можно судить по
            литературным памятникам или по монастырским традициям. Такие обители, как Катог (тиб.
            <em>Kah-thog</em>), Миндоллинг (тиб. <em>sMin-grol-gling</em>) и Паюл (тиб.
            <em>dPal-yul</em>), особенно стали известны тем, что в них жили и действовали ведущие
            открыватели терма. В частности, учитель терма Дуддул Дорже (тиб.
            <em>bDud-'dul-rdo-rje</em>, 1615-1672) помогал в реставрации монастыря Катог; Жигмед
            Лингпа работал там над компиляцией собрания текстов ньингмапы – «Ньингма Жудбум» (тиб.
            <em>rNying-ma'i rgyud-'bum</em>), в основном составленном из текстов, которые в то время
            хранились в монастыре Миндоллинг, бывшим в то время резиденцией его основателя Шойжала
            Тердага Лингпы (тиб. <em>Chos-rgyal gTer-bdag-gling-pa</em>).</p>

        <p>Нахождение текстов терма, сохранение этих бесценных наставлений и уникальная возможность
            пользоваться ими при наличии удачной кармы, позволяет проникнуть в удивительное поле
            Внутренних Тантр традиции ваджраяны и, следуя их бесценным методам, эффективно и
            безошибочно обретать просветленное состояние. При этом, преодолевая неумолимый барьер
            времени, можно почувствовать себя современником великих подвижников, чьё творчество не
            подвластно никаким сансарным ограничениям.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Садханы восьми Херук ньингмапы</h2>

        <p>Источником Восьми Херук ньингмапы, как и источником махайоги, является Изначальный Будда
            Самантабхадра, Адибудда. Известно, что ученик Шакьямуни Ананда сохранил Сутры,
            бодхисаттва Ваджрапани собрал и сохранил Внешние Тантры, Дорже Дагпоцал (тиб. <em>rDo-rje
                grags-po-rtsal</em>), получив от Самантабхадры, составил и сохранил Внутренние
            Тантры.</p>

        <p>Дорже Дагпоцал передал Внутренние Тантры дакине Лэйжи Ванмоче (тиб. <em>Las-kyi
                dbang-mo-che</em>). Эта дакиня, видя, что традиция среди людей нуждается в духовной
            поддержке, спрятала Тантры Развития (тиб. <em>bsKyed-rim</em>) и Тантры Завершения (тиб.
            <em>rDzogs-rim</em>), составляющие Три Внутренние Тантры (тиб. <em>Nang-rgyud
                sde-gsum</em>) – махайогу, ануйогу и атийогу – в ступе Дежед Цегпа (тиб. <em>bDe-byed
                brtsegs-pa</em>) на кладбище Шитавана (тиб. <em>bSil-ba'i tshal</em>).</p>

        <p>Лэйжи Ванмоче спрятала в основании ступы «Маяджалатантру» (тиб. <em>sGyu-'phrul
                sde-rgyud</em>), в середине – Дешег Дуйпа (тиб. <em>bDe-gshegs-'dus-pa</em>), а там,
            где ступа указывала на четыре главных направления, соответствующих четырём воротам,
            спрятала Особые Тантры (тиб. <em>Bye-brag sgom-rgyud</em>). В каннелюре (длинной выемке)
            ступы она спрятала «<em>gSang-ba-yongs-rdzogs</em>», в ободке шпиля – Тантру Ранжун
            Раншар (тиб. <em>Rang-byung rang-shar</em>). В середине шпиля спрятала Санжей Ньямжор
            (тиб. <em>Sangs-rgyas-mnyam-sbyor</em>) и на самой верхушке – Янсан Ламед Янти Нагпо
            (тиб. <em>Yang-gsang-bla-med-yang-ti-nag-po</em>), текст, восходящий к самому началу
            распространения атийоги. Хранителями ступы были назначены Праджнядакиня, Кармадакиня,
            другие Защитницы Учения и Виры (Герои). Такова «Традиция скрытых текстов, вверенных
            дакиням» (тиб. <em>mKha'-'gro gtad-rgya'i brgyud-pa</em>).</p>

        <p>Падмасамбхава во время одного из путешествий по Индии посетил на кладбище Шитавана
            описанную Ступу (тиб. <em>bDe-byed btsal-pa</em>, [разночтение с предыдущим названием
            этой же ступы – прим. ред.]), извлёк оттуда текст «Дешег Дуйпа» и проследовал с ним в
            Непал. В пещере Асуров он поклялся всем дакам, дакиням и дхармапалам, что не покинет
            пещеру, пока не обретёт высшие сиддхи. И Лотосорожденный Гуру приступил к созерцанию
            Чемчога (тиб. <em>Chem-chog</em>) Херуки, ознаменовав этим открытие «корзины» учений
            «Дешег Дуйпа». Он настойчиво практиковал методы Совершенной Мысли (тиб. <em>Yang-dag-thugs</em>
            = Чемчог) и Принлэй Пурбы (тиб. <em>'Phrin-las Phur-pa</em>, букв. Деяние Пурбы [(тиб.
            <em>rDo-rje Phur-ba-'phrin-las</em> = Ваджракила]), пока действительно не достиг высших
            сиддх. Во время расширенного созерцания «Дешег Дуйпа» Падмасамбхава обнаружил, что текст
            содержит внутри себя Восемь Садхан. Каждая из них предстала ему и была понята им как
            лепесток лотоса, растущего из небесноподобного купола, и каждая была соотнесена на
            жертвенно-ритуальном уровне с одним из Херук. Проследовав далее в Тибет, в монастырь
            Самье, Падмасамбхава уже имел все Садханы Восьми Херук. Этот бесценный цикл Тайных
            Садхан восходит к тому же источнику реализации Просветления, что содержится и в «Дешег
            Дуйпа».</p>

        <p>Каждая садхана представляет собой текст, содержит описание мандалы, обладает мантрой,
            описанием специальной формы Будды, наделена божественной силой визуализации в
            соответствующей цветовой гамме и одаривает специфическим свойством сознания. Хотя
            главным Видьядхарой (Владыкой Знания) Восьми Садхан Херук был Падмасамбхава, их
            благословляющей силой обладали также Тисрондэцан (в совершенстве овладел созерцанием
            Чемчога), Шантигарбха – владел Херукой Ямантакой (тиб. <em>gShin-rje'i-gshed</em>),
            Нагарджуна – Хаягривой (тиб. <em>rTa-mgrin</em>), Вималамитра – Херукой Дудзи Йонтаном
            (тиб. <em>bDud-rtsi-yon-tan</em>), Ньяг Джнянакумара – Дудзи Йонтаном и Хумкара –
            Херукой Яндагом (тиб. <em>Yang-dag-thugs</em>).</p>

        <p>В обители Чимпху (тиб. <em>mChims-phu</em>) Падмасамбхава дал своим восьми главным
            ученикам посвящения в одну из Восьми Садхан Херук. И каждый из них достиг многого в
            практике своей садханы. Эти восемь учеников впоследствии стали известны как Восемь
            Великих Ачарий (тиб. <em>sLob-dpon chen-po brgyad</em>), или Восемь Видьядхар (тиб. <em>Rig-'dzin
                slob-dpon brgyad</em>).</p>

        <p>Восемь Садхан Херук ньингмапы, распространение которых было начато Падмасамбхавой и его
            учениками, продолжено такими линиями передачи традиции махайоги, как Со (тиб.
            <em>So</em>), Зур (тиб. <em>Zur</em>), Нуб (тиб. <em>gNubs</em>), Ньяг (тиб.
            <em>gNyags</em>), Ма (тиб. <em>rMa</em>) и Ронг (тиб. <em>Rong</em>) – главнейшими
            направлениями распространения цикла Жупрул (тиб. <em>sGyu-'phrul</em> = Майя[джала]).
            Эти Садханы основываются на коренных текстах и обширных комментариях, безошибочно
            ведущих по пути духовного прогресса. Сопутствующие им тексты входят в уже упомянутый
            62-томный «Ринчен Терзод» (тиб. <em>Rin-chen gter-mdzod</em>, букв. Драгоценная
            Сокровищница терма) и являются прототипами многочисленных садхан, практикуемых по всему
            Тибету. В это собрание вошли две работы практического уровня, основополагающие для всей
            школы ньингмапа. Это – «Жупрул Шидо» (тиб. <em>sGyu-'phrul zhi-khro</em>) – «Мирные и
            Гневные (божества) Майя (джалы)» и «Жуджал Санба Ньингпо» (тиб. <em>rGyud-rgyal gsang-ba
                snying-po</em>) – «Тайная Сущность, или Царь Тантры». Символика этих работ включает
            пятьдесят восемь мирных и сорок два гневных божеств, составляющих вместе «Сто мирных и
            гневных божеств-хранителей» (тиб. <em>Zhi-khro-dam-pa-rigs-brgya</em>).</p>
    </div>

