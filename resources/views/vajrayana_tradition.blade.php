            от друга методы духовного развития — <em>яны.</em> При жизни Будды и при жизни его
            непосредственных учеников просветление достигалось часто просто при слушании проповеди,
            при обдумывании услышанного, при соединении учения с жизнью в личном опыте. В ранний
