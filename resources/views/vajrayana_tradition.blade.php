        <h2 class="text-3xl font-bold text-center my-8">Традиция ануйоги</h2>

        <p>Традиция До (тиб. <em>mDo</em>), или ануйога (тиб. <em>Lung-a-nu-yo-ga'i-theg-pa</em>),
            была проповедана Ваджрапани на горе Малайя собранию Видьядхар (тиб. <em>rig-'dzin</em>).
            После этой проповеди ануйога передавалась из уст в ухо среди бодхисаттв мира людей на
            земле. Так, с благословения Ваджрапани царь Жалпо-раджа (тиб. <em>rGyal-po-ra-ja</em>,
            Индрабодхи) имел семь видений, посвятивших его в смысл священных текстов ануйоги.
            Существует пять основных текстов ануйоги, но самыми главными считаются два коренных:
            Мулатантра «Кундуй Ригпаидо» (тиб. <em>Кun-'dus rig-pa'i mdo</em>) – коренная тантра и «<em>bShad-rgyud</em>»
            (тиб. <em>mDo-dgongs-pa 'dus-pa</em>) – разъясняющая тантра. Посвятив царя в суть этого
            учения, видьядхара Генен Лицаби Дримеддаг (тиб. <em>dGe-bsnyen Li-tsa-bi
                Dri-med-grags</em>) устно объяснил ему полностью её значение. Индрабодхи, в свою
            очередь, передал учение ануйоги сыну – Индрабодхи Младшему; через посредничество
            нескольких учителей тексты ануйоги попали к Дхармабодхи (Бодхидхарме) и Васудхаре. В
            дальнейшем учение ануйоги распространилось на территории Туркестана (тиб.
            <em>Bru-sha</em>), где их воспринял настоятель Руче Цанжей (тиб. <em>Ru-che
                bTsan-skyes</em>).</p>

        <p>Преемственность учителей, идущая от Индрабодхи, начинается с Кукураджи, воспринявшего
            традицию от учителя через Нагапутру и Гаясутри. Кукураджа передал традицию Веталакшеме
            (тиб. <em>Ro-langs-bde-ba</em>), чьим перерождением был Нирманакая Гараб Дорже. После
            обретения высших духовных достижений (сиддхи) Веталакшема издал «Солнце Знания йоги»
            (тиб. <em>rNal-'byor-rig-pa'i-nyi-ma</em>) – текст, вводящий в глубинный Путь Сутр Будды
            (тиб. <em>mDo'i-zab-lam</em>). Его двумя учениками были Ваджрахаста (тиб. <em>rDo-rje-bzhad-pa</em>)
            и Прабхахасти (тиб. <em>Pra-ba-ha-sti</em>) – царь Захора. Прабхахасти, получив на
            берегу Синдха посвящение, вступил в монашество, приняв имя Шакьяод (тиб.
            <em>Shakya-'od</em>). Достойными его учениками были: Шакьяод Младший, Шакья Шейнен (тиб.
            <em>Shakya-bshes-gnyen</em>, Шакьямитра) и Шакья Сэнге (тиб. <em>Shakya-seng-ge</em>).
        </p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Шакья Шейнен</h2>

        <p>Шакья Шейнен достиг высокого уровня понимания Тантр и составил комментарий на «Дуйпидо»
            (тиб. <em>'Dus-pa'i-mdo</em>, коренной текст ануйоги, сокращение от «Кундуй Ригпаидо» –
            тиб. <em>Kun-'dus rig-pa'i mdo</em>), называемый также «Украшением Косалы» (тиб. <em>Ko-sa-la'i-rgyan</em>),
            поскольку пришёл из страны Косала.</p>

        <p>У Шакьи Шейнена учился сутрам Дханаракшита, сам же учил Хумкару (тиб. <em>rDo-rje
                Hum-mdzad</em>) в Алмазной пещере Падмасамбхавы. Дханаракшита написал «Сто семь
            комментариев» на Мулатантру (тиб. <em>rTsa-rgyud</em>), на «Светильник йоги» (тиб. <em>rNal-'byor-pa'i
                sgron-ma</em>) и другие тексты. Хумкара, получив в пещере Асуров на границе Индии с
            Персией наставления от Гагасиддхи (тиб. <em>Ga-ga-si-dhi</em>), стал Видьядхарой.
            Практикуя метод Чудлен (тиб. <em>bcud-len</em>, букв. «Сущность соков»), Хумкара стал
            совершенно невидимым.</p>

        <p>Двумя основными учениками Хумкары были Стхирамати и Сукхапрасанна (тиб. <em>bDe-ba-gsal-mdzad</em>).
            Сукхапрасанна написал много работ, в том числе и «<em>mDo'i yig-sna bco-brgyad-dang
                rnal-'byor-gyi rim-pa theg-chen sgron-ma</em>».</p>

        <p>У Сукхапрасанны было четыре основных ученика:</p>

        <ul class="list-none pl-2 space-y-1 ml-2">
            <li>1) Дхармабодхи из Магадхи, который был учителем сутр (тиб. <em>mDo'i-mkhan-po</em>)
                и составителем трёх сочинений «<em>mDo'i-dob bsdu-ba</em>», «<em>Shes-rab
                    sgrom-ma</em>» и «<em>bKol-mdo</em>»;
            </li>
            <li>2) Дхармараджапала – настоятель (тиб. <em>mkhan-po</em>) монастырского университета
                в Наланде, получал от Сукхапрасанны устные наставления в тантре;
            </li>
            <li>3) Васудхара – царь Непала, получал Учение также и от Дхармаракшиты; и</li>
            <li>4) Цуглаг Палде (тиб. <em>Tsug-lag-dpal-dge</em>).</li>
        </ul>
        <p>Сукхапрасанна передал названным четырём ученикам три аспекта традиции: посвящение
            (тиб. <em>dbang-bka'</em>), пояснение (тиб. <em>bshad-ра</em>) и руководство тиб. <em>(man-ngag-bka'</em>).
        </p>
        <p>Дханаракшита, ученик Шакьи Шейнена, был приглашен наставником Руче Цанжеем из Туши
            (тиб. <em>Bru-sha</em>, Туркестан) для перевода сочинения «mDo-dgongs-pa 'dus-pa'i mdo».
            Однако он не
            смог закончить перевод и, вернувшись в Непал, упросил Васудхару и Дхармабодхи
            отправиться в Тушу, чтобы при участии наставника Руче Цанжея продолжить перевод.</p>
    </div>
    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <h2 class="text-3xl font-bold text-center my-8">Нубчен Санжей Ешей</h2>

        <p>Автор комментария на эту работу Нубчен Санжей Ешей (тиб. <em>gNubs-chen Sangs-rgyas
                Ye-shes</em>) был непревзойденным мастером распространения ануйоги в Тибете.
            Традицию До (ануйоги) он принял в основном от Дхармабодхи, Васудхары и Руче Цанжея.
            Среди многих его сочинений есть одно особо важное, содержащее проясняющее толкование
            ануйоги, называется оно «Самтан мигдон» (тиб. <em>bSam-gtan-mig-sgron</em>). Главными
            учениками Нубчена Санжей Ешея были Пейгур Лончен Пагпа (тиб. <em>sPa-gor
                Blon-chen-'phags-pa</em>), Ду Легпидонма (тиб. <em>Gru Legs-pa'i-sgron-ma</em>), Нан
            Йонтанчог (тиб. <em>Ngan Yon-tan-mchog</em>), Со Ешей Ванчуг (тиб. <em>So Ye-shes
                dBang-phyug</em>) и Кулунпа Йонтан Жамцо (тиб. <em>Khu-lung-pa Yon-tan
                rGya-mtsho</em>). Йонтан Жамцо был наиболее выдающимся учеником Нубчена, линию
            преемственности продолжил его сын Ешей Жамцо (тиб. <em>Ye-shes rGya-mtsho</em>).</p>

        <p>Затем эта традиция перешла к знаменитым Трём Зурам (тиб. <em>Zur-rnam-pa-gsum</em>),
            далее к Марпа-лоцзаве (1012-1096), к Шойжи Сэнге (тиб. <em>Chos-kyi-seng-ge</em>) и к
            блистательному Уржану Тердагу Лингпе (тиб. <em>O-rgyan gTer-bdag-gling-pa</em> или тиб.
            <em>'Gyur-med rDo-rje</em>). Несколько раз эта традиция, переплетаясь, сливалась с
            традицией «Гухьямулагарбхатантры» (махайога).</p>

        <p>Существует одна особая и редкая теперь традиция, которую Нубчен Санжей Ешей получил от
            Падмасамбхавы в стиле передачи Ка-Чимпума (тиб. <em>bKa' mChims-phu-ma</em>).
            Текстуально эта линия базируется на комментарии Нубчена Санжей Ешея, продиктованном ему
            Падмасамбхавой; комментарий называется «<em>bShad-rgyud mDo-dgongs 'dus-pa</em>». Это
            важный для ануйоги и практически ориентированный текст. В течение долгого периода между
            Нубченом Санжей Ешеем и Катогом Кадампа Дешегом (тиб. <em>Kah-thog Kah-dam-pa
                bDe-gshegs</em>) эта традиция не прослеживается.</p>

        <p>В XIX в. сиддха Катог необычным способом открыл этот текст. Он в совершенстве восстановил
            комментарий Нубчена в результате усиленного созерцания статуи самого Нубчена Санжей
            Ешея, содержащей драгоценные останки учителя. В предании говорится, что статуя,
            мистически оживленная, передала устно и тайно Катогу-сиддхе все содержание текста,
            которое тот незамедлительно записал. От Катога-сиддха эту традицию принял Канчен Легшад
            Жордан (тиб. <em>mKhan-chen Legs-bshad 'byor-ldan</em>) и, в свою очередь, передал её
            Канпе Тхубге (тиб. <em>mKhan-po Thub-dga'</em>) из традиции Петрула Ринпоче (тиб. <em>dPal-sprul
                Rin-po-che</em>).</p>

        <p>Канпа Тхубга передал традицию племяннице Лхандуцапо (тиб. <em>lHan-du-tsha-po</em>),
            которая затем передала её Шечену Конгтулу (тиб. <em>Zhe-chen Kong-sprul</em>, его другое
            имя – тиб. <em>Padma-dri-med-legs-pa'i-blo-gros</em>, родился в 1901 г.).</p>

        <p>Нубчен Санжей Ешей является важнейшим связующим звеном в линии передачи традиции
            ньингмапы и в традиции объяснения сокровенных текстов этой школы. В сущности, вся
            полнота передачи кама (тиб. <em>bka'-ma</em>) проходит через него или через членов его
            семьи. Нубчен Санжей Ешей был не только учеником Падмасамбхавы; много учений и практик
            он получил от Ешей Цогьял (тиб. <em>Ye-shes mTsho-rgyal</em>), духовной жены Гуру
            Ринпоче, которая во время репрессий царя Ландармы (тиб. <em>Glang Dar-ma</em>) несколько
            лет проживала среди родственников Нубчена Санжей Ешея. По преданию, он прожил около трёх
            сроков человеческой жизни, то есть около ста десяти лет.</p>

        <p>Во времена царя Ралпачана (тиб. <em>Ral-pa-can</em>, 814-836) было два типа общин –
            Красная сангха и Белая сангха. Красная сангха состояла полностью из посвященных монахов,
            носивших красные одеяния; Белая сангха состояла в основном из йогинов и старейшин родов,
            они во время официальных собраний облачались в белые одеяния и, как правило, носили
            длинные волосы. Большинство учеников Падмасамбхавы принадлежали к семейным, родовым
            традициям Белой сангхи. Для приверженцев ньингмапы принятие монашеского сана и полных
            обетов Винаи не было обязательным, ибо они считали, что духовное совершенствование и
            сострадание можно практиковать и в обычном потоке светской жизни, а не только за мощными
            стенами монастырей.</p>

        <p>При великодушной поддержке царя Ралпачана, которую он оказывал буддийской общине, Дхарма
            получила возможность влиять на жизнь общества. Этим он возбудил безумную ненависть у
            сводного брата – Ландармы, который затеял и осуществил убийство Ралпачана. Подыгрывая
            бонским жрецам, которые издревле были вассалами тибетских царей, Ландарма добился
            власти. Так он восстановил на короткое время древнюю иерархическую триаду:
            царь-придворный первосвященник-шаман в её правящей активности. Этот злодей-узурпатор
            постоянно демонстрировал ненависть к буддизму и к монашеской общине. Всякий раз, когда
            на его пути встречалась буддийская статуя, он издевательски приказывал ей говорить, и
            если статуя безмолствовала, он приказывал отрубить ей нос или палец. Эти частично
            изуродованные статуи в окрестностях Лхасы простояли много веков, свидетельствуя о
            трагедии этого темного периода в истории буддизма в Тибете. Ландарма приказывал казнить
            любого монаха Красной сангхи, не признававшего его антибуддийских указов. Но при этом он
            щадил членов Белой сангхи, так как считал их домохозяинами, а не монахами.</p>

        <p>Однажды Ландарма путешествовал далеко от Лхасы в поисках Нубчена Санжей Ешея, который в
            то время возглавлял значительную часть Белой сангхи. Царь хотел проверить слухи, что эта
            большая и процветающая сангха хранит у себя множество священных текстов и прибегает к
            практике устного посвящения в Дхарму. Когда царь прибыл в главное место коллективных
            практик (затворнический центр), Нубчен Санжей Ешей без тени страха предстал перед ним и,
            указывая на царя, исторг из конца пальца сноп света, и тут же появился гигантский,
            размером с яка, скорпион. Такое чародейство так напугало царя, что он пообещал больше
            никогда не чинить никаких препятствий Нубчену Санжей Ешею и его общине. Он также издал
            соответствующий указ.</p>

        <p>Именно в это время Нубчен Санжей Ешей и его соратники спасли от уничтожения большое
            количество древних текстов и ранних переводов. Деятельность этого знаменитого сиддха,
            ученого и переводчика сокровенных учений, идущих от Падмасамбхавы, знаменует конец
            раннего периода распространения Дхармы в Тибете. Этот заключительный этап представлял
            собой ряд сменяющих друг друга поколений семейной традиции клана Нуб (тиб.
            <em>gNubs</em>), состоящий из блистательной плеяды сиддхов, ученых, йогинов,
            созерцателей, переводчиков и мирских последователей Дхармы. Они смогли сохранить в
            течение почти полувека подавления буддизма в Тибете и устную передачу, и сами тексты.
        </p>
    </div>
