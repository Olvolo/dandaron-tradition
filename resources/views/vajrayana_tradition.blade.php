            <li>— монахини, бхикшуни (тиб. <em>dge-long-ma</em>),</li>
            <li>— миряне, упасака (тиб. <em>dge-bsnyen</em>),</li>
            <li>— мирянки, упасика (тиб. <em>dge-bsnyen-ma</em>).</li>
