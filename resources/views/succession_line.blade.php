            <p class="mb-3" id="citation-23"><sup>23</sup> Джампал Гьяцо (тиб. <em>'jam dpal
                    rgya
                    mtsho</em>) или Дубхан Гелег Гьяцо (тиб. <em>grub khang dge
                    legs rgya mtsho</em>) — выдающийся учёный и практик школы Гелуг, родился
                в 1641
                году (год Железной Змеи одиннадцатого 60-летнего цикла) в Чаре Сангкара
                (<em>lcags
                    ra sangs dkar</em>) в Верхнем Нгари (предположительно в районе Занскар,
                Ладакх).
                Его отца звали Гуру Дкончог (<em>gu ru dkon mchog</em>), а мать — Бумо Чог
                (<em>bu mo mchog</em>). В детстве его звали Мигза (<em>dmig za</em>). Когда
                Гелегу
                Гьяцо исполнилось шесть лет, его отец скончался. Впоследствии мать тяжело
                заболела
                (по некоторым данным, туберкулезом или тяжёлой формой бадкан — болезни
                слизи). Юный
                Гелег ухаживал за ней, и тогда же, в домашних условиях, начал своё обучение
                чтению и запоминанию молитв. После её смерти (в возрасте около семнадцати
                лет) он
                провел все необходимые погребальные ритуалы в соответствии с местными
                буддийскими
                традициями.<a href="#ref-23" class="text-blue-500 hover:underline">↩</a></p>
            <p>Вскоре после этого юный Мигза отправился в путь. Он посетил монастырь Джампа
                Линг
                (<em>byams pa gling</em>) в Тибете, где принял обеты упасаки (мирянина) от
                Лобпона
                Лобзанга Лекпы (<em>slob dpon blo bzang legs pa</em>) и обучался там около
                двух лет</p>
            <p>Направляясь в Центральный Тибет (У-Цанг) для дальнейшей учебы, по пути он
                посетил
                Ташилхунпо (<em>bkra shis lhun po</em>), где удостоился аудиенции у
                Четвёртого
                Панчен-ламы, Лобзанга Чокьи Гьялцена (<em>pan chen bla ma 4 blo bzang chos
                    kyi
                    rgyal mtshan</em>, 1570–1662).</p>
            <p>Затем он прибыл в Шанг Гоцанг (<em>shangs rgod tshang</em>), где принял обеты
                послушника (гецул, <em>dge tshul</em>) от наставника Друнгпы Цондру Гьялцена
                (<em>drung
                    pa
                    brtson 'grus rgyal mtshan</em>) и получил монашеское имя Гелег Гьяцо.
            </p>
            <p>Некоторое время он жил в монастыре Сэра (<em>se ra</em>), а затем, по
                рекомендации
                своего наставника Друнгпы Цондру Гьялцена, поступил в дацан Дагпо (<em>dwags
                    po
                    grwa tshang</em>), где под руководством Джецуна Лодро Тенпы (<em>rje
                    brtsun blo
                    gros brtan pa</em>) и Лхадинга Лобзанга Дондрупа (<em>lha sdings blo
                    bzang
                    don grub</em>) в течение шестнадцати лет изучал Пять основных трактатов
                Гелуг: Праманаварттику, Абхисамаяламкару, Мадхьямаку, Винаю и
                Абхидхармакошу.</p>
            <p>Администрация дацана Дагпо попыталась назначить его на должность надзирателя
                за
                дисциплиной (<em>dge bskos</em>) и руководителя обхода (<em>grwa skor</em>),
                но
                Гелег
                Гьяцо, не желая принимать эту должность, тайно покинул монастырь.</p>
            <p>Он вернулся в Шанг Гоцанг, где вновь встретился с Друнгпой Цондру Гьялценом и
                принял обеты полного
                монашеского посвящения (гелонг, <em>dge slong</em>). Он также получил
                посвящения,
                передачи и комментарии к учениям Ламрим, Вэнса Ньенгью (<em>dben sa snyan
                    brgyud</em>), Гандэн
                Махамудра (<em>dga' ldan phyag chen</em>) и Шести Учений Наропы от Тафукпы
                Лобзанга
                Дамчо Гьялцена (<em>rta phug pa blo bzang dam chos rgyal mtshan</em>, р.
                1595/1607).
            </p>
            <p>По настоянию своего учителя, Гелег Гьяцо вернулся в дацан Дагпо, чтобы
                официально
                объяснить свою позицию и получить разрешение на уход. Он был официально
                освобожден
                от обязанностей после того, как прочитал монахам комментарий к тексту
                Цонкапы
                «Различение достоверного и недостоверного» (<em>Drang ba dang nges pa’i don
                    rnam
                    ’byed</em>).</p>
            <p>После кончины Друнгпы Цондру Гьялцена, Гелег Гьяцо отправился в Цанг, где
                получил
                учения в Ташилхунпо от его главных учеников — Друнгпы Тафукпы Лобзанга Дамчо
                Гьялцена и монгольского ламы Кхачена Лобзанга Дондрупа (<em>hor dka' chen
                    blo bzang
                    don grub</em>). Он также получил наставления от многих других лам,
                включая
                Лобпона Джампу Чодена (<em>slob dpon byams pa chos ldan</em>) по практикам
                Чод.</p>
            <p>Вернувшись в Центральный Тибет, он встретился с настоятелем монастыря Сэра
                Дже, Джо
                Таном Сонамом Гьялценом (<em>jo stan bsod nams rgyal mtshan</em>), который
                предложил ему уединиться в ските (<em>sgrub khang</em>) в горах
                над Сэра, известном как Сэра Уцэ (<em>se ra dbu rtse</em>). В этом месте,
                как
                считается, медитировал сам Цонкапа. Гелег Гьяцо прожил в этом ските, ведя
                аскетический образ жизни, до пятидесятилетнего возраста (до или после 1692
                года),
                после чего начал свои странствия.</p>
            <p>Как странствующий лама, он совершил паломничество во многие священные места,
                включая
                Олкха (<em>'ol kha</em>), Гьял (<em>rgyal</em>), Дагпо (<em>dwags po</em>),
                Цари
                (<em>tsa ri</em>) и другие.</p>
            <p>В 1705 году (в возрасте 64 лет) Гелег Гьяцо вернулся в Сэра и основал первый
                из трёх
                своих ретритных центров (Друпкханг): Сэра Уцэ (<em>se ra dbu rtse</em>),
                рассчитанный на 17 монахов-гелонгов.</p>
            <p>Вторым центром стал Пурбучок (<em>phur lcog sgrub khang</em>), основанный в
                пещере
                Дочунг Чонгжи (<em>rdo cung cong zhi</em>) в Пурчоке, где, по преданию,
                практиковал
                Падмасамбхава. Он построил здесь храм, посвященный божествам трёх семейств
                (<em>rigs gsum mgon po’i lha khang</em>). Позднее он расширил это место до
                полноценного ретритного
                центра. Легенда связывает место Пурбучока с пророческим сном Гелега Гьяцо,
                который
                указал на необходимость строительства дома у белой ступы, отождествлённой с
                узкой
                вершиной Пурчока.</p>
            <p>Третьим ретритным центром был Ракадраг (<em>ra kha brag</em>),
                предназначенный для
                двенадцати монахов, построенный вокруг пещер, связанных с Цонкапой Лобзангом
                Дракпой
                (1357–1419), Кхедрубдже Гелег Пэлзангом (1385–1438) и Гьялцабдже Дарма
                Ринченом
                (1364–1432).</p>
            <p>Для управления этими тремя центрами он составил Устав Ретритного Сообщества
                (<em>sgrub sde'i bca' yig</em>). Предположительно, именно после основания
                этих
                центров он стал известен как Друпкханг Гелег Гьяцо.</p>
            <p>Среди многочисленных учеников Друпкханга Гелега Гьяцо наиболее выдающимся был
                Пурчок
                Агван Джампа (<em>phur lcog ngag dbang byams pa</em>, 1682–1762), которому
                он
                поручил управление ретритным центром Пурчок. Среди других известных учеников
                —
                Кхардо Зона Гьяцо (<em>mkhar rdo bzod pa rgya mtsho</em>, 1672–1749), Агван
                Чогден
                (настоятель дацана Гьюме и учитель Далай-ламы), 49-й Гандэн Трипа, Тричен
                Лобзанг
                Даргье (<em>co ne blo bzang dar rgyas</em>, 1662–1723), а также 25-й
                настоятель
                Кумбума, Агван Тенпа (<em>ngag dbang bstan pa</em>, 1683–1746).</p>
            <p>Его литературное наследие включает:<br>
                – Обширный комментарий: «Зеркало, ясно раскрывающее суть Прекрасного
                изложения
                различения достоверного и недостоверного» (<em>Drang ba dang nges pa'i don
                    rnam par
                    'byed pa'i bstan bcos legs par bshad pa'i snyin po'i dgongs pa'i don
                    gsal bar
                    byed pa'i me long</em>).<br>
                – Устав Ретритного Сообщества (<em>sgrub sde'i bca' yig</em>) для трех
                основанных им
                центров.<br>
                – Гурма (<em>mgur ma</em>) — поэтическая религиозная песня.</p>
            <p>Друпкханг Гелег Гьяцо ушел в нирвану в 1713 году (год Водяной Змеи
                двенадцатого
                шестидесятилетнего цикла) в возрасте семидесяти трех лет.</p>
            <!-- Разделительная черта -->
            <hr class="my-8 border-t-1 border-sky-500">

            <p class="mb-3" id="citation-24"><sup>24</sup> 54-й Гандэн Трипа Агван Чогден
                (тиб. <em>ngag dbang mchog ldan</em>, 1677–1751)<a href="#ref-24"
                                                                   class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>Агван Чогден родился в Чанца Кармо Пук (<em>gcan tsha dkar mo phug</em>) близ
                монастыря Джакхюнг (<em>bya khyung dgon</em>) в Амдо в 1677 году, в год
                Огненной
                Змеи одиннадцатого шестидесятилетнего цикла. Его отца звали Чакдор Таши
                (<em>phyag rdor bkra shis</em>), а мать — Почог Кьи (<em>spo chog skyid</em>).
            </p>

            <p>В возрасте одиннадцати лет Агван Чогден поступил в монастырь Джакхюнг
                (<em>bya khyung dgon pa</em>) и принял обеты отречения от мирской жизни и
                послушника
                (<em>rab byung</em> и <em>dge tshul</em>) от своего дяди-монаха Агвана
                Цултрима
                (<em>ngag dbang tshul khrims</em>). Дядя также дал ему базовое обучение,
                такое как
                чтение, письмо и запоминание молитв. Он также запомнил коренные тексты
                некоторых
                философских сочинений и в возрасте тринадцати лет начал изучать логику под
                руководством своего дяди. В некоторых источниках его биографии ошибочно
                указывается,
                что он принял обеты от 61-го Гандэн Тричена, Агвана Цултрима, который
                родился более
                чем на сорок лет позже Агвана Чогдена.</p>

            <p>В 1691 году, в возрасте пятнадцати лет, Агван Чогден отправился в У-Цанг и
                поступил
                в Монастырский университет Сэра (<em>se ra grwa tshang</em>), где изучал
                пять
                основных предметов учебной программы Гелуг: Абхисамаяламкару, Мадхьямаку,
                Абхидхармакошу, Праманаварттику и Винаю, уделяя особое внимание
                Праджняпарамите,
                Ламриму и трактатам Кадам.</p>
            <p>В возрасте двадцати лет он сдал традиционный экзамен по Праджняпарамите в
                Сангпу
                Неуток (<em>gsang phu ne'u thog</em>) и получил признание среди ученых в
                этой
                области. С двадцати двух лет он изучал другие основные тексты и сдал большой
                экзамен
                на Лхаса Монлам Ченмо среди более чем двадцати тысяч монахов.</p>
            <p>Впоследствии, по-видимому, не чувствуя удовлетворения в Сэра, он заявил, что
                предпочтет жизнь джадрел (<em>bya bral</em>), что означает «свободный от
                мирской
                деятельности», и покинул монастырь, чтобы жить странствующим монахом. Однако
                вскоре
                он отправился в Ташилунпо (<em>bkra shis lhun po</em>) и принял обеты
                полного
                посвящения (<em>dge slong</em>) от Пятого Панчен-ламы, Лобзанга Еше
                (<em>pan chen bla ma 5 blo bzang ye shes</em>, 1663–1737).</p>
            <p>Агван Чогден вернулся в Лхасу из Цанга и поступил в дацан Гьюме
                (<em>rgyud smad grwa tshang</em>), где изучал тантру, особенно тантру
                Гухьясамаджи
                с практиками стадии зарождения и стадии завершения (<em>bskyed rdzogs</em>),
                а также
                другие смежные темы. Затем он сдал традиционный экзамен по тантре, который
                успешно
                прошел. После этого, в возрасте двадцати девяти лет, он был назначен на
                должность
                надзирателя за дисциплиной (<em>dge bskos</em>) Гьюме, но, предположительно,
                в том
                же году, 1705, вернулся в свой родной регион Амдо.</p>
            <p>Вскоре после прибытия в Амдо Агван Чогден сдал традиционные экзамены по
                полным
                текстам основных предметов во всех крупных монастырях региона, включая
                Джакхюнг,
                Кумбум (<em>sku 'bum</em>), Гонлунг Джампа Линг (<em>dgon lung byams pa
                    gling</em>)
                и Цэнпо (<em>btsan po</em>), и его слава быстро распространилась по региону.
                Он построил ряд из восьми ступ будд (<em>bde gshegs brgyad kyi mchod
                    rten</em>)
                для своих родителей. Примерно через четыре года, проведенных на родине, он
                вернулся
                в Лхасу.</p>
            <p>В 1710 году, когда ему было тридцать четыре года, правительство Лхасы
                назначило
                Агвана Чогдена настоятелем монастыря Толинг (<em>mnga' ris mtho lding dgon
                    pa</em>)
                в Нгари, где он прослужил семь лет. Помимо своих обязанностей по дарованию
                учений и
                посвящений, проведению дхармических мероприятий и руководству религиозными
                праздниками, он восстановил большое количество храмов и ступ, построенных
                Ринченом
                Зангпо (<em>rin chen bzang po</em>, 958–1055). Затем он ушел с поста
                настоятеля
                Толинга и вернулся в Лхасу через гору Кайлас, где, как говорят, он оставил
                свой
                след на камне.</p>
            <p>Девять лет спустя, в возрасте сорока трех лет, Агван Чогден был назначен
                правительством Лхасы настоятелем дацана Гьюме, пост, который он занимал в
                течение
                десяти лет, преподавая тантру всех уровней в соответствии с их традицией. Он
                также
                восстановил Санг Нгаг Кхар (<em>gsang sngags mkhar</em>) в 1722 году.</p>
            <p>В 1728 году он был назначен наставником, особенно по тантре, для Седьмого
                Далай-ламы,
                Келсанга Гьяцо (<em>ta la'i bla ma 7 bskal bzang rgya mtsho</em>,
                1708–1757),
                который тогда находился в изгнании в Гартаре (<em>mgar thar</em>) в Кхаме.
                Он даровал Далай-ламе много важных передач, включая Ваджрамалу Митры,
                Калачакру и
                избранные темы из Ганжура и Данжурара. Он также давал много учений Третьему
                Чанкья Ролпей Дордже (<em>lcang skya 3 rol pa'i rdo rje</em>, 1717–1786).
                Его
                служение Далай-ламе принесло ему титул «a chi thu no mon han» от Императора
                Цяньлуна
                в Пекине.</p>
            <p>В 1739 году, в год Земляной Овцы двенадцатого шестидесятилетнего цикла, в
                возрасте
                шестидесяти трех лет, Агван Чогден был возведен на трон как 54-й Гандэн
                Трипа
                монастыря Гандэн (<em>dga' ldan</em>), уйдя с этого поста в 1746 году.</p>
            <p>Среди его учителей были Жамьянг Жэпай Дордже
                (<em>'jam dbyangs bde ba'i rdo rje</em>, 1682–1741), Первый Жамьянг Жэпа
                (<em>'jam dbyangs bzhad pa 1 'jam dbyangs bzhad pa'i rdo rje</em>,
                1648–1721/22) и
                50-й Гандэн Трипа, Тричен Гендун Пунцок
                (<em>dga' ldan khri pa 50 khri chen dge 'dun phun tshogs</em>, 1648–1724).
            </p>
            <p>Он служил учителем для многочисленных выдающихся лам, включая Лонгдол Ламу
                Агвана
                Лобзанга (<em>klong rdol bla ma ngag dbang blo bzang</em>, 1719–1794);
                Третьего
                Чубзанга Агвана Тубтена Вангчука
                (<em>chu bzang 3 ngag dbang thub bstan dbang phyug</em>, 1725–1796), который
                был
                28-м и 30-м держателем трона монастыря Лабранг (<em>bla brang</em>);
                40-го настоятеля Джакхюнг, Лобзанга Чодрака (<em>bya khyung mkhan rabs 40
                    blo bzang
                    chos grags</em>); Кангьюрва Сонама Зангпо (<em>bka' 'gyur ba bsod nams
                    bzang
                    po</em>) и Шестого Пагпа Лха Джигме Тенпай Гьяцо (<em>'phags pa lha 6
                    'jigs
                    med bstan pa'i rgya mtsho</em>), а также многих других.</p>
            <p>По его просьбе Седьмой Далай-лама передал монастырь Рэтинг
                (<em>rwa sgreng dgon pa</em>) Тричену Агвану Чогдену после его ухода на
                покой, и он
                работал над развитием монастыря до самой своей смерти в 1751 году, в год
                Железной
                Овцы тринадцатого шестидесятилетнего цикла, в возрасте семидесяти пяти лет.
                Его
                ступа (реликварий) была установлена слева от Джово Джампел Дордже
                (<em>rjo 'jam dpal rdo rje</em>) в монастыре Рэтинг.</p>
