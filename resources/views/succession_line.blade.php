            <p class="mb-3" id="citation-10"><sup>10</sup> Будон Ринчендуб (<em>bu ston rin chen
                    grub</em>, 1290–1364) — тибетский учёный, настоятель монастыря Жалу, редактор
                буддийского канона.<a href="#ref-10" class="text-blue-500 hover:underline">↩</a></p>
            <p>Будон Ринчендуб (<em>Bu ston Rin chen grub</em>) — один из самых влиятельных
                буддийских мастеров Тибета — родился в 1290 году (год Железного Тигра) в районе
                Тропу (<em>Khro phu</em>) в Цане. Его отец, Драктон Гьялцан Пелзанг, и мать, Сонам
                Бум, оба были практикующими школы Ньингма. С раннего детства Будон получал
                всестороннее образование: сначала от матери (чтение, письмо), затем от учителя
                Гьямсенга (математика), а также от деда, Цултрима Пелзанга — ньингмапинского ламы из
                традиции Нуб, передавшего ему учения Гухьясамаджи, Дзогчен и медицину.</p>
            <p>В 18 лет он принял обеты послушника (<em>dge tshul</em>) от двух учителей: Тропу
                Кхенчен Ринчен Сенге (<em>khro phu mkhan chen rin chen seng ge</em>) — настоятеля
                монастыря Тропу, и Геше Сонам Гона (<em>dge bshes bsod nams mgon</em>, также
                известного как <em>Ācārya Riktikpa</em>), который обучил его Праджняпарамите,
                Винайе, логике и устным наставлениям Дзогчен.</p>
            <p>Будон проявил незаурядную способность к усвоению сложнейших текстов: он освоил
                множество версий Праджняпарамиты, <em>Pañcaviṃśatisāhisrikāloka</em>, <em>Yogācārabhūmi</em>,
                <em>Mahāyānasaṃgraha</em>, <em>Abhidharmasamuccaya</em>, <em>Pramāṇasamuccaya</em>,
                <em>Pramāṇaviniścaya</em> и другие классические санскритские сочинения. Он учился у
                многих учителей разных школ: Дондруп Пел (<em>don 'grub dpal</em>) дал ему учения
                Хеваджры, Праджняпарамиты и <em>Śikṣāsamuccaya</em>; Кабжипа Кончог Жонну (<em>bka'
                    bzhi pa dkon mchog gzhon nu</em>) передал ему посвящения Авалокитешвары в
                традиции Кадам; Тишри Кунзанг (<em>ti shri kun bzang</em>) обучал его Мадхьямике;
                его отец передал ему учения «Тантры Подземного Ваджры» (<em>rdo rje sa 'og gi
                    rgyud</em>), связанной с достижением подземных царств (<em>pātālasiddhi</em>), и
                практики Ваджрапани. Он также получил тантрические передачи от Пелден Сенге (<em>dpal
                    ldan seng ge</em>), Дордже Гьялцена (<em>rdo rje rgyal mtshan</em>), Токден
                Сорин (<em>rtogs ldan bsod rin</em>) и Намнангва Йонтен Жамцо (<em>rnam snang ba yon
                    tan rgya mtsho</em>). Путешествуя по У и Цангу вместе с отцом, он заслужил
                репутацию блестящего ученика. Позже он учился у Кумарасиддхи из школы Кадам, ученика
                Жам Лингпы (<em>byams gling pa</em>).</p>
            <p>В 1312 году, в возрасте двадцати трёх лет, он принял полное монашеское посвящение в
                Цонгду Гурмо (<em>tshong 'dus 'gur mo</em>) в присутствии двадцати наставников,
                включая Жонну Пелзана (<em>gzhon nu dpal bzang</em>), ученика Шакьяшрибхадры, Сонама
                Драка (<em>bsod nams grags</em>) и его ученика Сонама Зангпо (<em>bsod nams bzang
                    po</em>). По этому случаю Жонну Пелзанг преподал ему учение по Пратимокше, а
                Сонам Драк — комментарии к <em>Vinayamūlasūtra</em> и <em>Ekottarakarmaśataka</em>.
                В это время Ринчендуб занимался учёбой днём и медитацией по вечерам.</p>
            <p>Вскоре после этого он отправился в Тарпа Линг (<em>thar pa gling</em>), где около
                четырёх лет изучал санскритскую поэзию и грамматику у Тарпы Лоцавы Ньимы Гьялцена
                (<em>thar pa lo tsA ba nyi ma rgyal mtshan</em>), выдающегося переводчика, которого
                Будон считал главным среди своих учителей. Тарпа Лоцава также передал ему учения
                Калачакры, Хеваджры и других традиций. В Ронге он изучал грамматику и Калачакру у
                Дордже Гьялцена Пелзанга (<em>rdo rje rgyal mtshan dpal bzang</em>), племянника ламы
                Шераба Сенгге (<em>bla ma she rab seng ge</em>).</p>
            <p>После этого он начал проповедовать. Согласно его ученику и биографу Ринчену Намгьелу
                (<em>rin chen rnam rgyal</em>, 1318–1388), Будон «победил сторонников ложных и
                порочных взглядов, включая бон, с помощью священных текстов и рассуждений, обратив
                их в буддизм».</p>
            <p>В 1320 году, в возрасте тридцати одного года, он был назначен одиннадцатым
                настоятелем Жалу (<em>zhwa lu</em>) Кужангом Дракпой Гьялценом (<em>sku zhang grags
                    pa rgyal mtshan</em>), членом правящей семьи региона. Дракпа Гьялцен
                восстанавливал храм, основанный в 1027 году Четсуном Шерабом Джунгне (<em>lce btsun
                    shes rab 'byung gnas</em>). При поддержке Дракпы Гьялцена и его семьи Будон
                значительно расширил Жалу, построив новые ступы и храмы. Благодаря этой
                реконструкции, в более поздних перечнях настоятелей Жалу Будон считается первым. Его
                традиция учения известна также как Жалук или Булук.</p>
            <p>Будон продолжал получать учения, особенно из традиции Сакья, от ламы Пакпы (<em>'phags
                    pa</em>), Чокьи Гьялцена Пелзанга (<em>chos kyi rgyal mtshan dpal bzang</em>,
                1332–1359), Кунга Гьялцена (<em>kun dga' rgyal mtshan</em>, 1310–1358), наставника
                императора Юань, Донйод Гьялцена (<em>don yod rgyal mtshan</em>) и Сонама Гьялцана (<em>bsod
                    nams rgyal mtshan</em>, 1312–1375). Всего он учился у двадцати восьми учителей.
            </p>
            <p>В Жалу Будон основал дацан для изучения сутровой философии и новых тантр. Летом и
                зимой он преподавал Праджняпарамиту, Абхидхарма-самуччайю, Праманавинишчайю,
                Бодхичарьяватару и Винаю; весной и осенью — Калачакру, Гухьясамаджу и другие
                тантрические дисциплины. Он написал множество трактатов по всем этим темам.</p>
            <p>В Жалу он также участвовал в редактировании буддийского канона. Он работал с «Старым
                изданием Нартанга» (<em>snar thang</em>) Ганжура (<em>bka' 'gyur</em>) и Данжура
                (<em>bstan 'gyur</em>), созданным в начале XIV века под руководством монаха
                Джамьянга (<em>'byams dbyangs</em>). Это рукописное собрание впервые чётко разделило
                Ганжур (слова Будды) и Данжур (комментарии индийских мастеров). Хотя оригинал
                утрачен, учёные считают, что это было собрание существовавших рукописей, а не новое
                издание.</p>
            <p>Будон возглавил один из двух проектов по редактированию и расширению «Старого
                Нартанга» (второй осуществлялся в монастыре Цел Гунгтанг в 1347–1351 гг.). При
                поддержке Дракпы Гьялцена он пригласил в Жалу учёных и писцов. Его Данжур
                насчитывал 3392 текста и был завершён зимой 1334–1335 гг.; Ганжур был также
                подготовлен, но подробности о нём не сохранились.</p>
            <p>Будон исключил из своего Ганжура ньингмапинские тантры, считая их неаутентичными — то
                есть не имеющими санскритских оригиналов. Этот критерий, однако, применялся
                избирательно: несмотря на наличие санскритских материалов, он исключил тантру
                Ваджракилы, важную в его собственной Сакья-традиции. В «Синих анналах» Го Лоцава
                критиковал Будона за исключение тантры Гухьямула из Ганжура, хотя он включил в
                Данжур комментарий, цитирующий её.</p>
            <p>По словам Ринчена Намгьела, Будон спонсировал росписи на темы прошлых жизней Будды,
                его Двенадцати деяний и тантрических сюжетов. Его список Восьмидесяти четырёх
                махасиддх стал одной из стандартных классификаций.</p>
            <p>В 1344 году, в возрасте 55 лет, он преподавал в монастыре Сакья по просьбе Сонама
                Гьялцена Пелзанга. Там он получил приглашения от юаньского императора Тохан-Темура
                (Хуэйцзуна) и непальского царя Пуньямаллы, но отказался от обоих, сохраняя
                независимость от политики.</p>
            <p>В Сакье он переводил и редактировал тексты, включая пояснительную тантру
                Гухьясамаджи, «Секоддешапанджику» и «Ваджрамритапанджику». В 62 года он участвовал в
                урегулировании спора между Ябзангом и Пакмодру, по пути давая учения в монастырях
                Ралунг и Нангто Чумик. Он посетил монастырь своего первого учителя — Тропу.</p>
            <p>В 1354 году он вернулся в Жалу, а затем снова — в Сакью, где вместе с Чокьи Гьялценом
                Пелзангом создал мандалу Сарвакуласамграхи. Он утверждал, что является реинкарнацией
                Джетсуна Дракпы Гьялцена, и переписал некоторые его тексты, вызвав недовольство
                сакьяпинских учёных.</p>
            <p>В 1356 году, прослужив 37 лет, он сложил полномочия настоятеля. Его преемником стал
                Ринчен Намгьел. В Жалу тогда насчитывалось 3800 монахов.</p>
            <p>Будон удалился в скит Рипук (<em>ri phug</em>), основанный на месте, посещённом
                Атишей. Там, вероятно, он написал свою «Историю буддизма» (<em>bde gshegs bstan pa'i
                    gsal byed chos kyi 'byung gnas</em>). В 1357 году кашмирский пандита Суманашри
                передал ему учения, включая «Падмаджалу», которую Будон перевёл. Он продолжал
                преподавать Калачакру, встречаться с Гьелсе Токме и другими мастерами.</p>
            <p>Среди его учеников — Чокле Намгьел (5-й настоятель Джонанга), Юнгтон Дордже Пел, Якде
                Панчен Цондру Даргье и другие. В начале 1364 года он заболел, ушёл в затвор и
                скончался на 21-й день шестого месяца года Деревянного Дракона.</p>
            <p>Его биографию в двух частях написал Ринчен Намгьел: первая — в 1355 году, вторая — в
                1366 году. Деревянные блоки для издания его сочинений были вырезаны в 1917 году по
                указу 13-го Далай-ламы. Современные издания включают 28 томов.</p>
