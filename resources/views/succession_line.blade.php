            <p class="mb-3" id="citation-17"><sup>17</sup> Сангье Еше (<em>sangs rgyas ye
                    shes</em>,
                1525–1591) родился в 1525 году в долине Цанг (<em>gtsang rong</em>) в
                Тибете, в
                городе Друкгья (<em>drug brgya</em>). Он был самым младшим из четырёх
                сыновей. Имя
                его отца — Лама Ринчен (<em>bla ma rin chen</em>), а матери — Чотен (<em>chos
                    bstan</em>).<a href="#ref-17"
                                   class="text-blue-500 hover:underline">↩</a></p>
            <p>Вскоре после рождения Сангье Еше встретил своего будущего коренного учителя,
                Вэнсапу
                Лобзанга Дондруба (<em>dben sa pa blo bzang don grub</em>, 1505–1556),
                который
                призвал его родителей хорошо о нём заботиться.</p>
            <p>В возрасте десяти лет он принял Прибежище у Ламы Йонтена Зангпо (<em>bla ma
                    yon tan
                    bzang po</em>) из монастыря Басо Лхундруб Дечен (<em>bas so lhun grub
                    bde chen
                    dgon</em>), получив имя Чокьяб Дордже (<em>chos skyabs rdo rje</em>).
                Вскоре
                после этого он принял у Йонтена Зангпо обеты послушника (гецула) и новое имя
                —
                Сангье Еше. Он также получил наставления по Гухьясамадже, Акшобхье,
                Ваджрабхайраве и
                другим учениям традиции Гелуг.</p>
            <p>В пятнадцать лет Сангье Еше поступил в монастырь Ташилунпо (<em>bkra shis
                    lhun
                    po</em>), где учился у Цондру Гьялцена (<em>brtson 'grus rgyal
                    mtshan</em>),
                настоятеля дацана Тосом Линг (<em>thos bsam gling</em>).</p>
            <p>В следующем году он отправился с товарищами в дацан Лекдруб (<em>legs grub
                    grwa
                    tshang</em>) в Ньянгто (<em>myang stod</em>), чтобы учиться у Джамьянга
                Гендуна
                Лобзанга (<em>jam dbyang dge 'dun blo bzang</em>), выдающегося мастера
                Праманаварттики Дхармакирти. Изучая Мадхьямаку в течение нескольких лет,
                Сангье Еше
                стал грозным оппонентом в философских диспутах в Ташилунпо.</p>
            <p>В возрасте двадцати пяти лет он получил степень Геше (<em>dge bshes</em>) в
                монастыре
                Пэлкхор Чоде (<em>dpal 'khor chos sde</em>) в Гьянце (<em>rgyal rtse</em>).
                В
                следующем году он служил главным монахом по соблюдению дисциплины в
                Ташилунпо.</p>
            <p>Позднее Сангье Еше отправился в монастырь Гангчен Чопел (<em>gangs can chos
                    'phel</em>), чтобы учиться у Панчена Доньо Гьялцена (<em>paN chen don
                    yod rgyal
                    mtshan</em>). Затем он поступил в тантрический дацан Гьюме (<em>rgyud
                    smad
                    gwra tshang</em>) в Лхасе, чтобы углубить свои знания в тантре. Там он
                изучал
                Гухьясамаджу, Чакрасамвару и Ваджрабхайраву.</p>
            <p>По пути в Лхасу Сангье Еше останавливался в монастыре Вэнса (<em>dben sa</em>)
                и
                принял Прибежище у самого Гьялвы Вэнсапы (Лобзанга Дондруба). Завершив
                обширные
                занятия и практики в Гьюме, Сангье Еше вернулся к Вэнсапе. В этот период
                своей жизни
                он получил наставления, устные передачи, ипосвящения и практики линии
                преемственности Гандэн Махамудры.</p>
            <p>В это время Сангье Еше развил истинное отречение и принял обеты полностью
                посвященного монаха (гелонг) в монастыре Риво Гемпел (<em>ri bo dge
                    'phel</em>), где
                настоятелем был Чокле Нампар Гьялва (<em>phyogs las rnam par rgyal ba</em>).
            </p>
            <p>После кончины Вэнсапы в 1556 году Сангье Еше взял на себя ответственность за
                его
                реликвии и заказал создание большого количества священных объектов в
                монастыре
                Вэнса.</p>
            <p>В монастыре Вэнса Сангье Еше встретил Сонама Гьяцо (<em>bsod nam rgya
                    mtsho</em>,
                1543–1588), который вскоре должен был отправиться в Монголию, где ему был
                присвоен
                титул Третьего Далай-ламы. Сангье Еше сопровождал его до Ташилунпо в
                качестве его
                слуги и получил многочисленные учения от лам из окружения Сонама Гьяцо перед
                их
                отъездом в Монголию, включая Лангмику (<em>glang mig pa</em>) и Ригпай
                Сэнгге (<em>rig
                    pa'i seng ge</em>).</p>
            <p>Сангье Еше дважды занимал пост настоятеля монастыря Риво Гемпел и в итоге
                стал
                учителем перерождения Вэнсапы, Лобзанга Чокьи Гьялцена (<em>blo bzang chos
                    kyi rgyal
                    mtshan</em>, 1570–1662), которому впоследствии будет присвоен титул
                Панчен-ламы.
                Сангье Еше стал основным держателем линии преемственности Вэнсапы.</p>
            <p>Сангье Еше ушёл в 1591 году в возрасте шестидесяти семи лет в Ронг Джамчен
                (<em>rong
                    byams chen</em>). Многочисленные реликвии, оставшиеся после его
                кремации, были
                возвращены в монастырь Вэнса.</p>
