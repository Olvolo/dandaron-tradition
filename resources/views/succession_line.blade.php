            <p class="mb-3" id="citation-6"><sup>6</sup> Тропу Лоцава Джампа Пел
                (<em>khro phu lo tsA ba 'byams pa'i dpal</em>), один из самых
                выдающихся переводчиков эпохи позднего расцвета тибетского буддизма, родился
                в 1172 году в нижней долине Шаб в Цанге. Его семья не была известной, но судьба
                мальчика определилась рано: в восемь лет он был отправлен к своему дяде, знаменитому
                наставнику Гьелца Ринчен Гону (<em>rgyal mtsha rin chen mgon</em>). Под его
                руководством Джампа Пел получил первые монашеские обеты и имя Цултрим Шераб
                (<em>tshul khrims shes rab</em>), а затем начал серьёзное образование вместе
                с другим родственником — ученым Кунден Репой.<a href="#ref-6"
                                                                class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>Юноша рано проявил вкус к учёности. Он изучал логику, мадхьямаку и обширный
                корпус
                индийских комментариев в монастырях Цанга, включая Сакью. Особую роль в его
                судьбе
                сыграла встреча с мастером Шанг Гевой, под руководством которого Джампа Пел
                освоил
                санскрит — знание, которое вскоре превратило его в одного из важнейших
                переводчиков
                своего времени. В девятнадцать лет он получил полное монашеское посвящение и
                имя,
                под которым вошёл в историю: Джампа Пел — «Слава Майтреи».</p>
            <p>Когда его учитель и дядя Гьелца подошёл к концу жизни, он произнёс
                пророческие слова:
                «Подобно медной трубе, твоя удача раскроется в поздние годы». И оказался
                прав.</p>
            <p>После смерти дяди Джампа Пел отправился в Непал. Там он встретил выдающегося
                пандиту
                Буддхашриджняну (<em>Buddhashrijñana, buddha shrI dznyA na</em>), родом из
                Бхактапура.
                Он изучал у него и сутры, и тантры, а главное — впервые оказался в живом
                контакте с
                индийской учёной средой, вдохновлявшей тибетцев уже несколько столетий.</p>
            <p>И именно там произошло событие, которое определило его дальнейшую роль в
                истории.
                Вернувшись в Тибет, он привёз с собой индийского сиддха Митрайогина,
                наставника, чьи
                учения оказали заметное влияние на тибетские школы. Митрайогин пробыл в Тибете
                восемнадцать месяцев. Джампа Пел сопровождал его обратно в Непал, а затем привёз в
                Тибет Буддхашриджняну — в качестве своего наставника и гостя.</p>
            <p>С Буддхашриджняной он работал два года, переводя его учения в разных областях
                У-Цанга. Именно
                в этот период Джампа Пел окончательно стал тем, кем его помнят — Тропу
                Лоцавой,
                великим переводчиком из монастыря Тропу (<em>khro phu dgon</em>).</p>
            <p>В 1204 году он предпринял сложное путешествие по Чумби к рынкам Ассама, чтобы
                пригласить ещё одного индийского мудреца — знаменитого Шакьяшрибхадру
                (<em>shAkya shrI bha dra,</em> 1145–1225). Тот жил в монастыре Джагаддала (<em>Jagaddala</em>),
                одном из последних оплотов индийского буддизма (монастырь был разрушен
                мусульманами
                около 1207 года). Шакьяшрибхадра согласился и провёл в Тибете десять лет.
                Всё это время Джампа Пел был его переводчиком, учеником и духовным
                соратником.
                Именно это сотрудничество окончательно закрепило за ним титул Лоцавы, то
                есть
                «великого переводчика».</p>
            <p>Но Тропу Лоцава прославился не только переводами. Когда ему было двадцать
                четыре, он
                заказал тканую икону Майтреи. Тогда же Буддхашриджняна предсказал: «Ты
                сможешь
                воплотить мечту — создать гигантскую статую будущего Будды».</p>
            <p>Через несколько лет Джампа Пел приступил к этому проекту в долине Шаб. Работы
                начались, когда ему было тридцать один, и длились более десяти лет. Когда
                ему
                исполнился сорок один, статуя была завершена и освящена. Это был
                поразительный по
                масштабу образ Майтреи из позолоченной меди, высотой около сорока метров —
                примерно
                шестнадцатиэтажное здание.</p>
            <p>Обряд освящения сопровождался знамениями, которые вошли в легенды:
                благоуханные
                ветры, цветы, сыпавшиеся с неба, световой шар, растворившийся в статуе.
                Вдохновлённый этим, Джампа Пел написал гимн Майтрее и затем, по просьбе
                учителя, сам
                перевёл его на санскрит. Шакьяшрибхадра сказал ему: «Эта поэма —
                благословение
                Майтреи. Не присваивай себе её авторство».</p>
            <p>Статуя, к сожалению, не пережила нашествия джунгар в 1717–1718 годах, но
                именно она
                дала начало целой традиции гигантских изображений Майтреи по всему Тибету —
                от Тропу
                до Ташилунпо.</p>
            <p>После ухода Шакьяшрибхадры Джампа Пел отправился в Дрикунг Тил (<em>'bri gung
                    mthil</em>),
                где стал учеником Джиктена Гонпо (<em>'jig rten mgon po,</em> 1143–1217) —
                основателя
                школы Дрикунг Кагью. Под его руководством он глубоко
                изучил наследие Марпы, Миларепы, Гампопы и самого Джиктена Гонпо. Так в
                линии Тропу
                Кагью слились сразу несколько мощных традиций: переводческая, индийская,
                сакьяпинская и кагьюпинская.</p>
            <p>Тропу Лоцава умер в 1236 году. В линии Тропу Кагью его последователями были
                многие
                выдающиеся учителя: Тропу Семпа Ченпо, которого считали его сыном; Чегомпа;
                йогини
                Мачиг Сангье Рема (<em>ma cig sangs rgyas re ma</em>) и другие. Но самым
                знаменитым
                наследником стал Будон Ринчендуб (<em>bu ston rin chen grub,</em>
                1290–1364), один
                из
                величайших учёных Тибета, позднее ставший центральной фигурой традиции Жалу
                (<em>zhwa lu</em>). Через Будона линии Тропу проникли в Сакью и затем в
                Гелуг,
                положив начало многим практикам, которые живут и сегодня.</p>
