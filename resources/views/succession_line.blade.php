            <p class="mb-3" id="citation-2"><sup>2</sup> Ратнамати — (тиб. <em>blo gros rin
                    chen</em>, «Драгоценность Мудрости»), почитается как бодхисаттва — эманация
                Дхармакаи, через которого Махамудра нисходит в мир. Это символическое звено, а не
                исторический учитель. Имя Ратнамати также встречается в сутрах (напр.,
                <em>Avadāna-śataka</em>) как имя будущего Будды, но это не та же личность, что в
                линии Кагью.<a href="#ref-2" class="text-blue-500 hover:underline">↩</a></p>
