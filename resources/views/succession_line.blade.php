            <p class="mb-3" id="citation-7"><sup>7</sup> Сонам Вангчуг (тиб. <em>bsod nams dbang
                    phyug</em>) — выдающийся держатель линии Тропу Кагью XIII века, ближайший ученик
                и духовный наследник основателя линии — Тропу Лоцава Джампа Пела. После смерти
                учителя в 1236 году он стал одним из главных хранителей передачи, завершив ряд
                проектов, начатых Тропу Лоцавой. Он получил от него полный объём учений,
                составляющих ядро Тропу Кагью:<br>
                1) линию Махамудры, восходящую через Митрайогина и Лалитаваджру к Тилопе;<br>
