                    ba</em>) и Ваджраварахи (<em>rdo rje phag mo</em>);<br>
                3) собрание гуру-йог, связанных с индийскими мастерами, включая пандиту
                Буддашриджняну (<em>Buddhaśrījñāna</em>);<br>
                4) учения Калачакры.<br>
                В «Синей Летописи» (<em>Deb ther sngon po</em>) Гой-лоцавы Шоннупэла он упоминается
