            <p class="mb-3" id="citation-3"><sup>3</sup> Тилопа (<em>Tilopa</em>, 988–1069) —
                индийский тантрический практик, один из Восьмидесяти четырёх махасиддх. Считается
                человеческим прародителем линии Кагью тибетского буддизма и коренным учителем
                Наропы. Предположительно, он жил в регионе, который сейчас является Западной
                Бенгалией и Бангладеш, в конце X — начале XI веков.<a href="#ref-3"
                                                                      class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>Согласно традиционной биографии, Тилопа родился в брахманской семье в Северной
                Индии. В юном возрасте он стал монахом в храме Сомапури в Бенгалии. В какой-то
                момент он почувствовал необходимость обучаться у различных тантрических учителей, в
                результате чего покинул монашество и стал странствующим йогином, тайно практикующим
                тантру. В течение многих лет он работал слугой у проститутки по имени Барима в
                Бенгалии, зарабатывая на жизнь размалыванием кунжутных семян для масла (санскр. <em>Til</em>).
                Эта деятельность дала ему имя, под которым он стал известен — Тилопа. Позднее он
                достиг сиддхи (сверхъестественных способностей) в пещерах на территории храма
                Пашупатинатх в Катманду.</p>
            <p>Однажды к нему в видении пришла Дакини, которая предложила ему свои знания. Тилопа
                попросил у неё учения и получил посвящение в Чакрасамвара-тантру. Считается, что он
                получил прямую передачу Махамудры (Великой Печати) от Будды Ваджрадхары, который
                стал его главным учителем. Он практиковал Чакрасамвара-тантру в Сомапури, но был
                вынужден покинуть монастырское сообщество, когда его увидели с женщиной для практики
                йоги союза.</p>
            <p>Изгнание пошло ему на пользу: он путешествовал по Индии, разыскивая многих учителей и
                изучая их методы. Несмотря на то, что он избрал жизнь в уединённых и негостеприимных
                районах, его слава как мастера медитации привлекла к нему выдающихся учеников, самым
                важным из которых для линии Кагью является Наропа. Тилопа передал Наропе два
                ключевых потока учений: особые тантрические практики, позже получившие название
                «Шесть йог Наропы», и наставления по Махамудре — прямому указанию на природу ума.
                Наиболее знаменитым из этих указаний стала <em>«Песнь Махамудры, произнесённая у
                    Ганга»</em> (<em>Ганга-Махамудра</em>), в которой Тилопа, стоя у берегов
                священной реки, обратился к Наропе словами: «Расслабь тело, как мёртвую змею; оставь
                ум, как сияющий кристалл, не тронутый ни помыслами, ни усилием. В этом — суть всех
                учений». Эта песнь вошла в канон линии Кагью как одно из самых сжатых и прямых
                указаний на природу ума. Наропа, в свою очередь, был учителем Марпы-переводчика,
                который первым принёс эти учения из Индии в Тибет.</p>
            <!-- Разделительная черта -->
            <hr class="my-8 border-t-1 border-sky-500">

            <p class="mb-3" id="citation-4"><sup>4</sup>
                Лалитаваджра (санскр. <em>Lalitavajra, Līlāvajra, Vilāsavajra;</em>
                тиб. <em>rol pa'i rdo rje, sGeg pa'i rdo rje,</em> «Игривый Ваджр»; другие
                имена/эпитеты: <em>Viśvarūpa</em> («Всеформенный»), <em>Sūryavat</em> («Подобный
                Солнцу»), <em>'Jo-sgeg rdo-rje</em>) — индийский махасиддха и тантрический пандит,
                занимающий центральное место в нескольких ключевых линиях преемственности. Тибетская
                историография (особенно традиции Кагью и Гелуг) знает его
                прежде всего как прямого ученика Тилопы (988–1069) и учителя
                Митрайогина (XII–XIII вв.), составив тем самым
                критическое звено в цепи передачи учений Махамудры и тантрических
                циклов Гухьясамаджи и Чакрасамвары из Индии в
                Тибет (линия: Тилопа → Лалитаваджра → Митрайогин → Тропу Лоцава (1172–1236)
                и Мачиг Сангье Рема). Согласно преданиям школы Шангпа Кагью, он
                также был одним из учителей Кхьюнгпо Налджора (990–1139), передав ему
                садханы богини Ремати, методы защитной магии и медицины. Ему приписывается получение
                в видении тантры Ямантаки (Ваджрабхайравы) от дакини Симхамукхи.
                Историографическая проблема заключается в том, что под этим именем, вероятно,
                объединены сведения о двух разных фигурах: 1) раннем тантрическом учёном
                VIII–IX вв., авторе важного комментария <em>Āmnāyamañjarī</em> на
                «Манджушринамасангити» и возможном знатоке тантр класса йоги (Гухьягарбха); и 2)
                махасиддхе-практике X–XI вв., связанном с Тилопой. В контексте
                передачи Махамудры и сиддхических линий обычно подразумевается вторая фигура.
                Основные источники: <em>«Синяя Летопись» (deb ther sngon po)</em> Гой-лоцавы
                Шоннупэла, жития Кхьюнгпо Налджора и учителей Кагью, а также канонический
                комментарий в Данджуре (Toh. № 2533).<a href="#ref-4"
                                                        class="text-blue-500 hover:underline">↩</a>
            </p>
            <!-- Разделительная черта -->
            <hr class="my-8 border-t-1 border-sky-500">

            <p class="mb-3" id="citation-5"><sup>5</sup> Митрайогин, которого тибетские источники
                называют также Джаганмитранандой (санскр. <em>Jaganmitrānanda</em>), был одним из
                последних великих махасиддхов Индии и одним из наиболее значимых переносчиков
                позднеиндийского тантрического буддизма в Тибет. Его биография дошла до нас почти
                исключительно в тибетской передаче.<a href="#ref-5"
                                                      class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>Источник, содержащий наиболее подробное описание его жизни, — «Синяя летопись»
                (<em>deb ther sngon po</em>) Гой-лоцавы Шоннупэла. Там говорится, что Митрайогин
                родился в восточной Индии, в Радхе, области,
                примерно соответствующей современному Майурбханджу в Ориссе. Об обстоятельствах его
                ранней жизни ничего не сохранилось, но дальнейшие сведения ясно показывают, что он
                был включён в высшие слои индийской буддийской традиции. На каком-то этапе он стал
                учеником Лалитаваджры — прямого ученика Тилопы, одного из ключевых основателей
                индийского тантризма и учителя Наропы. Таким образом, Митрайогин был включён в линию
                передачи, которая стала основой для множества позднейших тибетских школ, особенно
                Кагью.</p>
            <p>Следуя наставлениям Лалитаваджры, он провёл двенадцать лет в глубокой медитации на
                Авалокитешвару. Именно этот период считается основополагающим для формирования его
                образа как «владыки йогинов» (тиб. <em>rnal 'byor gyi dbang phyug</em>). В конце
                длительной аскезы он, по преданию, получил
                прямое откровение от самого бодхисаттвы: Авалокитешвара раскрыл ему методы
                созерцания и вручил символические атрибуты, которые в дальнейшем связали Митрайогина
                с защитой буддизма и чудесными проявлениями. С этого события в «Синей летописи»
                начинается серия из двадцати чудес, описывающих его деятельность. Эти чудеса
                подтверждают его сверхъестественные способности и определяют его как защитника
                буддизма в эпоху его упадка в Индии: укрощение людоедствующих якш, выживание после
                трёх дней пребывания на пылающем погребальном костре, защита 12 000 монахов в
                Отантапури от нападения войска правителя, парализация армии Султана Хана в Магадхе
                силой сосредоточения и «львиного рёва», вызвавшего землетрясение. Войска были
                освобождены только после того, как царь вымолил прощение.</p>
            <p>В его традиционной биографии отдельное место занимают отношения с индийскими царями.
                Он был духовным наставником правителя Варанаси, который настолько почитал его, что
                безуспешно пытался удержать в своём дворце. Другие сюжеты включают царей, которые
                меняли свои враждебные к буддизму взгляды под воздействием его силы:<br>
                Царь Упатра, который бросил Митру в яму, но позже покаялся и принял буддизм;<br>
                Царь Яшас, который пытался заставить буддистов поклоняться индуистским божествам,
                устроив испытание с деревянным троном. Митрайогин сделал трон неподвижным, и
                побежденные индуисты приняли буддизм;<br>
                Царь Джаясена и пандита Ананда пытались убить Митру, но он, используя мудру и
                сосредоточение на смысле одного стиха, «освободил их умы» (превратил их
                враждебность в реализацию).<br>
                Самый достоверный исторический эпизод связан с последним могущественным царём
                династии Гахадавала — Джаячандрой (1170–1194). Он был последним великим правителем
                династии Гахадавала, царствовавшим над территорией, включавшей Уттар-Прадеш и части
                Бихара с 1170 по 1194 год. Король поддерживал как индуистские, так и буддийские
                религиозные связи и правил своей обширной территорией с заметной религиозной
                терпимостью. Надпись, найденная в Бодхгае, называет монаха Шримитру наставником-гуру
                Джаячандры, что подтверждает связь Митрайогина с королём. Джаячандра был убит в
                битве против мусульманского завоевателя Мухаммада Гури в 1194 году. Это событие
                стало символом конца буддийской эпохи в Индии. Именно ему адресовано знаменитое
                «Письмо царю Чандре» (<em>Candrarājalekha</em>), пространно цитируемое Цонкапой в
                «Ламрим Ченмо». Это послание — редкий пример обращённого к мирскому правителю
                наставления о бренности власти, ответственности монарха и необходимости практиковать
                Дхарму.</p>
            <p>«Синяя летопись» также даёт Митрайогину имя Аджитамитрагупта
                (<em>Ajitamitragupta</em>). Однако некоторые колофоны предполагают, что
                Митрайогин/Джаганмитрананда (который был переводчиком) мог быть не тем же лицом,
                что Аджитамитрагупта (который был автором текста). Возможно, «Синяя летопись»
                объединила истории двух разных, но связанных фигур.</p>
            <p>Иной образ Митры проявляется в ньингмапинской биографии «Биография
                Митрайогина: Светильник, освещающий Учение»
                (<em>mi tra jo ka'i rnam thar bstan pa gsal ba'i sgron me</em>), созданной
                Дриме Кунга Ньингпо в XIV веке. Здесь Митрайогин предстает как сын царя Варанаси,
                который бежит из дворца для практики Дхармы, принимает полную
                Мула-сарвастивадинскую винаю в Наланде под именем Шри Дхармамитры и сразу
                оказывается под покровительством богини Экаджати. Ньингмапинское жизнеописание
                насыщено видениями, женскими божествами и йогинями, испытаниями, связанными с
                ведьмами и магическими существами, а также эпизодами, напоминающими практику Чод.
                Экаджати на пике его практики вскрывает ему голову; из света, излившегося из его
                черепа, появляются Авалокитешвара и свита божеств, которые посвящают Митру в сто
                восемь садхан, после чего Экаджати восстанавливает его череп.<br>Во время
                путешествий он сталкивается с городом ведьм (<em>phra man ma</em>) и их королевой,
                которая замораживает его и предостерегает не пытаться подчинить их, пока он не
                достигнет достаточной реализации. Королева предсказывает ему полное освобождение
                через шесть лет и дарует посвящение в Освобождающее Колесо Пути Авалокитешвары
                (<em>grol lam ye shes 'khor lo</em>).
                Экаджати, йогини, дакини и даже целые города ведьм в этой версии биографии выражают
                идею о том, что духовный путь Митры связан с преодолением как внешних опасений, так
                и внутренних препятствий — символических «женских сил», одновременно угрожающих и
                ведущих к освобождению.</p>
            <p>Кульминацией является посвящение Митры в сто восемь мандал, дарованное дакини
                Сукхадакини (дочь брахмана) и тантрическими проявлениями Авалокитешвары. После этого
                он достигает полного завершения пути и отправляется в священные земли Оддияны, где,
                как утверждается, встречается и даже «сливается» с Падмасамбхавой и поет ваджрную
                песню, что подчеркивает его важность также и в ньингмапинских линиях
                преемственности.</p>
            <p>Часть его жизни связана с Тибетом. Митрайогина пригласил Тропу Лоцава (<em>khro
                    phu lo tsA ba lo tsA ba 'byams pa'i dpal</em>, ок. 1172–1236),
                услышавший о нём от кашмирского пандита Буддхашриджняны (<em>Buddhaśrījñāna</em>).
                Митра прибыл в Центральный Тибет около 1197–1200 годов, хотя первоначально отказался
                — традиция
                объясняет это «кармическими омрачениями» самого Тропу. Согласно источникам,
                отчаявшийся
                ученик попытался лишить себя жизни, и Митра буквально подхватил его в воздухе в
                падении; этот эмоциональный эпизод считается моментом очищения ученика и началом их
                совместного пути.</p>
            <p>В Тибете Митрайогин пробыл лишь восемнадцать месяцев, но за это время оказал огромное
                влияние. Он передал обширный цикл, ставший знаменитым как «Сто передач Митры»
                (<em>mi tra bgya rtsa</em>) — собрание из около ста восьми тантрических циклов,
                включающих мандалы, садханы, посвящения и ритуалы. Эти
                материалы позднее стали одним из источников для «Сокровищницы Кагью тантр»
                (<em>bka' brgyud sngags mdzod</em>) Джамгона Конгтрула
                (<em>'jam mgon kong sprul,</em> 1813–1899).
                Митра также передал Тропу Лоцаве учения "Махамудры, Обрывающей Поток Сансары"
                (<em>phyag rgya chen po 'khor ba rgyun gcod</em>), по традиции происходящие от
                дакини Уддияны через Сараху. Среди его учеников традиция называет йогиню Мачиг
                Сангье Рему (<em>ma cig sangs rgyas re ma</em>), которая передала линию этих
                наставлений последующим поколениям (через Цэндэна Трулжика Ченпо в монастыре
                Джонанг). Эта линия сохранилась вплоть до эпохи VIII Кармапы Микьё Дордже
                (1507–1554), который придавал ей особое значение.</p>
            <p>Тропу Лоцава стал одним из основателей линии Тропу Кагью и по наставлению Митрайогина
                освятил место для знаменитой гигантской статуи Майтреи в монастыре Тропу (<em>khro
                    phu dgon</em>).</p>
            <p>Влияние Митрайогина на тибетскую традицию отражается в большом количестве
                комментариев к его текстам, созданных мастерами всех школ: от Второго и
                Седьмого
                Далай-лам до Будона Ринчендуба (<em>bu ston rin chen grub,</em> 1290–1364),
                4-го Панчен-ламы Лобсанга Чокьи Гьелцена (<em>blo bzang chos kyi rgyal
                    mtshan,</em>
                1570–1662), Падмы Карпо (<em>padma dkar po,</em> 1527–1592) и
                других выдающихся учёных. Существуют и другие жизнеописания Митры, включая
                текст
                Ринчена Гьелцена (<em>rin chen rgyal mtshan</em>), племянника Сакья Пандиты
                (<em>sa skya paN Di ta,</em> 1182–1251).</p>
