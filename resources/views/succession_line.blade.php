            <p class="mb-3" id="citation-15"><sup>15</sup> Чокьи Дордже (<em>chos kyi rdo rje</em>)
                был выдающимся мастером традиции Гелуг, известным благодаря получению и
                передаче тайной устной линии Гандэн Махамудры (<em>dga' ldan phyag chen</em>).<a
                    href="#ref-15" class="text-blue-500 hover:underline">↩</a></p>
            <p>Чокьи Дордже родился в 1457 году, недалеко от монастыря Шангпа Кагью Танаг Дорджеден
                (<em>rta nag rdo rje ldan</em>). Его родителями были Кунга Гьелпо (<em>kun dga'
                    rgyal po</em>) и Пэлдзом (<em>dpal 'dzom</em>) — странствующие аскеты из
                региона Цонгка (<em>tsong kha</em>) в Амдо. Детство он провёл в паломничествах,
                которые закончились в возрасте одиннадцати лет, когда его родители привели его в
                монастырь Гандэн Намгьелинг (<em>dga' ldan rnam rgyal gling</em>).</p>
            <p>В Гандэн Намгьелинге Чокьи Дордже встретил своего коренного учителя, Басо
                Чокьи Гьялцена (<em>ba so chos kyi rgyal mtshan</em>, 1402–1473), который занимал
                пост Шестого Гандэн Трипы (<em>dga' ldan khri pa</em>). Говорят, что Чокьи
                Гьялцен был предупрежден о приходе своего будущего ученика, поэтому он тепло
                приветствовал мальчика и его родителей, обеспечив их едой и припасами и попросив
                родителей
                отдать ему ребенка. Вскоре мальчик принял обеты послушника и получил имя Чокьи
                Дордже.</p>
            <p>Под руководством Чокьи Гьялцена Чокьи Дордже проявил себя как исключительный
                учёный. Он прошел стандартный курс обучения Гелуг, начиная с
                Бодхисаттвачарья-аватары и продолжая изучением Ламрим (<em>lam rim</em>) — ступеней
                пути. В должное время он получил линии преемственности Ламрима, а также инициации
                Гухьясамаджи, Чакрасамвары, Ваджрабхайравы и Калачакры. В частности, он получил
                передачу устной традиции Гандэн Махамудры (<em>dga' ldan phyag chen</em>) линии
                Цонкапы (<em>tsong kha pa</em>, 1357–1419), которую Цонкапа, как считается, получил
                от самого Манджушри, вместе с коренным текстом передачи, Трулпай Легбам Ченмо
                (<em>sprul pa'i glegs bam chen mo</em>).</p>
            <p>После Гандэна Чокьи Дордже продолжил обучение в монастыре Дрепунг
                (<em>'bras spung</em>), где он изучал философию Мадхьямаки с Делег Тобденом
                (<em>bde legs stobs ldan</em>) и принял полное монашеское посвящение (гелонг) у
                Джепона Лобзанга Ньимы (<em>je dpon blo bzang nyi ma</em>, 1439–1492), Девятого
                Гандэн Трипы.</p>
            <p>Затем Чокьи Дордже отправился в провинцию Цанг с целью углубить своё понимание
                священных писаний. Он принял учения от Джамьянга Монлама Пэлва (<em>’jam dbyangs
                    smon lam dpal ba</em>, 1414–1491) и Лодро Бепы (<em>blo gros sbas pa</em>,
                1400–1475).</p>
            <p>Следуя наставлениям своего учителя, Чокьи Дордже начал практиковать в одиночном
                затворе в дикой местности Тибета. В Пема Чен (<em>padma can</em>), месте, важном для
                лам-держателей линии Гандэн Махамудры, он, как сообщается, пережил видение Цонкапы,
                получив от него полную линию преемственности тайной устной традиции.</p>
            <p>Ближе к концу своей жизни Чокьи Дордже принял в ученики Вэнсапу Лобзанга Дондруба
                (<em>dben sa pa blo bzang don grub</em>, 1505–1556). Согласно легенде, Чокьи
                Дордже нашел Лобзанга Дондруба дома, пораженного оспой. Он пропел песню за его
                дверью, вдохновив своего будущего ученика верой.</p>
            <p>Вэнсапа присоединился к Чокьи Дордже в его ретритном центре Гармо Чодзонг
                (<em>mgar mo chos rdzong</em>), расположенном недалеко от скита Вэнса (<em>dben
                    sa</em>), и получил от него полную передачу Гандэн Махамудры.</p>
            <p>Чокьи Дордже скончался после того, как провел несколько лет со своим учеником,
                практикуя вместе в Гарма Чодзонг, Пема Чан, а также посещая монастыри в У и
                Цанге.</p>
