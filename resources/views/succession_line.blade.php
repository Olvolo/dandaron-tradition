            <p class="mb-3" id="citation-16"><sup>16</sup> Вэнсапа Лобзанг Дондруб
                (<em>dben sa pa blo bzang don grub</em>, 1505–1556) родился в 1505 году в Лхаку
                (<em>lha khud</em>) в Цанге, в месте, называемом Вэнса (<em>dben sa</em>). Его отца
                звали Сонам Дордже (<em>bsod nams rdo rje</em>), а мать — Пэл Дзомкьи
                (<em>dpal 'dzom skyid</em>). В детстве его звали Гомпо Кьяб
                (<em>mgon po skyabs</em>).<a href="#ref-16"
                                             class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>В самом юном возрасте Вэнсапа встретил своего первого учителя, Дже Кьябчока
                Пэлзанга
                (<em>rje skyabs mchog dpal bzang</em>), настоятеля монастыря Вэнса (<em>dben
                    sa
                    dgon</em>). Говорят, что он был необыкновенным учеником и практиком уже
                с
                юности, быстро превзойдя в знаниях и понимании всех своих сверстников.</p>
            <p>Вэнсапа обладал великой, спонтанной и естественной верой в Цонкапу Лобзанга
                Дракпу
                (<em>tsong kha pa blo bzang grags pa</em>, 1357–1419) и молился о
                возможности
                воплотить сущность его учений. Стремясь к этой цели, в возрасте одиннадцати
                лет он
                принял обеты послушника (гецула) в монастыре Чокхор Удинг (<em>chos 'khor
                    dbus
                    sdings</em>). Дракпа Дондруб (<em>grags pa don grub</em>) из Лхарице
                (<em>lha ri
                    rtse</em>) был там настоятелем, а Цултрим Ринченпа (<em>tshul khrims rin
                    chen
                    pa</em>) был наставником. Ему было дано монашеское имя Лобзанг Дондруб.
            </p>
            <p>Вскоре после этого Лобзанг Дондруб отправился в монастырь Дрепунг (<em>'bras
                    spungs</em>), где изучал Ламрим (<em>lam rim</em>) и философские тексты,
                а также
                получал наставления по практикам высшей йога-тантры от ряда лам, в частности
                от
                Лхарипы Дракпы Дондруба.</p>
            <p>В возрасте семнадцати лет, заболев оспой во время эпидемии в Лхасе, он стал
                учеником
                сиддхи Чокьи Дордже (<em>chos kyi rdo rje</em>, р. ок. 1457). От него он
                получил
                подробные наставления по устной традиции Цонкапы, заняв свое место в
                качестве
                держателя линии преемственности Гандэнской Устной Традиции.</p>
            <p>Чокьи Дордже передал ему устную традицию Гандэн Махамудры (<em>dga' ldan
                    phyag
                    chen</em>) линии Цонкапы (которую Цонкапа, как утверждается, получил от
                самого
                Манджушри) вместе с коренным текстом передачи, Трулпай Легбам Ченмо (<em>sprul
                    pa'i
                    glegs bam chen mo</em>).</p>
            <p>От Чокьи Дже Лодро Гьялцена (<em>chos kyi rje blo gros rgyal mtshan</em>) он
                получил
                наставления, передачи и разрешения на Нартанг Гьяца (<em>snar thang brgya
                    rtsa</em>),
                инструкции по медитации Атиши, Гухьясамадже и другие тантрические учения.
            </p>
            <p>Чокьи Дордже пригласил его присоединиться к нему в его скиту Гармо Чо Дзонг (<em>mgar
                    mo chos rdzong</em>) в Таши Дзонг (<em>bkra shis rdzong</em>), к западу
                от
                Шигаце. Вэнсапа отправился туда после выздоровления от оспы, и там Чокьи
                Дордже
                даровал ему полную передачу Ламрима и Гандэнской Устной Традиции.</p>
            <p>Оттуда Вэнсапа направился в пещеру, известную как Пема Чан (<em>pad ma
                    can</em>),
                также называемую Пема О (<em>pad ma 'od</em>), где он практиковал эти
                и другие учения, переданные ему Чокьи Дордже. Там он жил как сиддха в
                индийском
                стиле, обмазывая свое тело пеплом и сидя в семичастной позе дни напролёт.
                Есть
                предположения, что в это время он также практиковал сексуальную йогу с
                супругой.</p>
            <p>В возрасте двадцати лет он также практиковал Гухьясамаджу в скиту Гьялва
                Гьюнг (<em>rgyal
                    ba rgyung</em>) в Драггьяво (<em>'brag rgya bo</em>).</p>
            <p>Вэнсапа дал свои первые учения в скиту Пема О и с тех пор давал наставления
                по
                всей стране, проживая в различных монастырях Гелуг в Цанге и У, включая
                Дрепунг,
                где в возрасте тридцати трех лет он принял полные обеты посвящения (гелонг)
                от
                Гендуна Гьяцо (<em>dge 'dun rgya mtsho</em>, 1476–1542), который посмертно
                стал
                известен как Второй Далай-лама.</p>
            <p>В Ташилхунпо (<em>bkra shis lhun po</em>) в Шигаце он встретился с Лекпай
                Дондрубом (<em>legs
                    pa don grub</em>), с которым сотрудничал в создании нескольких своих
                сочинений.
                В монастыре Сэра (<em>se ra dgon</em>) он получил дополнительные учения и
                посвящения
                от Гендуна Гьяцо.</p>
            <p>Вернувшись в Цанг, он получил учения от Панчена Джангчуба Лодро (<em>paN chen
                    byang
                    chub blo gros</em>), Нгари Лочена Намгьела (<em>mnga' ri lo chen rnam
                    rgyal</em>)
                и Лекпай Лодро (<em>legs pa'i blo gros</em>).</p>
            <p>Самым важным учеником Вэнсапы был Сангье Еше (<em>sangs rgyas ye shes</em>),
                который
                стал его основным держателем линии преемственности.</p>
            <p>Вэнсапа ушёл в 1566 году. Говорят, что во время кремации его тела появилось
                множество чудесных знаков, включая радуги и цветы, падающие с неба. Его
                останки были
                захоронены в монастыре Вэнса.</p>
            <p>Около 1641 года его признанному перерождению, Лобзангу Чокьи Гьялцену (<em>blo
                    bzang chos kyi rgyal mtshan</em>, 1570–1662), Пятый Далай-лама, Агван
                Лобзанг
                Гьяцо (<em>ta la'i bla ma 5 ngag dbang blo bzang rgya mtsho</em>,
                1617–1682),
                присвоил титул Панчен-ламы. Таким образом, Вэнсапа посмертно получил титул
                Третьего
                Панчен-ламы.</p>
