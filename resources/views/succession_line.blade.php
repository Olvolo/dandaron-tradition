            <p class="mb-3" id="citation-12"><sup>12</sup> Цонкапа Лобзанг Дракпа (<em>tsong kha pa
                    blo bzang grags pa</em>) родился в 1357 году в области Цонгка (<em>tsong
                    kha</em>) в Амдо. Его матерью была Шингза Ачо (<em>shing bza' a chos</em>), а
                отцом — Лубум Гэ (<em>klu 'bum dge</em>).<a href="#ref-12"
                                                            class="text-blue-500 hover:underline">↩</a>
            </p>
            <p>Среди многочисленных чудесных знамений, связанных с его рождением, наиболее известным
                является предание о капле крови из пуповины, упавшей на землю и давшей жизнь
                сандаловому дереву, листья которого несли символы, относящиеся к проявлению
                бодхисаттвы Манджушри в форме Симханады (<em>Simhanāda Manjushri</em>, "Львиный
                Рык Манджушри"), с которым Цонгкапа впоследствии будет отождествлен. На этом месте
                его мать позднее возвела ступу. Сегодня это место отмечено монастырём Кумбум
                (<em>sku 'bum dgon pa</em>), основанным в 1583 году Третьим Далай-ламой Сонамом
                Гьяцо (<em>ta la'i bla ma 3 bsod nams rgya mtsho,</em> 1543-1588) на месте
                первоначальной ступы.</p>
            <p>В возрасте трёх лет Цонкапа временно принял обеты упасаки (<em>upāsaka</em>) от
                Четвёртого Кармапы Ролпая Дордже (<em>karma pa 4 rol pa'i rdo rje</em>, 1340–1383)
                и получил имя Кунга Ньингпо (<em>kun dga' snying po</em>).</p>
            <p>В возрасте восьми лет он принял обеты послушника (<em>srāmanera</em>) и имя Лобзанг
                Дракпа от мастера Кадам Чодже Дондруба Ринчена (<em>chos rje don grub rin chen</em>).
                Дондруб Ринчен, великий последователь Ваджрабхайравы, поддерживал связь с Цонкапой
                и его семьей с момента рождения мальчика и, как говорят, получал пророчества о
                важности ребенка от своего учителя и божества. Цонкапа провел большую часть своей
                юности, обучаясь у Дондруба Ринчена; говорят, он был настолько проницателен, что
                легко понимал и запоминал даже самые сложные тексты. От Дондруба Ринчена он получил
                многочисленные тантрические посвящения, наиболее важным из которых было посвящение
                в Ваджрабхайраву. Согласно его тайной биографии, в возрасте семи лет он пережил
                видения Атиши Дипамкары и божества Ваджрапани. Общение с различными историческими
                учителями и божествами в конечном итоге стало особенно важным в развитии понимания
                буддизма у Цонкапы.</p>
            <p>В возрасте шестнадцати лет Лобзанг Дракпа отправился в У-Цанг и больше никогда не
                возвращался на родину. В У-Цанге он учился у более чем пятидесяти различных
                буддийских ученых. Как отмечается в его автобиографии «Исполненные цели»
                (<em>rtogs brjod mdun legs ma</em>), он подробно изучал тексты и темы, такие как
                «Пять трактатов Майтрейи» (<em>byams chos sde lnga</em>) и связанные с ними работы
                Асанги (IV век), «Абхидхарма» Васубханду (IV век), логические системы Дигнаги и
                Дхармакирти (VI век), а также систему Мадхьямаки Нагарджуны (ок. 150-250 гг.) и его
                последователей, таких как Арьядева (III век). Следуя примеру таких подвижников, как
                Сакья Пандита Кунга Гьелцен (<em>sa skya paN Di ta kun dga' rgyal mtshan,</em>
                1182-1251) и Будон Ринчендуб (<em>bu ston rin chen grub,</em> 1290-1364), именно
                акцент Цонкапы на философских исследованиях и логике в конечном итоге стал одной из
                определяющих характеристик традиции Гелуг.</p>
            <p>Одним из его главных учителей был мастер Сакья Рэндава Жонну Лодро (<em>red mda ba
                    gzhon nu blo gros</em>, 1349–1412), убеждённый сторонник воззрения Прасангика
                Мадхьямаки. Преданность Цонкапы Рендаве была настолько велика, что он сочинил
                знаменитый стих «Мигцема» (<em>dmigs brtse ma</em>) в его честь. Согласно преданию,
                Рендава посчитал этот стих более подходящим и точно описывающим качества Цонкапы,
                и поэтому вознес эту молитву ему. Сегодня этот стих по-прежнему считается
                последователями Гелуг основным способом призывания благословения Цонкапы.</p>
            <p>Помимо Дондруба Ринчена, к числу главных тантрических гуру Цонкапы относились
                Ченнга Сонам Гьелцен (<em>spyan snga bsod nams rgyal mtshan,</em> 1378-1466), лама
                из Дрикунга, от которого он получил Шесть Йог Наропы (<em>na ro'i chos drug</em>);
                лама из Джонанга Чокле Намгьел (<em>phyogs las rnam rgyal, </em>1306-1386), от
                которого он получил цикл Калачакры; и мастер Сакья Ринчен Дордже
                (<em>rin chen rdo rje</em>), от которого он получил учения Ламдре
                (<em>lam 'bras</em>) и Хеваджра-тантру.</p>
            <p>Возможно, наиболее важным является то, что он получил цикл «Гухьясамаджи» от
                Кхьюнгпо Лхапы Жонну Сонама (<em>khyung po lhas pa gzhon nu bsod nams</em>),
                ученика Будона Ринчендуба, а цикл мандалы тела (<em>lus dkyil</em>) Херуки
                Чакрасамвары — от мастера Сакья Ламы Дампы Сонама Гьелцена Пелзангпо
                (<em>bla ma dam pa bsod nams rgyal mtshan dpal bzang po,</em> 1312-1375). Изучение
                тантры Цонкапой не ограничивалось ануттарайога-тантрами; он также подробно
                изучал крию, чарью и йога-тантры, отмечая в своей краткой автобиографии важность
                постепенного подхода к Ваджраяне. Более того, хотя это и не стало доктриной
                поздней традиции Гелуг, Цонкапа также изучал учение Дзогчена у Лодрака Друпчена
                Намкха Гьелцена (<em>lho brag grub chen nam mkha' rgyal mtshan,</em> 1326-1401).</p>
            <p>Благодаря этим исследованиям понимание Цонгкапой философии Мадхьямаки стало более
                конкретным и основанным на опыте. К двадцати годам он начал писать свою самую важную
                раннюю работу — «Золотой венок» (<em>legs bshad gser phreng</em>), посвященную
                Праджняпарамите. Цонкапа продолжал писать на протяжении всей своей жизни, создав
                восемнадцатитомное собрание текстов.</p>
            <p>Хотя Цонкапа признаётся автором своих сочинений, считается, что многие из них были
                написаны под руководством и вдохновением божеств, которых он лицезрел в видениях, в
                частности Манджушри, как описано в его тайной биографии. Считается, что Цонкапа
                первоначально полагался на своих учителей в общении с различными божествами от его
                имени. Например, его учитель из школы Ньингма, Намкха Гьелцен, как полагают, мог
                общаться с Ваджрапани и выступал в качестве посредника между божеством и Цонгкапой.
                Позже Цонгкапа хотел отправиться в Индию, но Ваджрапани, используя его как медиума,
                отговорил его от этого.</p>
            <p>Таким же образом Цонкапа первоначально полагался на своего учителя Умапу Паво Дордже
                (<em>dbu ma pa dpa' bo rdo rje</em>), который выступал в качестве посредника в
                общении с Манджушри. Цонкапа встретил этого ламу из школы Кагью, когда ему было
                тридцать три года. К этому времени Цонкапа завершил работу над «Золотым венком» и
                вместе с Паво Дордже изучал «Мадхьямакаватару» Чандракирти (VII век). В этот период
                Паво Дордже и Цонкапа вместе провели время в уединении, и, как говорят, Цонкапа
                задавал Манджушри множество вопросов через Паво Дордже. Однако со временем сам
                Цонкапа начал испытывать видения и смог напрямую общаться с Манджушри, получая
                наставления и тантрические посвящения, наиболее важными из которых были посвящения
                Манджушри и Ваджрабхайравы. На протяжении своей жизни Цонкапа продолжал переживать
                видения Манджушри, а также множества других божеств и учителей, таких как Асанга и
                Нагарджуна. Хотя Цонкапа общепризнанно считается проявлением Манджушри, природа его
                видений, тем не менее, оспаривалась некоторыми учителями, не принадлежащими к клану
                Гелуг, особенно учёным из клана Сакья Горампой Сонамом Сенгге
                (<em>go rams pa bsod nams seng ge,</em> 1429-1489), который критиковал Цонкапу и его
                подход к Мадхьямаке.</p>
            <p>За исключением короткого периода преподавания, Цонкапа продолжал проводить
                интенсивные уединения. Он и община из восьми учеников начали длительное уединение в
                ските Чадрел (<em>bya bral</em>) в 1392 году, а несколько лет спустя переехали в
                Олкха Чолунг (<em>'ol kha chos lung</em>). Во время этого уединения они, как
                известно, выполнили обширные предварительные практики, например, совершили
                3 500 000 простираний в сочетании с практикой Трискандхадхармасутры.</p>
            <p>После уединения Цонгкапа отправился в Дзинджи (<em>'dzing ji</em>), где совершил
                свой первый из четырех великих подвигов: реставрацию знаменитой статуи Майтреи.
                Считается, что в этот период, в 1398 году, Цонкапа достиг просветления и
                совершенного понимания Мадхьямаки благодаря видению собрания великих индийских
                мастеров Прасангики. Сразу после этого опыта он сочинил «Хвалу зависимому
                происхождению» (<em>rten 'brel bstod pa</em>). Этот опыт положил начало новой эпохе
                в жизни Цонкапы, которая сместилась в сторону сочинения и обучения других тому, что
                он открыл. Так, в 1402 году, в возрасте сорока шести лет, находясь в монастыре
                Ретинг (<em>rwa sgreng</em>), он сочинил «Ламрим Ченмо» (<em>lam rim chen mo</em>),
                несомненно, его самое известное произведение. Основанная на «Бодхипатхапрадипе»
                Атиши Дипанкары, эта работа подробно описывает постепенный путь к просветлению с
                точки зрения Сутраяны. Как говорят, Цонкапа, разделяя сомнения Будды после своего
                Просветления в том, что люди поймут его учение, был первоначально обескуражен
                мыслью о том, что большинство читателей не смогут понять его объяснения
                пустоты, составляющие заключительную часть работы. Однако видение Манджушри
                вдохновило Цонкапу на завершение произведения.</p>
            <p>В 1402 году Цонгкапа совершил своё второе великое деяние. Находясь в Намцеденге
                (<em>rnam rtsed ldeng</em>) во время сезона дождей со своим учителем Рендавой и
                Кьябчоком Пелзангпо (<em>skyabs mchog dpal bzang po</em>), он дал подробный
                комментарий к Винае большому собранию монахов. Помимо своего акцента на учёбе,
                Цонкапа, пожалуй, наиболее известен тем, какое значение он придавал монашеской
                дисциплине Винаи.</p>
            <p>После написания «Ламрим Ченмо» он создал несколько других работ около 1407 и 1408
                годов, в частности, свой комментарий к «Основным стихам о Срединном пути»
                Нагарджуны (Муламадхьямакакарика), называемый «Океан рассуждений»
                (<em>rigs pa'i rgya mtsho</em>), и «Суть красноречия»
                (<em>legs bshad snying po</em>). В 1415 году он написал «Ламрим Дринг»
                (<em>lam rim 'bring</em>), известный как «Трактат средней длины о стадиях пути к
                просветлению», который является сокращенной версией «Ламрим Ченмо».</p>
            <p>Цонгкапа был плодовитым автором тантрической литературы. В качестве дополнения к
                «Ламрим Ченмо» Цонгкапа написал в 1405 году «Агрим Ченмо»
                (<em>sngags rim chen mo</em>), «Великий трактат о тантрических стадиях пути к
                просветлению», охватывающий все четыре класса тантры согласно традициям сарма, с
                подробным объяснением двух стадий тантры ануттарайога. К числу других важных
                тантрических работ относятся его труды по Гухьясамадже, особенно «Комментарий к
                тантре Ваджраджнянасамуччаянама»
                (<em>ye shes rdo rje kun las btus pa zhes bya ba'i rgyud</em>) 1401 года и
                «Изложение пяти этапов Гухьясамаджи» (<em>gsang 'dus rim lnga gsal sgron</em>)
                1411 года. Тексты по тантре Гухьясамаджи занимают видное место в собрании сочинений
                Цонгкапы, составляя большую часть его восемнадцати томов.</p>
            <p>К этому времени слава Цонкапы как великого ученого и просветленного практика
                распространилась по всему Тибету и даже Китаю. В 1408 году император Юнлэ
                (правил в 1402-1424 годах) китайской династии Мин направил Цонкапе приглашение
                посетить его двор и столицу в Нанкине. Цонкапа отказался, и в 1413 году было
                отправлено второе приглашение. Хотя Цонкапа снова отказался, он поручил своему
                ученику Шакья Йеше (1354-1435) отправиться вместо него. Поездка Шакья Йеше в Китай
                оказалась успешной, и император присвоил ему титул Джамчен Чодже
                (<em>byams chen chos rje</em>). Полученные им в качестве подношений материалы
                позволили ему основать монастырь Сера в 1419 году. После смерти императора Юнлэ в
                1424 году Шакья Йеше посетил новую столицу императора Сюаньдэ (правил в
                1425-1435 годах) — Пекин. Благодаря этим визитам были установлены первые связи
                между традицией Цонкапы и императорами Китая, которые сохранялись до падения
                маньчжурской династии Цин в 1911 году.</p>
            <p>В 1409 году Цонгкапа учредил в Лхасе Монлам Ченмо (<em>smon lam chen mo</em>), или
                Великий
                молитвенный праздник, который отмечается примерно во время тибетского Нового года,
                Лосар (<em>lo gsar</em>). Это празднование традиционно сосредоточено в храме
                Джокханг в Лхасе и считается третьим великим деянием Цонкапы. В это время он также
                преподнес драгоценные украшения и корону статуе Джово Шакьямуни, самой священной
                статуе в Джокханге и во всем Тибете. Благодаря этим украшениям статуя преобразилась
                из изображения Нирманакаи Будды Шакьямуни в изображение его проявления Самбхогакаи.
            </p>
            <p>По просьбе своих учеников Цонгкапа основал монастырь, который был освящен в 1410
                году, через год после начала Монлам Ченмо. Монастырь получил название Ганден
                (<em>dga' ldan</em>) — тибетский перевод Тушита, чистой земли будущего Будды
                Майтрейи. Впоследствии монастырь стал крупнейшим монастырем в Тибете, возможно, и в
                мире, и считается главным монастырем традиции Гелуг. Цонкапа желал построить
                трехмерные изображения мандал своих трех главных божеств тантры ануттарайога:
                Гухьясамаджи, Ваджрабхайравы и Чакрасамвары. Храмы для этих построек были завершены
                в 1415 году, а мандалы и божества были установлены в 1417 году. Эти деяния
                считаются четвертым великим деянием Цонкапы. Он считается первым обладателем трона
                Гандена, или Ганден Трипа (<em>dga' ldan khri pa</em>), титул, который носили
                последующие настоятели монастыря.</p>
            <p>Цонгкапа умер в 1419 году в монастыре Ганден, через год после завершения работы над
                трактатом «Разъяснение мысли» (<em>dbu ma dgongs pa rab gsal</em>) в 1418 году. Ему
                было 62 года, и считается, что он достиг просветления посредством йогических
                практик в процессе смерти, обретя иллюзорное тело (<em>sgyu lus</em>). Его тело было
                погребено внутри украшенной драгоценностями ступы в Гандене. Смерть Цонкапы
                отмечается ежегодным празднеством Ганден Нгачо (<em>dga' ldan lnga mchod</em>), что
                переводится как «Ганденское подношение двадцать пятого», во время которого верующие
                зажигают масляные лампы на крышах и подоконниках. Цонкапа назначил своим преемником
                Гьелцабдже Дарму Ринчена (<em>rgyal tshab rje dar ma rin chen,</em> 1364-1432),
                который, в свою очередь, назначил Хедрубдже Гелека Пелзанга
                (<em>mkhas grub rje dge legs dpal bzang,</em> 1385–1438) следующим правителем
                Гандена.</p>
            <p>Помимо своих собственных учителей, многих из которых Цонкапа также обучал, у него
                был ряд других выдающихся учеников. К ним относятся Гьелцаб, Хедруб и Шакья Йеше.
                Среди его других учеников — Гендун Друб, посмертно признанный Первым Далай-ламой
                (<em>ta la'i bla ma 01 ge 'dun grub,</em> 1391-1474), и Джамьянг Чодже Ташо Пелден
                (<em>'jam dbyangs chos rje bkra shis dpal ldan,</em> 1379-1449), основатель
                монастыря Дрепунг в 1416 году. Сегодня Хедрубдже и Гьелцабдже считаются главными
                учениками Цонкапы, хотя это утверждение может быть и неверным. Истинность этого
                утверждения оспаривается современной наукой. Например, Дулдзин Дракпа Гьелцен
                (<em>'dul 'dzin grags pa rgyal mtshan,</em> 1374-1434), близкий ученик, согласно
                более поздней традиции, был отведён на более низкий статус. Тем не менее, все эти
                ученики продолжали распространять учение Цонкапы посредством своих собственных
                учений и трудов, а также другими способами, такими как основание монастырей, что
                позволило сформироваться традиции Гелуг.</p>
