            <p class="mb-3" id="citation-25"><sup>25</sup> VII Далай-лама Келсанг Гьяцо
                (тиб. <em>blo bzang bskal bzang rgyal ba rgya mtsho</em>, 1708–1757) — один
                из
                наиболее выдающихся Далай-лам как в политическом, так и в духовном
                отношении.
                <a href="#ref-25" class="text-blue-500 hover:underline">↩</a></p>
            <p>Келсанг Гьяцо родился в 1708 году (земля-крыса двенадцатого
                шестидесятилетнего
                цикла) в области Литанг (<em>li thang</em>) в Кхаме, на востоке Тибета. Его
                отец,
                Сонам Даргье (<em>bsod nams dar rgyas</em>, ум. 1744), и мать, Сонам Чоцо
                (<em>bsod nams chos mtsho</em>), были простыми людьми. Согласно агиографиям,
                рождение младенца сопровождалось чудесами: ребёнок произнёс удивительные
                слова, а
                дядя по материнской линии дал ему благоприятное имя Келсанг Гьяцо — «Океан
                благой
                судьбы».</p>
            <p>Вскоре после рождения местный монах, принявший (как оракул) защитным
                божеством Оден
                Карпо (<em>'od ldan dkar po</em>, "Светоносный Белый"), объявил, что мальчик
                является перерождением «учителя, которого радостно видеть» — так была
                истолкована
                фраза, указывающая на покойного Далай-ламу. Оракул также заявил, что мальчик
                не
                должен оставаться дома и его необходимо немедленно доставить в монастырь.
            </p>
            <p>Позже появилась легенда о стихотворении VI Далай-ламы Цангьянга Гьяцо
                (<em>tsangs dbyangs rgya mtsho</em>, 1683–1706), которое стало
                восприниматься как
                пророчество о личности мальчика:</p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                Белый журавль, одолжи мне свои крылья.<br>
                Я не полечу далеко;<br>
                Из Литанга я вернусь.
            </blockquote>
            <p>Весть о перерождении Далай-ламы в Кхаме постепенно распространилась по
                восточному
                Тибету, где тибетские и монгольские лидеры приветствовали эту новость.
                Однако
                Лхасанг-хан (1677–1717), унаследовавший титул «Короля Тибета» от своего
                предка
                Гуши-хана (покровителя V Далай-ламы), был крайне недоволен: после смещения и
                смерти
                VI Далай-ламы Лхасанг назвал своего собственного сына, Нгаванга Еше Гьяцо
                (<em>ngag dbang ye shes rgya mtsho</em>, род. 1686), Далай-ламой — акт, за
                который
                тибетцы его презирали.</p>
            <p>Лхасанг отправил двух военачальников расследовать дело литангского мальчика.
                Тибетский офицер Норбу Нгодруб (<em>nor bu dngos grub</em>), узнав о
                притязаниях,
                попытался разрядить ситуацию, утверждая, что, поскольку Цангьянг Гьяцо не
                был
                признан монголами и маньчжурами истинным Далай-ламой, Келсанг Гьяцо не
                представляет
                угрозы. Однако он же посоветовал отцу мальчика, Сонаму Даргье, найти
                безопасное
                убежище. Семья бежала в пустыню той же ночью.</p>
            <p>В 1714 году, услышав, что люди Лхасанга вновь приближаются, Сонам Даргье
                решил искать
                убежища в Дерге (<em>sde dge</em>). Хотя принц Дерге, Тенпа Церинг
                (<em>bstan pa tshe ring,</em> 1678–1738), принял их хорошо, безопасность
                была не
                гарантирована. Монгольский вождь Кукунора Цинван Батуртайджи и другие
                устроили
                переезд мальчика в Амдо, где представители главных гелугпинских монастырей
                Центрального Тибета смогли наконец его исследовать. В результате Келсанг
                Гьяцо был
                официально, но тайно признан новым Далай-ламой.</p>
            <p>Монастырь Намгьел Драцанг (<em>rnam rgyal grwa tshang</em>) — личный
                монастырь
                Далай-лам, основанный V Далай-ламой — был восстановлен, и его непрерывная
                история с
                этого времени ведётся до наших дней.</p>
            <p>Когда Келсангу Гьяцо исполнилось восемь лет, император Канси (правл.
                1661–1722),
                следуя прецедентам, установленным его отцом с V Далай-ламой, отправил
                представителей двора, и объединённая китайско-тибетско-монгольская кавалерия
                сопроводила перерождеца в Кумбум (<em>sku 'bum</em>) — знаменитый монастырь
                близ
                Синина, основанный на месте рождения Цонкапы. Здесь Келсанг Гьяцо был
                возведён на
                трон, и было публично зачитано императорское провозглашение, подтверждающее:
            </p>
            <blockquote class="border-l-4 border-sky-600 text-xl pl-4 my-4">
                Это воплощение является подлинным перерождением прежнего Далай-ламы... Как
                Всеведущий приходит в мир подобно солнцу, которое невозможно закрыть рукой,
                лучи
                света его сострадания и просветлённых деяний охватывают весь мир, так что
                учение
                Будды расширяется и возрастает.
            </blockquote>
            <p>Однако император продолжал утверждать легитимность правления Лхасанг-хана в
                Тибете, и
                поэтому молодому Далай-ламе пришлось оставаться в Кумбуме, где он продолжил
                обучение
                под руководством выдающихся наставников.</p>
            <p>Среди его учителей были:<br>
                II Чубсанг Лобсанг Тенпей Гьелцен
                (<em>chu bzang 02 blo bzang bstan pa'i rgyal mtshan,</em> 1652–1723), давший
                ему
                обеты послушника<br>
                Келсанг Принлей (<em>skal bzang 'phrin las</em>) — первый учитель мирских
                наук.
            </p>

            <p>В 1717 году джунгарские монголы вторглись в Центральный Тибет и Кхам, изгнав
                Лхасанга. В 1720 году, когда армия джунгаров начала рушиться под натиском
                объединённых маньчжурских и тибетских сил, тринадцатилетний Келсанг Гьяцо
                отправился занять свой трон в Лхасе.</p>
            <p>Император Канси благосклонно отнёсся к этому шагу и отправил своего
                четырнадцатого
                сына, принца Иньти (1688–1756), сопровождать Далай-ламу вместе с ведущими
                представителями тибетского буддизма при цинском дворе и маньчжурскими,
                китайскими и
                монгольскими военачальниками. Среди выдающихся тибетцев был II Туквaн
                Нгаванг Чокьи
                Гьяцо (<em>thu'u bkwan 02 ngag dbang chos kyi rgya mtsho,</em> 1680–1736).
            </p>
            <p>Келсанг Гьяцо прибыл во дворец Потала осенью 1720 года. Той же зимой он был
                посвящён
                в монахи V Панчен-ламой Лобсангом Еше (<em>pan chen bla ma blo bzang ye
                    shes,</em>
                1663–1737), который дал ему монашеское имя Лобсанг Келсанг Гьяцо
                (<em>blo bzang skal bzang rgya mtsho</em>).</p>
            <p>Молодой Далай-лама был ещё несовершеннолетним и продолжал полностью посвящать
                себя
                религиозному образованию. Среди его наставников были:<br>
                Лобсанг Даргье (<em>blo bzang dar rgyas,</em> 1662–1723), позже ставший 49-м
                Гандэн
                Трипой — обучал философии от логики до последней главы Абхисамаяланкары,<br>
                Пелден Дракпа (<em>dpal ldan grags pa,</em> ум. 1729), позже 51-й Гандэн
                Трипа,<br>
                Кхадо Сопа Гьяцо (<em>mkhar rdo bzod pa rgya mtsho</em>, 1672–1749) —
                помощник
                наставника.
            </p>
            <p>В 1720-х годах отношения между тибетскими олигархами, правившими при
                поддержке
                маньчжуров, становились всё более напряжёнными. В 1727 году произошло
                убийство
                лидера Кангченне (<em>khang chen nas bsod nams rgyal po,</em> ум. 1727)
                тремя
                заговорщиками, включая Нгапо Дордже Гьелпо, Лумпаву и Джараву. Отец
                Далай-ламы,
                Сонам Даргье, был замешан в заговоре.</p>
            <p>Полхане Сонам Топже (<em>pho lha nas bsod nams stobs rgyas,</em> 1689–1747),
                единственный
                союзник убитого Кангченне, поднял армии в западном Тибете и Цанге, чтобы
                свергнуть
                убийц. С помощью маньчжурских подкреплений восстание было подавлено.</p>
            <p>Цинский двор отказался от олигархии в пользу единоличного правления под
                руководством
                Полхане. Однако частью цены, которую Полхане потребовал за принятие своей
                новой
                должности, было изгнание Далай-ламы — единственный практический способ
                вытеснить
                Сонама Даргье из Лхасы.</p>
            <p>Прагматичное решение, принятое маньчжурами, заключалось в том, чтобы поселить
                Далай-ламу в недавно построенном монастыре Гартар (<em>mgar thar</em>) на
                восточной
                окраине
                Кхама, якобы для того, чтобы он мог продолжить своё религиозное образование.
            </p>
            <p>Следующие восемь лет (1728–1735) Далай-лама полностью посвятил себя изучению
                и
                медитации, преподавая и сочиняя тексты для тех, кто собирался получить его
                благословение.</p>
            <p>Нгаванг Чогден (<em>ngag dbang mchog ldan,</em> 1677–1751), позже ставший
                54-м Гандэн
                Трипой,
                был назначен наставником, в основном по тантрическим темам. Именно в этот
                период
                Келсанг Гьяцо стал известен как один из величайших тантрических мастеров
                традиции
                Гелуг, и поэтому его иногда изображают в тибетском искусстве как йогина.</p>
            <p>Его великий комментарий к мандале и обрядам посвящения Тантры Гухьясамаджи
                является
                наиболее обширным из его трудов и считается одним из ведущих шедевров
                гелугпинской
                тантрической литературы.</p>
            <p>В 1735 году было решено, что настало время разрешить Келсангу Гьяцо вернуться
                в
                Лхасу. Полхане, теперь уверенный в своей власти, хотя и не приветствовал
                идею
                возвращения иерарха, признал бессмысленность противодействия этому и
                согласился на
                императорское решение при условии, что Келсанг Гьяцо будет исполнять только
                религиозные и церемониальные функции.</p>
            <p>Религиозным лидером делегации из пятисот человек, сопровождавшей Далай-ламу,
                был не
                кто иной, как III Чанкья Ролпей Дордже
                (<em>lcang skya 03 rol pa'i rdo rje,</em> 1717–1786), тогда ещё
                восемнадцатилетний юноша.</p>
            <p>Чанкья с энтузиазмом воспринял возможность продолжить обучение под
                руководством
                Далай-ламы и его наставников. В течение следующих двух лет он стал близким
                наперсником и учеником Келсанга Гьяцо, а спустя много лет станет его
                официальным
                биографом.</p>
            <p>В 1737 году Чанкья отправился в Ташилунпо (<em>bkra shis lhun po</em>) в
                Шигадзе,
                чтобы
                получить полное посвящение от V Панчен-ламы, как это делал сам Далай-лама.
                Однако
                его пребывание с этим мастером было прервано новостями о внезапной смерти
                императора
                Юнчжэна, и Чанкья был вынужден спешно вернуться в Пекин. Новый император,
                Цяньлун
                (правл. 1735–1796), оказался его ближайшим другом среди принцев.
                Учитывая связи Чанкьи как с маньчжурским правителем, так и с Келсангом
                Гьяцо, он
                стал играть уникально важную роль в китайско-тибетских делах на протяжении
                последующих десятилетий.</p>
            <p>Смерть Полхане в 1747 году ознаменовала начало нового периода нестабильности.
                Его
                трон наследовал его второй сын, Гьюрме Намгьел (<em>'gyur med rnam
                    rgyal</em>),
                который попытался возобновить связи с джунгарами, вечными противниками
                маньчжурской
                гегемонии во Внутренней Азии. Говорят также, что он избегал Далай-ламу.</p>
            <p>Напряжённость достигла апогея в 1750 году, когда Гьюрме Намгьел был убит по
                приказу
                амбаней (маньчжурских представителей). В отместку сторонники Гьюрме Намгьела
                напали
                на канцелярию амбаней, убили амбаней и устроили общую резню китайцев в
                Лхасе.</p>
            <p>Делегация, посланная императором, без колебаний приняла суровые карательные
                меры; схваченые члены фракции Гьюрме Намгьела были либо казнены, либо
                заключены в
                тюрьму.</p>
            <p>После этих событий император Цяньлун решил, что тибетцам больше нельзя
                доверять
                управление собой, и впредь два назначенных двором амбаня будут действовать
                как
                единственные правители региона. Чанкья Ролпей Дордже, действуя как посредник
                между
                тибетским духовенством и императором, убедил, что попытка поставить тибетцев
                под
                прямое маньчжурское правление будет иметь неблагоприятные последствия, и
                рекомендовал, чтобы его учителю и другу, Далай-ламе, теперь было разрешено
                занять
                своё законное место.</p>
            <p>Император признал достоинства позиции Чанкьи и в длинном провозглашении,
                адресованном
                тибетским властям, установил систему совместного правления, в которой амбани
                и
                Далай-лама, при помощи уважаемых чиновников, таких как Доринг Пандита Гонпо
                Нгодруб
                Рабтен (<em>rdo ring paNDita mgon po dngos grub rab brtan,</em> 1721–1792),
                вместе
                будут отвечать за тибетские дела.</p>
            <p>Политический успех VII Далай-ламы был неожиданным. Благодаря своей репутации
                учёного и моральной чистоте, а также благодаря глубокой преданности тибетцев
                фигуре
                Далай-ламы, он заручился поддержкой как народа, так и влиятельных
                представителей
                духовенства, знати и маньчжурского двора.</p>
            <p>Среди важных политических институтов, созданных при VII Далай-ламе — Кашаг
                (<em>bka' shag</em>) — руководящий совет или кабинет министров, служивший
                вершиной
                светской администрации в Тибете до 1959 года и продолжающий функционировать
                сегодня
                в тибетском правительстве в изгнании.</p>
            <p>В 1754 году Далай-лама основал новую школу, специализирующуюся на
                каллиграфии,
                литературных искусствах и астрологии — основных предметах, необходимых для
                тибетской
                государственной службы, включая знаменитую художественную мастерскую Допел
                (<em>'dod dpal</em>) в квартале Жол под Поталой. Позже к этим новым
                учреждениям был
                добавлен архивный офис (<em>yig tshang las khung</em>), который вместе
                регулировал
                материальные аспекты тибетской светской и монашеской администрации.</p>
            <p>В 1756 году здоровье Далай-ламы начало слабеть, и он скончался в следующем
                году в
                возрасте всего пятидесяти лет. Бразды правления принял регент, VII Демо
                Джампел
                Делек Гьяцо (<em>de mo 'jam dpal bde legs rgya mtsho, </em>1723–1777).</p>
            <p>VII Далай-лама числится учителем десятков выдающихся гелугпинских лам своего
                времени, среди них:<br>
                Лонгдол Лама Нгаванг Лобсанг (<em>klong rdol bla ma ngag dbang blo
                    bzang,</em> ум.
                1794),<br>
                57-й Гандэн Трипа Самтен Пунцок (<em>dga' ldan khri pa bsam gtan phun
                    tshogs,</em>
                1703–1770),<br>
                61-й Гандэн Трипа Нгаванг Цультрим (<em></em>
                1721–1791),<br>dga' ldan khri pa ngag dbang tshul khrims,
                II Джамьянг Шепа Кончог Джигме Вангпо (<em>'jam dbyangs bzhad pa dkon mchog
                    'jigs
                    med dbang po,</em> 1728–1791).
            </p>
            <p>За свою жизнь он дал:<br>
                Обеты послушника 9774 мужчинам,<br>
                Полные монашеские обеты 16993 монахам,<br>
                Мирские обеты бесчисленным буддийским мирянам.</p>
            <p>Он подтвердил личности многочисленных перевоплощённых лам, включая:<br>
                IV Жабдрунг Карпо Лобсанг Тубтен Гелек Гьяцо (<em>zhabs drung dkar po blo
                    bzang thub
                    bstan dge legs rgyal mtshan,</em> 1729–1796),<br>
                VI Пагпа Лха Джигме Тенпей Гьяцо (<em>'phags pa lha 'jigs med bstan pa'i
                    rgya
                    mtsho,</em> 1714–1754),<br>
                III Туквaн Лобсанг Чокьи Ньима (<em>thu'u bkwan blo bzang chos kyi nyi
                    ma,</em>
                1737–1802).
            </p>
            <p>Семь томов его Собрания сочинений делают его, после Великого V и XIII,
                третьим по
                плодовитости писателем среди Далай-лам. Его труды включают комментарии,
                литургические тексты и широкий спектр официальных и освятительных
                документов.</p>
            <p>Однако наиболее восхищаемыми литературными достижениями являются его религиозные
                поэмы, включающие простые советы для буддийской религиозной жизни вместе с
                глубокими
                наставлениями для созерцания. В оригинале на тибетском эти стихи написаны в размере
                танцевальной песни (<em>glu gar</em>), который предпочитал VI Далай-лама для
                стихов на мирские темы.</p>
