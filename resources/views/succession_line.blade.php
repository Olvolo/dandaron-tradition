            <p class="mb-3" id="citation-18"><sup>18</sup> Лобзанг Чокьи Гьялцен (<em>blo
                    bzang chos
                    kyi rgyal mtshan</em>, 1570–1662) родился в деревне Друкгья (<em>brug
                    brgya</em>)
                в долине Лхан в Цанге, в 1570 году (по некоторым источникам, в 1567 году).
                Он
                принадлежал к знатному клану Ба (<em>sba</em>). Его отца звали Кунга Озер
                (<em>kun
                    dga' 'od zer</em>), он был племянником Вэнса Сангье Еше (<em>dben sa
                    sangs rgyas
                    ye shes</em>, 1525–1590/1591). Мать звали Цогьел (<em>mtsho rgyal</em>).
                При
                рождении ему дали имя Чогьел Пэлден Зангпо (<em>chos rgyal dpal ldan bzang
                    po</em>).<a
                    href="#ref-18" class="text-blue-500 hover:underline">↩</a></p>
            <p>Мальчик был признан Лангмикой Чокьи Гьялценом (<em>glang mig pa chos kyi rgyal
                    mtshan</em>) как перерождение Вэнсапы Лобзанга Дондруба (<em>dben sa pa blo
                    bzang don sgrub</em>, 1505–1566) и получил имя Чокьи Гьялцен.</p>
            <p>В юности Чокьи Гьялцен учился у Сангье Еше, который в то время был
                настоятелем монастырей
                Ташилунпо (<em>bkra shis lhun po</em>) и Вэнсапа (<em>dben sa pa</em>).
                Первые годы
                жизни он обучался у Сангье Еше каждую осень в Друкгья, получая от него множество
                благословений и посвящений. Также он получал учения и посвящения от своего
                брата и
                деда.</p>
            <p>В возрасте тринадцати лет Чокьи Гьялцен покинул Друкгья и отправился в
                монастырь
                Вэнса, чтобы продолжить обучение у Сангье Еше. Он принял обеты послушника
                (гецула) у
                своего наставника и получил имя Лобзанг Чокьи Гьялцен, а также начал
                изучать наставления по
                Ламриму (<em>lam rim</em>). Чокьи Гьялцен оставался в Вэнса в течение
                следующих пяти
                лет.</p>
            <p>На восемнадцатом году жизни Чокьи Гьялцен отправился в Ташилунпо, где
                поступил в
                дацан Тосам Линг (<em>thos bsam gling grwa tshang</em>) и учился у Пэлджора
                Гьяцо
                (<em>dpal 'byor rgya mtsho</em>). Однако следующие три лета он проводил в
                Вэнса, получая дальнейшие учения и передачи от Сангье Еше, включая Гандэн
                Махамудру
                (<em>dga' ldan phyag chen</em>) Цонкапы.</p>
            <p>В 1591 году он узнал, что Сангье Еше болен оспой, и быстро вернулся, чтобы
                навестить
                его в последний раз, незадолго до кончины учителя. После успешной сдачи
                экзамена по
                Праманаварттике в Ташилунпо, Чокьи Гьялцен вернулся в Вэнса, чтобы
                руководить
                похоронами.</p>
            <p>В том же 1591 году он принял обеты полного посвящения (гелонг). Церемонию
                проводили Панчен Дамчо Ярвел (<em>paN chen dam chos yar 'phel</em>), Пэлджор
                Гьяцо и Панчен Лхаванг Лодро (<em>paN chen lha dbang blo gros</em>).</p>
            <p>Затем он отправился в Лхасу, совершил подношения в Джоканге и продолжил свой
                путь в
                Гандэн, где продолжил обучение у Намкьяй Цэнчена (<em>nam mkha'i mtshan
                    can</em>),
                у которого изучал Калачакру, и у Гендуна Гьялцена (<em>dge' 'dun rgyal
                    mtshan</em>, 1532–1605/1607), 28-го держателя трона Гандэна, который
                преподал
                ему собрание сочинений Второго Далай-ламы. В свою очередь, Чокьи Гьялцен
                передал
                Гендуну Гьялцену учение Гандэн Махамудры, сделав его своим преемником в этой
                устной
                линии. Дамчо Пэлбар (<em>dam chos dpal 'bar</em>, 1523/1546–1599), 26-й
                держатель
                трона Гандэна, также преподал ему Чод.</p>
            <p>Вернувшись в Вэнса, который он расширил новыми храмами и статуями, Чокьи
                Гьялцен
                давал публичные учения по Ламриму и другим темам, но вскоре почувствовал
                необходимость удалиться в затвор. Он закрылся от мира на шесть или семь
                месяцев,
                читая Писания между сессиями медитации. Во время этого короткого ретрита он
                пережил
                видение Цонкапы и во сне получил от него ряд важных передач. Затем он
                перенес свой
                затвор в родную деревню, некоторое время живя как «одетый в хлопок» (рас па)
                в
                традиции аскетов Кагью, прежде чем вернуться в Вэнса.</p>
            <p>В 1601 году, когда его слава широко распространилась, Лобзанга Чокьи Гьялцена
                попросили занять пост настоятеля Ташилунпо. К тому времени
                тридцатиоднолетний лама
                уже был настоятелем Вэнса и, начиная с 1598 года, настоятелем Гангчен Чопел
                (<em>gangs can chos 'phel</em>), приняв этот пост по просьбе Лхунце Депы
                (<em>lhun rtse sde pa</em>).</p>
            <p>В том же году он инициировал проведение Великого Молитвенного Праздника
                (Монлам
                Ченмо, <em>smon lam chen mo</em>) в Ташилунпо, установив множество новых
                статуй в
                храмах.
                Восемь лет спустя, в 1609 году, он основал в монастыре тантрический дацан —
                Ташилунпо Гьюпа Драцанг (<em>bkra shis lhun po rgyud pa grwa tshang</em>).
            </p>
            <p>Вскоре после того, как он занял пост настоятеля Ташилунпо, монастырь посетил
                Четвертый
                Далай-лама, Йонтен Гьяцо (<em>yon tan rgya mtsho</em>, 1589–1616), впервые
                прибывший
                в Тибет из Монголии. Чокьи Гьялцен, по-видимому, сыграл ключевую роль в
                признании
                тибетцами монгольского мальчика законным воплощением Сонама Гьяцо (Третьего
                Далай-ламы). Четвертый Далай-лама попросил Чокьи Гьялцена сопровождать его в
                Дрепунг, где он некоторое время преподавал, а затем во время его путешествий
                по
                различным монастырям Кадампа и Гелугпа в регионе, включая Рэтинг (<em>rwa
                    sgreng</em>).</p>
            <p>В 1612 году Чокьи Гьялцен посетил Бутан по приглашению иерархов Лхапа из Ньо
                (<em>gnyos</em>)
                — клана, придерживающегося Друкпа Кагью, но враждовавшего с Жабдрунгом
                Агваном
                Намгьелом (<em>zhabs drung ngag dbang rnam rgyal</em>, 1594–1651). Позднее
                Лхапа
                перешли в традицию Гелуг, что связывают с влиянием и тесными отношениями с
                Чокьи
                Гьялценом. Он также способствовал слиянию других клановых религиозных
                традиций в
                Гелуг. Чокьи Гьялцен снова участвовал в тибетско-бутанских делах,
                договорившись о
                перемирии в конфликтах в середине 1650-х годов.</p>
            <p>Чокьи Гьялцен продолжал курсировать между Шигаце и Лхасой, преподавая в
                Ташилунпо,
                Дрепунге, Сэра, Гандэне и других монастырях Гелуг. В 1617 году, после
                кончины
                Четвертого Далай-ламы, Чокьи Гьялцен принял на себя пост настоятеля как
                Дрепунга,
                так и Сэра.</p>
            <p>В 1626 году он был назначен настоятелем дацана Джангце в Гандэне, а в 1642
                году —
                настоятелем Жалу (<em>zha lu</em>).</p>
            <p>В 1618 году правящая семья Цанга (сторонники Кагью) свергла семью Пагмодрупа
                и начала
                репрессии против учреждений Гелугпа в регионе Лхасы, хотя и терпимо
                относилась к
                Ташилунпо и Чокьи Гьялцену. Излечив царя Цанга от болезни, которую тот
                считал
                наведенной Четвертым Далай-ламой, Чокьи Гьялцен смог получить разрешение на
                подтверждение перерождения Четвертого Далай-ламы в лице мальчика, которого
                он назвал
                Лобзанг Гьяцо (<em>blo bzang rgya mtsho</em>, 1617–1682), позже ставшего
                Пятым
                Далай-ламой, хотя ему было запрещено торжественно возводить его на престол
                главы
                Гелуг в Лхасе.</p>
            <p>Отношения между Лхасой и Шигаце ухудшались, и Чокьи Гьялцену приходилось
                многократно
                выступать в роли посредника. В 1621 году он был вынужден вмешаться во время
                монгольского вторжения: после тайных переговоров с иерархами Гелуг
                монгольские
                войска осадили власти Цанга в Лхасе. Только после вмешательства Чокьи
                Гьялцена силам
                Цанга было разрешено отступить в Шигаце. После ухода сил Цанга из Лхасы, в
                1622 году
                Чокьи Гьялцен смог торжественно "возвести на престол" Пятого Далай-ламу в
                Дрепунге.</p>
            <p>После поражения царя Цанга и восшествия Пятого Далай-ламы на трон Тибета в
                1641 году,
                влияние Чокьи Гьялцена возросло ещё больше. Чокьи Гьялцену был присвоен
                титул
                Панчен-ламы.</p>
            <p>Существуют две системы нумерации: по системе Ташилунпо, три предыдущих ламы,
                признанных его воплощениями, считаются Первым–Третьим Панчен-ламами:
                Кхедрубдже
                Дгелег Пэлзанг (1385–1438), Сонам Чокьи Лангпо (1439–1505) и Вэнсапа Лобзанг
                Дондруб. По этой причине Чокьи Гьялцен указывается либо как Первый, либо как
                Четвертый Панчен-лама (в соответствии с общепринятым стандартом он часто
                именуется
                Четвёртым).</p>
            <p>Чокьи Гьялцен продолжал преподавать в течение следующих двух десятилетий и
                скончался в 1662 году.</p>
