            <p class="mb-3" id="citation-14"><sup>14</sup> Басо Чокьи Гьелцен
                (<em>ba so chos kyi rgyal mtshan,</em> 1402–1473) — 6-й Гандэн Трипа
                (держатель трона Гандэн, глава школы Гелуг, 1463–1473), выдающийся ученик Дже
                Цонкапы и ранний держатель линии Гандэнской Махамудры. Позднее был признан первым
                воплощением в линии Тацаг Джедрунг (<em>rta tshag rje drung</em>). Родился в 1402
                году в Лато (<em>la stod</em>) в знатной семье. Отец — Таши Пелсанг
                (<em>bkra shis dpal bzang</em>), благородного происхождения; мать — Будрен Гьелмо
                (<em>bu 'dren rgyal mo</em>). Басо был младшим братом Кедруб Дже Геле Пелсанга
                (<em>mkhas grub rje dge legs dpal bzang,</em>1385–1438) — одного из двух главных
                учеников Цонкапы и 1-го Гандэн Трипы. Принял монашество в молодом возрасте и
                обучался под руководством Йонгдзин Кедруба (<em>yongs 'dzin mkhas grub</em>) и
                своего прославленного старшего брата. Продолжил обучение у Джампела Гьяцо
                (<em>'jam dpal rgya mtsho,</em> 1356–1428) — одного из выдающихся учеников Цонкапы.
                От него получил:<br>
                посвящения в практики Гухьясамаджи, Чакрасамвары, Ваджрабхайравы и Чод,<br>
                Передачу Гандэнской Махамудры (<em>dga' ldan phyag chen</em>) — тайной линии
                практики Махамудры в традиции Гелуг,<br>
                Коренной текст этой передачи — «Великую книгу эманаций»
                (<em>sprul pa'i glegs bam chen mo</em>).<br>
                Отправился в Цанг, где основал или возглавил монастырь Басо Лхундруб Дечен
                (<em>ba so lhun grub bde chen dgon)</em>, получив титул Басо Чодже
                (<em>ba so chos rje,</em>"Владыка Дхармы из Басо"). Основал поблизости
                отшельнический центр Венне Дечен Пук (<em>dben gnas bde chen phug</em>) возле
                Сенгеце (<em>seng ge rtse</em>) в Цанг Шабто (<em>gtsang shab stod</em>), где
                обучал многочисленных учеников.<br>
                Стремясь удалиться в ещё более уединённое место, Чокьи Гьелцен готовился
                отправиться в Кашмир, когда получил приказ принять трон монастыря Гандэн.
                Согласно агиографиям, сам Цонкапа явился ему во сне и возложил на него эту
                обязанность, хотя фактический призыв исходил от предыдущего держателя трона
                Чодже Лодрё Чокьонга (<em>chos rje blo gros chos skyong,</em> 1389–1463) и Гендуна
                Друба (<em>dge 'dun grub</em>, 1391–1474) — будущего 1-го Далай-ламы, который
                тогда пребывал в монастыре Ташилунпо в Шигацзе. Лодрё Чокьонг просил Гендуна Друба
                принять трон, но тот отказался, рекомендовав вместо себя Чокьи Гьелцена. Басо Чокьи
                Гьелцен принял трон Гандэн в 1463 году и оставался на посту до своей смерти в 1473
                году. У Чокьи Гьелцена было три главных ученика, известных вместе как "Три брата
                Дордже" (<em>rdo rje mched gsum</em>): Чокьи Дордже (<em></em>chos kyi rdo rje, род.
                ок. 1457) — махасиддха, учитель Венсапы Лосанга Дондруба (3-го Панчен-ламы), Пелден
                Дордже (<em></em>dpal ldan rdo rje) из Толунга, Дордже Пелва
                (<em>rdo rje dpal ba</em>) из Кхама.<a href="#ref-14"
                                                       class="text-blue-500 hover:underline">↩</a>
            </p>
