            <p class="mb-3" id="citation-13"><sup>13</sup> Кхедрубдже Гелег Пэлзанг (<em>mkhas grub
                    rje dge legs dpal bzang</em>), позднее посмертно признанный Первым Панчен-ламой
                (как пре-инкарнация Четвертого Панчен-ламы Лобзанга Чокьи Гьелцена), родился в 1385
                году в области Цанг. Его отцом был Кунга Таши Пэлзанг (<em>kun dga' bkra shis dpal
                    bzang</em>), принадлежавший к клану Се (якобы происходившему из Хотана), а
                матерью — Будрен Гьелмо.<a href="#ref-13"
                                           class="text-blue-500 hover:underline">↩</a></p>
            <p>В возрасте семи лет он принял обеты послушника (шраманера) от Кхенчена Сенгге
                Гьялцена и получил имя Гелег Пэлзанг.</p>
            <p>С шестнадцати лет он обучался в монастыре Нгамринг Чоде
                (<em>ngam ring chos sde</em>), где преподавал мастер традиции Сакья. Здесь он
                учился у Бодонга Панчена Джигдрела Чокле Намгьяла
                (<em>bo dong paN chen 'jigs bral phyogs las rnam rgyal</em>, 1376–1451), основателя
                традиции Бодонг, который преподавал ему логику и философию.</p>
            <p>В возрасте двадцати одного года он продолжил обучение у Рэндавы Жонну Лодро (<em>red
                    mda' ba gzhon nu blo gros</em>, 1349–1412), от которого принял полное монашеское
                посвящение (бхикшу). Он изучал: Праманаварттику Дхармакирти, Абхидхарму, «Пять Книг
                Майтреи», работы Нагарджуны по Мадхьямаке и Винаю.</p>
            <p>В возрасте двадцати трех лет, в 1407 году, Гелег Пэлзанг отправился в У (Центральный
                Тибет), чтобы встретиться с Цонкапой Лобзангом Дракпой (1357–1419) в ретритном
                центре Сэра Чодинг (<em>se ra chos sdings</em>), расположенном над местом, где позже
                будет основан монастырь Сэра.</p>
            <p>Гелег Пэлзанг получил наставления как по сутре, так и по тантре от Цонкапы и быстро
                стал одним из его самых преданных учеников, обучаясь вместе с Гьелцабдже Дарма
                Ринченом и Дулдзином Дракпа Гьялценом.</p>
            <p>После обучения он вернулся в Цанг, где занял пост настоятеля в монастыре Чангра
                (<em>lcang ra</em>). Он также основал монастырь Риво Дангчен
                (<em>ri bo 'dangs chen</em>).</p>
            <p>В возрасте тридцати четырех лет он участвовал в основании Пэлкхор Чоде (<em>dpal
                    'khor chos sde</em>) в Гьянце, под покровительством местного правителя Рабтена
                Кунзанга Пака.</p>
            <p>В 1431 году, в возрасте сорока семи лет, по просьбе Гьелцабдже, Гелег Пэлзанг взошел
                на Золотой Трон Гандэна (<em>dga' ldan gser khri</em>), став третьим Гандэн Трипой
                (настоятелем Гандэна) после Цонкапы и Гьелцабдже Дарма Ринчена. Находясь на этом
                посту, Кхедрубдже активно преподавал, давал множество посвящений и лично наставлял
                ведущих ученых того времени в традициях Гелуг.</p>
            <p>Он скончался в Гандэне в 1438 году в возрасте пятидесяти трех лет.</p>
