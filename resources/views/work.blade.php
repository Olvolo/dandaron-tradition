        <p>Вся буддийская хинаянистическая практика сводится к тому, как нужно индивиду
            освободиться от морального осквернения – от клеш. Для полного подавления всех клеш
            хинаянистический святой (архат) проходит два пути: путь накопления добродеяний (тиб.
            <em>tshogs-lam</em>)
            и путь творческий (тиб. <em>sbyor-lam</em>).</p>
        <p>Метод подавления клеш сводится к тому, что индивид должен обдумать каждую клешу в
            отдельности и после осознания ее должен практически, умышленно подавить ее в
            жизненном опыте. Этот опыт от простого смертного человека (санскр.
            <em>pṛthagjana</em>, тиб. <em>so-so-skyes-bu</em>)
            до состояния архата имеет четыре плода.</p>

        <p>По утверждению буддистов, когда ищущий обдумал все (четыре) Благородные Истины и
            пришел к выводу о необходимости освободиться от страдания, то этим самым он попадает
            в состояние, именуемое «<em>вошедший в поток</em>» (санскр. <em>śrota-āpanna</em>,
            тиб. <em>rgyun zhugs</em>). Это и есть первый плод. Но до <em>сротаапанны</em>
            индивид
            проходит четыре стадии. На последней стадии (пути творческой практики – санскр. <em>bhāvanā-mārga</em>,
            тиб. <em>sgom lam</em>)
            он и обретает состояние <em>сротаапанны</em>.</p>
        <p>Второй плод – <em>сакридагамин</em> (санскр. <em>sakṛd-āgāmin</em>, тиб. <em>phyir
                'ong</em>, досл. «единожды возвращающийся»). Индивид напряженно
            сосредоточивается на каждой клеше в отдельности и подавляет их. В этом состоянии
            (<em>сакридагамина</em>) индивид полностью еще не освобождается от клеш, хотя у него
            воспитывается
            стоицизм, твердость духа и высшая бесстрастность. В этом состоянии индивид еще один
            раз рождается в сансаре.</p>
        <p>Третий плод – <em>анагамин</em> (санскр. <em>anāgāmin</em>, тиб. <em>phyir mi
                'ong</em>, досл. «не возвращающийся»). Так называется индивид,
            полностью очистившийся от клеш и тем самым больше не возвращающийся в сансару, то
            есть не подчиняющийся закону кармы. Он совершенствуется дальше и достигает последней
            ступени – <em>архатства</em> (санскр. <em>arhat</em>, тиб. <em>dgra bcom pa</em>).
            Архат – последний, четвертый
            плод совершенствующегося. Состояние архата – <em>высшая святость хинаяны</em>, где
            подавлены все основные и малые клеши. Для достижения состояния архата требуется
            реализация пяти элементов, перечисленных во втором моменте процесса мышления.</p>
        <p><strong>Последний, шестой, психический процесс</strong> называется «<em>обратимый
                психический процесс</em>» (тиб. <em>gzhan 'gyur bzhi</em>, досл. «четыре
            изменяемых»), где участвуют четыре
            элемента: 1) <em>сон</em> (тиб. <em>gnyid</em>), 2) <em>раскаяние</em> (тиб. <em>'gyod
                pa</em>),
            3) <em>иллюзия</em> (тиб. <em>rtog pa</em>), 4) <em>исследование</em> (тиб. <em>dpyod
                pa</em>).
            Эти четыре элемента не входят в состав клеш. Поэтому они не подавляются усилием
            воли, а сон может быть прекращен просто через напряженное сосредоточение. Остальные
            три элемента могут быть легко обращены на пользу совершенствования.</p>

