        <p>Сущность или основные характеристики вышеуказанных элементов добродеяния понятны. Но
            стоит остановиться на десятом элементе, т.е. на элементе, называемом "<em>Равное
                отношение ко всем живым существам</em>". Об этом элементе пишет известный
            тибетский ученый XIV в. Рендапа в книге «Комментарии к "Абхидхармасамуччае"»:
            «Сознание, воспитанное на <em>равном отношении ко всем живым существам</em>,
            достигается при помощи дхьян в результате реализации плода <em>анагамин</em>.
            Индивид, подлинно познавший учение "Об отсутствии индивидуального <em>Я"</em>,
            обретает невозмутимость. В этот момент в нем открывается истина, что все живое несет
            в себе частицу Татхагаты, поэтому данное живое существо должно стать для меня
            одинаковым со всем остальным. Не может здесь быть близких и далеких, любимых и
            нелюбимых... Индивид должен концентрировать свою мысль на том, что все живые
            существа в беспредельных сансарных перерождениях были его матерями. Мы утверждаем,
            говорят буддисты, что сансара безначальна, и поэтому наши различные перевоплощения
            безначальны также. Если это так, то мы не можем сказать, что данное живое существо
            когда-нибудь, в каком-нибудь перевоплощении не было моей матерью. Поэтому мы должны
            иметь равное сострадание ко всем живым существам, как к своей матери»<a
                href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>.</p>
        <p>Для постижения такого результата требуется подавление клеши неведения
            (тиб. <em>ma-rig-pa</em>) и элемента различения (тиб. <em>'du-shes</em>).</p>
        <p>Следующий, одиннадцатый, элемент, <em>ахимса</em> – <em>никому не вредить</em> –
            вытекает из убеждения, что каждое живое существо имеет частицу Татхагаты, и также,
            что каждое живое существо является «моей матерью». Поэтому нужно иметь равное
            сострадание ко всем живым существам. Естественно, анагамин, имеющий подобное
            убеждение, не может породить мысль <em>вредить кому-нибудь</em>.</p>
        <p>Указанные <em>одиннадцать</em> элементов добродеяния практикуются на махаянистическом
            и хинаянистическом путях.</p>
        <p><strong>Четвертым психическим процессом</strong> индивида виджнянавадины считают <em>психический
                процесс</em>, где участвуют оскверняющие элементы – клеши. Здесь все элементы,
            входящие в группу элементов <em>клеша</em> (тиб. <em>nyon-mongs</em>), также имеют
            интенсивную связь с сознанием. Они подразделяются на <em>основные</em> и <em>сопутствующие
                клеши</em>.</p>
        <p>Основных клеш <em>шесть</em>: 1) <em>страсть</em> (тиб. <em>'dod-chags</em>, санскр.
            <em>rāga</em>), 2)
            <em>гнев</em> (тиб. <em>khong-khro</em>, санскр. <em>dveṣaḥ</em>), 3)
            <em>гордость</em> (тиб. <em>nga-rgyal</em>,
            санскр. <em>māna</em>), 4) <em>неведение</em> (тиб. <em>ma-rig-pa</em>, санскр. <em>avidyā</em>),
            5) <em>сомнение</em>
            (тиб. <em>the-tshoms</em>, санскр. <em>vicikitsā</em>). 6) Шестой клешей являются
            <em>пять</em>
            различных небуддийских, стало быть, <em>ложных воззрений</em> (тиб. <em>log-lta
                lnga</em>, санскр. <em>dṛṣṭi</em>):
        </p>
        <ol class="list-decimal list-outside ml-2 space-y-1 pl-2">
            <li class="my-2"><em>воззрение о разрушаемости всех вещей и явлений</em> (санскр.
                <em>satkāyadṛṣṭi</em>,
                тиб. <em>'jig-tshogs la lta-ba</em>). Представители этого взгляда придерживаются
                мнения, что все объективные вещи и явления состоят из различных частей, поэтому
                они без исключения подвержены разрушению. Вне, за пределами указанных
                объективных вещей и явлений, ничто не существует, стало быть, и не существует
                абсолютной реальности, называемой нирваной;
            </li>
            <li class="my-2"><em>воззрение, признающее, что сансара имеет начало и конец</em>
                (санскр. <em>antagrāha-dṛṣṭi</em>, тиб. <em>mtha' 'dzin pa'i lta ba</em>), сюда
                входят еще
                два воззрения:
                <ul class="list-disc pl-1 space-y-1 ml-1">
                    <li class="my-2">тиб. <em>rtag-mthar lta-ba</em> – <em>воззрение,
                            утверждающее
                            положение о вечности материи</em>;
                    </li>
                    <li class="my-2">тиб. <em>chad-mthar lta-ba</em> – <em>взгляд, отрицающий
                            существование потустороннего мира</em>, загробной жизни, морального
                        закон причины и следствия (карма);
                    </li>
                </ul>
            </li>
            <li class="my-2"><em>воззрение, признающее свой взгляд выше всех других
                    взглядов</em> (санскр. <em>dṛṣṭiparāmarśa</em>, тиб. <em>lta-ba mchog
                    'dzin</em>);
            </li>
            <li class="my-2"><em>воззрение, признающее нравственный закон выше всего</em>
                (санскр. <em>śīla-vrataparāmarśa</em>,
                тиб. <em>tshul-khrims brtul-zhugs mchog 'dzin</em>). Во времена распространения
                буддизма в Индии были школы, считающие нравственное раскаяние самым высшим
                добродеянием, причем они признавали именно такую нравственность, которая
                противоречила буддийской: например, сюда относились те аскеты, которые
                занимались физическим истязанием своего тела и другими противонравственными, с
                точки зрения буддизма, поступками;
            </li>
            <li class="my-2"><em>ложное воззрение</em> (санскр. <em>mithyādṛṣṭi</em>, тиб. <em>log-lta</em>).
            </li>
        </ol>

