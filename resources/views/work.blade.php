        <p>Если характеризовать их по отдельности, то <em>ведана</em> – это обычное чувство,
            которое возникает через соприкосновение с внешними объектами.</p>
        <p><em>Санджня</em> – процесс различения, или функция сознания, различающая вещи по их
            свойствам и форме (длинное, короткое, твердое или мягкое и т.д.). На присутствии
            этого элемента вообще основана возможность сознательной жизни, т.к. иначе все в
            природе казалось бы одинаковым.</p>
        <p><em>Четана</em> – активность сознания. Под активностью сознания, которая опять-таки
            отвлечена от самого сознания, следует разуметь, по-видимому, творческое воображение,
            которое решает или задумывает совершить то или иное действие: телесное, словесное
            или мысленное. Идея конструктора, проектирующего какой-либо сложный механизм, будет
            относиться к элементам <em>четана</em>, т.е. к <em>активности сознания</em>.</p>
        <p><em>Намаскара</em> – сознание идейного. К этой группе элементов относится умственный
            процесс, здравый смысл человека в его повседневной жизни до глубоких идейных
            убеждений.</p>
        <p><em>Спарша</em> – соприкосновение. Это начало воздействия всех элементов, из которых
            состоит индивид. В настоящей жизни индивид проходит много этапов, или моментов, пока
            он не станет человеком, т.е. пока он не дойдет до расцвета жизни.</p>
        <p>Как утверждают буддисты, существо, будучи в эмбриональном состоянии, с момента
            зачатия имеет все элементы еще в некомбинированном виде. Первый момент новой жизни
            – пробуждение зачаточного сознания. Дальше идет момент чувственного и
            нечувственного возникновения шести баз (<em>шад-аятана</em>) и т.д. В момент
            соприкосновения (<em>спарша</em>) сознание вступает в связь с органами чувств и
            объективными элементами. Это значит, что живое существо в утробе матери начинает
            воспринимать объекты органами чувств, т.е. видит, слышит и т.д. Но оно еще не
            сознает чувства приятного и неприятного – всего, связанного с этим. По учению
            буддистов, такое состояние, т.е. состояние без эмоций, продолжается до тех пор, пока
            данное существо не родится на свет.</p>
        <p>Соприкосновение, <em>спарша</em>, – это момент, когда дхармы-элементы впервые в
            новом вихре принимают то расположение, которое эмпирически переживается как сознание
            ощущения чего-то объективного (тиб. <em>reg-pa dang yul spyad pas gzugs su
                rung-ba</em>).
            Посредством соприкосновения и ощущения объектов обнаруживается <em>рупа</em> (тиб.
            <em>gzugs</em>).
            Ф. И. Щербатской пишет: «Момент цвета (<em>rūpa</em>), момент видимого ощущения
            материи (санскр. <em>cakṣuḥ</em>) и момент чистого сознания (санскр. <em>citta</em>),
            одновременно
            вступающие в тесное соприкосновение, создают то, что называется ощущением (или
            чувственным соприкосновением) – <em>sparśa</em><a href="#citation-1" id="ref-1"
                                                              class="citation-mark"><sup>1</sup></a>.
            <em>Спарша</em> по своей универсальности может считаться элементом «вездесущим»
            (тиб. <em>kun-'gro</em>).</p>
        <p>В момент одновременного действия всех пяти указанных элементов в интенсивной связи с
            сознанием происходит познавательный процесс. Это есть <strong>первый момент</strong>
            в процессе человеческого мышления без нравственных категорий.</p>
        <p><strong>Вторым моментом процесса мышления</strong> с включением нравственных
            категорий является момент действия других пяти элементов в интенсивной связи с
            сознанием. Эти элементы виджнянавадины называют <em>пятью реальными психическими
                объектами</em> (тиб. <em>yul nges lnga</em>). Здесь пять элементов: 1)
            <em>чанда</em>
            – желание (тиб. <em>'dun-pa</em>), 2) <em>адхимокша</em> – внимание (тиб.
            <em>mos-pa</em>),
            3) <em>смрити</em> – память (тиб. <em>dran-pa</em>), 4) <em>самадхи</em> –
            созерцание (тиб. <em>ting-nge-'dzin</em>),
            5) <em>праджня</em> – интуиция (тиб. <em>shes-rab</em>).</p>

