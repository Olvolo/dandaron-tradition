                Полный список включает:<a href="#ref-5"
                                          class="text-blue-500 hover:underline">↩</a></p>
            <ol class="list-decimal list-outside ml-2 space-y-1 pl-2">
                <li>krodha (гнев) — раздражение против людей и вещей;</li>
                <li>upanāha (злопамятность) — неуклонная приверженность тому, что достойно
                    осуждения;
                </li>
                <li>mrakṣa (скрытность/лицемерие) — сокрытие собственных прегрешений;</li>
                <li>pradāsa (злоба/озлобление) — недоброжелательная направленность сознания,
                    которой предшествуют гнев и злопамятность;
                </li>
                <li>īrṣyā (зависть) — умственное раздражение, гнев по поводу чужого богатства;
                </li>
                <li>mātsarya (скупость/скаредность) — мысль о том, чтобы воздержаться [от
                    даяния], которая препятствует добродетельному подношению;
                </li>
                <li>māyā (обман/хитрость) — демонстрация ложных качеств теми, кто стремится к
                    богатству и славе;
                </li>
                <li>śāṭhya (нечестность/изворотливость) — «искривление» сознания как способ
                    скрыть свои дефекты от других;
                </li>
                <li>mada (самодовольство) — радость и восторг по поводу здоровья, молодости и
                    прочих преходящих преимуществ;
                </li>
                <li>vihiṃsā (насилие/жестокость) — дурное обращение, когда [индивид] причиняет
                    вред другим;
                </li>
                <li>āhrīkya (бесстыдство) — отсутствие стыда перед дурными деяниями;</li>
                <li>anapatrāpya (непристойность) — отсутствие приличия;</li>
                <li>styāna (уныние/апатия) — «тяжесть в теле, тяжесть сознания, отсутствие
                    телесной гибкости, отсутствие живости сознания»;
                </li>
                <li>auddhatya (возбужденность) — отсутствие спокойствия сознания;</li>
                <li>āśraddhya (неверие) — отсутствие веры;</li>
                <li>kauśīdya (лень) — нежелание прилагать усилия;</li>
                <li>pramāda (беззаботность) — небрежность в практике;</li>
                <li>muṣitasmṛti (забывчивость) — выпадение памяти;</li>
                <li>vikṣepa (рассеянность/бессистемность) — неспособность к сосредоточению;</li>
                <li>asaṃprajanya (невнимательность) — отсутствие ясного понимания.</li>
            </ol>
            <p>Эти двадцать вторичных аффектов распределяются по трем категориям: «малые
                вторичные аффекты» (parittopakleśa) — первые десять; «средние вторичные аффекты»
                (madhya) — бесстыдство и непристойность, которые присутствуют в любом неблагом
                состоянии сознания; «большие вторичные аффекты» (mahopakleśa) — остальные
                восемь, которые присутствуют в любом загрязненном сознании.</p>
