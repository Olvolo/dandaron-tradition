        <p>Указанные <em>пять различных воззрений</em> йогачары объединяют в одну группу
            элементов и называют шестым <em>основным</em> элементом из <em>клеш</em>.</p>
        <p><em>Клеши</em> (тиб. <em>nyon-mongs</em>) – это элементы, возбуждающие отрицательные
            (грешные) поступки. Некоторые определяют клешу как первобытное страдание. По учению
            буддизма, волнение дхарм, или сансара, безначально (тиб. <em>'khor-ba
                thogs-ma-med</em>),
            поэтому клеши не есть результат грехопадения, они не грехи, которые можно искупить,
            а первобытное страдание, которое должно быть приостановлено. Грех может быть
            совершен в результате волнения элементов, т.е. в основе безначального волнения лежат
            элементы <em>самскара</em> (тиб. <em>'du-byed</em>). Они производят (обусловливают)
            нравственно положительные (тиб. <em>dge-ba</em>) и отрицательные действия
            (тиб. <em>mi-dge-ba</em>). Нравственно отрицательные действия обусловлены клешами
            (тиб. <em>nyon-mongs</em>).
        </p>
        <p>Ф. И. Щербатской поясняет это положение так: «Элементы морального осквернения –
            клеша – всегда имеют место в нашей жизни – <em>santāna</em> (тиб.
            <em>mtshan-gzhi</em>)
            в связанном (скрытом) состоянии. Здесь они имеют форму <em>остатков</em> (санскр.
            <em>anuśaya</em>, тиб. <em>bag-la nyal</em>),
            они прикрепляются к другим элементам, загрязняют их, приводят их к столкновению и
            предотвращают от состояния покоя. Это влияние неспокойных элементов в жизни
            называется <em>общей причиной</em> (санскр. <em>sarvatraga-hetu</em>),
            так как оно действует на весь поток жизни – <em>santāna</em>, все его элементы
            становятся
            загрязненными. Первостепенная причина этого неблагоприятного состояния – иллюзия,
            или невежество, – основной член в колесе жизни. Она продолжает существовать и
            проявлять свое влияние до тех пор, пока крутится <em>колесо</em> и постепенно
            нейтрализуется и, наконец, останавливается противодействием в форме превышающей ее
            мудрости – <em>prajñā-mālā</em>»<a href="#citation-3" id="ref-3"
                                               class="citation-mark"><sup>3</sup></a>.</p>
        <p>Страсть, гнев, гордость, неведение, сомнение – эти элементы входят в группу
            элементов <em>клеша;</em> как они действуют на сознание, нам известно. Среди них
            элемент <em>неведение</em> является самым основным. Благодаря неведению
            феноменальный мир познается индивидом так, что в нем присутствуют все условия для
            реализации страсти, гнева, гордости, сомнения и т.д. По утверждению буддистов,
            индивид, подавивший в себе клешу неведения, становится просветленным. Он смотрит на
            мир иными глазами, через призму мудрости. Тогда, по утверждению Чандракирти, там не
            остается ничего, кроме бездны – шуньяты, где нет места и условия для реализации
            страсти, гнева, гордости и т.д.</p>

        <p><strong>Пятым психическим процессом</strong> является процесс актуализации
            <em>двадцати</em>
            видов сопутствующих <em>клеш</em> (тиб. <em>nye-ba'i nyon-mongs-pa nyi-shu</em>)<a
                href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>. К ним
            относятся:</p>

        <ul class="list-none list-outside ml-2 space-y-1 pl-2">
            <li>1) <em>зло</em> (тиб. <em>khro-ba</em>) [гнев (санскр. <em>krodha</em>)],</li>
            <li>2) <em>страх</em> (тиб. <em>khon-du-'dzin-pa</em>) [злопамятность (санскр. <em>upanāha</em>)],
            </li>
            <li>3) <em>скрытность</em> (тиб. <em>'chab-pa</em>, санскр. <em>mrakṣa</em>),</li>
            <li>4) <em>пламенеть душой</em> (тиб. <em>'tshig-pa</em>) [вредоносность (санскр.
                <em>pradāśa</em>)],
            </li>
            <li>5) <em>зависть</em> (тиб. <em>phrag-dog</em>, санскр. <em>īrṣyā</em>),</li>
            <li>6) <em>жадность</em> (тиб. <em>ser-sna</em>) [скупость (санскр.
                <em>mātsarya</em>)],
            </li>
            <li>7) <em>мираж, ложное видение</em> (тиб. <em>sgyu</em>) [обман (санскр.
                <em>māyā</em>)],
            </li>
            <li>8) <em>ложь</em> (тиб. <em>g.yo</em>) [лицемерие (санскр. <em>śāṭhya</em>)],
            </li>
            <li>9) <em>горделивость</em> (тиб. <em>rgyags-pa</em>) [гордыня (санскр.
                <em>mada</em>)],
            </li>
            <li>10) <em>наведение страха</em> (тиб. <em>rnam-par 'tshe-ba</em>) [вред/насилие
                (санскр. <em>vihiṃsā</em>)],
            </li>
            <li>11) <em>бесстыдство</em> (тиб. <em>ngo-tsha-med-pa</em>, санскр.
                <em>āhrīkya</em>),
            </li>
            <li>12) <em>нескромность</em> (тиб. <em>khrel-med-pa</em>) [беззастенчивость
                (санскр.
                <em>anapatrāpya</em>)],
            </li>
            <li>13) <em>язвительность, терзание других</em> (тиб. <em>rmugs-pa</em>) [вялость
                (санскр. <em>styāna</em>)],
            </li>
            <li>14) <em>утомляемость</em> (тиб. <em>rgod-pa</em>) [возбужденность (санскр. <em>auddhatya</em>)],
            </li>
            <li>15)<em>неверие</em> (тиб. <em>ma-dad-pa</em>, санскр. <em>āśraddhya</em>),</li>
            <li>16) <em>невыдержанность, аморальность</em> (тиб. <em>le-lo</em>) [лень (санскр.
                <em>kausīdya</em>)],
            </li>
            <li>17) <em>невнимательность, непредупредительность</em> (тиб.
                <em>bag-med-pa</em>) [небрежность (санскр. <em>pramāda</em>)],
            </li>
            <li>18) <em>забывчивость</em> (тиб. <em>brjad-nges-pa</em>, санскр.
                <em>muṣitasmṛtitā</em>),
            </li>
            <li>19) <em>непонятливость</em> (тиб. <em>shes-bzhin ma-yin-pa</em>) [неосознанность
                (санскр. <em>asaṃprajanya</em>)],
            </li>
            <li>20) <em>приходить в смятение</em> (тиб. <em>rnam-par-g.yeng-ba</em>)
                [рассеянность (санскр. <em>vikṣepa</em>)]<a
                    href="#citation-5" id="ref-5" class="citation-mark"><sup>5</sup></a>.
            </li>
        </ul>

