        <p>Васубандху утверждает, что полная реализация каждого элемента как психического
            процесса происходит в случае полного совпадения причины (тиб. <em>rgyu</em>) и
            условия
            (тиб. <em>rkyen</em>). Если присутствует причина процесса, но не будет при этом
            условия
            возбуждения, то не будет психического процесса <em>раскаяния</em>, или <em>угрызения
                совести</em> и т.п., и, наоборот, хотя есть условие для возбуждения, но не будет
            причины, то не произойдет самого процесса. Поэтому для прекращения такого
            нежелательного процесса, как сон при самадхи (сосредоточении), индивид усилием воли
            может разрушить совпадение причины и условия, т.е. отнимает один из компонентов –
            причину или условие. Вайбхашики говорят, что для ликвидации «сна» нарушение
            совпадения компонентов наступает при напряженном сосредоточении.</p>
        <p>Таким образом, основатели <em>школы виджнянавады</em>, или <em>йогачаров</em>, Асанга
            и Васубандху (IV в. н. э.) признали указанные 51 элемент психическими процессами
            (тиб. <em>sems-byung lnga-bcu-gcig</em>).</p>

