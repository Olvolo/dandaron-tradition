    <div class="prose text-justify max-w-4xl mx-auto leading-relaxed space-y-4">
        <p>С точки зрения буддийской философии, вся совокупность дхарм (элементов),
            рассматриваемая как иллюзорное видение, существует только в названиях, но, однако, в
            сознании она представляется чем-то цельным. В состав сознательного живого существа,
            или индивидуума, входят все разновидности элементов. По этой причине буддисты
            считают, что теория дхарм-элементов является анализом самого индивидуума, человека,
            ибо только в нем, а не в неодушевленных предметах объективного типа содержатся все
            элементы, т.е. чувственное, сознание и процессы. Однако по этой теории индивид
            анализируется целиком, т.е. не только материальное его тело, <em>рупа</em> и
            психическая жизнь, но и все объективное, что он переживает, т.е. весь внешний
            материальный мир. Эта теория постепенно перерастает целиком в теорию познания
            человеком самого себя в период анализа совершенствующих путей, где выявляются
            элементы морального осквернения (<em>клеши</em>) из общего состава всех психических
            элементов индивида, и они сознательно подавляются. В этом отношении философия
            Сократа подходит во многом близко к буддийской. Сократ больше других греческих
            мыслителей пренебрегал вопросами, связанными с космогонией и метафизикой, ибо он не
            видел в них пути для познания человеком самого себя.</p>
        <p>Психические процессы, по учению буддистов, это те единичные и мгновенные явления или
            процессы эмоциональных переживаний, различения, чувства, воспоминания, желания и
            т.д., которые могут быть усмотрены в моментах сознательной жизни. Эти психические
            процессы близко связаны с сознанием, однако рассматриваются отдельно от него, как
            соотносительность сознания, но не как функция его. Ранняя буддийская школа
            вайбхашиков насчитывала 46 разновидностей психических элементов, а йогачары признают
            51 элемент.</p>
        <p>Познавательный процесс сознания является действием психических элементов в тесной
            связи с элементами сознания. Даже некоторые представители школы вайбхашиков
            утверждали, что различные познавательные факторы были более тесно связаны с
            сознанием, чем атомы материи друг с другом. Этот фактор получил название <strong>сампраюкта</strong>
            (тиб. <em>mtshungs-ldan</em>), т. е. <em>последствие</em>, или <em>оболочка
                сознания</em>
            при сопутствии умственных элементов <em>чайтта</em>. Не менее важным фактором в
            процессе познания является элемент <strong>самскара</strong> (тиб. <em>'du-byed</em>).
            Термин <em>самскара</em> можно объяснить так: например, чувственные элементы (<em>рупа-дхармы</em>
            – тиб. <em>chos-kyi gzugs</em>) в независимом своем существовании являются ничем не
            связанными элементами. Для того чтобы произошло движение, вихрь дхарм, который
            создает сансару, необходимы еще элементы-двигатели, или <em>самскары</em>, которые
            направили бы сознание на внешние объекты и этим самым заставили бы элементы вступить
            в мгновенную комбинацию между собою и, таким образом, заставили бы дхармы-носители
            всплывать на момент бытия и исчезать.</p>
        <p>Одним словом, необходимы <em>элементы-двигатели</em>, которые поддерживают движение,
            процесс волнения дхарм, эти <em>элементы-двигатели</em> объединяются в одну группу
            под названием <em>самскара</em>. Интенсивная связь умственных факторов через
            элементы-двигатели с сознанием составляют психический процесс индивидуума.</p>
        <p>Эта связь – <strong>сампраюкта самскара</strong> (тиб. <em>mtshungs-ldan
                'du-byed</em>)
            – проявляется через пять групп элементов: 1) <em>ведана</em> – чувство (тиб. <em>tshor-ba</em>),
            2) <em>санджня</em> – различающее сознание (тиб. <em>'du-shes</em>), 3)
            <em>четана</em>
            – активность сознания (тиб. <em>sems-pa</em>), 4) <em>намаскара</em> – сознание
            идейного (тиб. <em>yid-la byed-pa</em>) и 5) <em>спарша</em> – соприкосновение (тиб.
            <em>reg-pa</em>).
            Они выступают как вездесущие элементы (тиб. <em>kun-'gro</em>).</p>

