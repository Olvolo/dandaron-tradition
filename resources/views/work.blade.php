        <p>В этом мыслительном процессе происходит не только интенсивная связь элементов с
            сознанием, но и превращение одних элементов в другие, по качеству высшие. Происходит
            диалектический переход количества в качество.</p>
        <p>Если первый, или общий, момент процесса мышления относится к процессу сознания
            обычного человека в объективном мире, к обычному познавательному процессу
            индивидуума, то второй процесс мышления относится к сознанию человека, находящегося
            на пути махаянистического спасения. И все пять психических элементов, входящих в
            этот процесс, будут рассматриваться с точки зрения махаянистического и
            хинаянистического путей.</p>
        <ol class="list-decimal list-outside ml-2 space-y-1 pl-2">
            <li class="my-2"><em>Чанда</em>, желание, – это активный элемент, который толкает
                сознание на путь к совершенствованию. Этот элемент обычно возникает и появляется
                в тот момент, когда индивид уверовал в <em>Четыре Благородные Истины</em>.
            </li>
            <li class="my-2"><em>Адхимокша</em> – внимание, которое постоянно сопровождает
                <em>чанду</em>
                во всех ее проявлениях, она (<em>адхимокша</em>) дисциплинирует и настораживает
                сознание.
            </li>
            <li class="my-2"><em>Смрити</em>, память, – элемент накопления или концентрации,
                который действует только во времени (прошедшем, настоящем и будущем).
            </li>
            <li class="my-2"><em>Самадхи</em> – сосредоточение ума. Этот элемент нарастает от
                простого внимания до полного сосредоточения ума на объекте мышления, и этим
                самым ум приходит к ясному пониманию данного объекта. Это <em>первый этап</em>,
                который называется <strong>сампраджнята-самадхи</strong>. На <em>втором
                    этапе</em> наступает момент, когда наблюдается прекращение всех умственных
                модификаций, т.е. полное отсутствие познания, в том числе и знания объекта
                размышления. Здесь происходит «переход от количества познавательного процесса в
                другое качество», <em>качество отсутствия познания</em>, что выражается потерей
                объекта мышления; этот процесс называется <strong>асампраджнята-самадхи</strong>.
            </li>
            <li class="my-2"><em>Праджня</em> – интуиция, мгновенно постигающая запредельную
                истину, т.е. природу шуньи и абсолюта – нирвану. В момент постижения <em>асампраджнята-самадхи</em>
                индивид теряет в объекте мышления его форму и различия. Вместо созерцаемого
                объекта появляется «неопределенность», «бездна», нечто совершенно
                противоположное всему феноменальному, даже нельзя сказать, что «вместо
                созерцаемого объекта появляется что-то бесформенное», и т.д. Но, утверждая, что
                «там теряется объект мышления», виджнянавадины имеют в виду потерю всех
                феноменальных качеств объекта. Таким образом, теряя феноменальную природу
                объекта, индивид раскрывает ноуменальную реальность мира, т.е. природу шуньи.
                Процесс раскрытия ноуменальной реальности совершается интуицией –
                <em>праджней</em> (тиб. <em>shes-rab</em>). Сама же <em>праджня</em>
                раскрывается на
                махаянистическом пути просветления (тиб. <em>mthong-lam</em>). Четвертый
                элемент –
                <em>самадхи</em> способствует раскрытию пятого элемента. Поэтому в шести
                парамитах махаянистического пути элемент <em>самадхи</em> всегда непосредственно
                предшествует <em>праджне</em>.
            </li>
        </ol>

        <p><strong>Третий момент психического процесса</strong>. Элементов, вызывающих
            добродеяния, насчитывается <em>одиннадцать</em> (тиб. <em>dge-ba bcu-gcig</em>). Эти
            элементы тоже находятся в интенсивной связи с сознанием:</p>

        <ul class="list-none list-outside ml-2 space-y-1 pl-2">
            <li>1) <em>вера</em> (тиб. <em>dad-pa</em>),</li>
            <li>2) <em>чувство стыда, совесть</em> (тиб. <em>ngo-tsha shes-pa</em>),</li>
            <li>3) <em>скромность</em> (тиб. <em>khrel-yod-pa</em>),</li>
            <li>4) <em>бесстрастность</em> (тиб. <em>'chags-med-pa</em>),</li>
