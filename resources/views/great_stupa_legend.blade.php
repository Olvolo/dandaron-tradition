        <h3 class="text-3xl font-bold text-center my-8">ГЛАВА ВТОРАЯ<br>ИСПОЛНЕНИЕ МОЛИТВЫ В
            СООТВЕТСТВИИ С ВИДЕНИЕМ БОДХИСАТТВЫ</h3>

        <p>И снова Тисрондецан заговорил с Лотосорожденным Гуру:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            О Великий Гуру! Молю Тебя, расскажи о молитвах и устремлениях, возникших возле этой
            чудесной Ступы Джарунхашор и об исполнении этих молитв.
        </blockquote>

        <p>Великий Гуру сказал:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            <p>
                Слушай меня, о Великий Царь! Когда при освящении Ступы были разбросаны цветы,
                свершены
                чудеса и восхитительные зрелища, огромное собрание будд и бодхисаттв обратилось к
                благотворителям и верующим в один голос:
            </p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Слушайте, о прекраснорожденные! Благодаря чистому уму, создавшему стремление
                построить Великую Ступу, высшее вместилище Тела Бесконечной Чистоты (Дхармакаи),
                которое не отделимо от Ума всех Победоносных прошлого, настоящего и будущего,
                благодаря этому добродеянию свершится любая ваша молитва.
            </blockquote>
            <p>Тогда сыновья умершей птичницы сказали друг другу:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Причиной постройки этой Ступы был обет нашей старой матери, однако мы, согласно ее
                воле, закончили постройку Ступы, освятили ее и удостоились видеть всех будд и
                бодхисаттв. Итак, накопив благодеяния, мы можем просить об исполнении любой молитвы;
                неразумно просить о каком-нибудь пустяке.
            </blockquote>
            <p>Старший сын, сын конюха, стал думать, какой молитвой взмолиться:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                На севере, в скованной льдами варварском царстве горы покрыты снегом, долины
                затоплены;
                летом это излюбленное место водяных птиц, а зимой – один огромный ледник. Было
                сказано,
                что воды спадут и явится бодхисаттва, Владыка Мироздания – Авалокитешвара, чтобы
                преподать учение Татхагаты Шакьямуни, и тогда учение будд распространится по всей
                стране. Я молюсь о том, чтобы я утвердил Учение будд в Тибете.
            </blockquote>
            <p>Итак, согласно этим размышлениям, он взмолился:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                С чистым сердцем и сильным стремлением да обрету я благодаря заслугам при завершении
                Ступы рождение в виде Великого Царя и Защитника Религии в скованной льдами
                пограничной стране дикарей на севере. Во время калиюги, в период разрушения и
                морального разложения, в период распространения Учения Татхагаты Шакьямуни, сила
                пяти ядов – ненависти, похоти, ленности, зависти и гордости – возрастает, поэтому
                да установлю я Учение будд в этой стране на все времена.
            </blockquote>
            <p>Затем сын свинопаса обратился со своей молитвой:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
