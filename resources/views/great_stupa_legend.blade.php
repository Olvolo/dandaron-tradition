        <h3 class="text-3xl font-bold text-center my-8">ГЛАВА ПЯТАЯ<br>РЕСТАВРАТОРЫ И ИХ ДЕЯНИЯ</h3>

        <p>Царь Тисрондецан еще раз обратился к Лотосорожденному Гуру:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            О Великий Гуру! Где переродится Пема Кунцен, Ринпоче из Гой, во время калиюги –
            периода разрушения и морального разложения, когда поврежденная Ступа должна быть
            реставрирована, как ты предсказал? Какое у него будет имя? Как мы узнаем его?
            Сколько друзей и учеников будет окружать его? Какую удовлетворенность и способность
            даст восстановление Великой Ступы этим людям?
        </blockquote>
        <p>Великий Гуру ответил:</p>
        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            <p>
                Слушай внимательно, о Великий Царь! Твои вопросы уместны. В конце эры упадка, когда
                продолжительность жизни человека сократится до пятидесяти лет, появится сто одно
                неблагоприятное предзнаменование. Когда мрак достигнет владений Пемы Кунцена,
                Ринпоче из Гой, он, благодаря мне и своей решимости достичь <em>состояния будды</em>,
                родится в семье тантрийского йогина, сведущего в искусстве магического
                перевоплощения, в год Свиньи в горах провинции Тсанг и назовут его Пелланом Зангпо.
                Он будет обладать большой умственной силой и ясным представлением о том, какой
                должна быть его жизнь. В результате благоразумия и ясности восприятия он будет
                рождаться в течение нескольких жизней с уверенностью, что Тройная Драгоценность не
                отличима от Вечного Блаженства. Он вполне осознает, что <em>деятельность Храма</em>
                – <em>это единство святости формы, звука и мысли</em>.
            </p>
            <p>
                Принадлежа к семье махаяны, он будет обладать состраданием к живым существам; его
                щедрость в пожертвовании, разговоре и мышлении не будет иметь предела. Он овладеет
                стремительностью Действий Будды и свирепостью гневных охранителей. Он завершит
                практики визуализации, чтения мантр и реализует совершенство внутренней природы;
                Защитники Сокровенного Святилища покорятся и будут служить ему. Наконец, он родится
                полностью посвятивший себя Уржану Ринпоче.
            </p>
            <p>
                Если клятва этого Махасаттвы останется нерушимой и нерасторжимой, то когда ему будет
                двадцать пять лет, Ступа будет реставрирована. Если работа будет отложена, но
                начнется до того, как ему будет тридцать три года, то она будет завершена. Но если
                работа будет отложена на большой срок, то над Тибетом и Китаем, подобно урагану,
                разразится война. И бесчеловечность этого бедствия утроит тяжесть голода и болезней
                и ввергнет все живые существа в ужасающий ад. С северо-восточной высокогорной
                границы с Китаем армия, чья численность будет равна пяти мерам белого горчичного
                зерна, вторгнется в Тибет, и вопли будут звучать, подобно грому, многие годы. Через
                южные низменности армия, чья численность будет равна девяти мерам белого горчичного
                зерна, вторгнется в Тибет, и голос страха будет звучать, как звук летящего дракона.
                Армии будут вторгаться из долины в долину, как испепеляющая молния, и затем заполнят
                и завоюют Западный Тибет. Войска с плоскогорий наводнят Китай, грабя и побеждая.
                Половина населения Тибета будет убита, храмы разрушены, иконы поруганы, священные
                писания брошены в пыль, а монахи и священники убиты. Разрушенные деревни станут
                безлюдными, ибо оставшиеся в живых после этого разгрома убегут в Сикким, Бутан,
                Непал и в тайные долины Гималаев. Те же, кто останутся, привязанные алчностью к
                земле и богатствам, погибнут со своим скотом от руки варваров-иконоборцев. Желая
                освободить от этого страдания, Махасаттва, наделенный могуществом блаженства,
                состраданием ко всем живым существам, свободный от собственных интересов, вновь
                обретет силу через божественный гнев и поддержит как беженцев, так и жителей
                приграничных стран.
            </p>
            <p>
                Когда сила добродетелей беженцев объединится, то друзья Махасаттвы начнут совершать
                объединенными усилиями восстановление Великой Ступы. Но мрачные силы обманом
                завлекут людей в грех, и лишь немногие сохранят веру и преданность Махасаттве – их
                будет так мало, как мало звезд в дневное время. Тем не менее вокруг него соберутся
                сто тридцать тысяч приверженцев добродетельных действий, тысяча шесть мастеров
                глубокой молитвы, восемьдесят восемь учеников йоги, двадцать три провидца, восемь
                воплощенных наставников, знающих секреты восьми бодхисаттв, двадцать пять преданных
                духовных сыновей, пять воплощений дакинь, семь, получивших благословение от дакинь и
                двадцать пять девиц высокого происхождения. Он уничтожит омрачающие их умы путы и
                устранит все препятствия на пути. Затем, встретив моего духовного сына, воплощение
                моего безграничного осознания, настоятеля Шантиракшиту, мастера дисциплины, и тебя
                самого, Тисрондецан-Дхармараджа, владыку телесных потребностей, и, получив их
                прикосновение блаженства, дающее силу, он полностью реставрирует Великую Ступу.
            </p>
            <p>
                Если Великая Ступа будет восстановлена согласно этому предсказанию, то жители всего
                южного континента Джамбудвипы в течение двадцати пяти лет пожнут обильные урожаи, а
                в течение тридцати трех лет – жители Индии, Непала, Сиккима. Земля Снегов в течение
                шестидесяти лет не будет подвергаться набегам варварских орд, и сорок лет будут
                обильные урожаи, люди будут счастливы. Воплотятся все чаяния Следующих по Пути.
                Проповедь Дхармы в храмах и монастырях Индии и Тибета, в Ваджрасане и Самьелинге не
                остановится. Будут уничтожены все препятствия объяснениям и наставлениям, и Дхарма
                будет распространяться. Махасаттвы – Учителя прямых традиций укрепят свои
                родословные и распространят примеры служения Будде. Признаки совершенства в
                завершении практик визуализации и чтения мантр мантрадхарами будут появляться без
                задержки. Содействующие боги и демоны Тибета, царь Тисрондецан, его семья,
                наследники и вожди страны будут верны духовным и мирским обязательствам, давая
                многим силу и удовлетворение. Духи порока и мстительности – причины разрушения
                Тибета и черные от эгоистических устремлений существа будут побеждены.
            </p>
            <p>
                Все живые существа, занимающиеся реставрацией Великой Ступы, в будущем родятся с
                телом мужчины или бога, станут чистым сосудом для нектара Дхармы и в конечном итоге
                достигнут <em>состояния будды</em> в западной области Сукхавати – Чистой Земле
                Блаженства. Те, кто поверил в Великую Ступу, кто нашел радость в природе Великой
                Ступы, кто высказал преданность Великой Ступе и кто вместе с бодхисаттвами радуется
                восстановлению Великой Ступы, после семи возрождений воссядут во главе Видьядхар в
                Бесконечном Дворце Лотосового Света на Славной цвета меди горе Нгаяб на юго-западе.
                Каждое живое существо, которое видело своими глазами восстановление Великой Ступы
                или слышало вибрацию шума восстановления своими ушами, или представляло все это в
                своем уме и чувствовало своим телом, получит избавление от всех следов бездумных
                действий, (кармические) следы шестидесяти тысяч эр будут уничтожены в его сознании.
                Все люди, восстанавливавшие вместе с Махасаттвой Великую Ступу, сознательно или
                бессознательно, с доверием, преданностью или без них достигнут высочайшей реализации
                и духовной силы и к концу своего человеческого существования обретут состояние
                Видьядхары у ног Уржана Ринпоче.
            </p>
            <p>
                Итак, существуют три уровня реализации в связи с Великой Ступой:
            </p>
            <ul class="list-disc pl-2 space-y-1 ml-2">
                <li>первое – вознаграждение от пожертвований и молитв, о чем говорилось в третьей
                    части;
                </li>
                <li>второе – вознаграждение за добродетели, пробуждающее неистощимый голос
                    интуиции, и восприятие наставлений будд и бодхисаттв прошлого, настоящего и
                    будущего;
                </li>
                <li>и третье – обретение достоинства (Дхармакаи) – Тела Бесконечной Чистоты, что
                    превыше любого уровня описания.
                </li>
            </ul>
        </blockquote>

        <p>Когда Гуру Ринпоче, настоятель из Уржана, Гуру, рожденный на Лотосе, закончил свою
            речь, то ученики (посвященные), охваченные изумлением, получили волну блаженства и
            силы для преодоления периода калиюги. Затем они возблагодарили и восхвалили Гуру за
            его провидение.</p>

