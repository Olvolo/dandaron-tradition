        <p>Иконографически он представлен с тысячью рук, и с каждой ладони взирает полный слез Глаз
            Мудрости. Он непрерывно действует, освобождая живые существа от мешающих мыслей и
            эмоциональных загрязнений. Он трудится, чтобы разрушить безжизненные образы и оживить
            скрытые способности. В этой легенде Шамвара, великая йогиня, является воплощением
            бодхисаттвы Авалокитешвары, а строительство Великой Ступы – это выполнение обета
            бодхисаттвы.</p>

        <p>Ее деяние было причиной того, что свет Великой Ступы рассеял тьму калиюги.</p>

        <h4 class="text-2xl font-semibold text-center my-6">ОПЫТ ВРЕМЕНИ</h4>
        <p>"Легенду о Великой Ступе" следует читать с пониманием ее места во времени. Просветленный
            взгляд на время одновременно историчен и метафоричен. Время делится на последовательные
            эпохи <em>(кальпы)</em>. Эпоха, в которую мы живем, называется Бхадракальпа,
            <em>Благоприятная Эпоха</em>, потому что тысяча будд воплотятся в этот период,
            чтобы освободить тех, кто замкнут в чувственном мире.</p>

        <p>Каждая эпоха делится на три эры <em>(юги)</em>. Первая эра, <em>сатьяюга</em>,
            характеризуется полной чистотой, в которой без ограничений проявляется совершенство
            <em>состояния будды</em>. Вторая эра, <em>дхармаюга</em>, отличается тем, что
            человеческая жизнь длится 8 тысяч лет. В начале третьей эры, <em>калиюги</em>,
            продолжительность жизни человека – сто лет. Говорят, что в течение калиюги она
            сократится до 10 лет.</p>

        <p>Во время дхармаюги будды проповедуют, но задача становится все труднее и труднее, а в
            калиюге чистого учения вообще не услышишь. Великая Ступа была построена во время Будды
            Махакашьяпы, когда жизнь человеческая длилась 20 тысяч лет и все могли слушать чистое
            учение. Будда Гаутама Шакьямуни, повелитель рода Шакьев, родился 2500 лет назад, когда
            продолжительность жизни людей была 100 лет. Он проповедовал учение, которое должно
            охватить начальные этапы калиюги.</p>

        <p>Калиюга – это время, предшествующее разрушению мира. У нее пять характерных черт:</p>

        <ul class="list-disc pl-2 space-y-1 ml-2">
            <li>уменьшение продолжительности жизни,</li>
            <li>порочность и эгоистичность жизни,</li>
            <li>бесконтрольное вожделение и жадность,</li>
            <li>материалистические философские взгляды и</li>
            <li>вера в неизбежный мировой пожар.</li>
        </ul>

        <p>Легенда предсказывает, что вибрации этих времен нарушат строение Великой Ступы; текст
            ясно описывает предзнаменования, которые возвестят упадок и разрушение высшей жизни. К
            концу <em>юги</em> безумное устремление к конечному разрушению уничтожит следы учения
            Будды Шакьямуни. Легенда предсказывает, что до тех пор пока не прекратится проповедь и
            практика учения тантры, осуществляемая воплощенными формами
            бодхисаттв, чьи добродетели, накопленные в предыдущих существованиях, дают им неодолимое
            мужество в подавлении возбуждающихся и воспламеняющихся животных чувств, внешняя форма
            Великой Ступы не будет разрушена окончательно и бесповоротно.</p>

        <p>Уменьшение срока жизни можно понять в двух аспектах. Во-первых, это можно понять на
            физическом уровне, где тело рождается, вырастает, дряхлеет, а затем умирает и
            разлагается на элементы, и, во-вторых, на духовном уровне, где существование описывается
            как непрерывный поток, изменяющийся в каждый момент жизни.</p>

        <p>Духовная жизнь (индивидуального) бытия, именуемая в буддийской терминологии
            <em>дхармой</em>, – это часть большого потока, который есть (всеобщая)
            Дхарма. Длительность каждой (индивидуальной) дхармы меняется в соответствии со степенью
            концентрации познающего. В калиюге ядовитые страсти, особенно вожделение, жадность,
            зависть, гордость, ведут к потере концентрации. В результате скорость жизни возрастает,
            а длительность уменьшается.</p>

        <p>На ментальном уровне эта эпоха представляет временной узел внутри сферы духовности. Эта
            эпоха может быть пережита в глубокой медитации, свободной от тяжелых материализующих сил
            энергии. Данная эпоха переживается как возрастающая скорость вибраций. По мере того как
            калиюга близится к своему завершению, скорость вибраций, то есть переживаемые единицы
            ментального времени становятся бесконечно малыми, пока наконец возрастающая скорость
            деления частиц времени не приведет к конечной аннигиляции всех форм в мировом
            пожаре.</p>

        <p>Сказано, что в этот момент родится Будда Майтрейя, воплощение сострадательной доброты, а
            продолжительность человеческой жизни снова достигнет 84-х тысяч лет.</p>

        <h4 class="text-2xl font-semibold text-center my-6">ЖИВОЙ БУДДА</h4>
        <p>В калиюге есть возможность освободиться от приближающегося разрушения. Хотя дисциплина
            хинаяны и махаяны сурова, но все же недостаточна, чтобы бороться с возрастающими силами
            страстей.</p>

        <p>Ваджраяна же дает средства преодолеть импульсивную природу человека. Если учение хинаяны
            предписывает полный отказ и отречение от страстей, а махаяна настаивает на противоядии и
            нейтрализации страстей, то ваджраяна – это постепенное овладение энергией этих страстей,
            трансформация их в Мудрость Будды.</p>

        <p>В легенде предсказан приход <em>тулку</em> (Нирманакаи). В ваджраяне
            <strong>тулку</strong>, или <em>просветленный человек последних дней калиюги</em>,
            будет известен как тантрист – йогин, овладевший методом постижения пустотности, присущей
            каждому существованию. Он будет мастером нанизывания любого случайного опыта на нить
            чистого сознания. С таким пониманием он сможет овладеть искусством волшебного
            превращения, способностью изменять свои вибрации, чтобы умиротворять, наставлять,
            подавлять и проявлять власть над темными силами, обусловливающими человеческий ум. Его
            <em>состояние будды</em> будет идентично природе всех будд. Он будет
            подобен просветленному человеку дхармаюги, а его способность различать будет столь же
            острой, как у отшельников пратьекабудд хинаянской традиции. Он будет бесстрашен в показе
            путей, которые драгоценное человеческое тело может использовать для создания жизни,
            света и любви.</p>

        <h4 class="text-2xl font-semibold text-center my-6">МЕТОДЫ СТРОИТЕЛЬСТВА</h4>
        <p>В ваджраяне представлены многочисленные средства "строительства" Ступы. При практике
            физической йоги, хатха-йоги (асан и пранаям) тело становится чувствительным, рефлексы –
            быстрыми, а восприятие характеризуется чрезвычайной ясностью и четкостью. Практика
            мантры – повторение серии особых звуков – усиливает осознание опасностей от мар.
            Практики шаматхи и випашьяны способствуют тончайшему распознаванию всего, что возникает
            в сознании. Все эти практики эффективны в построении Великой Ступы внутри самого
            ищущего.</p>

        <p>Никто не возьмется предсказать, что мирские желания людей будут удовлетворены. Желание же
            просветления неумолимо прорастет из семени, превращаясь в ствол и ветви сильного
            устремления, принося результаты и плоды. Таков всеобщий процесс духовного развития.
            Когда мысли, подобно скалящим зубы акулам, появляются в океане ума, глубокий разлад и
            конфликт препятствуют опознанию и пониманию любой из них. Если в спокойствии и
            недвижности океана ума плавает единственная рыба, весь объем занят ею и образ ее уже
            нематериален, независимо от ее конкретных свойств.</p>

        <h4 class="text-2xl font-semibold text-center my-6">ПРЕДСКАЗАНИЕ БЕДСТВИЙ</h4>
        <p>В Самьелинге, в первом монастыре, построенном в Тибете, Гуру Падмасамбхава
            (Лотосорожденный Гуру, Уржан Ринпоче), нищенствующий тантрист VIII в., обладавший
            великой силой магических превращений, учил царя Тисрондецана и двадцать пять ближайших
            учеников. Все традиции старой школы <em>(ньингма)</em> восходят к
            ним. Поэтому в первых двух главах кратко излагается ранняя история Тибета, а в следующих
            трех главах содержится предсказание позднейшей истории. Упомянуто монгольское вторжение,
            предсказаны приход мусульман в Индию и разрушение Ваджрасаны (ныне восстановленной в
            Бодхгайе), имеется много намеков на политические изменения в соседних странах.</p>

        <p>Отметим, что во время бури 1969 года было разрушено молнией навершие Ступы и что сын
            настоятеля монастыря при Ступе был арестован за торговлю ритуальными предметами,
            украденными в храмах долины Катманду. Тибетцы, интерпретирующие пророческие главы хорошо
            знакомого им и популярного текста, усматривают в этих событиях указания на глубину
            падения человека калиюги и видят их современное завершение в предсказании китайской
            агрессии против Тибета и Индии. Действительно, пророчество, предсказывающее опустошение
            Тибета, если деградация калиюги не будет остановлена воплощением Тантриста, уже
            исполнилось, и толпы беженцев, как и предсказывает текст, потоком устремились в северную
            Индию. Монастыри сожжены, а священные книги уничтожены. Землевладельцы, что были не в
            силах расстаться со своей собственностью, и монахи, оставшиеся в своих монастырях,
            убиты. Вера беженцев, страдающих от унижений в чужой стране, поддерживается обещанием,
            что если Великая Ступа будет восстановлена, они вернутся на родину, где на 60 лет
            воцарятся мир и процветание.</p>

        <h4 class="text-2xl font-semibold text-center my-6">СОКРОВИЩЕ БОДХНАТХА</h4>
        <p>Реальная Ступа в Бодхнатхе, называемая тибетцами Джарунхашор, стоит в центре долины
            Катманду и окружена горами, как драгоценная точка в центре природного мандала. Она –
            фокус всех вибраций в долине. Пилигримы идут с северо-восточной границы и от Гиндукуша
            на западе, чтобы исполнить ритуальные обходы вокруг Великой Ступы. Ступа стоит здесь с
            незапамятных времен, и каждый паломник – нищий, монах, ученый лама или торговец,
            пробирающийся из Индии в Тибет через Непал, молится здесь о благополучном переходе через
            Гималаи и кишащие разбойниками долины.</p>

        <p>Четыре школы тибетского буддизма относятся к Великой Ступе с равным почтением. Монастырь,
            что стоит теперь у ступы Бодхнатх, принадлежит желтошапочной школе – <em>гелуг</em>, но
            этот текст, который используется всеми почитателями Ступы в странах, где говорят
            по-тибетски, принадлежит красношапочной школе – <em>ньингма</em>. Это – <em>терма</em>,
            книга, скрытая супругой Падмасамбхавы, дакиней Ешей Мцожал, после того, как слова Гуру
            были зашифрованы языком <em>терма</em>. За время жизни Гуру тысячи терма
            были сокрыты в тайных местах: пещерах, скалах, реках и ручьях, храмах, статуях и ступах
            – чтобы их обнаружили, расшифровали и перевели, когда люди иных времен, но особенно в
            калиюге, будут нуждаться в разных видах наставлений для излечения от духовных недугов.
            Таким образом, не только нужные наставления обрели возможность стать доступными для всех
            времен, но и древние, вечно действенные традиции могут быть обновлены словами Гуру,
            результатами его совершенной практики. Где созерцают йоги, где призываются божества, где
            почва подготовлена просветленным духом, там могут быть найдены терма.</p>

        <p>Эта терма была обнаружена монахиней в изображении Махавайрочаны в верхней палате храма
            Самьелинг. Махавайрочана – бесконечное рассеяние синего света полного осознания, верхняя
            палата – центр мандала; все вместе: единство и бесформенность.</p>

        <p>Храмовый комплекс Самьелинга – это обширный мандал, по своему назначению и функциям
            повторяющий изображения в красках, надписях и рисунках. Терма была обнаружена с
