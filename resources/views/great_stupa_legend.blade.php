        <h3 class="text-2xl font-semibold text-center my-6">Предисловие к русскому переводу</h3>

        <p>Ступа (тиб. <em>mchod rten</em>, монг. <em>субурган</em>) как культовый
            памятник возникла в Индии до Будды Шакьямуни. Функционально — это результат исторической
            трансформации главного объекта ритуала сожжения тела умершего, то есть совокупности
            элементов погребального костра. Простейший вид подобного — это постамент или небольшое
            возвышение над землей, над ним глиняное, кирпичное или металлическое ограждение, подобие
            некоей огнеупорной емкости с телом, обложенным горючим материалом. В момент ритуала над
            подобным сооружением вздымаются языки пламени и клубы дыма. Именно в таком виде
            изображается доныне эпизод кремации Будды Шакьямуни в серии канонических изображений из
            известного цикла "двенадцати деяний Будды".</p>

        <p>Такая конструкция есть своеобразная "предступа", состоящая из минимального количества
            элементов:</p>

        <ul class="list-none pl-2 space-y-1 ml-2">
            <li>— постамент (земля),</li>
            <li>— некая условная емкость, или сосуд, с трупом, структурность которого (вода) должна
                исчезнуть,
            </li>
            <li>— разрушительное очистительное пламя (огонь).</li>
        </ul>

