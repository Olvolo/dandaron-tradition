        <h3 class="text-3xl font-bold text-center my-8">Введение к английскому изданию
            "Легенды…"</h3>

        <p>Тибетцы, что ныне беженцы в Индии, сохраняют живую традицию буддизма ваджраяны. Их
            мировоззрение уникально в эпоху, когда влияние западного рационализма проникает всюду и
            меняет жизнь людей. Священные тексты тибетцев представляют вселенную как поле действия
            духовных сил, которые могут быть направлены и на добро, и на зло, но должным образом
            обузданные могут гармонизировать бытие на благо всех. Эти тексты обращены как к рядовым
            тибетцам, так и к тем, кто следует суровой практике тантрических учений ваджраяны.</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            Та же умудренная опытом веков вера, что породила эти легенды, сегодня дает тибетскому
            народу силу жить в душевном мире и удовлетворении, которые почти не известны обществу в
            промышленно развитых странах.
        </blockquote>

        <p>Перевод "Легенды о Великой Ступе" был сделан с благословения Дуджома Ринпоче, духовного
            руководителя школы <em>ньингма</em>. Текст редактировался Джаттуром Ринпоче, настоятелем
            ньингмапинского монастыря в Гхуме, близ Дарджилинга, и был исправлен Нимой Норбу.</p>

        <p>Публикация стала возможной только под руководством и покровительством Тартанга Тулку
            Ринпоче, верховного Ламы Тибетского медитационного центра <em>ньингма</em> в Беркли
            (Калифорния) и благодаря усилиям его учеников.</p>

        <p>История Великой Ступы окружена тибетскими легендами о перерождениях, бедствиях, о
            воплощениях бодхисаттв и божеств.</p>

        <p>Таков тибетский способ наставлять идущего по духовному пути, начинающегося с первой
            вспышки прозрения и заканчивающегося достижением полной реализации. Этот метод
            содержится в переписке Гуру Падмасамбхавы и запечатлен в утонченных вибрациях храмового
            мандала Самьелинга, самого древнего из почитаемых храмов Тибета. Несколько столетий этот
            текст использовался в ритуале искоренения инерции умственных реакций искажения и
            ступора, вызывая исполненное веры сосредоточие на звуке и смысле. Эффективность этого
            ритуала, называемого <em>"Освобождение через слушание"</em>, следует испытать лично.</p>

        <p>Текст принадлежит к категории священных писаний, называемой <em>тойдол</em>
            (тиб. <em>Thos drol</em>), и означает, что посредством сосредоточения
            на звуке и понимания возникшие прозрения и интуиция разрушат узы, привязывающие ум к
            привычным стереотипам поведения и к наивным взглядам на природу. Следы скептицизма,
            оговорки или сомнения препятствуют достижению прозрения. Слушатель должен преисполниться
            веры в силу произнесенного слова и в безупречную интуицию Великого Гуру. Хотя интеллект
            может ошибиться, следуя многочисленным нитям повествования, и хотя ненаписанное часто
            говорит больше, чем написанное, открытый ум, способный интуитивно разрешать парадоксы,
            освобождается от узкой колеи двойственного суждения, порождая узор сложных чувств. В
            Учении написанное слово считается столь же священным, как и Будда, его произнесший, а
            каждое слово, написанное Учителем, несет на себе знак истины. Читатель, понимающий и
            практикующий дхарму, читает без критики, без разложения, интерполяции или модификации
            смысла. Он позволяет словам образовывать умственные конструкции, которые имел в виду
            автор, поддерживая этим веру в то, что они действенно помогают достижению желаемого
            результата. Критический интеллект усиливает эгоистический подход, а ритуальное
            многократное повторение текста нейтрализует его. Этот <em>тойдол</em>,
            пока он звучит, должен и наставлять, и просветлять.</p>

        <p>Для историка или хрониста в этой легенде мало интересного. Хотя исторические факты и
            вплетены в эту историю, легенда представляет типичный образец пренебрежения фактологией.
            В тибетской буддийской литературе история рассматривается как аналог продвижения по пути
            бодхисаттвы, физическая география – как параллель и символ структуры и топографии ума;
            детали биографии – как средства достижения <em>состояния будды</em>,
            а философия описывает различные видения, соответствующие разным уровням опыта. В то
            время как западная наука дала человеку возможность разрабатывать и манипулировать
            материальным окружением для решения вечных проблем пропитания, жилища, одежды, а также
            для удовлетворения своих страстей, культура Тибета, Страны Снегов, нацеливает человека
            на гармонизацию элементов его собственной личности, на переключение внутреннего
            сострадания, проистекающего из этой гармонии, на опыт внешнего бытия с простой
            чувственной заботливостью и спонтанным и непрерывным диалогом.</p>

        <h4 class="text-2xl font-semibold text-center my-6">РЕАЛЬНАЯ СТУПА</h4>
        <p>Предмет этой легенды – Великая Ступа, символ <em>состояния будды</em>.
            Ступа соединяет абсолютную и относительную реальность в едином образе. В своей
            абсолютной реальности Ступа неразрушима и неприступна. Она – чистое всепроникающее
            <em>Основание</em>, в котором все вещи имеют сущностное тождество. Она
            – царство тождественности, неделимой идентичности будд прошлого, настоящего и будущего.
            Она – Бесконечная и Вечная Чистота (Дхармакая). Пока бодхисаттва не разрушил ограничения
            своего восприятия и не справился с бесчисленными эмоциональными отклонениями,
            разрушающими чистое и интенсивное переживание, абсолютная реальность остается только
            потенциальностью <em>состояния будды</em> – врожденным совершенством,
            которое, как известно (хотя и смутно, и лишь на опыте), существует. Абсолютное
            совершенство не может быть реализовано одним лишь интеллектом. Обусловленное и
            дифференцированное не может познать подобное Единство, лишенное описуемых черт. Это
            Единство – немыслимая, непознаваемая и совершенная шуньята. Она чиста, безукоризненна и
            не поддается определению. Она называется также <em>дхармадхату</em>,
            или <em>Чистое Пространство (дхармовое пространство)</em>.</p>

        <p>В относительном аспекте Ступа пребывает как пространственно-временное проявление,
            подверженное рождению, созреванию, упадку и конечному разложению. В относительной
            природе вещей традиционно различаются чувственное, эстетическое и бесформенное царства
            (камадхату, рупадхату, арупадхату). В чувственном царстве бодхисаттва может выразить
            себя в спонтанном потоке действия. Его выбор действия стремится восстановить то, что
            деградировало до своей изначальности. Эстетическое царство (рупадхату) – это океан
            вибраций, турбулентность которых возрастает по мере удаления от его недвижных глубин.
            Чувствительный к малейшей дрожи, боли или удовольствию, эстетический импульс формулирует
            собственное средство против дисгармонии в облике божества, обладающего чувственной
            формой. Бесформенное царство (арупадхату) содержит варьирующиеся степени Пустоты
            (шуньяты), приближающие к абсолютной реальности. Вместе взятые, абсолютное и
            относительное, неделимое и нерушимое, образуют символическую природу Великой Ступы.</p>

        <h4 class="text-2xl font-semibold text-center my-6">ДОСТИЖЕНИЯ СОСТОЯНИЯ БУДДЫ</h4>
        <p>Птичнице Шамваре, что возжелала построить Великую Ступу, пришлось самой достичь
            совершенства. Путь, которым она следовала, начался с безусловной преданности Учителю
            Будде, а закончился, когда были искоренены последние остатки различий между деятелем и
            действием, между мыслью и ее выражением, между ожиданием и исполнением. Этот путь,
            ведущий от тревожности и напряжения к полному просветлению, дал ей тройственную награду
            в Трех Совершенствах <em>состояния будды</em>.</p>

        <p>Тройственное Совершенство – это <em>Тело Будды, Речь Будды</em> и
            <em>Мысль Будды</em>. Тело Будды означает достижение свободного
            действия в любой сфере. Это позволяет психике реагировать в непредвидимых
            обстоятельствах без колебаний, но с уверенностью и определенным контролем. Для
            достигшего <em>Тела Будды</em> реализуются возможности каждой
            ситуации, и поэтому его опыт есть наивысшее качество стиля жизни. Это достигается
            неэгоистическими действиями и благоговением: каждое действие становится
            жертвоприношением Великой Ступе – Сокровищу, исполняющему все желания, Исполнителю всех
            молений.</p>

        <p>Речь Будды – это реализация Внутреннего Голоса, Голоса Будды, Голоса Наставника,
            выражающегося в чистых вибрациях качества опыта и направляющего к совершенному действию
            в соответствии с неколебимым законом. Это достигается после устранения всех
            эгоистических мотивов из межличностного общения и разрушения любых эгоистических
            устремлений. Здесь Великая Ступа представляется божеством, в котором растворяются все
            другие божества, очищаясь светом и пламенем.</p>

        <p>Мысль Будды – это сознание, сводящее все восприятие к сущностной Пустоте, из чего и
            проистекает сострадание ко всем живым существам.</p>

        <p>Так Великая Ступа становится <em>Телом Бесконечной Чистоты (Дхармакаей)</em>.</p>

        <p>Каждый жаждущий вступить на путь махаяны, посвящает себя гуманному идеалу освобождения
            всех живых существ от уз эмоционального возбуждения и ограниченного видения. Средство
            достижения этого – самосовершенствование. Здесь духовный рост параллелен физическому
            строительству Великой Ступы.</p>

        <p>Авалокитешвара – это божественное сострадание, из которого проистекает завершение обета
            бодхисаттвы. Он – хранитель, защитник Тибета и обитает во дворце Потала, где Далай-лама
            правит как его регент и современное воплощение. Его имя означает
            <em>"Взирающий на страдания мира глазами, полными слез"</em>.
        </p>

        <div class="not-prose flex justify-center my-4">
            <figure class="rounded-lg shadow-md max-w-[600px] text-center">
                <img src="/images/articles/mani/11_face_Avalokita.webp"
