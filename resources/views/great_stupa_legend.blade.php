        <p>Сожжение тела умершего — его последнее жертвоприношение. Тибетское название ступы —
            <em>чортен</em> (тиб. <em>mchod rten</em>) буквально переводится как
            <em>опора жертвы;</em> углубленный смысл этой жертвы проступает в
            другом тибетском слове для обозначения ступы — <em>тугтен</em>
            (тиб. <em>thugs rten</em>) — <em>основа Мысли</em>, где Мысль пишется с большой буквы,
            ибо ритуал предназначен для возможного обретения умершим <em>состояния будды</em>
            с его тремя достоинствами — Телом, Речью и Мыслью, где Мысль Будды есть Дхармакая
            (тиб. <em>chos sku</em>). В реинтеграции элементов бытия до уровня Мудростей (к примеру:
            жгучий огонь — нежгучий огонь — красный цвет радуги — Мудрость Амитабхи; вот, кстати,
            намек на четыре стадии практики тхогей), при котором сознание снова совпадает на
            вневременное мгновение (мгновение не есть категория времени, ибо не есть длительность)
            с Дхармакаей, резонируя с так называемой метрикой дхарм или с
            <em>дхармовым пространством</em> (тиб. <em>chos dbyings</em>), и заложен легко
            обнаружимый внешний смысл ритуального сожжения трупа.</p>

        <p>Метафизика буддизма помогла подтянуть наглядность происходящего до очертаний, известных
            нам как архитектурный образ ступы <em>(дагоба, чортен, субурган)</em>.
            Сосуд с прахом помещается в среднюю часть ступы — реликварий — это главная часть
            памятника, предмет поклонения и памятования. Удлиненный шпиль над ним — символическое
            изображение вихря <em>"все грехи сжигающего пламени"</em>. Этот вихрь
            — шпиль — состоит из тринадцати дисков (тиб. <em>chos 'khor bcu gsum</em>), каждый из
            которых есть мандал: центр — сознание, диск — воспринимаемое. То есть это тринадцать
            уровней сознания на пути реализации <em>состояния будды</em>. Есть несколько трактовок
            этих тринадцати уровней. Согласно "Абхисамаяламкаре" — это <strong>три</strong> стадии
            на <em>пути архата</em> и <strong>десять</strong> ступеней на <em>пути бодхисаттвы</em>.
        </p>

        <p>Шпиль завершается зонтом (символ махабхута <em>воздух</em> и защиты от лучей
            <em>неведения</em> — удивительно близкая трактовка к
            современному представлению о защитной роли атмосферы от жесткого и ультрафиолетового
            излучений солнца и космоса) и, наконец, сочетание трех — луны, солнца, бинду — это
            своеобразная буддийская троица, символизм которой объясняет несколько триад буддийской
            метафизики: от <strong>триады Трех Тел</strong>
            <em>(Нирманакая, Самбхогакая, Дхармакая)</em> до <strong>триады Ваджрного Тела</strong>,
            или <strong>внутреннего мандала</strong> <em>(нади, праны, бинду)</em>.</p>

