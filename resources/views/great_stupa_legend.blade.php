        <h4 class="text-2xl font-semibold text-center my-6">ГЛАВА ПЕРВАЯ<br>ПОСТРОЙКА И ОСВЯЩЕНИЕ
            СТУПЫ</h4>

        <p>В этой легенде изложена история Джарунхашор, Великой Ступы, вместилища Тела Бесконечной
            Чистоты (Дхармакаи), тождественной Уму всех будд и бодхисаттв трех времен и десяти
            направлений. В году Огня–Обезьяны, на десятый день месяца Обезьяны, в среднем зале
            Великого Монастыря Самьелинг, название которого означает "Непостижимое, неизменяющееся и
            самовозникшее", великий дхармараджа Тисрондецан и двадцать пять учеников Гуру собрались,
            чтобы получить посвящение в <em>«Практики достижения Самого Тайного Ума Учителя»</em>
            (тиб.<em>bla ma yang gsang thugs kyi sgrub pa</em>). Они попросили
            Уржана<a href="#citation-2" id="ref-2" class="citation-mark"><sup>2</sup></a>
            Ринпоче, Драгоценного Лотосорожденного Гуру, воссесть на троне из девяти подушек. Затем
            царь Тисрондецан поднес Гуру золотую чашу вина, сладости, собранные со всего мира,
            двадцать одну бирюзовую бусину из собственного ожерелья, пять золотых чаш, семь кубков
            золота, восемь одеяний из тонкого шелка и множество других богатств.</p>

        <p>Совершив тысячу раз поклонение Гуру, царь обратился к нему:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            <p>
                О Великий Гуру! Я родился в Тибете, варварской стране краснолицых обезьян, и
                пригласил
                святого бодхисаттву Шантиракшиту из страны Захор и Вас, настоятеля из Уржана,
                Лотосорожденного Гуру, утвердить Дхарму в этой стране. Я построил Великий Монастырь
                Самье, где пребывает Тройная драгоценность,<a href="#citation-3" id="ref-3"
                                                              class="citation-mark"><sup>3</sup></a>
                вместилище накопленных заслуг всех живых существ, не сравнимая ни с чем во всей
                южной
                стране Джамбудвипе. Я осуществил все это. Теперь, в этой варварской стране, подобной
                острову мрака, Учение Тройной драгоценности распространяется, как свет утреннего
                солнца
                с горных вершин, и все люди могут услышать объяснение Святой Религии, как это было в
                Золотом Веке: когда Будда Махакашьяпа учил в Магадхе.
            </p>
            <p>
                Когда Будда Махакашьяпа проповедовал, благодетельница Жадзима, родившая четырех
                сыновей, построила Ступу Джарунхашор в районе Магута, что в царстве Непал.
            </p>
            <p>
                Великий Гуру! Если бы Вы смогли рассказать нам о плоде сильного стремления,
                порожденного строителями первой Великой Ступы, тогда мы, построившие этот Великий
                Монастырь в Самье, зная, как молиться, можем преисполниться верой в будущее. Чтобы
                преисполниться преданности, просим Вас рассказать подробно о строительстве первой
                Великой Ступы Джарунхашор.
            </p>
        </blockquote>

        <p>Тогда Великий Гуру ответил:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            <p>
                О Великий Царь, слушай внимательно и запоминай мои слова! Я расскажу легенду о
                Джарунхашор. В далеком веке, бесчисленные кальпы тому назад, Бодхисаттва – Господь
                Авалокитешвара дал обет у ног своего Гуру, Будды Амитабхи, освободить всех живых
                существ
                от страдания этого мира.
            </p>
            <p>
                Освободив бесчисленные существа от страдания, он взобрался на вершину дворца Потала,
                думая, что все существа без исключения освобождены. Взглянув на шесть миров, он
                увидел,
                что еще много существ томится в нижних мирах, подобно мухам на груде отбросов.
            </p>
            <p>
                Подумав, что невозможно освободить всех существ от океана страданий, он заплакал и,
                сняв указательным пальцем две слезинки с глаз, взмолился, чтобы даже эти две
                слезинки помогли будущим существам одолеть страдания. Для исполнения молитвы эти две
                слезинки были превращены в двух дочерей владыки Индры, который пребывает на небе
                Тридцати Трех Богов. Дочери были названы Богиней Пурной (Завершенной) и Апурной
                (Незавершенной). Однажды Апурна украла несколько цветов и в наказание за нарушение
                Закона Богов переродилась в мире людей в Непале в районе Магута в семье птицевода
                Ансу и его жены Пурны. Ее называли птичницей Шамварой.
            </p>
            <p>
                Достигнув зрелости, она сошлась с четырьмя различными мужчинами из низшей касты и
                родила от каждого из них по сыну: первого – от конюха, второго – от свинопаса,
                третьего – от псаря и четвертого – от птицевода.
            </p>
            <p>Птичница Шамвара скопила достаточно денег, чтобы поставить на ноги сыновей. Некоторое
                время спустя она накопила денег и подумала:</p>
            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Моих сбережений хватило, чтобы поставить на ноги сыновей. Теперь я накопила еще, и
                от
                этого все люди смогут получить пользу; построю Великую Ступу, вместилище Ума всех
                будд,
                воплощение моего божества. Ей будут поклоняться бесчисленные существа, она будет
                ракой
                неразрушимых останков Татхагат. Но прежде всего я должна попросить позволения
                Махараджи.
            </blockquote>

            <p>Подумав так, она отправилась к Махарадже, поклонилась ему, совершила прадакшину<a
                    href="#citation-4" id="ref-4" class="citation-mark"><sup>4</sup></a>, стала на
                колени, сложила руки и обратилась к нему:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                О Великий Царь! Я – бедная женщина, птичница, одна вырастила четырех сыновей от
                разных
                отцов и на доходы от своего хозяйства поставила их на ноги. Я прошу позволения
                построить
                Великую Ступу для поклонения бесчисленных существ, вместилище Ума всех будд, раку
                неразрушимых останков Татхагат. Она будет построена на средства, которые я накопила
                с
                тех пор, как мои сыновья стали самостоятельными.
            </blockquote>

            <p>Великий Царь никогда не принимал поспешных решений. Он сосредоточился и подумал:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Эта птичница – бедная женщина, однако ей удалось достаточно скопить денег, чтобы
                вырастить четырех незаконнорожденных сыновей, и теперь она желает построить Великую
                Ступу. Это удивительно.
            </blockquote>

            <p>Он дал ей разрешение построить ступу. Птичница Шамвара преисполнилась радости,
                поклонилась Царю и, обойдя вокруг него много раз, вернулась домой. Вскоре женщина,
                четверо ее сыновей, осел и слон начали постройку Великой Ступы. К месту
                строительства
                натаскали земли, заложили фундамент и возвели стены до третьего яруса. В это время
                непальцы, раздраженные поведением бедной птичницы, собрались, чтобы решить, какую же
                постройку смогут воздвигнуть царь, министр и другие богатые и знатные люди, если
                какая-то бедная птичница может построить такую Ступу. Непальцы сочли себя
                оскорбленными
                и отправились к царю с прошением остановить работу. Они сказали:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                О, Великий Царь! Если эта бедная птичница может построить такую Ступу, что же тогда
                ожидать от тебя, Царь, твоих министров и богатых людей страны? Если ты позволишь
                продолжать строительство, мы будем унижены. Лучше отвезти землю и камни обратно к
                карьеру. Не следует позволять ей строить эту Ступу.
            </blockquote>

            <p>Великий Царь ответил им:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Слушайте меня внимательно! Эта бедная птичница сэкономила достаточно средств, чтобы
                вырастить четырех незаконнорожденных сыновей и накопила деньги на постройку Ступы. Я
                считаю это удивительным подвигом. Я уже дал ей обещание (тиб. <em>kha shor</em>)
                разрешить начать работы (тиб. <em>bya rung</em>). Я, Царь, говорю только один раз.
            </blockquote>

            <p>Помешать строительству пытались и другие люди, но – безуспешно.</p>

            <p>Так Великая Ступа стала известной под названием <strong>ДЖАРУНХАШОР</strong>, что
                значит:
                <em>если дано разрешение на постройку, то можно преодолеть любое препятствие</em>.
                Работы по строительству продолжались в течение четырех лет без остановки, пока не
                добрались до горловины Ступы.</p>

            <p>В это время бедная птичница заболела и, почувствовав, что умирает, позвала к себе
                четырех
                сыновей, их слуг и сказала:</p>

            <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
                Завершите постройку Великой Ступы, этого воплощения моего божества для поклонения
                земных
                и неземных существ. Поместите неразрушимые останки Татхагат в Ступу и освятите ее с
                большими почестями и благоговением. Такова моя воля и, когда она будет исполнена,
                будут
                реализованы возвышенные намерения всех будд прошлого, настоящего и будущего. Вы, мои
                сыновья, достигнете этой цели в следующей жизни, выполнив мои желания.
            </blockquote>

            <p>С этими словами она умерла. Зазвучали цимбалы, и Боги ниспослали дождь цветов; на
                небе
                засияли радуги, и бедная птичница Шамвара достигла <em>состояния будды</em>
                благодаря
                щедрости, проявленной ею при постройке Великой Ступы, и была поименована
                <strong>Чамси Лхамо Прамша</strong>.</p>

            <p>Четыре сына решили достроить Ступу в соответствии с желанием матери, чтобы
                отблагодарить
                ее за доброту. Таким образом, они, как и прежде, грузили камни на слона и осла и
                продолжали работу.</p>

            <p>Через три года они закончили; всего на постройку ушло семь лет. Неразрушимые останки
                Татхагаты Махакашьяпы (Будды предыдущей кальпы) были помещены в древо жизни внутри
                Ступы.</p>

            <p>Когда было устроено богатое подношение и повсюду разбросаны перед Ступой жертвенные
                цветы, на небе появился Татхагата Махакашьяпа в окружении бодхисаттв. Все будды и
                бодхисаттвы десяти направлений в окружении бесчисленных архатов, пять родов Татхагат
                –
                Владыки Трех миров и неисчислимые гневные и спокойные божества, подобные цветущим
                бутонам сезама, появились, разбрасывая цветы, и почтили своим добрым присутствием
                это
                событие. Зазвучали гонги, в воздухе стали носиться насекомые, распространяя
                благоухание,
                боги ниспослали дождь цветов. Земля вздрогнула три раза. Безграничный свет
                божественной
                мудрости, исходящий от Тел собравшихся Татхагат, затмил солнце и сиял в течение пяти
                дней подряд.</p>
        </blockquote>
