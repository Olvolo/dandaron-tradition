        <h3 class="text-center text-gray-800 font-bold text-xl my-4">Послесловие переводчика</h3>

        <p>Хотя это сочинение Первого Панчен-ламы и написано в форме молитвы, оно, тем не менее,
            содержит в себе краткое описание процессов, происходящих во время умирания. Эта,
            обусловленная жанром, краткость отсылает читателя к обширному корпусу учений о смерти,
            который предполагается известным. Фактически наш текст дает свертку основных моментов
            этого учения в мнемоническую форму молитвы. Это обстоятельство несколько затрудняет
            чтение текста читателем, мало знакомым с воззрениями буддистов на смерть, немало,
            впрочем, затруднило оно и перевод.</p>

        <p>В качестве авторитетного источника для разъяснения сомнительных мест мы использовали
            работу Лончена Рабжампы "Зеркало памятования: разъяснение различных положений о бардо",
            вышедшую в английском переводе Эрика Пема Кунсана (E.&nbsp;P.&nbsp;Kunsang. The mirror of
            mindfulness. The cycle of the Four bardos by Tsele Natsok Rangdrol. Shambhala. 1989).
            Представленная ниже компиляция второй и третьей глав этой работы будет, как нам кажется,
            хорошим путеводителем по молитве Первого Панченя.</p>

        <h4 class="text-center text-gray-800 font-bold text-xl my-4">Болезненное бардо умирания.
            Выявление его сущности</h4>

        <p>Под болезненным бардо умирания понимают весь период от момента начала недуга, приведшего
            к смерти, до появления Ясного Света дхарматы. Обычно появление этого Ясного Света
            включают в болезненное бардо умирания. Однако здесь, следуя дзогченовскому учению, я
            опишу лишь бардо умирания. Бардо Ясного Света дхарматы будет обсуждаться отдельно.</p>

        <p>Высшие по своим способностям, те, кто освобождаются еще в течение этой жизни, не попадают
            в бардо. В дзогченовском учении говорится:</p>

        <p>Высшие йоги умирают четырьмя способами:</p>

        <div class="flex justify-center">
            <ul class="list-none text-left text-black pl-2 space-y-2">
                <li>как пространство снаружи и внутри сосуда соединяется, если сосуд разбить, так же
                    тело и ум растворяются в пустоте Дхармакаи;</li>
                <li>как пламя гаснет, когда кончился хворост;</li>
                <li>как умирает Видьядхара, наполняя небо светом;</li>
                <li>как умирает дакиня, не оставляя тела.</li>
            </ul>
        </div>

        <p>Это – высшие способы умирания. Эти четыре способа смерти свободны от описанных ниже
            процессов растворения.</p>

        <p>Сказано также:</p>
        <p>Средние йоги умирают тремя способами:</p>

        <div class="flex justify-center">
            <ul class="list-none text-left text-black pl-2 space-y-2">
                <li>как маленький ребенок, который, умирая, не отличает умирание и не-умирание;</li>
                <li>как нищий бродяга, не боящийся обстоятельств;</li>
                <li>как лев в безлюдных снежных горах, отбросив привязанности.</li>
            </ul>
        </div>

        <p>Это – средние способы умирания.</p>
        <p>Умирающий таким образом уже реализовал и не зависит от напоминаний.</p>

        <h3 class="text-center text-gray-800 font-bold text-xl my-4">Подробное объяснение процесса
            умирания</h3>

        <p>Для низших йогов и обычных людей здесь разъясняются два вопроса:</p>

        <div class="flex justify-center">
            <ul class="list-none text-left text-black pl-2 space-y-2">
                <li>растворение внешних элементов – индрий и аятана;</li>
                <li>растворение внутренних грубых и тонких мыслей.</li>
            </ul>
        </div>

        <h4 class="text-center font-bold italic my-4">Растворение внешних элементов</h4>

        <p>Тело живого существа возникает благодаря пяти махабхутам, поддерживается ими и погибает с
            их растворением.</p>
        <p>Во время смерти ветер кармы поворачивает вверх, и поскольку он контролирует остальные
            ветры, то пять чакр разъединяются и пять ветров начинают исчезать. Детали процесса
            растворения можно найти в дзогченовской тантре
            <strong>Rig-pa rang-shar chen-po'i-rgyud</strong>, но,
            боясь такой массы слов, я не буду пускаться здесь в столь пространное изложение.
            Суммируя общие положения новых и старых школ, я разъясняю их так.</p>
        <p>С исчезновением "поддерживающего ветра" (<em>mnyam-gnas, удана</em>) умирающий не способен
            усваивать пищу, и тепло исчезает из тела, начиная с конечностей. С исчезновением
            "жизненной силы" (<em>srog-'dzin, прана</em>) ум теряет свою ясность. С исчезновением "праны,
            очищающей (вниз)" (<em>thur-sel, апана</em>), умирающий не может испражняться. После
            исчезновения "восходящей праны" (<em>gyen-rgyu, самана</em>) он не может глотать, и
            дыхание становится прерывистым. С исчезновением "проникающей праны" (<em>khab-byed,
                вьяна</em>) конечности не слушаются и вены ссыхаются.</p>
        <p>После этого начинается разрушение чакр, и с исчезновением <em>поддерживающих ветров</em>
            махабхуты растворяются друг в друге.</p>
        <p>Одновременно с этим процессом исчезают также и <em>пять вторичных ветров</em> и связанные
            с ними индрии (органы восприятия) и аятаны. С растворением в пространстве виджняны
            прекращается внешнее дыхание, и здесь пролегает тот рубеж, из-за которого нет возврата.
            Все, вплоть до этого момента, называют обычными знаками смерти.</p>

        <h4 class="text-center font-bold italic my-4">Растворение внутренних мыслей</h4>

        <p>В изложении этого вопроса я следую Калачакратантре.</p>
        <p>В верхнем конце центрального канала находится доставшийся от отца белый элемент. С
            исчезновением верхнего ветра он опускается в сердце, и умирающий видит белый свет,
            похожий на лунный. Одновременно исчезают тридцать три мысли, связанные с гневом.</p>
        <p>В нижнем конце центрального канала, в области пупка, находится красный элемент,
            доставшийся от матери. С исчезновением <em>нижнего ветра</em> он поднимается в сердце, и
            появляется видение красного света. Одновременно исчезают сорок мыслей, связанных со
            страстью.</p>
        <p>Вслед за этим сознание растворяется в соединении белой и красной бодхичитт, и является
            "видение темноты". Одновременно исчезают семь мыслей, связанных с неведением.</p>

        <h3 class="text-center text-gray-800 font-bold text-xl my-4">Бардо Ясного Света
            дхарматы</h3>

        <h4 class="text-center font-bold italic my-4">Его сущность</h4>

        <p>После завершения этих стадий растворения энергетическая поддержка двойственного ума
            (<em>rlun-sems</em>) растворяется меж двух бодхичитт, и проявляется Дхармакая
            изначального Ясного Света.</p>

        <h4 class="text-center font-bold italic my-4">Подробное объяснение</h4>

        <p>Во многих тантрах видения миража и так далее называются явленным Ясным Светом, а Ясный
            Свет Основы – неявленным пустым Ясным Светом. С его появлением связано видение безоблачного
            неба как символа осознания и пустоты изначальной чистоты. Тот, кто узнает этот Ясный
            Свет, тут же освобождается, не входя в бардо.</p>

