    <h1 class="text-center text-blue-900 font-bold text-2xl sm:text-2xl md:text-2xl mt-6 mb-6">Песнь
        о бардо Первого Панчен-Ламы</h1>
    <div class="text-justify text-base sm:text-lg leading-relaxed space-y-4">

        <p>Ныне и всегда был велик интерес к бытию за порогом смерти. Многочисленны свидетельства
            людей, переживших состояние клинической смерти и затем подробно описавших "увиденное".
            Поразительно, что эти свидетельства во многих чертах совпадают. Значит, бытие по ту
            сторону смерти – объективно, хотя обычному человеку практически недоступно.</p>

        <p>Тем не менее, уже более двух тысяч лет существует цивилизация Тибета,
            духовно-психологический опыт жителей которой дает возможность, использовав специальные
            йогические практики, не только заглянуть за черту смерти, но, что важно, помочь тем, кто
            странствует в неизведанном посмертном мире, преодолеть непривычное существование, а
            главное – достичь освобождения или снова удачно выбраться на берег воплощенной жизни.
            Существуют подробные описания всех стадий, переживаемых сознанием после смерти вплоть до
            нового рождения. Одно из таких описаний – знаменитая "Тибетская Книга Мертвых". Мы же
            знакомим читателей с небольшим текстом – молитвой о беспрепятственном преодолении
            опасного периода жизни человека: "Молитвой об избавлении от опасной тропы бардо,
            именуемой <em>Рыцарь, спасающий от страха</em>" (Bar do 'phrang sgrol gyi gsol 'debs
            'jigs sgrol gyi dp'a bo zhes bya ba bzhugs so). Текст написан <strong>Первым
                Панчен-ламой Лобсаном Чойжи Джалцаном</strong> (1570–1662), одним из учителей Пятого
            Далай-ламы (1617–1682), и представляет собой замечательный образец оформления буддийской
            схоластики художественно-поэтическим стилем.</p>

        <p>После смерти человека буддисты обычно приглашают ламу – специалиста по системе <strong>чод</strong>.
            Его задача: помочь сознанию умершего безболезненно покинуть тело и безопасно пройти
            путями бардо. Среди текстов, читаемых ламой в непосредственной близости от умершего,
            обычен и тот, что ниже публикуем мы. Этот текст может читать не только лама, но и сами
            верующие, родственники и друзья умершего, если они достаточно грамотны и имеют обычные в
            Тибете для мирян низшие степени духовных посвящений.</p>

