        <p>Так завершена мольба об избавлении от ужасов бардо, именуемая "Рыцарь, спасающий от
            страха", которую во исполнение нескольких просьб, в соответствии со святыми
            наставлениями сутр и тантр, я, буддийский монах Лобсан Чойжи Джалцан, составил в доме
            монастыря Балдан Брайбун.</p>

