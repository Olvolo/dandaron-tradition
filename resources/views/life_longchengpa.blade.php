        <p>Шестнадцатого числа двенадцатого месяца того же года он исполнил сложные жертвоприношения
            дакам и дакиням. Затем произнес последнюю публичную проповедь:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-xl italic">
            Вы должны посвятить себя исключительно практике Дхармы. Нет самостоятельной сущности в
            составных явлениях. Особенно вы должны обратить внимание на практики <em>тэгчо</em> и
            <em>тогэл</em>. Если вы столкнетесь с какими-либо трудностями в понимании практики,
            читайте "Янтиг Ишин Норбу" (или "Лама Янтиг"), это будет подобно обретению
            драгоценности, исполняющей все желания – чинтамани. Вы достигнете нирваны в состоянии
            Растворения Всех Феноменов в Конечной Природе (тиб. <em>Chos-nyid Zad-pa</em>).
        </blockquote>

        <p>Восемнадцатого числа он сказал нескольким ученикам: "Приготовьте жертвоприношение и
            покиньте затем комнату". Когда ученики пожелали остаться с ним, Лончен Рабжампа добавил:
            "Потея, я собираюсь покинуть это тело. Не производите никакого шума и пребывайте в
            созерцании".</p>

        <p>Затем его сознание установилось в Изначальном состоянии и покинуло тело, оставив его
            сидящим в <em>позе Дхармакаи</em>. Так оставил этот мир Лончен Рабжампа. Его смерть
            сопровождалась содроганием земли и глухим рокотом в глубине окружающих гор; священное
            писание определяет такие приметы, как свидетельство высших достижений умершего. Ученики
            хранили его тело двадцать пять дней. В течение этого времени в небе постоянно сияли
            радуги, образуя как бы чудесный шатер над местом смерти Лончена Рабжама. Хотя истекал
            двенадцатый месяц и наступал первый (февраль), наиболее холодное время для Тибета, земля
            стала теплой, лед растаял и расцвели розы.</p>

        <p>Во время кремации трижды содрогнулась земля и семь раз разнесся по окрестности необычный
            и громкий звук. Прах его сердца, язык и глаза были найдены несгоревшими. Останки его
            костей походили на пять видов раковин и на множество иных реликвий, что еще раз
            свидетельствовало о его реализации Пяти Тел и Пяти Мудростей состояния Будды. Много
            выдающихся личностей в будущем почитались как воплощения Лончена Рабжампы. Но главным
            воплощением считается великий тертон <strong>Падма-Линпа</strong> (1450-?) родом из
            Бутана.</p>

        <p>Такова жизнь Лончена Рабжама в кратком изложении ламы Тондуба. Жизнеописание, к
            сожалению, слишком канонично и мифологизировано, чтобы понять, каков же он был в жизни,
            чтобы почувствовать за иконописным образом живого человека. Этому могут поспособствовать
            несколько замечательных примеров из его жизни и поэтического наследия.</p>

        <p>Этот величайший из философов и созерцателей Тибета был непритязателен и прост в жизни,
            все его время было отдано проповеди Дхармы, в чем и был смысл его жизни и цель
            воплощения.</p>

        <p>Что бы ни жертвовалось ему с верой, шло всегда исключительно на благо Дхармы и никогда
            ради личных или мирских целей. Он не позволял, чтобы какие-либо принадлежности Дхармы
            использовались не по предназначению. Он не выражал почтения мирским людям, как бы
            значительны и могущественны они ни были, он говорил: "Почтение должно оказываться Трем
            Драгоценностям, а не доброжелательным людям".</p>

        <p>Когда он распределял пожертвования среди монахов, он начинал с конца ряда: "Мы должны
            быть внимательны, чтобы не пропустить никого в конце ряда; люди в начале ряда получат
            пожертвование всегда". Согласно Писанию, если обыкновенный человек принимает почтение от
            более духовного лица, то большая часть его заслуг утрачивается. Ему часто делали богатые
            подношения, на что он в ответ произносил посвятительные молитвы, но никогда не выражал
            благодарности, объясняя: "Покровители должны иметь возможность накопить заслуги". Он был
            свободен от надежд и страхов мирского стереотипа и практически всю жизнь провел в хижине
            отшельника, отвергая жизнь в богатых домах и предложения о постройке личной обители.
            "Если бы я постарался, то мог бы организовать монастырь, но такое деяние лишь порождает
            раздражение. Гораздо лучше сосредоточить усилие на тренировке ума людей".</p>

        <p>Он всегда соблюдал ритуалы жертвоприношений в десятые дни возрастающей и убывающей луны,
            то есть на десятый и двадцать пятый дни лунного тибетского календаря, а также и в
            восьмой день каждого месяца. Лончен Рабжампа был очень добр к бедным и несчастным людям
            и с великим удовлетворением раздавал им жертвенную еду, вознося молитвы в их благо. С
            социальной точки зрения можно подумать следующим образом: раз Лончен Рабжампа был так
            добр к бедным людям, то не лучше ли бы было отдать им все ценное, что только он имел.
            Казалось бы, жизнь часто давала для этого повод, но нет в его биографии ни одного
            подобного случая. За таким поведением стоит четкая духовная установка. Ведь большинство
            ценных вещей, которые попадали к Лонченпе, он не хранил у себя, а передавал на
            религиозные нужды; все эти ценности были так называемыми "жертвоприношениями вере", т.е.
            подношениями его последователей и почитателей во имя религиозных целей. Предметы веры
            должны служить Будде, Дхарме и Сангхе, но не мирским целям, в противном случае будет
            лишь накапливаться неблагоприятная карма. Исходя из таких соображений, лама, принимающий
            подношение, создает для жертвователей возможность совершать благо и накапливать условия
            будущего счастливого рождения. Так что он действительно приносил пользу своим
            почитателям тем, что молча принимал их пожертвования, создавая им благоприятный способ
            накопления заслуг. И хотя богатые люди могли совершить, естественно, более ценные
            подношения, он никогда не отказывался от подношений бедных людей, оказывая этим по
            отношению к ним милосердие.</p>

        <p>В "Истории Лама Янтиг" Лончен Рабжампа в простых словах советует: "Мои будущие
            последователи! Оставьте мир этой жизни позади, начните подготовку к новой жизни.
            Доверьтесь совершенному, добросовестному Учителю. Удостойтесь наставлений о Сущности
            Ясного Света. Практикуйте в уединении сокровенные учения Ньинтиг. Обретите еще в этой
            жизни результат, выходящий за пределы и сансары, и нирваны. Если вам будет пророчество,
            посвятите себя служению учением другим настолько, насколько можете. Наперекор любым
            препятствиям проповедуйте Учение".</p>

        <p>Характерной и трогательной особенностью учений Лончена Рабжама является обращение к
            чувству глубокой и тонкой гармонии с природой вокруг нас, что должно помочь реализовать
