                (1) полностью созревший — начальный уровень, где карма созрела для вступления на путь;<br>
                (2) владеющий жизненной силой — контроль над продолжительностью жизни;<br>
                (3) махамудра — реализация недвойственности;<br>
                (4) спонтанно совершённый — полная буддовость.<br>
                Эти уровни — не внешние достижения, но степени устойчивости в узнавании и удержании
                природы ригпа в контексте тантрической практики.<a href="#ref-47" class="text-blue-500 hover:underline">↩</a>
