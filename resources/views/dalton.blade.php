        <h2 class="text-center text-blue-900 font-bold italic text-2xl sm:text-2xl md:text-2xl mt-6
        mb-6">The Questions and Answers of Vajrasattva<a href="#citation-1" id="ref-1"
                                                         class="citation-mark"><sup>1</sup></a></h2>

        <p>Йога как единение практикующего и божества может порождать некоторые сбивающие с
            толку
            теоретические вопросы. С точки зрения йогического единения, если тело, речь и ум
            человека уже тождественны Телу, Речи и Уму Будды, зачем беспокоиться о ритуальном
            исполнении (этого единения)? Если нет различия между сакральным и мирским, зачем
            создавать отдельное особое ритуальное пространство? Зачем обращаться к учителю за
            советом? Зачем добиваться благословения и достижения своего божества? Именно такого
            рода вопросы поставлены в тибетском тексте IX века «Вопросы и ответы о
            Ваджрасаттве».
            В конечном счете подобные вопросы могут быть неразрешимыми (концептуально),
            поскольку
            они во многих отношениях воплощают в себе парадокс буддийской практики: парадокс
            обнаружения божества внутри себя, стремления к цели, которая уже присутствует.
            Безусловно, схожие проблемы продолжают преследовать
            практикующих йогу и сегодня. Но такие вопросы могут быть особенно мучительными для
            начинающих, для тех, кто только знакомится с концептуальными системами йоги и
            поэтому еще не освоил выработанные традицией ответы. Именно такая ситуация
            просматривается за нашими «Вопросами и ответами».</p>

        <p>В конце VIII — начале IX веков тибетцы впервые столкнулись с буддийскими тантрами
            йоги и махайоги («Великой йоги»). Их одновременно привлекали таинственность и сила
            этих эзотерических текстов, и сбивал с толку их парадоксальный, а порой даже
            отвергающий моральные законы подтекст. Возможно, именно по этой причине ранние
            тибетские
            увлечения тантрами, по-видимому, вызывали беспокойство при императорском дворе.
            Издавались царские указы, ограничивающие перевод и распространение этих опасных
            текстов. Официальные каталоги тибетских буддийских переводов явно исключали наиболее
            радикальные тантры махайоги из своих списков, а когда тантры все же переводились, их
            наиболее оскорбительные пассажи тщательно подвергались цензуре. Нарушающие
            установленные правила и
            отвергающие принятые моральные законы учения тантр, по-видимому, угрожали более
            широкому проекту двора по
            продвижению буддийского монашества, этики и благочестивого поклонения среди
            населения.</p>

        <p>Тем не менее, при дворе и среди аристократических семей центрального Тибета
            Тантры оставались популярными и продолжали изучаться на протяжении всего раннего
            имперского периода (который завершился примерно в середине IX века). Текст «Вопросы
            и ответы о Ваджрасаттве» является свидетельством их популярности. Сам факт его
            существования уже говорит о том, что интерес тибетцев первой половины IX века к этим
            учениям сохранялся. Более того, колофон, приложенный к одной из версий текста,
            объясняет цель автора в его составлении:</p>

        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
            Как можно заключить из названия, составления и тантры класса Махайоги могут
            расходиться от одного текста к другому, поэтому эта [работа] была написана с целью
            прояснить любые неясные моменты среди тантр или сомнения, которые могут возникнуть.
            [Также] что касается её назначения, она была составлена для кланов Нанам (Sna nam) и
            Донг (Ldong) и для умов будущих поколений йогинов, с целью прояснить любые неясные,
            сомнительные или трудные моменты. Эта цель, которая присутствует от начала до конца,
            была сформулирована в виде пятидесяти трех вопросов в стихах, каждый из двух частей —
            как
            вопрос и ответ.
        </blockquote>

        <p>Текст был написан специально для двух кланов, хорошо известных аристократических
            семей,
            поддерживавших тесные связи с императорским двором. Возможно, имея в виду именно эту
            аудиторию,
            Пэльянг (Pelyang) в сорок втором стихе упоминает, что правильное выполнение йоги
            приносит
            силу «в уме и в клане». Во всяком случае, текст Пэльянга кажется свидетельством
            того,
            что запретительные указы двора не были направлены на полное запрещение Тантр, а лишь
            ограничивали их распространение в собственных внутренних кругах.</p>

        <p>Как указывает колофон, «Вопросы и ответы о Ваджрасаттве» организованы в виде серии из
            пятидесяти одного вопроса и ответов на них (несмотря на указанные в переведённом
            выше колофоне пятьдесят три). Буддийские катехизисы подобного рода были популярны в
            раннем Тибете. Только в Дуньхуанском архиве мы находим «Отрывки из восьмидесяти
            сутр» (Mdo sde brgyad bcu khungs), другое раннее тибетское произведение, которое
            сосредоточено на практике йоги с нетантрической точки зрения, а также второй
            катехизис (в Библиотеке Индийского управления [IOL] Tib J 419/6) о практиках тантр
            Махайоги. Кажется, что катехизические сочинения помогали ранним тибетцам понять
            буддизм, и в этом смысле, возможно, стоит отметить распространённость подобных
            текстов в формате вопросов и ответов в современных книгах, стремящихся принести
            буддизм на Запад; см., например, книги ламы Еше, Чогьяма Трунгпы и Джозефа
            Голдстейна.</p>

        <p>Колофон к «Вопросам и ответам о Ваджрасаттве» приписывает это произведение некоему
            Пэльянгу (Dpal dbyangs), который также упоминается как учёный Ньен Пэльянг (Mkhan po
            Gnyan dpal dbyangs) в «Светильнике для ока в созерцании» Нубчена Сангье Еше начала
            десятого века. Ньен Пэльянг, по-видимому, был активен в центральном Тибете в течение
            первой половины девятого века. Его учения пользовались большим авторитетом в своё
            время, и ему приписываются несколько дополнительных работ по тантрийскому буддизму
            в тибетском каноне. В других своих сочинениях Пэльянг часто ссылается на Гухьягарбха
            Тантру, тантру Махайоги, которая оказала значительное влияние в раннем Тибете и была
            тесно связана с возникновением традиций медитации Атийоги, или Дзогчен
            («Великое Совершенство»).</p>
        <p>В «Гухьягарбхе» и других ранних тантрах Махайоги, датируемых примерно концом восьмого
            века, можно различить два взаимодополняющих направления риторики: одно подчёркивает
            важность соблюдения надлежащих (и часто сложных) ритуальных процедур, а другое
            предполагает, что такие условные аспекты не являются необходимыми. Эти два
            направления можно
            соотнести с двумя истинами более раннего экзотерического буддизма, то есть с
            условной и абсолютной истинами, но это различие также подпитывалось
            внутренними противоречиями, с которых мы и начали. Йога одновременно включает в себя
            условные практики, способствующие постижению своего изначального единства с
            божеством, и подразумевает, что изначальная природа этого единства в конечном счёте
            делает такие практики несущественными. Хотя оба эти риторических направления
            распространены в тантрах Махайоги, несколько более поздние трактаты так называемого
            класса Атийоги в большей степени сосредоточиваются на абсолютной перспективе
            второго направления. «Вопросы и ответы о Ваджрасаттве», возможно, отражают тот
            момент в
            развитии тантрического буддизма, когда подобная риторика Атийоги ещё не отделилась
            полностью от своих истоков в Махайоге.</p>
        <p>В самих «Вопросах и ответах» во вступительных строках говорится, что текст был
            написан для «тех, кто желает с осознанностью постичь путь высшей Махайоги», и на
            всём своём протяжении он использует ритуальную терминологию тантр Махайоги. Однако,
            несмотря на технический язык произведения, его взгляд на ритуал глубоко укоренён в
            абсолютной перспективе — настолько, что его ранние читатели понимали, что текст, по
            крайней мере в некоторых частях, излагает учение Атийоги. Например, дуньхуанская
            рукопись X века, содержащая «Вопросы и ответы», включает в себя обильные межстрочные
            примечания неустановленного автора. Согласно этому межстрочному комментатору,
            воззрение Атийоги разъясняется в следующих строках тринадцатой строфы:</p>
        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
            <ul class="list-none pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">Явление себе самому — это Дхармакая,</li>
                <li class="my-2">Осознаваемая как неизменная, подобно небу.</li>
                <li class="my-2">В почитании (<em>sevā</em>), когда не воспринимается ни
                    действие, ни действующий,
                </li>
                <li class="my-2">Нет ни слов, ни усилий — это высшее почитание.</li>
            </ul>
            <ul class="list-none pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">Тот, кто обладает подобным пониманием,</li>
                <li class="my-2">Ясно созерцает три символические мудры.</li>
                <li class="my-2">Если усердствовать, не отвлекаясь и не оставляя [практику],
                </li>
                <li class="my-2">То приблизишься к божеству изначальной мудрости, обладающему
                    всеми ритуалами.
                </li>
            </ul>
        </blockquote>
        <p>Позже тот же междустрочный комментатор истолковывает заключительную строку тридцать
            первого стиха как ещё одну отсылку к Атийоге. В ответ на вопрос о месте усилия в
            йоге мы читаем:</p>
        <blockquote class="border-l-4 border-gray-500 pl-6 my-6 text-gray-800 text-lg">
            <ul class="list-none pl-5 space-y-1 ml-8 mb-4">
                <li class="my-2">Что бы ни следовало осуществить,</li>
                <li class="my-2">Следует снова и снова прилагать усилие, пребывая в равновесии.</li>
                <li class="my-2">Когда благодаря привычке поток [созерцания] установится сам,</li>
                <li class="my-2">Тогда без усилий, естественно, возникнет спонтанное самадхи.</li>
            </ul>
        </blockquote>
        <p>В этом стихе мы видим, как Пэльянг занимает то, что можно было бы назвать взвешенной
            позицией в дискуссии о внезапном и постепенном [просветлении], которая так волновала
            ранних тибетцев, поскольку он допускает постепенный прогресс по пути, который
            завершается моментом внезапного просветления. Получающийся прорыв, говорит наш
            комментатор, равнозначен «смыслу Атийоги».</p>
        <p>Несмотря на признание Пэльянгом в этом стихе того, что некоторые усилия и постепенная
            тренировка могут понадобится, по крайней мере на начальном этапе, он посвящает
            большую часть своих «Вопросов и ответов» более абсолютной и прямой перспективе. В
            этом духе двадцать четвёртый стих объясняет, что йогин должен отбросить как простую
            иллюзию все философские различения, даже положения двух высших философских школ в
            раннем Тибете — Йогачара-мадхьямаки и Саутрантика-мадхьямаки. Аналогично в
            девятнадцатом стихе всякая медитация признаётся невозможной, в то время как в
            седьмом и восьмом стихах говорится, что ничего никогда не достигается.</p>
        <p>Пэльянг применяет ту же парадоксальную логику абсолютной йоги и в своей оценке
            добуддийских тибетских религиозных практик. В тридцать шестом стихе он поднимает
            вопрос о том, должен ли правильный йогин поклоняться местным богам и демонам Тибета.
            В системе йоги, объясняет он, «для того, кто дал обет Самантабхадре-Ваджрасаттве,
            поклонение мирским богам и нагам ("драконам") как высшим существам подобно царю,
            совершающему деяния простолюдина. Не просите [у таких существ даже] своего
            пропитания; это противоречит цели йоги». Здесь Пэльянг занимает необычайно жёсткую
            позицию против религиозных практик добуддийского Тибета. Многие ранние тибетцы
            воспринимали буддизм как угрозу благополучию своих местных богов. По мере того как
            буддизм продолжал распространяться в Тибете в последующие столетия, это напряжение
            было в значительной степени разрешено общим принятием буддистами поклонения местным
            духам для мирских и материальных целей, с оговоркой, что такие практики не приблизят
            практикующего к просветлению. Но Пэльянг занимает более крайнюю позицию, отказываясь
            признать даже мирскую ценность поклонения духам, и для обоснования своего взгляда
            Пэльянг применяет риторику абсолютной йогической перспективы: учитывая врождённое
            единство себя и своего божества, такие низменные практики в лучшем случае неуместны,
            а в худшем — нарушение йогических обетов.</p>
        <p>С такой точки зрения все религиозные практики, кроме полной и совершенной йоги,
            заблуждаются и могут принести только мирские (лаукика, <em>laukika</em>) блага.
            Можно обрести богатство, излечить болезнь или обеспечить хороший урожай; можно
            переродиться богом в небесах или даже достичь возвышенных медитативных состояний
            Брахмы (как в двадцать девятом стихе); но никогда не достигнешь трансцендентного
            (локоттара, <em>(lokottara</em>) состояния будды. Только преодолев все
            концептуальные различения посредством буддийской йоги, можно постичь абсолютное.</p>
        <p>Даже несмотря на то, что «Вопросы и ответы» настаивают на неуместности низших
            практик, они явно написаны в контексте йоги, или Махайоги, то есть тантрического
            ритуала. В тексте используется множество технических терминов и регулярно
            упоминается сложный ритуальный аппарат Махайоги. Один из таких терминов — или даже
            пара терминов — который требует пояснения, это «почитание и достижение» (тиб. <em>bsnyen
                sgrub</em>; санскр. <em>sevāsādhana</em>). В более ранних текстах, основанных на
            дхарани, и тантрических ритуальных текстах эта пара обозначала две основные стадии
            ритуальной практики. Во время почитания практикующий делал подношения и, в
            частности, произносил мантры божества, чтобы очиститься и обрести силу для
            следующего этапа. Затем во время стадии достижения он направлял силу, обретённую
            через почитание, к конкретной цели, будь то обретение богатства, причинение вреда
            врагам, достижение просветления или что-либо ещё. Примерно во второй половине
            восьмого века эта ключевая терминологическая пара была расширена до четырёх стадий:
            почитания, близкого почитания, достижения и великого достижения. Полученная
            четверичная схема затем стала применяться по-разному в различных ритуальных
            контекстах и Пэльянг также ссылается на неё.</p>
        <p>Аналогично в тридцать девятом стихе Пэльянг рекомендует использование мирских
            «внутренних сакраментальных субстанций», обычно состоящих из семени, менструальной
            крови, фекалий, мочи и человеческой плоти. Подобным же образом стихи с сорок
            третьего по сорок седьмой составляют развёрнутое обсуждение «уровней видьядхар»,
            стадий реализации, которые должен пройти успешный практикующий Махайоги. А
            восемнадцатый стих ссылается на «сосредоточение таковости» (<em>tathatā-samādhi</em>),
            бесформенную медитацию на пустотность, с которой начинается множество ритуальных
            действий Махайоги и за которой обычно следуют два дальнейших сосредоточения —
            «всеозаряющее сосредоточение» и «причинное самадхи», с помощью которых происходит
            порождение визуализированной мандалы.</p>
        <p>Таким образом, несмотря на свою высшую (абсолютную) перспективу, «Вопросы и ответы о
            Ваджрасаттве» функционируют в рамках более или менее привычной ритуальной структуры;
            этот текст остаётся вполне частью буддийской дхармы (то есть «учения» или «истины»).
            Собственно, слово «дхарма» неоднократно встречается в тексте — как самостоятельный
            термин и как часть двух ключевых словосочетаний. Первое из них, Дхармадхату (<em>dharmadhātu</em>),
            относится к «пространству истины», просветлённой сфере пустоты, в то время как
            второе, Дхармакая (<em>dharmakāya</em>) — «тело истины», наиболее неописуемое,
            неоформленное проявление Будды. Дхармакая, как правило, включается в триединую
            систему и часто упоминается вместе с двумя другими телами Будды: самбхогакая
            (<em>sambhogakāya</em>)(«тело совершенного наслаждения»), описывает блаженные
            божества, возникающие как формы чистого света в видениях практикующих и часто
            изображаемые на буддийских танках; и нирманакая (<em>nirmāṇakāya</em>)
            («тело эманации»), которое обозначает более грубые эманации Будды, его тела из плоти
            и крови. Очевидно, что Будда в этой системе йоги, как и в буддизме Махаяны в целом,
            простирается далеко за пределы исторической личности Шакьямуни, жившего около 2500
            лет назад. Здесь Будда — это скорее абстрактная сила, которая может быть воплощена
            каждым, кто успешно последовал его примеру, кто является татхагатой, «тем, кто ушёл
            таким образом», или сугатой, «тем, кто ушёл хорошо».</p>
        <p>В «Вопросах и ответах» этот высший Будда воплощается в образе Ваджрасаттвы —
            божества, имя которого вынесено в заглавие текста и которое отождествляется с
            Самантабхадрой в третьем и тридцать шестом стихе (см. выше). «Ваджра» в имени
            Ваджрасаттва — это центральное понятие в тантрическом буддизме, охватывающее широкий
            семантический диапазон. В качестве молнии, ваджра — это оружие, которое традиционно
            держит в руке бодхисаттва Ваджрапани. Как неразрушимый алмаз, она также используется
            как ритуальный атрибут в руках тантрического практикующего. Она символизирует
            мужской
            аспект просветления, который в ритуале сочетается с женским аспектом, представляемым
            колокольчиком (ганта) в другой руке. Таким образом, Ваджрасаттва, буквально
            «Существо Ваджры», — это персонификация ваджры, неразрушимой сути просветления. Он
            олицетворяет высшего Будду пантеона буддийской Йога-тантры. Как таковой, он включает
            в себя всех других Будд и воплощает абсолютную перспективу, с которой всякое
            различие — между Ваджрасаттвой и другими Буддами, между собой и божеством, и вообще
            между чем-либо и чем-либо ещё — становится совершенно несущественным. Именно с этой
            высшей перспективы и ведётся изложение «Вопросов и ответов о Ваджрасаттве».</p>
