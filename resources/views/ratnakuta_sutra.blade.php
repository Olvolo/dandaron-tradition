        <h4 class="text-center text-gray-800 font-bold text-xl my-4">ПРИМЕЧАНИЯ</h4>

        <div class="text-sm">
            <p>Из текста неясно, является ли Ганготтара, упомянутая здесь, той же личностью, что и Ганготтара, присутствующая при данной проповеди. Хотя это мог быть другой человек с тем же именем и схожей кармой, вполне вероятно и то, что это одна и та же личность. Может возникнуть вопрос: "Если Ганготтара давным давно оставила мирскую жизнь и вступила в безостаточное успокоение (паринирвану), как могла она здесь появиться в качестве вопрошающей мирянки?"</p>

            <p>Согласно учению хинаяны, на этот вопрос практически невозможно ответить, поскольку эта традиция полагает, что тот, кто вступил в безостаточную нирвану, никогда более в мир не возвращается. В махаяне, однако, ситуация проповеди Дхармы рассматривается как пьеса, которая может быть сыграна снова и снова, а человек, вступивший в нирвану, может вновь появиться в теле ради блага живых существ. Махаянское описание нирваны, называемой "непребывающая нирвана", утверждает, что возможно достичь освобождения и все же остаться в мире ради всеобщего спасения. Такой человек не пребывает ни в сансаре, ни в нирване. Однако непребывающая нирвана махаяны не противоречит хинаянскому пониманию нирваны. Это явственно показывает знаменитый "Рассказ о молчании Будды", когда он отказался ответить на вопрос о том, существует ли после паринирваны кто-то, способный осознать это состояние. Таким образом, даже согласно буддийскому учению палийской традиции нельзя сказать, что нирвана – это уничтожение, или что тот, кто вступил в нирвану, никогда не вернется в мир.</p>

            <p>На этот вопрос Будда не дал ни утвердительного, ни отрицательного ответа. (<em>Прим.</em> Гарма Чжан Чжэнь-цзи.)</p>
